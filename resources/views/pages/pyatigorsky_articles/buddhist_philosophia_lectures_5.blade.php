            <p>Этот текст из Сандхинирмочана-сутры, то есть, по-видимому, первый священный (то есть
                вложенный в уста Будды) текст, излагающий Учение о Сознании <em>(vijnana-vada)</em>.
                Немного позднее, в комментариях и трактатах Асанги и Васубандху (III-IV вв. н. э.?),
                это учение заняло центральное место в философской школе того же имени. Но пока в
                самой сутре оно еще не более чем развернутая позиция, не только вполне совместимая с
                позициями текстов (14) - (16), но и легко выводимая из них. Но есть и очень важные
                различия. Посмотрите, в тексте о возникновении мысли мысль возникает и исчезает,
                оставаясь в своем месте («случае»). Точнее, возникает и исчезает вместе со своим
                «случаем». Если отвлечься от времени ее возникновения, то есть нулевого времени, то
                она пространственно замкнута в свой «случай». Вместе с тем, если брать Возникновение
                Мысли в его биллионах и триллионах моментов — здесь уже будет не только время, но и
                направление: условное направление условного времени условного же потока мысли.
                Условного, поскольку последний еще не обрел своей конкретизации в континууме мысли,
                «разрезанном» на индивидуальальные континуумы жизней отдельных одушевленных существ.
                В этом случае просто говорится: мысль возникает в потоке. В следующем тексте, о
                невозможности и непостижимости мысли, мысль полностью «денатурализирована», так
                сказать. Ни о каком, даже условном, времени не может быть и речи, так же как и о
                возникновении или прекращении мысли.</p>

            <p>Мысль имеет «свое» направление только в беседе Будды с Кашьяпой. И это «нулевое»,
                несуществующее направление, ибо объект мысли Будды здесь — это любая мысль
                (заметьте, а не мысль вообще!), направление которой только в чистом пространстве
                (akasa) мысли к тому, что она <em>есть</em>, то есть к мысли Будды. И наконец, в
                тексте континуума сознания мысли условно «локализуются» в потоке, но не как в
                пространстве, а как именно во времени их серийного движения. Тогда направлением их
                движения будет в индивидуальальном отрезке континуума — от рождения к смерти и от
                смерти к другому рождению, а в целом континууме — из его безначальности к возможному
                (а по некоторым текстам — обязательному) концу в Полном и Совершенном Пробуждении
                Буддства.</p>

            <p>В последнем нашем тексте сознание не имеет своего времени вообще; оно дано всегда как
                уже имеющееся и аппроприирующее, не только сознаваемое им «его» тело (<em>vijnanakaya</em>),
                но и все свои прошлые состояния (в виде того, что я назвал «после-запах» или «запах
                запаха»). В этом смысле всякое «остаточное» или «накопленное» сознание (<em>alayavijnana</em>)
                — это не только «мгновенный снимок» индивидуального сознания, но и такая его
                «картина», которая, по крайней мере во многих своих чертах, будет сохраняться на
                протяжении всего «отрезка» континуума или даже длиться за его пределами, до «конца»
                континуума. Но обратимся к § [а] сутры.</p>

            <p>Сознание (или мысль, здесь это опять синонимы) всегда является уже аппроприирующим
                <em>все</em> семена. Иначе говоря, все после-факты, остатки мыслей, все «запахи
                запахов» розы. Но эти семена, составляющие аппроприирующее или остаточное сознание,
                — не куча, не частицы в броуновском движении. Они являются только в определенной,
                более или менее стойкой конфигурации, и каждое из этих семян имеет тенденцию к
                образованию данной конфигурации, то есть данного индивидуального остаточного
                сознания <em>(alayavijnana)</em>. Более строго говоря, каждое семя и есть такая
                тенденция, а данное остаточное сознание — конфигурация всех тенденций. Но что значит
                «тенденция»? Вопрос, как в смысле Учения о Сознании, так и общей теории Абхидхармы,
                — безумно трудный. Отвечая на него именно в этом смысле, напомню, что сама по себе
                (то есть как возникающая) мысль не имеет содержания [опять напоминаю из текста (14),
                что ее объекты (<em>alambana</em>) не есть содержание, ибо они возникают вместе с
                ней в составе «случая»]. Тенденция здесь — это чистая «склонность». Возьмем для
                примера детскую игру мозаику, где каждый кусочек картины имеет свое содержание
                только как фрагмент целого. Теперь постараемся себе представить, что такие кусочки
                никогда, при всем умении играющего, не сложились бы в целую картину, если бы каждый
                из них не обладал своей склонностью в нее сложиться. Палеонтолог, например, мне
                может возразить, что, исходя из известных правил строения живого организма, он
                восстановит весь скелет мамонта из фрагмента нижней челюсти. На что я ему скажу,
                прекрасно, но у вас есть мысль, имеющая своим содержанием то, что называется
                «мамонт», а в нашем распоряжении такой мысли не будет иметься, и, следовательно, мы
                не можем от ее содержания идти к ее фрагменту как к <em>ее</em> фрагменту, потому
                что не будет задано ее содержание как целого. Ибо в нашем случае, то есть в случае
                Учения о Сознании, содержание будет возникать вторично (скорее, третично,
                четвертично и т. д.) в процессе реализации (букв. — «приготовления»,
                <em>vipaka</em>) этих фрагментов, семян-тенденций, остатков в одном целом данной
                конфигурации остаточного сознания. На самих отдельных фрагментах нашей мозаичной
                картинки ничего не изображено. В том же § [а] это сознание названо глубоким и
                тонким. Это не эпитеты поэтического языка, а специальные термины учения о сознании,
                Виджнянавады. «Глубокий» здесь характеризует каждую точку (мысль) конфигурации
                остаточного сознания как возникающую взаимообусловленно из - или с - другой. Каждая
                из этих точек ведет вглубь, в порядке, обратном порядку возникновения, как в
                приведенном выше примере: от запаха запаха розы — к запаху розы как осознанному в
                сознании ума — к запаху розы как факту сознания обоняния — к контакту органа
                обоняния с его объектом (рассуждая строго абхидхармистски, мы можем продолжать идти
                «вглубь» в обе стороны до бесконечности). Но если перевернуть это пространство
                конфигурации и идти вглубь от серий мысли, образованных чувственными контактами, к
                их осознаниям, осознаниям осознаний, осознаниям осознаний осознаний и т. д., то с
                каждым следующим шагом «содержание» мысли будет все более и более утончаться, все
                более и более превращаясь в то, что мы условно называем тенденцией. Тенденцией или
                склонностью к определенным конфигурациям, которые, согласно Виджнянаваде, чем они
                «тоньше», тем устойчивее в воспроизведении тех же или сходных остаточных сознаний.
            </p>

            <p>И вот тут-то оказывается, что, думая об этих семенах, остатках, тенденциях,
                результирующих в индивидуальных конфигурациях остаточного сознания, мы оказываемся
                философски вынужденными признать, что они должны обладать чем-то еще, точнее, быть
                чем-то еще, кроме того, чем они, номинально по крайней мере, являются (то есть
                сознанием). И это «что-то еще», чем они являются, не может быть ничем иным, кроме
                силы или энергии связи, о которой говорилось в предыдущих текстах как о санскарах.
                Это энергия, которой каждый остаток должен быть уже заряжен в своей тенденции (может
                быть, лучше — в направлении) к воспроизведению конфигурации остаточного сознания.
                Или, скажем так, энергия, которая не только связывает мгновения возникновения мысли
                в особые (по органам чувств) серии мысли, результирующие в агрегате сознания
                обоняния как «первоначальный» запах розы, и которая не только «синтезирует» запах
                розы в сознании ума. Но эта дополнительная энергия, которая выделяет — поверьте, я
                сам в ужасе от натурализма моих выражений — из запаха розы запах запаха (и т. д.) и
                которая воспроизводит этот остаток в конфигурации остаточного сознания. И дальше, в
                § [в] и [с] Сутры, мы видим конкретизацию того, что аппроприируется остаточным
                сознанием, конкретизацию, пределом которой «вниз» будет тело и шесть органов чувств,
                а «вверх» — сверхтончайшие «остатки остатков» мыслей. И наконец, это сознание все
                связывает в одно, в какое-то «телосознание» (<em>vijnanakaya</em>). В [с] мы читаем,
                что оно — мысль, вбирающая в себя все феномены (дхармы) всех органов чувств (и ума).
            </p>

            <p>Сейчас мы подошли к тому моменту, когда нам придется пренебречь рекомендацией
                Витгенштейна и, полностью отвлекшись от значений слова «сознание» в наших словарях,
                попытаться определить сознание только на основании позиции только что цитированной
                сутры. Но, подчеркиваю, это определение все равно мое, и по очень простой причине:
                его нет в самих виджнянавадинских текстах. Итак, думая о сознании как об «остатках»
                (сознательного содержания), тенденциях (направлениях) и энергиях, я говорю: Мысль
                возникает только в присутствии других мыслей. Тогда, то есть относительно этой
                данной возникающей мысли, эти другие мысли будут называться «сознание». Остаточное
                сознание виджнянавадинских текстов будет тогда одной из возможных конкретизации
                сознания в смысле этого определения.</p>

            <p>Переходя, наконец, к § [е], мы видим, что при всей почти синонимичности сознания и
                мысли последняя выступает как философски более широкое понятие: «мысль мыслит только
                мысль (<em>cittamatra</em>)». Заметим опять же, это не постулат, а позиция. Ибо
                именно как постулат «только мысль» может быть (и была неоднократно за последние
                полторы тысячи лет) понята только неправильно. «Только мысль» совсем не означает,
                что ничего, кроме мысли, нет, — ибо не забывайте, что в буддизме нет ни «есть», ни
                «нет», а только «возникает» или «не возникает». Это означает только, что ничего,
                кроме мысли, мысль не мыслит. Что же тогда о том, что не мыслится или немыслимо (<em>асintya</em>)?
                — А ничего, ведь об этом и не говорится (в этой сутре, по крайней мере). Говорится
                только о том, что взаимообусловленно возникает, то есть опять же о мысли. А мое
                философское — назовем это так — объяснение является не более чем парафразой
                виджнянавадинской позиции.</p>

            <p>Позиция этого текста обычно оценивается историками философии и многими буддологами
                как крайняя. Это совершенно неверно. Скорее, мы имеем здесь дело с очень
                своеобразным (даже для буддийской философии) опытом систематизации материала учения
                о дхармах (<em>abhidhanna</em>), которое само остается более или менее тем же во
                всех основных школах буддийской философии и во все времена. Мысль и сознание
                остаются исходной точкой философствования в большинстве текстов, о чем бы в них ни
                шла речь. Подумайте, если вас спросят, что есть самое главное в буддизме, то вы, не
                колеблясь, ответите: ну, это Четыре Благородные Истины, это Будда, Совершенный
                Пробужденный, достигший освобождения от страдания и открывший людям Путь к
                Освобождению от Страдания, прежде всего через устранение неведения и т. д. Но
                спросим себя, а что такое страдание? — Повторяю, философски, страдание — это
                сознание. (Напоминаю, не наоборот — сказать, что сознание есть страдание, будет в
                этом смысле непростительной философской банальностью!) А неведение, ошибка,
                заблуждение? Тут надо быть очень осторожным с ответом. Да, конечно, и неведение, и
                ошибка, и заблуждения рассматриваются как состояния непробужденного (или, по крайней
                мере, йогически не культивированного) сознания. Но необходимо уточнить: а насчет
                чего будут эти неведения, ошибки и заблуждения? — Никак не насчет того, что на нашем
                философском языке называется «внешний мир». Потому что в буддийской философии мир не
                может быть ни внешним, ни внутренним — слов таких нет. Поэтому сказать, что для нее
                весь мир — это мираж, иллюзия, майя, будет такой же философской пошлостью, как
                сказать, что мир непознаваем (или, напротив, познаваем). Иллюзия, неведение, ошибка,
                это о мысли, которая различает внешнее и внутреннее, мыслящее и мыслимое и, может
                быть, самое главное — мысль и мыслящего. Последнего нет, ибо, как сказано в нашем
                последнем тексте, мысль и мыслимое — одно. Это мысль мыслит, только условно
                называясь «мыслящим». Мышление (сознание) и есть его (свой!) собственный субъект.
                Отсюда прямо следует невозможность философской антропологии (как, впрочем, и
                теологии) в буддийской философии.</p>

            <p>Однако было бы грубым преувеличением представлять себе дело так, как если бы в
                буддийской философии не было темы человека. Напротив, в текстах как Малой, так и
                Большой Колесниц есть немало мест, где говорится о том, что только человек может
                стать Буддой в данном (последнем?) своем рождении, и о том, что «оказаться» в
                человеческом рождении — редчайшая удача. Но ни в одном из известных мне буддийских
                текстов эта тема не была философской позицией. Заметьте, феноменологически тема и
                позиция — вещи совершенно разные, и прежде всего в отношении текста (в смысле того,
                о чем говорилось в первой лекции). Тема всегда предполагает центростремительность
                текстовой интенции. Говоря совсем просто, тема — это о чем-то, что само вовсе не
                обязательно станет точкой отсчета в рассмотрении самого себя. Позиция —
                интенционально — всегда центробежна. И если она оказывается точкой рассмотрения
                самой себя, то это предполагает, что она сама уже вынесена, уже стала объектом,
                отличным от того, чем она была (и есть) как позиция. Человек в буддийских текстах
                всегда фигурирует как потенциальный «не-человек», то есть в своей возможности
                достижения Полного Пробуждения или Предельного Состояния Мысли Пробуждения <em>(bodhicitta)</em>
                Бодхисаттвы. Именно как «не-человек», ибо никакая человеческая черта (включая
                «человеческое» сознание, при всей буддологической неправомерности этого выражения)
                не переходит, не включается в состояние, запредельное человеческому, — с позиций
                Взаимообусловленного Возникновения Мысли, Невозможности Мысли и Остаточного
                Сознания.</p>

            <p>Так, мы подходим к концу этого короткого и одностороннего экскурса в буддийскую
                философию. В этих лекциях я почти полностью опустил такой важнейший аспект буддизма,
                как сотериологию и связанное с ней учение об Архатах, Буддах и Бодхисаттвах, то есть
                собственно буддологию. Хуже того, я даже не коснулся такой интереснейшей и чисто
                философской части буддизма, как учение о двух (или трех) истинах, не говоря уже об
                учении о телах Будды. Просто так получилось, что в фокусе моего философствования
                оказалось буддийское учение о мысли и сознании, которое я и попытался здесь
                изложить. И если представить это учение как одну обобщенную позицию, то основными
                моментами этой позиции будут — в сопоставлении с нашим привычным набором
                постулятивно вводимых философских категорий:</p>
            <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
                <li class="my-2">Текст — вместо идеи.</li>
                <li class="my-2">Дхарма как мысль — вместо вещи или события.</li>
                <li class="my-2">Мысль — как мышление, мыслимое и мыслящее.</li>
                <li class="my-2">Возникновение, становление — вместо бытия.</li>
            </ol>

            <p>Я думаю, что исторически буддизм был первой попыткой исследования мысли и сознания с
                точки зрения мысли и знания.</p>

            <h2 class="text-center text-zinc-600 font-bold text-xl sm:text-xl md:text-xl mt-6 mb-6">
                Дополнение о философии сознания</h2>

            <p>Самое трудное в понимании того, о чем говорилось в этих лекциях, — это то, что
                сознания (мысли, мышления и т. д.) — <em>нет</em>. Нет не в смысле «невозможности
                мышления» в начале четвертой лекции, а в буквальном смысле абхидхармистского
                положения, что сознание мыслимо только как возникновение и что все возникновения
                мыслимы только как сознания. Таким образом, сознание здесь — условное обозначение
                <em>(vacana matra) мыслимости</em> возникновения чего-либо как сознания. И когда
                Будда говорил Кашьяпе о <em>не-мыслимости</em> сознания, то речь шла об опять же
                условном обозначении того, что <em>не-возникает</em>.</p>

            <p>Теперь возвратимся к концу нашей последней лекции и спросим: в каком же смысле можно
                говорить, что буддийская философия была первой попыткой исследования со знания? Ведь
                какие бы тексты мы ни брали, само сознание не исследуется. Всегда исследуется,
                анализируется, классифицируется что-то другое в <em>отношении</em> сознания. Говоря
                более точно, исследуется именно это отношение, мыслимость которого называется
                сознанием. При этом <em>отсутствие</em> такого отношения в своей немыслимости будет
                называться тоже «сознанием», но другим, служащим как бы «обозначением
                необозначимого», тем пределом условности где слово есть произвольное название точки,
                от которой происходит обратное движение мысли: от не-мыслимого к мыслимому, от
                отсутствия отношения (то есть необусловленности) к отношению, от не-смысла к смыслу.
                Тогда на заданный выше вопрос я бы ответил следующим образом: исследованием сознания
                здесь называется исследование движения мысли (йогической мысли, мысли Бодхисаттвы,
                которая сама себя исследует) от сознания как отношения к себе других (всех)
                феноменов <em>(дхарм)</em> к сознанию немыслимому (и не мыслящему себя) в отношении
                чего бы то ни было другого. Притом что это движение мысли исследуется с позиции
                «сознания» в его втором, трансцендентальном смысле, то есть как «не-исследуемого», с
                позиции которая сама неопределима как «позиция чего-то». Но вернемся к сознанию в
                первом смысле.</p>

            <p>Сознание здесь не онтологический абсолют, а пустая (лишенная содержания) позиция, о
                которой можно говорить только с того момента, когда что-то (то есть какой-то
                мыслимый объект) с ней <em>соотносится</em>. Но и тогда, как об этом уже говорилось
                выше, речь может идти только о содержании соотносимых с этой позицией обьектов, да и
                то лишь со стороны этих объектов, а не со стороны «обратно» соотносимого с ней
                сознания. Тогда объекты будут <em>мыслимы</em> как что-то другое (то есть сознание),
                а сознание будет немыслимым, то есть не-объектом. <em>Не-мыслимость</em> здесь
                условно обозначает неустановимость отношения объекта к сознанию, отношения, которое,
                в силу сказанного выше, устанавливается («генерируется») сознанием и в «обратном»
                порядке осознается как переносимое на объект или, скажем, как свойство объекта. Ну,
                такое же в конечном счете, как и мыслимость.</p>

            <p>Тут будут трудности с языком. Хотя, на самом деле, не с языком, а с нашим <em>пониманием</em>
                того, что такое язык, весьма отличающимся от буддийского понимания. Начнем с того,
                что в нашем современном понимании языка мы исходим из <em>общей идеи</em> о языке
                как универсальной <em>общечеловеческой</em> константе, как инвариантной постоянной
                человеческого существования, исторически (и географически) представленной в
                многообразии ее конкретных языков-вариантов. Тогда каждый данный естественный язык
                оказывается <em>интуитивно</em> (теория здесь совершенно невозможна!)
                противопоставленным «языку вообще», <em>представленному</em> языком описания
                (метаязыком), то есть другим естественным языком, на котором данный язык описывается
                (при том, разумеется, что они могут и совпадать, — тогда различие между ними будет
                чисто функциональным). Одновременно — и это другая интуиция, лежащая в основе
                современного понимания языка, — мы исходим из идеи о сознании (или мышлении) как
                эпифеномене языка, как о том, что предоставлено мышлению исследователя в языковых
                репрезентациях.</p>

            <p>В буддийской философии сознания язык всегда понимается не как язык вообще, а только
                <em>в данном случае</em> (в том смысле, в котором «случай» объясняется в третьей
                лекции). Этих случаев очень много, их классификация всегда будет
                <em>политетической,</em> то есть основанной на двух или более признаках. Я сейчас
                вкратце рассмотрю некоторые из них, предупреждая при этом, что порядок их
                рассмотрения никак не следует принимать в смысле иерархической или какой-либо иной
                ценностной системы (последнее возможно, но будет особым «случаем»):</p>
            <ul class="list-none pl-5 space-y-1 ml-8">
                <li class="my-2">1) Случай, когда о языке (не важно, вообще или конкретном языке) можно сказать, что
                    «язык — это дхарма»<br>Или, поскольку «все дхармы ментальны» [как в тексте (1)] что «язык — ментален»
                    (сравним наше: «сознание — языково»). Или: «язык — эпифеномен сознания».</li>
                <li class="my-2">2) Случай, когда Дхарма (текст Учения) излагается (в данном случае — пишется) на
                    каком-либо конкретном языке, ну, скажем тибетском или монгольском. Такой текст
                    всегда начинается словами: «На санскрите название этой сутры (тантры, шастры и т.
                    д.) такое-то, на тибетском — такое-то». Это никак не предполагает, однако, ни того,
                    что тибетский язык как язык перевода противопоставлен языку оригинала (санскриту) по
                    аутентичности, ни того, что оба они противопоставлены «языку вообще». Ибо Слово
                    Будды (<em>buddhavacana</em>) в этом случае есть <em>не-слово,</em> есть факт
                    (случай!) Пробужденного Сознания, только условно приуроченный к языку, все равно
                    какому, санскриту или тибетскому. И только в <em>слышании</em> этого учеником эта
                    приуроченность становится фактом языка — поэтому все сутры начинаются словами
                    слышавшего: «Так я слышал» (<em>evam maya srutam</em>). Именно поэтому в позднейших
                    учениях о двух (или трех) истинах утверждается, что только в устах Будды слово -
                    высшая истина (<em>paramartha satya</em>), в слушании же и передаче учеников оно не
                    более чем относительная истина (<em>sam-vrti satya</em>).</li>
                <li class="my-2">3) Случай, когда (как в Алмазной Сутре) все то, что называется, есть не более чем
                    язык, — «оттого так и говорится, Субхути, что для знающего, что этого — нет, оно так
                    и называется». Это, конечно, предельный случай, когда в соотношении с высшей истиной
                    в Запредельности Сознания (<em>prajnaparamita</em>) слово — знак не-существования
                    того, что оно обозначает. Таким образом, это случай негативного соотнесения языка и
                    сознания.</li>
                <li class="my-2">4) И наконец, случай, когда язык противопоставлен тексту (см. начало первой лекции и
                    текст (9) опять же из Алмазной Сутры). В этом случае текст (сказанный или
                    написанный) обладает полнотой <em>вещности</em> и находится в совсем другом
                    измерении (он «неизмерим» и «неисчерпаем»), в сфере сознания, где обретаются
                    Благородные (<em>aryapudgala</em>). Язык здесь — то, чем «обходятся», читая,
                    переписывая и разъясняя текст. Текст — единственный объект информации. Его
                    священность — в его передаче от эона к эону, от мира к миру.</li>
            </ul>

            <p>Разумеется, то, что мы называем «философия языка», где язык фигурирует как особый
                объект философского рассмотрения, будет, с точки зрения буддийской философии
                сознания, такой же бессмыслицей, как «философия религии» или «философия любви». Ибо
                язык здесь чисто функционален (его нет вне «случаев») и вторично производен в
                отношении сознания. Нет такого случая, в котором сознание репрезентировало бы себя в
                языке. Но можно говорить о каких-то правилах рассмотрения сознания, мышления о нем с
                позиции другого сознания (сознания другого, скажем, йогического уровня),
                репрезентируемых языком. Язык здесь репрезентирует своего рода метасознание, то есть
                <em>то о сознании, к</em> чему обращено йогическое мышление медитирующего. Именно
                «о», а не саму эту сторону сознания, не говоря о сознании в целом (какового,
                разумеется, нет). Это можно понять по аналогии, скажем, с инструкцией по обращению с
                электроприбором, которая основана в конечном счете на законах электродинамики
                (уравнениях Кларка Максвела), но которая сама не есть ни эти законы, ни, менее
                всего, <em>то</em>, о чем в этих законах говорится. (Тем не менее мы не можем
                говорить, что эта инструкция не имеет к этим законам никакого отношения.)</p>

