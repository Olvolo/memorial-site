            <p>Эту лекцию я не начинаю с вопроса «возможно ли сознание?» — ибо в смысле позиций
                Возникновения Мысли и континуума мысли, изложенных в предыдущей лекции, сознание
                всегда есть. Но чтобы это было легче понять, обратимся сначала к слову «сознание» в
                нашем повседневном языке. Сейчас я суммирую основные значения этого слова по
                Оксфордскому словарю, Словарю Коллинза и Словарю современного русского языка Ушакова
                что, я думаю, вполне достаточно для нашего с вами, <em>опять</em> же обыкновенного,
                понимания этого слова (мои добавления — в скобках).</p>

            <p>Первое значение. Сознание — это осознание, осознавание того, с чем данный человек
                имеет дело, включая его, самого, совершаемые им действия, говоримые им слова,
                мыслимые им мысли, так же как и действия, слова и мысли других людей, факты и
                события мира и т. д. [Ну, разумеется, это предполагает, что кто-то (этим кто-то
                может оказаться и сам данный человек) знает или может знать, с чем данный человек
                имеет дело. То есть это содержит импликацию (крайне рискованную!) существования
                некоторого «объективного положения вещей» и «объективного наблюдателя» этого
                положения вещей. Кроме того, это предполагает конкретный вопрос (запрос,
                необходимость и т. д.) относительно того, <em>что</em> осознается, ответом на
                который и будет экспонирование человеком его осознания.]</p>

            <p>Второе значение. Сознание — это состояние, в котором осознавание, в смысле первого
                значения, имеет место либо может иметь место. (Что, разумеется, предполагает, что
                есть другие состояния, в которых осознавание не происходит или не может происходить,
                но которые, чисто семантически, являются производными от первых. Примеры таких
                состояний даны в широчайшем диапазоне — от глубокого сна до полных амнезий.)</p>

            <p>Третье значение. Это — способность осознавания, мыслимая как некое органическое
                свойство, приписываемое одним объектам и неприписываемое другим. Во всех трех
                значениях самосознание предполагается как производное от сознания по объекту, то
                есть когда объект сознания является и его субъектом.</p>

            <p>Никогда не спорьте со словарем. Спорить со словарем — это не деконструкция, а
                глупость. Но деконструкция, которая интуитивно исходит из тождественности слова
                концепту (как Витгенштейн исходил из тождественности концепта слову), не философия,
                а вырожденная филология. Заметьте, что, вводя в первой лекции «текст» вместо «идеи»,
                я уже этим отрицал возможность их тождества. Какое может быть тождество, когда
                «идея» в буддийской философии не деконструируется, а отсутствует? Как отсутствует
                «Я» в качестве субъекта мышления и сознания, но присутствуют мышление и
                сознание.</p>

            <p>Теперь, перед переходом к нашему последнему тексту, который я называю текстом о
                сознании, я постараюсь объяснить буквальный смысл слова «сознание» в буддийской
                философии. Но объяснить его таким образом, как если бы я пользовался буддийским
                словарем для перевода значений этого слова в смысле его значений в наших словарях.
                (Из этого уже должно быть ясно, что процедура объяснения слова «сознание» будет
                совсем другой, чем процедура объяснения слова «мысль». Помните, в предыдущей лекции,
                «мысль — это citta?»)</p>

            <p>Я думаю, что в первом буддийском значении слово «сознание», применительно к
                отдельному континууму мысли (или одушевленному существу), будет означать сумму тех
                содержаний, которые возникающая мысль находит уже готовыми при своем возникновении.
                Или, говоря примитивно-эмпирически, скажем берклеански, это те уже имеющиеся в
                континууме «идеи», которыми эта мысль может оперировать как осознаваемым или могущим
                быть осознанным. Взятое в этом значении сознание может классифицироваться по органам
                чувств (включая manas, ум, разум) или по другим, так сказать, «органическим»
                признакам, связанным с его условной локализацией в отдельном живом существе (как
                континууме мысли). При этом, однако, сознание как сознание, то есть в смысле <em>сознания
                    ума (manovijnana)</em>, в отличие от сознания зрения, слуха и т. д., служит
                здесь единственным синтезирующим уровнем, на котором все воспринимаемое, воспринятое
                и могущее быть воспринятым есть сознание (сейчас я не буду вдаваться в детали, как
                это происходит в буддийской теории сознания).</p>

            <p>В своем втором буддийском значении, сознание — в отличие от его более простого и
                однолинейного понимания в текстах палийской Абхидхармы — является не только фактом
                осознания, осознанностью, осознаваемым и т. д., но и своего рода (какого, будет
                сказано ниже) «после-фактом» этого факта. Объяснить это безумно трудно в силу той же
                двойственности трансцендентального и не трансцендентального в суждениях, о которой
                говорил Эдвард Конзе. Возьмем простой (описуемый) факт чувственного восприятия в его
                самой простой буддийской трактовке, факт обоняния розы. Этот факт предстает (точнее,
                конечно, созерцается) в порядке серий (<em>vithi</em>) дхарм, примерно таким
                образом:</p>
