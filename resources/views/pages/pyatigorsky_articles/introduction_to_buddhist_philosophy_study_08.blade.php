            <h3 class="text-xl text-center font-semibold text-zinc-700 my-4"><em>ДХАРМА</em>, УМ И МЫСЛЬ. КТО
                ЕСТЬ <em>БРАХМАН?</em></h3>
            <p><em>Дхаммапада</em> — самый известный и наиболее широко распространенный из текстов Палийского
                канона, быть может, даже самый известный из всех буддийских текстов вообще. Это, по
                существу, буддийский катехизис, составленный из изречений, высказанных в поэтической
                форме Буддой по разным поводам, в разные периоды его жизни и обращенных к разным
                собеседникам. Изречений, весьма различных по содержанию, с трудом поддающихся даже
                самой общей тематической классификации. Оттого и наш выбор изречений в <em>Дхаммападе</em>
                представляется произвольным. Но — только на первый взгляд.</p>

            <p>Итак, вначале мы читаем: «<span class="underline">(Все) феномены, дхармы умственны</span>». Таков кратчайше и
                точнейше сформулированный постулат буддийской философии о <em>дхармах</em>. Но заметьте,
                именно о <em>дхармах</em>, не об уме. Тут же приводится двойной пример: «Если кто-нибудь
                говорит или делает с нечистым умом, то за ним следует несчастье ... и если
                кто-нибудь говорит или делает с чистым умом, то за ним следует счастье». Этот пример
                нам необходимо помнить все время, пока мы будем рассуждать о <em>дхармах</em> и уме. Но где
                же в этом примере <em>дхармы</em>? Да очень просто. То, что кто-нибудь говорит, — одна
                <em>дхарма</em>; то, что кто-нибудь делает, — другая; нечистый ум — третья; чистый —
                четвертая и т.д. И поскольку ум в буддийском операциональном определении — это то,
                что «мыслит в мыслях», то <em>дхарма</em> — это объект мысли, любой и всякой мысли,
                настоящей, прошлой и будущей, включая, разумеется, саму мысль, так же как и ум.
                Тогда постулат о <em>дхармах</em> оказывается чистой тавтологией, не так ли? Так и не так,
                ибо здесь тавтология служит ключом к разгадке самой тайны буддийской философии:
                «Есть ли то, что не мыслит и не мыслится? А если есть, то как оно соотносится с тем,
                что мыслит и мыслится?»</p>

            <p>Следующий шаг. Раз все <em>дхармы</em> умственны согласно постулату о <em>дхармах</em>, а ум бывает
                чистым или нечистым, то получается, что и <em>дхармы</em> определяются умом как нечистые или
                чистые. На самом деле это совсем не так. <em>Дхармы</em> необязательно отмечены плюсом или
                минусом, ибо ум, «мыслящий в мыслях», необязательно либо чист, либо нечист. Да
                вообще все, что угодно, необязательно является злом или добром. Здесь должно быть и
                нечто третье, в объяснении чего нам придется выйти за пределы нашего текста. Из
                предыдущих семинаров мы знаем, что <em>дхармы</em> бывают не только благими или не-благими,
                но и неопределимыми или нейтральными (как — если обратиться к психологии — чувства
                или эмоции). Та же тройственность приписывается и мыслям, а также и <span class="underline">умам</span>, эти мысли
                генерирующим. Однако, чтобы перейти от общей тройственности <em>дхарм</em> к тому, что мы
                назвали «третьим», вернемся к постулату об умственности <em>дхарм</em>. Оказывается, что в
                этой общей умственности <em>дхармы</em> опять-таки неодинаковы.</p>

            <p>Они четко разделяются на две категории. Во-первых, это <em>дхармы</em>, умственность,
                ментальность которых является их общим, не дифференцирующим признаком; такие,
                скажем, как <span class="underline">объекты</span> мысли или ума, т.е. все, что мыслится, будь этот объект
                ментальным, как сам ум, или физическим, материальным. Во-вторых, это специфически
                умственные объекты, такие как ум, мысль, идея, знание, воление, намерение и т.д. В
                <em>абхидхармистской</em> философской традиции <em>дхармы</em> первого рода стали называться «имеющие
                форму», а <em>дхармы</em> второго рода — «бесформенными». Но опять же, поскольку все <em>дхармы</em>
                умственны как <span class="underline">объекты мысли</span>, то мы могли бы переопределить само понятие «дхарма».
                Тогда <em>дхарма</em> — это <span class="underline">индикатор умственности</span> в самом элементарном смысле: если есть
                что-то, что по определению является объектом мысли, то оно <span class="underline">указывает</span> на то, что там
                есть и мысль, а возможно, и пока еще гипотетическое «то, что мыслит». Но
                одновременно о <em>дхарме</em> можно мыслить и как о своего рода <span class="underline">единице мыслительности</span> и,
                таким образом, как о <span class="underline">минимуме всего мыслимого и мыслящего</span>, неделимом в пространстве
                и времени. <em>Дхарма</em> — это предел атомизации мыслительного космоса буддийской
                философии.</p>

            <p>Но эти квазиопределения <em>дхарм</em> подводят нас к пределу, за которым древние буддийские
                мыслители, вместо того чтобы поставить точку, резко меняют направление своего
                философствования и задают вопрос, казалось бы, парадоксальный, если не бессмысленный
                в свете этих квазиопределений: «А что, если мы возьмем не <em>дхарму</em> вообще, а <em>дхарму</em>
                как конкретный феномен, событие, факт, поступок, суждение, — который, как мы уже
                знаем, мыслителен и соответственно благ, дурен или неопределенен, и отделим его от
                столь же конкретной благой или дурной мысли? Или же, что, если мы — в смысле
                сказанного нами в начале этого параграфа — отделим в нашем рассмотрении <em>дхармы</em>
                первого рода от <em>дхарм</em> второго рода, <em>дхармы</em> вообще от <em>дхарм</em> специфически
                умственных?</p>

            <p>Ответ на этот вопрос дается во многих буддийских текстах, но именно в комментарии на
                высказывание «(все) <em>дхармы</em> умственны... и так далее» он предельно конкретен и
                однозначен: убийство без мысли об убийстве не есть убийство, т.е. не есть <em>дхарма</em>,
                называемая «убийство». А что же это тогда будет за <em>дхарма</em>? Да просто <em>дхарма</em>, сама по
                себе, ни дурная ни благая, возникшая ни с нечистым умом, ни с чистым. Словом, <span class="underline"><em>дхарма</em> как она есть</span>. Тогда убийство из случайно выстрелившего пистолета будет столь же
                абсолютно нейтральной <em>дхармой</em>, как и любое катастрофическое явление природы.
                Следующим шагом в буддийском философствовании была экстраполяция этого ответа на все
                <em>дхармы</em>, иначе говоря, на всё; экстраполяция, нашедшая свое выражение <span class="underline">во втором
                постулате о <em>дхармах</em>, все <em>дхармы</em> таковы, какие они есть, иначе говоря — никакие</span>.</p>

            <p>На этом мы останавливаемся в нашем рассмотрении <em>дхарм</em> в их отношении к уму, мысли и
                т.д., то есть в нашем рассмотрении <em>дхарм</em> в их умственности. Теперь мы переходим к
                рассмотрению ума, сознания, мысли <span class="underline">как <em>дхарм</em></span>, то есть в их дхармичности, так сказать.
                В этом рассмотрении <em>дхармы</em> становятся сами «позицией», с которой будет исследоваться
                любой объект (т.е. опять же любая <em>дхарма</em>!). Не будет преувеличением сказать, что в
                Северной Индии, примерно в IV—V веках до н.э. возникла «странная» традиция, тогда
                еще только устная, — традиция дхармического мышления и дхармического созерцания (не
                будем при этом забывать, что еще до этого времени само слово «<em>дхарма</em>» имело за собой
                долгую историю употребления в самых различных значениях, чисто религиозном,
                этическом, ритуальном, юридическом и многих других). Традиция эта была создана
                древними учителями буддизма, которые придумали, выдумали (не будем бояться слов!)
                радикально новый способ мышления — <span class="underline">мышление в <em>дхармах</em></span>. Последнее выражение надо
                понимать столь же буквально, как, скажем, «говорение в словах» (а не в жестах или
                криках). Значит, каждая из <em>дхарм</em>, в которых буддийский йог думает об определенном
                объекте, может, в свою очередь, сама превратиться в позицию, являющуюся частным
                случаем общей дхармической позиции.</p>

            <p>Тогда нам придется допустить, что те древние буддийские учителя уже не жили, как
                живем (или думаем, что живем) мы, в мире чувств, мыслей, поступков и событий, а жили
                в совсем другом мире, мире <em>дхарм</em>. <em>Дхарма</em> стала <span class="underline">универсальным деноминатором</span> в языке
                буддийской философии и основным инструментом буддийского созерцания. Поэтому и нам в
                наших попытках <span class="underline">понять</span> буддийскую философию будет необходимо попытаться думать и о
                своем думанье с дхармической точки зрения. Идем дальше. Теперь при всех оговорках
                насчет нашего как бытового, так и научного и философского употребления понятий
                «объективное/субъективное», мы могли бы условно охарактеризовать буддийскую
                дхармическую позицию как <span class="underline">объективную</span>. Она объективна в двух смыслах. Во-первых, в
                смысле отсутствия субъекта в его противопоставлении объекту (мышления), поскольку
                «нет такой <em>дхармы</em>, как некое абсолютное «Я». Во-вторых, в том смысле, что мышление в
                <em>дхармах</em> полагается исходящим из трансцендентального Высшего Знания всеведущего
                трансцендентального Будды, то есть из знания объективного по преимуществу. Но все же
                главным, чисто прагматическим препятствием к нашему пониманию дхармического аспекта
                буддийской философии является отсутствие у нас <span class="underline">воспитанной привычки думать о
                собственном</span> (а затем и о любом другом) <span class="underline">думанье</span>, что безусловно являлось основой
                буддийского созерцания, <em>дхьяны</em> и основным занятием буддийского йога. Для буддийского
                йога его собственное мышление — это чужой, как бы «вынесенный» объект, вроде камня
                или дерева. Да и сам йог становится чужим своему мышлению (а тем самым и самому
                себе).</p>

            <p>Итак, опять от <em>дхарм</em> к уму. Зная из предыдущего семинара, что ум есть то, что думает,
                и что «Я» есть «Я, что думаю», мы, занимаясь сейчас <em>Дхаммападой</em>, обнаруживаем, что
                здесь все в третьем лице: «он, кто...», «если кто делает или говорит ... думает» и
                т.д. Здесь ум оказывается отделенным от того, кто говорит, делает, поступает и
                думает, но он отделен совершенно другим образом, чем он отделен от «меня» в тексте
                VII. В <em>Дхаммападе</em> только ум дает тот кармический эффект, который будет его (ее,
                кого-то) будущим счастьем или несчастьем. В <em>Дхаммападе</em> ум — это <span class="underline">константа</span>, что,
                однако, не значит, что он <span class="underline">производит</span> мысль, ибо, как будет показано в следующем
                семинаре, когда мысль возникает, ум, <span class="underline">который мыслит</span>, уже здесь, как бы совпадая с
                мыслью в моменте ее возникновения. Но каково же тогда «будущее» ума? Да и вообще,
                где он будет в то время, когда «кому-то, поступающему, говорящему и думающему»
                придется пожинать плоды его слов, действий и мыслей, определяемых тем же умом?
                Простого ответа на эти вопросы нет. А сложным будет такой: при условии, что мы
                говорим о <span class="underline">будущем</span> ума только с точки зрения <span class="underline">настоящего времени</span> в будущем того, кто
                «делает», «говорит» и т.д., будущим умом явится <span class="underline">прошлый ум</span> этого условного
                «кого-то». Из этого ответа может следовать, что <span class="underline">ум — в отличие от отдельной мысли,
                всегда моментальной и всегда в настоящем времени — является феноменом, внутренне
                связанным со временем</span>. Тогда «обратной» формулировкой будет: время — это время
                умственной деятельности, время работы ума. А отсюда прямо следует, что время, в
                котором кто-то говорит и делает, мыслит и поступает, — это время ума, <span class="underline">ментальное время</span>.</p>

            <p>Если считать, что ум в первых строках <em>Дхаммапады</em> синхронен словам и действиям того,
                кто говорит и действует с чистым или нечистым умом, то время этой синхронности будет
                <span class="underline">микровременем</span> ума, тогда как время между этим событием «ума-речи-действия» и его
                кармическим эффектом (счастьем или несчастьем) будет <span class="underline">макровременем</span> ума. Однако все
                это не значит, что ум в чьем-то прошлом, настоящем и будущем является одним и тем же
                самым феноменом, «длящимся» из прошлого через настоящее в будущее. Ум не может быть
                «тем же самым» не только в силу универсального непостоянства и универсальной
                не-самотождественности всех <em>дхарм</em>, но прежде всего потому, что говорить о «чьем-то
                уме сейчас» и «чьем-то уме в другом случае» как об одном уме будет возможным только
                для чьего-то ума, который думает о другом уме как «о себе, но в другое время».</p>

            <p>Наше краткое рассуждение о времени в <span class="underline"><em>Дхаммападе</em> мы могли бы заключить словами: это —
                время без бытия чего-либо мыслимого во времени, время как производное от мышления,
                но не как условие последнего</span>.</p>

            <p>Попробуем теперь перейти от времени к <span class="underline">пространству</span>. Посмотрите, ведь «он», «кто-то» в
                начале нашего текста можно считать своего рода <span class="underline">местом ума</span>, пространственной <span class="underline">единицей умственности</span>, ограниченной <span class="underline">формой</span>, т.е. телом живого существа. В случае же, когда мы
                имеем дело (как в двух предыдущих текстах) с бесформенным или бестелесным существом,
                т.е. с набором не из пяти, а из четырех агрегатов, <em>скандх</em> индивидуального
                существования, ум помещается буддийскими созерцателями-философами в пустом,
                недифференцированном пространстве, <em>акаше</em>. Как философское понятие пространство
                полагается той <span class="underline">средой</span>, в которой ум различает объекты внешнего восприятия.
                Соответственно пространство будет тем же и для <span class="underline">внешнего</span> восприятия умом <span class="underline">внутренних</span>
                объектов. Из этого можно было бы сделать вывод о <span class="underline">пространстве как о чистой потенциальности</span> быть занятым каким-либо объектом (умом, конечно, в первую очередь).
                Отсюда только один шаг до несколько более поздней буддийской концепции Пустоты, или
                Пустотности.</p>

            <p>В <em>Дхаммападе</em> уже намечается феноменология мысли — именно отдельной мысли, без отсылок
                к уму или сознанию и даже без упоминания возможности перехода от мысли к уму
                (считать «ум» и «мысль» синонимами в <em>Дхаммападе</em> — явная ошибка). Мысль здесь
                подразумевается как нечто более подвижное, изменчивое, динамичное, чем ум. Но еще
                важнее то, что она вообще может не приписываться ни «ему», ни «кому-то». Совершенно
                ясно видно, что, во всяком случае в <em>Дхаммападе</em>, одна мысль противопоставлена другой,
                по тому или иному признаку. Именно отсюда берет свое начало абхидхармистская идея
                абсолютной атомарности мысли. (<em>Все сказанное выше никак не исключает того, что
                    мысль является динамическим аспектом ума.</em>)</p>

            <p>Основные моменты феноменологии мысли в <em>Дхаммападе</em> можно себе представить следующим
                образом.</p>

            <p><em>(1)</em> Мысль здесь рассматривается только с точки зрения тех, чья мысль
                направлена, твердо установлена в направлении <em>Дхармы</em>, полностью обуздана и правильно
                сосредоточена. Иначе говоря — с йогической точки зрения. Поэтому все естественные,
                природные черты мысли отметаются как негативные.</p>

            <p><em>(2)</em> Вместе с тем мысль часто описывается так, как если бы она обладала своей
                собственной волей, своей автономной интенциональностью. В этом можно видеть
                тенденцию к тому, чтобы приписывать мысли, именно мысли, а не «мыслителю»,
                трансцендентальную субъективность. Это особенно ясно видно в таких словах, как: «О
                монах, не дай мысли погрязнуть в чувственных удовольствиях». Однако для мысли, чтобы
                превратиться в своего рода «микросубъекта», будет необходимо пройти через йогическую
                самообъектификацию, о которой речь пойдет в семинаре X.</p>

            <p><em>(3)</em> В общем я предполагаю, что только благодаря дхармическому подходу к
                мышлению буддийским йогам-учителям удалось открыть и описать феномен отдельной,
                атомарной мысли. Я подчеркиваю, не идею или понятие, а именно феномен. Как если бы
                речь шла о какой-то «микросущности», доступной лишь всепроникающему взгляду
                созерцателя, прошедшего через все уровни трансцендентального созерцания, дхьяны.</p>

            <p><em>(4)</em> Теперь спросим: является ли отдельная атомарная мысль самосознающей,
                рефлексирующей самое себя? Если исходить из моего собственного рефлексивного,
                не-йогического опыта, то ответ будет — нет. Я не знаю ни одного современного
                философа, который бы попытался спросить «что такое мысль?». Никто — от Декарта до
                Мэн де Бирана, от Бергсона до Витгенштейна и Серла.</p>

            <p><em>(5)</em> Итак, на основании нескольких строф, произвольно (и непроизвольно, как
                это уже обсуждалось выше) выбранных из <em>Дхаммапады</em>, мы можем выделить три основные
                темы, организующие философское содержание этого, по-видимому, позднего
                компилятивного канонического текста: <span class="underline"><em>дхарма</em></span>, <span class="underline">ум</span> (мышление, разум, сознание) и <span class="underline">мысль</span>.
                На основании одного этого текста оказалось возможным сформулировать три
                квазионтологических постулата. Первый постулат — о <em>дхармах</em> как по преимуществу
                умственных, т.е. о <em>дхармах</em> <span class="underline">как уме</span>. Второй постулат — о <em>дхармах</em> <span class="underline">как только <em>дхармах</em></span>.
                Третий постулат — об <span class="underline">уме как <em>дхармах</em></span>. Понятие «мысли» обретет свое
                квазионтологическое обоснование и соответственно свою постулятивную формулировку
                только в абхидхармистских текстах, к которым мы обратимся в следующем семинаре.</p>

            <p>Сейчас нам еще остается подумать об одном понятии, которое даже в таком разнородном
                по содержанию, хрестоматийном тексте, как <em>Дхаммапада</em>, кажется странным и
                неожиданным. Это — <em>брахман. Брахман</em> — ведийский или, более поздний, индуистский
                жрец, свершитель древнего жертвоприношения, знаток и произноситель священных
                текстов. <em>Брахман</em>, уже в буддийское время ставший вершиной и пределом древнеиндийской
                социально-культурной иерархии. И наконец, <em>брахман</em> как <span class="underline">идеальный тип человека</span>, только
                относительно которого (и сравнительно с которым) устанавливались все другие типы
                человека. В качестве идеального типа <em>брахман</em> может гипотетически рассматриваться как
                ось древнеиндийской антропологии, и не только структурно и синхронно, т.е. сейчас, в
                составе данного социокультурного организма, но и диахронно, в истории (все равно,
                реальной или мифологической), т.е. «раньше», «тогда» или «всегда» в отношении
                времени его существования. Но главным в <em>брахмане</em> остается: он — лучший. Лучший в
                обоих смыслах: синхронно-лучший, чистейший среди всех людей, живущих сейчас, и
                диахронно-лучший, как происходящий из рода или клана лучших. Именно в смысле
                «лучший» <em>брахман</em> вводится в последней главе <em>Дхаммапады</em>. Но каким образом он
                вводится? Совсем не в качестве метафоры или синонима.</p>

            <p>Но заметьте, вопросом «что такое <em>брахман</em>?» не могли задаваться ни современники Будды,
                ни их потомки, жившие через тысячу лет после него. Что за глупый вопрос? <em>Брахман</em>
                есть <em>брахман</em>. Когда же в самом конце текста Будда говорит, что «<em>архат</em> — это
                <em>брахман</em>», то этим он фактически аннулирует такой ответ, ибо ничего больше «само
                собой не разумеется», и перевертывает вопрос, открывая возможность появления в нем
                другого смысла. Но пока <em>брахман</em> — не домохозяин, не отшельник, никто другой. Правда,
                его судьбу не ведают ни боги, ни <em>гандхарвы</em>, ни люди. Появляется возможность
                постановки вопроса «<em>брахман</em> — почему?». Но <em>брахман</em> не потому <em>брахман</em>, что никто не
                знает, откуда он приходит и куда уходит — не-манифестированность не может служить
                признаком чего-либо. Нельзя сказать, «если есть некто, кого никто не ведает, то он —
                <em>брахман</em>». Тогда изменим нашу, так сказать, эпистемологическую позицию и посмотрим на
                <em>брахмана</em> с точки зрения того, кто начал разговор о нем со слов «пересеки же поток, о
                <em>брахман</em>», т.е. Будды.</p>

            <p>А Будда говорит: «Того я называю <em>брахманом</em>, кто...» и т.д. Будда знает, <span class="underline">кого</span> он
                называет <em>брахманом</em>. Он еще до произнесения этих слов, или в момент их произнесения
                <span class="underline">видит</span> того, кого называет <em>брахманом</em>. <em>Брахман</em>, таким образом, вводится как
                <span class="underline">обозначение</span> этого «того», который до этого момента пребывает в необозначенности, а
                может быть, и вообще его нет как <em>брахмана</em>. Последнее предполагает и возможность
                обратного хода рассуждения: ведь Будда <span class="underline">мог бы</span> начать главу со слов: «Есть такие
                <em>брахманы</em>... которые...», а потом перейти к: «Того я называю <em>брахманом</em>, кто...».
                Отсюда — бессмысленность для нас вопроса «что такое <em>брахман</em>?». Ибо загадка не в
                слове «<em>брахман</em>», а в том, кого Будда называет <em>брахманом</em>. <em>Брахман</em> в <em>Дхаммападе</em> никак
                не дополняет буддийскую номенклатуру. Он не станет ни <span class="underline">обозначением достижения</span> как
                архат, Будда или Благородный, арья, ни <span class="underline">обозначением средства</span>, как аскет, монах,
                <em>бхикшу</em>, отшельник, странствующий отшельник, <em>шрамана</em> или йог-созерцатель. Когда Будда
                говорит: «<em>Архат</em>а я называю <em>брахманом</em>», то этим он одновременно противопоставляет
                <em>брахмана</em> <em>Дхаммапады</em> <em>брахману</em> индуистских текстов (определяемому по формуле «<em>брахман</em>
                есть <em>брахман</em>») и включает архата в общеиндийский антропологический контекст <span class="underline">как <em>брахмана</em></span>. Но, как об этом уже было сказано выше, в этом контекста <em>брахман</em>
                противопоставляется всем остальным людям, что находит свое отражение в стандартной
                буддийской концовке: «Слыша эти слова Будды, возрадовался этот мир со всеми его
                богами, полубогами, <em>брахманами</em>, странствующими аскетами, шраманами и людьми...», в
                которой слово «<em>брахман</em>» употребляется в чисто традиционном смысле и означает только
                «ведийский жрец и член касты <em>брахманов</em>». Но и это, пожалуй, самое интересное: в
                любом брахманистском или индуистском словоупотреблении <em>брахман</em> — это человек, в то
                время как <em>архат</em> (не говоря уже о Будде) в специфически буддийском смысле, —
                определенно <span class="underline">не-человек</span>. Или, точнее говоря, он — в смысле синхронного его
                рассмотрения в момент, когда Будда его называет (или он сам себя называет, обращаясь
                к Будде) <em>архатом</em> — <span class="underline">уже не-человек</span>. Но ведь тогда <em>брахман</em> как «высший человек»
                является названием <em>архата</em> «уже не-человека», о котором можно думать как о
                существующем только с момента, когда кто-то уже назван <em>архатом</em>.</p>

            <p>Размышление над словом «<em>брахман</em>» в <em>Дхаммападе</em> может явиться введением в понимание
                нами одной из интереснейших тем буддийского философии: язык в его отношении к
                мышлению говорящего и к существованию того, о чем говорится.</p>

