                    <a href="#ref-14" class="text-blue-500 hover:underline">↩</a></p>

                <p class="my-2" id="citation-15"><sup>15</sup> Имеется в виду троичная
                    махаянская классификация «персон» Малой Колесницы на тех, кто был учениками
                    Будд, <em>шраваками</em>, тех, кто достиг архатства в этом рождении,
                    <em>архатов</em>, и тех Будд, которые оставались Буддами для себя и никого
                    не учили, <em>Пратьека-будд</em>.

                    <a href="#ref-15" class="text-blue-500 hover:underline">↩</a></p>

                <p class="my-2" id="citation-16"><sup>16</sup> «Ни снаружи тела» — к тому, что как
                    бы внутри тела, относятся пять агрегатов индивидуального существования, <em>скандх</em>,
                    а также все психические и психофизиологические механизмы, связанные с
