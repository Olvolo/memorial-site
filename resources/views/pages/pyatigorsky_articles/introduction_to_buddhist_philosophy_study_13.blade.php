                <p class="my-2" id="citation-3"><sup>3</sup> «Никак не очевидно», точнее —
                    «неразличимо» (санскр. <em>anabhāsa</em>).
                    <a href="#ref-3" class="text-blue-500 hover:underline">↩</a></p>

                <p class="my-2" id="citation-4"><sup>4</sup> «Бесформенно» — то есть неощущаемо,
                    невоспринимаемо, ибо то, что не имеет формы (санскр. <em>arūpya</em>), не может
                    быть воспринято органами чувств.
                    <a href="#ref-4" class="text-blue-500 hover:underline">↩</a></p>

                <p class="my-2" id="citation-5"><sup>5</sup> «Невыразимо» — точнее было бы сказать
                    «непонятно», хотя возможно также и «необъяснимо» (санскр. <em>avijñaptika</em>).
                    Это крайне трудное эпистемологическое понятие.
                    <a href="#ref-5" class="text-blue-500 hover:underline">↩</a></p>

                <p class="my-2" id="citation-6"><sup>6</sup> «Исследования реальности дхарм» — это
                    эпистемологическое понятие (и обозначающий его технический термин, санскр. <em>bhūtapratyavekṣā</em>),
                    по-видимому, впервые введённое в этой сутре.
                    <a href="#ref-6" class="text-blue-500 hover:underline">↩</a></p>

                <p class="my-2" id="citation-7"><sup>7</sup> «В смысле Срединного Пути» — здесь
                    точно в том же смысле, как он определялся в I (но в санскритской терминологии
                    Срединный Путь — <em>madhyamā-pratipad</em>).
                    <a href="#ref-7" class="text-blue-500 hover:underline">↩</a></p>

                <p class="my-2" id="citation-8"><sup>8</sup> «Мысль реальна» — санскр. <em>bhūtacitta</em>,
                    может значить как «мысль о действительно сущем», так и «действительно сущая
                    мысль».
                    <a href="#ref-8" class="text-blue-500 hover:underline">↩</a></p>

                <p class="my-2" id="citation-9"><sup>9</sup> The Kāśyapaparivarta. P. 82–86.
                    <a href="#ref-9" class="text-blue-500 hover:underline">↩</a></p>

                <p class="my-2" id="citation-10"><sup>10</sup> «Пуста и личность» — сказанное
                    предполагает радикальное изменение в общей ориентации восприятия. Вместо
                    направленности на человека, на личность, оно становится направленным на
                    пустотность самих понятий «человек», «личность».
                    <a href="#ref-10" class="text-blue-500 hover:underline">↩</a></p>

                <p class="my-2" id="citation-11"><sup>11</sup> The Kāśyapaparivarta. P. 94–97.
                    <a href="#ref-11" class="text-blue-500 hover:underline">↩</a></p>

                <p class="my-2" id="citation-12"><sup>12</sup> «Три области вселенной» — то есть
                    земля, небо и подземный мир.
                    <a href="#ref-12" class="text-blue-500 hover:underline">↩</a></p>

