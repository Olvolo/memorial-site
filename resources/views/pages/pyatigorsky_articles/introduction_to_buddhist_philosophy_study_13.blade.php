            <h3 class="text-xl text-center font-semibold text-zinc-700 my-4">БОЛЬШАЯ КОЛЕСНИЦА И
                ТЕМЫ ФИЛОСОФИИ <em>МАХАЯНЫ</em>.<br>БУДДИЙСКАЯ ФИЛОСОФИЯ И БУДДОЛОГИЯ.</h3>
            <p><em>(0)</em> Что делать? Переход в этом семинаре от философии Малой Колесницы, или
                <em>Хинаяны</em>, к философии Большой Колесницы, или <em>Махаяны</em> (кстати,
                напоминаем, что
                противопоставление друг другу этих колесниц было введено post factum учеником
                Нагарджуны Арьядевой в конце III века), ставит нас в позицию наблюдателей истории
                философии. Поэтому нам придется сначала разъяснить эту позицию «внешнего»
                наблюдателя. До самого последнего времени в изучении истории буддизма мы имели дело
                с двумя альтернативными историческими концепциями. Первая, <span class="underline">эволюционистская</span>,
                выводит
                все поздние и позднейшие формы религии и философии буддизма из (пока еще
                гипотетического) «раннего исторического буддизма», часто отождествляемого (также
                гипотетически) с буддизмом Малой Колесницы, т.е. с <em>Хинаяной</em> (обычно
                называемой
                <em>Тхеравада</em>, Учение Старейшин Сангхи). Тогда Большая Колесница,
                <em>Махаяна</em>, оказывается
                происходящей из Малой Колесницы через промежуточные версии, такие как <em>Махасангхика</em>,
                <em>Сарвастивада</em> и т.д. Вторая концепция, <span class="underline">преформистская</span>.
                исходит позднейшего
                тантристского представления о том, что Будда в самом начале своей миссии
                проповедовал учения обеих Колесниц (да и тантризм в придачу), но различным
                контингентам учеников, которые впоследствии создали свои устные и письменные
                традиции распространения Дхармы в пространстве и продления ее во времени. Таким
                образом, собственно <span class="underline">история</span> буддизма начинается,
                согласно этой концепции, <span class="underline">после одновременного</span>
                изложения Буддой <span class="underline">всех</span> версий <em>Дхармы</em>. То же,
                что было до этого
                мифологического события, относится к до-истории, обычно к особому периоду, <em>кальпе</em>,
                предшествовавшему нынешнему периоду так называемой «истории».</p>
            <p>В нашей «наблюдательной» позиции мы исходим из двух эпистемологических предпосылок, в
                которых предполагаем возможность нетривиальных выводов в отношении перехода от
                философии Малой Колесницы к философии Большой Колесницы. Первая предпосылка нашей
                наблюдательной позиции является отрицательной в том смысле, что она противоречит
                эпистемологической концепции генезиса научного знания; концепции, нашедшей свое
                наиболее крайнее и четкое выражение в последних работах Эдмунда Гуссерля. Формула
                «сущность (всякого) научного знания — в аксиомах, образующих его генезис»
                отвергается нами в пользу предположения о возможности одновременного существования
                нескольких первичных аксиом, лежащих в основе генезиса данного научного
                (философского, религиозного, какого угодно) знания или даже нескольких генезисов для
                одного рассматриваемого нами знания. Тогда первая предпосылка будет называться
                <span class="underline">предпосылка полигенезиса</span> учения Большой Колесницы.
            </p>
            <p>Вторая эпистемологическая предпосылка нашей наблюдательной позиции может быть
                сформулирована следующим образом. В рассмотрении любой историко-философской проблемы
                следует отдавать предпочтение философии как тому общему, что включает в себя историю
                как частный случай, а в рассмотрении философии отдавать предпочтение ее
                самоосознанию, т.е. сумме ее мыслей о самой себе, которые и должны лечь в основание
                нашего философствования о данной философии. Это — <span class="underline">предпосылка философской привилегированности</span>:
                история философии — это осознание себя (данной) философией,
                изменяющейся во времени.</p>
            <p>Вообще, поскольку мы не занимаемся специально хронологией буддийского философского
                мышления, такие слова, как «начало», «развитие», «расцвет», «упадок», являются для
                нас не более чем искусственно введенными и случайно выбранными точками или моментами
                нашего собственного философствования по поводу содержания махаянских текстов этого
                семинара. Сейчас эти тексты являются объектом нашего неуемного философского
                любопытства (заметьте, здесь «философское любопытство» — это обозначение особой
                <span class="underline">интенциональности</span>). Но все-таки возможно ли с учетом
                сделанной выше оговорки и в
                свете эпистемологического постулата философской привилегированности, говорить о
                <span class="underline">начале</span> философии Большой Колесницы? Сам я по
                культуре, воспитанию и образованию
                являюсь человеком, склонным к историко-генетической модели всего познаваемого и с
                трудом отхожу от нее — но только в рассмотрении другого, чужого, «не моего»
                философского мышления. Ничего не поделаешь, ведь великие философы Большой Колесницы
                ни в коей мере не были людьми, мыслящими исторически. Ибо были они людьми мыслящими
                <span class="underline">традиционно</span>. Т.е. для них генезис любой концепции
                четко редуцируется к условной
                последовательности передачи знания от учителей ученикам, а от них к их ученикам,
                последовательности, начало которой безнадежно утеряно в бездонном недатируемом
                прошлом доисторических <em>кальп</em>. Что касается нас, то мы бы могли представить
                себе этот
                редуцированный к традиции квазиисторический генезис в виде набора основных тем, к
                которым нас будут вновь и вновь отсылать буддийские священные (т.е. приписываемые
                Будде) тексты, сутры и ученые трактаты, <em>шастры</em>. Собственно говоря,
                последовательность таких отсылок и лежит в основе традиции как квазиисторического
                процесса, в то время как основные темы являются как бы точками, в которых
                кристаллизуется философское содержание текстов. В содержании фрагментов сутр этого
                семинара мы выделяем пять таких основных тем.</p>
            <p><em>(1)</em> <span class="underline">Срединность</span>. В Первой проповеди Будды
                (см. I) срединность — это <span class="underline">позиция</span>.
                Пусть необыкновенная, единственно правильная, исключительная, но все же — позиция.
                Во фрагменте из <em>Кашьяпа-Париварта-Сутры</em>, которым открывается этот семинар,
                срединность поднята на уровень трансцендентальной умопостигаемой сущности, но вместе
                с тем — и это совсем не противоречие, а обычный буддийский <span class="underline">перенос</span>
                (строго говоря,
                это и есть «метафора» в буквальном смысле слова) члена одного класса в другой на
                основании общего для них обоих признака — она может и быть постигнута посредством
                особого типа исследования, которое называется «рефлективное исследование
                <em>дхарм</em>». В
                таком исследовании срединность в большей степени видится как то, что есть между двух
                противоположностей, нежели как способ познания этих противоположностей в их
                ошибочности и неправильности. Иначе говоря, здесь срединность превращается из
                квазиэпистемологической категории в онтологическую категорию недостижимой реальности
                всего сущего (санскр. <em>bhūtatā</em>).</p>
            <p><em>(2)</em> <span class="underline">Пустотность</span> в XIII представлена во всех
                своих трех аспектах. В
                <span class="underline">позитивно-синтетическом</span> аспекте она является
                имманентной во всех <em>дхармах</em>: все <em>дхармы</em>
                пусты не потому, что они познаются или созерцаются как пустые, а потому, что каждая
                из них, взятая в отдельности, пуста <span class="underline">как она есть</span>. В
                своем <span class="underline">негативном аналитическом</span> аспекте, пустотность
                приближается по своему смыслу к идее <span class="underline">универсальной лишенности</span>,
                которая находит свое выражение в таких формулировках, как, скажем, «все вещи не
                являются самими собой», что можно понимать и в смысле «все вещи равны в том, что не
                являются самими собой». Равенство <em>дхарм</em> здесь вторично и производно от лишенности
                всех вещей их отличительных признаков. Все <em>дхармы</em> пусты, необусловленные <em>дхармы равны</em> обусловленным, а последние равны между собой в силу их лишенности каких бы то
                ни было признаков. Эта универсальная привативность имеет своим интереснейшим
                философским следствием идею о <span class="underline">не-состоянии</span>. Так, состояние буддства является
                не-состоянием, или, точнее, не-состояние и есть состояние буддства в смысле
                Пустотности. Аналогичным образом мы можем говорить о <span class="underline">не-действии</span> как о действии в
                смысле Пустоты и, наконец, о <span class="underline">не-мысли</span> как о любой необусловленной <em>дхарме</em>,
                синонимичной Пустотности.</p>
            <p>В своем <span class="underline">позитивно-аналитическом</span> аспекте Пустота уподобляется чистому пространству.
                Последнее по определению <span class="underline">не дуально</span>, как не дуальны Нирвана, буддство и <em>Дхармичность</em>
                (санскр. <em>dharma-dhātu</em>), хотя в отличие от них чистое пространство может делиться на
                стороны, сферы и т.д. Более того, Пустотность в своем позитивно-аналитическом
                аспекте часто называется «истинной формой реальности» — несколько загадочное
                название, в котором имплицирована идея <span class="underline">одной</span> формы <span class="underline">одной</span> реальности.</p>
            <p><em>(3)</em> Мысль Пробуждения (санскр. <em>bodhicitta</em>), весьма кратко описанная в
                Кашьяпа-Париварта-Сутре, является не только новым <span class="underline">понятием</span> Большой Колесницы, но и
                другой, особой <span class="underline">сущностью</span>, радикально изменившей направление всего буддийского
                философствования. В самом простом истолковании Мысль Пробуждения — это мысль
                (намерение, желание, решение) стать Буддой. В этой же сутре говорится о двух
                ограничительных характеристиках Мысли Пробуждения. Первая из них приписывает Мысль
                Пробуждения тем существам, <em>саттвам</em>, которые уже вступили на путь достижения Полного
                и Абсолютного Пробуждения, т.е. Бодхисаттвам. Вторая же ограничительная
                характеристика заключается в том, что Мысль Пробуждения полагается первой мыслью
                каждого Бодхисаттвы и сама генерируется им. Отчего один из эпитетов Бодхисаттвы —
                это «производитель», «генератор» (санскр. <em>utpādika</em>). Чем же тогда является Мысль
                Пробуждения <span class="underline">как мысль</span>: такой же серией возникновений мыслей или «случаев» мысли, как
                и (все) другие мысли, рассматриваемые в их объективности? (Здесь «объективный»
                значит «рассматриваемый как не имеющий субъекта».) Или Мысль Пробуждения — это
                случай или последовательность случаев <span class="underline">состояний сознания</span>, приписываемых Бодхисаттве?
                Вопрос, я думаю, приглашает к феноменологическому ответу. Тогда моим пробным
                операциональным определением будет следующее: Мысль Пробуждения, согласно ранним
                сутрам Большой Колесницы, — <span class="underline">это сложная интенпиональность, включающая в себя и
                условного «субъекта», т.е. Бодхисаттву и те опять же условно принимаемые за
                «объективные» состояния сознания, которые могут быть этому Бодхисаттве приписаны
                post factum</span>.</p>
            <p>Но в каком отношении Мысль Пробуждения находится к мысли вообще и к возникновению
                мысли (в абхидхармистской трактовке)? На этот вопрос можно с уверенностью ответить
                следующее. Весь ход развития философской мысли Большой Колесницы отмечен
                напряжением, возникающим снова и снова между абхидхармистской концепцией мысли как
                возникновения мысли и другими («новыми») концепциями, требующими радикального
                пересмотра абхидхармистских аксиом.</p>
            <p><em>(4)</em> Разъяснение о мысли в <em>Кашьяпа-Париварта-Сутре</em> дает самую краткую и
                точную характеристику мысли и мышления во всей махаянистской литературе. Главное в
                этом разъяснении — то, что оно определяет основное направление в исследовании мысли
                и мышления ранними мыслителями Большой Колесницы. Идя по их стопам, мы не спросим у
                мысли: «Откуда ты?» Ибо из данного в нашей сутре разъяснения мы уже поняли, что
                мысль — не «откуда», а «куда», т.к. она объективно направлена из «ниоткуда» своего
                возникновения в «никуда» абсолютной необусловленности <em>Нирваны</em> и Рода (<em>готры</em>)
                Благородных. Тогда в порядке поверхностной историко-философской аналогии мы могли бы
                заключить, сказав, что как у Парменида (по Хай-деггеру, во всяком случае) мысль есть
                мысль о бытии, так у ранних махаянистов мысль есть мысль о Пробуждении.</p>
            <p>Во фрагментах из других сутр «Собрания Драгоценностей» проводится четкое различие
                между мыслью и сознанием, отсутствующее или едва намечаемое в сутрах и <em>Абхидхарме</em>
                Малой Колесницы. Это различие имело чрезвычайно важные последствия для поздних
                махаянистских школ Индии, Китая и Тибета. Я думаю, что для нас понимание этого
                различия может начаться с рассмотрения различия между пространством (<em>topos</em>) и местом
                (<em>locus</em>). Ведь как об этом уже говорилось выше, мысль находится в слабой корреляции с
                пространством. «Слабой» в том конкретном смысле, что она не может быть обнаружена в
                каком-то конкретном месте в пространстве. Иначе говоря, у мысли просто нет своего
                места. Тогда единственно, что может быть о ней сказано — это ее направление, ее
                «куда».</p>
            <p>Однако сознание, как «то, что перевоплощается», движется в пространстве от одного
                тела к другому. Тело — его место, хотя при этом оно не может быть локализовано
                <span class="underline">внутри</span> тела или отождествлено с каким-либо органом последнего. Но сознание
                коррелируется с пространством, занимаемым телом, в максимально «сильном» порядке.
                Притча о куклах и кукловоде прекрасно иллюстрирует эту корреляцию. Здесь сознание —
                не просто «внутри тела» — сказать это было бы непростительной тривиализацией,
                попробуй найди кукловода внутри куклы, смешно — но оно создает все <span class="underline">пространство сцены</span>, включая и аудиторию, и ландшафт.</p>
            <p>Таким образом, сознание является сложным функционирующим целым, тогда как мысль (в ее
                возникновении) остается атомарным фактом или событием. Именно в своем
                функционировании сознание подпадает под власть Взаимообусловленного Возникновения.
                Мысль же не имеет никакой функции, кроме ее возникновения и исчезновения. Далее о
                сознании можно сказать, что в каких-то случаях оно находится <span class="underline">вне</span> топоса,
                пространства своего функционирования. В то время как мысль, движущаяся в
                пространстве, не имеет ни «вне», ни «внутри» в качестве своих топологических
                индикаторов. Поэтому даже сказать, что «сознание находится в теле», будет не более
                чем полуистиной, ибо — как это было сказано Буддой — сознание не «находится» в теле,
                а <span class="underline">появляется</span>, подобно лицу, появляющемуся в зеркале.</p>
            <p>И наконец, в отличие от мысли, которая может быть кармически благой, дурной или
                нейтральной, сознание полагается нейтральным по преимуществу. Это приводит нас к еще
                одному различию между мыслью и сознанием, различию, установление которого имело
                весьма важные последствия для дальнейшего хода буддийской философской мысли.
                Посмотрите, в знаменитой строке <em>Дхаммапады</em> (см. I, 1): «Если кто-то действует и
                говорит с нечистой мыслью...»: «кто-то» противопоставлен «мысли» как условный
                субъект мышления противопоставлен мышлению. С точки зрения концепции сознания в
                Собрании Драгоценностей этот «кто-то» и есть сознание. Или даже: с этой точки зрения
                сознание и есть этот «кто-то» <em>Дхаммапады</em>. В этом можно увидеть решительный
                методологический отход от <em>Абхидхармы</em> Малой Колесницы с ее принципиальным
                не-различением между мышлением, объектом мышления и мыслящим.</p>
            <p>В Собрании Драгоценностей сознанию приписывается, в качестве дополнительного
                атрибута, <span class="underline">сила</span>. Мы читаем, что только силой кукловода деревянная кукла прыгает,
                пляшет и т.д. Тело — это деревянная кукла. Но здесь притча останавливается — кто мы:
                мыслящие куклы или каждый из нас является одновременно куклой и кукловодом? Ответ
                напрашивается сам собой. Но вопрос гораздо сложнее. Ведь и кукловод, как о нем
                говорилось выше, может быть скрыт где-то вне наших тел или даже далеко за пределами
                сцены нашей жизни, сюжет которой он уже наговорил из-за занавеса. Я думаю, что
                именно отвечая на наш вопрос, великие мыслители Большой Колесницы и прибегли к идее
                <span class="underline">силы</span> в своем объяснении сознания. Сила сознания — это не просто сила «мудрости»
                (санскр. <em>prajñā</em>), которая, когда наступит время, приведет сознание к последней
                остановке, к Нирване. Она — и <span class="underline">сила памяти</span>, из-за которой (и на основе которой)
                сознание становится «семенем» (санскр. <em>bīja</em>) следующей жизни «кукловода». Тогда в
                конечном счете сила сознания может рассматриваться как главная сила трансформации,
                радикально изменяющая мысли, тела и жизни «кукловода».</p>
            <p><em>(5)</em> Кажется, что концепция «Зародыша Татхагаты» (санскр. <em>tathāgata-garbha</em>)
                появляется в философских текстах Большой Колесницы как дополнительная к концепции
                Мысли Пробуждения. Может быть, она появляется в порядке, подобном порядку появления
                понятия «сознания» в его дополнительности к понятию «мысли» в абхидхармистских
                текстах. Если мы рассмотрим соотношение понятий Зародыша Татхагаты и Мысли
                Пробуждения, абстрагируясь от возможных семантических и этимологических
                истолкований, и пойдем путем чисто эпистемологического сопоставления этих понятий,
                то не преминем заметить, что Зародыш Татхагаты представляется, по крайней мере на
                первый взгляд, концептом более субстантивистским, чем концепт Мысли Пробуждения.
                Оговариваю, что <span class="underline">субстантивистский</span> здесь значит не более чем «предполагающий
                возможность истолкования как субстанции». В таком же примерно смысле, в каком было
                бы возможно сказать, что elan vital Анри Бергсона представляется более
                субстантивистским, чем абсолютный дух Гегеля, или что «живое существо» Эдмунда
                Гуссерля более субстанциально, чем Монада Лейбница.</p>
            <p>В Собрании Драгоценностей этот субстанциализм находит свое описание в двух основных
                терминах «зародыш» и «семя». (Дальнейшее развитие махаянистской философии покажет,
                сколь важную роль эти термины сыграли в образовании нового религиозно-философского
                мировоззрения, буддийского тантризма.) Начнем с понятия «семени». Семя описывается в
                Собрании Драгоценностей как ориентированное на <span class="underline">будущее</span>. Когда мы читаем, что
                «сознание есть семя будущей жизни его носителя», то этим предполагается, что семя
                является субстанцией будущего сознания. Более того, возможно предположение, что сама
                <span class="underline">идея</span> семени коррелируется с сознанием как дополнительная к последнему. «Пробное»
                определение, которое царица Шримала дает Зародышу Татхагаты в последних фрагментах
                текстов этого семинара, имеет в виду, что Зародыш Татхагаты — это понятие,
                ориентированное на <span class="underline">прошлое</span>. Это определение звучит как догматическая онтология.
                Зародыш Татхагаты есть не что иное, как Тело <em>Дхармы</em>, находящееся в <em>самсаре</em>, в мире
                обусловленных <em>дхарм</em>. Оно, так же как и <em>самсара</em>, безначально и вечно. «Поэтому-то, —
                продолжает царица свое объяснение, — феноменальная вселенная не знает истока или
                происхождения. В то же время Мудрость Татхагаты есть Высшее Знание освобождения от
                <em>самсары</em>, т.е. Мудрость Абсолютной Пустотности».</p>
            <p>Отсюда не выпутаешься, не выходя за рамки наших текстов. Не напоминает ли все это
                современную текстам Большой Колесницы гностическую концепцию предвечной Софии —
                мудрости или гнозиса ранних христианских гностиков? Т.е. Мудрости, предшествующей
                тварному космосу, но одновременно являющейся планом и проектом последнего. Можем ли
                мы считать сказанное в этих фрагментах <span class="underline">началом</span> философии <em>Махаяны</em>? Отвечаю: нет, но
                это будет ее началом, когда много позднее философия <em>Махаяны</em> вернется к сказанному
                здесь как к своему источнику и своей основе.</p>
