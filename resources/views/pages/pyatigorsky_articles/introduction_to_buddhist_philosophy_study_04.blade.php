                    а в санскрите — <em>padmāsana</em> («лотосная поза»). Эта поза:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>Была заимствована из до-буддийских йогических практик.</li>
                    <li>В буддизме именуется также «позой льва» (<em>sīhāsana</em>).</li>
                    <li>Считается устойчивой и способствующей длительному сидению в состоянии
                        сосредоточенности.
                    </li>
                </ul>
                <p>«Направленный взгляд перед собой» символизирует не просто физическую ориентацию,
                    но внутреннее состояние внимательности и умственной собранности.<a href="#ref-3"
                                                                                       class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-4"><sup>4</sup> «Освобождение» (пали <em>vimutti</em>,
                    санскр. <em>vimukti</em>) — технический термин буддийской психологии,
                    обозначающий освобождение от загрязнений ума (<em>āsava</em>). Различают:</p>
                <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
                    <li><em>Временное</em> — через достижение джхан</li>
                    <li><em>Постоянное</em> — как результат реализации ниббаны</li>
                </ol>
                <p>См. также: I, прим. 42 и Ангуттара-никае II.24, где описывается как «неколебимое
                    освобождение ума» (<em>akuppā vimutti</em>).<a href="#ref-4"
                                                                   class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-5"><sup>5</sup> «Направив внимание» буквально означает
                    «твердо установив мысль на мыслимый объект». Это ключевой момент в медитативной
                    практике, соответствующий термину <em>manasikāra</em> — «направление ума на
                    объект».
                    В буддийской психологии это:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>Предварительное условие для сосредоточения (<em>appanā-samādhi</em>).</li>
                    <li>Один из основных психических факторов (<em>cetasika</em>) в Абхидхамме.</li>
                    <li>Процесс, аналогичный <em>ekāgratā</em> в йогической традиции Веданты.</li>
                </ul>
                <p>Считается необходимым шагом для возникновения ясного восприятия и глубокой
                    концентрации.<a href="#ref-5" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-6"><sup>6</sup> «Прямой порядок» (пали <em>anuloma</em>,
                    санскр. <em>anuloma</em>) означает естественную последовательность рассуждения
                    или восприятия, при которой явления рассматриваются в их обычной
                    причинно-следственной связи: например, сначала причина, затем следствие.
                    В буддийской практике этот порядок используется при:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>Изложении цепи зависимого возникновения (<em>paṭicca-samuppāda</em>).</li>
                    <li>Медитативном анализе условий возникновения страдания.</li>
                    <li>Развитии прозрения (<em>vipassanā</em>) в отношении непостоянства, страдания
                        и безличности.
                    </li>
                </ul>
                <p>Часто противопоставляется <em>обратному порядку</em> (<em>patiloma</em>) — когда
                    анализ направлен на прекращение страдания через разрыв цепи условностей.<a
                        href="#ref-6" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-7"><sup>7</sup> «Обусловленный» (пали <em>paṭicca</em>,
                    санскр. <em>pratītya</em>) означает не просто «связанный с причиной», но
                    буквально — «возникший вследствие взаимодействия условий». Это ключевой термин в
                    буддизме, обозначающий:</p>
                <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
                    <li>Зависимость явления от причин и условий (<em>hetu-pratyaya</em>).</li>
                    <li>Причинную обусловленность всех составных явлений.</li>
                    <li>Отсутствие самостоятельного, независимого существования.</li>
                </ol>
                <p>Входит в составную формулу <em>paṭicca-samuppāda</em> — «взаимообусловленное
                    возникновение», лежащую в основе буддийского анализа реальности.<a href="#ref-7"
                                                                                       class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-8"><sup>8</sup> «Неведение» (пали <em>avijjā</em>,
                    санскр. <em>avidyā</em>) — это не просто отсутствие знания или невежество, но
                    глубинная помрачённость ума, являющаяся:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>Первым звеном в 12-членной цепи зависимого возникновения (<em>paṭicca-samuppāda</em>).
                    </li>
                    <li>Фундаментальным корнем цикла перерождений (<em>saṃsāra</em>).</li>
                    <li>Отсутствием прямого знания о Четырёх Благородных Истинах.</li>
                </ul>
                <p>В Абхидхамме <em>avijjā</em> классифицируется как одна из четырнадцати неблагих
                    формаций
                    сознания и служит основой для возникновения других загрязнений ума.<a
                        href="#ref-8"
                        class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-9"><sup>9</sup> «Синергии» — искусственный термин,
                    введённый Д. Т. Судзуки в 1923 г. для передачи значения палийского слова <em>saṅkhāra</em>
                    (санскр. <em>saṃskāra</em>). Он используется здесь с оговорками, поскольку этот
                    термин считается одним из самых трудных для перевода в буддийской философии.
                    <em>saṅkhāra</em> может означать:</p>
                <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
                    <li><em>Волевые формирования</em> — второе звено в цепи зависимого возникновения
                        (<em>paṭicca-samuppāda</em>).
                    </li>
                    <li><em>Все обусловленные явления</em> — как одна из трёх характеристик
                        существования.
                    </li>
                    <li><em>Четвёртый агрегат</em> (<em>saṅkhāra-khandha</em>) — совокупность
                        ментальных факторов и волевых импульсов.
                    </li>
                </ol>
                <p>Тем не менее, предложенный Судзуки термин «синергии» не получил широкого
                    распространения в современной буддологии.<a href="#ref-9"
                                                                class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-10"><sup>10</sup> «Имя-форма» (пали <em>nāma-rūpa</em>,
                    санскр. <em>nāma-rūpa</em>) — четвёртое звено в цепи зависимого возникновения
                    (<em>paṭicca-samuppāda</em>), обозначающее психофизический комплекс
                    индивидуального существования.
                    Он включает:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li><em>nāma</em> — четыре нематериальных агрегата: чувство (<em>vedanā</em>),
                        восприятие (<em>saññā</em>), волевые формирования (<em>saṅkhārā</em>) и
                        сознание (<em>viññāṇa</em>),
                    </li>
                    <li><em>rūpa</em> — физическая форма или тело, представляющая первый из пяти
                        агрегатов.
                    </li>
                </ul>
                <p>В буддийской феноменологии это понятие символизирует не просто разделение на
                    психическое и физическое, но их неразрывное единство — как единое целое,
                    особенно в
                    зародышевом состоянии бытия. Это ключ к пониманию буддийского взгляда на природу
                    личности без идеи постоянного "Я".<a href="#ref-10"
                                                         class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-11"><sup>11</sup> «Сфера чувств» (пали <em>āyatana</em>,
                    санскр. <em>āyatana</em>) — система из двенадцати баз восприятия, лежащая в
                    основе анализа чувственного опыта. Она включает:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li><em>Шесть внутренних сфер</em>: глаз (<em>cakkhu</em>), ухо (<em>sota</em>),
                        нос (<em>ghāna</em>), язык (<em>jivhā</em>), тело (<em>kāya</em>), ум (<em>mano</em>).
                    </li>
                    <li><em>Шесть внешних сфер</em>: видимые формы (<em>rūpa</em>), звуки
                        (<em>sadda</em>), запахи (<em>gandha</em>), вкусы (<em>rāga</em>),
                        осязательные объекты (<em>phoṭṭhabba</em>), ментальные объекты
                        (<em>dhamma</em>).
                    </li>
                </ul>
                <p>Каждая сфера содержит: орган чувства, соответствующее сознание и объект
                    восприятия.
                    Вместе они образуют условия для возникновения <em>phassa</em> — контакта,
                    который
                    становится началом чувствования, желания и дальнейшего цепляния.
                    В Абхидхамме (<em>Dhammasaṅgaṇī</em>, 1) эти сферы служат основой
                    систематического
                    анализа психофизического процесса восприятия.<a href="#ref-11"
                                                                    class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-12"><sup>12</sup> См. III, прим. 71.<a href="#ref-12"
                                                                                    class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-13"><sup>13</sup> В буддийской психологии термин
                    «эмоции» здесь ближе всего соответствует понятию <em>vedanā</em> — «чувства»,
                    входящие в состав второго агрегата (<em>vedanā-kkhandha</em>). Они делятся на
                    три типа:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li><em>Приятные</em> (<em>sukhā</em>).</li>
                    <li><em>Неприятные</em> (<em>dukkhā</em>).</li>
                    <li><em>Нейтральные</em> (<em>adukkham-asukhā</em>).</li>
                </ul>
                <p>Это не просто эмоциональные реакции, а базовые оценочные состояния ума,
                    возникающие
                    при контакте с объектами через органы чувств. Именно на основе этих чувств
                    формируются желание, отвращение и прочие окраски сознания.<a href="#ref-13"
                                                                                 class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-14"><sup>14</sup> См. I, прим. 27.<a href="#ref-14"
                                                                                  class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-15"><sup>15</sup> «Привязанность» (пали
                    <em>upādāna</em>, санскр. <em>upādāna</em>) — девятое звено в цепи зависимого
                    возникновения (<em>paṭicca-samuppāda</em>), означающее не просто эмоциональную
                    привязанность, но глубинное цепляние за:</p>
                <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
                    <li><em>Чувственные наслаждения</em> (<em>kāmupādāna</em>).</li>
                    <li><em>Догматические воззрения</em> (<em>diṭṭhupādāna</em>).</li>
                    <li><em>Обряды и предписания</em> (<em>sīlabbatupādāna</em>).</li>
                    <li><em>Учение о „я“</em> (<em>attavādupādāna</em>)</li>
                </ol>
                <p>Этот термин буквально означает «основание», «субстрат» или даже «пищевую базу»
                    существования, что подчёркивает его роль как источника поддержания цикла
                    перерождений (<em>saṃsāra</em>).<a href="#ref-15"
                                                       class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-16"><sup>16</sup> «Становление» (пали <em>bhava</em>,
                    санскр. <em>bhava</em>) — десятое звено в цепи зависимого возникновения
                    (<em>paṭicca-samuppāda</em>), обозначающее не статичное «бытие», как это часто
                    понимается в брахманизме (<em>sattā</em>), а динамический процесс существования.
                    Он включает:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li><em>kamma-bhava</em> — кармическое становление, формирующее будущие
                        состояния бытия.
                    </li>
                    <li>Три уровня существования: чувственный (<em>kāma-bhava</em>),
                        тонкоматериальный (<em>rūpa-bhava</em>) и нематериальный
                        (<em>arūpa-bhava</em>).
                    </li>
                </ul>
                <p>Согласно большинству современных переводчиком, термин «становление» лучше
                    передаёт
                    смысл <em>bhava</em>, поскольку подчёркивает его процессуальную природу —
                    непрерывное
                    обусловливание и изменение, которое само формирует течение времени. В <em>Висуддхимагге</em>
                    (гл. XVII) это состояние описывается как «процесс непрерывного
                    обусловливания».<a
                        href="#ref-16" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-17"><sup>17</sup> «Рождение» (пали <em>jāti</em>,
                    санскр. <em>jāti</em>) — одиннадцатое звено цепи зависимого возникновения,
                    обозначающее процесс появления индивидуального существования. Оно:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>Начинается с момента зачатия (<em>okkanti</em>).</li>
                    <li>Включает период внутриутробного развития.</li>
                    <li>Завершается физическими родами (<em>pātubhāva</em>).</li>
                </ul>
                <p>Это событие охватывает не только биологический аспект рождения, но и формирование
                    нового психофизического комплекса. В трактате <em>Abhidhammattha-saṅgaha</em>
                    (VI.9)
                    рождение определяется как момент возникновения пяти агрегатов
                    (<em>pañcakkhandha</em>), что
                    знаменует начало нового этапа в цикле перерождений (<em>saṃsāra</em>).<a
                        href="#ref-17"
                        class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-18"><sup>18</sup> «Возникновение» (пали
                    <em>samuppāda</em>, санскр. <em>saṃutpāda</em>) в буддийской философии близко по
                    смыслу к современному понятию <em>эмержентности</em> — возникновения нового
                    качества из взаимодействия условий. При этом подчёркивается, что всё, что
                    возникает, возникает не изолированно, а <em>взаимообусловленно</em>, всегда в
                    связи с другими явлениями. Это лежит в основе учения о зависимом возникновении (<em>paṭicca-samuppāda</em>).<a
                        href="#ref-18" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-19"><sup>19</sup> «Истинный смысл» (пали <em>attha</em>,
                    санскр. <em>artha</em>) в буддийской экзегетике обозначает не просто буквальное
                    значение текста, но и его:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li><em>Буквальный смысл</em> (<em>nītta</em> или <em>nippariyāya</em>).</li>
                    <li><em>Контекстуальное значение</em> (<em>pariyāya</em>).</li>
                    <li><em>Окончательную истину</em> (<em>paramattha</em>).</li>
                </ul>
                <p>Эта система интерпретации подробно разработана в палийском трактате <em>Nettippakaraṇa</em>,
                    где представлены методы различения поверхностного и глубинного значения
                    учений.<a
                        href="#ref-19" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-20"><sup>20</sup> «Дхарма» (пали <em>dhamma</em>,
                    санскр. <em>dharma</em>) — многозначный термин, имеющий разные уровни значения:
                </p>
                <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
                    <li><em>Учение Будды</em> — особенно в формулировке «Учение и Дисциплина»
                        (<em>Buddha-sāsana</em>).
                    </li>
                    <li><em>Элементарный феномен опыта</em> — единица анализа реальности в
                        Абхидхамме.
                    </li>
                    <li><em>Нравственный закон</em> — принципы этики и кармического порядка.</li>
                    <li><em>Истина реальности</em> — как она раскрывается в просветлении.</li>
                </ol>
                <p>В контексте Абхидхаммы слово <em>dhamma</em> охватывает все эти значения
                    одновременно, подчёркивая универсальность и многослойность учения Будды. Более
                    подробно проблема перевода и интерпретации этого термина рассматривается в
                    статье:
                    <a href="/resources/views/pages/pyatigorsky_articles/descriptive_language_problem_in_buddhology.blade.php"
                       class="text-blue-500 hover:underline">О. О. РОЗЕНБЕРГ И ПРОБЛЕМА ЯЗЫКА
                        ОПИСАНИЯ В БУДДОЛОГИИ</a>.<a href="#ref-20"
                                                     class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-21"><sup>21</sup> «Созерцатель» (пали <em>jhāyī</em>,
                    санскр. <em>dhyāyī</em>) — тот, кто практикует «созерцание» или «медитацию»
                    (<em>jhāna</em>/<em>dhyāna</em>), одну из центральных форм буддийской йоги. Это
                    не просто концентрация, а глубокая система внутреннего развития, включающая:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>Освоение четырёх уровней <em>jhāna</em> — состояний всё более тонкого
                        сосредоточения.</li>
                    <li>Развитие умственного спокойствия (<em>samatha</em>) и ясности восприятия.</li>
                    <li>Подготовку ума к прозрению (<em>vipassanā</em>) и освобождению.</li>
                </ul>
                <p>В трактате <em>Висуддхимагга</em> описано 40 объектов медитации, служащих основой
                для достижения этих состояний. В широком смысле термин обозначает любого серьёзного
                практика, стремящегося к духовному пробуждению.<a href="#ref-21"
                              class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-22"><sup>22</sup> «Брахман» (пали <em>brāhmaṇa</em>,
                    санскр. <em>brāhmaṇa</em>) — в буддийском контексте это не указание на
                    представителя ведической жреческой касты, а характеристика духовно совершенного
                    человека:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li><em>Тот, кто достиг архатства</em> и освободился от всех загрязнений ума
                        (<em>āsava</em>).</li>
                    <li><em>Носитель мудрости и нравственности</em>, вне зависимости от
                        происхождения.</li>
                    <li><em>Преобразованное значение древнего индийского идеала</em>, как это
                        показано, например, в <em>Дхаммападе</em> (стихи 383, 396).</li>
                </ul>
                <p>В отличие от ведийской традиции, где «брахман» определялся рождением, в буддизме это
                состояние достигается через практику Учения Будды и считается высшим званием,
                доступным каждому, кто преодолел эго и страдание.<a href="#ref-22"
                              class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-23"><sup>23</sup> «Причина» (пали <em>hetu</em>,
                    санскр. <em>hetu</em>) здесь близка по смыслу к выражению «то, что
                    обуславливает», и служит основой учения о зависимом возникновении
                    (<em>paṭicca-samuppāda</em>). В буддийской причинности различают несколько видов:</p>
                <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
                    <li><em>Коренная причина</em> (<em>mūla-hetu</em>) — внутреннее побуждающее
                        начало явления.</li>
                    <li><em>Объективная причина</em> (<em>ārammaṇa-hetu</em>) — внешний объект,
                        вызывающий реакцию ума.</li>
                    <li><em>Непосредственная причина</em> (<em>sahajāta-hetu</em>) — фактор,
                        возникающий одновременно с явлением и поддерживающий его.</li>
                </ol>
                <p>В трактате <em>Paṭṭhāna</em> подробно рассматриваются 24 типа причинных связей,
                демонстрируя сложную систему взаимоотношений между психическими и физическими
                явлениями.<a href="#ref-23" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-24"><sup>24</sup> «Невозникновение» (пали
                    <em>nirodha</em>, санскр. <em>nirodha</em>) здесь означает не просто отсутствие
                    возникновения, а полное прекращение страдания и всех его причин. Это:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li><em>Прекращение загрязнений ума</em> (<em>āsavakkhaya</em>).</li>
                    <li><em>Угасание страдания</em> (<em>dukkhanirodha</em>).</li>
                    <li><em>Синоним ниббаны</em> — окончательного освобождения.</li>
                </ul>
                <p>Как третья из Четырёх Благородных Истин, <em>nirodha</em> указывает на возможность полного
                выхода из цикла перерождений через реализацию пути просветления. В <em>Saṃyutta-nikāya</em>
                (IV.251) это состояние определяется как «устранение жажды» (<em>taṇhākkhayo</em>), то есть
                коренного источника страдания.<a href="#ref-24"
                           class="text-blue-500 hover:underline">↩</a></p>
