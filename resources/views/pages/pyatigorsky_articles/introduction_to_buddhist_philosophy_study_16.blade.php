            <h3 class="text-xl text-center font-semibold text-zinc-700 my-4">Алайя-Виджняна,
                сознание-вместилище, или остаточное сознание, —<br>но что это за вещь?</h3>

            <p><em>(0)</em> Название этого семинара — не случайно. Ведь в самом деле, в
                алайя-виджняне — давайте так пока и обозначим этот «предмет» нашего (моего, во
                всяком случае) неуемного философского любопытства — мы имеем дело не с «чистой
                идеей» какого-то еще там сознания, а с чем-то «почти материальным»,
                психофизиологическим, если не биофизическим, наподобие, скажем, совсем не
                фантастического генома сегодняшней генетики. Тем самым мы оказываемся на пороге
                натурфилософии (как она трактовалась Лейбницем, Ньютоном и немного позднее
                Дарвином), что, разумеется, предполагает и изменение в эпистемологии нашего подхода.
                Начнем с эпистемологического различия двух понятий, алайя-виджняны и континуума
                мысли. Второе из них, как об этом было сказано в IX, не может рассматриваться как
                что-то в смысле отличия от чего-то другого или от чего угодно, тогда как первое и
                было введено великими учителями йогачары именно как «нечто, отличное от X ... или
                возникающее, когда Y». Ключевое слово в этой формуле — «когда». То есть когда в
                живом существе, саттве нет никакого сознания, тогда и только тогда в нем есть
                (именно уже есть, а не возникает) сознание-вместилище, алайя-виджняна.
                Алайя-виджняна здесь вводится не в постулятивно-аксиоматическом порядке, а в порядке
                вывода из абсолютного для любой и всей буддийской философии первичного (часто не
                высказываемого, а подразумеваемого) постулата о живых (т.е. обладающих по крайней
                мере одним органом чувства) существах, саттвах. Тогда эпистемологически
                алайя-виджняна в обоих наших текстах является a posteriori синтетической категорией,
                типично различительным понятием, вторичным и по преимуществу эмпирическим.</p>

            <p>Эмпирика, с которой мы имеем дело в школе йогачара-виджнянавада, одна, это — эмпирика
                непосредственного наблюдения и созерцания феноменов, возникающих в четырех
                созерцаниях, дхьянах, и в трансцендентальных сосредоточениях сознания, в самадхи. В
                отношении алайя-виджняны особую, если не главную, роль в этой эмпирике будут играть
                «чувства», «эмоции» (Skr. vedana). Но почему же чувства, а не, скажем, ощущения или
                восприятие? Прежде всего потому, что именно из эмоциональной сферы (как сферы «чисто
                психологический», так сказать) буддийская философия взяла свое оценочное измерение
                по оси «приятное — не-приятное — ни приятное — ни не-приятное», дополняющее
                этическо-кармическое измерение по оси «благое — не-благое — ни благое — ни
                не-благое». Первые учителя йогачары-виджнянавады считали, что
                эмоционально-мотивированная сфера психики входит в подоснову всей сознательной жизни
                живого существа вместе с шестью органами чувств и соответствующими им шестью
                сознаниями. Более того, чувства и эмоции могут как бы в обратном порядке служить
                индикаторами самого наличия восприятия, т.е. через них происходит наблюдение реакций
                и рефлексов на внешние и внутренние раздражители. Здесь, конечно, речь идет в первую
                очередь о наблюдении из трансцендентального состояния Абсолютного Прекращения, на
                эмпирике которого и основывалась вся философия этой школы.</p>

            <p>Таким образом, начиная с настоящего момента, мы будем одновременно иметь дело с двумя
                рядами феноменов. В первый ряд, мыслительный, будут входить основные феномены ума,
                мысли и сознания, минимальными единицами, «атомами» которых являются дхармы
                («моменты мысли»). Второй ряд, условно говоря, психологический, будет включать все
                психические факторы и состояния, эмпирически воспринимаемые по субъективной
                оценочной шкале как положительные, отрицательные и безразличные, но они же, как
                дхармы вообще, мыслятся как благие, дурные и неопределенные. При этом, однако, такое
                «параллельное» существование этих двух рядов феноменов (так же как и их
                одновременное наблюдение йогом-созерцателем) возможно только поскольку эти феномены
                возникают в одном и том же месте, т.е. в данном наблюдаемом живом существе, саттве.
                А из этого опять же следует переориентировка с времени на место, с привычной (и с
                точки зрения современных научных теорий) смены хронологии феноменов на их топологию.
                Но тогда мы, пытающиеся разобраться в результатах в основном созерцательной работы
                учителей нашей школы, оказываемся лицом к лицу со сложнейшей проблемой: хорошо, все
                временные факторы, функции и модальности нашей психики устранены, словом, устранена
                психология, о которой только что говорилось. Остается одна задача: определить, что
                же после этого остается в месте, которое в этом невиданном по дерзости и силе
                воображения философском эксперименте было «очищено» от психологии, кроме самого
                этого места, разумеется, т.е. живого существа, саттвы? Но ведь это место и то, что в
                нем остается, становятся как бы одним объектом, т.е. одним, но объективированным как
                другое. В этой связи можно вспомнить блестящее топологическое определение Жака
                Лакана: «Реальное... — это то, что всегда находится в том же месте». Только в нашем
                случае это и место реального, которое само будет рассматриваться как реальное.</p>

            <p>Как это ясно описано в обоих наших текстах, мастера <em>йогачары</em>, наблюдая
                психику и сознание в трансцендентальном состоянии Абсолютного Прекращения,
                установили два факта. Первый факт — в этом состоянии полностью уничтожаются все
                эмоции, мотивации и шесть органов чувств с их соответствующими сознаниями. Иначе
                говоря, прекращается вся психическая и умственная жизнь. Второй факт, что при этом
                созерцатель, пребывающий в состоянии Абсолютного Прекращения, продолжает жить.
                Именно таким, чисто Абсолютного образом мастера йогачары пришли к концепции о том,
                что должно существовать какое-то другое, особое сознание, способное выдержать не
                только самые радикальные трансформации, вызванные Абсолютным Прекращением, но и все
                спонтанно возникающие трансформации (Skr. parinama) психического и физического
                характера.</p>

            <p>И наконец, в своем наблюдении дхьянических и нормальных состояний сознания мастера
                йогачары обнаружили и описали особый ряд состояний, получивших название
                «промежуточных состояний» (Skr. antarabhava). Главным признаком этих состояний
                является редукция или приостановление психических функций и модальностей вплоть до
                полного их устранения. Это также побудило мастеров йогачары к попыткам найти и
                сформулировать новую теорию, которая могла бы объяснить эти состояния. Так,
                возможно, и увидело свет это «особое», или «дополнительное», сознание, описанное в
                своих основных чертах, в текстах настоящего семинара. По существу, три основных
                первоначальных названия этого сознания — «апроприирующее сознание» (Skr.
                adana-vijnana), «мысль как все семена» (Skr. sanva-bijaka citta) и
                «сознание-вместилище» (Skr. alaya-vijnana) — отражают три основных аспекта этого
                важнейшего для йогачары и для буддийской философии в целом понятия
                алайя-виджняны.</p>

            <p><em>(1)</em> В своем первом аспекте, который я называю «динамическим
                центростремительным», алайя-виджняна апроприирует все, что не есть сознание; и в
                первую очередь — «новое» тело живого существа в момент возникновения
                «сознания-связки», о котором говорилось выше. Здесь мы имеем дело фактически с
                биологической апроприацией, когда и тонкая, и грубая материи, составляющие тело с
                его органами чувств, уже «абсорбированы» алайя-виджняной. Здесь крайне интересным
                моментом является различие между апроприирующим сознанием и апроприируемым
                не-сознанием, точнее, между сознанием, которое сейчас апроприирует и не-сознанием,
                которое вот-вот будет апроприировано. Последнее в самый момент апроприации еще
                принадлежит неопределенному будущему именно «еще», ибо «стрела времени» в этот
                момент направлена из будущего времени апроприирующего сознания к настоящему времени
                апроприирования.</p>

            <p><em>(2)</em> Во втором своем аспекте, «статическом центробежном», алайя-виджняна
                представляет собой сумму всех кармически заряженных мыслей, чувств и ощущений живого
                существа, называемых «семена». «Семена» — это не сущности, не вещи, а латентные
                факторы, скорее даже тенденции, которые могут производить кармический эффект (как
                материальный, так и сознательный или психический) в будущем. Два слова в этом
                определении особенно важны: «могут» и «будущее», неопределенное в отношении «когда»
                это случится. Если следовать за Шмитхаузеном, получается, что поскольку йогачара
                отвергла идею школы сарвастивада о (реальности) дхарм прошлого и будущего, идею, из
                которой следовало, что последующее созревание «кармических семян» имеет своей
                непосредственной причиной прошлое действие, прошлую карму, то возникла необходимость
                найти какие-то другие, особые силы или факторы, как бы «спящие», не действующие в
                промежутке между прошлым и будущим, т.е. все семена. Тогда, с точки зрения сознания
                как суммы всех семян, «стрела времени» должна быть направлена из прошлого в будущее.
                Но почему я сейчас назвал это будущее «неопределенным»? В ответе на этот вопрос нам
                придется опять, в который раз, вернуться к теме времени в буддийской философии.</p>

            <p>Когда Бертран Рассел говорил, что мы не можем указать на само время, но можем указать
                лишь на событие, случившееся в это (или другое) время, то он сформулировал проблему
                времени точно так, как это бы сделал какой-нибудь учитель Абхидхармы, живший за две
                тысячи лет до него. Единственным различием между позициями Рассела и Буддхагхоши
                будет то, что для последнего «время вообще» есть осознание чего-то как времени.
                Более того, то, что для Рассела было событием, для Буддхагхоши было вечным и
                вездесущим событием мышления (или сознания). Слово «будущее» в нашем рассуждении о
                йогачаре имеет два смысла. Во-первых, оно обозначает все события, являющиеся
                результатом кармических факторов и тенденций (т.е. «семян»), события, еще не
                актуализированные к настоящему времени. Во-вторых, оно обозначает время их
                актуализации, т.е. время, в котором данное событие произойдет или могло бы
                произойти. Однако, рассуждая таким образом, нам следует учесть и время, в течение
                которого происходит как бы «созревание» (Р. и Skr. vipaka) кармически латентных
                семян, которые и станут событиями и фактами будущего. Иначе говоря, учесть временной
                интервал между возникновением семени и актуализации этого семени в, так сказать,
                «результирующем» событии. Но поскольку все семена являются по определению
                безначальными, это «время, в течение которого...» оказывается в принципе
                неопределенным, каким, по существу, будет и время возникновения семени. Тогда можно
                было бы заключить этот разговор о времени, просто сказав, что сознание хотя и
                соотносится с временем, но само — безвременно («бездонно во времени», как говорили
                абхидхармисты).</p>

            <p><em>(3)</em> В своем третьем аспекте, синтетическом, алайя-виджняна — это буквально
                предел синтеза, ибо она «вмещает в себя все, что есть», а оттого и означает
                «сознание-вместилище». Следовательно, она по преимуществу соотносится с понятием и
                феноменом живого существа, саттвы. А это вынуждает нас к рассуждениям о таких
                предметах, которые никак не укладываются в рамки не только буддизма Большой
                Колесницы, но буддизма вообще. Более того, как мы видим в том пассаже нашего текста,
                где речь идет о калале, трактовка калалы мастерами виджнянаводы живо напоминает
                аналогичные по предмету (скажем, предмету эмбриологии) трактовки в европейских
                натурфилософиях XVII и XVIII веков. По Шмитхаузену получается, что алайя-виджняна,
                сознание-вместилище (во всяком случае, в ортодоксальной трактовке школы
                виджнянавада-йогачара), имеется у каждого живого существа, саттвы. В связи с этим
                напомню, что минимумом сознания живого существа является один орган чувств (скажем,
                поверхность тела, как орган осязания), а максимумом сознания будет комплекс шести
                органов чувств вместе с соответствующими им специальными сознаниями, в первую
                очередь «сознанием ума» (Skr. manovijnana).</p>

            <p>Теперь вернемся к названию нашего семинара и спросим: если сознание-вместилище есть
                вещь, то что же это за вещь? Я думаю, что ответить на этот вопрос мы сможем, только
                исходя из четырех объективных фундаментальных концепций, установленных нашими
                предшественниками — мастерами йога- чары-виджнянавады, на этот вопрос уже
                отвечавшими. Вот эти четыре концепции.</p>

            <p><strong>I.</strong> Концепция сознания (или мысли) как того, что осознает (или
                мыслит) или может осознавать свои объекты, а также самое себя в качестве одного из
                своих объектов.</p>

            <p><strong>II.</strong> Концепция не-сознания (заметьте, никак не «подсознания»!) или
                материи как того, что не осознает и не может осознавать ни объектов, ни самое себя.
                Подчеркиваю, что эта концепция никак не противоречит первой, но является
                дополнительной к ней, поскольку не-сознание (или не-мысль) полагается одной из
                разновидностей сознания, точнее, одним из подклассов класса объектов сознания.</p>

            <p><strong>III.</strong> Концепция живого существа, саттвы, которое является
                индивидуальной и, по определению, пространственной единицей сознания.</p>

            <p><strong>IV.</strong> Концепция кармы. В школе йогачара-виджнянавады дается следующее
                определение кармы. Карма — это сила воздействия одних мыслей (действий, слов и
                т.д.), благих, не-благих или нейтральных, на другие мысли (действия, слова и т.д.) и
                места, где эти другие мысли возникают (такие, как живые существа, люди, боги, духи и
                т.д.). Таким образом, эти другие сами оказываются результатами первых. Возможно, что
                именно в этой школе была предпринята первая попытка превратить почти общеиндийскую
                полуэтическую, полумифологическую идею кармы в фундаментальную категорию философской
                теории сознания. В основе последней была заложена аксиома о том, что мысль (или
                сознание) в момент, предшествующий ее возникновению в том или ином живом существе,
                является кармически нейтральной (столь же нейтральной, как материя или погода) и
                становится кармически заряженной только в последующий момент (когда она уже
                локализована) и тем самым способной произвести будущий кармический эффект.</p>

            <p>В этой связи будет своевременным заметить, что слово «будущий» здесь значит «могущий
                случиться в будущем месте возникновения данной мысли или данного сознания, месте,
                которым будет являться и будущее существование (Skr. bhava) данного живого
                существа». И эти вот кармически заряженные, но еще не произведшие своего «будущего»
                кармического эффекта мысли и являются «семенами» (Skr. bija), составляющими
                содержание алайя-виджняны. Они-то и являются как бы «единицами кармы» в этой школе.
                И наконец, поскольку сознание-вместилище содержит в себе все кармически заряженные
                семена, благие, не-благие и нейтральные, то само оно абсолютно нейтрально
                кармически.</p>

            <p>Теперь совершим маленький экскурс в феноменологию кармы. В самом деле, хотя, как нам
                это известно из предыдущих параграфов, стрела времени для сознания-вместилища всех
                семян направлена из прошлого в будущее, вся «кармическая картина» алайя-виджняны
                будет выглядеть совершенно иначе, если мы примем во внимание следующее
                обстоятельство. Только с точки зрения уже результатирующего кармического эффекта
                будет возможен какой-либо разговор о семенах как носителях кармического заряда или
                даже о карме вообще. Вспомним, ведь когда мы рассуждали о времени в связи с
                возникновением мысли, мы были вынуждены принять идею абсолютного настоящего как
                единственную возможность мыслить о прошлых мыслях (дхармах). Так и сейчас, говоря о
                концепции кармы в йогачаре, мы оказываемся вынужденными принять идею абсолютного
                будущего.</p>

            <p>Сосредоточимся теперь на наиболее философски интересных моментах алайя-виджняны,
                понимаемых в свете только что изложенных четырех объективных концепций.</p>

            <p><strong><em>А.</em></strong> Сознание-вместилище объективно (то есть при наблюдении
                со стороны
                опытным йогом-созерцателем) представляет собой индивидуальный субстрат каждого
                живого существа, включая и существа, не имеющие так называемого «шестого сознания»,
                сознания ума, и, таким образом, не могущие быть для себя субъектами никакого
                сознания или мышления. Отсюда, собственно, и наше «объективно», ибо субъективность
                здесь исключается в силу уже рассмотренной нами выше концепции не-мысли или
                не-сознания. Именно как субстрат, сознание-вместилище, в отличие от любого другого
                сознания (так же как и от отдельной атомарной мысли), является длительным:
                длительным прежде всего в пространстве, в котором оно образует своего рода
                подпространство, в котором невозможно найти точку, где бы ни находилось то же самое
                сознание-вместилище. Но оно обладает также и временной длительностью, т.е. когда
                имеется сознание-вместилище — как, например, во время трансцендентальной медитации
                или во время, непосредственно предшествующее моменту смерти, — тогда невозможно быть
                даже единому мигу, в котором данное сознание-вместилище не было бы присутствующим;
                по контрасту, скажем, с описанным выше потоком или континуумом мысли, который
                прерывается трансцендентальными созерцаниями и другими промежуточными состояниями.
                Это означает и гомогенность алайя-виджняны в любой точке занимаемого ею пространства
                и в любом моменте ее временной длительности.</p>

            <p>На этом витке нашего размышления над концепцией алайя-виджняны мы снова сталкиваемся
                с излюбленной у буддийских философов «игрой со временем». Время алайя-виджняны
                безначально, ибо в ней буквально ничего не происходит. Ни событий, ни перемен, ни
                случаев — только в смысле каковых мы, согласно Васубандху или сэру Артуру
                Эддингтону, могли бы говорить о времени вообще. Более того, «бездонное прошлое» и
                «открытое будущее» делает воображаемое время алайя-виджняны неопределенным и в
                отношении измерения в его единицах длительности событий и интервалов между ними.
                Опять-таки, будущее — хотя и не определенное относительно времени, когда могут
                произойти события, результирующие из семян, — все равно уже есть в этих семенах. Но
                и здесь будет необходима оговорка. Потому что, хотя будущее «открыто», но, говоря об
                одной данной алайя-виджняне, ее микрокосмическое время закончится в Нирване вместе с
                завершением данного кармического процесса, а космическое время закончится, когда
                будут нирванизированы все живые существа саттвы и прекратится сансара. На этом и
                основана идея космического абсолютного будущего в йогачаре, в то время как
                абсолютное прошлое остается невозможным для этой школы. Здесь абсолютное будущее
                играет такую же роль, какую в классической Абхидхарме играет абсолютное
                настоящее.</p>

            <p><strong><em>B.</em></strong> Будучи длительным, непрерывным и гомогенным,
                сознание-вместилище
                связывает последний момент мысли живого существа до его смерти или до одного из пяти
                других промежуточных состояний, таких, скажем, как Абсолютное Прекращение или Высшее
                Трансцендентальное Самадхи, с первым моментом после смерти или после выхода из
                другого промежуточного состояния. Разумеется, о последних можно говорить только в
                случае человеческих или божественных рождений, то есть только в случае, когда живое
                существо имеет шестой орган чувств, ум. Ведь лишь они могут, во-первых, себя
                осознавать, а во-вторых, генерировать йогические, медитативные состояния дхьяны и
                самадхи, в которых все шесть сознаний элиминируются. Таким же образом алайя-виджняна
                служит средой, связывающей кармический фактор (причину) в одном рождении с
                кармическим эффектом (следствием) в другом рождении (Skr. bhava) данного живого
                существа. Но напоминаю, что ни кармические факторы, кармически заряженные семена, ни
                карма вообще (в смысле приведенного в этом семинаре операционального определения) не
                могут мыслить или осознавать.</p>

            <p><strong><em>C.</em></strong> Только благодаря алайя-виджняне живое существо в его данном
                существовании связано с одной материальной формой, определяемой маткой (Skr. yoni),
                с одним образом, ходом (Skr. gati) существования, и пребывает в одной сфере (Skr.
                dhatu) существования. И в какой бы сфере или форме данному существу ни предстояло
                обрести новое рождение, алайя-виджняна (та же) будет основой его существования.</p>

            <p><strong><em>D.</em></strong> Возвращаясь к тому, что было сказано в начале семинара об
                эмпирицистском походе к алайя-виджняне, можно высказать предположение, что уже в
                ранней йогачара-виджнянаваде концепция сознания-вместилища существовала в двух
                версиях. В первой, почти натурфилософской, алайя-виджняна непосредственно выводилась
                из установленного в опыте трансцендентального созерцания факта отсутствия (точнее,
                может быть, устранения) остальных сознаний (шестого, сознания ума, конечно, в первую
                очередь), наблюдаемого в таких промежуточных состояниях (Skr. antarabhava), как
                момент смерти, глубокий сон, глубокий обморок, первичное состояние эмбриона,
                непосредственно следующее за зачатием, и состояние не-восприятия, генерированное
                сверхъестественными (или полубожественными) существами. Нет алайя-виджняны только в
                Нирване, поскольку в последней нет и скандх. В своей второй, универсалистской
                версии, сознание-вместилище является тем, что испытывает перерождения, и тем, что
                содержит семена всех мыслей (дхарм). Из этого возможен вывод: оно является всем, что
                есть, всем миром. Это, однако, нуждается в эпистемологическом разъяснении. «Всё»
                значит «все дхармы», а «весь мир» значит «все живые существа». Получается, что тогда
                неизбежен такой вывод: все есть или все, что есть, — это только сознание (Skr.
                vijnapti-matra). Однако, чье сознание? Какое сознание? Дело ведь в том, что
                индивидуальное живое существо не может познать свое сознание-вместилище, свою
                алайя-виджняну посредством своего «шестого сознания», то есть «сознания ума» (Skr.
                manovijndna), потому что в момент (или в месте), где алайя-виджняна могла бы быть
                познана сознанием ума, последнее уже прекращено, отключено в смерти,
                трансцендентальной медитации или в любом другом промежуточном состоянии, оттого и
                великие учителя йогачары могли воспринимать, познавать алайя-виджняну,
                сознание-вместилище только как что-то остающееся в их трансцендентальной медитации,
                когда все остальные когнитивные процессы и модальности остановлены или прекращены.
                Отсюда и название — остаточное сознание. Оттого-то Будда, который знал, что у него
                нет сознания-вместилища (именно потому, что он знал о его отсутствии), открыл своим
                ученикам, йогачаринам, что это нечто, что-то, остающееся «в остатке» их
                трансцендентальной медитации, и есть сознание-вместилище, остаточное сознание,
                алайя-виджняна. Отсюда следует, что алайя-виджняна может познаваться либо
                посредством вывода и обобщения эмпирических данных практики трансцендентальной
                медитации, либо посредством наблюдения внешним наблюдателем всех живых существ,
                который сам живым существом не является, ибо у него нет ни одного из пяти агрегатов
                индивидуального существования, скандх. Только для такого внешнего наблюдателя, то
                есть для Будды-Татхагаты, алайя-виджняна является объектом. Но является ли она
                объектом для самой себя?</p>
            <p><strong><em>E.</em></strong> Ответом может быть только — конечно не является. Ибо,
                рассуждая строго абхидхармистски, алайя-виджняна лишена самосознания (Skr.
                svasamvedana), хотя она и является сознанием (седьмыми, так сказать), и как таковое
                по определению имеет свой объект. Но в отличие от других шести сознаний, ее объект
                всегда как бы «здесь» и всегда тот же самый, неизменный. Хотя и не-вечный (поскольку
                и он исчезнет в Нирване), этот объект является длящейся в бесконечности непрерывной
                серией моментов-мыслей о «Я» (Skr. ahamkara) и о «Я есть» (Skr. asmimana). В этом
                смысле алайя-виджняна могла бы рассматриваться как такая серия мысли о мысли, как
                если бы вторая мысль была самим «Я» в каждый момент этой серии. Иначе говоря,
                алайя-виджняна могла бы рассматриваться как безначальная ошибка, иллюзия
                непробужденного сознания. Только неустанным упражнением в созерцании и наращиванием
                своей йогической энергии сможет созерцатель достичь сверхъестественное знание (Skr.
                lokotara-jnana) и постигнуть Абсолютную Реальность (Skr. dharmadhatu), за чем
                последует прекращение алайя-виджняны.
            </p>
            <p><strong><em>F.</em></strong> Мы знаем из предыдущих семинаров, что три «вечных»
                термина буддийской философии — мысль, ум и сознание — являются синонимами или
                квазисинонимами. Однако в виджнянавадинской версии Абхидхармы они представляют три
                различные временные стороны именно «ума»: «мысль» (Skr. citta) относится к моментам
                ума в настоящее время, «ум» (Skr. manas) к моментам прошедшего времени ума, а
                «сознание» (Skr. vijnana) — к его будущим моментам. Эта временная троичность нашла
                свое отражение и в том факте, что сознание-вместилище в йогачаре оказывается по
                преимуществу ориентированным на будущее. Возможно, это звучит слишком категорично и
                не согласуется с духом и стилем буддийской «игры со временем». Так, по мнению
                Ламберта Шмитхаузена, когда настоящее сознание-вместилище, являющееся результатом
                прошлой кармы, иссякает, то отпечатки, следы, «послевкусие» (Skr. vasana) еще
                полностью не проявившейся прошлой кармы вместе со словами основных ложных идей
                (таких как дуальность «добра и зла» или дуальность «объекта и субъекта») генерирует
                сознание-вместилище. Но это утверждение нисколько не упрощает дело, оно ставит нас
                перед сложнейшим вопросом: что тогда порождает что, время — карму или карма — время?
                Из предшествующих семинаров мы знаем, что в классической Абхидхарме есть только одно
                время, время настоящего мгновения мысли. Но мы уже знаем, что в йогачаре время — это
                время созревания (Skr. vipaka) кармы, то есть время промежутка между данной
                предположительно кармически заряженной мыслью и какой-то прошлой, также
                предположительно кармически заряженной мыслью, еще полностью не результировавшей в
                настоящем времени. И тут хочется так ответить на поставленный выше вопрос: пожалуй,
                все-таки это время порождает карму, а не карма время, что, впрочем, никак не
                согласуется с классической абхидхармистской аксиомой о порождении времени мышлением
                и не вполне согласуется с дополнительной к ней аксиомой о времени как
                самодостаточном и независимом от того, временем чего оно является. Можно
                предположить, что именно в йогачаре впервые в человеческой истории была высказана
                идея о времени как первичном в отношении того, временем чего оно является.
            </p>
            <p>
                Говоря о времени, следует добавить, что не может быть никаких временных интервалов
                между семенами в данном сознании-вместилище, потому что все, что в ней есть,
                абсолютно синхронно — в отличие от континуума мысли. В то же время вполне возможно
                предположить, что исторически идея континуума-сознания оказалась отправным моментом
                в формировании концепции сознания-вместилища, которое можно рассматривать как
                «синхронизированный» континуум мысли.
            </p>
            <p><strong><em>G.</em></strong> Если исходить из изначального абхидхармистского определения мышления как того, что
                мыслит об объекте, и сознания как того, что осознает объект, то, в качестве
                сознания, сознание- вместилище, алайя-виджняна, окажется в позиции, промежуточной
                между сознанием и не-сознанием (материей, пространством, кармой и так далее). Я
                думаю, что в понимании такой промежуточности алайя-виджняны нам может помочь одна из
                самых устойчивых концепций европейской философии XVII — XVIII веков — концепция
                саморефлексируюшего субъекта, нашедшая свое привычное лингвистическое выражение в
                таких словах, как «Я», «Кто-то», «Кто-нибудь», «человек» и так далее, и ставшая
                (зачастую спонтанно, неотрефлексированно) основой для нашего мышления о мышлении и
                сознании. Эта концепция четко контрастирует с абхидхармистской и особенно
                йогачаринской концепцией сознания, которое может оказаться осознающим (или
                не-осознающим) тот или иной объект, включая и самое себя. Говоря о промежуточности
                алайя-виджняны, можно было бы добавить, что она не может рассматриваться в качестве
                саморефлексирующего субъекта еще и по причине своей крайне слабой энергетичности,
                фоновости, так сказать. К ней более всего подходит английский термин
                background-consciousness, сознание-основа. Подходит, во-первых, потому, что оно
                существует в безначальном прошлом живого существа, и, во-вторых, потому, что
                продолжается в живом существе до конца сансары.
            </p>
            <p><strong><em>Н.</em></strong> Теперь вновь попытаемся ответить на вопрос в названии семинара: что это за вещь
                такая, алайя-виджняна? Не концепт, не понятие, а именно вещь. В мире есть
                бесконечное число живых существ, у каждого из которых есть своя алайя-виджняна,
                которая определяет индивидуальность каждого живого существа. Почему же тогда не
                считать «сознание-вместилище» и «живое существо» синонимами?
            </p>
            <p>
                Во-первых, потому, что живое существо является не только объектом, но и вещью,
                ограниченной во времени и пространстве. Живое существо, саттва, существует во
                внешнем времени и в терминах его физической длительности и «исторической»
                хронологии. Другими словами, живое существо существует во времени своих изменений,
                обусловленных в порядке Взаимообусловленного Возникновения. Алайя-виджняна — одна и
                та же, та же самая; то есть тот же самый вневременной объект. Объект в смысле объект
                мышления, созерцания, внешнего наблюдения, наконец. Такой же вневременной объект,
                как и Взаимообусловленное Возникновение, так же как и последнее, не являющееся
                дхармой в терминологическом смысле этого слова.
            </p>
            <p>
                Во-вторых, мы знаем, что живое существо находится в теле, рамками которого оно
                отделено от других живых существ, оно конечно. Алайя-виджняна находится в живом
                существе, но само не имеет формы («тела внутри живого тела»). Вообще говоря, с
                буддистской точки зрения, пространство есть пространство живых существ, пробужденных
                и не пробужденных, саттв и Бодхисаттв. Но сознание-вместилище не имеет
                пространственных границ во «внешнем» пространстве, а его «внутреннее» пространство
                бесконечно во всех направлениях и таким образом (здесь «таким образом» — это
                метафорический оборот) содержит в себе все живые существа прошлого, настоящего и
                будущего. В связи с этим напоминаю, что в Абхидхарме пространство, как и Нирвана,
                является необусловленной дхармой.
            </p>
            <p>
                Наконец, в-третьих, живое существо — это вещь, поскольку в принципе оно себя не
                осознает — это так, по крайней мере в тех случаях, когда оно лишено сознания ума. В
                йогачаре, как, впрочем, и во многих брахманистских философских школах, вещью
                является объект, который не может себя осознавать, то есть не может быть
                «рефлексирующим субъектом». Об алайя-виджняне можно мыслить, что она в принципе
                способна осознавать себя — пусть сколь угодно слабо, не энергетично, как относящуюся
                к данному живому существу и в этом смысле может рассматриваться как условный
                (потенциальный, латентный) рефлексирующий субъект, которому, опять же только
                условно, могут быть приписаны все действительные и возможные состояния сознания и
                модификации психики данного живого существа. Более того, в общефилософском смысле
                алайя-виджняна может представляться чем-то вроде универсальной медитирующей среды:
                она медитирует между сознательным и не-сознательным в живом существе, соединяет
                сознательные состояния в сериях мыслей, прерванных смертью и другими промежуточными
                состояниями, определяет форму и характер отношения сознания и ума к материи,
                психологии к физиологии и биологии во всяком данном живом существе. В этой связи
                будет возможным такое операциональное (и конечно, частичное) определение:
                сознание-вместилище или алайя-виджняна — это то, что в одном месте связывает одно
                сознание с другим, в другом месте связывает сознание с не-сознанием, а в третьем
                месте связывает одно не-сознание с другим. Притом что «место» — это живое существо.Я
                думаю, что концепция алайя-виджняны — это наистраннейшая философская странность во
                всей буддийской философии. Это подчеркивается еще тем, что и в учении йогачары она
                занимает несколько обособленное место. Ведь ее едва ли можно вывести из общего
                принципа этой школы: «все это — только сознание» (точнее будет сказать, что все
                осознаваемое есть только сознание). И она уж никак не выводима из
                религиозно-философской теории Большой Колесницы в целом. Я думаю, все, что в этой
                концепции есть нового и интересного, является новым опытом переосмысления и
                переработки основных положений Абхидхармы.
            </p>

