                    еще по крайней мере двумя сознаниями. Но с точки зрения Абхидхармы и это будет
                    весьма проблематичным, ибо в большинстве абхидхармистских школ считалось, что
                    несколько сознаний или мыслей не могут возникать одновременно в одном и том же
                    континууме сознания»: Schmithausten L. Alayavijnana: On the Origin and the Early
