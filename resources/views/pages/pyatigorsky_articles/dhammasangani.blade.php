            <p>Как уже об этом было сказано в предыдущем сообщении, в
                основе структуры первого раздела [cite: 2] лежит разделение текста на
                то, что относится к «дхармам» Состояниям сознания
                (т. е. перечисление их названий и объясняющих эти названия
                групп терминов, иначе наборов ключевых слов), и на то, что
                относится к «случаям» (samaya), в которых эти дхармы фигури-
                руют, как «благие», «не-благие» или «неопределимые» в отноше-
                нии кармы. [cite: 2]</p>
