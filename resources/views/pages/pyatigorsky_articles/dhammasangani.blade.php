                считаю здесь отдельными «случаями» повторения внутри «слу-
                чая» их имеется несколько десятков). [cite: 7] «Случаи» пронумеро-
                ваны в порядке следования в тексте римскими цифрами. [cite: 7]</p>
            <p>1.2. Просмотр всех 230-ти «случаев» показал, что каждый
                «случай» является комплексом единиц описания, которые
                я называю элементами. [cite: 8] При рассмотрении всех «случаев»
                в порядке встречаемости в тексте раздела элемент выступает,
                как тот минимум текста, на который один «случай»
                отличается от другого. [cite: 9] Таких элементов имеется около 130. В со-
                ставе различных «случаев» насчитывается от 6-ти до 20-ти эле-
                ментов. [cite: 10] Все элементы пронумерованы арабскими цифрами в по-
                рядке встречаемости в «случаях» текста. [cite: 11] Три наши примера мо-
                гут быть обозначены как наборы номеров элементов: І (1, 2,
                3,4,5,6);IV-(1,2,7,8,4,5,6)$ ; [cite: 12] CCXXI (122, 125, 127,
                9, 4, 5. [cite: 12]</p>
            <p>1.3. Слова, из которых состоят элементы «случаев», я назы-
                ваю «терминами элементов случаев». Я предполагаю,
                что все эти слова в контексте элементов имеют специаль-
                ный технический смысл (даже такие, как upannam hoti B
                элементах 1 и 122). Это означает, прежде всего, что одно и тоже
                слово может иметь различный смысл, в зависимости от того,
                фигурирует ли оно как название дхармы, или в контексте
                элемента (например, слово cittam «сознание» в названии
                пятой дхармы, или в элементе. 1)[cite: 13].</p>
            <p>2. Но дихотомия «дхарма случай» (dhamma
                samaya)
                представляется мне и центральным содержательно-праг-
                матическим разграничением в разделе, ибо именно в смыс-
                ле этого разграничения оба эти понятия обнаруживают конкрет-
                ное психологическое и не-психологическое содержание. [cite: 14]</p>
            <p>Одно и то же слово (термин), фигурируя в элементе «слу-
                чая», реально обозначает конкретные психические состояния
                (факты, феномены, процессы) и, таким образом, здесь слову-
                термину соответствует значение. В названии же дхар-
                мы оно выступает как чисто условное (символическое)
                обозначение необозначаемого (т. е. здесь отсутствует ре-
                альная семантическая связь). [cite: 15]</p>
            <p>И если принять ставший традиционным перевод слова «дхар-
                ма», как «состояние сознания», то «сознание» здесь будет вы-
                ступать, как неописуемая (только символически обозна-
                чаемая) рефлексивная процедура высшего ранга, по отношению
                к которой все психические явления «случаев» выступают как опи-
                суемые. [cite: 16] Я полагаю, что проблема дихотомии «сознание-психи-
                ка» одна из главных проблем буддийской метафизики [cite: 17]</p>
            <p>мо-
                жет найти свое схематическое, предварительное разрешение в
                дихотомии «дхарма-самайя». [cite: 17]</p>
            <p>3. Сами-же «случаи» в смысле этой дихотомии могут рас-
                сматриваться как описания психической жизни обыкновенного
                человека, и йога. [cite: 18] Но, в известном смысле, и, очевидно, в контек-
                сте раздела и трактата в целом, всякая психическая жизнь мо-
                жет быть описана как определенный уровень (низший, нуле-
                вой?) йоги. [cite: 19] Тогда каждый «случай» будет выступать, как неко-
                торый самозамкнутый психический акт, соотноси-
                мый с определенным уровнем дхьяны-самадхи[cite: 20].</p>
