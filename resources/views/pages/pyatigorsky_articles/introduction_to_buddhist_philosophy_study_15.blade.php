            <h3 class="text-xl text-center font-semibold text-zinc-700 my-4"><em>МАДХЬЯМИКА</em> КАК
                ФИЛОСОФИЯ ФИЛОСОФИИ.<br>ВЗАИМООБУСЛОВЛЕННОЕ ВОЗНИКНОВЕНИЕ, <em>СВАБХАВА</em> И
                ПУСТОТА</h3>
            <p><em>(0)</em> Общее объяснение в этом семинаре будет резко отличаться от всех
                предшествующих тем, так как здесь я рассуждаю из своего современного философского
                контекста и оттого должен осознавать <span class="underline">мое</span> философствование наряду с тем, о котором
                идет рассуждение — не пытаясь целиком становиться на точку зрения последнего. Такой
                подход оправдан самим характером философии <em>Мадхьямики</em>. Такой же, я думаю, подход
                применял Мартин Хайдеггер в своих «Семинарах о Мышлении», рассуждая о философии
                Парменида. Ибо при абстрактном подходе, не берущем в расчет самого рассуждающего,
                <em>Мадхьямика</em> в своих основных положениях может показаться начинающему абракадаброй.
                Притом, конечно, что сам я — тоже начинающий, каким будет любой, философ или не
                философ, кто <span class="underline">сейчас</span> начинает думать о <em>Мадхьямике</em>, даже если он уже думал о ней сто
                раз. Однако раз я уже заявил, что исхожу из современного философского контекста, то
                этим уже обязал (а точнее — связал) себя обращаться с этим контекстом как с объектом
                <span class="underline">моей</span> философской рефлексии. Ну а «современный» — что это такое? В моем философском
                рассмотрении современный значит то, как я <span class="underline">сейчас</span> думаю о своем думаний и о других
                думаниях, но тоже сейчас. «Современность» в обычном словоупотреблении — это
                историческая абстракция (чтобы не сказать «аберрация»), обращение с которой в
                философии должно быть крайне осторожным. Сомневаюсь, чтобы понятие современности
                присутствовало в буддизме Большой Колесницы. Ну а философия, как мы будем
                справляться с этим странным понятием?</p>
            <p>Начнем со смысла нашего слова «философия» в его применении к <em>Мадхьямике</em>, а точнее, со
                смысла, в котором мы будем понимать слово «Мадхьямика». Самое интересное в
                <em>Мадхьямике</em> — это то, что она философствует не о <em>дхармах</em>, как <em>Абхидхармах</em>, но о
                <em>дхармах</em>, ставших объектами <span class="underline">другой</span> философской рефлексии, и о понятиях и терминах, в
                которых такая рефлексия совершается. Иначе говоря, <em>Мадхьямика</em> — это
                <span class="underline">философствование, имеющее своим объектом философию <em>Абхидхармы</em></span>. Вот почему знаменитая
                тетралемма Нагарджуны: ((l) S. есть P; (2) S. не есть Р.; (3) S. не есть ни Р., ни
                не-Р.; (4) S. и Р. не знают причинной связи) является, по существу, не чем иным, как
                истолкованием Взаимообусловленного Возникновения.</p>
            <p><em>(1)</em> Название этой философии — <em>Мадхьямика</em> — означает «срединный» (санскр. <em>madhya</em>, «средний»), ибо она является философией срединности по преимуществу.
                Срединности не была чужда и древнегреческая философия, вспомним хотя бы «золотую
                середину» у Аристотеля. Как мы знаем из первого семинара, в провозглашенном Буддой
                Благородном Пути благородство было синонимом срединности (кстати, не будем забывать,
                что Будда и Сократ вполне могли быть современниками). Срединность в палийских сутрах
                является все же в основном этической чертой правильного поведения и мышления аскета,
                хотя она уже в Первой Проповеди превращается в некую общую позицию, выражающую
                радикальную <span class="underline">смену угла зрения</span> аскета. Затем в трактатах Запредельного Знания эта
                позиция становится трансцендентальной. Но только в <em>Мадхьямике</em> срединность выступает
                как абсолютная не-позиция, выводимая из абсолюта Пустоты. С точки зрения этой
                не-позиции срединное значит не просто что «S не есть Р, ни не-Р», но что оно как бы
                извечно находится между (либо совсем в стороне от) «есть» и «нет». Но не будем
                забывать, что рефлексия над тем, что находится не между S и Р, а между «есть» и
                «нет» имеет свою долгую историю в Древней Индии. Так мы читаем в «Бхагавадгите»:
                «граница между тем, что на самом деле есть, и тем, чего нет, настолько тонка и
                неуловима, что лишь Мудрые могут ее различить».</p>
            <p>Теперь допустим, в смысле только что сказанного о «есть», что причинность вообще — не
                «есть», а что-то другое, например, «видится», «слышится», «созерцается»,
                «рассматривается». Тогда «не есть» четвертого члена тетралеммы может значить либо
                что причинность здесь не обсуждается, а как бы «подвешивается», берется в скобки,
                либо что понятию «есть» приписывается какой-то другой, особый смысл, который в
                тексте не эксплицируется. В этом случае, «не есть» может означать <span class="underline">исключение</span>
                причинности из класса объектов (дхарм), предикатом которых будет как существование,
                так и не-существование. Такое исключение — тоже своего рода «вход» в
                срединность.</p>
            <p><em>(2)</em> Взаимообусловленное Возникновение, рассуждая строго буддистически, — это
                не аксиоматически введенная истина, подобная четырем Благородным Истинам, а
                универсальное <span class="underline">состояние</span> всех феноменов, <em>дхарм</em>, как они есть на самом деле. Точнее,
                все они находятся в этом состоянии в каждый момент и в каждой точке пространства.
                Тетралемма Нагарджуны не отменяет Взаимообусловленное Возникновение, но
                философствует о нем, как если бы оно было чисто философской концепцией, попавшей в
                поле Запредельного Знания (а не становится ли философией <span class="underline">все</span>, попавшее в поле
                Запредельного Знания?). Тогда тетралемма Нагарджуны может мыслиться и пониматься как
                <span class="underline">обратная формулировка</span> Взаимообусловленного Возникновения. В смысле тетралеммы
                последнее утрачивает свою фактичность, его всеприменяемость также ставится под
                вопрос. Но философское значение такого переистолкования в том, что в нем объекты
                (вещи, лица, события, мысли) превращаются из объектов нефилософского (обыденного,
                научного, религиозного) мышления в объекты мышления философского, т.е. в объекты
                философии <em>Мадхьямики</em>, в которой это превращение достигается <span class="underline">обратным</span> путем, путем
                отсылки к Пустоте.</p>
            <p><em>(3)</em> Понятие Пустоты — камень преткновения для трех поколений буддологов и
                историков философии XX века. Оно безумно трудно, ибо чтобы разбираться в этом
                понятии, нынешнему философу придется отказаться от практически всех привычных
                альтернатив европейского философского и научного мышления. Таких прежде всего, как
                «монизм/дуализм», «частное/общее», «мысль (или сознание)/материя (или тело)»,
                «теория/практика». Пока же, только как первый шаг в понимании этой центральной
                категории <em>Мадхьямики</em> (другое самоназвание которой «учение о Пустоте», санскр. <em>Śūnyavāda</em>), заметим, что (как это было сформулировано Эдвардом Конзе полвека назад)
                в текстах этой школы Пустота почти всегда фигурирует как бы постфактум. То есть
                после факта предикации субъекта суждения S его предикатом Р — иначе говоря, в
                качестве обратной отсылки, о которой говорилось выше. Таким образом,
                философствование не начинается с Пустоты, но она «держится в уме», чтобы
                философствование продолжалось.</p>
            <p>Вторым шагом в нашем понимании Пустоты будет ее рассмотрение в ее двухаспектности.
                Первый аспект раскрывается в вопросах: «какая Пустота?», «Пустота чего?» и в
                ответах: «Пустота всех феноменов, <em>дхарм</em>. Пустота <em>скандх</em>», «Пустота Пустоты».
                Представить Пустоту в ее втором аспекте трудно до невозможности, для меня по крайней
                мере. Не более чем намек на содержание Пустоты в этом аспекте можно найти в
                вопросах: «<em>дхарма</em> — где?», «все это — где?», ответом на которые будет: «в Пустоте».
                Но последнее косвенно предполагает и точку зрения, т.е. Пустоту как
                позицию-не-позицию, о которой я только что говорил. Как можно видеть, Пустота в этом
                аспекте приобретает и какие-то черты идеального, ничем не заполненного пространства,
                <em>акаши</em> (в <em>Абхидхарме</em> единственной, кроме Нирваны, необусловленной <em>дхармы</em>).</p>
            <p>Таким образом, в первом своем аспекте, <span class="underline">негативно-различительном</span>. Пустота — это
                состояние лишенности данной <em>дхармой</em> ее отличительных признаков, т.е. того, что
                только и делает эту <em>дхарму</em> <span class="underline">данной</span>, отличной от других. Во втором же своем аспекте,
                квазионтологическом, Пустота — это состояние, которое как бы «есть», но только как
                <span class="underline">место</span>, в котором все мыслится лишенным своих признаков. Такое состояние тоже
                является состоянием чего-то, какой-то <span class="underline"><em>дхармы</em></span>, но в разговоре о последней мы уже
                сделали <span class="underline">обратную отсылку</span> к этому ее (и чего угодно другого) состоянию, Пустоте, а
                сама <em>дхарма</em> уже исчезла из нашего мышления и разговора.</p>
            <p>Здесь, конечно, велик соблазн — равно для буддийских тибетских мистиков-тантристов
                XII века н.э. и для наших современников — считать, что Пустота в этом аспекте
                скрывает какое-то неизречимое «нечто», которое не только «есть», но и может служить
                <span class="underline">причиной</span> возникновения (или не-возникновения) других «бытий». Или такое абсолютное
                нечто, какое не негативируется и даже не релятивизируется Пустотой в первом ее
                аспекте. Возможно, такая тенденция имелась, латентно, в самой <em>Мадхьямике</em>, но ее
                трудно проследить в тестах Нагарджуны и его первых учеников и комментаторов.</p>
            <p><em>(4)</em> Сейчас едва ли возможно установить, каким ветром понятие (и термин)
                «своебытия», <em>свабхавы</em> в его буддийском значении, было заброшено в учение
                Запредельного Знания, а оттуда в <em>Мадхьямику</em>? Был ли это ветер, возникший от
                «возмущений в пространство» (который упоминался в семинаре IX) или «ветер <em>дхармы</em>»
                (санскр. <em>dharma-vega</em>), о котором говорится в Алмазной Сутре Запредельного Знания? Так
                или иначе, но именно <em>свабхава</em> оказалась исходным пунктом философствования в
                <em>Мадхьямике</em>, фигурируя всегда ante factum того или иного утверждения. Если речь идет
                о <span class="underline">своебытии</span>, <em>свабхаве</em>, то о каком <span class="underline">бытии</span> здесь идет речь; бытие ли это всего, или
                чего-то одного, или любой вещи, <em>дхармы</em>, взятой в отдельности? Эдвард Конзе считает,
                что понятие своебытия, <em>свабхавы</em>, ведет свое начало от древнеиндийской традиции
                <em>Упанишад</em>, в которых бытие являлось, в первую очередь, не «бытием вообще», а каким-то
                особенным, странным бытием в каждом феномене того, что извечно предшествовало бытию
                или небытию этого феномена, так же как и бытию или небытию всего. Имеется в виду
                бытие или небытие, как два универсальных состояния вселенной. Тогда, возвращаясь к
                фрагменту (II. c) нашего текста, заметим: в смысле <em>Мадхьямики</em>, есть ли данный
                (конкретный) огонь или его нет — это никак не изменит того факта, горит ли он или не
                горит; жар остается своебытием, <em>свабхавой</em> факта или вещи (<em>дхармы</em>), называемой
                «огонь». Я думаю, что безусловное вытеснение бытия своебытием в философии <em>Мадхьямики</em>
                восходит к абхидхармистской идее о том, что <em>дхармы</em> не существуют или не-существуют,
                а возникают или не-возникают. Замечательно, в этой связи, что понятие «своебытие»,
                <em>свабхава</em> иногда замещается понятиями «свой знак» (санскр. <em>svalakṣaṇa</em>) и «своя форма»
                (санскр. <em>svarūpa</em>) (причем последнее не случайно ассоциируется с формой или идеей
                Платона). Но главное для нас в своебытии данной <em>дхармы</em> — это то, что оно не есть
                бытие (санскр. <em>bhāva</em>) <em>дхармы</em>, возникающее в порядке Взаимообусловленного
                Возникновения, но ее особость, объективированная как своего рода «первосущность».
                Именно таким образом понимаемая <em>свабхава</em> может созерцаться как воображаемый внешний
                объект. Такие термины, как «форма», «знак», «мета», подчеркивают ее, опять же
                воображаемую, зрительность. Внешняя объектность <em>свабхавы</em> тут же исчезает, как только
                мы переходим к ее квазионтологическому аспекту. В этом аспекте <em>свабхава</em>
                непредставима, одна <em>дхарма</em> не противопоставлена другой, не соотнесена с другой
                никаким образом, а отдельная для каждой <em>дхармы свабхава</em> становится собственным
                бытием всех <em>дхарм</em>.</p>
            <p><em>(5)</em> Как «свое бытие всего», <em>свабхава</em> тождественна Пустоте, которая не
                является <em>дхармой</em> и у которой поэтому нет своего бытия, т.е. <em>свабхавы</em> в
                различительном аспекте. Однако если мы включим Пустоту во «всё», то будет возможно
                говорить о Пустоте Пустоты как об одном из конкретных видов Пустоты, об одной из
                «пустот» — в каковом случае у последней будет мыслимой ее <em>свабхава</em>. Я думаю, что
                только в рассуждении о <span class="underline">своем</span> бытии как Пустоте понятие <span class="underline">другого</span> приобретает в ранних
                текстах Большой Колесницы философский смысл. В современной философии «другой» давно
                превратился в популярное обозначение <span class="underline">чего-то, что не есть «Я»</span>, лишенного какого-либо
                конкретного содержания. Самый элементарный анализ контекстов употребления понятия
                «другой» показывает, что оно в конечном счете обозначает чисто субъективное (т.е.
                устанавливаемое только в субъективном порядке) отношение двух индивидуальных
                сознаний (включая сюда и случай индивидуального бессознательного). В <em>Мадхьямике</em> же —
                поскольку все сознательные субъекты также являются <em>дхармами</em>, возникающими в порядке
                Взаимообусловленного Возникновения, — «другой» обозначает то единственное, что
                противопоставлено «своему» как высшая объективность Таковости, которая и есть
                Пустота, причем противопоставлено как «сумма» отдельных субъективностей. Именно в
                этом смысле следует понимать слова Эдварда Конзе о том, что Запредельное Знание —
                это субъективное дополнение к объективности Пустоты. Отсюда следует, что даже если
                мы («мы» здесь обозначает низший уровень субъективности) достигнем какого-то
                минимального понимания Запредельного Знания (что будем условно считать пределом
                достижимого на низшем уровне субъективности), то все равно останемся в этом
                феноменальном мире, в <em>сансаре</em>, по причине прежде всего нашей безнадежной
                субъективности. Только полностью поняв это, мы сможем начать понимать самые
                элементарные истины философии <em>Мадхьямики</em> в их истинном смысле. Так, например, самая
                избитая в ее тривиальном понимании истина, что «<em>Нирвана</em> и <em>сансара</em> — это одно и то
                же», будет переосмыслена в смысле, что «различие между <em>Нирваной</em> и <em>сансарой</em> само
                является пустым, т.е. субъективным в его отношении к высшей объективности Пустоты».
                Это нелегко понять, но следует вспомнить, что Пустота была вторично введена в
                <em>Мадхьямику</em> из сутр Запредельного Знания, введена как своебытие, <em>свабхава</em> всех <em>дхарм</em>
                (включая и себя самое), но тут же стала «трансцендентальным порогом», через который
                не переступит нога человека с улицы или профессора университета. Но почему же? Да
                просто потому, что <span class="underline">идея Пустоты как «своебытия» всего не релятивизирует, а
                практически упраздняет укорененное в нас понятие бытия</span> (и связанное с ним понятие
                небытия тоже). Введением же <em>свабхавы Мадхьямика</em> открыла дверь самым радикальным
                изменениям в философском мышлении. Историко-философски <em>Мадхьямику</em> можно поместить
                где-то между гегелевским онтологическим абсолютизмом и берклианским крайним
                эмпиризмом.</p>
            <p><em>(6)</em> В заключение попробуем себе вообразить ситуацию мышления, в которой
                могли бы оказаться первые из великих учителей <em>Мадхьямики</em> (Нагарджуна, Арьядева и
                другие, до Чандракирти). Представим себе, как они начинают размышлять об идеях,
                таких, скажем, как срединность, Таковость и Пустота, уже предоставленных в их
                распоряжение ранними сутрами Большой Колесницы и особенно сутрами Запредельного
                Знания. Эти идеи сами по себе не были началом философии <em>Мадхьямики</em>. Они становились
                таким началом, только став особыми объектами совсем иного философского мышления
                учителей этой школы. Именно такую ситуацию я имел в виду, когда в заключение к
                семинару XIII сказал об этих идеях, что они <span class="underline">будут</span> началом <em>Мадхьямики</em>. Они-то и
                образовали философское пространство нашей воображаемой ситуации мышления. В то же
                время само наличие этих идей можно рассматривать как начальную, точнее, <span class="underline">«нулевую» фазу</span> реконструируемой нами ситуации мышления. Затем в <span class="underline">первой фазе мыслители мыслят</span> о
                невозможности познать Истину посредством мышления, потому что мышление,
                употребляемое нами в познании Истины, неправильно, тривиально и субъективно. Но
                прежде всего наше мышление <span class="underline">объективно неадекватно</span> познаваемой истине, — это и есть
                основное во <span class="underline">второй фазе</span>. Затем мыслители приходят к заключению, что Истина доступна
                только Высшему Знанию, которое имеет своей основой не мышление, а особую интуицию. И
                наконец, в <span class="underline">третьей фазе</span> эти мыслители, как бы в порядке «эпистемологического
                отступления» будут заниматься радикальной трансформацией своего обыденного мышления
                Запредельным Знанием, только посредством которого и становится возможным постижение
                Истины Срединности, Истины Таковости и Истины Пустоты. Мне представляется, что
                такого рода эпистемологическое отступление поможет нам понять, почему первые из
                великих учителей <em>Мадхьямики</em> философствовали столь странным, казалось бы, образом,
                необычным не только для нас, но и для учителей других школ буддизма Большой
                Колесницы.</p>
