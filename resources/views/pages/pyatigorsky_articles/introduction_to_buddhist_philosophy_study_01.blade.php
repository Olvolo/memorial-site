                <blockquote class="my-6">
                    <p>Так я слышал<a href="#citation-2" id="ref-2"
                                      class="citation-mark"><sup>2</sup></a>. Однажды, когда Господь<a
                            href="#citation-3" id="ref-3" class="citation-mark"><sup>3</sup></a>
                        пребывал в Оленьем парке, в Исипатане («Прибежище Риши»), что близ Бенареса,
