                <p class="mb-3" id="citation-25"><sup>25</sup> «Пять скандх» — буквально «куча»,
                    «масса» (Р. khandha, Skr. skandha) — каждый из пяти агрегатов, составляющих
                    индивидуальное существование живого существа. Этими агрегатами являются: (1)
                    «форма» или «телесная форма» (Р. и Skr. тра); (2) «чувства» или «эмоции» (Р. и
                    Skr. vedana); (3) «восприятие» (Р. sanna, Skr. samjna); (4) «волевые акты» и
                    энергетические импульсы, ответственные как за поддержание единства живого
                    существа в каждый данный момент его существования, так и за динамику изменения
                    живого существа во времени и пространстве. Иногда эти импульсы называются
                    «синергии» (Р. sankhara, Skr. samskara — слово употребляется только во
                    множественном числе); (5) «сознание» (Р. vinnana, Skr. vijnana). Иногда эти
                    агрегаты — в силу того, что в их основании лежат привязанности к феноменам
                    жизни, «схватывание» этих феноменов сознанием — называются «агрегатами
                    привязанности». Но чаще, поскольку они приводят к страданию, их называют
                    «агрегатами страдания».<a href="#ref-25" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-26"><sup>26</sup> «Благородная Истина о Возникновении
                    Страдания» — более точным переводом будет «эмергенция» (Р., Skr. samudaya),
