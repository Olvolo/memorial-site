            <section>
                <h3 class="text-xl font-semibold text-zinc-700 mb-4">Методологическое предисловие:
                    маленький разговор о чужести в связи с изучением буддийской философии</h3>
                <p>Всякое мышление (включая мое собственное), о котором говорится, которое записано,
                    говорено, услышано, уже является чужим в данный момент разговора о нем, да
                    фактически и до этого момента, во всякий прошлый (да и будущий) момент
                    разговора, письма, чтения, слушания, имеющих своим объектом это мышление. Но что
                    же я называю словом «чужое»? Ведь это не просто «не мое», «твое» и т. д., что
                    неизбежно возвратило бы нас к одному из самых вульгарных понятий так называемой
                    «гуманистической» философии прошедшего двадцатого века, к понятию «другого». В
                    последнем случае тривиальные оппозиции «Я-Ты», «Я-Другой» (или «Другое», мир,
                    Бог, все, что угодно) полагались эпистемологическими константами человеческого
                    существования. Феноменологически «другость» редуцируется (как гуманистическими
                    философами, начиная с Бубера, так и экзистенциалистами, кончая Сартром) к чисто
                    формальному приписыванию «моего» мышления другому субъекту; субъекту
                    индивидуальному, то есть эмпирически выделенному члену класса или
                    неиндивидуальному, то есть классу, выделенному в качестве условного субъекта
                    не-моего, а в общем случае — любого не-данного, мышления. Такой субъект может
                    произвольно помещаться в нынешнем времени или в прошлом, в данном месте или в
                    другом. Эпистемологически в «концепции другого», именно другой, а не производное
                    от моего другое мышление является первичным, и только через него, в
                    противопоставлении ему, в паре с ним, я есть я сам, а мое мышление, уже будучи
                    приписанным ему, возвращается ко мне как мое.</p>
                <p>Применяемая мною в разговоре о буддийской философии «концепция чужести» сводится,
                    в основных чертах, к следующему. Во-первых, чужое — это объект моего мышления, а
                    не субъект другого мышления, объект, в котором мое мышление открывает какое-то
                    не-мое, то есть такое, на которое мое мышление не может перенести себя самое (не
                    может им себя себе представить, так сказать). В этом, собственно, и состоит как
                    бы «минимум» чужести и — вместе с тем — основной ее критерий. Во-вторых, переход
                    от чужого, не-моего мышления к иному, чем Я, субъекту чужого мышления возможен
                    (как вообще переход от мышления к мыслящему) только в порядке антропологической
                    гипотезы. Чужесть субъекта все равно полагается вторичной, производной от
                    чужести мышления. Тогда в принципе не существенно, будет ли мне чужим мышление
                    Будды или моего соседа, нынешних русских или древних инков. Важным здесь может
                    оказаться другое: возможность найти, «засечь» (по выражению Мераба Мамардашвили)
                    различные чужие мышления в точках локализации их субъектов во времени и
                    пространстве. В-третьих, как феномен моего мышления, чужесть может быть
                    установлена только путем рефлексии моего мышления над самим собой. То есть когда
                    мое мышление уже вынесло себя самое в качестве внешнего, не-своего объекта и тем
                    самым объективно («объективно» здесь значит буквально «как объект») уже отличило
                    себя как рефлексию от рефлексируемого им моего же мышления (как от
                    не-рефлексии); иначе говоря, уже сделало себя чужим. Только при условии
                    выполнения моим мышлением этого рефлексивного акта оно становится способным
                    мыслить о любом не-моем мышлении как о чужом. Таким образом, то, что я называю
                    «чужим», может возникнуть только в своего рода «эпистемологическом зазоре» между
                    моим объектным мышлением о другом мышлении и моей рефлексией над моим
                    собственным мышлением. Притом что второе является первичным в отношении первого.
                    Иначе говоря, феномен чужести «другого» производен от чужести как бы «себя
                    самого».</p>
                <p>В своих попытках изложения буддийской философии и моего философствования о ней я
                    специально подчеркиваю ее чужесть. Она ведь на самом деле чужая мне, вам, да
                    кому угодно, если не валять дурака. Но прежде всего она была чужой и
                    современникам Будды, древним индийцам, жившим в VI—IV веках до н. э. в долине
                    Ганга. До того чужим оказался буддизм этим древним индийцам, что так никогда и
                    не став господствующей религией в Северной Индии, он уже с III века до н. э.
                    начал перемещаться к югу, затем (если говорить о Малой Колеснице) — на Цейлон, а
                    спустя пару столетий началось его победное шествие в Китай, позднее (уже из
                    Цейлона) — в Индокитай и еще позднее — в Центральную Азию и на Дальний Восток.
                    Притом что очаги буддийской учености еще долго оставались и в Северо-Западной
                    Индии, и в Кашмире, и в Бенгалии.</p>
                <p>Теперь совсем немного о конкретной методологии моего подхода к буддийской
                    философии в этих семинарах. Здесь нам не избежать двоякости. Ведь, с одной
                    стороны, чистота методологического подхода требует, чтобы, рассуждая о любой
                    не-твоей философии (да что там философии, о любом, ставшем тебе известным
                    не-твоем мышлении), следует прежде всего исходить из того, что говорит о себе
                    сама эта философия в своих текстах, устных или письменных; исходить из того, как
                    она сама себя формулирует. Но с другой стороны, полнота методологического
                    подхода требует, чтобы в то же самое время ты и отрефлексировал свое собственное
                    мышление, свою, так сказать, интеллектуальную апперцепцию текстов изучаемой
                    философии. Начинать такое отрефлексирование приходится с тщательного учета и
                    анализа твоего же понятийного языка, которому предстоит стать языком описания
                    изучаемой тобой философии. Но этот язык описания не может по определению быть
                    только твоим — это язык твоей культуры, твоего времени, да наконец, твой
                    естественный язык (английский, русский, какой угодно), который даже в его
                    обычных культурных употреблениях остается тобою не отрефлексированным как
                    естественно данный.</p>
                <p>Теперь, если говорить о языке описания именно буддийской философии европейскими
                    исследователями XIX и XX веков, то здесь четко различаются две тенденции. Первой
                    (и по сю пору господствующей) является тенденция описывать эту философию так,
                    как ее могли бы выразить древние авторы, если бы они знали установившийся между
                    XVII и XIX веками язык новоевропейской философской традиции. Вторую и пока
                    только начинающуюся тенденцию я бы связал с попыткой исследователя буддизма
                    сначала отрефлексировать свою философскую позицию в терминах буддийской
                    философии, а затем описать последнюю как частный случай некоторой общей
                    вневременной и пространственно неопределенной философии (Эдвард Конзе называл
                    <em>еерhilosophiaреerennis</em>), в отношении которой «моя» философия будет
                    являться другим частным случаем.</p>
                <p>Это и сталкивает нас с методологической неизбежностью своей собственной философии
                    — в моем случае, моей при изучении любой другой. Лучше скажем так: изучение
                    буддийской философии будет сколько-нибудь реальным, только если у того, кто ее
                    изучает, есть его собственная философия. У кого своей философии, какой угодно,
                    нет, тот пусть займется чем угодно другим. Так, самый, пожалуй, интересный
                    исследователь буддийской философии в XX веке, Эдвард Конзе, всегда говорил о
                    себе, что он — гностик, а крупнейший знаток философии Большой Колесницы Этьен
                    Ламот был одновременно католическим теологом. Тот, кто начинает познание
                    буддийской философии со своего, так сказать, «философского нуля», обречен на
                    поражение. В самом лучшем случае он останется в рядах «обращенных»
                    энтузиастов.</p>
