                                                                             id="ref-14"
                                                                             class="citation-mark"><sup>14</sup></a>,
                        путь, что ведет к Пробуждению<a href="#citation-15" id="ref-15"
                                                        class="citation-mark"><sup>15</sup></a> и
                        Нирване<a href="#citation-16" id="ref-16"
                                  class="citation-mark"><sup>16</sup></a>. И что же это за Срединный
                        Путь<a href="#citation-17" id="ref-17"
                               class="citation-mark"><sup>17</sup></a>, о монахи? Это просто
                        Благородный<a href="#citation-18" id="ref-18"
                                      class="citation-mark"><sup>18</sup></a> Восьмеричный Срединный
                        Путь<a href="#citation-19" id="ref-19"
