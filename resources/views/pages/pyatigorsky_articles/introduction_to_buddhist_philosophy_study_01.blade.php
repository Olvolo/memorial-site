                                                                     class="citation-mark"><sup>25</sup></a>.
                    </p>

                    <p>Теперь, о монахи, Благородная Истина о Возникновении Страдания<a
                            href="#citation-26" id="ref-26" class="citation-mark"><sup>26</sup></a>.
                        Оно — в жажде, в алчбе<a href="#citation-27" id="ref-27"
                                                 class="citation-mark"><sup>27</sup></a>, которые
                        производят новое существование<a href="#citation-28" id="ref-28"
                                                         class="citation-mark"><sup>28</sup></a>,
                        связано оно с наслаждением и страстью<a href="#citation-29" id="ref-29"
                                                                class="citation-mark"><sup>29</sup></a>.
                        Алчба всегда находит себе удовлетворение то здесь, то там. Это — жажда
                        наслаждения, жажда существования<a href="#citation-30" id="ref-30"
                                                           class="citation-mark"><sup>30</sup></a> и
                        жажда несуществования, самоизбывания<a href="#citation-31" id="ref-31"
                                                               class="citation-mark"><sup>31</sup></a>.
                    </p>

                    <p>Еще, о монахи, вот Благородная Истина о Прекращении Страдания<a
                            href="#citation-32" id="ref-32" class="citation-mark"><sup>32</sup></a>.
                        Оно — в полном бесстрастном<a href="#citation-33" id="ref-33"
                                                      class="citation-mark"><sup>33</sup></a>
                        устранении этой алчбы, в ее окончательном отбрасывании, в отстранении от
                        нее, в избавлении себя от нее.</p>

                    <p>Наконец, о монахи, Истина о пути, ведущем к прекращению страдания. Это и есть
                        Благородный Срединный Восьмеричный Путь, а именно — правильное видение,
                        правильное намерение, правильная речь, правильное действие, правильный образ
                        жизни, правильное усилие, правильное обратное вспоминание и правильное
                        сосредоточение сознания</p>

                    <p>Итак, о монахи, при мысли «вот Благородная Истина о Страдании» появляется во
                        мне видение, появляется знание, появляется мудрость<a href="#citation-34"
                                                                              id="ref-34"
                                                                              class="citation-mark"><sup>34</sup></a>,
                        свет появляется во мне — в отношении вещей доселе неслыханных. И мысль, что
                        страдание должно быть полностью, совершенно постигнуто.</p>

                    <p>И далее, о монахи, при мысли «вот Благородная Истина о Возникновении
                        Страдания» появляется во мне видение, появляется знание, появляется
                        мудрость, появляется свет — в отношении вещей доселе неслыханных. И мысль,
                        что возникновение страдания должно быть покинуто.</p>

                    <p>Затем, о монахи, при мысли «вот Благородная Истина о Прекращении Страдания»
                        появляется во мне видение, появляется знание, появляется мудрость,
                        появляется свет — в отношении вещей доселе неслыханных. И это прекращение
                        страдание уже постигнуто.</p>

                    <p>И наконец, о монахи, при мысли «вот Благородная Истина о Пути, ведущем к
                        Прекращению Страдания» появляется во мне видение, появляется знание,
                        появляется мудрость, появляется свет — в отношении вещей доселе неслыханных.
                        И этот Путь должен быть тщательно проработан в созерцании<a
                            href="#citation-35" id="ref-35" class="citation-mark"><sup>35</sup></a>.
                    </p>

                    <p>Пока, о монахи, это знание Четырех Благородных Истин мне не стало совершенно
                        ясно в трех его аспектах<a href="#citation-36" id="ref-36"
                                                   class="citation-mark"><sup>36</sup></a>, я не
                        притязал на достижение Совершенного Пробуждения, что есть наивысшее в этом
                        мире<a href="#citation-37" id="ref-37"
                               class="citation-mark"><sup>37</sup></a> с его Марами<a
                            href="#citation-38" id="ref-38" class="citation-mark"><sup>38</sup></a>,
                        Брахмами<a href="#citation-39" id="ref-39"
                                   class="citation-mark"><sup>39</sup></a>, с его странствующими
                        аскетами<a href="#citation-40" id="ref-40"
                                   class="citation-mark"><sup>40</sup></a>, брахманами<a
