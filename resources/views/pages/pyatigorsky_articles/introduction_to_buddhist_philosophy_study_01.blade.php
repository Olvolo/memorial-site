                </p>
                <h3 class="text-center text-xl font-semibold text-zinc-700 mb-4">
                    Дхаммачаккаппаваттана-сутта (Сутта о повороте Колеса Дхаммы)</h3>

                <p>
                    Так я слышал. Однажды Благословенный пребывал в Оленьем парке, в Исипатане
                    («Прибежище Риши»), что близ Бенареса. Там он обратился к группе из пяти
                    монахов:
                </p>
                <p>
                    «Двух крайностей следует избегать тому, кто, став отшельником, покинул свой дом.
                    Каковы эти две крайности?
                </p>
                <p>
                    Одна – это приверженность плотским удовольствиям. Это низменно, грубо, мирско,
                    неблагородно и бесполезно.
                </p>
                <p>
                    Вторая – это приверженность умерщвлению плоти и самоистязанию. Это болезненно,
                    неблагородно и бесполезно.
                </p>
                <p>
                    Избегнув обеих этих крайностей, Татхагата постиг Срединный Путь, дарующий
                    видение, дарующий знание, ведущий к умиротворению, к высшему постижению, к
                    пробуждению, к Ниббане.
                </p>
                <p>
                    И что же это за Срединный Путь, о монахи, который постиг Татхагата, дарующий
                    видение, дарующий знание, ведущий к умиротворению, к высшему постижению, к
                    пробуждению, к Ниббане? Это есть Благородный Восьмеричный Путь, а именно:
                </p>
                <ul class="list-disc pl-5 space-y-1 ml-8 my-2">
                    <li class="my-2">Правильное воззрение (самма диттхи): Понимание Четырех
                        Благородных Истин.
                    </li>
                    <li class="my-2">Правильное намерение (самма санкпа): Намерение отказаться от
                        злобы, жестокости и похоти.
                    </li>
                    <li class="my-2">Правильная речь (самма вача): Отказ от лжи, клеветы, грубой
                        речи и пустословия.
                    </li>
                    <li class="my-2">Правильное действие (самма камманта): Отказ от убийства,
                        воровства и неверности.
                    </li>
                    <li class="my-2">Правильный образ жизни (самма аджива): Зарабатывание на жизнь
                        честным способом, не причиняя вреда другим.
                    </li>
                    <li class="my-2">Правильное усилие (самма ваяма): Усилие предотвращать
                        возникновение злых, неблагих состояний и развивать благие.
                    </li>
                    <li class="my-2">Правильное памятование (самма сати): Осознанность тела, чувств,
                        ума и феноменов.
                    </li>
                    <li class="my-2">Правильное сосредоточение (самма самадхи): Развитие
                        концентрации через медитацию.
                    </li>
                </ul>
                <p>
                    И этот путь ведет к умиротворению, к высшему постижению, к пробуждению и к
                    Ниббане.
                </p>
                <p>
                    Вот, о монахи, Благородная Истина о Страдании (Дукха): Рождение – это страдание;
                    старость – страдание; болезнь – страдание; смерть – страдание; печаль, стенание,
                    боль, горе и отчаяние – страдание. Соединение с неприятным – страдание, разлука
                    с приятным – страдание, неполучение желаемого – страдание. Вкратце, пять
                    совокупностей привязанности – это страдание.
                </p>
                <p>
                    Вот, о монахи, Благородная Истина о Причине Страдания (Самудая): Это жажда
                    (танха), которая порождает новое бытие, сопровождается наслаждением и страстью,
                    находя наслаждение то здесь, то там, а именно: жажда чувственных удовольствий,
                    жажда существования, жажда несуществования.
                </p>
                <p>
                    Вот, о монахи, Благородная Истина о Прекращении Страдания (Ниродха): Это полное
                    угасание и прекращение этой самой жажды, отказ от нее, отказ от привязанности к
                    ней, освобождение от нее, отсутствие склонности к ней.
                </p>
                <p>
                    Вот, о монахи, Благородная Истина о Пути, Ведущем к Прекращению Страдания
                    (Магга): Это есть Благородный Восьмеричный Путь, а именно: правильное воззрение,
                    правильное намерение, правильная речь, правильное действие, правильный образ
                    жизни, правильное усилие, правильное памятование и правильное сосредоточение.
                </p>
                <p>
                    «Видение, о монахи, возникло у меня относительно этих Четырех Благородных Истин,
                    которых я не слышал ранее; знание возникло; мудрость возникла; свет возник.
                </p>
                <p>‘Это есть страдание’ – так у меня, о монахи, возникло видение, знание, мудрость,
                    свет относительно вещей, которых я не слышал ранее.
                </p>
                <p>
                    ‘Это страдание должно быть постигнуто’ – так у меня возникло видение, знание,
                    мудрость, свет относительно вещей, которых я не слышал ранее.
                </p>
                <p>
                    ‘Это страдание было постигнуто’ – так у меня возникло видение, знание, мудрость,
                    свет относительно вещей, которых я не слышал ранее.
                </p>
                <p>
                    ‘Это есть причина страдания’ – так у меня, о монахи, возникло видение, знание,
                    мудрость, свет относительно вещей, которых я не слышал ранее.
                </p>
                <p>
                    ‘Эта причина страдания должна быть оставлена’ – так у меня возникло видение,
                    знание, мудрость, свет относительно вещей, которых я не слышал ранее.
                </p>
                <p>
                    ‘Эта причина страдания была оставлена’ – так у меня возникло видение, знание,
                    мудрость, свет относительно вещей, которых я не слышал ранее.
                </p>
                <p>
                    ‘Это есть прекращение страдания’ – так у меня, о монахи, возникло видение,
                    знание, мудрость, свет относительно вещей, которых я не слышал ранее.
                </p>
                <p>
                    ‘Это прекращение страдания должно быть реализовано’ – так у меня возникло
                    видение, знание, мудрость, свет относительно вещей, которых я не слышал ранее.
                </p>
                <p>
                    ‘Это прекращение страдания было реализовано’ – так у меня возникло видение,
