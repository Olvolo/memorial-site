                <h3 class="text-xl text-center font-semibold text-zinc-700 mb-4">Предисловие</h3>
                <p>Заглавие этой книги следует понимать буквально. Во-первых, книга представляет
                    собой введение в изучение именно и только философии буддизма, оставляя по
                    большей части в стороне буддизм как религию (и как случай общего мировоззрения,
                    культуры, искусства). И конечно, эта книга ни в коем случае не претендует на
                    роль введения в историю буддийской философии. В ней философия, представленная
                    каноническими и неканоническими текстами, дается в разрезах, каждый из которых
                    являет синхронную картину состояния буддийского философского мышления, а все
                    они, вместе взятые, составляют (опять же синхронную) картину общего состояния
                    буддийской философии в целом — как она может представляться философскому
                    мышлению сегодняшнего дня.</p>
                <p>Второе. Эта книга есть именно введение в изучение буддийской философии, а не
                    систематическое изложение основных ее положений. Иначе говоря, перед читателем
                    не очерк самой системы буддийской философии, а пособие по предуготовительному
                    ознакомлению с этой философией, предназначенное тому, кто пожелает ею
                    сколько-нибудь серьезно заняться. И конечно, эта книга не является
                    систематическим изложением духовной практики буддизма, буддийской йоги.</p>
                <p>Третье. Каждая глава книги состоит из отрывка буддийского текста, примечаний (в
                    основном терминологического, но иногда и общего содержательного характера) и
                    собственно авторского рассуждения относительно философского содержания
                    приведенного отрывка. Получается, что первая часть каждой главы содержит версию
                    одного из основных постулатов буддийской философии и некоторые выводы, сделанные
                    тогда из этого постулата. Во второй части читатель найдет объяснение философских
                    (а иногда и религиозных) понятий и терминов, употребляемых в формулировании этих
                    постулатов и выводов. Третья часть представляет собой нынешний опыт философского
                    осмысления того, что представлено в первой и второй частях, — разумеется,
                    авторского осмысления.</p>
                <p>Еще одно замечание — на этот раз историко-философского характера. Необычность для
                    нас буддийской философии и несомненная новизна для современных ей индийских
                    философов (V—III вв. до н. э.) определяется следующим обстоятельством. С одной
                    стороны, первые буддийские мыслители выдвинули несколько идей и концепций,
                    которые сложно или невозможно представить в традиционной брахманистской
                    философии. С другой стороны, первые буддийские философы включили в свое
                    философствование ряд традиционных брахманистских идей и концепций, исключив эти
                    идеи и концепции из их привычных связей, контекстов. Я думаю, это обстоятельство
                    послужило основной причиной того, что буддизм воспринимался тогда в Индии как
                    нечто новое, даже принципиально чуждое и враждебно-странное.</p>

                <p>Эта книга есть краткое изложение 19 семинаров по буддийской философии,
