            <section>
                <h3 class="text-center text-xl font-semibold text-zinc-700 mb-4">О ЧЁМ ЭТО ВСЁ?</h3>
                <p>Начнем с самого начала. Вначале Будда обратился со своими словами к пятерым
                    поименованным отшельникам. Заметьте, не к миру, не к человечеству, даже не к
                    отшельникам вообще, а только к тем пятерым, которых он хорошо знал из прежнего
                    периода своей жизни, проведенного среди аскетов и лесных учителей. Именно им
                    было предназначено стать его первыми слушателями, а затем и учениками. Момент
                    выделенности, особости, даже избранности здесь очень важен. Ведь в первую
                    очередь Будда высказывает идею срединности, срединности в действиях, словах и
                    мыслях, чувствах и намерениях, срединности, которая чужда человеческой природе,
                    человеческой нормальности появляется весьма редким качеством даже у аскетов.
                    Отсюда и вторая идея, идея о Благородном, как о том, что противопоставлено всему
                    нормальному, обыденному, то есть отмеченному крайностью и оттого банальному и
                    вульгарному. Получается, что Благородные личности — это исключение не только из
                    людей, но и из отшельников, что они как бы исключение из исключения. Таким
                    образом уже в самом начале первой проповеди вводятся три понятия: крайность,
                    срединность и благородность.</p>
                <p>Крайность здесь — не только качественная характеристика, но прежде всего
                    самостоятельное качество. Так, крайняя чувственность оказывается в большей
                    степени противопоставлена умеренной чувственности, чем полному отсутствию
                    чувственности. Более того, умеренная чувственность сильнее отличается от полного
                    аскетического воздержания от чувственных желаний, чем, скажем, последнее — от
                    неумеренной чувственности. Иначе говоря, здесь различие в степени проявления
                    условно-противоположных качеств (таких как «привлекательность» и
                    «отвратительность», или «любовь» и «отвращение») оказывается более значимым, чем
                    их качественное содержание. Срединность в буддийской философии как бы
                    нейтрализует обычные противоположности вещей и качеств, и Срединный Путь
                    является, по существу, путем (и методом!) такого рода нейтрализации. Идея
                    срединности в дальнейшем (то есть в гораздо более поздних философских текстах)
                    подвергнется весьма сложным интерпретациям и станет одним из наиболее
                    абстрактных философских понятий буддизма. Но и здесь дело обстоит далеко не так
                    просто, ибо уже в первой проповеди Будды срединность приобретает черты
                    радикальной философской позиции, которая не только нейтрализует
                    противоположности, но и релятивизирует (а иногда и совсем лишает смысла) само
                    существование противоположных качеств и феноменов, которым эти качества
                    приписываются.</p>
                <p>Но вернемся к основному вопросу этого семинара: «О чем все это?». Разумеется, о
                    страдании. Однако уже в самом начале это никак не «разумеется». Самое интересное
                    в Первой проповеди — это то, что страдание не постулируется онтологически, как
                    то, что <span class="underline">есть</span>. Ибо здесь нет <em>бытия</em>, как нет и <em>не-бытия</em>. Далее, страдание также не
                    является ни «вещью», ни «состоянием» какой-либо вещи, включая вселенную в целом.
                    Страдание ближе всего к тому, что мы бы назвали <em>универсальным фактом</em>, неким
                    мыслимым и наблюдаемым во всем, что существует. Но мыслимым и наблюдаемым только
                    <span class="underline">с позиции внешней всему тому, что страдает</span>, внешней всему существующему, то есть
                    с точки зрения позиции Благородной Срединности.</p>
                <p>Теперь переходим к двум «универсальным состояниям». Все вещи, факты и события
                    вселенной, иначе говоря, все <em>дхармы</em>, находятся в одном из двух состояний: в
                    состоянии «возникновения» или в состоянии «прекращения» (пали и санскр. <em>nirodh</em>a),
                    точнее, в состоянии «прекращения (остановки) возникновения». Но что такое
                    страдание? Риторическим, не по содержанию, ответом будет: то, что утверждается
                    как универсальный факт Первой Благородной Истиной. Тогда <span class="underline">что</span> страдает? Ответ на
                    этот вопрос — вечный камень преткновения для буддийских философов с третьего
                    века до нашей эры до двадцать первого века нашей. Потому что страдание, как оно
                    описано в качестве универсального факта в Первой проповеди Будды, по содержанию
                    является <span class="underline">страданием существующего, понимаемого как то, что осознает и то, что
                    осознается</span>. Тогда, грубо говоря, <span class="underline">сознание есть страдание</span>. Или — страдание есть
                    сознание, что, безусловно, звучало бы философски более заманчиво. Но — чтобы
                    хоть немного сдвинуться с мертвой точки, на которой невозможно конкретно
                    ответить на наш второй вопрос — отойдем в сторону и попробуем взглянуть на
                    ситуацию с точки зрения <span class="underline">знания</span>. Тогда становится возможным говорить о «двух
                    страданиях»: одно — это страдание всех тех, кто страдает, <span class="underline">не зная</span> Благородной
                    Истины о Страдании, и другое — страдание <span class="underline">знающих</span> Благородную Истину о Страдании
                    и тем самым уже вступивших на путь «прекращения возникновения страдания» (пали
                    <em>dukhasamudaya nirodha magga</em>). На основании такого различия возможно было бы
                    предположение о двух разных страданиях — эмпирическом и теоретическом. Ведь
                    сколь парадоксально ни звучит буддийское <span class="underline">философское</span> утверждение о том, что «те,
                    кто <span class="underline">знает</span> страдание, не страдают, ибо они прежде всего знают, что нет ни
                    страдающего, ни даже знающего, и что это пять агрегатов, <em>скандх</em> индивидуального
                    существования страдают, и ничто иное», — его невозможно с уверенностью
                    опровергнуть, сославшись на то, что эти агрегаты, <em>скандхи</em>, не могут <span class="underline">знать</span>
                    страдание (как, впрочем, не могут его и <span class="underline">не знать</span>).</p>
                <p>Вновь и вновь в ходе буддийского философствования вопрос о страдании упирается в
                    вопрос отношения страдания к знанию. Эмпирицистские суждения, такие как «я знаю,
                    что я страдаю, потому, что я страдаю», будут столь же неубедительны, как и
                    теоретические, типа «я страдаю потому, что я знаю, что страдаю». Ибо страдание
                    может быть познано только высшим, трансцендентальным Знанием (концепция
                    трансцендентального знающего будет сформулирована в семинаре о «Запредельном
                    Знании»). Первая Проповедь является первым — не хронологически, а условно, в
                    порядке, установленном ранними комментаторами сутр Палийского Канона, —
                    изложением <em>Дхармы</em>, Учения Будды; первым, потому что в нем речь идет об
                    универсальном факте Страдания и двух универсальных состояниях, которые вводятся
                    прежде всего в их отношении к Страданию. Более того, само понятие «истины»
                    фигурирует только в отношении страдания, в его состояниях возникновения и
                    прекращения. Поэтому не стоит удивляться тому, что вне фокуса рассмотрения в
                    четырех Истинах оказывается то, что страдает. Это эпистемологически следует из
                    центральной идеи буддийской философии о невозможности постулирования никакого
                    автономного бытия чего бы то ни было, включая и страдание. Ведь последнее
                    относится к <span class="underline">существующему</span>, а не к сущему. Сущего здесь не имеется.</p>
                <p>Закончим первый семинар словами о том, что мир в нем предстоит нам лишенным
                    какого бы то ни было основания, не имеющим какой-либо оси и не подчиняющимся
                    какому-либо личному или не-личному принципу. Даже страдание является тем, что
                    обусловлено факторами его возникновения и прекращения. Буддийский мир очень
                    слабо внутренне дифференцировал и нечетко и разнообразно структурирован. К этому
                    можно было бы добавить, что Путь чисто философски представляется (в рамках этого
                    семинара) единственным возможным фактором, организующим и структурирующим
                    вселенную.</p>
