                    «тот, кто пришёл к истине» (<em>tatha-āgata</em>); 3) «тот, кто ушёл в нирвану»
                    (<em>tatha-gata</em>)). В то же время «Так Ушедший» (пали и санскр. <em>tathāgata</em>) может иметь смысл определённости одного пути Будды (включая, возможно, и направление этого пути после завершения им существования в данном рождении), что подразумевает абсолютный характер всего, что Будда думает, говорит и делает.
                    Тогда «так» (пали и санскр. <em>tathā</em>) будет определением этой абсолютности, а «идущий» (или «ушедший») (пали и санскр. <em>gata</em>) обозначает определённость направления. В этой связи интересно сопоставить последний термин со словом, обозначающим
                    «назначение» или «судьбу» (пали <em>gati</em>, санскр. <em>gati</em>).<a href="#ref-11" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-12"><sup>12</sup> «Путь видения» — в оригинале
                    используется понятие «глаз» (пали <em>cakkhu</em>, санскр. <em>cakṣus</em>), означающее не
                    физическое зрение, а способность духовного прозрения. В буддизме различают: 1)
                    физический глаз (<em>maṃsacakkhu</em>), 2) божественный глаз (<em>dibbacakkhu</em>), 3) глаз
                    мудрости (<em>paññācakkhu</em>).<a href="#ref-12" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-13"><sup>13</sup> «Знание» (пали <em>ñāṇa</em>, санскр. <em>jñāna</em>) —
                    в буддизме различают: 1) мирское знание (<em>lokiya ñāṇa</em>), 2) сверхмирское знание
                    (<em>lokuttara ñāṇa</em>), 3) знание пробуждения (<em>sambodhi ñāṇa</em>). В данной сутре термин
                    употреблён в самом широком смысле.<a href="#ref-13" class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-14"><sup>14</sup> «Особое знание» — точнее «высшее
                    знание» (пали <em>abhiññā</em>, санскр. <em>abhijñā</em>), включающее шесть видов
                    сверхспособностей: 1) магические силы, 2) божественный слух, 3) чтение мыслей,
                    4) воспоминание прошлых жизней, 5) божественное зрение, 6) знание уничтожения
                    загрязнений.<a href="#ref-14" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-15"><sup>15</sup> «Пробуждение» (пали <em></em><em></em>, санскр. <em>sambodhi</em>) — более точный перевод, чем «просветление», так как передаёт идею
                    «пробуждения от сна неведения». Термин подчёркивает активный аспект реализации
                    Будды, который уже достиг полного пробуждения до этой проповеди.<a
                        href="#ref-15" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-16"><sup>16</sup> <em>Нирван</em>а (пали <em>nibbāna</em>, санскр. <em>nirvāṇa</em>) — конечная цель буддийской практики, состояние полного освобождения от
                    страданий и круговорота перерождений. Будда уже достиг этого состояния к моменту
                    первой проповеди, но продолжал земное существование ради блага существ.<a
                        href="#ref-16" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-17"><sup>17</sup> «Путь» (пали <em>paṭipadā</em>, санскр. <em>pratipad</em>) — конкретная практика или способ продвижения к освобождению. В данном
                    контексте означает уникальный путь, открытый и реализованный Буддой.<a
                        href="#ref-17" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-18"><sup>18</sup> «Благородный» (пали <em>ariya</em>, санскр. <em>ārya</em>) — первоначально означало представителей высших варн в древней Индии, в
                    буддизме приобрело значение духовной элиты, достигшей определенных ступеней
                    реализации (четыре уровня благородных личностей).<a href="#ref-18" class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-19"><sup>19</sup> В буддийских текстах используются два
                    основных термина для «Пути»: 1) пали <em>magga</em> (санскр. <em>mārga</em>) — путь как метод
                    практики (Восьмеричный путь); 2) пали <em>paṭha</em> (санскр. <em>patha</em>) — путь как
                    направление или дорога. Хотя эти термины иногда взаимозаменяемы, первый чаще
                    относится к духовной практике.<a href="#ref-19" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-20"><sup>20</sup> «Правильный» (пали <em>sammā</em>, санскр. <em>samyak</em>) в буддийском контексте имеет два основных аспекта: 1) качественный —
                    «совершенный», «полноценный», «точный» (например, правильное воззрение); 2)
                    онтологический — «соответствующий реальности», «истинный», «неиллюзорный». Оба
                    аспекта важны в понимании Восьмеричного пути.<a href="#ref-20"
                                                                    class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-21"><sup>21</sup> «Памятование» (пали <em>sati</em>, санскр. <em>smṛti</em>) — ключевой элемент буддийской практики. Можно даже говорить об «обратном
                    вспоминании» как о <span class="underline">сознательном</span> процессе обращения памяти от настоящего момента
                    в прошлое. Часто переводится как «осознанность». Включает четыре основы: 1) осознание тела, 2) осознание чувств,
                    3) осознание ума, 4) осознание дхарм. Концепция «обратного вспоминания»
                    относится к практике ретроспективного анализа прошлых действий.<a href="#ref-21" class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-22"><sup>22</sup> «Сосредоточение» (пали <em>samādhi</em>,
                    санскр. <em>samādhi</em>) — состояние глубокой медитативной поглощенности. В буддизме
                    различают: 1) мгновенное сосредоточение (<em>khaṇika-samādhi</em>), 2) предварительное
                    сосредоточение (<em>upacāra-samādhi</em>), 3) полное сосредоточение (<em>appanā-samādhi</em>).
                    Является восьмым элементом Благородного Восьмеричного Пути.<a href="#ref-22"
                                                                                  class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-23"><sup>23</sup> «Истина» (пали <em>sacca</em>, санскр. <em>satya</em>)
                    — в буддийском контексте относится к Четырем Благородным Истинам: 1) истина о
                    страдании, 2) истина о причине страдания, 3) истина о прекращении страдания, 4)
                    истина о пути. Эти истины составляют суть первого поворота Колеса Дхармы.<a
                        href="#ref-23" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-24"><sup>24</sup> «Страдание» (пали <em>dukkha</em>, санскр. <em>duḥkha</em>) — в буддизме понимается на трех уровнях: 1) обычное страдание
                    (<em>dukkha-dukkha</em>), 2) страдание от изменений (<em>vipariṇāma-dukkha</em>), 3) страдание
                    обусловленного существования (<em>saṅkhāra-dukkha</em>). В данной сутре термин охватывает
                    все эти аспекты.<a href="#ref-24" class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-25"><sup>25</sup> «Пять скандх» (пали <em>khandha</em>, санскр. <em>skandha</em>) — буквально «группы», «совокупности», «куча», «груда» (подобно
                    муравейнику, издалека кажущемуся единым объектом, но при ближайшем рассмотрении
                    состоящему из разрозненных веточек, песчинок и прочего) — компоненты,
                    составляющие индивидуальное существование. Эти агрегаты создают иллюзию целостного «Я» и включают:
                <ul class="list-disc pl-5 space-y-1 ml-8 my-2">
                    <li>«Форму» (пали, санскр. <em>rūpa</em>) — телесная составляющая,</li>
                    <li>«Чувства» (пали, санскр. <em>vedanā</em>) — приятный, неприятный и нейтральный опыт,
                    </li>
                    <li>«Восприятие» (пали <em>saññā</em>, санскр. <em>saṃjñā</em>) — категоризация ощущений,</li>
                    <li>«Формирующие факторы» (пали <em>saṅkhāra</em>, санскр. <em>saṃskāra</em>) — волевые импульсы и
                        кармические образования,
                    </li>
                    <li>«Сознание» (пали <em>viññāṇa</em>, санскр. <em>vijñāna</em>) — базовое осознавание.</li>
                </ul>
                <p>Эти совокупности называются «агрегатами привязанности» (<em>upādānakkhandha</em>), так как
                    служат объектами цепляния, и «агрегатами страдания», поскольку их непостоянная
                    природа ведёт к дуккхе.<a href="#ref-25" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-26"><sup>26</sup> «Благородная Истина о Возникновении
                    Страдания» (пали <em>samudaya</em>, санскр. <em>samudaya</em>) точнее переводится как «источник»
                    или «совозникновение», подчёркивая зависимую природу страдания. Возможен также
                    перевод как «эмергенция» — в этом случае акцент делается на прерывистости (а
                    иногда и случайности) проявления феномена, его «всплытии» в поле сознания.
                    Термин указывает на процессуальность: страдание не существует само по себе, а
                    возникает только при наличии соответствующих условий (подобно тому как огонь
                    зависит от топлива).<a href="#ref-26" class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-27"><sup>27</sup> «Жажда» или «алчба (алчность)» (пали
                    <em>taṇhā</em>, санскр. <em>tṛṣṇā</em>) — ключевое понятие буддийской психологии. Оно обобщает и
                    описывает всю эмоционально-мотивационную сферу в контексте обусловленного
                    существования, охватывая три основных формы:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8 my-2">
                    <li>Жажда чувственных удовольствий (<em><em>kāma-taṇhā</em></em>)</li>
                    <li>Жажда бытия (<em><em>bhava-taṇhā</em></em>)</li>
                    <li>Жажда небытия или уничтожения (<em>vibhava-taṇhā</em>)</li>
                </ul>
                <p>Это не просто эмоция, а глубинный механизм привязанности к сансаре — цепляние,
                    поддерживающее круговорот рождения и смерти.<a href="#ref-27" class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-28">
                    <sup>28</sup> «производят новое существование» — здесь точнее сказать: «ведут к
                    новому существованию» (пали <em>ponobhavikā</em>), поскольку термин указывает на
                    кармический процесс: жажда создаёт условия для будущих рождений через:
                </p>
                <ul class="list-disc pl-5 space-y-1 ml-8 my-2">
                    <li class="my-2">волевые формации (<em>saṅkhāra</em>),</li>
                    <li class="my-2">цепляние (<em>upādāna</em>).</li>
                </ul>
                <p class="mb-3">
                    Это не механическое «производство», а сложная причинно-обусловленная цепь.<a
                        href="#ref-28" class="text-blue-500 hover:underline">↩</a>
                </p>


                <p class="mb-3" id="citation-29">
                    <sup>29</sup> «Страсть» (пали <em>rāga</em>, санскр. <em>rāga</em>) — активная и в
                    определённом смысле психически позитивная сила. Её можно рассматривать как
                    конкретизацию чувственного удовольствия и чувственности. Однако в буддизме она
                    относится к числу «трёх ядов» наряду с гневом и неведением.
                    Хотя в мирском контексте страсть может казаться позитивной, в духовной практике
                    она выступает как:
                </p>
                <ul class="list-disc pl-5 space-y-1 ml-8 my-2">
                    <li class="my-2">помеха для сосредоточения (<em>nīvaraṇa</em>),</li>
                    <li class="my-2">фактор привязанности (<em>upādāna</em>),</li>
                    <li class="my-2">одна из десяти оков (<em>saṃyojana</em>).</li>
                </ul>
                <p class="mb-3">
                    <a href="#ref-29" class="text-blue-500 hover:underline">↩</a>
                </p>


                <p class="mb-3" id="citation-30">
                    <sup>30</sup> «Жажда существования», также переводимая как «жажда становления»
                    или «жажда бытия» (пали и санскр. <em>bhava-taṇhā</em>) — это стремление к
                    продолжению эго-идентичности. Она может проявляться в следующих формах:
                </p>
                <ul class="list-disc pl-5 space-y-1 ml-8 my-2">
                    <li class="my-2">стремление к вечной жизни (в том числе — в раю),</li>
                    <li class="my-2">привязанность к духовным достижениям как к «моим»,</li>
                    <li class="my-2">тонкая форма цепляния за представление о «Я».</li>
                </ul>
                <p class="mb-3">
                    Эта форма жажды считается наиболее трудной для преодоления.<a href="#ref-30"
                                                                                  class="text-blue-500 hover:underline">↩</a>
                </p>


                <p class="mb-3" id="citation-31">
                    <sup>31</sup> «Жажда небытия» (пали <em>vibhava-taṇhā</em>, санскр. <em>vibhava-tṛṣṇā</em>)
                    — один из трёх видов жажды, проявляющийся в следующих формах:
                </p>
                <ul class="list-disc pl-5 space-y-1 ml-8 my-2">
                    <li class="my-2">стремление к самоуничтожению,</li>
                    <li class="my-2">желание «исчезнуть» после смерти,</li>
                    <li class="my-2">отрицание духовных принципов (например, кармы).</li>
                </ul>
                <p class="mb-3">
                    Её крайняя форма — материалистический нигилизм, то есть учение о полном
                    прекращении сознания и существования после смерти.<a href="#ref-31"
                                                                         class="text-blue-500 hover:underline">↩</a>
                </p>


                <p class="mb-3" id="citation-32">
                    <sup>32</sup> «Прекращение» (пали и санскр. <em>nirodha</em>) — ключевое понятие
                    буддийской психологии, особенно в контексте Третьей Благородной Истины. Оно
                    имеет несколько значений:
                </p>
                <ul class="list-disc pl-5 space-y-1 ml-8 my-2">
                    <li class="my-2">
                        <strong>Пассивное прекращение</strong> — естественное угасание феномена, который
                        прекращается сам по себе (например, ощущение или эмоция).
                    </li>
                    <li class="my-2">
                        <strong>Активное прекращение</strong> — прекращение, достигаемое практикой, когда одно
                        состояние сознания подавляет или устраняет другое (напр., в <em>ниродха-самапатти</em>
                        — "созерцании прекращения").
                    </li>
                    <li class="my-2">
                        <strong>Абсолютное прекращение</strong> — синоним ниббаны (нирваны), полного угасания
                        жажды, невежества и страдания.
                    </li>
                </ul>
                <p class="mb-3">
                    Также возможен философский перевод <em>nirodha</em> как «невозникновение»,
                    подчёркивающий отсутствие повторного возникновения при устранении причин. Третий
                    аспект — центральный в Третьей Благородной Истине.<a href="#ref-32"
                                                                         class="text-blue-500 hover:underline">↩</a>
                </p>


                <p class="mb-3" id="citation-33">
                    <sup>33</sup> «Бесстрастность» (пали <em>virāga</em>, санскр. <em>vairāgya</em>) —
                    это не просто отсутствие страсти, а позитивное духовное состояние, включающее:
                </p>
                <ul class="list-disc pl-5 space-y-1 ml-8 my-2">
                    <li class="my-2">свободу от привязанностей и зависимости,</li>
                    <li class="my-2">результат глубокого видения непостоянства (<em>аничча</em>),</li>
                    <li class="my-2">одно из десяти совершенств (<em>парами</em>) в традиции
                        Тхеравады.
                    </li>
                </ul>
                <p class="mb-3">
                    В Абхидхамме бесстрастность трактуется как «прохладность» ума — внутреннее
                    охлаждение, свидетельствующее о прекращении возбуждения, страсти и цепляния.<a
                        href="#ref-33" class="text-blue-500 hover:underline">↩</a>
                </p>


                <p class="mb-3" id="citation-34">
                    <sup>34</sup> «Мудрость» (пали <em>paññā</em>, санскр. <em>prajñā</em>) — высшее
                    знание, один из ключевых факторов освобождения. В классической буддийской
                    традиции различаются три вида мудрости:
                </p>
                <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
                    <li class="my-2">Мудрость, возникающая от слушания Дхаммы (<em>sutamayā paññā</em>).
                    </li>
                    <li class="my-2">Мудрость, порождаемая размышлением (<em>cintāmayā paññā</em>).
                    </li>
                    <li class="my-2">Мудрость, рождаемая через медитацию (<em>bhāvanāmayā paññā</em>).
                    </li>
                </ol>
                <p class="mb-3">
                    В Махаяне мудрость — это одна из совершенств (<em>праджняпарамита</em>) и
                    важнейший компонент пути бодхисаттвы.<a href="#ref-34"
                                                            class="text-blue-500 hover:underline">↩</a>
                </p>


                <p class="mb-3" id="citation-35">
                    <sup>35</sup> «Культивированный», «развитый» (пали <em>bhāvita</em>) — технический
                    термин буддийской медитации, означающий качество ума, сознательно
                    сформированное, развитое
                    в результате систематическойпрактики (<em>bhāvanā</em>). Термин противоположен
                    спонтанным или
                    природным состояниям и применяется, в частности, к:
                </p>
                <ul class="list-disc pl-5 space-y-1 ml-8 my-2">
                    <li class="my-2">сосредоточению (<em>samādhi</em>),</li>
                    <li class="my-2">памятованию (<em>sati</em>),</li>
                    <li class="my-2">другим факторам пути (например, усилию, мудрости)</li>
                </ul>
                <p class="mb-3">
                    Таким образом, <em>bhāvita</em> указывает не на случайное качество, а на
                    устойчивый, «культивированный» результат сознательной йогической практики.<a
                        href="#ref-35" class="text-blue-500 hover:underline">↩</a>
                </p>


                <p class="mb-3" id="citation-36"><sup>36</sup> «Три аспекта знания» (пали <em>tisso vijjā</em>, санскр. <em>trīḥ vidyāḥ</em>) в буддийской гносеологии представляют собой ключевые
                    этапы духовного развития:</p>
                <ol>
                    <li><strong>Знание «как оно есть»</strong> (пали <em>yathābhūtaṃ</em>, санскр. <em>yathābhūtam</em>), в каковом случае <span class="underline">знание и есть истина</span> —
                        прямое постижение Четырёх Благородных Истин через медитацию и правильное видение.</li>
                    <li><strong>Знание о необходимых действиях</strong>, знание о том, что <span class="underline">должно быть сделан</span>о для реализации истины — понимание и применение Восьмеричного
                        пути для достижения освобождения.</li>
                    <li><strong>Знание о завершённости</strong> (пали <em>kataṃ karaṇīyaṃ</em>), знание о том, что <span class="underline">уже сделано</span> для реализации истины — осознание того, что
                        все необходимые усилия завершены и достигнута конечная цель (нирвана)
                        («сделано то, что нужно было сделать»).</li>
                </ol>
                <p>Эти три аспекта соответствуют трём фазам духовного пути: пониманию, практике и
                    плодам.<a href="#ref-36" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-37"><sup>37</sup> «что есть наивысшего в этом мире»:<a
                        href="#ref-37" class="text-blue-500 hover:underline">↩</a></p>
                <p>
                    «Этот мир» (пали, санскр. <em>loka</em>) в буддизме подразумевает:
                </p>
                <ul class="list-disc pl-5 space-y-1 ml-8 my-2">
                    <li>Чувственный мир (<em>kāma-loka</em>).</li>
                    <li>Мир форм (<em>rūpa-loka</em>).</li>
                    <li>Мир не-форм (<em>arūpa-loka</em>).</li>
                </ul>
                <p>
                    В отличие от брахманизма, где «мир» часто понимается как сотворённый конкретным
                    богом, буддизм рассматривает его как проекцию коллективной кармы и отрицает
                    концепцию единого творца
                </p>

                <p class="mb-3" id="citation-38"><sup>38</sup> <em>Мара</em> (пали, санскр. <em>māra</em>) —
                    персонификация помех на духовном пути, символизирующая силы, противодействующие
                    пробуждению. В буддизме Мара проявляется в четырёх аспектах:</p>
                <ol>
                    <li><em>Мара</em> как воплощение смерти — символизирует страх перед смертью и
                        привязанность к жизни.</li>
                    <li><em>Мара</em> как божество чувственных наслаждений — олицетворяет соблазны
                        чувственного мира, которые отвлекают от практики.</li>
                    <li><em>Мара</em> как скандхи (агрегаты существования) — представляет пять агрегатов
                        (форма, чувства, восприятие, формации ума, сознание), которые создают
                        иллюзию «я».</li>
                    <li><em>Мара</em> как демоническое сопротивление пробуждению — символизирует внутренние и
                        внешние препятствия на пути к освобождению, включая сомнения, гнев и
                        невежество.</li>
                </ol>
                <p>В истории пробуждения Будды его победа над <em>Марой</em> знаменует окончательное
                    преодоление всех препятствий и достижение полного освобождения (нирваны). Это
                    событие подчёркивает ключевую идею буддизма: освобождение возможно только через
                    преодоление внутренних и внешних помех.<a href="#ref-38" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-39"><sup>39</sup> <em>Брахма</em> (санскр. <em>brahmā</em>) — в
                    буддийской космологии многозначный термин, который переосмысливает
                    брахманические представления:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>Титул божеств мира форм (<em>rūpa-loka</em>) — обозначение высших существ, обитающих
                        в небесах мира форм. Эти существа отличаются долгой жизнью и утончённым
                        сознанием, но всё же подвержены перерождению.</li>
                    <li>Конкретное божество-должностное лицо (<em>Брахма Сахампати</em>) — одно из божеств,
                        которое играет роль защитника <em>Дхармы</em>. Например, <em>Брахма Сахампати</em> просил
                        Будду начать проповедь после его пробуждения.</li>
                    <li>Символ заблуждения о вечном «Я» — в текстах, таких как «Брахма-джала сутта»,
                        <em>Брахма</em> используется для иллюстрации ошибочности веры в вечного творца или
                        абсолютное «Я». Это подчеркивает критику буддизмом метафизических учений
                        брахманизма.</li>
                </ul>
                <p>Буддизм переосмысливает брахманические концепции, отрицая статус <em>Брахмы</em> как
                    верховного творца. Вместо этого акцент делается на взаимозависимости всех
                    явлений и отсутствии вечной сущности.<a href="#ref-39" class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-40"><sup>40</sup> «Странствующий аскет» (пали <em>samaṇa</em>, санскр. <em>śramaṇa</em>) — представитель небрахманических духовных течений, возникших в
                    древней Индии как альтернатива ведической традиции. Характерные черты
                    <em>śramaṇa</em>-движения включают:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>Отказ от ведических ритуалов — акцент на внутреннюю духовную практику вместо
                        внешних обрядов.</li>
                    <li>Практику медитации — развитие сосредоточения и познания через
                        самонаблюдение.</li>
                    <li>Непривязанность к месту — жизнь в скитаниях, без постоянного дома или
                        имущества.</li>
                </ul>
                <p>Будда Шакьямуни относился к этому движению, но трансформировал его идеи, предложив
                    «Срединный путь» — учение, избегающее крайностей аскетизма и чувственных
                    удовольствий. В отличие от других śramaṇa-учений, буддизм делает акцент на
                    практическом пути к освобождению через Четыре Благородные Истины и Восьмеричный
                    путь.<a href="#ref-40" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-41"><sup>41</sup> <em>Брахман</em> (санскр. <em>brāhmaṇa</em>) — в
                    буддийском контексте многозначный термин, который отражает как социальную, так и
                    духовную реальность:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8 my-2">
                    <li>Представитель высшей варны — в традиционной индийской системе брахманы
                        считались хранителями ведийских знаний и исполнителями ритуалов. Буддизм
                        критиковал их приверженность формальным обрядам и претензии на
                        исключительность.
                    </li>
                    <li>Символ социальной иерархии — буддизм подвергал сомнению саму концепцию варн,
                        считая, что духовное развитие зависит от личных качеств, а не от рождения.
                        Это было вызовом господствующему порядку.
                    </li>
                    <li>В переносном смысле — человек, достигший святости — в текстах, таких как
                        «Дхаммапада» (стих 383: «Кто не делает зла — тот брахман»), этот термин
                        используется метафорически. Здесь акцент сделан на моральных и духовных
                        достижениях, а не на происхождении.
                    </li>
                </ul>
                <p>Будда переосмыслил это понятие, сделав акцент на духовных качествах, таких как
                    нравственность, мудрость и освобождение, а не на социальном статусе или
                    ритуалах. Это подчеркивает универсализм буддийского учения, доступного всем
                    независимо от происхождения.<a href="#ref-41" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-42"><sup>42</sup> «Освобождение сознания» (пали <em>cetovimutti</em>, санскр. <em>cetovimukti</em>) — высшая степень свободы ума, достижимая через
                    медитативную практику и мудрость. В буддийских текстах это понятие описывается в
                    двух аспектах:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8 my-2">
                    <li>Временное освобождение — состояние, возникающее в результате глубоких
                        медитативных погружений (например, <em>джхан</em>). Это освобождение от временных
                        загрязнений ума, таких как гнев, жадность или невежество, но оно не является
                        окончательным.</li>
                    <li>Постоянное освобождение — полное и необратимое освобождение, достигаемое с
                        реализацией <em>ниббаны</em>. На этом уровне ум полностью очищен от всех загрязнений
                        (<em>āsavas</em>), и человек становится архатом.</li>
                </ul>
                <p>В «Ангуттара-никае» (II.24) это состояние описывается как «неколебимое
                    освобождение ума» (пали <em>akuppā cetovimutti</em>), подчёркивая его абсолютную независимость от
                    внешних условий и внутренних помех.<a href="#ref-42" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-43"><sup>43</sup> «Избавление от новых рождений» (пали
                    natthi dāni punabbhavo) — означает:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8 my-2">
                    <li>Прекращение действия кармических сил — исчерпание всех накопленных
                        кармических последствий, которые могли бы привести к новым перерождениям.
                        Это достигается через полное очищение ума от загрязнений (<em>āsavas</em>).
                    </li>
                    <li>Окончательный выход из круговорота сансары — достижение состояния, при
                        котором индивидуум больше не подвержен циклу рождения, старения, болезни и
                        смерти. Это состояние называется <em>параббхава</em> (пали), или «окончательное
                        прекращение».</li>
                    <li>Достижение «необусловленного» (<em>asaṅkhata</em>) состояния — реализация <em>ниббаны</em>,
                        которая является необусловленной реальностью, свободной от влияния кармы,
                        времени и условностей. Это высшая цель духовной практики.
                    </li>
                </ul>
                <p>В «Тхеригатхе» (104) это описывается как «разрушение всех рождений», подчёркивая
                    полное прекращение зависимости от условий и причин, поддерживающих существование
                    в сансаре.<a href="#ref-43" class="text-blue-500 hover:underline">↩</a></p>

                <p id="citation-44"><sup>44</sup> Существуют разные варианты перевода этой сутры на
                    русский язык.<a href="#ref-44" class="text-blue-500 hover:underline">↩</a></p>
