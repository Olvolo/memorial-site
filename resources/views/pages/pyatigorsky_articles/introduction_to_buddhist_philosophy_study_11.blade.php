            <h2 class="text-2xl font-bold text-center text-zinc-700 mb-8">Семинар одиннадцатый</h2>
            <h3 class="text-xl text-center font-semibold text-zinc-700 mb-4">Текст XI. Анупада-Сутта<a
                    href="#citation-1" id="ref-1" class="citation-mark"><sup>1</sup></a>: разные
                состояния сознания<a href="#citation-2" id="ref-2"
                                     class="citation-mark"><sup>2</sup></a>, рассматриваемые одно за
