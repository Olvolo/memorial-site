            <blockquote class="my-6 pl-4 border-l-4 border-gray-300">
                <p><em>0.</em> Так я слышал. Однажды, когда Господь пребывал в Шравасте, в роще
                    Джета, что в парке Анатхапиндады, вот что сказал он собравшимся вокруг него
                    монахам: «Велика мудрость Шарипутры, о монахи. Широка его мудрость, чего она
                    только не охватывает. Радостна его мудрость, о монахи. Полмесяца проникал<a
                        href="#citation-3" id="ref-3" class="citation-mark"><sup>3</sup></a> он в
                    дхьянические состояния в одно за другим по мере того, как они пред ним возникали<a
                        href="#citation-4" id="ref-4" class="citation-mark"><sup>4</sup></a>. Итак,
                    о монахи, Шарипутра, полностью отстранив себя от чувственных удовольствий и
                    избавившись от не-благих дхарм, вошел в первую дхьяну<a href="#citation-5"
                                                                            id="ref-5"
                                                                            class="citation-mark"><sup>5</sup></a>.
                    Эта дхьяна сопровождается «приложением мысли», «поддержанием мысли», а также
                    радостью и наслаждением, порожденными полным отстранением.</p>
                <p><em>I.</em> Таковы состояния сознания в первой дхьяне. Во-первых, те, что
                    непосредственно предшествуют вхождению в нее, либо сопутствуют этому вхождению.
                    Это: (1) приложение мысли, (2) поддержание мысли, (3) одноточечность мысли, (4)
                    радость и (5) наслаждение — всего пять состояний сознания. Во-вторых, это пять
                    агрегатов, скандх, индивидуального существования: (6) контакт (или осязание),
                    (7) эмоции, (8) восприятие, (9) воление и (10) ум (или сознание)<a
                        href="#citation-6" id="ref-6" class="citation-mark"><sup>6</sup></a>,
                    которые остаются еще от времени до вхождения в первую дхьяну. В-третьих, это
                    шесть состояний сознания, которые, сами по себе будучи благими <em>дхармами</em>, могут
                    как возникать естественным образом, так и развиваться йогическим упражнением.
                    Это: (11) рвение, (12) решительность, (13) энергичность, (14) памятование, (15)
                    равность и (16) внимательность. Всего состояний сознания в первой дхьяне тогда
                    будет шестнадцать. В эти состояния и впадал Шарипутра в одно за другим по мере
                    их возникновения<a href="#citation-7" id="ref-7"
                                       class="citation-mark"><sup>7</sup></a>. Знал он их
                    возникающими, знал уже возникшими, знал исчезнувшими. Вот так понимал он эти
                    состояния: «Не быв, начинают они становиться. Только став, исчезают». Сам же он
                    в отношении к этим состояниям оставался равнодушным, отстраненным,
                    непривязанным, независимым. Ибо ум его был свободен от пут. И вот еще как
                    понимал Шарипутра эти состояния: «О, есть же еще одно избавление<a
                        href="#citation-8" id="ref-8" class="citation-mark"><sup>8</sup></a>, что за
                    пределами (всего) уже достигнутого<a href="#citation-9" id="ref-9"
                                                         class="citation-mark"><sup>9</sup></a>.
                    Подтверждаю — есть».</p>
                <p><em>II.</em> Тогда, о монахи, он останавливает приложение и поддержание мысли
                    ((1) и (2)) и пребывает во второй дхьяне. Состояния сознания в ней — это: (1)
                    спокойная уверенность, (2) установленность ума<a href="#citation-10" id="ref-10"
                                                                     class="citation-mark"><sup>10</sup></a>,
                    (3) одноточечность мысли (также (3) в первой дхьяне). В ней нет (1) и (2) первой
                    дхьяны, но остаются ее (4) и (5) под теми же номерами. Опять же в ней есть те же
                    пять скандх, а также состояния (11) — (16), фигурирующие под теми же номерами. И
                    эти шестнадцать состояний второй дхьяны Шарипутра знал не возникшими,
                    возникающими и исчезающими.</p>
                <p><em>III.</em> Потом, о монахи, Шарипутра приостанавливает радость и наслаждение
                    ((4) и (5) в I и II) и пребывает в третьей дхьяне. В третьей дхьяне он упражняет
                    обратное вспоминание<a href="#citation-11" id="ref-11"
                                           class="citation-mark"><sup>11</sup></a> (I—II, (15)) и
                    все ясно осознает (III, (17))<a href="#citation-12" id="ref-12"
                                                    class="citation-mark"><sup>12</sup></a>. Но он
                    еще способен чувствовать телесное удовольствие (I—II, (5)). Остальные же
                    состояния в третьей дхьяне будут такими же как и в первой и второй. И так
                    далее...</p>
                <p><em>IV.</em> Теперь, о монахи, Шарипутра, покинувший удовольствие, равно как и
                    страдание, оставивший позади себя радость и горе, веселье и грусть, входит в
                    четвертую дхьяну. В ней нет ни страдания, ни удовольствия. В ней есть чистота
                    обратного вспоминания (IV, (18)), порожденная полной невозмутимостью. Другие
                    состояния в четвертой дхьяне — это равность (I — III, (15)), чувство ни
                    страдания, ни удовольствия, ни приятного, ни неприятного (IV, (19)) и полная
                    беззаботность ума (IV, (20))<a href="#citation-13" id="ref-13"
                                                   class="citation-mark"><sup>13</sup></a>.
                    Остальные же состояния сознания здесь будут, как в первых трех, и так далее...
                </p>
                <p><em>V.</em> Наконец, о монахи, когда полностью оставлены позади форма и
                    восприятие формы, чувственное восприятие, восприятие (т.е. состояния сознания
                    (6)-(10)), тогда Шарипутра, осознающий, что «пространство бесконечно», входит в
                    сферу бесконечного пространства. Состояния сознания этой сферы таковы:
                    восприятие сферы безграничного пространства (V, (21)), одноточечность мысли
                    (I—IV, (3)), а также все остальные перечисленные в первых четырех дхьянах.</p>
                <p><em>VI.</em> Затем Шарипутра выходит за пределы сферы бесконечного пространства
                    и, осознавая, что «сознание бесконечно», входит в сферу бесконечного сознания.
                    Состояния сознания этой сферы таковы: восприятие сферы бесконечного сознания
                    (VI, (22)), одноточечность мысли, а остальные — как в четырех дхьянах и т.д...
                </p>
                <p><em>VII.</em> Затем, покинув сферу бесконечного сознания и осознав, что «ничего
                    нет», Шарипутра вступает в сферу ничто, состояниями сознания которой являются
                    восприятие сферы ничто (VII, (23)) и одноточечность мысли. А остальные опять же,
                    как в четырех дхьянах и т.д...</p>
                <p><em>VIII.</em> Но и сферу ничто Шарипутра покидает и входит в сферу ни
                    восприятия, ни не-восприятия. Там он пребывает, а, выйдя из нее, созерцает
                    прошлые дхармы, те, что исчезли, ушли из становления. Так он осознает:
                    «Воистину, не быв, эти дхармы возникли. Возникнув, исчезли, ушли<a
                        href="#citation-14" id="ref-14" class="citation-mark"><sup>14</sup></a>». И
                    были ему абсолютно безразличны все они, ни отталкивали они его и не притягивали.
                </p>
                <p><em>IX.</em> Уйдя из сферы ни восприятия, ни не-восприятия, Шарипутра достиг
                    сферы прекращения восприятия и чувствования. И все его умственные помрачения и
                    притоки были уничтожены его всепроникающей Мудростью. И осознав свое достижение,
                    спросил: «А есть ли еще избавление далее того, что достигнуто?» Ответил: «Нет,
                    далее нет»<a href="#citation-15" id="ref-15" class="citation-mark"><sup>15</sup></a>.
                    О монахи, если можно о ком-то сказать, что он достиг совершенства в благородстве
                    поведения, благородстве сосредоточения сознания и в благородстве мудрости<a
                        href="#citation-16" id="ref-16" class="citation-mark"><sup>16</sup></a>, то
                    это должно быть сказано о Шарипутре. И еще, о монахи, если и можно о ком-то
                    сказать, что он — сын Господа, Будды, рожденный из груди Будды, из уст Будды,
                    рожденный из Дхармы, созданный Дхармой, наследник Будды в Дхарме, не по плоти,
                    не в плотских вещах, то следует это сказать о Шарипутре. О монахи, Шарипутра —
                    тот, кто продолжает вращать Колесо Дхармы, которое начал вращать Так Ушедший».
                </p>
                <p>Вот что Господь сказал монахам. И возрадовались они его словам.</p>
            </blockquote>
            <h3 class="text-xl text-center font-semibold text-zinc-700 my-4"><em>ДХАРМЫ</em> В ПРОСТРАНСТВЕ СОЗЕРЦАНИЯ<br>ОТНОШЕНИЕ <em>ДХАРМ</em> И <em>ДХЬЯНЫ</em> —<br>ОСНОВНОЕ СОДЕРЖАНИЕ АБХИДХАРМИЧЕСКОЙ
                ФИЛОСОФИИ.<br><em>ДХЬЯНА</em> КАК СИНТЕТИЧЕСКАЯ АПОСТЕРИОРНАЯ ФИЛОСОФСКАЯ КАТЕГОРИЯ.</h3>
            <p><em>(0)</em> Эта маленькая сутра является самым кратким и, может быть, самым ранним
                изложением философии <em>Абхидхармы</em>. В предыдущем семинаре <em>дхьяна</em> подробно
                рассматривалась как особое пространство порождения мыслей с их объектами. В конце
                отмечалось, что есть такие мысли (с их объектами), такие умственные состояния и
                состояния сознания, словом, такие <em>дхармы</em>, которые возникают или генерируются <span class="underline">только</span>
                в <em>дхьяне</em>, т.е. только в качестве <span class="underline">объектов созерцания</span>. В этой же сутре распределение
                <em>дхарм</em> по ступеням созерцания предполагает: все то, что возникает, все мыслимые
                феномены (т.е. все <em>дхармы</em>) могут быть мыслимы <span class="underline">как <em>дхармы</em></span> только в созерцании,
                <em>дхьяне</em>. Отсюда следует, что и центральная идея буддизма, <em>дхарма</em>, является по своему
                определению идеей дхьянической.</p>
            <p>Однако, если все <em>дхармы</em> возникают <span class="underline">как <em>дхармы</em></span> только в <em>дхьяне</em>, тогда последняя может
                рассматриваться, в свою очередь, не только в качестве <span class="underline">места йоги</span> — в том смысле, в
                каком обратное вспоминание нами рассматривалось, как если бы оно было временем йоги
                — но и в качестве своего рода «суперпространства» всякого мышления вообще.
                Суперпространства, в котором происходят, произойдут или уже произошли все события,
                факты и ситуации. Такое суперпространство мы могли бы себе представить как
                дополнительное к «нормальному» или внешнему пространству, подобно тому как <span class="underline">время йоги</span> было нами воображено как дополнительное к нашему «нормальному» времени, времени
                нашего обыкновенного мышления. И как об этом уже было сказано в предыдущем семинаре,
                <em>дхьяна</em> образует «параллельный космос» буддийской мифологии, ее «вторую» вселенную.
                Только из этой «второй» вселенной обычно, повседневно видимая и мыслимая «первая»
                вселенная может быть видима и мыслима <span class="underline">как она есть</span>, во всей множественности и
                сложности составляющих ее микрокосмических, макрокосмических и космических вещей,
                фактов и событий.</p>
            <p>Выше мы подробно говорили о промежуточности <em>дхьяны</em>, о том, что она всегда — <span class="underline">между</span>.
                Между возникновением и генерированием. Между сознанием и не-сознанием. Между
                феноменальным миром, <em>сансарой</em> и Нирваной. И разве что с известной натяжкой можно
                назвать <em>дхьяну</em> просто одной из <em>дхарм</em>. Ибо, хотя она и является <em>дхармой</em> в самом общем
                смысле этого слова, но — заметьте — <span class="underline">ее нет среди <em>дхарм</em>, созерцаемых и определяемых
                Шарипутрой в тексте XI</span>. Почему же это так, ведь и обратное вспоминанье есть, и
                полная невозмутимость, и отстранение, а созерцания как такового, <em>дхьяны</em> (в списке 20
                <em>дхарм</em> четырех <em>дхьян</em>) — нет? Это и ведет нас к догадке, что <em>дхьяна</em> — это своего рода
                <span class="underline">метадхарма</span>, т.е. такая <em>дхарма</em>, такое состояние сознания, которое обладает
                способностью (или энергией) индуцировать другие <span class="underline">обусловленные</span> <em>дхармы</em>, само оставаясь
                автономным от (тех же) обуславливающих факторов. Или скажем иначе. <em>Дхьяна</em>, хоть сама
                и обусловлена, подводит сознание созерцателя к необусловленному.</p>
            <p><em>(1)</em> Именно промежуточность <em>дхьяны</em> делает ее столь трудной для нашего — да и
                какого угодно другого — понимания; времена разных созерцательных процессов
                оказываются как бы «спрессованными» вместе в одном и том же <span class="underline">месте</span> <em>дхьяны</em>. Таким
                образом — в дополнении к тому, что уже было сказано о <em>дхьяне</em> как о месте и
                пространстве, — <em>дхьяна</em> это место, в котором <em>дхармы</em> (состояния сознания, феномены,
                все) возникают или генерируются так, как если бы они подчинялись плану, порядку, в
                каком они созерцаются созерцателем. Итак, мы читаем в сутре: «В течение полумесяца
                монах Шарипутра проницал <em>дхармы</em>, одну за другой, в порядке их возникновения... Тогда
                он вступил в первую <em>дхьяну</em> и т.д... Состояния сознания, <em>дхармы</em> первой <em>дхьяны</em>, были
                таковы (сначала от (1) до (5))... Он знал их в их возникновении и в их исчезновении
                и т.д... » Теперь рассмотрим этот пассаж в отношении времени и места созерцания,
                практикуемого Шарипутрой.</p>
            <p><em>1.</em> «В течение полумесяца» — это <span class="underline">внешнее</span> макрокосмическое время, в каком
                Шарипутра проницал <em>дхармы</em>.</p>
            <p><em>2.</em> «проницал <em>дхармы</em>, одну за другой» — это порядок возникновения <em>дхарм</em>,
                порядок, который может быть как их собственным порядком возникновения в
                макрокосмическом внешнем времени, так и порядком возникновения моментов созерцания
                Шарипутры во <span class="underline">внутреннем</span> микрокосмическом времени.</p>
            <p><em>3.</em> «Тогда он вступил в первую <em>дхьяну</em>» — здесь «тогда» относится к
                внутреннему микрокосмическому времени созерцания Шарипутры, но времени, которое с
                «внешней» точки зрения сжато в одном мгновении входа в первую <em>дхьяну</em>.</p>
            <p>Теперь мы видим, что в (3) временная последовательность в возникновении состояний
                сознания, <em>дхарм</em> превращается в пространственную конфигурацию их распределения.
                Словом, время <em>дхьяны</em> переходит в ее пространства и, так сказать, «относительная
                хронология» актов и фактов созерцания становится чистой <span class="underline">топологией</span>. Но тогда и
                <em>дхарма</em>, с точки зрения <em>дхьяны</em>, представляет собой <span class="underline">момент мысли, «уже» отделенный от
                других моментов мысли и определяемый (и терминологически обозначенный согласно
                дхьянической классификации <em>дхарм</em> на классы и подклассы) по занимаемому им месту
                (<em>locus</em>) в пространстве (<em>topos</em>) <em>дхьяны</em></span>.</p>
            <p><em>(2)</em> Из сказанного выше следует, что сам термин «<em>дхарма</em>» употребляется в
                буддийских текстах в двух смыслах, а именно: <span class="underline">как момент мысли, мысль, состояние
                сознания, феномен, «<em>дхарма</em> как одна <em>дхарма</em>»: и как обозначение разновидности, класса
                или подкласса <em>дхарм</em></span>. Из чего также следует: с точки зрения <em>дхьяны</em>, каждой мысли при
                ее возникновении приписывается набор <em>дхарм</em>, число и состав которых определяется
                йогическим статусом этой мысли и порождающего ее ума — от «нулевой йоги» косной,
                неразвитой, нечистой и невежественной мысли до мысли созерцателя, сосредоточенной на
                трансцендентальной сфере не-восприятия и не-чувствования. В этой связи отметим одну
                интереснейшую черту возникновения мысли в <em>дхьяне:</em> чем выше дхьянический уровень или
                статус возникающей мысли, тем меньшее число <em>дхарм</em> ее сопровождает и тем проще (по
                своей номенклатуре) состав этих <em>дхарм</em>. Это можно было бы назвать «правилом
                дхармической редукции». К этому можно добавить в порядке тавтологического вывода: в
                «нормальном», не-дхьяническом мышлении «вещь <strong>А</strong> есть вещь <strong>А</strong> и факт <strong>Б</strong> есть факт <strong>Б</strong>», но
                что бы ни возникло в <em>дхьяне</em>, оно само есть не то, чем оно является, а является
                <em>дхармой</em> определенного класса и подкласса. Это так, поскольку все феномены
                психической субъективности созерцающего, все «его» ментальные <em>дхармы</em>, так сказать по
                определению <span class="underline">временные</span>, уже нашли свое «разрешение» в пространстве <em>дхьяны</em> и стали
                <span class="underline">чистыми объектами</span> созерцания.</p>
            <p><em>(3)</em> Один философский вопрос пока еще остается неразъясненным. Каково же
                вообще отношение сознания к созерцанию, <em>дхьяне</em>? Следует уточнить: <span class="underline">какого</span> сознания?
                Или точнее: какой <em>дхармы</em>, какого класса <em>дхарм</em>, названной здесь «сознанием»? По
                тексту XI мы видим, что основой, сердцевиной дхармического состава мысли во всех
                девяти фазах созерцания (4 <em>дхьяны</em> плюс 5 трансцендентальных сфер) остаются пять
                <em>дхарм</em>, соответствующих пяти агрегатам, <em>скандхам</em> индивидуального существования. Здесь
                пятая скандха, ум, или сознание <span class="underline">как ум</span>, приобретает особое значение. Так, во второй
                трансцендентальной сфере оно становится <span class="underline">бесконечным</span> и тем самым противопоставленным
                бесконечному пространству первой трансцендентальной сферы. В четвертой
                трансцендентальной сфере сознание также противопоставлено пространству, но
                фактически как <span class="underline">равноместное</span> последнему. В этой связи отметим, что «ничто» пятой
                сферы можно понимать (только в порядке догадки) как предел того, что может быть
                мыслимо мыслью и осознано сознанием. Однако здесь последнее слово за Буддхагхошей,
                который сказал: «Ничто — это сознание, исчезнувшее в пространстве». Или
                «превращенное в пространство»?</p>
            <p>Отметим, что вектор трансформации умственного микрокосма созерцателя в трех первых
                трансцендентальных сферах направлен от «Пространство — это все» через «Сознание —
                это все» к «Сознание — это пространство» (т.е фактически «вообще ничего»), но в
                четвертой и пятой сферах вектор удивительным образом меняется. Теперь это —
                восприятие (третий агрегат индивидуального существования), которое сначала
                нейтрализуется, а затем отбрасывается вместе с эмоциями. Таким образом
                психо-ментальный комплекс созерцателя лишается не только своей когнитивной
                составляющей, но и своей перцептивной основы. Последнее особенно важно, ибо в
                <em>Абхидхарме</em>, да и в буддийской философии в целом, восприятие играет роль
                <span class="underline">универсального ментального</span> деноминатора и представляет всю сумму функций и
                модальностей ума. Вот почему уже на пороге <em>Нирваны</em> — ибо, как сказано в сутре, «не
                будет иного избавления за сферой прекращения восприятия и чувствования» — весь мир
                мыслимых и воспринимаемых вещей уже прекратил свое существование для
                созерцателя.</p>
            <p><em>(4)</em> Теперь, наконец, о самой загадочной, если не сказать таинственной,
                особенности <em>дхьяны</em> как феномена, <em>дхармы</em> — в ее, этой <em>дхармы</em>, отношении к другим
                <em>дхармам</em>. Что бы ни происходило в <em>дхьяне</em>, какая бы <em>дхарма</em> ни возникла, ни возникает и
                в будущем ни возникнет в ее пространстве, остается <span class="underline">неопределенным</span> следующее:
                возникла (возникает, возникнет) ли эта <em>дхарма</em> сама, спонтанно, только в силу
                Взаимообусловленного Возникновения, или была генерирована в созерцании созерцателем.
                Я думаю, что именно эта неопределенность обусловила ту весьма высокую степень
                свободы толкования начальных постулатов буддизма, которая отличала
                философов-буддистов от их брахманистских коллег в толковании последними основных
                постулатов ведийской религии. Вместе с тем та же дхьяническая неопределенность имела
                своим философским результатом появление в буддизме Большой Колесницы целой группы
                солипсистских (и «иллюзионистских») школ и концепций, получивших дальнейшее развитие
                в буддийском тантризме.</p>
