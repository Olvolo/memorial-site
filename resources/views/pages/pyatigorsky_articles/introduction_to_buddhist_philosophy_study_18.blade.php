            <h3 class="text-xl text-center font-semibold text-zinc-700 my-4">МЫСЛЬ И ВРЕМЯ, КОНТИНУУМ МЫСЛИ;<br>В КАКОМ ВРЕМЕНИ ОН СУЩЕСТВУЕТ? </h3>
            <p>Мы уже говорили о континууме в семинаре о созерцаниях, <em>дхьянах</em> (и в некоторых других
                семинарах). Даже буддийские мыслители обеих Колесниц считают, что в отношении этой
                темы существует неясность — но, может быть, она и должна здесь существовать? В
                названии этого семинара каждое слово требует разъяснения, начиная с «он» в
                заключительном вопросе. В самом деле, что это такое, этот континуум? Чтобы ответить,
                надо радикально сменить угол зрения отвечающего, изменить его начальную позицию.
                Итак, начну с моего же мышления сейчас, в это мгновение, и попытаюсь схватить эту
                мысль, мыслимую мною, когда пишу эти строки. Условно обозначу ее как <span class="underline">последнюю</span>, то
                есть непосредственно предшествующую той, которой я начал мое рассуждение сейчас.
                Тогда мысль, выраженная всей фразой, начиная со слова «тогда», будет уже «третьей»
                мыслью и так далее. Но мысль, условно названная «последней», останется своего рода
                <span class="underline">шифтером</span> — названием, которое будет переноситься на любую мысль, регистрируемую в
                моей рефлексии как возникающую перед или после мысли о «сейчас». Расстояние между
                двумя мыслями, названными «последними», мы будем рассматривать как «серию» в данном
                мышлении. Мысль о «сейчас» и мысль о каждой мысли как о «последней» могут
                рассматриваться в качестве двух элементов одного <span class="underline">класса</span> или <span class="underline">множества</span>, каждый
                элемент которого будет либо «сейчас», либо «последняя мысль». Однако понятие класса
                или множества является чисто пространственным, а «сейчас», «до» и «после» обозначают
                случаи, соотносимые со временем. Более того, континуум сознания или поток мысли в
                названии нашего семинара является по определению понятием временным. Но так ли это
                на самом деле и что такое «на самом деле» в нашем случае? Математик Юрий Сафаров
                предложил лемму, где совмещаются топологический и временной аспекты мысли: «Мысль,
                которая имеет своей причиной только непосредственно предшествующую ей мысль и,
                непосредственно предшествующая ей мысль должны принадлежать одному и тому же
                множеству». Но является ли абсолютно необходимым для мыслей следовать друг за другом
                во времени в силу того, что одна является причиной другой, или в силу того, что обе
                принадлежат одному и тому же множеству? Если да и континуум мысли темпорален по
                определению, то мы можем считать, что здесь речь идет о <span class="underline">внутреннем</span> времени,
                отделяющем возникновение одной мысли от возникновения другой в том же самом
                континууме.</p>
            <p>Но что же тогда можно сказать о времени существования данного континуума как целого?
                В каком времени продолжает стремиться поток мысли? Наконец, обязательно ли
                существует «внешнее» время континуума? Да и должен ли я знать, что обозначается
                словом «время»? Ведь сказано же Буддхагхошей:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                Мудрец описывал мысль как время,<br>И время он описывал как мысль,<br>Чтобы показать различные классы дхарм.
            </blockquote>
            <p>Ибо время — это либо время моментов мысли и длительности серий мысли, либо время
                <span class="underline">процесса</span> мышления. Или это время не-мышления, то есть
                интервалов между мыслями или перерывов в
                мышлении. Или, наконец, это время, в котором возникают мысли и длится мышление,
                своего рода воображаемая «среда» мысли и мышления. В последнем случае, однако, можно
                видеть тенденцию представлять время как особую субстанцию. Эта тенденция ясно видна
                в субстанциалистских представлениях о времени, особенно в <em>Махабхарате</em> (в частности,
                и в <em>Бхагавад-Гите</em>), где время выступает как крайне враждебное мышлению и сознанию (и
                с ними абсолютно несовместимое!), как господствующее космическое анти-сознание,
                разрушающее мировой порядок.</p>
            <p>Теперь маленькое отступление. Предположим, что, в порядке развития субстанциалистской
                концепции времени, можно говорить и о континууме как о потоке времени, а не мысли.
                Примерно к такого рода концепции склонялись приверженцы <em>сарвастивады</em>, наиболее
                философской школы буддизма Малой Колесницы. Приверженцы <em>сарвастивады</em> утверждали
                (возможно, впервые в истории буддийской философии), что <em>дхармы</em>, как моменты мысли,
                не возникают, не исчезают, но все время <span class="underline">несутся из будущего в прошлое через настоящее</span>. Подобным же образом индивидуальный континуум мысли длится из будущего в
                прошлое. Но откуда тогда берется будущее, настоящее и прошлое, а точнее, откуда
                возникают будущие, настоящие и прошлые дхармы? Заметьте, что, хотя приверженцы
                <em>сарвастивады</em> и были в известных пределах онтологистами, но в наибольшей степени и
                прежде всего они были упорными эмпирицистами. «Будущее», «настоящее» и «прошлое»
                были для них словами обыденного языка, обозначающими направление потока времени в
                отношении континуума мысли. Мне же, чтобы вообразить поток времени, стремящийся ко
                мне из будущего в момент моего воображения (которое есть тоже мышление), придется
                прибегнуть к воображению себя внешним наблюдателем «моего» континуума мысли. Но
                чтобы это сделать, я должен буду уничтожить любую мысль о том, что осталось до
                данного мгновения, то есть уничтожить прошлое время. Только тогда я смогу
                представить себе время протекающим из будущего в прошлое. Но такое <span class="underline">обратное время</span> в
                принципе возможно, только если оно является внешним в отношении континуума мысли,
                наблюдаемого внешним наблюдателем в качестве <span class="underline">внешнего объекта</span> (из чего, в частности,
                следует, что для внешнего наблюдателя и «его собственный» континуум будет внешним
                объектом). Именно в этом смысле великий Дхармакирти называл континуум мысли
                «объектом», ибо он его наблюдал с позиции, никак не принадлежащей его континууму
                мысли, то есть с позиции внешнего наблюдателя.</p>
            <p>Эпистемологически отсюда следует: то, что здесь называется «континуумом мысли»,
                является внешним объектом и таким образом, по определению, <span class="underline">другим</span> континуумом, то
                есть иным, чем «свой собственный», так сказать. Ибо для внешнего наблюдателя он
                всегда «какой-то», «чей-то», «не его» континуум, «один континуум». Напомним при
                этом, что строго абхидхармистски рассуждая, будет невозможно ни непосредственное
                восприятие возникновения одной отдельной <em>дхармы-мысли</em> в континууме в целом, ни,
                наконец, той кармической силы, благодаря которой случается их совпадение.
                Подытоживая сказанное о концепции континуума мысли в <em>сарвастиваде</em>, нельзя не
                согласиться с Эдвардом Конзе, по мнению которого концепция континуума была введена,
                чтобы объяснить, что каждая <em>дхарма</em> является включенной в серию <em>дхарм</em>, а каждая
                мгновенная мысль — в индивидуальную серию (вежливое слово для обозначения индивида).
                Здесь «серия» — это чисто временное понятие, как, впрочем, и понятие континуума
                мысли, пока оно не рассматривается топологически как класс.</p>
            <p>Таким образом, слово «континуум» здесь имеет два смысла. В первом — оно обозначает
                <span class="underline">серии мысли</span>, следующие друг за другом во времени в силу Взаимообусловленного
                Возникновения. В понимаемом таким образом континууме мысли ни одна отдельная мысль
                не может возникнуть одновременно с другой. Во втором смысле слово «континуум»
                обозначает особое «сгущение» («компактность») <em>дхарм</em>-моментов мысли, которое и дает
                возможность говорить об отдельности одного мышления от другого. Такого рода
                «сгущение» обеспечивается синергиями, <em>самскарами</em> (соответствующими четвертому
                агрегату, <em>скандхе</em> индивидуального существования). Только благодаря <em>самскарам</em> длится
                «существование» континуума во <span class="underline">внешнем</span> континууме времени. В этом времени каждый
                континуум безначален, то есть нет такого времени, когда истощатся все <em>самскары</em> и
                полностью прекратится возникновение мыслей в индивидуальном потоке сознания.</p>
            <p>Как мы уже знаем из предыдущих семинаров, смерть — это такой особый момент мысли в
                индивидуальном континууме, который как бы разделяет континуум на различные жизни или
                существования. Но этот момент мысли является и своего рода «связкой» (санскр. <em>pratisandhi</em>), тем латентным переходным сознанием, которое соединяет данное
                существование с последующим, в том же континууме. Именно в этом смысле буддийские
                философы принимают, хотя и с оговорками, концепцию перерождения. Но перерождения не
                континуума мысли, ибо он никогда не «рождается» и всегда продолжается, а
                перерождения только того, чем сам этот континуум оказывается отмеченным, то есть
                перерождения условного «носителя» имени, имени действительной или потенциальной
                Благородной Личности, Будды или Бодхисаттвы. Не будем забывать, что слово «имя»
                (санскр. <em>nāma</em>) также обозначает сознание в <em>Абхидхарме</em>. Так в концепции континуума мысли
                эпистемология связывается с онтологией, а через последнюю — и с буддологией.</p>
            <p>В то же время в текстах Малой Колесницы континуум мысли часто описывается в более
                узком смысле, как часть, отрезок данного континуума, начинающийся с первой мысли
                после зачатия живого существа (это — мысль о данном перерождении и о грядущей
                смерти) и кончающийся его последней, «предсмертной» мыслью (это — мысль о настоящей
                смерти и о будущем перерождении). Понимаемый таким образом континуум становится
                практически синонимом живого существа (санскр. <em>sattva</em>) и тем самым понимается как <span class="underline">место</span>
                возникновения мыслей в силу Взаимообусловленного Возникновения. Напомним, что
                последнее предполагает сознание как основу индивидуального существования (или, если
                придерживаться концепции сознания-вместилища в <em>виджнянаваде</em>, как индивидуальное
                <span class="underline">место</span>, топос мысли). Возможно также, что идея о «предсмертной» мысли, охватывающей
                только еще заканчивающееся существование была, введена для конкретизации концепции
                континуума мысли. Вместе с тем эта идея могла явиться и как бы для удовлетворения
                «философской нужды» в таком апостериорно-синтетическом понятии, которое было бы
                одновременно и «натуральным» и «ментальным». Ибо не исключено, что эта «предсмертная
                мысль», «сознание-связка» сама есть проявление бессознательной силы, <span class="underline">длящей</span>
                континуум мысли от рождения к смерти, от смерти к перерождению и так далее.</p>
            <p>И наконец, поскольку каждый континуум безначален в своих истоках и, следовательно,
                рождался или перерождался в бесконечном числе живых существ, <em>саттв</em>, то из этого
                может следовать, что в каждом континууме мысли любая мысль любого континуума
                возникала в то или иное время. Отсюда же и возможность предположения, что любой
                отрезок данного континуума содержит все мысли этого континуума.</p>
            <p>Я думаю, что «предсмертная мысль» (или «сознание-связка») послужила абхидхармистским
                мыслителям в качестве рабочего метода, инструмента для объяснения природной
                длительности, так сказать, продолжительности нашего сознательного опыта. Так
                мгновенные возникновения <em>дхарм-мыслей</em> были сначала сведены к сериям мысли, затем к
                одной ментальной основе, топосу мысли, в котором все отдельные мысли и серии мыслей
                оставляют свой отпечаток и который тем самым обеспечивает взаимосвязь и
                взаимозависимость мыслей и их серий. Отсюда — один шаг к постулированию некой особой
                силы (пали <em>satti</em>, санскр. <em>śakti</em>), присущей каждой мысли, силы, способной вызывать
                следующую мысль. Позднее в некоторых тантристских версиях <em>Абхидхармы</em>
                «сознание-связка» превращается в «первичную сознательную основу» (пали <em>pa-kati-māno</em>, санскр. <em>prakṛtimanas</em>), извечный источник Природного Света.</p>
            <p>Теперь небольшое добавление относительно пространственного аспекта континуума мысли.
                Возможно, что исторически введение концепции континуума сознания было выражением
                тенденции к отклонению от строгой абхидхармистской ортодоксии и от буквального,
                неукоснительного следования принципу <span class="underline">не-сущностности</span> или «не-я» (пали <em>anattā</em>, санскр. <em>anātman</em>). Мы знаем, что каждый континуум отделен <span class="underline">пространственно</span> от всех других.
                Поэтому он и сам всегда другой по данному выше определению. Он всегда «имеется» как
                <span class="underline">место</span>, но не «постоянно», то есть всегда, когда мы думаем о нем как «имеющемся».
                Таким образом, он может рассматриваться в качестве <span class="underline">позиции</span>, дополнительной к позиции
                мгновенно возникающей <em>дхармы-мысли</em>. В этой позиции устанавливаются три основных
                пространственных различия:</p>
            <ol class="list-decimal pl-6">
                <li>различие между одним живым существом, <em>саттвой</em>, и другим;</li>
                <li>различие между тем, что полагается внутренним (санскр. <em>ādhyātmika</em>) и внешним (санскр. <em>bāhya</em>) в живых существах;
                </li>
                <li>различие между различными классами <em>дхарм</em>, принадлежащими к различным континуумам
                    и сериям мысли.
                </li>
            </ol>
            <p>В заключение этой экспозиции учения о континууме мысли отметим важнейший философский
                момент. Ведь континуум мысли <span class="underline">не существует</span>, он только «имеется» в мышлении. Но он и
                <span class="underline">не возникает</span>, как возникает отдельная мысль (вместе с ее объектами и так далее).
                Может быть, именно поэтому континуум мысли вводился в буддийские философские
                рассуждения как бы «явочным порядком», но никогда так и не был постулирован как
                особая онтологическая или эпистемологическая первичная категория.</p>
