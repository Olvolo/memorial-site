                    доступной органам чувств, тогда как «тонкий» (пали <i>sukhuma</i>, санскр. <i>sūkṣma</i>)
                    обозначает уровень материальности, недоступный прямому восприятию и
                    проявляющийся через более тонкие формы бытия, например, в состоянии глубокой
                    медитации.<a href="#ref-4" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-5"><sup>5</sup> «Как есть» (пали <i>yathābhūtaṃ</i>) —
                    это ключевой эпистемологический принцип буддизма, означающий непосредственное
                    восприятие реальности без искажений, проекций или концептуальных добавлений. Он
                    составляет основу прозрения в три характеристики существования и
                    противопоставляется обычному заблуждающемуся восприятию. В
                    <i>Мадджхима-никае</i> (MN 1) этот подход называется «правильным знанием» (<i>sammā-ñāṇa</i>)
                    и рассматривается как знак вхождения в Высшее Знание.<a href="#ref-5"
                                                                            class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-6"><sup>6</sup> «Благородный» (пали <i>ariya</i>) — так
                    называется тот, кто достиг одного из четырёх этапов духовного освобождения:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>Вхождение в Поток (<i>sotāpanna</i>).</li>
                    <li>Однократное возвращение (<i>sakadāgāmi</i>).</li>
                    <li>Невозвращение (<i>anāgāmi</i>).</li>
                    <li>Полное просветление и освобождение (<i>arahant</i>).</li>
                </ul>
                <p>Такой человек характеризуется непосредственным прозрением и прямым знанием
                    Четырёх
                    Благородных Истин, что делает его восприятие реальности свободным от
                    заблуждений.<a
                        href="#ref-6" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-7"><sup>7</sup> См. I, прим. 43.<a href="#ref-7"
                                                                                class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-8"><sup>8</sup> «Притоки» (пали <i>āsava</i>, санскр.
                    <i>āśrava</i>) — внутренние загрязнения или тенденции, которые заливают ум и
                    поддерживают цикл перерождений (<em>saṃsāra</em>). Они делятся на четыре типа:</p>
                <ol class="list-decimal list-outside ml-4 space-y-1 pl-5">
                    <li>Приток чувственных желаний (<i>kāmāsava</i>).</li>
                    <li>Приток существования (<i>bhavāsava</i>).</li>
                    <li>Приток воззрений (<i>diṭṭhāsava</i>).</li>
                    <li>Приток неведения (<i>avijjāsava</i>).</li>
                </ol>
                <p>Эти загрязнения происходят как из внешних, так и из внутренних источников и
                полностью искореняются на пути к архатству, что знаменует завершение духовного пути.<a
                    href="#ref-8" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-9"><sup>9</sup> Архат (пали <i>arahant</i>, санскр. <i>arhat</i>)
                    — тот, кто достиг высшего духовного состояния в буддизме:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>Уничтожил все внутренние загрязнения (<em>āsava</em>)</li>
                    <li>Прямо познал Четыре Благородные Истины</li>
                    <li>Освободился от цикла перерождений (<em>saṃsāra</em>)</li>
                </ul>
                <p>Первые пять архатов — это ученики Будды, достигшие пробуждения вскоре после его
                первой проповеди. Считая самого Будду, их было шестеро — все они стали первыми
                носителями просветлённого знания в новой традиции.<a href="#ref-9"
                        class="text-blue-500 hover:underline">↩</a></p>
            </div>
