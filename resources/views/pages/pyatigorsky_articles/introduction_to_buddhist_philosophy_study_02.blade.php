            <h3 class="text-xl text-center font-semibold text-zinc-700 mb-4">Текст II. О знаке
                «Не-Я»<a href="#citation-1" id="ref-1" class="citation-mark"><sup>1</sup></a>
                (Вторая проповедь)</h3>

            <blockquote class="my-6 pl-4 border-l-4 border-gray-300">
                <p>Так я слышал. Господь тогда пребывал в Оленьем Парке, в Исипатане («Прибежище
