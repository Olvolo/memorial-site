            <h3 class="text-xl text-center font-semibold text-zinc-700 my-4">ПОЧЕМУ ЭТО — НЕ Я? ИЛИ
                ПОЧЕМУ Я — НЕ ЭТО?</h3>
            <p>Заметим прежде всего, что в этой сутре Будда приглашает пятерых монахов к
                философскому рассуждению о «Я», но предлагает при этом свой порядок рассуждения —
                иной, чем тот, который был принят в рассуждении о «Я» в брахманистской философской
                традиции, восходящей к текстам ранних прозаических Упанишад (появление которых в
                устном виде датируется примерно VIII—VII веками до н.э.) В брахманистской традиции
                порядок рассуждения о «Я» задается формулой: «Ты есть то» (санскр. <em>tat tvam asi</em>),
                где «ты» представляет того, к кому эта формула обращена (им вполне могу быть и я сам,
                эту формулу произносящий, но тогда получается «я есть то»), а «то» (санскр. <em>tad</em>)
                является общепринятым обозначением «Я». Но «Я» не в смысле, в котором оно
                употребляется в качестве личного местоимения первого лица единственного числа, а в
                смысле «абсолютного Я», которое в Упанишадах полагалось осью, принципом и основой не
                только «меня» («тебя», «его» и так далее), но и всего <span class="underline">сущего</span>, всего того, что <span class="underline">есть</span>.
                Тогда условно будем называть «Я» (в первом смысле) «эмпирическим Я», а во втором —
                «абсолютным Я». Последнее в той же брахманистской традиции обозначается санскритским
                словом <em>атман</em>, которое с начала первого тысячелетия до н.э. и по сей день является
                центральным понятием индийской философии. Вот что существенно — Вторая
                Проповедь, отрицая «Я» именно во втором смысле, то есть как абсолютное Я, <em>атман</em>, не
                говорит, что «ты» («Я», «он» и так далее) <span class="underline">не есть</span> <em>атман</em>. Она утверждает, что любой
                феномен, <em>дхарма</em>, приписываемый твоему («моему», «его») эмпирическому Я, менее всего
                само это эмпирическое Я (являющееся всего лишь лингвистической условностью, звуком,
                названием), <span class="underline">не есть</span> <em>атман</em>, или — что здесь философски особенно важно — есть
                <span class="underline"><em>не-атман</em></span>. Таким образом, уже в постановке вопроса о бытии или не-бытии абсолютного Я
                неизбежна двойственность. Такие суждения, как «я не есть мое тело» или «я не есть
                мое сознание», имеют по крайней мере два смысла. В первом смысле, <span class="underline">объективном</span>, эти
                суждения будут означать, что никакой из феноменов, <em>дхарм</em>, включая и тело, и чувства,
                и ум, не есть (или не может быть) абсолютным Я, то есть <em>атманом</em>. Во втором смысле,
                <span class="underline">субъективном</span>, те же самые суждения будут значить следующее: то, что я обозначаю
                словом «я», когда говорю, «я говорю», «я знаю», «я чувствую», не является ни (моим)
                телом, ни (моим) сознанием, ни (моими) чувствами, а является чем-то еще, чем-то,
                скажем, иным тем ... иным, чем <span class="underline">что</span>? — <span class="underline">Иным, нежели что бы то ни было</span>. Эти два
                смысла, объективный и субъективный, не могут быть сведены ни друг к другу, ни к
                чему-то третьему, двумя частными случаями которого они бы являлись. Ведь когда
                мудрецы Упанишад придумали священную формулу «ты есть то», то есть «ты есть <em>атман</em>»,
                то она вполне могла пониматься в наипростейшем смысле: ты — не твое тело, не твой
                ум, не твое имя, не твой клан, не твое племя, не <span class="underline">что бы то ни было, что не есть То</span>.
                Именно на это древние буддийские мудрецы возражали: «Ты ни есть, ни не есть <em>То</em>, ибо
                как можно определить нечто (хотя бы предположительно существующее) через то, чему
                невозможно приписать ни существования, ни не-существования». Здесь, по существу, мы
                уже имеем дело (как и в случае со «срединностью» в первом семинаре) с другой
                философской позицией, которая, собственно, и была обозначена понятием «Не-Я».
                Особенно явно это различие между буддийским и брахманистским философствованием
                проявилось не столько в начальных постулатах, утверждавших (в брахманизме) и
                отрицавших (в буддизме) «Я», сколько в характере и <span class="underline">правилах вывода</span> из этих начальных
                постулатов. Так, в то время как в нашем тексте утверждается, что ты не можешь ничего
                сделать со своим телом и умом, ибо они не есть «Я», в ранних прозаических Упанишадах
                утверждается, что ни ты, ни что другое не могут ничего сделать с <span class="underline">тобой</span>, ибо ты есть
                то, <em>атман</em>. Эта антитеза дополняется в нашем тексте важнейшим философским
                утверждением — то, что не есть, является невечным, непостоянным, беспрерывно
                изменяющимся, а все то, что невечно, непостоянно, беспрерывно изменяется — <span class="underline">страдает</span>.
                Так «Не-Я», второй <em>универсальный знак</em>, связывается в этой проповеди с невечностью
                (непостоянством, изменчивостью), первым универсальным знаком и со страданием,
                третьим универсальным знаком всех дхарм. Притом что порядок вывода здесь — от
                второго знака к первому, а от первого — к третьему.</p>
            <p>
                Из всего сказанного вытекает, что «Не-Я» не является простым отрицанием абсолютного
                Я, <em>атмана</em> Упанишад, также и потому, что они принадлежат двум разным уровням
                философской абстракции. Понятие <em>атмана</em> возникло как чисто мифологическая идея (и
                образ) в ритуалистическом, жреческом ведийском контексте. Только много позднее оно
                было философски осмыслено, проработано в ранних брахманистских текстах,
                непосредственно восходящих к ведийским комментариям. В Упанишадах <em>атман</em> уже во
                многом утерял свое ведийское ритуально-мифологическое прошлое и стал основным
                онтологическим понятием брахманистской философии. Буддийское понятие «Не-Я» уже в
                самом своем возникновении было чисто философской идеей, придуманной и быстро
                получившей распространение не в жреческой, а в <span class="underline">аскетической</span> среде, и было наиболее
                тесно связано не с ритуалом, а <span class="underline">йогой</span>.
            </p>
            <p>
                Теперь попробуем пофилософствовать по поводу «Не-Я», как оно фигурирует во второй
                проповеди Будды, стараясь при этом — насколько это возможно — избежать ставших уже
                привычными тривиальных интерпретаций, в которых любое рассуждение определяется
                такими банальными формулами, как «само собой разумеется, что...» или «из этого с
                очевидностью следует, что...», ибо самое трудное и самое интересное в буддийской
                философии далеко не всегда «разумеется само собой» и еще реже «с очевидностью
                следует» из того, что уже было сказано. Начнем с двух чисто гипотетических
                предпосылок.
            </p>
            <p>
                <em>(1)</em> Поскольку «Не-Я», как универсальный знак, фигурирует во второй
                проповеди в своем
                отношении к непостоянству и страданию, можно было бы предположить, что «Не-Я» не
                относится к случаям, когда непостоянство и страдание <span class="underline">не возникают</span>. Это ослабляет
                определенность «Не-Я» как понятия и вместе с тем делает неопределенной сферу
                феноменов, признаком которых «Не-Я» служит, иными словами, делает «Не-Я» менее
                универсальным. Но именно это не дает «Не-Я» стать абсолютом, крайностью, об
                опасности впадения в которую предупреждает первая проповедь. Но что же это за
                случаи, когда непостоянство и страдание не возникают? Люди ли это, личности или
                какие Другие существа?
            </p>
            <p>
                Так мы возвращаемся к вопросам нашего семинара, «Почему это — не я?» или «Почему я —
                не это?». Ответ на первый вопрос ясен и прост: потому, что ты в чисто условном
                (лингвистическом) смысле есть не что иное, как пять агрегатов, скандх
                индивидуального существования. Ответ на второй вопрос гораздо более сложен и
                гипотетичен по своему содержанию: «Ты — не это, потому что не являешься исключением
                из всего того, что есть «Не-Я» (<em>anatta</em>), т.е. ты не являешься исключением из
                универсального правила существования всего того, что существует. Более того, строго
                говоря, ты не являешься даже и субъектом страдания и изменчивости — ибо, как нам уже
                известно, это «твое» тело, «твои» чувства, воля и так далее, изменяются и страдают,
                те пять агрегатов, которые лишь условно «приписаны» тебе как к фиктивному
                обладателю. Однако ты можешь спросить: «А что будет в случае, если я уже стал
                <em>Архатом</em>, Пробужденным, Буддой?» Ответ таков: «В этом случае никакие пять агрегатов
                тебе не смогут быть приписаны, поскольку в Нирване невозможно существование
                эмпирического «Я» с его именем, формой и сознанием, а «Не-Я» отменяется, так же,
                впрочем, как и абсолютное Я».
            </p>
            <p>
                <em>(2)</em> «Не-Я» рассматривается в буддийской комментаторской традиции как
                наиболее
                труднопонимаемая идея во всей буддийской философии. Это объясняется, во-первых, тем,
                что в отношении «Не-Я» невозможен никакой феноменологический (так же, впрочем, как и
                в отношении абсолютного «Я» — <em>атмана</em> Упанишад) подход. Во-вторых, исключительная
                трудность понимания «Не-Я» объясняется еще и тем, что это понятие получает свое
                радикальное философское переосмысление в концепции, которую можно было бы назвать
                <span class="underline">концепцией чистого отсутствия</span>. Последняя обрела свое развитие и конкретизацию
                позднее — в центральной философской идее Пустоты, в которой ясно прочитывается
                «Не-Я» как источник ее происхождения. Скорее всего, именно <span class="underline">относительная
                позитивность</span> «Не-Я» явилась причиной того, что в более поздних концепциях чистого
                отсутствия и Пустоты категорически — как ложная крайность — отрицается абсолютная
                негативность в любой ее форме.
            </p>
            <p>
                Исходя из сказанного, я бы рискнул закончить семинар предположением, что «Не-Я»,
                <em>анатта</em> (пали <em>anattā</em>), в буддийской философии является своего рода
                <span class="underline">трансцендентальной переменной</span>, в то время как
                абсолютное Я, <em>атман</em>, в брахманистской философии играет роль
                <span class="underline">трансцендентальной постоянной</span>.
            </p>

