                    ПРИМЕЧАНИЯ</h4>
                <p class="mb-3" id="citation-1"><sup>1</sup> Ādittapariyāya-sutta (Saṃyutta-nikāya
                    XXXV.28). Pali Text Society ed. Vol. IV, p.19. Название означает «Наставление о
                    горящем» (от āditta — «горящий» + pariyāya — «наставление»). Текст представляет
                    собой классическое объяснение природы страдания через метафору шести чувственных
                    основ как «объятых пламенем».<a href="#ref-1"
                                                    class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-2"><sup>2</sup> «Зрительные формы» (пали <em>rūpa</em>,
                    санскр. <em>rūpa</em>) — это видимые явления, воспринимаемые зрением, но не просто
                    объекты зрения (<em>ārammaṇa</em>), а их более элементарные, физические
                    составляющие. В Абхидхамме они классифицируются на:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>Первичные (<em>nibbatika</em>) — такие как цвет и форма.</li>
                    <li>Вторичные (<em>panibbatika</em>) — производные качества, например движение
                        или твёрдость.
                    </li>
                </ul>
                <p>Хотя в повседневном языке под «формой» может пониматься целостный объект, в
                    буддийской психологии и эпистемологии <em>rūpa</em> обозначает скорее
                    элементарную
                    структуру чувственного опыта, чем концептуальное восприятие. Зрительные формы
                    считаются наиболее выраженной формой материи и даже отождествляются с телом в
                    широком смысле, что может быть связано с доминирующей ролью зрения среди органов
                    чувств.<a href="#ref-2" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-3"><sup>3</sup> «Зрительное сознание» (пали <em>cakkhu-viññāṇa</em>)
                    — один из шести типов сознания, возникающий при взаимодействии трёх условий:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>Глаза (<em>cakkhu</em>) как органа восприятия.</li>
                    <li>Видимого объекта (<em>rūpa</em>).</li>
                    <li>Направленного внимания (<em>manasikāra</em>).</li>
                </ul>
                <p>Каждому из шести органов чувств соответствует свой вид сознания: зрительное,
                    слуховое, обонятельное, вкусовое, осязательное и умственное. Эти формы сознания
                    следует отличать от более общего понятия — <em>viññāṇa-khandha</em>, или
                    агрегата сознания,
                    который охватывает все ментальные процессы как составную часть индивидуального
                    существования.<a href="#ref-3" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-4"><sup>4</sup> «Зрительный контакт» (пали <em>cakkhu-samphassa</em>,
                    от <em>samphassa</em>; санскр. <em>samsparśa</em>) — это момент взаимодействия
                    между:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>Органом зрения (глазом, <em>cakkhu</em>).</li>
                    <li>Объектом зрения (формой, <em>rūpa</em>).</li>
                    <li>Сознанием (<em>viññāṇa</em>).</li>
                </ul>
                <p>Этот контакт (<em>phassa</em>) является важным звеном в цепи условного
                    возникновения
                    страдания (<em>paṭiccasamuppāda</em>) и служит основой для возникновения чувства
                    (<em>vedanā</em>), желания или отвращения.<a href="#ref-4"
                                                               class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-5"><sup>5</sup> «Любая эмоция» здесь относится к так
                    называемым «чувствам» (пали <em>vedanā</em>) — второму из пяти агрегатов
                    индивидуального существования. Под <em>vedanā</em> понимается не только
                    эмоциональная окраска опыта, но и любое субъективное ощущение, возникающее в
                    результате контакта органа чувств с объектом через соответствующее сознание.
                    Чувства делятся на три или пять типов и служат основой для возникновения
                    цепляния и страдания (см. I, прим. 25).<a href="#ref-5"
                                                              class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-6"><sup>6</sup> См. I, прим. 25.<a href="#ref-6"
                                                                                class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-7"><sup>7</sup> «Ненависть» (пали <em>dosa</em>, санскр.
                    <em>dveṣa</em>) — одна из трёх коренных неблагих причин (<em>mūla-kilesa</em>),
                    наряду с
                    жаждой (<em>lobha</em>) и заблуждением (<em>moha</em>). Проявляется как гнев,
                    отвращение,
                    раздражение или враждебность, и считается главной негативной силой ума,
                    препятствующей внутренней гармонии и ясному восприятию.
                    В Абхидхамме <em>dosa</em> анализируется как отдельная психическая формация
                    (<em>cetasika</em>), которая:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>противопоставляется любящей доброте (<em>mettā</em>),</li>
                    <li>устраняется развитием терпения (<em>khanti</em>) и прощения,</li>
                    <li>создаёт условия для страдания и неблагих действий (<em>akusala-kamma</em>).
                    </li>
                </ul>
                <p>Это состояние является одной из основных преград на пути духовного очищения и
                    просветления.<a href="#ref-7" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-8"><sup>8</sup> «Заблуждение» (пали <em>moha</em>,
                    санскр. <em>moha</em>) — фундаментальная помрачённость и искажение ума, являющаяся
                    корнем всех неблагих состояний. Оно проявляется как:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>неведение (<em>avijjā</em>) относительно Четырёх Благородных Истин,</li>
                    <li>естественная склонность к ошибочному восприятию реальности,</li>
                    <li>умственная пассивность, лень и неприязнь к знанию.</li>
                </ul>
                <p>Вместе с жаждой (<em>lobha</em>) и ненавистью (<em>dosa</em>), <em>moha</em>
                    образует три «яда»
                    (<em>triviṣa</em>), порождающих страдание и поддерживаемые циклом перерождений (<em>saṃsāra</em>).
                    Побеждается развитием мудрости (<em>paññā</em>).<a href="#ref-8"
                                                                       class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-9"><sup>9</sup> «Тело» (пали <em>kāya</em>, санскр. <em>kāya</em>)
                    здесь понимается как орган осязания — то есть как физическая основа для
                    восприятия тактильных ощущений. В отличие от обычного бытового представления о
                    теле как об объекте ощущения, в буддийской психологии тело выступает субъектом
                    восприятия, наряду с глазом, ухом и другими органами чувств. Телесное восприятие
                    — это не просто реакция на
                    внешние прикосновения, но сложный процесс взаимодействия чувственного элемента
                    (<em>phoṭṭhabba</em>), сознания (<em>viññāṇa</em>) и внимания
                    (<em>manasikāra</em>).<a href="#ref-9"
                                             class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-10"><sup>10</sup> «Ум» (пали <em>mano</em>, санскр. <em>manas</em>)
                    выполняет двойную функцию в буддийской психологии:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>Как шестой орган чувств (<em>manāyatana</em>), он воспринимает ментальные
                        объекты (<em>dhammā</em>) — такие как мысли, эмоции, идеи и другие явления
                        ума.
                    </li>
                    <li>Как элемент пятого агрегата — сознания (<em>viññāṇa-khandha</em>) — он часто
                        используется как его синоним или инструмент.
                    </li>
                </ul>
                <p>Кроме того, ум играет роль координатора всех органов чувств и служит «ментальной
                    дверью» (<em>manodvāra</em>) для возникновения сознательного опыта. В этом
                    смысле он не
                    просто рефлексивная способность, но и активный участник процесса восприятия и
                    формирования реакций на внешний и внутренний опыт.<a href="#ref-10"
                                                                         class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-11"><sup>11</sup> «Объектами ума» (пали <em>dhammārammaṇa</em>,
                    от <em>dhamma</em>) здесь называются явления, воспринимаемые шестым органом чувств
                    — умом (<em>mano</em>). Они включают не только мысли и идеи, но и:</p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li>эмоции,</li>
                    <li>абстрактные понятия,</li>
                    <li>ментальные образы,</li>
                    <li>состояния сознания,</li>
                    <li>духовные объекты медитации.</li>
                </ul>
                <p>В отличие от чувственных объектов, они не связаны напрямую с внешними органами
                    восприятия, но всё же формируют содержание сознания и могут вызывать цепляние
                    или
                    освобождение.<a href="#ref-11" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-12"><sup>12</sup> Здесь термин «сознание» следует
                    понимать не как пятый агрегат индивидуального существования
                    (<em>viññāṇa-khandha</em>), охватывающий все ментальные процессы, а именно как
                    особую разновидность ума — <em>умственное познавательное функционирование</em>,
                    связанное с обработкой ментальных объектов (<em>dhammā</em>). В Абхидхамме такое
                    сознание рассматривается как моментальная психическая формация (<em>citta</em>),
                    возникающая в зависимости от условий и служащая основой для мышления, восприятия
                    и чувств.<a href="#ref-12" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-13"><sup>13</sup> В данном контексте термин
                    «Благородный» используется в строго терминологическом смысле: либо как <em>Пробуждённый</em>,
                    познавший <em>Четыре Благородные Истины</em>, либо как <em>Архат</em> —
                    полностью освобождённый от всех загрязнений (<em>āsava</em>) и внутренних
                    помрачений ум
                    (<em>klesa</em>). Такое использование отличается от обычного значения слова
                    «благородный» как этически добродетельный или уважаемый человек.<a
                        href="#ref-13" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-14"><sup>14</sup> См. I, прим. 36.<a href="#ref-14"
                                                                                  class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="mb-3" id="citation-15"><sup>15</sup> «Скверна» здесь является одной из
                    конкретизаций понятия <em>умственный приток</em> (<em>āsava</em>) — глубинных
                    загрязнений ума, порождающих цикл перерождений (<em>saṃsāra</em>). Среди них выделяются
                    четыре основные:</p>
                <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
                    <li><em>kāmāsava</em> — приток чувственности,</li>
                    <li><em>bhavāsava</em> — приток становления, или стремление к существованию,</li>
                    <li><em>diṭṭhāsava</em> — приток воззрений, связанный с умственными концепциями
                        и догмами,
                    </li>
                    <li><em>avijjāsava</em> — приток неведения, фундаментальное непонимание
                        реальности.
                    </li>
                </ol>
                <p>Полное уничтожение всех <em>āsava</em> знаменует достижение архатства и освобождение
                от страдания. В трактате <em>Висуддхимагга</em> они сравниваются с «вытекающими
                ядами», которые заливают ум и затрудняют просветление.<a href="#ref-15"
                                                                         class="text-blue-500 hover:underline">↩</a></p>
                <p class="mb-3" id="citation-16"><sup>16</sup> Encyclopaedia of Buddhism. Vol. I . Fasc. II. Ceylon, 1963. P. 225-226.<a href="#ref-16"
                                                    class="text-blue-500 hover:underline">↩</a></p>
