                состояния сознания в виде соотношения V.</p>

            <p>В свете постулированного соотношения V можно интерпретировать и известные буддийские
                негативные формулировки типа «нет такой дхармы, как «живое существо», «нет такой
                дхармы, как «душа». В этом случае может иметься в виду, что когда о чем-то
                говорится, что такой дхармы нет, то это означает, что ее нет в <em>универсальном
                    наборе
                    дхарм</em>. Вместе с тем, соотношение V можно рассматривать как <em>выход,
                    переход</em> в область
                более широкого набора дхарм, включающего в себя S<sub>u</sub>, либо как переход в
                область
                <em>другого</em> набора дхарм, не пересекающегося с S<sub>u</sub>.</p>
            <p>5. И, наконец, буддологические исследования последних лет позволяют, опираясь на
                установленное Розенбергом соотношение феноменологического и абсолютного аспекта
                дхарм, предположить о возможности того, что буддийская философия в <em>принципе</em>
                допускала возможность существования некоторого абсолютного «йогического «Я», как
                субстрата иллюзорного «Я» мгновенных состояний сознания<a href="#citation-29"
                                                                          id="ref-29"
                                                                          class="citation-mark"><sup>29</sup></a>.
            </p>
            <p>
                Я столь подробно остановился на изложении Розенбергом теории дхарм именно из-за
                того, что она является ядром и основой всей и всякой буддийской философии. Но, в то
                же время, следует иметь в виду, что анализ этой Теории — <em>условен</em>,
                рассмотрение ее по аспектам (статическому, динамическому и т. д.) и сторонам
                (психологической, этической и т. д.) допустимо лишь в известных пределах. «В
                сущности, — заключает Розенберг свой раздел о теории дхарм, — теория дхарм
                составляет целую системы мировоззрения, и разложение ее на психологию и
                гносеологию, с одной стороны, и на метафизику и теорию спасения, — с другой, сделано
                исключительно в интересах большей наглядности изложения» (стр. 124).

            </p>
            <p>
                Теперь я остановлюсь на чрезвычайно важном высказывании Розенберга
                о характере буддийской философской терминологии. Розенберг устанавливает
                в качестве непреложного правила при изучении любого буддийского философского
                текста <em>двойственное понимание смысла</em> философских терминов. Каждый термин
                может иметь
                <em>первичное значение</em> (как, например, для слова «индрия» первичное
                значение чисто физиологическое — «орган чувств», «анализатор»), и <em>вторичное</em>,
                уже <em>специфически буддийское</em> значение («индрия» уже в
                <em>психологическом</em>
                значении — «психическая способность»)<a href="#citation-30" id="ref-30"
                                                        class="citation-mark"><sup>30</sup></a>. Но
                здесь подчеркивается <em>именно двойственность</em>, то есть то обстоятельство, что, зная специфически буддийское значение
                термина, мы одновременно должны иметь в виду его «первое» значение<a href="#citation-31" id="ref-31" class="citation-mark"><sup>31</sup></a>. Причем из
                текста самого по себе это различие далеко не всегда явствует. Я думаю, что
                большинство буддийских философских текстов изучалось учениками только под
                руководством учителя; теперь же для нас живой язык этих текстов — исчез — остались
                тексты!

            </p>
            <p>
                Мне представляется, однако, возможным считать розенберговский принцип
                двойственности «принципом множественности понимания», ибо внутри самой буддийской
                философии почти всякий важный термин может одновременно иметь более одного
                специального значения.
            </p>
            <p>Вторая часть книги, посвященная разбору классификаций дхарм и соотношений дхарм друг
                с другом, носит более частный буддологический характер и потому не
                представляет столь же большого интереса для философов-небуддологов, как первая. Но и
                она ярко талантлива и конкретна. Остановлюсь лишь на одном наиболее общем положении
                этой части.</p>
            <p>С точки зрения Розенберга классификация дхарм <em>не-формальна по преимуществу</em>.
                Существование каждого живого существа (напоминаю, что живое существо в буддийской
                теории дхарм есть длящийся континуум мгновенных вспышек дхарм-состояний сознания)
                регулируется рядом <em>генеральных естественных тенденций</em>. Например: тенденция к
                возмущению, замутнению психики (сасрава), тенденция к невозмущению психики, к ее
                деэнтропизации (анасрава), тенденция к неизменению, к инерции бытия (санскрита),
                тенденция к изменению, к прекращению феноменального бытия (асанскрита)<a
                    href="#citation-32" id="ref-32" class="citation-mark"><sup>32</sup></a>, и т. д.
                Дхармы же как бы естественным образом группируются по этим тенденциям, и эта
                группировка <em>как бы отражает</em> «селективную ассимиляцию» индивидом тех или иных дхарм,
                в зависимости от того, какие тенденции регулируют психическую жизнь этого индивида.
            </p>
            <p>B заключение позволю себе выразить глубокую признательность О. Ф. Волковой, Д. Е.
                Бертельсу и В. Рудому за советы и сведения.</p>
