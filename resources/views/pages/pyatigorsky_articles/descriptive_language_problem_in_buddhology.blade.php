                истории буддийской или индийской философии.</p>
            <p>Я полагаю, что понимание философии буддизма предшественниками и современниками
                Розенберга имело три существенных недостатка, которые и предопределили их полный или
                частичный неуспех в этой области.</p>
            <p>Во-первых, почти все они были склонны отождествлять общую концепцию буддизма с его
                философией, в то время как на самом деле в буддизме философия существовала как
                <em>особая, в высшей степени специализированная</em> дисциплина. Дисциплина,
                овладение
                которой предполагало высокую ступень йогического тренинга адепта. Дисциплина, само
                занятие которой предполагало уже и относительно высокую степень эзотерической
                посвященности адепта. И, наконец, философия являлась (равно как для хинаянских, так
                и для махаянских школ) дисциплиной, <em>строго ориентированной на определенный
                    раздел корпуса буддийских канонических текстов</em> — а именно на «абхидхарму» и
                связанную с ней комментаторскую и схоластическую литературу. Два других раздела —
                «Сутра» и «Винайя»,
                хотя и содержат некоторые философские идеи, никак не могут рассматриваться как
                первоисточник в отношении древней буддийской философии, а лишь как производный и
                вспомогательный материал. Однако до начала ХХ века почти все исследователи буддизма
                описывали его философию прежде всего на основании раздела «сутр», безусловно гораздо
                более доступного лингвистически и гораздо более понятного по содержанию, ибо, строго
                говоря, тексты раздела «Абхидхармы», по-видимому, вообще могли изучаться только при
                помощи и под руководством опытного наставника. Розенберг был первым, кто сделал опыт
                систематического описания именно буддийской философии (а не буддизма вообще!),
                основываясь на «Абхидхарме» и производных комментариях и трактатах.</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                Главная причина неправильного понимания буддийской философии, — писал Розенберг,
                кроется, конечно, в том, что литература систематических трактатов долгое время
                оставалась неизученной (стр. 65).
            </blockquote>
            <p>
                Ему удалось тщательно изучить только небольшую часть этих трактатов, но он достиг
                правильного их понимания.
            </p>
            <p>Во-вторых, предшественники и современники Розенберга постоянно смешивали при описании
                буддийской философии философскую и религиозную стороны буддизма. Это зачастую
                приводило (и приводит) к весьма парадоксальным выводам, уж слишком тесно связанным с
                мировоззрением или вероисповеданием самого исследователя. Так, будучи типичным
                прогрессистом либерального толка, Эжен Бюрнуф считал в своем «Введении» ранние
                махаянские сутры смешением мифов и суеверий. Католик Де ла Валле Пуссэн считал
                философию буддизма «ложной теологией»<a href="#citation-4" id="ref-4"
                                                        class="citation-mark"><sup>4</sup></a>, г-жа
                К. Рис Дэвис (в «дорозенберговский» период) оценивала ее как этическое и
                психологическое учение (что, кстати, вполне согласуется с типичным для ее времени и
                среды англиканско-протестантским агностицизмом), а позитивистски и атеистически
                настроенный интеллигент Ф. И. Щербатской по существу вовсе отвергал самостоятельную
                религиозную сторону буддизма<a href="#citation-5" id="ref-5"
                                               class="citation-mark"><sup>5</sup></a>, полагая
                буддизм некой рафинированной философией, сродни бергсонианству. И только Розенберг
                глубоко понимал то важнейшее обстоятельство, что буддизм одновременно выступает <em>на
                    нескольких уровнях</em> — на уровне «чистого философского умозрения», на уровне
                культа, на
                уровне йоги, на уровне бытовой этики. И он ясно осознавал необходимость раздельного
                аналитического исследования буддизма по этим уровням<a href="#citation-6" id="ref-6"
                                                                       class="citation-mark"><sup>6</sup></a>.
                Он верно почувствовал принципиальную несводимость буддизма к одному уровню, равно
                как и невозможность синтетического рассмотрения и описания буддизма на всех уровнях
                одновременно. И, может быть, именно многолетнее пребывание молодого Розенберга в
                Японии, в специфически буддийской среде, открыло ему сначала <em>прагматический</em>
                аспект
                буддизма и показало полную несуразность всех попыток рассмотрения одной стороны
                буддизма как формы, а другой — как соотносимого с ней содержания (скажем, религия —
                форма, философия — содержание, или этика — форма, психология — содержание, или культ
                —
                форма, мифология — содержание. Такой набор «дихотомических» пошлостей имеет по сю
                пору широкое хождение). Ибо всякая выделенная в буддизме сторона прагматически
                соответствует определенному уровню, и в зависимости от того, какую именно сторону
                буддизма мы берем, меняется и иерархия уровней. На уровне этики в буддизме одна
                иерархия, на уровне философии — другая, а на уровне йоги — третья. Таким образом
                Розенбергу была очевидна бессмысленность описания «буддизма вообще»,
                абстрагированного от конкретного уровня, в данном случае — от уровня философии.
                (Впрочем, однако, я допускаю возможность и иного подхода: кроме описания «по
                уровням» был бы весьма интересен и продуктивен опыт описания буддийской философии на
                основе <em>отдельного конкретного текста</em> — но это уже другой вопрос).</p>
            <p>В-третьих, почти все буддологи до Розенберга (и очень многие — после него) исходили
                при описании буддизма из определенных академических (как правило
                немецко-академических) историко-философских трафаретов, полагая их <em>универсальными</em>
                клише при создании практически <em>любого текста</em> по истории философии. Так, о
                каком бы древнем философе ни шла речь, в тексте будут представлены рубрики: «учение
                о бытии», «учение о душе», «этика», «теория познания». И несмотря на то, что буддизм
                в отношении такой рубрики материал наинеблагодарнейший, она все равно буддизму
                насильно навязывается едва ли не всеми авторами<a href="#citation-7" id="ref-7"
                                                                  class="citation-mark"><sup>7</sup></a>.
                А ведь онтология в буддизме не была эксплицирована, хотя и могла быть косвенно
                выведена. И не могло быть учения о душе, потому что буддисты полагали, что ее нет. А<em>вместо</em>
                (если можно так выразиться!) онтологии была <em>чистая психология</em>. Но
                большинство историков буддийской философии «успешно» справлялись с этими
                трудностями. Они полагали, что отрицание души и есть сущность буддийского учения о
                душе, что психологический подход и есть <em>особый вариант</em> онтологии, и т. д.
                Ничего подобного, разумеется, в самих буддийских текстах не было. Розенберг же мог
                просто не знать этих клише, но зато он превосходно знал абхидхармистские тексты<a
                    href="#citation-8" id="ref-8" class="citation-mark"><sup>8</sup></a>и написал по
                ним книгу «как есть»<a href="#citation-9" id="ref-9"
                                       class="citation-mark"><sup>9</sup></a>.
            </p>
            <p>Так что же «есть» в буддийской философии по книге Оттона Оттоновича Розенберга?</p>
            <p>Прежде всего, согласно Розенбергу, в буддийской философии содержится совершенно
                особое и непохожее ни на какое другое понимание <em>самой себя</em>. Это понимание
                сохраняется почти неизменным, идет ли речь о палийском каноне, санскритском каноне,
                или о произведениях двух крупнейших буддийских теоретиков раннего индийского
                средневековья Васубандху и Асанги, и его суть состоит в следующем: буддийский
                философ, если он на самом деле буддийский философ, не может считать <em>свою</em>
                (то есть
                буддийскую) философию единственно правильной, или даже самой правильной
                <em>вообще</em>. Он,
                напротив, должен полагать, что она правильна лишь для него, ибо соответствует складу
                его психики (на санскрите — <em>свабхава</em>), его судьбе (<em>карма</em>), и его
                (опять-таки, только
                его) духовному пути (<em>марга, патха</em>). И только в этом смысле он может считать
                свою
                философию истинной или правильной. Развивая последовательно эту релятивистскую
                идею, мы без большой натяжки могли бы заключить, что с точки зрения буддизма не
                только буддийская, но и всякая философия есть вещь, которой одним людям следует
                заниматься, а другим безусловно не следует ни в коем случае<a href="#citation-10"
                                                                              id="ref-10"
                                                                              class="citation-mark"><sup>10</sup></a>.
            </p>
            <p>Однако, еще более удивительно (и это особо подчеркивается Розенбергом в 4-ой
                главе), что этот своеобразный релятивизм прилагается в буддизме и к решению
                конкретных метафизических вопросов. Когда философские оппоненты Будды (в основном
                последователя санкхьи, вайшешики и ранних брахманистских школ) спрашивали его,
                «существует ли атман (то есть «Я», «душа»), то Будда (согласно каноническим текстам)
                отвечал отрицательно. Розенберг же совершенно верно и в абсолютном соответствии с
                текстами Васубандху пишет: «... не существует <em>в том смысле</em>, как полагают
                <em>обыкновенные</em> люди и оппоненты Будды» (стр. 57). А существует ли
                «<em>вообще</em>»? нет,
                потому что, согласно самой сущности буддийского умозрения, ни одно утверждение не
                имеет смысла «вообще». Исследование Розенбергом Абхидхармакоши и Виджнянаматрашастры
                ясно показывает и совершенно иное отношение философии буддизма к самой <em>идее
                    спора</em>,
                нежели то, какое мы встречаем у большинства античных мыслителей.
                Сократо-платоновская традиция понимания спора такова, что <em>истина</em>, если она
                и не одна
                для спорящих сторон, <em>то одна в споре</em> (отсюда и распространеннейший по сю
                пору
                античный предрассудок, что «истина рождается в споре»). Для буддизма же самым
                существенным была проблема понимания <em>субъекта</em>, а не проблема
                <em>предикации</em>. Так, спор о
                том, добр человек по природе или зол, абсолютно бессмыслен, поскольку одна из
                сторон полагает, что «нет такого понятия, как <em>человек</em>». Равно бессмысленно
                для
                буддизма спорить о существовании души: есть ли она, или нет — неустановимо, так как:
                1) возможно, что спорящие неодинаково понимают слово «душа», 2) возможно, что
                спорящие неодинаково понимают слово «есть», 3) возможно, что для одной из спорящих
                сторон вообще не существует <em>слов</em> «душа» или «есть», 4) в случае же, если
                обе стороны
                понимают и «душа», и «есть» одинаково, можно сказать о «душе», что она «у одних
                есть», а «у других — нет». Таким образом, согласно буддийскому пониманию спора,
                <em>ответ</em> может быть отрицательным, положительным, отрицательно-положительным и
                ... ответа
                может не быть вообще (и это тоже ответ).</p>
            <p><em>Исходной точкой буддийской философии</em> был, согласно Розенбергу, анализ
                человека, но не
                в отношении физиологии («... Для буддизма материальная природа должна была иметь
                гораздо меньше значения, чем для брахманизма», стр. 67), а в отношении психологии
                и преимущественно и прежде всего — <em>психологии сознания</em>. Брахманистская и
                частично
                раннебуддийская схоластическая традиция в своем анализе исходила из того, что
                психика, так же, как и объект, на который направлена психика — <em>равно
                    материальны</em>, то
                есть <em>анализируются</em> аналогичным (если не идентичным) образом. При этом,
                скажем,
                органы чувств (на санскрите — <em>индрии</em>) мыслятся как некоторые элементарные в
                отношении сознания, реальные, телесные механизмы. Однако, начиная уже с ранних
                абхидхармистских текстов, содержание анализа значительно изменяется. Органы из
                материальных механизмов, воспринимающих материальные же объекты собственного тела
                и окружающего мира, превращаются в «<em>способности</em>», «<em>специализированные
                    силы</em>»,
                воспринимающие уже не материальные объекты, а «зримое», «слышимое», «обоняемое»,
                «вкушаемое», «осязаемое» и, соответственно, акцент в психологическом анализе
                перемещается сначала с объекта психики (а более точно — <em>сознания</em>) на
                субстрат, а затем
                с субстрата на сам акт сознания. Если взять классический пример, фигурирующий в
                текстах вайбхашиков (поздняя хинаянская школа, к которой сначала принадлежал и
                Васубандху) — «Человек смотрит на солнце», то буддийская теория не будет заниматься
                ни
                «тем, на что смотрит» (солнце), ни «смотрящим» (человек), но только «смотрит» (то
                есть психическим актом «смотрения»). При этом такие феномены, как «мышление»,
                «мысль», «мыслимое», «восприятие», «воспринимаемое», «память», «помнимое»,
                «ощущение», «ощущаемое», оказываются в соответствующем порядке <em>иерархически
                    подчинены сознанию как уровню, где целостно, синтетически реализуется
                    психическая
                    деятельность вообще</em>. Это изменение буддийского психологического анализа не
                было ни «переворотом», ни
                «поворотом». Оно совершалось постепенно от абхидхармистских разделов канона (4-3 вв.
                до н. э.) до трактатов вайбхашиков (2-3 вв. н. э.) и завершилось в уже
                принадлежащих Большой Колеснице (Махаяне) учениях — <em>праджняпарамите</em> (1-3
                вв. н. э.),
                <em>шуньяваде</em> (или <em>мадхьямике</em> — 3-4 вв.) и <em>виджнянаваде</em> (или
                <em>йогачаре</em> — 4-6 вв.).</p>
            <p>Но менялось не только содержание буддийского психологического анализа. Существенно
                изменились его задачи, его функция, его мотивация, его прагматика. Изменение его
                объекта выразилось в конечном счете в интереснейшем явлении: превратившись из науки
                о «психическом объекте» в науку о «психическом акте», буддийская психология в конце
                концов (т. е. в период праджняпарамитских, мадхьямских и йогочаринских текстов)
                превратилась в «науку об осознании акта осознания», иначе говоря, в «науку о самой
                себе», т. е., применяя современную лингвистическую терминологию, она стала своего
                рода <em>метатеорией</em>. Отсюда — труднейшая задача различного истолкования
                специальных
                буддийских терминов, ибо один и тот же термин приобретает совершенно иной смысл в
                зависимости от того, принадлежит ли он к языку-объекту или языку описания. Поскольку
                же основной прагматической целью буддийской философии было и осталось освобождение
                от уз и омраченности феноменального существования, то на каждом этапе своего
                развития она определенным образом модифицировалась в соответствии с сотэрологией.
                (Вместо слова «освобождение» Розенберг по прежней буддологической традиции пишет
                «спасение». Однако различие в словах здесь несущественно, если мы отвлечемся от
                привычных христианских и античных ассоциаций, стр. 75-80).
            </p>
            <p>Большая часть книги Розенберга посвящена <em>теории дхарм</em>, которую он считает
                сущностью и
                основой всего буддийского умозрения вообще. Я склонен думать, что изложение теории
                дхарм является одновременно самым слабым и самым сильным местом в книге. Слабым,
                потому что Розенберг не мог достичь полного и правильного понимания дхарм, не зная
                канонических абхидхармистских текстов и праджняпарамитских сутр. Сильным, потому что
                и не зная их, Розенберг подошел к правильному пониманию дхарм<a href="#citation-11"
                                                                                id="ref-11"
                                                                                class="citation-mark"><sup>11</sup></a>.
            </p>
            <p>Так что же такое дхарма? Суммируя все сказанное о дхармах у Васубандху, Розенберг
                дает следующие значения этого слова:</p>
            <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
                <li class="my-2">качество, атрибут, предмет;</li>
                <li class="my-2">субстанциональный носитель, трансцендентный субстрат единичного
                    элемента сознательной жизни;
                </li>
                <li class="my-2">элемент, то есть составной элемент сознательной жизни;</li>
                <li class="my-2">нирвана, то есть дхарма, объект учения Будды;</li>
                <li class="my-2">абсолютное; истинно-реальное и т. п.;</li>
                <li class="my-2">учение, религия Будды;</li>
                <li class="my-2">вещь, предмет, объект, явление.</li>
            </ol>
            <p>Уже сам этот список показывает, что Розенберг, совершенно справедливо полагая дхарму
                универсальной категорией буддийской философии, был склонен приписывать слову
                «дхарма» универсальное значение. Более того, этот список показывает, что дхарма,
                понимаемая в самом широком смысле слова, есть <em>всякий объект</em> (но,
                разумеется,
                поскольку нечто <em>уже стало объектом</em>). В несколько менее широком смысле слова
                она есть
                всякий внешний, равно как и внутренний (то есть относящийся к психике, сознанию)
                объект, но поскольку он является объектом уже не вообще, а объектом, мыслимым именно
                в сфере буддийской философии, в абхидхарме (то есть в той, третьей части буддийского
                канонического корпуса текстов, в которой трактуются и описываются дхармы). Таким
                образом, по Розенбергу, «дхарма вообще» является элементом (точнее, элементарной
                единицей) сознательного бытия, а абхидхарма рассматривает разновидности, <em>состав
                    этого бытия</em> (стр. 92-93). Таков, по Розенбергу, чисто статический аспект
                теории
                дхарм<a href="#citation-12" id="ref-12"
                        class="citation-mark"><sup>12</sup></a>.</p>
            <p>Далее следует изложение Розенбергом теории дхарм в ее <em>динамическом аспекте</em>.
                Предполагается, что существование каждой дхармы измеряется мгновеньями, и что
                существование сознающего «... является, следовательно, цепью беспрестанно
                сменяющихся мгновенных комбинаций, состоящих из появляющихся на мгновение единичных
                дхарм» (стр. 101). То есть в этом аспекте <em>существование дхарм</em> соответствует
                <em>беспрерывно</em> меняющемуся (но <em>прерывному, дискретному</em> — по
                определению!) содержанию
                сознания. При этом, вследствие именно <em>мгновенности</em> существования дхарм
                (которые в
                данном случае могут рассматриваться уже не только как мгновенные состояния сознания,
                но и как мгновенные <em>изменения</em> состояний, переходы в состояниях сознания),
                «... смена
                в содержании сознания происходит настолько быстро, что самый процесс перехода к
                новому содержанию не поддается наблюдению» (стр. 102). И в этом смысле сам
                сознающий (а не только посторонний наблюдатель) не может ни устанавливать конкретную
                причинную связь этих состояний, ни <em>помнить</em> их конкретное содержание в
                континууме,
                потоке (на санскрите — <em>сантана</em>) психической <em>жизни</em>. И отсюда общая
                буддийская идея о
                <em>принципиальном «не-наличии», отсутствии памяти</em>, как у конкретного живого
                существа
                (отождествляемого в буддизме с отдельным «потоком сознания») — о «начале»
                психического
                процесса (то есть самого себя!), так и у мира живых существ — о начале своего
                совокупного существования. Эта идея, общая почти всем разновидностям хинаянского и
                махаянского буддизма, была более подробно и полно изложена Розенбергом в другой его
                известной работе. «Вдумайтесь в вашу текущую жизнь; что она такое? <...> вы заметите
                тогда лишь поток всевозможных мыслей и чувств, различных воспоминаний, различных
                ощущений, то световых, то звуковых, то других. Все они переплетаются и бывают
                сложены то так, то иначе. Содержание вашей сознательной жизни меняется
                ежемгновенно<a href="#citation-13" id="ref-13"
                               class="citation-mark"><sup>13</sup></a>, причем самой смены вы не
                замечаете, <...> вы не помните всего, вы не помните, когда вы начали жить; и
                возникает вопрос, имеем ли мы вообще право говорить о начале потока мыслей, чувств и
                ощущений <...> ведь такое начало мы доказать не можем. Нужно ли предполагать, что
                поток нашей жизни имеет начало? <...> Не проще ли предположить, что мы не созданы,
                не имеем начала, а были всегда». И далее — «С точки зрения буддийской философии, все
                попытки объяснить начало сознательной жизни — Создателем ли, или механическим
                процессом — совершенно произвольны, ... ибо ... начала нет! Европейская же мысль
                настолько
                привыкла к вопросу о начале, что он кажется ей неизбежным. У буддийских народов мы
                видим обратное. Им совершенно непонятен сам наш вопрос о начале, о создании мира, о
                создании людей»<a href="#citation-14" id="ref-14"
                                  class="citation-mark"><sup>14</sup></a>. Приведенные выдержки
                показывают, что буддийское умозрение исходит из предпосылки о полном параллелизме
                механизмов, порождающих и воспроизводящих весь мир и микрокосм человеческого
                сознания.</p>
            <p>Третий, <em>личностный аспект</em> теории дхарм, излагается Розенбергом следующим
                образом.
                Поскольку личность, «Я», как самостоятельная (не только метафизически, но и
                психологически понимаемая) сущность в буддизме категорически отвергается, и
                поскольку все, с чем мы имеем дело, говоря о «себе», есть поток состояний сознания,
                дхармический континуум, постольку о «себе» мы можем говорить, как о некотором
                <em>специфическом</em>, а точнее, <em>обособленном</em> вследствие своей
                специфичности, <em>дхармическом процессе</em>. Эта специфичность заключается в том,
                что для <em>данного живого существа</em> поток
                дхарм обладает известной стабильностью, обладает тенденцией к определенным,
                свойственным данному существу комбинациям психических состояний. То есть, хотя
                данный человек может быть психически, ментально гораздо больше похож на другого
                человека, чем на самого себя, каким он был 20 лет назад, но <em>характер
                    процесса</em>
                изменения его психики, его менталитета будет постоянно воспроизводить сам себя,
                коренным образом отличаясь от характера такого процесса у другого человека<a
                    href="#citation-15" id="ref-15" class="citation-mark"><sup>15</sup></a>.</p>
            <p>Если в понимании указанных трех аспектов буддийской теории дхарм Розенберг
                существенно отличался от других буддологов, то понимание им четвертого, <em>абсолютного</em>
                аспекта теории дхарм можно считать его самостоятельным открытием. Идя от изучения
                трактатов Васубандху и поздних абхидхармистских комментариев, Розенберг установил,
                что в буддийской философии существует идея о том, что дхармы, понимаемые в смысле
                психических состояний, можно рассматривать, как феноменальные проявления дхарм,
                понимаемых в абсолютном смысле, то есть понимаемых в качестве «<em>носителей</em>»
                этих
                проявлений, как своего рода «<em>признаков</em>»<a href="#citation-16" id="ref-16"
                                                                   class="citation-mark"><sup>16</sup></a>.
            </p>
            <p>И здесь Розенберг как бы окончательно синтезирует все смутные догадки, все попытки
                «прорваться» сквозь слова к значению и смыслу этого, быть может самого загадочного в
                истории восточных философий понятия, давая следующее определение: «Дхармами
                называются истинно сущие, трансцендентные, не познаваемые носители-субстраты тех
                элементов, на которые разлагается поток сознания со своим содержанием» (стр. 106). И
                далее: «Для древней буддийской схоластики дхарма — гипостазированный внеопытный
                субстрат каждого единичного элемента, найденного при критическом анализе опыта» (там
                же)<a href="#citation-17" id="ref-17" class="citation-mark"><sup>17</sup></a>.</p>
            <p>Но Розенберг не только открыл европейской буддологии «абсолютный аспект» теории
                дхарм. Он вместе с тем раскрыл тот механизм, посредством которого буддийская
                философия мыслила <em>переход</em> от дхарм-феноменов к дхармам-абсолютам, то есть
                от
                <em>психологии к метафизике</em>. Абхидхармистские тексты (особенно поздние)
                позволяют
                реконструировать буддийское мифологическое представление о некой сверхбытийной,
                сверхчувственной дхармической сфере, так сказать, об особом мире, где дхармы
                пребывают в чистом, абсолютном состоянии. И сам факт переживания человеком того или
                иного психического состояния толкуется таким образом, что та или иная дхарма
                появляется в феноменальном мире, «рождается» там, как то или иное состояние. <em>Но
                    на
                    самом деле</em>, согласно абхидхарме, в мире и в человеке <em>ничего не может
                    рождаться</em>. Речь
                идет только о «вплывании» той или иной дхармы в феноменальный мир, ибо сами дхармы
                не сотворены и не уничтожимы, чувственно невоспринимаемы, этически неоценимы. Отсюда
                —
                соответственная градация познания: простой, обыкновенный человек не способен
                отрешиться от субъективного оценочного отношения к людям и вещам; его духовный
                прогресс начинается <em>с элементарной объективизации</em> — он отрешается от себя и
                воспринимает людей и вещи «<em>как они есть</em>»; йог идет дальше он уже видит не
                людей и
                вещи, а потоки и комбинации дхарм (такое йогическое видение носит название <em>праджня</em>
                и
                достигается не дискурсивным мышлением, а йогическим созерцанием и сосредоточением);
                и, наконец, на самом высшем уровне «запредельного переосознания» (праджняпарамита)
                йог непосредственно постигает дхармы уже в их абсолютном аспекте<a
                    href="#citation-18" id="ref-18" class="citation-mark"><sup>18</sup></a>,
                полностью отрешаясь не только от «отношений» и «вещей», но и от самих «психических
                состояний» как феноменальных проявлений дхарм<a href="#citation-19" id="ref-19"
                                                                class="citation-mark"><sup>19</sup></a>.
            </p>
            <p>Анализ всех этих аспектов теории дхарм привел Розенберга к выводу, который казался
                парадоксальным некоторым его современникам, да и теперь представляется несколько
                странным историкам философии, не связанным с буддийским, да и вообще с индийским
                материалом. На традиционный вопрос о том, была ли буддийская философия
                <em>реализмом</em><a
                    href="#citation-20" id="ref-20" class="citation-mark"><sup>20</sup></a>, или
                нет, Розенберг отвечал в своей книге следующим образом. Буддийская философия,
                понимаемая как психологический анализ сознания личности и опирающаяся на теорию
                дхарм в смысле трех ее первых аспектов — абсолютно нереалистична. Но буддийская
                философия в той ее части, которая трактует теорию дхарм в абсолютном аспекте
                последней, является абсолютно реалистической: «С точки зрения онтологии все буддисты
                являются реалистами, в том смысле, что они все единогласно признают истинно реальную
                сущность, которая лежит за цепью моментов» (стр. 113)<a href="#citation-21"
                                                                        id="ref-21"
                                                                        class="citation-mark"><sup>21</sup></a>.
                Этот вывод глубоко гомологичен самой буддийской философии. Розенберг исходит из
                чисто релятивистской предпосылки о том, что всякое положение буддизма имеет
                конкретный смысл, только будучи рассматриваемо на конкретном уровне. То есть, в
                смысле приведенного выше примера, точка зрения «смотрящего на солнце» меняется в
                зависимости от эмоционального, интеллектуального или духовного состояния<a
                    href="#citation-22" id="ref-22" class="citation-mark"><sup>22</sup></a>
                «смотрящего» в момент «смотрения». Таким образом, дхармы, рассматриваемые <em>на
                    уровне
                    этики</em>, выступают как «благие», «неблагие» и «нейтральные» (здесь
                подразумевается — в
                отношении духовного освобождения человека). На уровне <em>психологического
                    анализа</em>
                дхармы будут выступать просто как «<em>те или иные</em>» (то есть классифицироваться
                по таким
                разновидностям, как «эмоции», «думание», «успокоенность сознания»,
                «сосредоточенность», «несосредоточенность» и т. д.). На уровне <em>абсолютного
                    аспекта</em>
                они «просто дхармы» (или «только дхармы») без дальнейшей спецификации. И, наконец,
                на высшем йогическом уровне дхармы лишены как этической нагрузки, так и какого бы то
                ни было психологического содержания (равно как и онтологической характеристики).</p>
            <p>
                Эти выводы, сделанные Розенбергом, делают еще более очевидной ту известную
                буддологическую истину, что разные школы буддизма различаются по своим философским
                учениям гораздо менее, нежели они различаются по уровням даже в пределах одной и той
                же философской школы.</p>
            <p>Интересно, что буддология в течение тридцати лет, прошедших
                после смерти Розенберга, почти не внесла коррективов и дополнений в его понимание и
                изложение теории дхарм. В этой связи однако следует упомянуть о трех выдающихся
                буддологах, углубивших и развивших отдельные стороны этой теории.
                Первый из них, Евгений Евгеньевич Обермиллер, в своих работах обращал особое
                внимание на проблему терминологии и при этом был более склонен рассматривать дхармы
                (в самом общем значении этого слова), как <em>исходные онтологические единицы</em>,
                «элементы
                бытия» (elements of being)<a href="#citation-23" id="ref-23"
                                             class="citation-mark"><sup>23</sup></a>, при почти
                полном отвлечении от психологического содержания. Такую же позицию занимал и второй
                из них, Федор Ипполитович Щербатской. Для него дхармы представлялись категорией, с
                одной стороны, абстрактно-догматической, с другой стороны — чисто гносеологической
                (как «элемент-объект»), и потому также по преимуществу депсихологизированной<a
                    href="#citation-24" id="ref-24" class="citation-mark"><sup>24</sup></a>. И,
                наконец, замечательный польский буддолог Станислав Шайер, интересовавшийся более
                натурфилософскими проблемами буддологии, предложил свою исключительно оригинальную
                трактовку прагматической стороны теории дхарм<a href="#citation-25" id="ref-25"
                                                                class="citation-mark"><sup>25</sup></a>.
            </p>
            <p>Однако буддологические исследования последних 20 лет позволяют во многом если не
                изменить, то уточнить и дополнить розенберговское понимание дхарм, особенно
                поскольку речь идет о конкретных моментах содержания этого понятия в смысле
                статического и индивидуально-психологического аспектов теории дхарм. Здесь, пожалуй,
                даже более всего были важны не столько собственно буддологические исследования,
                сколько превосходные издания канонических абхидхармистских и праджняпарамитских
                текстов, на которые как раз Розенберг меньше всего ориентировался<a
                    href="#citation-26" id="ref-26" class="citation-mark"><sup>26</sup></a>.
            </p>
            <p>
                Эти уточнения и дополнения в понимании буддийской теории дхарм в основном сводятся к
                следующему.
            </p>
            <p>1. Абхидхармистская психологическая теория не является научной концепцией в нашем
                смысле этого слова прежде всего вследствие своей
                <em>замкнутости</em>: положения, имеющие силу в отношении <em>перечисленных</em> в
                текстах объектов, не могут быть перенесены на сходные объекты, поскольку последние
                <em>не перечислены</em>. Какая бы то ни было экстраполяция чужда абхидхармистской
                методологии.
            </p>
            <p>2. «Состав дхарм» установлен в пределах каждой школы абхидхармы. Количество дхарм (в
                смысле состояний психики или сознания) <em>конечно</em>. Количество психических
                состояний <em>вообще</em> (равно как и актов поведения) <em>бесконечно</em>.
                Выражение
                «всё — дхармы» следует понимать строго в том смысле, что «всё, что входит в
                абхидхармистскую теорию, — это дхармы» (то
                есть, что кроме дхарм туда ничего не входит). Это выражение ни в коем случае не
                следует трактовать в смысле, что «всё <em>вообще</em>, это — дхармы», ибо, как уже
                было
                сказано выше, буддийская философия не занимается ничем «вообще». Равным образом
                указание на то, что «данный объект не является дхармой», или что «нет такой дхармы,
                как данный объект», следует
                трактовать строго в том смысле, что «данный объект не находится <em>в списке
                    дхарм</em>». Все
                это, конечно, лишь постольку, поскольку речь идет <em>о психологическом</em>
                понимании дхарм,
                либо о понимании дхарм в «аспекте абсолюта» (в последнем случае мы могли бы сказать,
                что «никакая дхарма не является субстратом для данного объекта»)<a
                    href="#citation-27" id="ref-27" class="citation-mark"><sup>27</sup></a>.</p>
            <p>3. В то же время некоторые более поздние абхидхармистские тексты дают основание
                предполагать, что любое явление, любой факт феноменального мира субъектов-объектов
                могут быть истолкованы в смысле дхармы, как некоторого универсального субстрата
                психического существования.</p>
            <p>4. Теперь стало возможным более точное понимание <em>личности в смысле теории
                    дхарм</em>, и в частности — <em>в смысле набора дхарм</em>.</p>
            <p>Исходя из этих текстов, можно представить всю совокупность дхарм<a
                    href="#citation-28" id="ref-28" class="citation-mark"><sup>28</sup></a> в виде
                некоторого универсального набора (all dharmas's set, universe's set — S<sub>u</sub>),
                совокупность дхарм, <em>могущих</em> быть приписанными <em>данному</em> индивиду — в
                виде<em>потенциального набора дхарм индивида</em> (individual's potential set of
                dharmas
                — S<sub>p</sub>), и совокупность дхарм, в <em>действительности</em> реализованных
                данным индивидом — в виде <em>набора актуализации</em> (individual's set of
                actualization — S<sub>a</sub>).
            </p>
