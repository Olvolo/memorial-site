            <p>В чем же основная причина успеха «Проблем буддийской философии»? В чем условие
                этого удивительного свершения?</p>
            <p>Мне думается, что рассказать об этой книге нужно не только для того, чтобы напомнить
                о ней нашим историкам и теоретикам культуры (а надо сказать, что в Англии и Японии
