            <p>
                После прочтения этого романа весной 1995 года я совершенно ясно понял, что хочу
                встретиться с его автором. Наш первый разговор состоялся в августе того-же года, в
                его университетском кабинете. Меня не покидает чувство, что этот разговор еще не
                кончился, хотя я и никогда больше не услышу его живого голоса. Возможно, мое желание
                последних лет встречаться с живущими сегодня философами в какой-то мере было
                определено стремлением уменьшить влияние, которое Пятигорский оказывал на мое
                мышление, пытаясь удостовериться, что он не единственный живой философ в мире. И он
                в действительности не единственный. У одного из встреченных мною философов – я имею
                в виду Стенли Кавела из Гарварда, с которым мне так и не удалось познакомить
                Пятигорского – я заметил аналогичную мышлению Пятигорского интенсивность и
                насыщенность, в некоторых других плотность мысли подобна мышлению Пятигорского в его
                книгах и в лучших беседах, в которых мне посчастливилось участвовать, однако ни у
                одного из них я не видел такой страстной любви к философии и такую невероятную
                открытость, широту и свободу мысли. И свободу тут надо понимать не только как
                независимость от изобретенной другими и ставшей привычной или популярной мысли, но
                как поле, в котором может возникнуть непредсказуемые вопросы, в котором может
                случиться что-то интересное.
            </p>
            <p>
                «Интересно то, что может изменить направленность прежнего мышления», – примерно так
                пояснил один из своих постулатов Пятигорский в изданной в Риге в 2002 году книге
                «Мышление и наблюдение». На момент ее выхода двое из тех, кто мог бы эту книгу
                понять (я думаю о Зильбермане и Мамардашвили), уже умерли, тогда как другие,
                возможно, еще не родились. Сравнения ради стоит вспомнить, что написав свой
                «Трактат», Витгенштейн полагал, что его могут понять только два человека (Фреге и
                Рассел), но как оказалось, не поняли даже и они. Книга «Мышление и наблюдение»
                знаменовала для Пятигорского смену области интересов – его главной философской темой
                стало мышление, а не сознание, исследуемое теперь только в связи с мышлением.
                Пятигорский, который всегда подчеркивал, что его философия предназначена для него
                самого и его друзей, никогда не старался быть понятным («понимание – дело читателя
                или слушателя, а не автора или лектора»). Усилия быть понятным, на его взгляд,
                ослабляют внимание к предмету своего мышления. В то же время я не знаю никого, кто
                бы с такой охотой вслушивался в критику своей философии («философ, которому не
                нравится, что его критикуют – это говно, а не философ»), однако чтобы критиковать,
                надо было понимать, а понимать всегда было и есть трудно. «Философу не надо быть
                убедительным, быть убедительным это дело актера. Философ, которому верят, это не
                философ, а актер на идеологической сцене».
            </p>
            <p>
                Пятигорский ни в коем случае не был и не хотел быть академическим философом, если
                под этим понимать ремесленника, работающего в сфере мышления и проблематики других.
                Еще менее хотел он быть специалистом в какой-нибудь области философии (хотя
                несомненно был таковым в древнеиндийской и буддийской философии, которой занимался
                больше половины жизни), и совсем мало его интересовало филологическое отношение к
                какому-либо из канонизированных философов – все равно, будь это Гуссерль, Гегель или
                Кант. В молодости он внимательно читал Витгенштейна, некоторые элементы философии
                Ницше стали частью его собственного мышления, в конце жизни он несколько раз
                обращался к текстам Хайдеггера, но говоря о XX веке, он чаще упоминал таких
                полузабытых философов как Жан Гебсер и Чарли Броуд, а Гуссерля считал принадлежавшим
                XIX веку. Как-то на мой вопрос, кто из живых философов для него самый интересный,
                Пятигорский без промедления ответил — Шанкара (который, вероятно, жил в начале IX
                века). При необходимости Пятигорского можно считать «религиозным философом», однако
                религиозный фон его философии составляет ваджраяна, а не какая-либо из религий
                монотеизма. И в этой связи стоит заметить, что одна из нитей проходящих через все
                философские тексты и беседы Пятигорского это стремление понять и мышление, и
                сознание таким образом, чтобы не исключалась возможность совершенно другого, чужого,
                йогического мышления и сознания. Попытка выработки чужого мышления в Лондонском
                классе тесно связано с разработками индийских и тибетских йогов, но отстранена от
                изначальной почвы и включена в оборот (европейской) философии самого Пятигорского.
            </p>
            <p>
                Как-то осенью 2003 года я процитировал американского философа Роберта Брендома,
                определившего философию как «непрекращаемый разговор». Эта фраза так понравилась
                Пятигорскому, что к моему удивлению через пару часов перед ним уже был набросок
                статьи под названием «Непрекращаемый разговор», а на следующий год в
                Санкт-Петербурге под таким же названием вышла его книга, собравшая несколько ранее
                опубликованных работ. Разговор не только был главной формой его жизни, но постепенно
                приобрел и черты самостоятельного философского понятия. Разговор становился одним из
                условий и ситуаций философствования и был выбран как первичный инструмент
                преподавания в его Лондонском классе последних трех лет, которого он очень любил и
                ради которого был готов бросить все остальное. Он настаивал, что без разговора нет
                философии, но ради философии разговор должен происходить в независимости от
                социальных различий и психологических состояний. На одном из занятий Пятигорский
                напоминал участникам класса: «Вас здесь нет как кого-то, кто умнее другого, сильнее
                другого, главнее другого или слабее другого. Вы должны быть исключены из всех
                социальных сравнений и сопоставлений. Это совсем не призыв к равенству, наоборот.
                Это призыв к индивидуальности, другого здесь нет. Есть один я! Каждый из вас должен
                быть одним». Обучение происходило не в иерархических отношениях лектора и аудитории,
                а на примере разговора стремящегося к независимости от любых психических настроений
                и социальных связей. Как гласит третий пункт Устава участника класса,
                сформулированного в начале 2009 года: « Он должен непрестанно стараться прекратить
                восприятие любых негативных факторов, действующих – все равно со стороны общества,
                близких или далеких лиц и обстоятельств – как направленных лично против него, но
                видеть в них лишь препятствие своим умственным усилиям, причина которого в нем
                самом. Это – Благородное Старание».
            </p>
            <p>
                Я не раз слышал от Пятигорского: «я не хочу думать о смерти, но вынужден». Занятие
                Лондонского класса, совпавшее с 80-летием Пятигорского, была третей из четырех
                занятий посвященных теме смерти. Пятигорский, следуя Прусту, Мамардашвили и
                «тибетским йогам 11-го столетия», считал, что смерть это единственная позиция
                сознания, из которой возможно смотреть на жизнь («средний человек, не думающий о
                смерти, может быть только страшным, свирепым космическим животным, пожирающим всех и
                самого себя»). Ведь «без взгляда из за пределов смерти мы были бы полными
                ничтожествами». С другой стороны, стараясь дать феноменологическое или словесное
                определение смерти, Пятигорский предлагал думать о смерти как о мысли о прекращении
                собственного мышления, как о замеченной рефлексией остановке или прекращении
                мышления, и сразу же, из-за отсутствия объекта рефлексии, оказывающейся концом
                самой рефлексии. Это, конечно, не знание о том, что такое смерть, а предложение
                подумать особым образом. В ситуации полного отсутствия знания смерти, характерной
                для западной философии, этот ход имел смысл только как начало возможной философии
                смерти, неосуществленная работа над которой не легче, чем работа над философией
                мышления или сознания.
            </p>
            <p>
                Я не уверен, что кто-то понял философию Пятигорского во всей ее широте и пестроте.
                Понимание остается делом будущего. Но для любой работы понимания следует помнить,
                что философия Пятигорского жила в нескольких трудно понимаемых книгах, во множестве
                легче понимаемых статьей, лекций и семинаров, но более всего – в никогда не
                прекращающихся беседах с друзьями. Чтобы фиксировать беседы Пятигорского, необходим
                платоноподобный автор, способный из этих бесед создать философский текст. Ведь
                философствование в разговоре, по крайней мере внешне, можно считать сократической
                стороной философии Пятигорского, но в отличие от выдуманного Платоном Сократа,
                которого с некоторыми исключениями прельщали красивые и талантливые идиоты,
                Пятигорский искал собеседников в людях, в которых, по его мнению, жила энергия
                сознания, хотя иногда это оказывалось почти тавтологией, потому что сознание там,
                где энергия, а энергия там, где сознание. Пятигорский любил напоминать классификацию
                идиотов Гурджиева, считавшего переход от объективного идиотизма (когда человек <em>не знает</em>, что он идиот) к субъективному идиотизму (когда человек знает, что он идиот)
                чрезвычайно важным событием в жизни сознания человека. «Объективного идиота» тут
                следует понимать как нормального, недумающего человека, каких «по подсчетам одного
                моего друга в точности 92,5 % всех жителей земли», хотя по подсчетам других друзей
                Пятигорского это «ровно 99 % всех людей».
            </p>
            <p>
                Одним из самых сложных постулатов философии Пятигорского является
                «трансцендентальное единство философа и его философии». Возможно, но маловероятно,
                что кому-нибудь когда-то удастся содержательно переформулировать и сделать философию
                Пятигорского понятной «широким народным массам» (однажды, вынужденный вопросом
                типологически охарактеризовать содержание своей философии, он ответил, что «это не
                очень удавшийся эклектизм, отбросивший ненавистный мне систематизм и вобравший в
                себя сливки некоторых малоизвестных философов ХХ века и самые сливки буддийских
                философов»). Для него философия никогда не была системой идей и аргументов, а
                главным образом – способом жить и мыслить. Его неизмеримо больше интересовал процесс
                и активность философствования чем результаты этого процесса. Философия для него была
                постоянным желанием, страстью, определенным состоянием или скорее работой и
                отношением сознания – к жизни, если хотите, но больше всего – к своему собственному
                мышлению («если вы философ, то вы философов всегда и во всем»). В этом смысле
                философия не была одним из его атрибутов или признаков, а его сутью. «Философия для
                меня это мышление о своем мышлении, думающем о чем угодно. Но это исключительно
                личное, мое понимание философии, а не определение философии вообще». Идеи при этом
                возникают только как остатки или даже отбросы философской работы – в виде
                содержательно готовых мыслей, способных как помочь, так и остановить движение
                мышления (и Пятигорский хорошо знал, в какой степени готовые идеи и даже слова <em>могут</em>
                останавливать мышление). Аргументы в этом подходе появлялись совсем на периферии,
                там, где кто-то хочет свои идеи пояснять, обосновывать или, в самом худшем случае,
                кого-то в них убеждать. Понятность высказываний, истинность утверждений, всеобщность
                положений – все это было вторичным. Главное, чтобы было интересно, чтобы
                продолжалось мышление, не прекращался разговор.
            </p>
            <p>
                В беседах с ним меня больше всего поражала не его феноменальная память,
                энциклопедические познания в самых немыслимых вещах, неповторимый талант рассказчика
                или его трансцендентальная несерьезность («Как сказал бы Кришна, все смешно. Не так
                ли?» – такими словами он начал разговор при нашей второй встрече после только что им
                прочитанной лекции о средневековой индийской философии), а что-то совершенно другое.
                Когда речь шла о философии – а говорить ему нравилось о вещах самых разнообразных –
                возникало впечатление, что о каждом предмете он уже думал несчетным количеством
                способов (в этом отношении его было очень трудно удивить) и более всего стремился
                отыскать такой способ мышления, каким о данном предмете еще никто не думал. Чтобы
                было интересно.
            </p>
            <p>
                Одна из моих наибольших жизненных удач была возможность беседовать с Александром
                Моисеевичем Пятигорским. Я его очень любил. Я думаю, что он прожил исключительно
                интересную и поэтому – счастливую жизнь. Он жил и думал, как хотел. Даже умер он,
                как хотел – полный сил, при полном сознании. «Уходить надо, пока стол еще накрыт».
                Вскоре после 80-го дня рождения Пятигорский некоторым своим друзьям говорил, что
                скоро умрет. В последний год он несколько раз пытался об этом говорить со своей
                женой Людмилой, но это было воспринято как шутка и никакого разговора не вышло. Свой
                последний день он провел как обычно – думал, сделал несколько заметок, продолжал
                изучать грамматику тибетского языка, читал. Последней книгой, которую он читал уже
                который раз, был «Степной волк» Гессе. Последние слова, которые слышала его жена,
                были: «Погаси свет!». До этого он никогда так не говорил. Он умер вскоре после
                пробуждения.
            </p>

