            <p>
                В то же самое время Пятигорский попал в еще один интеллектуальный поворот того
                времени. Это было изучение вторичных знаковых систем (от гадания на картах до поэзии
                и кино), начатое Юрием Михаиловичем Лотманом, который с первой половины 60-х в своих
                летних школах собирал лучших литературоведов и лингвистов, создавая позднее всемирно
                известную Тартуско-Московскую школу семиотики. Именно в тетрадях летней школы Тарту
                в 60-х Пятигорский публиковался больше всего. Среди его публикаций можно отыскать
                как описание воздействия мескалина на речевой поток, так и фундаментальные
                размышления о тексте и мифе (в этих текстах заметно присутствие витгенштейновского
                «Трактата», хотя содержательную сторону Пятигорский порой объяснял влиянием
                Владимира Топорова, что последний, впрочем, категорически отрицал). К этому же
                периоду относится и упомянутый совместный труд с Мерабом Мамардашвили, который, по
                желанию Мамардашвили, ни в коем случае не должен был быть «научным». Таким он и не
                был, и тем самим в глазах ортодоксального звена семиотиков повлиял на их отношение к
                Пятигорскому, как к чужеродному элементу в их рядах («я для Лотмана был пределом
                допустимого, Мераба он вовсе не принимал»). Присутствие семиотического подхода видно
                и в позднейших работах Пятигорского, но к концу 80-ых семиотика в его глазах
                полностью потеряла свою привлекательность. Оглядываясь на свой семиотический период
                («мы думали, что писали о культуре со стороны; она водила нашей рукой изнутри») и на
                свои старания рассматривать «культуру как совокупность текстов», Пятигорский ясно
                видел наивность семиотики как «науки» и невозможность ее как философии. Не смотря на
                это, Пятигорский сохранил возникшую дружбу с Лотманом вплоть до его смерти в 1993
                году («мы первый раз встретились в 1963 году на вокзальном перроне в Тарту и в тот
                же миг поняли, что мы давние друзья»).
            </p>
            <p>
                Хотя среди семиотиков философ Пятигорский воспринимался как чужеродное тело (так же
                как в некоторых кругах философов он воспринимался как чужеродный семиотик или
                востоковед), его присутствие среди блестящих российских лингвистов, представлявших
                ядро Тартуско-Московской школы, было неслучайным. Скоро после окончания философского
                факультета он принялся за изучение восточных языков (в каком-то смысле это началось
                уже в средней школе на домашних семинарах одного из первых «учителей жизни» молодого
                Пятигорского, бывшего артиллериста Юрия Валентиновича Кнорозова – позднее
                расшифровавшего письменность майя – где изучался язык и культура Древнего Египта). У
                одного из первых историков тамильской литературы Пурнама Сома Сундарама Пятигорский
                выучил тамильский язык – скорее всего, интерес к тамильскому языку возник из-за
                того, что на тамильском сохранилось почти столько же древнеиндийских философских
                текстов, как на санскрите, и в 1960-ом году Пятигорский вместе с Семеном Рудиным
                издал первый тамильско-русский словарь. В 1962-ом году в переводе Пятигорского с
                тамильского вышла книга «Материалы по истории индийской философии. Материализм
                локаяты, индуистская система шайва-бхакти и другие философские и религиозные системы
                в средневековых тамильских источниках» а в 1963-ом году – сборник его переводов
                древних тамильских легенд «Повесть о закoлдованных шакалax». До своего изгнания в
                1965-ом (?) году из Института народов Азии Академии наук СССР (впоследствии Институт
                Востоковедения) за «антисоветсткую деятельность» в связи с арестом еще одного друга,
                будущего профессора Сорбонны Андрея Синявского Пятигорский уже был признанным
                исследователем культуры, истории, религии и философии Индии. Это обстоятельство
                иногда ему позволяло, сидя на столе в позе лотоса, читать лекции по философии йоги в
                Высшей школе Марксизма-Ленинизма.
            </p>
            <p>
                Благодаря хорошим отношениям как между СССР и Индией, так и СССР и Цейлоном (теперь
                – Шри-Ланка), в 50-ых и 60-ых годах в Москве можно было встретить знаменитых ученых
                с мировым именем. Уже в 1949 году первым индийским послом в Советском Союзе был
                назначен прекрасный знаток индийской философии и будущий президент Республики Индия
                Саврапалли Радхакришнан. Мне неизвестно, удалось ли Пятигорскому побеседовать с ним,
                однако известно, что Пятигорский оказался одним из переводчиков «Индийской
                философии» Радхакришнана, которая вышла на русском языке в двух томах в 1956 и 1957
                годах. В то время часто приходилось писать и переводить тексты, чтобы потом их под
                своим именем публиковало бы руководство научного института, и Пятигорский так
                поступал неоднократно, хотя список этих книг едва ли кому удастся составить. В 1958
                году первым послом Цейлона в СССР был назначен Гунапала Пийасена Малаласекера,
                ставший впоследствии главным редактором, возможно, лучшей, энциклопедии буддизма и
                бывший частым гостем Института народов Азии, в то время рабочего места Пятигорского.
                Он побуждал возобновить развитую еще в царской России традицию по изучению буддизма.
                Но наибольшее впечатление на Пятигорского произвел человек, которого он всю жизнь
                называл своим учителем – великолепный тибетолог и монголист, энциклопедический
                знаток традиции буддизма Юрий Николаевич Рерих, перебравшийся в 1957 году из Индии в
                Москву. У него Пятигорский не только продолжал изучать санскрит, пали и тибетский,
                но в первую очередь занимался буддизмом и, как сам признавался, начал понимать
                буддийскую философию. Именно за эти «три счастливых года» ученичества у Пятигорского
                сформировалось понимание буддизма как определенной позиции жизни и сознания, а не
                только собрания доктринальных текстов или ритуальных и медитативных практик. Эта
                позиция проистекала из абсолютной индивидуальности каждого сознания и необходимости
                для каждого сознания стать самодостаточной целостностью (как говорит Будда в
                палийских сутрах – «стань островом»), иначе остается только быть ходячим гробом,
                таская измышленные другими, но самим непродуманные, и поэтому мертвые мысли.
            </p>
            <p>
                В Москве и Ленинграде конец 60-ых был временем религиозного возрождения.
                Представители интеллигенции становились практикующими христианами, иудеями и
                буддистами. В том числе и Пятигорский. Это случилось после запрета на участие в
                буддистских ритуалах и после внезапной смерти Рериха в мае 1960-го года от инфаркта,
                возможно вызванного выговорами за выпуск им же отредактированного перевода
                «Дхаммапады», сделанного В.Н. Топоровым. В 1968 году Пятигорский по командировке
                «Комсомольской правды» отправился в одно из редких мест вращения колесницы дхармы в
                Советском Союзе – в Бурятию (заметим, что в то время в первом буддийском храме в
                Европе, построенном в Санкт-Петербурге одним из учителей тринадцатого Далай-Ламы
                хамбо-ламой Агваном Доржиевым и освященном в 1915 году, располагалась радиостанция
                для глушения радиопередач с Запада). С бурятским Дхармараджой Бидией (точнее,
                Видьядхарой) Дандароном Пятигорский познакомился еще в 1957 году в Москве, на
                квартире крупного монголиста Константина Михайловича Черемисова, автора
                бурятско-монгольско-русского словаря (1951), но реально Пятигорский стал учеником
                Дандарона только в конце 60-ых. В 1956 году Дандарон был освобожден от почти
                двадцатилетнего заключения в советских тюрьмах и лагерях, где он с помощью
                сокамерников подучивал монгольский язык, выучил русский и несколько западных языков,
                занимался западной философией, а сам преподавал буддийскую философию и вырабатывал
                свою концепцию необуддизма. После освобождения, в самом конце 50-ых Дандарон
                сотрудничал с Ю.Н. Рерихом в работе над программой буддологических исследований.
            </p>
            <p>
                Но Дандарон был не только ученый и зэк со стажем – он был и йог, и лама, и философ.
                Вскоре после своего рождения в 1914 году Дандарон был признан перерожденцем
                (хубилганом) известного тибетского ламы Джаяг Ринпоче XIII (Кэлсан Цултим Танпэ
                Ньима) из монастыря Гумбум (или Кумбум, в провинции Амдо, Тибет) умершем в 1913
                году. Этот лама школы гелуг, будучи в Бурятии в начале 20-го века, передал линию
                тантры Ямантаки Лубсану Сандану Цыденову, великому реформатору
                бурятского буддизма, который в свою очередь стал наставником и учителем Дандарона.
                Дандарон принял эту традицию от Цыденова, который шестилетнего Дандарона объявил
                дхармараджой, владыкой Дхармы трех миров (в 1919 году так себя назвал сам Цыденов,
                на короткое время создавший теократическую Балагатскую республику, не зависящую ни
                от красных, ни от белых). Возможно, что в тридцатые годы Дандарон занимался
                тибетским языком у Андрея Ивановича Вострикова, приезжавшего (1927-1932) в Бурятию
                для обучения бурятской молодежи и для ученых разговоров с бурятскими ламами. [Это
                происходило в Ленинграде, после 1932 г. По рекомендации Агвана Доржиева Дандарон
                слушал курс тибетского языка у Вострикова] В 1929 году начался разгром бурятских
                дацанов и расстрелы лам, в 1937 году Дандарон был арестован, а Востриков –
                расстрелян.
            </p>
            <p>
                Осенью 1968 года Пятигорский и группа москвичей интересующихся буддизмом в центре
                Улан-Удэ, в ресторане гостиницы «Байкал», встретились с Дандароном как с младшим
                научным сотрудником Бурятского Института Общественных наук готовым им объяснить
                специфику бурятского буддизма. Эта встреча была первой из серии разговоров, которые
                переросли в ученичество Пятигорского. Во время одного из последующих приездов
                Пятигорский получил посвящение в несколько ваджраянских [садханы Ваджрасаттвы и
                Калачакры] текстов от Цыренжаба Гатавона (для других – просто дедушка или лама
                Гатавон, он имел титул аграмбы – высокое звание знатока тантры), жившего в селе
                Ульдурга. Гатавон, который был йогом, посвященным в высшие тантры школы гелуг и едва
                ли не единственным носителем традиции Калачакра-тантры в Бурятии, согласился на
                инициацию в тантристские тексты «русского» только по настоянию Дандарона, который
                взял на себя всю ответственность и обязательства по продолжению обучения.
                Ваджраянское ученичество Пятигорского продолжалось в Бурятии, а позже – в Москве (в
                квартире Октябрины Федоровны Волковой) и Ленинграде до ареста Дандарона осенью 1972
                года. Со своих поездок в Бурятию и до конца жизни Пятигорский каждый день начинал с
                повторения сутр и мантр. Можно даже сказать, что его день начинался только после
                этих занятий с текстами, хотя иногда звуки санскрита и тибетского сопровождали
                приготовление утреннего кофе.
            </p>
            <p>
                В отличие от серьезности по отношению к ритуалу (ритуалу посвящен ряд работ
                Пятигорского), традиция буддийского тантризма ваджраяны (или мантраяны) не
                интересуется социальными условностями, что, кажется, совпало с отношением к жизни и
                самого Пятигорского (как говорил Дандарон, «нечего заключать сделки с сансарой»,
                или, как говорил один тибетский йог 16-го столетия, «о сансаре думать – как на говно
                наступить»). После поездок в Бурятию Пятигорский стал следовать и традиции никогда
                не пить водку, прежде не прочитав молитвы для очищения места от плохих мыслей и злых
                духов («если пьешь без молитвы, определенно происходит что-то плохое – или у кого-то
                на следующий день болит голова, или кого-то бросает женщина...»). Ритуальная
                молитва, которой никогда не придавалось преувеличенной серьезности, как-то очень
                органично укладывалась в легкое пиршество, в какое превращался любой ужин с
                Пятигорским. Способность излучать неиссякаемую радость и веселье у Пятигорского была
                тесно связана с активным неприятием чувства какой-либо жалости к себе и к сетованиям
                на жизнь. Жалобы, подавленность, уныние, обиженность, зависть – все, что отнимает
                жизненную радость и препятствует полноценной работе сознания, в его присутствии
                становилось жалким и смешным, подобно застреванию в ловушке собственного сознания.
                Все жизненные неудобства для него были необходимым дополнительным стимулом мышления
                – без жизненных трудностей и их неудобства «никто бы вообще ни о чем не думал, и все
                бы превратились в свиней».
            </p>
            <p>
                В каком-то смысле с той же традицией ваджраяны было связано понимание Пятигорским
                сексуальной энергии как одной из форм сознания. Ни с кем из его первых трех жен мне
                не приходилось встречаться, и я виделся только с одной из любовниц времени его
                профессуры, однако некоторые легендарные истории свидетельствуют, что он имел
                удивительный успех у женщин. Разумеется, об этой бурной стороне его жизни знали
                только его самые близкие друзья. Но сторона эта была-таки иногда бурная. Некая
                английская аристократка долгое время по завершении их отношений носила в сумочке
                пистолет, дабы при первой возможности его пристрелить. Людмила, любовь Пятигорского
                последних 15-ти лет, их совместную жизнь описывает как время невероятно интенсивного
                счастья.
            </p>
            <p>
                Оставив Советский Союз в 1973-ом году, Пятигорский хотел отправиться в Лондон, а не
                в Израиль, «как все порядочные евреи». Лондон, о котором в детстве ему много
                рассказывал его отец, Пятигорский любил не только как город, в котором приятно быть,
                но в то же время как место, где каждый может быть чужим – всему и всем. Стремление к
                чужести или отчуждению от своего времени, культуры («я человек никакой культуры») и
                места частично объясняет интенцию занятий Пятигорского последних трех лет в Лондоне
                на им самим придуманном классе по «выработке чужого или <em>очуженного</em>
                мышления» (во
                первых, чужого своему времени и месту, а потом, если удастся, чужого и самому себе).
                Это же стремление в последние годы было связано и с работой над
                «не-антропоцентрической философией». В ее границах поле мышления открывается только
                тому, кто стал чужим себе и своей среде, а в содержательном плане эта философия была
                направлена на превращение человека и его проблематики в философскую маргиналию.
            </p>
            <p>
                В доброжелательном к чужакам Лондоне Пятигорский оказался благодаря приглашению
                прочитать несколько лекций в Оксфордском университете (о его приглашении больше
                всего хлопотала теперешняя профессор истории религии Чикагского университета Венни
                Доннигер, знавшая Пятигорского по своим поездкам в Москву). Возможность прочесть
                серию лекций в Оксфорде через пару недель после приезда в Англию конечно было
                удачей, но его способность это сделать было результатом серьезных занятий в Москве.
                Один из известных британских буддологов, долгие годы профессор-санскритолог
                Оксфордского университета Ричард Гомбрих из этих лекций запомнил только то, что они
                читались на никому непонятном семиотическом жаргоне, а в конце Пятигорский сказал:
                «Не беспокойтесь, если вы эти лекции не поняли. Я тоже их не понял». С другой
                стороны, на некоторых молодых слушателей эти лекции произвели такое впечатление, что
                они решили серьезно заняться буддизмом.
            </p>
            <p>
                Преподавание было одним из тех редких вещей, к которым Пятигорский относился
                серьезно и с сознанием долга. После окончания философского факультета в Москве в
                1951-ом году он четыре года работал учителем истории от 6 до 9 класса в послевоенном
                Сталинграде, и учительство он всю жизнь считал своей единственной профессией. Не в
                смысле гуру или ринпоче, а в простом смысле учителя средней школы, который должен
                иногда объяснять простые вещи, но более всего – вызывать интерес к своему предмету.
                И это возможно только при условии, что ты сам свой предмет любишь. Чтобы вызвать
                интерес, годятся разные средства, в том числе и эксцентричный, чтобы не сказать –
                театральный стиль преподавания Пятигорского. Постоянная ссылка во время его лекций
                на «идиотов» кажется заимствована из педагогической практики буддизма и Георгия
                Ивановича Гурджиева. Слушатели первых его лекций в Риге в 1997 году скорее всего
                помнят, что конспект пяти лекций по философии буддизма у него был нацарапан на пачке
                сигарет. Именно потому, что своей педагогической целью он ставил не передачу
                информации (без этого не обойтись), а порождение интереса, его лекции всегда в той
                или иной степени были ситуативны, будучи предназначены именно для той аудитории,
                перед которой они читались. Например, шесть лекций о политической философии,
                прочитанных перед молодежной аудиторией в Москве, были изданы в 2007 году <em>без</em>
                желания автора, поскольку их целью было только вызвать у слушателей интерес к
                политической рефлексии. Но Пятигорский действительно думал о возможности
                политической философии («Одно из выражений свободы философа это выработка своей
                собственной политической философии»), и, хотя в последний год жизни эта тема для
                него исчерпала свой интерес, плод этих размышлений виден в книге «Размышляя о
                политике» вышедшей в 2008 году в соавторстве с Олегом Алексеевым. Внутри того же
                искания возможностей рефлексии над другими объектами мышления, думается, надо
                рассматривать и несколько заходов Пятигорского в тематику корпоративной философии,
                разрабатывая темы личности, репутации и управления.
            </p>
            <p>
                За первые десять лет жизни в Лондоне единственным человеком в Советском Союзе, не
                побоявшимся поддерживать связи с Пятигорским, был Мераб Мамардашвили. Они снова
                встретились только в 1986 году в Париже, четыре года после того, как в Иерусалиме
                была издана их совместная книга «Символ и сознание. Метафизические размышления о
                сознании, символике и языке» с очень подходящим для нее посвящением «Авторы – друг
                другу». Рукопись этой книги в начале 80-ых британский философ и социолог Эрнест
                Гельнер после встречи с Мамардашвили нелегально вывез из Советского Союза. В
                предисловии Лидия Воронина из Бостона, ученица обоих авторов, утверждает, что эту
                книгу понять невозможно – ее можно лишь по-разному не понять. Основанная на
                разговорах ее авторов, книга «Символ и сознание» является безусловно эзотерическим
                текстом, однако писалась она как предварительная работа любому подходу к философским
                текстам, посвященным сознанию. В предисловии ко второму изданию 1997 года
                Пятигорский пишет, что в книге недостает одного постулата – «мы (я имею в виду
                авторов книги) не можем (или не хотим, что здесь одно и то же) думать о сознании как
                о чем-то другом, ином, чем сознание, но мы можем думать о чем угодно другом как о
                сознании».
            </p>
            <p>
                За почти тридцать лет работы преподавателем восточно-азиатской истории, а позже и
                религии в школе Восточных и африканских исследований (SOAS) Лондонского университета
                возможности встречаться с необычными людьми у Пятигорского отнюдь не уменьшились.
                Очень скоро после приезда в Англию началась дружба с Исайей Берлином, Лешеком
                Колаковским и Эрнестом Гельнером, также быстро он познакомился с прекрасным
                историком западной философии Фредериком Коплстоном; Энгус Чарльз Грейем, выдающийся
                знаток классической китайской философии, был в то время его коллегой и многие годы
                частым собеседником; Пятигорский регулярно встречался с Дэвидом Сейфортом Руегом,
                одним из ведущих мировых специалистов по школам мадхьямики и тибетского буддизма.
                Несколько лет назад Руег о Пятигорском говорил: «Он очень оригинальный философ и
                потому неизбежно маргинал и одиночка». Это только наиболее известные его лондонские
                собеседники, о разнообразности же остальных можно было судить по его бесконечным
                рассказам о казалось бы невероятных встречах и беседах. «Казалось бы» потому, что в
                рассказах Пятигорского никогда нельзя было отделить действительно случившееся от
                того, что он выдумал сам. А «невероятных» по той простой причине, что большинство
                слушателей этих рассказов едва ли имело представление, что разговоры могут быть
                настолько интересными.
            </p>
            <p>
                В 1984 году в Лондоне вышла книга Пятигорского «Буддийская философия мышления»
                (<em>Buddhist Philosophy of Thought</em>), посвященная Абхидхарме (или Абидамме на пали) и
                разрабатываемой в ней классификации мышления (в книге продолжена тематика работы
                Дандарона «51 психический элемент виджнянавадинов»). Книга почти настолько же
                сложная, как и ее предмет. Понимание этой книги еще больше затрудняет ряд
                применяемых в ней понятий, придуманных самим автором или взятых из области
                семиотики. Пятью годами позже Пятигорский эту свою книгу охарактеризовал как
                «непростительно дилетантски и фрагментарно написанную», но притом не отрицал наличие
                в ней и некоторых «забавных мыслей». Книгу оценили те буддологи, которых
                интересовала философия (известный исследователь философии буддизма Пол Вильямс,
                после сетований на ее непонятность, рекомендовал прочесть книгу по крайней мере
                дважды), остальные скорее всего ее просто не поняли. Представить, что кто-то из
                философов западных университетов мог бы заинтересоваться пониманием мышления в
                раннем буддизме, было бы наивно. Такая наивность, похоже, соответствует мнению
                Пятигорского о свойственной XX веку «невероятной вульгаризации философии».
            </p>
            <p>
                Есть люди, которые вышедшую в 1993 году книгу «Мифологические размышления. Лекции о
                феноменологии мифа» (<em>Mythological Deliberations. Lectures on the Phenomenology of Myth</em>, впервые русский перевод опубликован в 1996 году) считают самой существенной
                книгой двадцатого века о мифологии. Есть и такие, кто его книгу 1997 года «Кто
                боится масонов?» (<em>Who is Afraid of Freemasons?</em>, русский перевод опубликован в 2009
                году) не только считают лучшим исследованием, когда-либо написанным о раннем
                масонстве, но также одной из выдающихся книг об определенной религиозной традиции
                вообще. В первой можно увидеть, насколько Пятигорский отошел от своих ранних
                семиотических попыток осмыслить мифологию как особого рода текст и насколько
                плодотворно рассматривать мифологию как феномен сознания. Вторая книга чисто
                историческое исследование, хотя «чисто» тут не стоит понимать буквально – в книге
                интерес Пятигорского к выдуманной масонами собственной истории и к придуманными ими
                же ритуалам следует желанию понять, как и что думали создатели движения свободных
                каменщиков.
            </p>
            <p>
                Параллельно историческим исследованиям о происхождении масонства Пятигорский работал
                над своим вторым романом «Вспомнишь странного человека», посвященным Михаилу
                Ивановичу Терещенко — масону, одному из богатейших промышленников царской России,
                министру финансов, а позже – иностранных дел Временного правительства, скоро после
                большевистского переворота освобожденного из-под ареста и затем прибывшего в Англию
                без гроша в кармане, но вскорости ставшего одним из богатейших людей Великобритании.
                Однако история не о деньгах, а о жизни сознания необычного человека и о возможностях
                эту жизнь воспроизвести. За этот роман Пятигорский в 2000 году получил премию Андрея
                Белого, хотя едва ли это имеет какое-нибудь значение.
            </p>
            <p>
                К концу жизни Пятигорский, по мнению некоторых, становился похожим на
                греко-армянского мистика Георгия Ивановича Гурджиева, которого сам Пятигорский
                никогда не встречал, но интерес к которому у него появился уже в ранней молодости и
                продолжался в Лондоне, где не было недостатка ни в учениках Гурджиева, ни в людях,
                знавших того лично («Я никогда не был последователем Гурджиева, но я его очень
                люблю»). Феномен Гурджиева трудно понять и еще труднее описать, но его жизнь и
                отношение к жизни несомненно составляли один из горизонтов мышления Пятигорского
                (под горизонтом подразумевая то, что по ходу продвижения или мышления постоянно
                меняется). Некоторые линии этого меняющегося горизонта составили содержательный
                скелет первого и самого гурджиевского романа Пятигорского «Философия одного
                переулка» (роман первый раз вышел в 1989 году в Лондоне в русском издательстве,
                поддерживаемом на средства Центрального разведывательного управления).
            </p>
