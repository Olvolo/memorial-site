                совершенно различных текстов и пользуясь разными материалами мышления. Тем не менее,
                их объединяла уверенность, что сознание только одно, независимо от того, проявляется
                ли оно в IV веке до нашей эры в Афинах, в V веке нашей эры в Индии, в XIX веке в
