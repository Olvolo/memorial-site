                места и разнородности ее текстов.
            </p>
            <p>
                Мамардашвили порой называли «единственным свободным человеком в Советском Союзе» или
                «грузинским Сократом» а позже он для многих был «единственным философом в Москве».
                Пятигорский всегда отзывался о беседах с ним как о школе свободы мышления. Хотя
                Мамардашвили был самым близким его другом, это был далеко не единственный разговор,
                в котором участвовал Пятигорский. Похоже, его инстинктивно влекло все интересное,
                любая интеллектуальная энергия, нет, он бы сказал, энергия сознания. Как, например,
                в случае с Давидом Бениаминовичем Зильберманом в 68-ом году перебравшимся из Одессы
                в Москву и скоро ставшим еще одним регулярным собеседником и соавтором Пятигорского
                (до переезда Зильбермана в Бостон в 73-ем году, где тот спустя несколько лет погиб,
                попав велосипедом под машину и оставив более десяти тысяч страниц, из которых только
                небольшая часть опубликована на русском и английском языках). Их общий доклад в
                69-ом году о «наблюдательной психологии» спустя много лет послужил Пятигорскому
                толчком для введения к лекционному курсу в
                Лондонском университете, посвященном феноменологии религии, а еще через несколько
                лет превратился в книгу «Мышление и наблюдение. Четыре лекции по обсервационной
                философии», у которой уже не было ничего общего с психологией и в которой, как
                считал Владимир Вениаминович Бибихин, Пятигорский «совершенно голый» (стоит
                заметить, что Бибихина Пятигорский считал последним русским философом, после смерти
                которого в 2004 году «никого больше не осталось, один Калиниченко в Вятке», т.е. так
                он говорил до смерти Калиниченко в 2008 году). Молодой Зильберман, работая в
                Ашхабаде метеорологом, изучил санскрит и шесть философских систем индуизма, а затем,
                подобно Пятигорскому, включился в интеллектуальные события Москвы, в частности, в
                методологические семинары Георгия Петровича Щедровицкого, еще одного близкого друга
                Пятигорского (эти семинары начались на квартире Щедровицкого еще в 50-ых годах, и в
                их создании активно участвовал и студент философского факультета Мераб
                Мамардашвили).
            </p>
            <p>
                Семинары Щедровицкого были посвящены мышлению как особой деятельности и тому, каким
                образом разные модели мышления проявляются в комплексных системах и структурах (от
                футбола до политики, от планировки улиц до хирургической операции).
                Марксистско-гегельянская идея «мышления как особого типа деятельности» была одной из
                любимых идей Мераба Мамардашвили, который от семинаров Щедровицкого скоро отказался
                потому, что считал философию одиноким занятием, а не групповой работой. В свою
                очередь Пятигорский, будучи частым собеседником и другом Щедровицкого, в них вообще
                не участвовал. Может быть потому, что всю жизнь не хотел где-либо «участвовать» и к
                чему-либо присоединяться или принадлежать, к тому же ему был глубоко противопоказан
                любого рода догматизм и его совершенно не интересовала <em>применимость</em> мышления к чему
                угодно. В философском кругу людей, к которому принадлежали Пятигорский, было принято
                рассматривать любое жизненное событие и человеческое действие с точки зрения того,
                как в этом явлении или действии присутствует – если присутствует – мышление, как его
                заметить, распутать или понять. Характерным было и отношение к человеку, как только
                случайному и необязательному носителю мысли, а к мысли – как наиболее активному и
                устойчивому элементу мира. Общим было и понимание, что никакому взгляду, никакой
                мысли наблюдаемый предмет не виден полностью, со всех сторон (в этом смысле ни у
                какого предмета нет «всех сторон») и что большая часть ошибок мышления появляется
                из-за неучитывания своей позиции наблюдения обдумываемого предмета, откуда он может
                наблюдаться (мыслиться) только в одном определенном срезе.
            </p>
            <p>
                Это может оказаться непростительным упрощением, однако в каком-то смысле и
                методология Щедровицкого, и попытка Эвальда Ильенкова понять философию как «науку о
                мышлении и его закономерностях», и в середине 60-ых годах начатые частные семинары
                Владимира Библера, и путь Мамардашвили от Маркса и Гегеля к Гуссерлю, и обращение
                Пятигорского к философии Древней Индии и буддизма – все это можно рассматривать как
                выражение стремления к мышлению (у Щедровицкого, Ильенкова и Библера) или к сознанию
                (у Мамардашвили и Пятигорского) при внешних обстоятельствах максимальной
                бессмыслицы. В то же время, кажется, что только для Мамардашвили и Пятигорского это
                стремление было неразрывно связано с полной <em>свободой</em> мысли – свободой от техник,
                методологий и правил мышления и свободой от контекста, внешних обстоятельств и, в
                какой то мере, даже от времени. В их подходе и техника, и контекст могли быть только
                внешними и вторичными самому мышлению.
            </p>
            <p>
                Перечитывая статьи Пятигорского о «метафизической ситуации» в Москве 60-х,
                открывается картина необычайной интеллектуальной интенсивности. Никому не надо
                доказывать, что в то время в Москве работали выдающиеся физики-теоретики, психологи,
                лингвисты и филологи мирового уровня. Многие физики были собеседниками Пятигорского,
                а филологи его коллегами (Владимира Николаевича Топорова, «одного из самых
                замечательных людей Москвы», он называл своим учителем). Со многими психологами он
                тесно сотрудничал, участвуя и в их семинарах (с докладами), и в их экспериментах,
                требующих, например, почти йогических усилий по освобождению мышления от его
                предметов и употребления мескалина и LSD-25 для наблюдения за всеми изменениями
                работы сознания (только некоторые результаты этих экспериментов были опубликованы).
            </p>
