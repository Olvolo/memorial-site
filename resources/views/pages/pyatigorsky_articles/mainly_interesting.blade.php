                мышление, сопротивляющееся всем готовым идеям, схемам, клише, будь то клише языка,
                метафизические штампы или готовые «онтологические картинки» – другими словами,
                всему, что оглупляет и останавливает мышление. Конечно, как любая попытка понять
                происхождение оригинальной мысли, это только одна из версий.
            </p>
