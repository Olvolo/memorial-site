                </p>
                <p>Итак, ответ на вопрос «какие же в этом случае (возникают) благие дхармы?» таков:
                    «Это 56 благих дхарм, а именно, и так далее...» Что же тогда значит «случай»?
                    Случай означает случай возникновения мысли. Если подробнее, во-первых, случай
                    здесь обозначает момент этого возникновения, что подчеркивает минимальное время,
                    в течение которого продолжается только что возникшая мысль. Во-вторых, случай
                    здесь также обозначает одновременность возникновения 56 дхарм как с данной
                    благой мыслью, так и друг с другом. Далее, в-третьих, согласно другому
                    известному комментарию<a href="#citation-9" id="ref-9"
                                             class="citation-mark"><sup>9</sup></a>, в понятии
                    случая подразумевается, что дхармы, возникающие в момент возникновения данной
                    мысли, т.е. в случае ее возникновения, не существуют (или, лучше, не возникают)
                    ни до, ни после этого момента. И наконец, в-четвертых, об этом случае говорится,
                    что он имел место в чувственной сфере. А это указывает на то обстоятельство, что
                    будущие перерождения, в которых проявляются (кармические) последствия данной
                    возникшей мысли, также будут иметь место в чувственной сфере феноменального
                    мира, сансары, а не в сферах, соответствующих дхьяническому плану вселенной.
                    Значит, так и только так должно представлять себе, что 56 дхарм возникают в
                    момент возникновения благой мысли (условно названной «мыслью первого типа» или
                    «первой мыслью»). Сейчас важно указать, что, говоря об этих дхармах, будет
                    неправильным сказать об одной из них: «эта возникла первой», а о другой - «а
                    та возникла второй». Ибо, поскольку никакая мысль не возникает одна, точное
                    значение слова «возникать» - это «возникать одновременно с...».</p>
                <p>Но - и это самое главное - ведь эти 56 дхарм являются чистыми состояниями
                    (сознания), без сущности, без наполняющего их содержания. И они здесь просчитаны
                    (и пронумерованы), чтобы показать пустоту всего, что возникает. Вместе с тем эти
                    возникшие дхармы предельны как таковые, за ними ничего нет, кроме них, ничего
                    нет. Ни существа, ни субъекта, ни человека, ни личности.</p>
                <p><em>III. Из следующего комментария Буддхагхоши.</em></p>
                <p>Теперь нам будет необходимо остановиться на двух важнейших обстоятельствах
                    возникновения мысли. Первое. Мысль возникает либо как осознанный акт воления, в
                    каковом случае оно возникает, будучи связана (посредством санскар) с другими
                    мыслями и дхармами, и называется санскарической, либо без воления. Мы ведь уже
                    знаем, что воление (как и санскары) прямо обусловлено неведением<a
                        href="#citation-10" id="ref-10" class="citation-mark"><sup>10</sup></a>.
                    Второе обстоятельство. Между тем есть еще апперцептивная мысль, которая, имея
                    своим объектом объект одного из органов чувств, возникает одновременно из двух
                    дверей: из двери органа чувств и из двери ума.</p>
                <p>Теперь спросим, почему контакт идет первым в нашем перечислении дхарм. А потому,
                    что именно контакт органа чувств с его объектом совпадает с мыслью в первом
                    моменте ее возникновения. Таким образом, совпав через контакт с органом чувства,
                    мысль (ум, сознание) чувствует объект чувством, воспринимает его восприятием,
                    волит его волей. Восприятие идет после контакта в перечислении дхарм (дхарма (3)
                    и соответствует третьему агрегату, скандхе индивидуального существования).
                    Восприятие имеет две функции: замечать объект и узнавать его. Первое означает,
                    что данный объект уже был в сознании, так сказать, как «одно» или «другое», «то»
                    или «это», и уже был назван словом, его обозначающим. Второе же значит узнавание
                    того, что уже было прежде замечено. Воля или воление ((4), соответствует
                    четвертому агрегату, скандхе санскар) - это то, что связывает все 56 дхарм в
                    одном моменте возникновения мысли в данном случае. Главное в волении (как и в
                    санскарах) - это энергия. Что же касается ума или сознания, то ум возникает
                    как мысль. Здесь они абсолютно синонимичны. Одно сознание (ум, мысль) возникает
                    немедленно после другого, образуя таким образом серию связанных друг с другом
                    мыслей или сознаний<a href="#citation-11" id="ref-11"
                                          class="citation-mark"><sup>11</sup></a>. Мыслей или
                    сознаний вне таких серий связи нет, они так не возникают.</p>
                <p>Тогда последний вопрос: является ли мысль, возникшая в данном случае, самайе и
                    обозначенная нами как «первая», той же самой, которая непосредственно
                    предшествует ей? Ответ таков: нет, не является, потому что «первая» мысль
                    является мыслью, которая возникла вместе с этими 56 дхармами, а «предшествующая»
                    возникла как одна из этих дхарм, а именно пятая в списке.</p>
            </blockquote>
            <h3 class="text-xl text-center font-semibold text-zinc-700 my-4">Дхарма как мысль. Мысль
                как возникновение мысли. Теорией чего является Абхидхарма</h3>
            <p><em>(0)</em> Сначала о тексте. По содержанию он кажется продолжением (во многом даже
                повторением) текста XI, где мы видим те же дхармы, только распределенные по фазам и
                степеням созерцания в дхьяническом пространстве. Само название текста XII,
                «Перечисление Дхарм», говорит о чисто описательном его характере. В то же время
                крайняя лапидарность текста, его, так сказать, «формульность», предполагает
                сравнительно высокий уровень понимания слушателей (не будем забывать, что текст, по
                всей вероятности, первоначально существовал только в устной передаче) и, позднее,
                читателей. В этом тексте, как в большинстве абхидхармистских текстов, объяснения
                полностью отсутствуют. Это не учебник для начинающих. Но он безмерно важен для нас,
                вечных начинающих, настырных второгодников начальной школы буддийской философии. Как
                наиболее яркий пример жанра абхидхармистской литературы, он является почти идеальным
                объектом, поводом, в конце концов - предлогом для нашего философствования в
                буддийской манере; философствования, так сказать, буддистическим образом.</p>
            <p>Вообще же наш текст является самым кратким изложением буддийской философии Абхидхармы
                во всей буддийской литературе. Но что такое Абхидхарма, в конце концов? Буддхагхоша
                отвечает. Буквально, Абхидхарма - это то, что идет после Дхармы, Учения Будды, как
                оно изложено в сутрах Палийского Канона. Действительно, многие абхидхармистские
                формулировки и определения (не говоря уже о прямых цитатах) находятся в сутрах, что
                дает основание для предположения о существовании какой-то (устной?) Абхидхармы в
                одно время с ранними сутрами или даже до последних. Но переходя к тому, что можно
                назвать философским содержанием нашего текста, и особенно комментариев на него
                Буддхагхоши (написанных минимум полтысячелетия спустя), начнем наше рассуждение с
                вопроса, что это за философия и о чем она? Ответ на первый вопрос был дан в семинаре
                X: «Абхидхарма - это дхьяническая философия». Ответ на второй вопрос в том же
                семинаре: «Абхидхарма- говорит о возникновениях мыслей». Здесь «дхьяническая»
                означает, что все основные идеи, понятия и положения этой философии - вместе со
                словами, обозначающими эти идеи, понятия и положения, - целиком взяты из
                созерцательного, медитативного опыта буддийской йоги и затем употреблялись в
                не-созерцательных, дискурсивных контекстах. Это, однако, не помешало тому, что
                многие из понятий, идей и терминов затем возвратились в дхьяническую практику, в
                качестве, прежде всего, теории дхарм. Абхидхарма - своего рода теория дхьяны.</p>
            <p><em>(1)</em> Возникновение мысли - это отправная точка (и, вместе с тем фокус)
                всего абхидхармистского философствования. Но заметьте, в Абхидхарме просто нет
                взятых в отдельности «возникновения» и «мысли». Они - одно событие, один факт,
                неделимый во времени и пространстве, но абсолютно дискретный в своей временной и
                пространственной отделенности от любого другого возникновения мысли. Данное
                возникновение мысли ни продолжает предыдущего, ни переходит в следующее, ни, менее
                всего, превращается в какую-нибудь «другую» мысль. Но почему это так? Да потому, что
                никакая мысль не существует вне своего возникновения. Как нет вообще того, что могло
                бы возникнуть, либо «стать», либо «не случиться» в промежутке между возникновением
                данной мысли и возникновением другой. Ибо такое то немыслимо (или, скажем, так: нет
                точки, момента, в которых могла бы возникнуть мысль о том!).</p>
            <p>Таким образом, я, начиная рассуждать о возникновении мысли согласно Абхидхарме,
                оговариваю, что мое рассуждение неизбежно становится в какой-то мере
                абхидхармистским, ведь я здесь - философ, а не историк философии. Но уже с первого
                шага я знаю, что попал в философскую ловушку, которую сам же себе и поставил. Чтобы
                как-то из нее выбраться, придется, вместе с Буддхагхошей, признать, что мысль здесь
                - это только обозначение. Но обозначение чего? Да того, что само не имеет никакого
                мыслимого содержания, в котором можно было бы увидеть различие, скажем, между мыслью
                и мышлением, мыслью и объектом мысли, да и между мыслью и мыслящим в придачу.</p>
            <p>И это не говоря уже о различии между мыслью и возникновением мысли, по каковой
                причине они и употребляются столь часто как синонимы. Да и в чем мысль может
                отличаться от возникновения мысли, когда оба они по определению лишены того, чем
                одно из них могло бы отличаться от другого. То же самое можно сказать и о любых двух
                возникновениях мысли. К этому можно добавить, но в порядке чистого предположения,
                что именно от этой абхидхармистской идеи «неотличия» дхарм (мыслей) друг от друга и
                ведет свое происхождение возникшая где-то между II и I веками до н.э. концепция
                Пустоты в смысле отсутствия у мыслимого объекта его содержания.</p>
            <p>Теперь, однако, пришло время вернуться к общим философским положениям, предельно
                кратко сформулированным в XII, 1 и впоследствии прокомментированным в XII, 2 - 3.
                Но сначала два замечания.</p>
            <p>Первое: традиционное комментирование начиналось со слова с его семантикой и
                этимологией. Одно слово было нередко единицей комментирования (иногда группа из
                нескольких слов), что впоследствии, при переходе от устно запоминаемого текста к
                письменно фиксируемому, послужило основным принципом членения, сегментации самого
                текста. И уже в позднейших списках текст оказывался зачастую вторичным - в
                отношении комментария (не только в буддийских, но и в брахманистских письменных
                традициях).</p>
            <p>Второе замечание. Буддийское философствование о мыслях и возникновении мысли по
                своему характеру указывает на наличие стойкой эмпирицистской основы для философии,
                возможно заложенной еще в добуддийские времена философствующими аскетами, джайнскими
                или даже ведийскими. С каким типом эмпиризма мы здесь имеем дело, сказать весьма
                трудно - бэконовским, берклианским или просто с эмпиризмом в смысле
                непосредственного дхьянического знания (см. выше: XI и X)? Но именно исходя из
                «первичного» эмпирического подхода, ранние абхидхармисты сформулировали два
                философских постулата, к которым в конечном счете сводится содержание текста XII.
                Первый: все, что есть, на самом деле не есть, а возникает. Второй: все, что
                возникает, есть мысль, т.е. только мысль и возникает. Тогда мы можем
                переформулировать начало Дхаммапады (см. VIII, 1) следующим образом: все дхармы
                умственны, в том смысле, что они являются либо мыслями, либо объектами мыслей, либо
                связаны с мыслями каким угодно другим мыслимым образом.</p>
            <p><em>(2)</em> Теперь перейдем в нашем философствовании о возникновении мысли к
                несколько «боковым» эмпирическим соображениям. Начнем с самого простого. Возникают
                миллиарды и миллиарды мыслей. Так ли это? Нет. Это общее, ни о чем конкретно не
                говорящее суждение, которое станет содержательным, только если, высказав его, мы
                укажем на тот конкретный способ, посредством которого кто-то мог бы увидеть
                возникновение мыслей в их конкретности и дискретности и тогда сказать: «возникают
                миллиарды и миллиарды мыслей». Если же мы спросим «где», то тот, кто этот способ
                употреблял, ответит терминологически точно: «Миллиарды и миллиарды мыслей возникают
                там, где они оказываются созерцаемыми в той или иной дхьяне». Таким образом, «где»
                возникновения мысли есть одно с «где» дхьянического созерцания. И соответственно
                «это возникновение мысли», «то возникновение мысли» будет также значить «этот момент
                созерцания», «тот момент созерцания». Но если мы, превысив меру любопытства,
                спросим: «А откуда они возникают?», то ответ будет: «Да ниоткуда, ибо нет такого
                состояния созерцания, созерцая в котором можно было бы дойти до источника или
                причины данной мысли, как и мысли вообще».</p>
            <p><em>(3)</em> Как мы видим в XII, 1, объяснение первого случая (или типа)
                возникновения мысли начинается с особенности, воспринимаемой только в созерцании и
                недоступной чувственному восприятию. Место возникновения или «где» мысли (первого
                типа) - это сфера чувственности, как бы «нижний этаж» дхьянического пространства.
                И мысль, сейчас возникшая в этой сфере, будет иметь кармические последствия в одном
                из перерождений также в чувственной сфере, в неопределенном будущем. Сфера
                чувственности является кармически заряженной; каждая мысль, в ней возникающая, будет
                по своему кармическому эффекту благой, неблагой или неопределенной. Время здесь
                кармическое и направлено от настоящего момента в будущее. Эта, так сказать,
                кармическая «качественность» мысли, возникшей в чувственной сфере, не может быть
                замечена внутри этой сферы, как не может быть замечено, «узнано» действие кармы
                вообще иначе как извне, т.е. из пространства, где такого действия нет. В
                абхадхармистской дхьянической космологии каждое возникновение мысли случается в
                одной из восьми (или девяти, или четырнадцати) сфер. Тогда от того, в какой из этих
                сфер возникла мысль и каков будет кармический эффект данной мысли, зависит и состав
                и число дхарм, приписываемых данной мысли, т.е. зависит случай, с которым мы имеем
                дело. Вот почему Буддхагхоша в XII, 2-3 говорит: «Если мысль возникает такая-то и
                такая-то, и возникает так и так, то и дхармы, с ней связанные, будут такими-то и
                такими-то». Иначе говоря, тогда и случай будет таким-то и таким-то.</p>
            <p><em>(4)</em> Случай, самайя - одно из самых интересных понятий Абхидхармы и
                буддийской философии в целом. Далее мы попробуем разобраться в этом понятии и с
                точки зрения нашего, нами пока еще не определенного философского подхода. Пока же
                спросим: что, в конце концов, означает слово «случай», когда говорится «в случае,
                когда такая-то и такая-то мысль возникает...» и так далее, или когда говорится «в
                этом случае, когда...»? Простой ответ невозможен. Попробуем рассмотреть «случай» в
                трех его аспектах.</p>
            <p>I. В первом аспекте, относительно временном, случай указывает на время возникновения
                данной мысли в отношении возникновения (или не-возникновения, или исчезновения)
                других феноменов или состояний сознания, т.е. других дхарм. Сказать здесь «в том
                случае», «в этом случае» будет то же самое, что сказать «в то время», «в это
                время».</p>
            <p>II. В своем втором аспекте, абсолютно временном, случай, самайя может рассматриваться
                в качестве предельно малой единицы, своего рода «атома» времени, одного «момента»
                (Р. khana, Skr. ksana) возникновения мысли.</p>
            <p>III. В своем третьем аспекте, пространственно-временном, случай, самайя - это
                сложный комплекс, включающий в себя, во-первых, факторы, условия и обстоятельства
                данного возникновения мысли, сами этой мыслью не являющиеся. Во- вторых, объекты
                данной мысли. В-третьих, те умственные состояния, которые возникли вместе с данной
                мыслью и одновременно с ней.</p>
            <p>Сейчас, однако, чтобы идти дальше в понимании (ведь какое-то знание все-таки у нас
                уже есть), я должен мое мышление, мою мысль соотнести с мышлением о самайи, случае
                (случаях), о котором говорится в тексте. Начну с важнейшего обстоятельства. Дело в
                том, что основные черты, особенности мысли оказываются противопоставленными случаю
                - как постоянное противопоставлено переменному. Но заметим, что эти черты
                зафиксированы и остаются теми же самыми только на время моего мышления о них или
                моего созерцания их. Случай же - это переменная для меня. Для Буддхагхоши он
                такой, как он есть и никакой другой. Более того, исходя из Буддхагхоши, я бы мог
                предположить, что эти постоянные черты возникающей мысли как бы уже там, в месте ее
                возникновения, момента которого они словно «ждут», не только чтобы ограничить
                количество возможных случаев последним (как об этом говорилось выше), но чтобы
                определить контуры и план всего космоса мысли в учении Абхадхармы. Космоса,
                структура которого задана формулой: «Когда (или где) мысль возникает как такая-то и
                такая-то, тогда (или там) и только тогда (или там), т.е. только в таком случае
                (связанные с этой мыслью) дхармы будут таким- то и такими-то». Но каковым будет
                отношение самой мысли к случаю ее возникновения? Напоминаю в этой связи: самой этой
                мысли в Абхидхарме нет, каждая мысль возникает как один или другой случай, «мысль
                вообще» не возникает». Она, строго абхидхармистски говоря, - не дхарма, хотя,
                конечно, дхарма, как факт, феномен чьего угодно (или ничьего) мышления, сейчас -
                моего.</p>
            <p>Давайте предположим теперь, что мы каким-то образом отметили одну мысль как уже
                определенную в смысле первого аспекта случая, самайи, и обозначили эту мысль как
                «мысль №17». Эта мысль будет по определению, т.е. в смысле третьего аспекта случая,
                самайи, иметь свое содержание (объекты и т.д.), а также, в смысле второго аспекта, и
                свою «длительность», так сказать. Тогда наконец у нас будет право спросить: возможно
                ли допущение, что эта мысль, № 17, могла бы возникнуть в какое-либо иное время или в
                каком-либо ином месте в безначальной и бесконечной вселенной абхидхармистской
                философии? Или, если слегка переформулировать этот вопрос, возможна ли в философии
                абхидхармы теорема о рекурренции мысли? Это, я думаю, - самый трудный вопрос всей
                буддийской философии, на который возможны два ответа: «Нет», поскольку мысль, если
                она уже отмечена нами в смысле первого аспекта самайи, не может быть (или
                возникнуть) другой, чем она есть в данный настоящий момент ее «отличания» нами.
                Тогда мы и решимся сказать, что каждая мысль уникальна в своей таковости (Р. и Skr.
                tathata). Уникальна, добавим, как уникален и момент ее возникновения. Отсюда может
                следовать - и некоторые из позднейших абхадхармистов на этом особенно настаивали,
                - что все моменты времени уникальны и неповторимы, как и все феномены, все дхармы.
                Это, конечно, весьма широкая экстраполяция первичных постулатов о дхарме. Однако
                ответ на поставленный вопрос будет «Да» при условии, что мы полностью и
                безоговорочно отбросили время. Тогда наше философствование будет идти под совсем
                иным углом зрения. При этом не надо забывать, что, строго абхадхармистски говоря, не
                только «все дхармы умственны», но и «время умственно по определению», т.е. само
                является эпифеноменом ума, мышления и что нет никакого времени, кроме времени
                возникновения мысли. Тогда будет вполне возможным предположить, что в сфере
                трансцендентального созерцания (см. выше X, 3), где нет возникновения мысли, нет
                также и времени, и поэтому там может «опять» возникнуть любая мысль, но без каких бы
                то ни было ее прошлых, настоящих или будущих возникновений. Тогда феномены, события
                и факты утрачивают «навсегда» свою уникальность, неповторимость, и любое
                возникновение может быть повторением.</p>
            <p>Теперь обратимся к третьему аспекту самайи, которому, собственно, и посвящено
                большинство абхидхармистских работ. Рассматриваемая в этом аспекте самайя является
                набором переменных, который варьируется от одного возникновения мысли к другому. Мы
                разделили эти переменные на три класса (A), (B) и (C). Первый класс - это факторы,
                условия, обстоятельства возникновения мысли. (A) представляется весьма гетерогенным
                по своему составу. В самом деле, где найти некий принцип, объединяющий столь
                различные факторы, как «знание», «спонтанность», «радостность». Мы и не найдем
                такого принципа, пока не рассмотрим (A) эпистемологически. Но поскольку, как следует
                из текста XII, знание еще не приобрело своего универсального значения и остается в
                рамках оппозиции «знание/не-знание», эпистемология пребывает как бы в
                «подвешенности», ожидая своей систематизации и разработки в трудах великих
                абхидхармистов конца первого тысячелетия н.э. Когда мы читаем в XI и XII, 2, о
                спонтанности только возникшей мысли, то напрашивается заключение, что сама идея
                спонтанности редуцируется к двум важнейшим абхидхармистским понятиям: понятию
                санскар (четвертая скандха) и понятию воли (Р. и Skr. cetana). Тогда «спонтанный»
                будет просто значить «не-направляемый волей» или «не-генерированный сознательно» в
                созерцании, дхьяне.</p>
            <p>Рассуждая о втором классе, (B), включающем в себя объекты мысли (Р. arammana, Skr.
                atambana), мы должны иметь в виду, что в Абхидхарме объект не имеет своего
                эпистемологического статуса и ни в коем случае своего онтологического содержания,
                т.е ему не приписывается никакого существования вне его отношения к мысли, уму или
                сознанию. Потому не может быть объекта в отсутствии мысли о нем, но возможна мысль,
                не имеющая своего объекта (как в случае некоторых трансцендентальных созерцаний).
                Естественно, что ситуация осложняется, когда мы переходим к специфическим объектам
                шестого органа чувств - ума. Эти объекты, называемые «дхарма-объекты», составляют
                особый подкласс и более всего соответствуют таким нашим понятиям, как «идея»,
                «мыслимое» и т.д.</p>
            <p>Третий класс (C) представлен списком 56 дхарм, начиная с контакта (также «осязания»),
                т.е контакта органа чувств с его объектом. На первый взгляд все эти дхармы -
                чистая психология (если не психофизиология). Но чья психология и психология чего? Да
                ничья, просто психология ничьей мысли. Однако сам термин «психология» - как мы его
                употребляем сегодня, пусть даже в его наиболее абсурдных физических или
                биофизических контекстах - имплицирует какую-то минимальную, лучше сказать -
                минималистскую, субъективность, тогда как в буддийских философских контекстах эта
                субъективность полностью устранена. Ибо в XII дхармы - это умственные состояния,
                приписываемые случаю возникновения мысли, самайе, а не субъекту мышления. То, что мы
                бы назвали «субъективностью», найдет свое место среди этих состояний, но только в
                виде, уже очищенном йогическим созерцанием от субъекта.</p>
            <p>Возможно, именно поэтому «субъективный» (Р. ajjhatta, Skr. adhyatma) понимается в XII
                исключительно в смысле самоотсылки и не несет никакого психологического смысла. Если
                и можно говорить о психологии в Абхидхарме, то лишь о какой-то «урезанной до
                дефективности», ибо ее человеческая специфика здесь отсутствует.</p>
            <p>Теперь отметим три философски интересных момента в нашем списке дхарм. Первый. Они
                называются «нетелесные» или «бесформенные» (Р. и Skr. arupa). Это значит, что в
                данном случае все они возникают при отсутствии первого агрегата, скандхи (об этом
                см. также в VIII) - отчего и считаются умственными по определению. Второй.
                Обратите внимание на повторение в этом списке одних и тех же дхарм, а также на то,
                что иногда два или три обозначения дхарм фигурируют как синонимы. Так, например,
                «мысль», «ум», «сознание», «способность ума» имеют в этом списке то же самое
                значение. Но как дхармы они различны, ибо занимают различные места в списке дхарм и
                соответственно имеют разные номера. Таким образом список 56-ти дхарм можно себе
                представить как умственный микрокосмос случая, самайи, и тогда каждую дхарму можно
                будет представить топологически как определенную точку в этом микрокосмосе, не
                совпадающую с точкой никакой другой дхармы. Третий момент. Даже в одном данном
                случае, самайе, возможно возникновение любого числа не-телесных дхарм, не входящих в
                приведенный список. Ибо этот список не является закрытым, не подводит черту, так
                сказать, но лишь служит примером, «идеальным образом» дхармического набора одного
                случая, или самайи. В этой связи можно заметить, что постулат Взаимообусловленного
                Возникновения всех дхарм имеет много различных правил вывода, что препятствует
                превращению дхармической философии (S. dharmavatda) в закрытую систему.</p>
            <p><em>(5)</em> Итак, все, что возникает, есть мысль, и только то, что есть мысль, и
                возникает. Это могло бы стать завершением и тупиком Абхидхармы, но не стало, ибо
                все, что возникает, возникает в силу Взаимообусловленного Возникновения. А то, что
                не подходит под действие последнего, то просто не возникает, как уже упомянутые выше
                две дхармы: Нирвана и Чистое Пространство. Чистое Пространство - это пространство,
                в котором нет возникновений мысли и, таким образом, нет моментов, нет случаев, из
                которых время, собственно говоря, и состоит (или «складывается»). В другом месте
                Буддхагхоша говорит: «Мудрый описывает мысль через самайю, а самайю через мысль».
                Здесь слово «описывает» очень важно, ибо сам метод подхода является описательным,
                дескриптивным более, чем индуктивным. Внутри этой системы описания само
                возникновение мысли является не метафизической сущностью, а универсальным
                обозначающим. В отсутствие же последнего мы оказывается в другой вселенной, во
                вневременном космосе немыслимых и не: мысленных объектов.</p>
            <p>Не будем при этом забывать, что во всем вышесказанном постулируются не только
                возникновения мыслей, но также и интервалы между ними, в которых мысль не возникает,
                интервалы пустоты, входящие в серии мыслей на правах «безмысленных фаз», также
                неделимые и отделенные друг от друга. Длительность этих интервалов варьируется от
                одного «дхарма-момента» до бесконечности. Из-за полной неопределенности времени
                интервала он иногда интерпретируется в терминах места (или пространства), а иногда
                даже описывается как один гигантский дхарма-момент.</p>
            <p>Все это, однако, не более чем еще одна чисто абхадхармистская гипотеза, каких много в
                этой традиции. Мы еще вернемся к времени и пространству мышления в наших последних
                семинарах. Пока же необходимо предупредить, что - поскольку в XII мысль является
                ни моей, ни твоей, но безусловно предполагается уже радикально измененной в
                созерцании, дхьяне - она уже не только «очищена» от всех психологических
                модальностей и модификаций, но и «освобождена» от своей когнитивной функции.
                Возможно, именно последнее обстоятельство повлияло на то, что уже ранние
                абхидхармисты и Буддхагхоша выделили «восприятие» в качестве как бы «общего
                когнитивного оператора», превратив его в инструмент, посредством которого любое
                умственное состояние будет мыслиться как когнитивное, т.е. как частный способ
                знания. Пока мы знаем о восприятии как о втором агрегате, скандхе в пятеричном
                комплексе индивидуального существования и как о дхарме № 3 в нашем списке 56 дхарм.
                Буддхагхоша же в тексте XII говорит о восприятии как о категории, репрезентирующей
                (и, соответственно, заменяющей) весь подкласс «умственных» дхарм, в который входят
                мысль, ум, сознание, разумение, логическое мышление и т.д., и тем самым кладет его в
                основание знания и познания. При этом он приписывает восприятию три сложные (т.е
                состоящие из двух или более дхарм) функции в отношении объекта восприятия: (1)
                дискурсивность, включающую в себя называние объекта; (2) признавание объекта, т.е.
                его отождествление с тем, что было воспринято прежде; (3) память об этом объекте.
                Последним, возможно, и объясняется такая странность, что память не фигурирует в
                качестве одной из дхарм нашего списка.</p>
            <p>При всем этом, однако, самой важной чертой дхармического состава случая, самайи,
                является то обстоятельство, что здесь все умственные процессы и факторы уже
                превращены в места чисто пространственных, топологических конфигураций «моментов» и
                «точек» дхармичности. В этой связи не будет большим риском предположить, что именно
                в Абхидхарме был сделан первый в истории философии шаг к элиминации времени. Сначала
                мастера Абхидхармы редуцировали более длительные органические психические процессы к
                гораздо более кратким умственным процессам (так в XII Буддхагхоша говорит, что «одна
                дхарма телесной жизни равно по длительности 16 дхармам умственным»). Затем они
                редуцировали умственные процессы к моментам возникновения мысли. И наконец, они
                превратили последние в точки микрокосмического пространства данного случая
                возникновения мысли, самайи. Теперь нам остается заметить, что чисто дхьяническая
                идея бесконечности пространства (или бесконечного пространства как одного из
                объектов трансцендентального созерцания) должна была циркулировать в йогических
                отшельнических кругах еще до появления первых абхидхармистских (устных) текстов, но
                идея бесконечного времени не нашла своего текстуального воплощения, не говоря уже об
                идее «пустого» времени. Наверное, это вполне естественно в развитии философского
                мышления. Ибо где мышление оказывается в фокусе философствования, там не обойтись
                без времени. И только там, где оказываются гипостазированными состояния отсутствия
                мышления и сознания, становится возможной элиминация времени. Как, скажем, в случае
                Нирваны как дхармы, приравненной к «чистому пространству» (Р. akasa, Skr.
                akasa).</p>
            <p>Наконец, четыре дополнительных соображения к семинару XII.</p>
            <p><em>(1) Первое.</em> Скорее в порядке повторения - об аспекте всеобщности,
                не-различительности дхармы как центрального понятия буддийской философии. В этом
                смысле «дхарма» обозначает все, что возникает, все возникновения мысли, все мысли,
                все случаи мышления и все объекты мышления. Эпистемологически, дхарма, именно в этом
                ее значении - назовем ее дхарма А - есть единица любой умственной активности и
                вместе с тем предел любой феноменологической редукции. В конечном счете дхарма и
                ничего, кроме дхармы, - вот с чем мы имеем дело, включая сюда «мы» и «имеем дело»,
                так же как и все это высказывание в целом. Однако было бы совершенно вульгарным
                представлять себе дхармы как «кирпичики», из которых состоит вселенная, ну, хотя бы
                в том смысле, в каком Ричард Фейнман говорил, что «все тела состоят из атомов». Ибо
                космос Абхидхармы - в отличие от космоса Эйнштейна и Фейнмана - есть космос
                умственный, ментальный и как таковой не знает противопоставления «умственное -
                не-умственное» или «мыслящее - не-мыслящее».</p>
            <p><em>(2) Второе</em> дополнительное соображение - о дхарме в различительном ее
                аспекте. В этом аспекте мы уже имели дело с несколькими классификациями дхарм по их
                разновидностям. Мы знаем, что дхармы бывают обусловленные и необусловленные, дурные
                и благие и т.д. Дхармой в этом аспекте - назовем ее дхарма Б - может быть что
                угодно, но она, будучи, так сказать, «универсальным предикатом», остается тем, что
                она есть. Так, мы говорим: «Нирвана - это дхарма, хотя бы необусловленная, но
                дхарма, или Взаимообусловленное Возникновение - это дхарма, обусловленная или
                необусловленная». Т.е как дхарма Б любая идея, высказанная или невысказанная, любое
                высказывание, истинное или ложное, любое понятие, абстрактное или конкретное, -
                это дхарма. Мы читаем в Дхаммападе. «Ненависть не остановишь ненавистью, только
                не-ненавистью прекращается ненависть - такова вечная дхарма». Таким образом, здесь
                ненависть - дурная дхарма, не-ненависть - благая дхарма, обе они дхармы Б. Все
                высказывание в целом - благая дхарма Б. Но все эти три дхармы в то же время
                являются и дхармами Б. «Являются» они, однако, только в ограниченном смысле их
                позитивного логического отношения к А и ни в каком ином смысле.</p>
            <p>Но разумеется, тот же «дхарма-принцип» - т.е. принцип мышления (или высказывания) о
                чем бы то ни было, как о дхарме А или Б, - действителен и для всех случаев
                отрицания. Хак, например, вместо того, чтобы сказать (или подумать) «Я не
                существует», абхадхармист говорит: «Не существует такой дхармы (дхармы А), как Я».
                Тогда, говоря строго абхидхармистски, не-существование (точнее, не-возникновение)
                чего-либо понимается в данном конкретном случае редукции дхармы Б к дхарме А.
                Поэтому, было бы неправильным высказывание: «Нирвана не является обусловленной
                дхармой Б», но вполне правильным высказывание: «В Абхидхарме нет такой дхармы (т.е
                дхармы А), как Нирвана.</p>
            <p><em>(3) Третье</em> дополнительное соображение - насчет позиционного аспекта
                дхармы. В Абхидхарме дхарма играет роль абсолютной позиции, некоего особого угла
                зрения, под которым видится, рассматривается и созерцается любой возможный или
                реально наличествующий объект мышления, т.е. видится, рассматривается и созерцается
                как дхарма. Эта дхармическая позиция абсолютна, потому что Абхидхарма не допускает
                никакой метапозиции, с точки зрения которой эта позиция могла бы рассматриваться как
                частный случай какой-то другой, более общей позиции. Дхарма, мыслимая в позиционном
                аспекте - назовем ее «дхарма Б», - практически то же самое, что учение о
                дхармах, Абхидхарма, ибо последняя является конкретным аналитическим описанием
                абсолютной дхармической позиции.</p>
            <p><em>(4) Четвертое</em> дополнительное соображение совершенно иного рода, хотя оно и
                является продолжением и развитием идеи дхармы как абсолютной позиции. Это -
                итоговое соображение относительно не-антропичности абхидхармистской (а в
                значительной степени и всей буддийской) философии. Давайте снова обратимся к дхарме
                (дхарме А, конечно) «смерть», уже довольно подробно рассмотренной в предыдущих
                семинарах. В самом деле, смерть мы можем представить либо как «мою», либо как чужую,
                но обязательно человеческую. В то время как в Абхидхарме смерть - это либо
                распадение комплекса пяти агрегатов, скандх, индивидуального существования (все
                равно чьего), или перерыв континуума сознания. Либо, наконец, смерть редуцируется к
                «сознанию в момент смерти» (Р. patisandhi). Казалось бы, в таком случае для человека
                не осталось роли в привычной драме смерти. И для тебя самого, пока ты, как
                абсолютное большинство тебе подобных, продолжаешь себя определять как «человека
                вообще». Это - обо мне и о тебе, о вас. Но раз ты уже стал созерцателем и вступил
                в первую дхьяну, тем самым прервав, пусть на мгновение, континуум сознания, ситуация
                резко меняется. Теперь ты уже - не «ты», т.е. не твое обычное эмпирическое «Я», а
                твое радикально измененное йогическим созерцанием сознание. Это сознание, а не ты
                переходит сначала в финальное сознание смерти, а затем в последующие серии
                после-смертных мыслей и дальше. Оно будет длиться в неопределенном будущем
                дальнейших перевоплощений или невоплощенных трансцендентальных состояний как своего
                рода «дхармический остаток» прошлых (включая и твое настоящее) рождений, будь они
                человеческие, животные или божественные. В этом отношении истинный созерцатель будет
                сильнее отличаться от человека, чем тот от животного или бога. Я думаю, что именно
                из-за своего «расчеловеченья» смерти, Абхидхарма заслуживает прозвище «перехитрившая
                смерть». Абхадхармистская концепция «смерти - не-смерти» или особой
                не-человеческой смерти созерцателя легла в основу учения о смерти или танатологии в
                буддийской философии.</p>
            <p>Однако, поскольку каждый умирает таким образом, который определяется образом его
                мышления (или созерцания, если он созерцатель), и поскольку его смерть является сама
                особым случаем его думанья или особым (промежуточным) состоянием его сознания, то
                смерть созерцателя будет более отличаться от любой другой смерти, чем все другие
                смерти отличны друг от друга, человеческие в первую очередь.</p>
            <p>Но Абхидхарма является не-антропической философией также и в силу того важнейшего
                обстоятельства, что абхидхармистекая иерархия не уделяет человеку особого места,
                топоса, так сказать, в пространстве дхармической классификации живых существ. Более
                того, и в абхидхармистской аксиологии человек не играет роли особого ценностного
                уровня. Еще интересней выглядит тот факт, что и в буддийской мифологической
                космологии «место людей» отсутствует как выделенная из общей топологии сфера. Космос
                заполнен Буддами и Бодхисаттвами, число которых во много раз превышает число вместе
                взятых всех живых существ.</p>
            <p>Но кто же тогда были великие учителя Абхидхармы - все- таки люди? С точки зрения
                Абхидхармы, ответ будет: да и нет. «Да», потому что, как учителя, продолжатели и
                распространители традиции, они совершали человеческие действия, говорили на
                человеческих языках и писали, как писали другие люди. «Нет», потому что тексты, с
                которыми они работали, были дхьянические, произведены в дхьяне, имели
                сверх-человеческое, сверх-мирное (P. lokuttara) содержаниe и были трансцендентально
                познаваемы. Да и сам великий мастер Абхидхармы был человеком только условно, т.е. по
                условиям контекста его проповедования и учительства. Более того, он был философом
                только в силу своей не-принадлежности к людям (а не наоборот). Именно поэтому
                Буддхагхоша утверждает, что Абхидхарма - это Высшая Истина, и оттого она - не
                для людей, только для Будд и Благородных.</p>
            <p>Нигде буддийский образ мышления не манифестировался с такой силой и ясностью, как в
                Абхидхарме. Абхидхарму я рассматриваю как философию par excellence. Буддийская
                философия, по-видимому, ведущая свое начало от философских фрагментов в сутрах
                Палийского Канона, обрела свою форму как философия в текстах палийской Абхидхармы и
                письменных комментариях на эти тексты. В заключение уточним само понятие
                «абхидхармистский». Здесь это слово употребляется в трех смыслах. Во-первых, оно
                значит «принадлежащий комплексу текстов, именуемому «Корзина Абхидхармы» Палийского
                Канона. Во-вторых, оно обозначает те фрагменты и высказывания (Будды) в сутрах, на
                которые позднее мастера Абхидхармы ссылались как на абхидхармистские. В-третьих,
                абхидхармистскими считались все идеи, положения и высказывания, которые, несмотря на
                их источник, мастера Абхидхармы рассматривали как правильно выведенные из первичных
                абхидхармистских постулатов.</p>
