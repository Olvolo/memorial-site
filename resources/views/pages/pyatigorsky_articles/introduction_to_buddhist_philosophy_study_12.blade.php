                <p>Эти 56 не-телесных (или лишенных формы)<a href="#citation-6" id="ref-6"
                                                             class="citation-mark"><sup>6</sup></a>
                    благих дхарм, а также и дхармы других разновидностей и возникают в силу
                    Взаимообусловленного Возникновения в данном случае<a href="#citation-7"
