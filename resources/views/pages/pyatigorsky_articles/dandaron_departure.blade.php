                о другом я не буду здесь подробно писать. Речь пойдет об иных вещах,
                представляющихся мне сейчас, после его смерти, очень значимыми именно в связи с
                его жизнью и смертью. Заметку об этом удивительном человеке я хочу начать с
                разговора об одном престранном явлении, которое просуществовало в России,
                пожалуй, лет 50-60 и которое я бы назвал не менее странным словом «имперская
                культура» (именно и м п е р с к а я, а не императорская). Название это чисто
