                явления: какие бы диковинные и, казалось даже, чуждые русской культуре по
                конкретному содержанию вещи ни являлись на её периферии, какие бы неожиданные
                импульсы ни возникали вне основного её течения, они неизбежно «затягивались» в
                неё, сливаясь с общим её потоком и сообщая ей особую универсальность и особое
                разнообразие. Она ничего не гнала из себя, но стремилась вобрать в
                себя всё, как с в о ё (но только на высшем, элитном уровне своего
                существования).
