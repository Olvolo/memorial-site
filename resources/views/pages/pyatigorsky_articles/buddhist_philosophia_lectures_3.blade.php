            <p>Отвечаю: разумеется, знаем. Все мы, начиная с Декарта, сказавшего: «Я мыслю,
                следовательно, существую», через Канта, считавшего, что наука психология невозможна,
                ибо объект (мышление) не может изучаться своими собственными средствами, и кончая
                Витгенштейном, который хотя и полагал, что говорить о мышлении можно только в
                терминах высказываний повседневного естественного языка, но несколько раз досадно
                оговорился (как в случае, когда, говоря о религии, утверждал, что в ней все — и
                мышление, и язык, и действие — действие), все мы знаем. Знаем, потому что как же это
                может быть иначе, если это я мыслю и осознаю мышление как мое (или чье-либо еще) в
                моей же рефлексии как факт, процесс, событие, о котором, кроме того, можно еще
                мыслить как о содержании (то есть объекте) мышления. Можно, конечно, говорить о
                природе мышления (психофизиологической, физиологической, генетической, биофизической
                — дальше, кажется, пока идти нельзя) или о логике как опыте описания мышления, но ни
                то, ни другое — не мышление. Мысль, мышление как идея, в любой философии, выросшей
                из Просвещения (а других пока нет, хотя были), — всегда антропологична, субъектна и
                объектна. Идея мышления во всех ее версиях и модификациях безнадежно банальна, ибо
                остается той же самой на протяжении столетий существования философии. Может быть,
                потому, что мышление никогда не оказывалось в фокусе философии (в отличие, скажем,
                от «идеи», «бытия» и ряда других философских категорий), никогда не было особым
                объектом европейского философского мышления. (В самом деле, — спрашивал Фреге, —
                чего уж такого особенного в мышлении? Такой же объект, как и всякий другой.)</p>

            <p>Хорошо, но что такое особый объект мышления? Отвечаю с позиции буддийских
                объяснительных комментариев на текст Взаимообусловленного Возникновения: такого
                объекта нет. Но он может быть произведен, сгенерирован искусственно (то есть
                йогически, в специфически буддийском смысле этого слова). И таким объектом может
                быть только сама мысль <em>(сitta),</em> но мыслимая только как «возникающая» <em>(utpanna),</em>
                как одно со своим возникновением <em>(utpada).</em> Этот объект называется
                «возникновение мысли» <em>(cittotpada),</em> но, строго говоря, это просто «мысль
                как особый объект», поскольку она йогически представима только как возникновение.
                Что значит йогически? Это значит, что данный объект, обозначенный как «возникновение
                мысли», существует (точнее, опять же, возникает) в процессе, где все (то есть любой
                мыслимый и названный объект) созерцается как дхарма, то есть как мгновенная мысль.
                (Этот процесс, или эта разновидность йоги, обычно называется <em>dhyana,</em> иногда
                <em>bhavana</em>.) Возникновение мысли, если смотреть на него со стороны, является
                нам как объект, но сначала попытаюсь объяснить — а это безумно трудно, — что значит
                «объект», опять же, дхарма, в смысле дхьяны? Так вот, его можно себе представить как
                такую, ну, «штуку», скорее, «штучку» (физик бы сказал — «частицу»), кроме которой в
                данный момент созерцания ты ничего не видишь; а во всем процессе созерцания, что бы
                ты ни видел, это будут такие же «штучки». По выходе из дхьянического созерцания ты
                можешь систематизировать и классифицировать эти «штучки» как мыслимые объекты в
                соответствии с их весьма краткими устными списками, матриками, о которых говорилось
                выше. То есть в соответствии с текстами, которые ты всегда помнишь и повторяешь, ибо
                слова этих текстов обозначают то, что в дхьяне созерцается как «штучки», — они
                предшествуют йогическому созерцанию и следуют за ним как термины его осознания и
                описания.</p>

            <p>Возникновения мысли, как такие объекты, такие дхармические «штучки», можно себе
                представить как обладающие следующими четырьмя особенностями.</p>

            <p>Первая. Сами по себе они абсолютно просты, то есть не обладают никакой внутренней
                сложностью, не редуцируемы ни к чему более простому.</p>

            <p>Вторая. Они мгновенны. То есть нет времени меньшего, чем время их возникновения,
                которое, собственно, и есть своего рода «квант времени». Или, говоря буддистически
                более точно, время мыслится только в их возникновениях (и в столь же мгновенных
                промежутках между ними).</p>

            <p>Третья. Они абсолютно дискретны. То есть нет непрерывного возникновения мысли.
                Мыслимы точки во времени и пространстве, где их нет.</p>

            <p>Четвертая. Они различаются (в смысле «одна мысль, другая мысль») по обстоятельствам
                их возникновения (или «случаям», samaya), а не сами по себе.</p>

            <p>Нетрудно заметить, что первые три особенности возникновения мысли являются также и
                особенностями <em>дхарм</em> (в третьем смысле их понимания, см. о дхармах в первой
                лекции). Четвертая же — это об отношении мысли к тому, чем она сама не является, но
                <em>вместе с чем</em> она возникает. Иначе говоря, об отношении <em>дхармы,</em>
                именуемой «(данная) мысль», ко всем другим дхармам (которые также есть «мысль», в
                смысле текста I. 1 (1) первой лекции). Набор дхарм, которые «всегда уже там» при
                возникновении данной мысли, и называется <em>случаем.</em> Число таких случаев
                бесконечно. В особой (и, по-видимому, относительно более поздней) устной традиции
                дхьянического созерцания эти случаи классифицируются по разновидностям составляющих
                их дхарм в порядке вопросов о том, какие дхармы (присутствуют) в данном случае.
                Понятно, что вопрос «какая мысль?» не может возникнуть в силу того, что я описал как
                первую особенность возникновения мысли.</p>

            <p>Эти вопросы и ответы о дхармах и составляют первый раздел первой части первой книги
                Корзины (Закона, то есть тоже Дхармы) о Дхармах <em>(abhidhamma)</em> Палийского
                канона, книги, называемой Перечисления Дхарм <em>(dhamma-sangani).</em> Этот текст
                прямо восходит к кратким формулировкам <em>(матрикам) признаков</em>, по которым
                дхармы классифицируются по разновидностям, в порядке чисел этих разновидностей.
                Приведу три примера таких матрик (слова в скобках добавлены мною для понятности):
            </p>

            <p>(а) дхармы (бывают по признаку обусловленности) — обусловленные и необусловленные;
                (b) дхармы (бывают по признаку их кармического эффекта в последующих рождениях) —
                благие, неблагие и неопределенные; (с) дхармы (бывают по признаку причинности) —
                причинные, непричинные и не причинные и не непричинные. Именно из повторного
                созерцания (зрительно-слухового, или, так сказать, эйдетического) дхарм и
                генерировались тексты, подобные приведенному ниже.</p>

            <p>III. 10 (14). Первый (случай возникновения) мысли.</p>

            <p>Каковы (тогда будут) благие дхармы? В том случае, когда мысль возникает (А) в сфере
                чувственности <em>(kama-vacara),</em> (В) сопровождается хорошим (веселым, легким)
                умонастроением, (С) сопряжена со знанием и (D) имеет своими объектами
                <em>(arammana)</em> объекты зрения (то есть <em>ruра</em> здесь — видимые формы),
                или слуха, или обоняния, или вкуса, или осязания, или разума (то есть дхармы,
                которые полагаются здесь объектами разума как шестого органа чувств), или что угодно
                другое (что оказывается в месте возникновения этой мысли), — в этом случае (такие
                будут) дхармы (ДД здесь — наше обозначение дхарм):</p>

            <p>Д 1. Контакт (<em>phassa</em>, седьмая <em>нидана</em> Взаимообусловленного
                Возникновения во второй лекции); 2. чувствование (или эмоциональные состояния,
                vedana, шестая <em>нидана</em>); 3. восприятие в понятиях (или "идеях", samjna); 4.
                (сознательные) интенции (cetana); 5. мысль (citta); 6. рассуждение (далее следует
                перечисление еще пятидесяти <em>дхарм</em>) — вот какие в этом случае (будут) благие
                дхармы, а также и другие дхармы без формы (<em>arupino</em>), возникающие
                взаимо-обусловленно (вместе с данной мыслью).</p>

            <p>Д 1. Что такое, в этом случае, контакт? Это контакт (с тем, с чем возникает
                соприкосновение), это само соприкосновение, действие соприкосновения, это то, с чем
                (возникает) соприкосновение. Вот что такое, в этом случае, контакт.</p>

            <p>Д 2. Что такое, в этом случае, чувствование? Это то, что (возникает в) сфере <em>сознания
                    ума</em> (<em>manovijnanadhatu</em>) как приятное, то приятное уму, что возникает из
                контакта ума (с объектом), то, что ощущается умом как приятное умственно. Вот что
                такое, в этом случае, чувствование.</p>

            <p>Д 3. Что такое, в этом случае, восприятие? Это — восприятие, возникающее из контакта
                с воспринимаемым в сфере сознания ума, действие восприятия, возникающее из контакта
                с воспринимаемым в сфере сознания ума, действие восприятия, воспринимаемое,
                состояние восприятия. Вот что такое, в этом случае, восприятие.</p>

            <p>Д 4. Что такое, в этом случае, сознательные интенции (<em>cetana</em>; они
                соответствуют санскарам, энергиям связи)? Это — интенции, возникающие из контакта (с
                воспринимаемым) в сфере сознания ума, интенции восприятия, (интенциональный) акт
                восприятия. Вот что такое, в этом случае, сознательные интенции.</p>

            <p>Д 5. Что такое, в этом случае, мысль? Это — мысль, ум, умственное, состояние ума,
                умонастроение, способность разума (<em>manindriya</em>), сознание, агрегат сознания
                (<em>vijnanak-handha</em> - один из пяти агрегатов индивидуального существования).
                Вот что такое, в этом случае, мысль.</p>

            <p>Д 6. Что такое, в этом случае, рассуждение? Это — рассуждение, намерение рассуждения,
                вывод, ход рассуждения, приложение (ума) в рассуждении. Правильное конструирование
                понятий в рассуждении. Вот что такое, в этом случае, рассуждение.</p>

            <p>(Далее следуют объяснения случаев остальных пятидесяти дхарм.)»<a href="#citation-1"
                                                                                 id="ref-1"
                                                                                 class="citation-mark"><sup>1</sup></a>
            </p>

            <p>Состав каждого случая четко разделяется на четыре части, за которыми следует
                перечисление дхарм данного случая. Эта четырехчастность постоянна для всех случаев,
                хотя содержание частей в каждом случае (точнее, в каждой разновидности случаев)
                будет изменяться (как иногда и число дхарм, подлежащих объяснению). Первая часть
                (А), то есть сфера (<em>avacara</em>), в которой возникает мысль, — важнейший
                элемент того, что можно было бы только условно назвать буддийской космологией. Этих
                сфер три: сфера чувственного (kama, как в нашем случае), сфера формы (<em>ruра</em>)
                и сфера, где нет формы (<em>аruра</em>). Напрасно было бы искать их на карте, ибо
                они обретают свой буквальный смысл только в отношении определенных йогических
                (точнее, дхьянических) состояний, сами таковыми не являясь. Так, первая из них — это
                сфера действия всех шести органов чувств при преобладании осязания (контакта,
                соприкосновения) и одновременно при отсутствии дхьянического созерцания. Сфера — это
                <em>образ места</em> дхьянического созерцания, места, в котором уже измененное
                сознание направлено на конкретные йогические объекты (<em>kasina</em> —
                этимологически, возможно, «борозда»). Мир, как он есть (включая нормальную психику и
                нормальные состояния сознания), не может себя созерцать без предварительной
                трансформации себя в созерцаемый объект. В нашем тексте «сфера» является одним из
                факторов такой трансформации. Тогда первая сфера, сфера чувственности, в которой в
                данном «случае» возникает мысль, является дхьяническим «образом места», где нет йоги
                или, по крайней мере, высших ступеней последней. Сфера формы является местом, где
                созерцаемые объекты лишены некоторых чувственных характеристик психики, где объекты
                созерцаются как нечувственные формы сознанием, уже значительно йогически
                трансформированным на всех четырех ступенях (иногда их пять) дхьяны. И наконец,
                третья сфера, бесформенности, генерируется только четырьмя высшими состояниями йоги
                сосредоточения. Сознание здесь уже полностью трансформировано и объекты
                сосредоточения (<em>samadhi</em>) созерцаются как лишенные <em>всех</em> чувственных
                характеристик, так же как и отсутствия этих характеристик. Так, здесь нет ни формы,
                ни не формы, ни протяженности, ни не протяженности, ни цвета, ни не цвета и т. д.
                Это предел йоги, так сказать, то место, за которым йога уже ничего не генерирует. Из
                всего сказанного о «сфере» ясно видно, что она, с одной стороны, обозначает <em>все,
                    что созерцается в дхъяне и самадхи</em>, а с другой — <em>все, что
                    созерцает</em>, то есть уже трансформированное йогой сознание. Три сферы
                практически «покрывают» весь мир, как мир реально или потенциально трансформируемых
                в созерцании вещей и событий, устанавливая этим некоторую «область йоги», которая
                «космологически» соответствует вертикальному (иерархическому) расположению трех
                миров буддийской (да отчасти и древнеиндийской в целом) мифологии: миру плотских
                существ, миру богов и миру высших (трансцендентальных) богов. В этом смысле эта
                область представляется <em>промежуточной</em> между феноменальным миром и не-миром
                нирваны. (Последнее — только весьма условно, ибо, буддистически говоря, даже
                трансцендентальная йога <em>не ведет</em> к нирване, хотя и соответствует Телу
                Вкушения (<em>sambhogakaya</em>) Будды.)</p>

            <p>Вторая часть (В) состава «случая» описывает общее психическое (скажем даже,
                эмоциональное) состояние, которым сопровождается возникновение мысли. В нашем случае
                это хорошее (или легкое, приятное) умонастроение. В другом случае оно может быть
                плохим (тяжелым), в третьем — никаким (то есть, йогически, высшим). В (В) можно
                видеть пример <em>редукции психологического</em>, хотя эта редукция возможна опять
                же только в дхьяническом созерцании; в не-йогическом состоянии нормальная,
                естественная тенденция сознания — развивать и распространять психическое, утрировать
                и усложнять жизнь эмоций, настроений и мотиваций. Йогу же предписывается настроений
                и мотиваций не иметь категорически, о них не говорить, так же как и о здоровье, и о
                болезнях, своих или чужих. Здесь философски интересно заметить, что (В) может
                мыслиться <em>как переменная созерцаемой психики</em> (подобно тому как (А) может
                мыслиться как <em>переменная созерцающего сознания)</em>.</p>

            <p>Третья часть (С) состава, знание (<em>nana</em>), с которым в нашем случае сопряжена
                возникающая мысль, может мыслиться как <em>не психическое</em> или, во всяком
                случае, вторичное в отношении психики. Это видно хотя бы из того, что знание может и
                <em>не быть</em> сопряжено с мыслью (как в ряде других случаев). То есть место
                знания может занимать как его отсутствие или незнание (вещи, кстати, разные), так и
                высшее знание (<em>panna</em>) или никакое. В нашем случае это, по-видимому,
                какое-то нормальное (или, скажем, здоровое) знание вещей, необходимых на пути к
                пробуждению либо благоприятствующих вступлению на этот путь. Оно не обязательно
                ведет к высшему знанию, являясь, скорее, средней общей нормой, объективированной в
                созерцании. В комментариях оно анализировалось как простое знание, вместе со
                способами его получения и правилами его экспозиции. Философски здесь важнее всего
                то, что такое знание не является универсально присущим ни возникающей мысли (по
                составу «случая»), ни живому существу, ни даже человеку.</p>

            <p>Четвертая часть состава «случая», (D), объекты (органов) чувств (<em>ārammaṇa</em>),
                которые, однако, не являются объектами в смысле нашего философского понимания этого
                слова. Последнее обязательно исходит — даже если оставить в стороне вопрос «Чьих
                органов чувств эти объекты?» — из традиционного отделения объекта от его органа
                чувств. В понимании же «случая» возникновения Мысли, объекта <em>нет</em> вне (или
                без) его органа чувств, так же как и органа чувств вне его специфического объекта.
                Вообще в буддизме, как и в большинстве других индийских философий, «орган» мыслится
                скорее как <em>способность, сила</em> (<em>indriya</em>), нежели в более «материальном»
                организматическом смысле (хотя и последнее возможно, как, например, в джайнизме).
                Так (по Отто Розенбергу, из его «Проблем буддийской философии»), «моя» способность
                смотреть на солнце не существует без солнца. Иначе говоря, в предложении «я смотрю
                на солнце» — «я» означает чисто лингвистическую условность приписывания «смотрения»
                несуществующему субъекту, а «солнце» есть как объект, только одна сторона факта
                (феномена, <em>дхармы</em>) «смотрения на солнце», другой стороной которого является
                «смотрение». В нашем тексте (и «случае»), однако, объекты органов чувств
                перечисляются (наряду с другими объектами) именно как объекты возникшей мысли, то
                есть опять же как уже готовые «штучки», или «вещички», которые <em>всегда там</em>,
                где появляется мысль. Тогда нам, вообще говоря, все равно, об объектах каких органов
                чувств может идти речь, будь то осязание или разум, ибо они уже объективированы в
                созерцании как объекты мысли (<em>cittaram-mana</em>). То есть в мысли о солнце как
                об объекте органа зрения «зрительность» редуцирована к «мыслительности», так
                сказать. Последнее является лишь одной из конкретизации сказанного в тексте
                <em>I.</em> 1 (1): «Все дхармы мысленны». Объект — это объект мысли. Других объектов
                нет, как и «мира объектов». Как и возникновения мысли без ее объекта.</p>

            <p>Переходя к основному содержанию «случая» Возникновения Мысли, дхармам (ДД), я должен
                сделать три дополнения к тому, что было о них сказано в первой лекции. Первое. В
                нашем, как и во многих других текстах Абхидхаммы (на пали <em>abhidhamma</em>, на
                санскрите <em>abhidharma</em> — учение о дхармах и название текстов, излагающих это
                учение), дхармы созерцаются и осмысливаются в <em>порядке их классификации</em>.
                Здесь можно увидеть прямую аналогию с осмыслением в современной физике —
                <em>атома</em> (это ведь тоже «такая штучка», или «вещичка»). Так, в одних случаях
                физик может говорить об атоме вообще, ну как, например, когда Ричард Фейнман
                говорил, что «все тела состоят из атомов», или когда говорят о «хаотическом движении
                атомов». Тогда, в принципе, такое выражение, как «один атом... другой атом», следует
                понимать буквально в смысле наблюдения двух дискретных объектов, и «2» будет
                означать «два атома». В других случаях физик, говоря об атомах, будет иметь в виду
                конкретные разновидности атомов, то есть атом железа, атом гелия или атомы тяжелых
                элементов, атомы нейтральных газов и т. д. Подобно атому, говоря о <em>дхарме</em>,
                мы можем сказать «одна дхарма... другая дхарма» в смысле любых феноменов, любых
                состояний сознания, но одновременно понимать дхармы и как таксоны, единицы
                классификации дхарм. Тогда в выражении «какие будут дхармы в случае, когда мысль
                возникает... и т. д.» слово «дхарма» будет буквально означать «разновидность дхарм».
                В нашем «случае» (самом обширном по составу дхарм) речь идет о 56-ти разновидностях
                дхарм в дхьяническом созерцании Возникновения Мысли. Притом, разумеется, что оно
                само тоже дхарма, но в первом, не классификационном смысле этого слова.</p>

            <p>Классификация дхарм в нашем «случае» явно <em>политетическая</em>, то есть основанная
                на двух и более признаках, в отличие, скажем, от <em>монотетической</em>
                классификации дхарм на обусловленные и необусловленные, где признак один —
                обусловленность (см. приведенные выше <em>матрики</em>).</p>

            <p>Второе дополнение. <em>Как разновидности, то есть взятые только в классификационном
                смысле, дхармы обладают определенным мыслимым содержанием</em>. Это философское
                положение, сформулированное мною чисто <em>этологически</em>, то есть с точки зрения
                <em>моей</em> философской позиции. Чтобы это было понятнее, приведу пример
                противоположного суждения, а именно: <em>все дхармы</em> (взятые в
                неклассификационном смысле, см. здесь выше) <em>не имеют мыслимого содержания</em>.
                Это тоже философское суждение, но не мое, а буддийское (точнее, абхидхармистское),
                которое я принимаю наравне с первым, является <em>эмическим</em>. (В специфически
                философском смысле этого термина, <em>эмическим</em> будет такой подход к описанию
                объекта, в котором я буду исходить из того, что он сам о себе думает. Точнее, из его
                собственных правил описания себя самого. Тогда как этологическим будет подход, в
                котором я сам формулирую правила описания объекта.) Эти два положения — ядро
                философии дхарм. Теперь, возвращаясь к нашему первому положению, спросим: какое же
                будет мыслимое содержание у дхарм данного «случая»?</p>

            <p>Если говорить о ДД 1-5, то их содержание будет, конечно, психологическое, но только в
                смысле терминов, которых описывается психика. Последнее уже предполагает, что
                психика, как материал, предмет, может описываться (исследоваться, созерцаться,
                наконец) и как не-психологическое. В этом смысле Д 1, «контакт», занимает особое
                положение как все-таки более или менее «телесный», «материальный» контакт органов
                чувств с их объектами [об этом выше, в (D)], в этом отношении резко отличаясь от
                остальных четырех дхарм и приблизительно (и очень упрощенно) соответствуя «психике»
                в ее Павловско-Шеррингтоновском понимании. ДД 2-4, «чувствование», «восприятие в
                понятиях» и «сознательные интенции», выступают как три разных модальности одного и
                того же — «сферы сознания ума», которая и есть Д 5, «мысль» (<em>citta</em>), но не
                та, о которой мы говорим как о возникшей (<em>utpanna</em>), а та, которая возникает
                при возникновении первой. Тогда, возвращаясь к тому, что было сказано выше о солнце
                как объекте зрения, мы могли бы сказать: да, с точки зрения «контакта солнца
                (психологически — объекта зрения) с органом зрения», контакт не ментален, не
                сознателен и как таковой противостоит мысли (Д 5). На что буддийские учителя
                Абхидхармы тотчас бы возразили: но ведь у «контакта» не может быть точки зрения, она
                может быть только у «мысли». Таким образом, — и это и есть «дхармическая» философия
                — содержание, в нашей первой (да и второй) формулировке, может быть только <em>мысленным</em>
                содержанием. Иначе говоря, такая «телесная» дхарма, как контакт, может быть
                «телесной» (и дхармой!) только в соотношении с «мыслью» (Д 5), возникающей при
                возникновении <em>мысли</em> и в <em>составе</em> данного случая. При том,
                разумеется, что возникшая мысль, вместе со всем составом ее «случая» (включая
                «мысль» Д 5 и еще по крайней мере 8 дхарм, синонимичных мысли), сама возможна только
                как объект созерцания мысли йогической.</p>

            <p>Все дхармическое разнообразие состава нашего «случая» (от Д «телесная легкость» до Д
                «правильное сосредоточение мысли» и от Д «ложные идеи» до Д «высшее знание») есть
                результат <em>отбора</em> того, что именно <em>здесь и сейчас</em> созерцается <em>как</em>
                дхармы. Отсюда возможность частного и чисто номиналистского определения: дхармы —
                это то, что называется дхармами (или перечисляется как дхармы) в (любом) данном
                случае возникновения мысли и во всех случаях, взятых вместе.</p>

            <p>Первые 5 ДД соответствуют (точнее — изоморфны) пяти агрегатам (<em>skandha, khandha</em>) индивидуального существования, учение о которых составляет особый
                раздел буддийской философии сознания. Совсем вкратце об этом учении. Мы уже знаем,
                что «Я» как субъекта приписываемых ему фактов сознания, как особой «вещи», могущей
                существовать и <em>вне</em> этих фактов, — нет. Но что значит «нет»? Ведь в силу
                Взаимообусловленного Возникновения ничего нет, а все — возникает (или не возникает).
                Поэтому и реально существующего «Я» нет, ибо — как во всякой древней индийской
                философии — реально только сущее (<em>sat</em>), то, что не возникает, а
                <em>есть</em> и, таким образом, не связано причинно—следственной связью ни с каким
                другим событием, что также невозможно в силу универсальности Взаимообусловленного
                Возникновения. Значит, «Я» может фигурировать и в отношении возникновения только как
                условность языка, как именно идея (samjna), не имеющая своего коррелята не только в
                бытии, но и в возникновении. Тогда мы можем сформулировать «введение» этой
                условности следующим образом: то, что мы условно (или ошибочно — по другим текстам)
                называем «Я», — это пять агрегатов <em>дхарм</em>: (материальная) форма
                (<em>ruра</em>), чувствование (<em>vedaпа</em>), идеи (<em>samjna</em>, также
                вербализированное восприятие), энергии связи (<em>samskaras</em>, также волитивные
                акты) и сознание (<em>vijnana</em>).</p>

            <p>Трудно себе представить — в не-буддийском описании, конечно, — более общее место или
                более избитую формулировку, которая повторяется в тысячах книг и статей о буддизме
                за последние два века. Только в смысле позиций Взаимообусловленного Возникновения и
                Возникновения Мысли она может быть объяснена философски. Вот одно из нескольких
                возможных объяснений: пять агрегатов — это то, как сознание (или мысль) себя
                конструирует, представляя себя одним сложным целым, состоящим из одновременно
                возникающих (действующих, работающих) элементов (мыслей, дхарм и т. д.). Это
                объяснение можно было бы, с нашей точки зрения, назвать <em>объективным</em>,
                поскольку оно возможно только на уровне другого сознания — то есть сознания,
                йогически наблюдающего другие, сознания и себя само как <em>другое</em>, — и
                поскольку наблюдаемое «конструирующее» сознание само себя не осознает как пять
                агрегатов и может ошибочно думать о себе как о «Я». И этим объяснением
                предполагается, что какие-то процессы мышления уже завершены и что к ним не будет
                возвращения в порядке бесконечных регрессивных рефлексий. Но вернемся к самим
                агрегатам. Д 1, «контакт» (<em>phassa</em> — букв, «осязание») здесь замещает работу
                всех органов чувств (кроме ума), соответствуя первому из пяти агрегатов, форме (<em>ruра</em>
                    — в смысле «зрения»), и, как последняя, является единственной
                разновидностью агрегатных состояний дхарм, которая <em>не мыслит</em> (или не
                осознает). То есть и «контакт», и «форма» относятся к мышлению (и являются им!) как
                его пассивная сторона, как мыслимое (об этом в следующей лекции). Остальные 4 ДД
                буквально те же, что и 4 следующих агрегата. И там, и там сознание фигурирует как
                синтезирующий уровень, на котором мысль знает не-мысль (мыслимое) и от которого она
                может перейти к знанию не-мысли как мысли и себя как «не-Я». Это и есть то
                йогическое знание, в котором мысль созерцается как конструирующая себя в виде
                особого объекта — пяти агрегатов. Знание — как результат йогической объективации
                сознания, а не «Я» (ибо такого объекта нет, не говоря уже об одушевленном существе
                или человеке). Именно объективации, а не абстракции, хотя возможны случаи, когда
                философские результаты обеих будут одними и теми же.</p>

            <p>Третье дополнение. Еще раз (последний) возвращаясь к тому, что было сказано о дхармах
                в первой лекции и об их содержании (или его отсутствии) — в этой, я хочу снова
                напомнить: сущность понимания дхарм (именно сущность их <em>понимания</em>,
                поскольку у них самих сущности нет!) в том, что мы идем от <em>слова</em> «дхарма»,
                а не от его значения. Мы всегда <em>уже</em> знаем, что такое дхарма, в порядке ее
                йогически «предуготовленного» знания, которое реализуется как знание в нашем смысле
                только задним числом. Тогда мы говорим: «это — дхарма», «все — дхармы» и т. д.</p>

            <p>Теперь несколько замечаний о философии возникновения мысли. Первое и, наверное, самое
                важное — это то, что всякая мысль есть возникновение мысли. Более того, только мысль
                возникает, ничто другое не возникает. Отсюда делается вывод, что возникновение — это
                только возникновение мысли. То есть <em>все, что возникает, есть мысль</em>. При
                всей тавтологичности последней формулировки, она имела очень интересные последствия
                для формирования и развития философии школ Большой Колесницы. Ведь здесь нет
                онтологического противопоставления мысли — не мысли. Потому, что нет онтологии. А
                онтологии не может быть в силу Взаимообусловленного Возникновения. Тогда единственно
                возможным будет противопоставление по признаку обусловленности: обусловленная мысль
                — необусловленная мысль (но никак не противопоставление, скажем, мысли тому, о чем
                эта мысль, поскольку мы уже знаем, что все ее объекты всегда уже включены в случай
                ее возникновения). Снятие этого противопоставления, об одном случае которого я буду
                говорить в следующей лекции, было весьма важным шагом в переходе от того, что я бы
                условно назвал буддийским «не-монизмом», к тому, что я бы столь же условно обозначил
                как буддийский «недуализм» в философии мысли.</p>

            <p>Второе замечание носит скорее общеметодологический характер. Нормальное, не
                йогическое мышление может схватить мысль только в ее агрегатном, комплексном
                состоянии, которое не может быть редуцировано к возникновению мысли. Но и в
                йогическом созерцании мысль схватывается только как мгновенный, атомарный факт. Ее
                «путь» неизвестен. Она, строго говоря, не имеет происхождения (<em>anutpanna</em>).
                Об обусловленности ее возникновения можно говорить только в смысле ее возникновения
                <em>вместе с</em> тем, что мы называем термином «случай». Ее возникновение
                <em>без</em> «случая», то есть как <em>необусловленной</em> мысли полагается
                недоступным не только йогическому созерцанию, но и Высшему Знанию Будд и Бодхисаттв.
            </p>

            <p>Третье замечание — о времени. Мы говорили выше о том, что возникновение мысли играет
                роль своего рода «единицы времени» и что оно не отделено во времени от своего
                «случая». Но и сам «случай», как бы он ни бы, сложен по своему составу, также не
                имеет своего «внутреннего» времени. Его скорее можно было бы представить как <em>топос</em>
                (от греч. τόπος — «место, пространство») возникновения мысли. Время имеет смысл только когда речь идет об агрегатных, сложных
                состояниях мысли, или, говоря иными словами, об эмпирически воспринимаемых событиях.
                В этом смысле время — это категория, характеризующая наше восприятие более, чем
                воспринимаемые им объекты, то есть категория квазипсихологическая. В <em>дхьяне</em>
                объекты созерцаются синхронно.</p>

            <p>Все, что здесь было сказано о мысли, — очень трудно понять прежде всего потому, что
                дхармическая философия мысли ранних буддийских школ (да и многих поздних тоже)
                крайне <em>не эпистемологична</em>. Ибо здесь мы имеем дело не с нормальным, а с
                особым йогическим знанием. Эпистемология в буддизме начинается либо когда нормальное
                знание (которое само может явиться одним из <em>объектов</em> йогического)
                выделяется из контекста созерцания, либо когда само йогическое знание становится
                объектом нормального. Если отвлечься от различий между этими двумя знаниями — как
                пример <em>этологического</em> подхода, — приведенный нами текст Возникновения Мысли
                можно, вместе с его объяснением, представить как особую позицию (особую философию
                мысли). Философски главным в этой позиции будет понимание мысли как мысли о чем
                угодно обо всем или ни о чем. При том, что все, о <em>чем</em> она, будет не в ней
                самой, а в «случае» ее возникновения, а сама она остается той же одной, отдельной
                бескачественной мыслью. В этом уже есть возможность того, что позднее получило
                название философии пустоты (<em>sunyavada</em>), о некоторых выводах из которой я
                буду говорить в следующей лекции.</p>

