            <h2 class="text-2xl font-bold text-center text-zinc-700 mb-8">Семинар семнадцатый</h2>
            <h3 class="text-xl text-center font-semibold text-zinc-700 mb-4">Текст XVII.
                Ланкаватра-Сутра<a href="#citation-1" id="ref-1" class="citation-mark"><sup>1</sup></a>
            </h3>
            <blockquote class="my-6 pl-4 border-l-4 border-gray-300">
                <p class="mb-2"><em>0.</em> Так я слышал. Тогда Господь пребывал во дворце Ланки... на вершине
                    горы Малайя, над великим океаном. Он был с великим собранием монахов и
                    неисчислимым множеством Бодхисаттв, пришедших из различных полей Будд.
                    Бодхисаттвы-махасаттвы, возглавляемые Бодхисаттвой Махамати, все искушенные в
                    тайнах различных трансцендентальных самадхи и обретшие помазанье из рук всех
                    Будд. Таковы были эти Бодхисаттвы-махасаттвы, в совершенстве постигшие ту
                    истину, что объективный мир, ими зримый, есть не что иное, как манифестация их
                    же сознания<a href="#citation-2" id="ref-2"
                                  class="citation-mark"><sup>2</sup></a>.</p>
                <p class="mb-4">... Господь улыбнулся и сказал: «Истину эту Татхагаты прошлого обсуждали в своих
                    беседах, истину, постижимую Благородным Знанием<a href="#citation-3" id="ref-3"
                                                                      class="citation-mark"><sup>3</sup></a>,
                    в его сокровенных глубинах. Теперь я, ради блага Раваны, верховного властителя
                    якш<a href="#citation-4" id="ref-4" class="citation-mark"><sup>4</sup></a>,
                    возвещу эту истину. Действием сверхъестественной силы Будды Равана услышал слова
                    Господа. Господь же, взглянув на волны океана и на завихрения мысли и ума у тех,
                    кто собрался вокруг него, стал размышлять о волнах, поднимаемых в
                    сознании-вместилище могучим ветром объективности<a href="#citation-5" id="ref-5"
                                                                       class="citation-mark"><sup>5</sup></a>,
                    ветром, который вносит возбуждение в возникающие семь сознаний<a
                        href="#citation-6" id="ref-6" class="citation-mark"><sup>6</sup></a>. Так
                    размышлял он, погруженный в самадхи, когда Равана стал его просить, чтоб он,
                    ради просветления богов и людей, поведал им эту сутру, восхваляемую Буддами
                    прошлого, и открыл собравшимся тайны глубин сознания, пока не поведанные ни в
                    одном учении.</p>
                <p class="mb-2"><em>I.</em> Сказал Господь: «Три состояния различаются в сознаниях: сознание как
                    развивающееся, меняющееся; сознание как производящее эффект, как причина
                    последствий; сознание как остающееся тем, что оно есть по своей первичной
                    природе.</p>
                <p class="mb-2">В восьми сознаниях<a href="#citation-7" id="ref-7"
                                        class="citation-mark"><sup>7</sup></a> различаются две
                    функции: восприятие и различение объектов. Эти функции питаются таинственной
                    энергией привычки<a href="#citation-8" id="ref-8"
                                        class="citation-mark"><sup>8</sup></a>, аккумулированной
                    посредством ошибочных действий разума за все безначальное прошлое. Однако же, о
                    Махамати, прекращением шести сознаний мы называем прекращение
                    сознания-вместилища и прекращение притока энергии привычки. А это и есть, о
                    Махамати, устранение аспекта формы из всех сознаний.</p>
                <p class="mb-2">Известно, что, кроме пяти сознаний органов чувств, есть еще шестое — сознание
                    ума. Посредством шестого сознания производятся все индивидуальные черты и
                    особенности. В шестом сознании лежит происхождение физического тела. Но в этих
                    шести сознаниях нет мысли о том, что они вырастают из своей привязанности к
                    умственным различениям и определениям, то есть к проекциям ума на мир объектов,
                    принимаемым ошибочно за сам этот мир. Поскольку сознания продолжают свое
                    функционирование, не осознавая своего функционирования, то даже йоги,
                    находящиеся в трансцендентальном самадхи Абсолютного Прекращения, не в состоянии
                    осознавать постоянное действие тончайшей энергии привычки в самих себе».</p>
                <p class="mb-2">И сказал еще Господь: «Сознание ума — это основа и причина как пяти сознаний
                    органов чувств и ума, так и сознания-вместилища. Ум развивается вместе с идеей
                    «Я» и прирастает к ней как к своему постоянному объекту<a href="#citation-9"
                                                                              id="ref-9"
                                                                              class="citation-mark"><sup>9</sup></a>.
                    У самого же ума нет ничего своего: ни знака, ни отметины. Таким образом, когда
                    сознание ума устранено, то этим устраняются все другие сознания. Оттого и было
                    сказано: «Я вхожу в Нирвану, когда прекращается сознание, вырастающее из
                    различений»<a href="#citation-10" id="ref-10"
                                  class="citation-mark"><sup>10</sup></a>.</p>
                <p class="mb-4">И еще: «Ум, произведенный интеллектуальными различениями, сам — чистая кажимость.
                    Мысль — это танцор. Опять же ум — это шут, сознание ума вместе с пятью
                    сознаниями органов чувств создает сцену «объективного» мира».</p>
                <p class="mb-2"><em>II.</em> «Вот четыре причины, о Махамати, по которым возникает сознание
                    зрения. Первая — привязанность к внешнему миру; вторая — привязанность к форме и
                    в силу энергии привычки; третья — действие внутренней природы, свабхавы,
                    заложенной в сознании; четвертая — склонность к восприятию разнообразия форм
                    явлений. Эти-то четыре причины и ведут к возмущениям и завихрениям в
                    сознании-вместилище.</p>
                <p class="mb-4">Мира, как мы его воспринимаем, просто нет. Многообразие вещей и явлений
                    порождается умом».</p>
                <p class="mb-2"><em>III.</em> Трансцендентальные самадхи, Абсолютное Прекращение и все четыре
                    созерцания, дхьяны не обретаются в том, что мы называем только мысль или только
                    сознание, ибо там нет ничего, кроме самих мысли или сознания. Ведь только там,
                    где есть освобождение от дуализмов «обусловливающее/обуславливаемое», «то, что
                    зависит/то, от чего зависит», только там есть «ничего кроме мысли», «ничего,
                    кроме сознания». Отсюда — мое Учение о сознании, полностью исключающее
                    существование и не-существование, сконструированные интеллектом.</p>
                <p class="mb-2">Таковость, Пустота, Высшая Область Дхармы — вот что такое мысль и сознание без
                    чего-либо другого. Да еще и сверхъестественные тела, сотворенные волей и умом<a
                        href="#citation-11" id="ref-11" class="citation-mark"><sup>11</sup></a>, они
                    тоже принадлежат «только мысли» и «только сознанию». Всякая множественность
                    объектов и различительных свойств порождена умом и сознанием как таковыми,
                    ничего другого как такового нет. Оттого мое высшее трансцендентальное Знание не
                    признает ни Большой Колесницы, ни Малой, ни каких-либо иных истин, школ или
                    учений, противопоставляющих себя другим.</p>
                <p class="mb-4">Весь мир — это чистая интеллектуальная конструкция ума, не постигшего себя как
                    конструктора, а мир как сконструированный. Только ум, освобожденный от всех
                    условий и не опирающийся на мысль о себе, не пребывает более в теле, достигает
                    самадхи майи из которого создает любые умственные тела и тела воли.</p>
                <p class="mb-4"><em>IV.</em> Будда сказал: «Когда те, кто рожден Буддой, постигнут, что весь мир
                    — это ум и сознание, они возобладают телом трансформации. Это такое тело,
                    никакие действия которого не производят кармического эффекта, но которое имеет
                    сверхъестественные свойства и способности. Подобно мысли, оно беспрепятственно
                    движется по всей вселенной, а также может перемещаться во времени силой
                    обратного вспоминания как в прошлое, так и в будущее. Будда, сотворивший из
                    своего ума такое тело, является Буддой Трансформации, но одновременно он
                    существует и как Будда Дхармичности. Только как Будда Трансформации достигает он
                    непостижимую Трансформационную Смерть, никому, кроме Будд, недоступную. Что же
                    до Будд-Татхагат, то они — не Будды Трансформации, но и не не-Будды
                    Трансформации».</p>
                <p class="mb-2"><em>V.</em> Господь: «Совершенное Знание, о Махамати, вот в чем сущность Зародыша
                    Татхагаты».</p>
                <p class="mb-2">Махамати: «Господь же описал Зародыш Татхагаты как Свет. Он обладает тридцатью
                    двумя превосходными знаками (тела Будды), но скрыт в теле каждого живого
                    существа подобно драгоценному камню, завернутому в грязную тряпку... »</p>
                <p class="mb-2">Господь: «Зародыш Татхагаты — это Пустота, Нирвана и Абсолютный Предел Сущего. Он
                    содержит в себе добро и зло и производит все формы существования. Подобно
                    актеру, он принимает бесконечные обличья, сам оставаясь без «Я» и какой-либо
                    самости».</p>
                <p class="mb-4">Господь: «Понимаемый как сознание-вместилище, Зародыш Татхагаты развивается
                    вместе с семью сознаниями, которые мгновенны, ибо имеют своей причиной энергию
                    привычки. Они подвержены нечистым притокам кармической энергии и не переходят из
                    рождения в рождение живого существа, только Зародыш Татхагаты переносится с
                    последним, оставаясь причиной всего зла и страдания, так же как и причиной
                    Нирваны. Извечно чистая мысль — вот что такое Зародыш Татхагаты. Он — высшее
                    благо, ошибочно привязанное к «Я» и «Себе». Он свободен от ограничений и
                    неограниченности».</p>
                <p class="mb-4"><em>VI.</em> Будда сказал: «Те, кто видят вещи, как эти вещи виделись до того и
                    как они виделись и видятся (всеми) другими, — те не видят Будду, даже преодолев
                    в своем уме привычку различительности и оперирования противоположностями. Но
                    только мир начинает восприниматься как он есть, происходит Переворот Ума».
                    Услышав эти слова, повелитель Ланки тут же достиг Пробуждения и пережил
                    Переворот Ума<a href="#citation-12" id="ref-12"
                                    class="citation-mark"><sup>12</sup></a>, постигнув ту истину,
                    что мир, как он есть, это только мгновенная проекция его собственного ума. Тогда
                    Будда сказал: «Покончить со всем словесным потоком, отбросить все, сказанное
                    тобой и другими, отшвырнуть прочь все идеи и понятия, связанные с «Я» и «Собой»
                    — в этом и состоит Переворот Ума». И еще: «Ведь Нирвана — это
                    сознание-вместилище, в котором уже случился Переворот Ума посредством
                    Запредельного Знания». Когда же Бодхисаттва Махамати спросил: «Что же это такое,
                    Переворот Ума, не есть ли он состояние без-образности, испытываемое
                    Бодхисаттвами?», Господь отвечал: «Те же, о Махамати, кто страшится страданий,
                    не понимая, что страдания возникают из иллюзорного противопоставления рождения
                    смерти, кто стремится к Нирване, вообразив ее себе как полное уничтожение
                    органов чувств и объектов чувств, те не осознают, что Нирвана и есть
                    сознание-вместилище, в котором произошел Переворот Ума». И еще: «Бодхисаттвы,
                    достигшие восьмой ступени, испытывают Переворот Ума, вырвутся за пределы мысли,
                    ума и сознания и перевернут всю систему психики и сознания. Это ведь и есть
                    состояние, где нет ни порождения, ни уничтожения, которое Я называю Нирваной».
                    Потом он сказал: «Пусть кармические силы еще действуют, но когда двойственность
                    различительного — мыслящего в понятиях и словах — устранена посредством
                    Запредельного Знания и Переворот Ума уже сотряс сознание-вместилище, тогда
                    Бодхисаттва пребывает во Дворце Лотоса, порожденном в сокровенных глубинах
                    майи». И наконец: «Когда сознание ума перевернуто, сама мысль становится
                    Буддой». Затем Господь заключил так: «Для перевернувшего свой ум, для глядящего
                    вспять и испытавшего Переворот Ума, для опирающегося на Запредельное Знание, для
                    него нет нужды ни в законах и правилах логики, ни даже в тетралемме. Он — мой
                    сын».</p>
                <p class="mb-2"><em>VII.</em> Господь сказал: «...Теперь, о Махамати, поведай-ка
                    Бодхисаттвам-махасаттвам о трех свабхавах. Первая свабхава — это ложная
                    различительность, на основании которой создаются конструкции мысли, которые
                    нереальны, ибо привязаны к имени и кажимости вещей, к их внешним признакам,
                    индивидуальным и родовым. Вторая свабхава — это знание релятивности,
                    соотносительности всех вещей, но все же основанное на кармически питаемой
                    интуиции их дуализма «субъект/объект». Третья свабхава — это Совершенное Знание,
                    которое и есть сущность Зародыша Татхагаты и знание Таковости. К этому остается
                    добавить, что все просто мыслящие идиоты и невежи льнут к первой свабхаве, ибо
                    привычно мыслят о вещах и явлениях как существующих либо не-существующих. Даже
                    приверженные ко второй свабхаве не могут избавиться от дуализма
                    «существование/не-существование»<a href="#citation-13" id="ref-13"
                                                       class="citation-mark"><sup>13</sup></a> и от
                    противопоставления одного многому. Только мудрецы знают, что иллюзия, майя,
                    каковой являются все явления и вещи, не является ни одной для всех феноменов, ни
                    разной для каждого из них в отдельности».</p>
                <p class="mb-4">«Соответственно трем свабхавам, есть и две условные истины, и одна абсолютная. И
                    еще, о Махамати. «Природа мира объектов» — одна, она и есть «только мысль» и не
                    знает дуализма «есть/нет» или «одно/многое». Наши же понятия природы, свабхавы и
                    истины относятся ко второй свабхаве, паратантре.</p>
                <p class="mb-4"><em>VIII.</em> Господь сказал: «Нет, о Махамати, это мой Зародыш Татхагаты,
                    каковой должно знать как абсолют, как Предел Реальности. Не-рождение, говорите
                    вы? Есть еще и другое нерожденное, недостижимое, непознаваемое, что, хотя и
                    нерожденное, родилось до вселенной. Он — сущность всего. Но помните, нет и не
                    было начала перерождений».</p>
                <p class="mb-4"><em>IX.</em> Господь сказал: «Не-дуальность, о Мохамати, значит, что все
                    противоположности относительны. Черное и белое, есть и нет, Нирвана и сансара не
                    имеют ни своего самостоятельного существования, ни не-существования. Там, где
                    нет самсары, не может быть и Нирваны, ибо условия существования не являются
                    взаимоисключающими по самой своей природе. Сознание подвержено рождению и
                    прекращению, а знание не-подвержено им. Сознание отмечено дуализмом
                    «формы/не-формы», «бытия/не-бытия». Благородное Знание трансцендентно. Не
                    приходит оно и не уходит, подобно отражению луны в воде».</p>
                <p class="mb-4"><em>X.</em> Господь сказал: «Карма аккумулируется мыслью и познается
                    различительным знанием. Само знание различается и неразличается с тем, что оно
                    знает. Он не выразим и не-невыразим. А что невыразимо, то не рождено. А что не
                    рождено, то неуничтожимо и подобно пространству, которое не может быть ни
                    причиной, ни следствием чего-либо, то есть служит другим именем для Татхагаты,
                    когда его Тело Дхармы обретает форму Тела Ума, нерожденного».</p>
                <p class="mb-4"><em>XI.</em> Господь сказал: «Рождение и смерть следуют друг за другом без
                    интервалов и перерывов, но это — не для идиотов. Различительное знание следует
                    через смерть и рождения по путям шести сознаний. Неведение является причиной. Но
                    умы и сознания возникают везде еще до того, как возникает форма (тело), и
                    являются местом для среднего существования<a href="#citation-14" id="ref-14"
                                                                 class="citation-mark"><sup>14</sup></a>.
                </p>
                <p class="mb-4"><em>XII.</em> И в конце Господь сказал: «Все сознания мгновенны, потому что
                    возникают из кармических следов и под влиянием притоков. Но они не
                    перерождаются, только Зародыш Татхагаты переходит из рождения в рождение и
                    является причиной Нирваны, страдания и удовольствия».</p>
                <p class="mb-4"><em>XIII.</em> Господь сказал: «Пять Татхагат или пять Тел Трансформации не
                    являются настоящими Буддами-Татхагатами. Они не подвержены карме в своем
                    возникновении. Нет в них буддства или татхагатства, но нет его и вне их. Они не
                    учат Дхарме ради блага всех живых существ. Ибо Высшая Истина Тела Дхармы
                    принадлежит Абсолютному Пределу. И нет там ни Малой Колесницы, ни Большой, ни
                    Нирваны, ни Будды, ни Бодхисаттв».</p>
            </blockquote>
            <h3 class="text-xl text-center font-semibold text-zinc-700 my-4">Переосмысление
                Йогачара-Виджнянавады и переизобретение Вселенной</h3>
            <p><em>(0)</em> Удивительный текст. Не будем забывать: он писался не для нас, даже уже
                имеющих какое-то представление о виджнянаваде, а для тех, кто, зная основные
                положения этой школы, исходил из другого, нежели наше, представления о том, что
                такое понимание текста. Нам, конечно, хочется перевести философский калейдоскоп
                Ланкаватары с его, казалось бы, полной произвольностью умственных актов воли и
                представления (прямо по Шопенгауэру) в плоскость наших современных представлений о
                мире и нас самих, этот мир себе представляющих. Именно в плоскость, в двухмерную
                поверхность системы мышления, уже отграниченной от того, что этим мышлением мыслимо
                (и от других систем мышления тоже).</p>
            <p>На первый взгляд, бесспорно, этот текст хаотичен, фрагментарен, в нем нет никакой
                системы. Но, спросим, что же нам в нем систематизировать? Ответом будет: две
                совершенно различные вещи. Во-первых, учение школы виджнянавада-йогачара. Во-вторых,
                предмет йогачаринской философии, то есть сознание. Последнее можно было бы
                систематизировать несколькими различными (а иногда и взаимоисключающими) способами.
                Так, в сутре говорится, что она исследует глубины глубин сознания посредством
                Благородной Мудрости (санскр. aryaprajna). Сознание в сутре — не первичная категория в
                нашем понимании и объяснении нас самих и вселенной, но универсальный объект, только
                в отношении которого (и в смысле которого) могут пониматься и мыслиться все другие
                объекты. В то же время — и это уже совсем другой способ — сознание в нашей сутре
                предстает как универсальный предикат всех мыслимых и мыслящих объектов (включая
                мысль, ум и сознание). Эти два чисто эпистемологических способа связаны с двумя
                аспектами сознания как предмета исследования. В своем аспекте универсальной
                объектности сознание изучается дифференциальной виджнянавадой. а в аспекте
                универсального предиката оно является предметом не-дифференциальной
                виджнянавады.</p>
            <p><em>(1)</em> Начнем с вопроса. Какие существуют сознания? Ответ: пять сознаний
                органов чувств, сознание ума и сознание-вместилище, алайя-виджняна. В сознании ума
                различается так называемый «объективный мир». В сознании ума обретает свое
                происхождение и тело — так говорится в сутре. Но тут же добавляется, что у ума нет
                своего тела. Вот здесь-то мы и сталкиваемся с «топологическим парадоксом» в
                рассмотрении сознания-вместилища. Ведь, с одной стороны, утверждается, что это
                сознание имеет тело живого существа как свое «естественное» место. Но, с другой
                стороны, само это сознание трактуется как универсальное место «всего, что есть»,
                «всей вселенной», как какая-то «сверхкосмическая поверхность», «мировой океан»,
                «поток существования». Ибо все, что возникает, возникает в алайя-виджняне, то есть
                возникает как факт ума. Но все возникает в алайя-виджняне не оттого, что оно (это
                «всё») ментально, а, напротив, оно ментально оттого, что возникает в алайя-виджняне.
                И совсем уж виджнянавадинское заключение: как пространство, алайя-виджняна не может
                быть ни существующим, ни не-существующим. Оно также не может себя осознавать (что в
                предыдущем семинаре объяснялось как следствие его чрезвычайной слабости,
                «фоновости»). Таким образом, в дифференциальной виджнянаваде объектный аспект
                алайя-виджняны является и ее пространственным аспектом. Сутра, однако, не ставит на
                этом точку. В ней видно стремление переопределить сознание-вместилище как
                пространство и пространство как сознание-вместилище. Эта космологическая картина
                дополняется тем, что пространство становится «местом» возмущений, завихрений и
                флюктуации сознания или даже самими возмущениями, завихрениями и флюктуациями, ибо
                сознание находится под воздействием двух ветров: «ветра дхармы» (санскр. dharma-vega) и
                «ветра вещности» (санскр. visaya-vega). Ни точный смысл, ни происхождение последних
                выяснить пока не удалось. Кроме того, в сутре «вместилище» (санскр. alaya) объясняется
                и отдельно от сознания, как своего рода пустой контейнер, представляющий собой
                чистую потенциальность сознания и даже полагающийся высшим сознанием-вместилищем.
                Сказанное становится еще более фантастичным, когда мы узнаем, что все возмущения,
                завихрения и флюктуации сознания-вместилища не отделены временем ни друг от друга,
                ни от сознания-вместилища в целом, ибо последнее остается вечно спокойным и не
                возбужденным. Господствует абсолютная синхронность. Генезис каждого отдельного
                сознания вытеснен в его безначальное прошлое, генезис того, чего не было момент
                назад, но что есть сейчас (в созерцании и трансцендентальном самадхи мастеров
                виджнянавадинской йоги). Не будем забывать, что «генезис» здесь практически
                синонимичен «возникновению мысли» классической палийской Абхидхармы и по
                бессодержательности, номинальности, формальности вполне сопоставим с бытием в новой
                европейской философии.</p>
            <p>Теперь спросим: будет ли правильным на основании абхидхармистской аксиомы о
                возникновении мысли допустить возможность не-возникновения мысли? Вопрос трудный;
                классический абхидхармист ответит, что это невозможно. Но с точки зрения
                дифференциальной теории сознания возникновение и не-возникновение мысли будут
                синхронны, хотя, может быть, отделены друг от друга в пространстве, а тогда почему
                бы и не случиться возникновению в какой-то точке космоса сознания? Но почему же
                все-таки сознания, умы и мысли оказываются различными в чистом вневременном
                пространстве сознания? Этому, согласно Ланкаватаре, есть две причины; точнее, за это
                ответственны два постоянно действующих фактора.</p>
            <p>Первый фактор — принцип работы, действия, направления (интенциональности) самого
                сознания, различительного по своей природе. Начиная с различения сознания и
                осознаваемого, сознание создает и воссоздает «свой» мир различных вещей и явлений,
                мир перемешанных друг с другом и противопоставленных друг другу идей, вещей и
                явлений, «мешанину» (санскр. prapanca) сотворенного и несотворенного, осознанного и
                неосознанного, в которой сознание не может навести порядок, ибо оно само такое же,
                основанное на ложной различительности, само часть созданной им же вселенной
                беспорядка.</p>
            <p>Второй фактор является действующим на каждое отдельное (индивидуальное) сознание в
                каждый момент функционирования данного сознания. Или, точнее, он и есть все то
                прошлое, которое актуализируется в сознании, он — энергия актуализации прошлого. В
                Ланкаватаре этот фактор отождествляется с кармой. Карма имеет два аспекта. В своем
                статическом аспекте она — это сумма «отпечатков», «следов», оставленных в сознании
                воздействующим на него миром. В этом она родственна «памяти» (здесь санскр. smerana). В
                своем динамическом аспекте карма — это те же отпечатки и следы прошлых действий, но
                обладающие латентным стремлением к «переносу» сознания в новые рождения (и тела).
                Заметим при этом (о чем шла речь и в предыдущем семинаре), что в случае
                трансцендентального самадхи Абсолютного Прекращения, в котором созерцающий достигает
                пределов индивидуального сознания, прекращается и действие кармы в ее динамическом
                аспекте.</p>
            <p>Теперь важнейший эпистемологический момент. Только посредством йогического знания
                можно узнать, что кармично и что нет и о какой карме идет речь в данном случае.
                Только такое знание знает о карме, что она не есть мысль, а есть энергия
                манифестации мысли (в словах, поступках и так далее).</p>
            <p><em>(2)</em> Переворот Сознания — это интереснейший феномен на границе философии и
                психологии, предмет особого внимания дифференциальной виджияпавады. С одной стороны
                — это предел того, что может случиться с индивидуальным сознанием, и предел
                трансформации сознания, за которым оно уже не может оставаться самим собой. С другой
                стороны, поскольку этот переворот происходит в сознании-вместилище живых существ,
                его основной эффект оказывается направлен на ум и сознание ума. Заметим также, что
                Переворот Сознания не просто «случается» безличным образом в алайя-виджняне, но
                четко сводится к интенциональности йога или Бодхисаттвы, который сознательно
                индуцирует его в «своей» алайя-виджняне. Добавим, что такой йог или Бодхисаттва пока
                еще остается в своем теле и подвержен эффектам (пусть значительно ослабленным)
                кармы. Но его ум и сознание ума радикально изменились. Полностью сломлен сам режим
                различительной работы сознания, устранен, невозможен более и страх смерти,
                происходящий из противопоставления смерти жизни. Иссякло стремление к Нирване как
                противопоставленной Сансаре. Из ума полностью исчезли мысли, идеи и слово «всех»,
                «других», все, «прежде данное» и «прежде высказанное» тобой самим и другими. Так
                твоя мысль становится Буддой.</p>
            <p>Я готов предположить, что идея Переворота Сознания является как бы «йогическим
                рефлексом» на гораздо более фундаментальную философскую (чтобы не сказать
                космологическую) идею об одной вселенной сознания. Можно попытаться представить себе
                эту вселенную как спонтанно работающую, саморегулирующую систему, как гигантский
                «космический автомат» со встроенными в него механизмами корректировки, остановки и
                перемены направления работы. Эти механизмы могут остановить иллюзорную эволюцию
                космической системы, направив «стрелу времени» от конца к началу, от будущего к
                настоящему. Без этих механизмов вселенная уже давно бы иссякла, выдохлась и
                перестала существовать. Это, конечно, не более чем гипотеза, лишь слабо поддержанная
                данными конкретных текстов. Однако эта вселенная, хотя и одна, согласно
                дифференциальной виджнянаваде, «обязана» быть двойной. Ибо, с одной стороны, она —
                внешний мир, как бы «окутывающий» сознание-вместилище, мир оболочки (санскр.
                bhajana-bka). Но с другой стороны, эта вселенная существует только как сумма
                различительных репрезентаций алайя-виджняны, энергетически питаемых кармой. Что же
                касается гипотетического «иссякания» одной вселенной, то опять же возможно
                предположение, что имеется в виду какая-то определенно не-кармическая энергия или
                сила, которая держит вместе, не давая им расходиться, оба мира: так называемый
                «внешний», то есть мир иллюзорных проекций и репрезентаций сознания извне, и
                «внутренний» мир алайя-виджняны, понимаемой как одна и та же алайя-виджняна всегда и
                везде. Возможно, что великий Переворот Сознания, производимый мастерами йоги в
                алайя-виджняне, так же как и другие, менее радикальные изменения, производимые,
                скажем, в трансцендентальных самадхи, вызывают остановки в естественном эволюционном
                процессе и тем самым приостанавливают спонтанный расход космической энергии.</p>
            <p><em>(3)</em> Теперь перейдем к Телу Трансформации, или Телу, сотворенному умом (санскр.
                manomaya-kaya) в Ланкаватара-Сутре. Они имеют огромное философское значение в
                дифференциальной виджнянаваде. Такое тело может сотворить великий йог, Бодхисаттва
                восьмой ступени, или Будда во время их нахождения в самадхи Абсолютного Прекращения
                (отчего такое тело часто называется «Тело Самадхи»). В сутре описываются три
                фактора, действующие при сотворении такого тела. Первым фактором является очищение
                сознания-вместилища от всех иллюзорных различений, уже произведенное посредством
                трансцендентального самадхи, очищение, сделавшее ум гибким и пластичным и
                устранившее все завихрения и флюктуации сознания (в данный момент). Именно эта
                гибкость и пластичность ума превращает его в материал, удобный для произведения из
                него любой формы, тела, человеческого или божественного. Вторым фактором является
                полная не-кармичность Тел Ума, ибо ни сами они не генерированы кармой, ни какое-либо
                их действие не имеет кармического эффекта. Третий фактор в сотворении этих тел — это
                Трансцендентальная Мудрость, достигаемая только на восьмой ступени бодхисаттства.
            </p>
            <p>Определения Тела Ума или Тела Трансформации нет ни в самой Ланкаватаре, ни в книге
                Д. Т. Судзуки, посвященной исследованию этой сутры. Тело Ума неопределимо в обычном
                смысле определения. Вопрос: «Что такое Тело Ума?», с точки зрения дифференциальной
                виджнянавады, бессмыслен, спросить следовало бы об уме, а не о теле, и ответом будет
                что-то вроде: «Тело Ума — это такой ум, который ...» и так далее. Я бы предложил
                следующее весьма примерное определение: Тело Ума — это ум Будды, Бодхисаттвы или
                великого йога, настолько трансформированный в Запредельном Знании и в Великом
                Перевороте, что он может принимать любую мыслимую или мыслящую форму. Здесь
                трансформация изменяет всю природу не только ума, но и тела живого существа, и
                только опытный созерцатель во время пребывания в трансцендентальном самадхи сможет,
                видя какое-нибудь тело, сказать, «настоящее» ли оно или тело трансформации.</p>
            <p>Тела ума никак не взаимодействуют ни с какими другими телами, живыми или неживыми.
                Поэтому они могут беспрепятственно перемещаться в пространстве и во времени
                (последнее — по причине обратного вспоминания, см. в VIII). Только по причине
                полного отсутствия для них любых препятствий они могут распространять Дхарму в любой
                сфере вселенной и помогать освобождению от страдания любым живым существам. Все
                Будды и Бодхисаттвы обладают сверхъестественной способностью мгновенного порождения
                дхармического импульса — эффектом которого явится умственное сотворение любого
                тела.</p>
            <p>Отсюда следует, что каждый Будда или Бодхисаттва в каждый данный момент может
                существовать в одном из трех тел: в своем «натуральном» теле, в теле Ума или в Теле
                Дхармы.</p>
            <p>Феномен Тела Ума радикально изменяет всю систему буддийской (и какой угодно другой)
                космологии, полностью отменяя традиционный философский и теологический дуализм
                сотворенного и сотворяющего, не говоря уже о дуализме естественного и
                искусственного. Гностическое противопоставление креатуры плероме здесь также теряет
                смысл, поскольку полностью исключается возможность конкретно отождествить «творца»
                сотворенного. Антропологически введение Тела Ума уже разбивает единство понятия не
                только человека или лица, но и личности, поскольку вульгарная фундаментальная
                оппозиция «человек/не-человек» нейтрализуется введением особого «третьего»
                контингента Тел Ума, человечность которого принципиально не установима.</p>
            <p><em>(4)</em> Переход от дифференциальной виджнянавады к не-дифференциальной, то есть
                от сознания как универсального объекта к сознанию как универсальному предикату,
                крайне труден. Он нас вынуждает на время отвлечься от онтологических постулатов
                относительно сознания и переключить наше внимание на причудливую эпистемологию
                Ланкаватары, объектом которой является одно Знание, которое знает всё. Это — самое
                неспециализированное из всех возможных знаний. Здесь знающий — это тот, чьим
                единственным стремлением в этой его жизни является достижение Полного Пробуждении.
                Нам же придется пока забыть о трансцендентальной медитации и сосредоточиться на двух
                чисто абстрактных, платонических концепциях — концепции «Ничего, кроме сознания
                (мысли, ума и так далее)» и концепции Зародыша Татхагаты. Но это — только первый шаг
                в нашем переходе к не-дифференциальной виджнянаваде. Вторым будет наше понимание
                того, что сознание — этот «объект всех объектов» в дифференциальной виджнянаваде,
                объект, включающий в себя и всех актуальных и потенциальных субъектов сознания — в
                недифференциальной виджнянаваде не может быть субъектом. Ибо идея о сознании как
                универсальном предикате предполагает полную объективацию любого субъекта и полную
                потерю им всего, что связано с «Я», «Я мыслю» или «Я осознаю». Так мастера
                виджнянавады около VI в. н.э. отменили до сих пор господствующего в европейской
                философии привилегированного субъекта рефлексии.</p>
            <p>Теперь для облегчения нашего (хотя бы приблизительного) понимания сознания в его
                аспекте универсального предиката попробуем ввести понятие «возможного» или
                «виртуального» субъекта рефлексии. Этот субъект будет эпистемологически близок к
                сознанию-вместилищу, трактуемому как возможный субъект сознания, хотя такая
                трактовка была принята далеко не всеми виджнянавадинскими философами. Еще
                радикальнее такой субъект будет условным обозначением пустого места, оставленного в
                алайя-виджняне после ее полной «перетряски» Переворотом Ума. Ибо, следуя за сутрой,
                ум не есть то, что мыслит, сознание — не то, что осознает, все, что есть, — это
                мышление, которое познано как абсолютное отсутствие мыслящего.</p>
            <p>Но ведь это не есть основание для довольно распространенного философски вульгарного
                мнения, что виджнянавада — это субъективный идеализм, солипсизм. Ведь отсутствие
                субъекта мышления здесь — эпистемологически является тавтологией отсутствия объекта.
                Ибо формула «Все — только сознание, нет ничего, кроме сознания» не должна пониматься
                в смысле «только сознание есть, ничего, кроме него, не есть». Ибо ее реальный
                философский смысл должен читаться: «все осознаваемое, все мыслимое существует только
                как осознаваемое и мыслимое, только в сознании и мышлении, а не само по себе». И уж
                если давать этому общую эпистемологическую оценку, то в этом гораздо больше
                абсолютного релятивизма, чем абсолютного субъективизма или солипсизма. Ибо такому же
                релятивистскому снятию, такой же де-абсолютизации и де-субъективизации подлежат
                Нирвана и пространство.</p>
            <p>В связи с только что сказанным спросим, нарушая все правила виджнянавадинской игры:
                «Что такое ум, что такое мышление?», или, перефразируя Хайдеггера, что называется
                «мышлением»? На это я отвечаю, стараясь придерживаться духа, а не слова
                Ланкаватара-сутры: ум — это чисто символический термин. Символический в том смысле,
                что он репрезентирует всё, все вещи, явления и события, все то, чем сам он не
                является, но чему он приписывается в качестве универсального предиката. Тогда можно
                было бы и сказать, что когда что-то (скажем, мир, вселенная) мыслится как
                несуществующее, то оно «не существует» только в том смысле, в каком о нем говорится,
                что «он — это только мысль, ничего, кроме мысли» и ни в каком ином смысле.</p>
            <p>Хотя не-дифференциальная виджнянавада в своих основных предпосылках остается верной
                Абхидхарме, между ними есть весьма существенные различия. Так в Дхаммападе (см. в
                VII) мы читаем, что «все дхармы умственны, сотворены умом, умом направлены». Притом
                что дхармы и ум здесь принадлежат одному уровню реальности, на котором формулировки
                «дхарма как ум» и «ум как дхарма» будут равноценны. В не-дифференциальной
                виджнянаваде, где «все дхармы — это только ум и ничего кроме ума», положение «только
                ум и ничего, кроме ума» равно себе и само является только умом и ничем, кроме ума.
                Последнее высказывание может быть произнесено (или мыслимо) только с точки зрения
                какой-то привилегированной исключительной позиции, которая релятивизирует все другие
                позиции (включая даже позиции Будд и Бодхисаттв). Это, конечно, не помешало авторам
                Ланкаватары отождествить Высшую Истину с истиной «только ума». Так что, возможно,
                Д.Т. Судзуки был прав, утверждая, что теория «только ума» не принимает во внимание
                индивидуальные умы и сознания, подчиняющиеся закону причинности Взаимообусловленного
                Возникновения, ибо эта теория выходит за пределы фундаментального дуализма «
                существование/не-существование».</p>
            <p><em>(5)</em> Зародыш Татхагаты — это буддологическое дополнение к концепции
                алайя-виджняны, концепции по преимуществу онтологической. Эта двойственность
                Зародыша Татхагаты сильно затрудняет его понимание как самостоятельной философской
                концепции. В сутре описываются следующие моменты этой концепции.</p>

            <ul class="list-none pl-5 ml-8">
                <li class="mt-6">A. Зародыш Татхагаты есть во всех живых существах. Он — жизнь их жизни, сознание их
                    сознания, сама сущность этих существ (оттого он «называется» алайя-виджняной в
                    сутре).</li>
                <li class="mt-6">B. Он — то единственное, что переходит в будущее существование данного живого
                    существа, ибо все другое в сознании живого существа — мгновенно (так как кармически
                    обусловлено), а Зародыш Татхагаты не-кармичен.</li>
                <li class="mt-6">C. Буддологически он является основой индивидуального Татхагаты в каждом живом
                    существе, своего рода «внутренним телом» во всяком натуральном теле. Телом, даже
                    обладающим тридцатью двумя признаками тела Будды.</li>
                <li class="mt-6">D. Эпистемологически он несет в себе Запредельное Знание, только ждущее своей
                    манифестации по достижении данным живым существом Полного Пробуждения.</li>
                <li class="my-6">E. Космологически он — первопричина не только зла и добра, знания и неведения,
                    Нирваны и сансары, но всего, что «есть», всей вселенной. Как первопричина, Зародыш
                    Татхагаты исключается из любой причинности и не подлежит порядку
                    Взаимообусловленного Возникновения. В этом он противопоставляется сознанию. Он —
                    само отрицание природы и хода космической эволюции.</li>
            </ul>

            <p>В Ланкаватара-сутре Зародыш Татхагаты представляется как априорно синтетическая
                категория. В своей первоначальности и вездесущности он довольно близок к Святому
                Духу и другим теологемам раннего христианского гностицизма. Я думаю, что концепция
                Зародыша Татхагаты была возможна только в том «эпистемологическом вакууме», каким
                являлась Индия I I -III вв. н.э. Возможно, что тот же «острый недостаток»
                эпистемологии объясняет введение сутрой таких понятий, как Предел Реального. Ведь
                «реальное» в этом понятии — это то, что познается, а предел — это не только предел
                познаваемости, но и, в первую очередь, предел, за которым о вещи невозможно говорить
                как о существующей или не-существующей, и она в силу этого непознаваема, ибо
                не-обнаруживаема.</p>
            <p><em>(6)</em> Учение о трех свабхавах представляется искусственным эпистемологическим
                добавлением к никак не систематизированной онтологии сутры (Д.Т. Судзуки называл
                последнюю «методологическим хаосом»). Хотя, разумеется, сутра здесь остается
                традиционной формой письменного текстотворчества Большой Колесницы (как, скажем,
                трактат в средневековой европейской схоластике или комментарий в раннем
                протестантстве). Возможно, что в силу необычайной устойчивости древнеиндийской
                комментаторской традиции столь поздняя буддийская сутра, как Ланкаватара, по самой
                своей интенциональности должна быть несистематичной, оставляя систематизирования
                будущим (а иногда и современным) авторам трактатов, шастр и комментаторам. В
                Ланкаватаре ясно просматривается как тенденция к универсализации и унификации
                сознания, так и противоположная ей тенденция к плюрализации сознания, что делает
                крайне трудным эпистемологическую систематизацию содержания сутры. В самом деле, как
                отождествить одно из сознаний с одним субъектом знания, с единственным истинно
                знающим? В сутре такое отождествление возможно только посредством высших
                дхьянических состояний, трансцендентальных самадхи, в которых само понятие объекта
                знания в его отличии от другого объекта, как и от субъекта знания, теряет свою
                эпистемологическую определенность.</p>
            <p>Учение о трех свабхавах служит своего рода эпистемологическим ядром в виджнянаваде.
                Свабхава здесь — это технический термин, обозначающий уровень знания,
                соответствующий одной из трех истин. Но в то же время свабхава — это некая начальная
                точка движения в не-дифференциальной виджнянаваде, завершающегося достижением Высшей
                Истины (санскр. parinispanna). Отсюда — несколько неожиданное определение третьей
                свабхавы: третья свабхава — это такое мышление, которое знает, что природа
                «объективного мира» (мира хаоса и мешанины) одна, то есть одно сознание и ничего,
                кроме сознания. Здесь мы имеем дело с элементарной эпистемологической подстановкой:
                три свабхавы в конечном счете являются тремя позициями, определяемыми опять же
                сознанием живого существа, а не знанием, приписываемым ему
                философом-наблюдателем.</p>
            <p>С точки зрения первой свабхавы, третья свабхава — это не только нормальное мышление,
                не-йогическое и не-рефлексирующее, но и нормальное естественное функционирование
                сознания-вместилища, алайя-виджняны. В нормальном обыкновенном мышлении проявляются
                две основные тенденции. Первая — это думать, как думают другие. Вторая — это думать,
                как ты сам думал раньше, за мгновение до нынешнего твоего думанья. Тогда условная
                вульгарная истина — это истина, понятная и приемлемая для более чем одного мыслящего
                в данный момент.</p>
            <p>Вторая свабхава, называемая паратантра, является одним из наиболее труднопонимаемых
                концептов виджнянавады. На основе только нашей сутры можно было бы считать
                паратантру особым знанием, коррелирующим друг с другом данные знания, полученные на
                уровне первой свабхавы, и соотносящим эти эмпирические данные с абстрактными формами
                мышления, такими, скажем, как «причинность», «обусловленность», «карма» и так далее.
                Попросту говоря, вторая свабхава— это уровень мышления, на котором вещи, события и
                факты редуцируются к отношениям, уровень абсолютного релятивизма, обретшего свое
                символическое обозначение в слове майя. Или так: это — иллюзия, иллюзия всего,
                включая иллюзорность формулы «все — только сознание», иллюзорность Высшей Истины, да
                и иллюзорность самой майи.</p>
            <p>Но что это за учение? Сама сутра проговаривается, называя его «одиноким учением»,
                «учением, взращиваемым в одиночестве» (санскр. vivikta-dharma). Но не лежит ли эта
                «одинокость», изолированность в интенциональности самой сутры? Я думаю, что в этих
                словах символически выражена сущность виджнянавады, одной из основных черт которой
                является изоляция (возможно, самая радикальная в истории философии), почти полный
                отрыв знания, познания и средств познания от ума, мышления и сознания. Последние уже
                как бы «забыли», что являются естественной основой первых. Ибо не только Высшее
                Знание, принадлежащее уровню третьей свабхавы, но и знание паратантры — по
                определению не-естественны. Сами мы в нашем мышлении живем в сфере изоляции,
                изоляции паратантры от Высшей Истины, в своего рода «космическом вакууме», который
                непознаваем как «природное». Ибо природа вещей не в них самих и не в познании, их
                познающем, но в особом Высшем Знании, которое виджнянавадинский мастер йоги
                отождествляет с паратантрой, второй свабхавой. Последняя же, как об этом уже было
                сказано, редуцирует все феномены мира к отношению, которое само не имеет ни своей
                природы (санскр. svabhava), ни какой-либо вещественности (санскр. vastuta).</p>
            <p><em>(7)</em> Учение виджнянавады, как оно изложено в Ланкаватаре, содержит три
                совершенно новых теоретических момента, радикально изменивших характер и направление
                всей школы и во многом предопределивших ее последующее развитие. Во-первых, это —
                переосмысление концепции сознания-вместилища. Это сознание перестало трактоваться
                как инертная, более или менее устойчивая квазипсихическая «масса» индивидуального
                сознания, заполненная едва различимыми в их отличии друг от друга отдельными мыслями
                (иными словами, что-то вроде крайне слабого «фонового сознания»). В Ланкаватаре оно
                становится субстратом всех возможных трансформаций, даже таких, которые могут
                исключить само сознание-вместилище из нормального хода событий во вселенной, более
                того, могут изменить сам этот ход событий, весь порядок вещей. Во-вторых,
                отождествляя сознание-вместилище с Зародышем Татхагаты, сутра сообщает этому
                сознанию высший онтологический статус и тем самым полностью его де-психологизирует.
                Собственно говоря, ведь именно в качестве инструмента тотальной депсихилогизации
                сутра и использует феномен «обратного разрыва» или «переворота», параврити.
                В-третьих, сутра существенно расширяет учение о Буддах, буддологию Большой Колесницы
                за счет включения в класс Будд или буддоподобных существ так называемые «Умственные
                Тела», «Тела Трансформации» или «Тела Самадхи». Очевидно, концепция этих «сделанных»
                или «искусственных» тел восходит к идее «трех тел Будды» в Малой Колеснице. В
                дифференциальной виджнянаваде прямым следствием этой концепции стала идея
                «тройственной вселенной» — вселенной, состоящей из «мира мысли», «мира мыслимых
                вещей» и «мира вещей, произведенных пробужденной или радикально йогический
                трансформированной мыслью». Несколько более подробно такая вселенная описывается в
                терминах «трех сфер космоса».</p>
            <p>Первая — сфера абсолютной объективности Дхармы, соответствующая Телу Дхармы (первое
                тело Будды). Это тело находится вне мыслимой вселенной и исключено из космических
                процессов, с которыми оно может быть соотнесено только посредством высших
                трансцендентальных сосредоточений сознания и созерцаний. Из опыта этих созерцаний
                существование Тела Дхармы выводится косвенно, чисто логическим путем, ибо само это
                тело, строго абхидхармически говоря, нигде не находится. Иными словами, сфера
                абсолютной объективности Дхармы или Тело Дхармы -есть все Будды, мыслимые как один
                Будда, одно буддство, одна таковость, одна дхармичность. И это целокупное Одно не
                есть ни субъект, ни объект какого-либо знания, пусть самого высшего. Последнее
                обстоятельство, однако, не дает нам права считать виджнянаваду системой
                «объективного идеализма» гегелевского типа. Ведь виджнянавада, изменяя религиозную,
                буддологическую основу концепции «трех тел Будды» (возникшую в рамках Малой
                Колесницы), радикально переосмыслило ее. Искусственно, йогически генерированные Тела
                Ума были произведены не из пассивного инертного материала «естественного ума», но из
                ума, йогически трансформированного трансцендентальным созерцанием мастеров, которые
                находятся на пороге Полного Пробуждения.</p>
            <p>Вторая сфера, или сфера трансформации, соответствующая телу Преображения Будды (санскр.
                sambhoga-kaya), может быть названа и «сферой репрезентации», поскольку в ней
                представлено содержание всего знания, достигнутого в созерцании и сосредоточении
                сознания, самадхи, всех трансформированных умов и сознаний. В этой сфере (и только в
                ней) ум, сознание созерцателя превращается в созерцаемый объект. Будду, совершенного
                бодхисаттву или в иные буддоподобные сущности. Таким образом, здесь репрезентируются
                в виде зрительных, слуховых и эйдетических образов ума все отношения различных
                уровней состояний сознания. В этом она соответствует второй свабхаве, паратантре.
                Пробным определением сферы трансформации тогда будет такое: сфера трансформации
                состоит из созданных умом тел Будд-Татхагат, бодхисаттв и всех существ, мест,
                ландшафтов и прочих вещей, генерированных в трансцендентальных созерцаниях и
                сосредоточениях сознания. Эта сфера имеет свою иерархию, на вершине которой
                находятся пять Татхагат, манифестирующих пять аспектов Тела Будды, и,
                соответственно, пять Трансцендентальных Знаний. Может ли эта сфера мыслиться как
                «параллельный космос», то есть как космос феноменов, параллельных чувственному и
                умопостигаемому космосу феноменов и событий, доступных нашему (не
                трансформированному йогическим созерцанием) сознанию? Только в том смысле, что (по
                крайней мере теоретически) все феномены и события вселенной могут быть найдены в
                этой сфере в виде уже произведенных йогических репрезентаций. Впрочем, можно
                предположить и что сфера трансформации каким-то непостижимым и загадочным образом
                сама является генерированной (или индуцированной) абсолютной объективностью Тела
                Дхармы или первой сферы. Это возможно лишь с точки зрения какой-то уже совсем
                гипотетической позиции, внешней по отношению к сфере трансформации, то есть с
                позиции Тела Дхармы.</p>
            <p>Третья сфера, или «сфера внешнего мира» (санскр. bhajanaloka), — мир вещей, событий и
                фактов, мыслимых нормальным, не-трансформированным мышлением, — более или менее
                соответствует «физическому Телу Будды» (санскр. nirmana-kaya). Но неизбежен вопрос:
                чему, собственно, внешен этот внешний мир и что значит «внешний» в терминологии
                виджнянавады? В дифференциальной виджнянаваде слово «внешний» употребляется в чисто
                эпистемологическом смысле: «А есть внешний к В» значит, что А является объектом
                знания (сознания, ума и так далее) В. Таким образом, мир называется «внешним» только
                в том смысле, что он противопоставлен сознанию-вместилищу как объект последнего. Но
                в не-дифференциальной виджняна-ваде внешний мир приобретает онтологический смысл и
                тогда «А есть внешний В» будет означать, что А существует в другом измерении космоса
                В и не может соотноситься с В никаким мыслимым образом.</p>
            <p>Теперь три буддологические оговорки. Первая — что сфера абсолютной объективности Тела
                Будды может быть постигнута из сферы внешнего мира только через сферу трансформации.
                Вторая — что Будды-Татхагаты могут по желанию генерировать посредством своей
                сверхъестественной дхармической силы любые объекты внешнего мира непосредственно, то
                есть не через сферу трансформации. Третья — что с точки зрения концепции «только
                сознания» в не-дифференциальной виджнянаваде внешний мир, будучи по определению
                ошибочным, поскольку он мыслим непросветленной мыслью, является не просто иллюзией,
                но иллюзией иллюзии, коренящейся в полной ошибочности, нереальности «условной
                истины» (санскр. samvrtti-satya). Но можно ли сказать, что майя — одна и та же, или их
                много и они различны? С точки зрения дифференциальной виджнянавады (и
                сознания-вместилища как ее основы), их столько же, сколько мыслимых объектов,
                мышлений или сознаний. В этом смысле можно говорить об их множественности так же,
                как мы говорим о множественности Пустоты в сутрах Запредельного Знания. Но даже
                рассматривая майю как одну космическую иллюзию, мы можем выделить два ее аспекта.
                Так, в суждении «А есть В — является иллюзией, ибо это — конструкция воображения
                (санскр. parikalpita), относящаяся к третьей свабхаве», мы можем соотносить термин
                «иллюзия» и со второй свабхавой, паратаптрой. Но если мы возьмем суждение «А есть В
                является иллюзией, поскольку и А, и В нереальны», то понятие иллюзий, майи
                приобретает абсолютный характер. Тогда мы можем заключить, что в суждении «А есть
                только сознание» имплицирована майя в ее абсолютном аспекте.</p>
            <p>Интересно заметить, что подобно тому, как в палийской Абхидхарме карма
                противопоставлена мысли (благой, не-благой или нейтральной), майя в виджнянаваде
                противопоставляется сознанию. Различие между кармой и майей как между двумя
                универсальными категориями отношения состоит в том, что первая соотносит одну мысль
                с другой во времени, в то время как вторая соотносит одну мысль или одно сознание с
                тем, что мыслится или осознается во вневременном пространстве сознания-вместилища.
                Притом что последнее понимается в качестве одного универсального объекта. Таким
                образом, майя в суждении «все есть майя» обозначает отношение между «всем» как
                объектом знания и знанием о том, что все есть майя.</p>
            <p>В заключение этого несколько фрагментарного семинара, имитирующего фрагментарность
                Ланкаватары, я рискну высказать предположение о том, что в этой сутре
                «психологический реализм» концепции сознания-вместилища все больше и больше уступает
                место «иллюзионизму» (санскр. mayavada), близкому к адвайта-веданте Шанкары.</p>
