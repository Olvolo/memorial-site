            <h2 class="text-2xl font-bold text-center text-zinc-700 mb-8">Семинар четырнадцатый</h2>
            <h3 class="text-xl text-center font-semibold text-zinc-700 mb-4">Текст XIV. Запредельное
                знание.<br>Фрагменты из Восьмитысячника<a href="#citation-1" id="ref-1"
                                                          class="citation-mark"><sup>1</sup></a>
            </h3>
            <blockquote class="my-6 pl-4 border-l-4 border-gray-300">
                <p class="mt-6"><em>0.</em> Сказал Господь достопочтенному Субхути, старейшине<a
                        href="#citation-2" id="ref-2" class="citation-mark"><sup>2</sup></a>:
                    «Объясни теперь, о Субхути, так, чтоб это стало ясным для Бодхисаттв, великих
                    Махасаттв, как они Бодхисаттвы-Махасаттвы отправляются в Запредельное Знание!»
                    Тогда достопочтенный Шарипутра так подумал про себя: «Будет ли достопочтенный
                    Субхути излагать Запредельное Знание сам, только от себя и посредством своей
                    собственной силы или (сверхъестественной) мощью Будды<a href="#citation-3"
                                                                            id="ref-3"
                                                                            class="citation-mark"><sup>3</sup></a>
                    будет он излагать и объяснять Запредельное Знание?» Но достопочтенный Субхути,
                    который уже постиг посредством мощи Будды то, что достопочтенный Шарипутра
                    подумал про себя, сказал достопочтенному Шарипутре: «Чему бы ни учили, о
                    Субхути, ученики Господа, все это — работа (самого) Татхагаты. Ибо в Дхарме
                    Татхагаты искушают они себя, постигают ее истинную природу, твердо устанавливают
                    ее в своем сознании. Оттого ничто из того, чему они учат, не противоречит
                    истинной природе Дхармы. Да и само объяснение ими Дхармы — это не более чем
                    выплеск объясненного Татхагатой...»</p>
                <p>Тогда достопочтенный Субхути силой Будды<a href="#citation-4" id="ref-4"
                                                              class="citation-mark"><sup>4</sup></a>
                    вот что сказал Господу: «Если кто в разговоре о Бодхисаттве скажет
                    «Бодхисаттва», то что это слово будет значить? Что до меня, о Господи, то я не
                    вижу такой дхармы, как Бодхисаттва, ни дхармы, называющейся «Запредельное
                    Знание». Но если я не знаю, ни что такое Запредельное Знание, ни что такое
                    Бодхисаттва, то каково мне будет наставлять Бодхисаттв в Запредельном Знании? Но
                    все же, о Господи, раз Бодхисаттва непоколебим в своем решении, бесстрашен,
                    сердце его не дрогнет, умом он не усомнится, то такой Бодхисаттва и должен быть
                    наставлен в Запредельном Знании. Но даже обретаясь в нем, он таким образом
                    должен себя упражнять, чтобы никогда не появилось у него гордости за возникшую
                    прежде Мысль Пробуждения, с которой и начался его путь Бодхисаттвы<a
                        href="#citation-5" id="ref-5" class="citation-mark"><sup>5</sup></a>. Ибо
                    эта мысль есть не-мысль, так как (любая) мысль является чистой, просветленной и
                    пробужденной по своей природе».</p>
                <p class="mt-6"><em>I.</em> Шарипутра: Но эта мысль, которая есть не-мысль, есть ли она или нет?
                </p>
                <p>Субхути: Но можно ли, говоря о состоянии не-мысль, говорить о «есть» или
                    «нет»?</p>
                <p>Шарипутра: Нет, так невозможно говорить.</p>
                <p>Субхути: Отсюда и невозможность говорить о не-мысли как существующей или не
                    существующей.</p>
                <p>Шарипутра: Что же тогда есть не-мысль?</p>
                <p>Субхути: Это — состояние неизменяемости и неразличения.</p>
                <p class="mt-6"><em>II.</em> Субхути: Эта дхарма, таковость Татхагаты объясняется как
                    не-наблюдение всех дхарм (ведь наблюдается только то, с чем что-то происходит,
                    чему есть препятствия), а эта дхарма беспрепятственна, оттого не оставляет
                    следов. Она — то же, что пространство. Оттого неизменяема. Оттого нет у этой
                    дхармы ни противоположной ей другой дхармы, ни тождественной. Она — одна без
                    другой. У нее не было причин или условий ее становления, возникновения. Она
                    невоспроизводима в будущем и нет следов, ведущих к ней в прошлом. Значит, нет и
                    пути, ведь его не заметишь.</p>
                <p class="mt-6"><em>III.</em> Шакра<a href="#citation-6" id="ref-6"
                                         class="citation-mark"><sup>6</sup></a> (и другие боги): По
                    образу Господа рожден этот старейшина Субхути. Ибо какую бы дхарму он ни
                    объяснял, всегда начинает с Пустоты.</p>
                <p>Субхути: Теперь я научу тебя, о Шакра, как Бодхисаттва устанавливает себя в
                    Запредельном Знании. Только имея в своем основании Пустоту, пребывает он в
                    Запредельном Знании. Ни в телесной форме не основывается он, ни в чувствах, ни в
                    восприятии, ни в воле, ни в сознании, ни в зрении, ни в слухе, ни в запахе, ни
                    во вкусе, ни в осязаемом, ни в объектах ума. Никакой идеи не должен
                    придерживаться Бодхисаттва. Ни идеи «это — чувство», ни идеи «это —
                    Бодхисаттва». Так стоит он в Запредельном Знании, но ведь Бодхисаттва не имеет
                    своего места нигде, значит, негде ему стоять. Подобно Татхагате, который нигде
                    не стоит, ни здесь ни там, ни вместе, ни отдельно.</p>
                <p>Господь: Глубоко сказанное тобой о Пустоте, о Субхути. Ведь Пустота — это синоним
                    Беззнаковости, неотмеченности, бесстрастия. Синоним не-рождения,
                    не-существования, прекращения, ухода, Нирваны. Пустота — это синоним всех дхарм
                    (в их не-различении).</p>
                <p>Шакра: О чем бы ни говорил Благородный Субхути, он ни к чему не прицепляется, ни
                    за что не зацепляется, ничего не задевает в своем движении в Пустоте, как
                    стрела, летящая в воздухе.</p>
                <p>Господь: Это так, именно так, даже Пробуждения он не достигает, ибо в Пустоте
                    возможно ли достижение чего-либо?</p>
                <p class="mt-6"><em>IV.</em> Субхути: Оттого, что он не рожден, рожден старейшина Субхути по
                    образцу Татхагаты, таковость Субхути есть таковость Татхагаты. И так есть с
                    самого начала, ничто не приходило, не уходило, не придет. По образу Татхагаты —
                    значит по образу таковости Татхагаты, которая та же, что и таковость Субхути, и
                    та же, что и таковость всех дхарм. Но эта таковость, она же есть и не-таковость,
                    и по ее образу рожден Субхути. Тогда таковость и не-таковость есть одно без
                    второго, одно несотворенное, неизменное — оно всегда таковое, таковость, что
                    никогда не будет не-таковостью. И хотя кажимость дуальности можно усмотреть в
                    идее, что Субхути является сверхъестественно (волшебно) произведен из таковости
                    Татхагаты, то и тогда таковость осталась абсолютно той же. Ведь никак невозможно
                    помыслить что-то или кого-то, могущего нарушить целостность, полноту и
                    неделимость таковости. Тогда правильным будет сказать, что таковость Субхути не
                    есть только его таковость, а не Татхагаты. Как и в случае Будды, таковость
                    которого, когда он был Бодхисаттвой в прежних рождениях, и по достижению Полного
                    Пробуждения — одна таковость, не две. Оттого и название «Так ушедший,
                    Татхагата».</p>
                <p class="mt-6"><em>V.</em> Господь: Необратимым зовется Бодхисаттва, который навсегда отвратился
                    от пути Учеников и Пратьекабудд и пошел по пути Всезнания<a href="#citation-7"
                                                                                id="ref-7"
                                                                                class="citation-mark"><sup>7</sup></a>.
                    Сначала он вступает в первую, вторую, третью и четвертую дхьяны и в них
                    пребывает. Там он становится совершенным мастером дхьян, но его будущее рождение
                    никак не определено их кармическим эффектом (т.е. прекращением кармических
                    эффектов, происходящим в созерцании<a href="#citation-8" id="ref-8"
                                                          class="citation-mark"><sup>8</sup></a>). И
                    свое следующее рождение он основывает на дхармах чувственной сферы, к ним
                    направлено его сознание. Приходит ли он или уходит, он остается в обратном
                    вспоминании<a href="#citation-9" id="ref-9"
                                  class="citation-mark"><sup>9</sup></a>. Живя жизнью домохозяина,
                    он, как и другие, пользуется приятными для чувств вещами, но особенно их не
                    любит, не привязывается к ним, не жаждет их, просто их имеет. Так и живут
                    Бодхисаттвы жизнью домохозяев, проникнутые энергией Запредельного Знания — таков
                    еще один признак необратимости Бодхисаттвы. «Необратимый» здесь значит, что
                    Бодхисаттва, достигший Полного Пробуждения, уже никогда не возвратится к
                    прежнему состоянию.</p>
                <p class="mt-6"><em>VI.</em> Субхути: Если Бодхисаттва достигает Полного Пробуждения, то
                    происходит ли это вследствие первой мысли о Пробуждении или вследствие последней
                    мысли о Пробуждении<a href="#citation-10" id="ref-10"
                                          class="citation-mark"><sup>10</sup></a>?</p>
                <p>Господь: Как ты думаешь, Субхути, загорается ли фитиль масляной лампы от первого
                    момента его зажигания или от последнего, перед тем как ему вспыхнуть?</p>
                <p>Субхути: Ни от первого, о Господи, ни от последнего, ни в какой-либо зависимости
                    от них обоих, ни совсем независимо от них.</p>
                <p>Господь: Но фитиль-то все ж таки загорелся, не так ли?</p>
                <p>Субхути: Загорелся, о Господи.</p>
                <p>Господь: Таким же образом Полное Пробуждение достигается ни первой и ни последней
                    мыслью о Пробуждении, ни без них, ни в связи с возникновением каких бы то ни
                    было мыслей о пробуждении, ни без связи с этим возникновением. Но оно все ж таки
                    достигается, Полное Пробуждение, вот оно.</p>
                <p>Субхути: Сколь глубоко и трудно для понимания Взаимообусловленное Возникновение<a
                        href="#citation-11" id="ref-11" class="citation-mark"><sup>11</sup></a>!</p>
                <p>Господь: Скажи, Субхути, возникнет ли вновь во второй мысли первая мысль, тотчас
                    же прекратившаяся после своего первого возникновения?</p>
                <p>Субхути: Не возникнет, о Господи.</p>
                <p>Господь: Но мысль, которая уже возникла, не обречена ли она по самой своей
                    природе на прекращение?</p>
                <p>Субхути: Обречена, о Господи.</p>
                <p>Господь: Но если что-то по своей природе обречено на прекращение, то может ли оно
                    быть уничтожено?</p>
                <p>Субхути: Не может, о Господи.</p>
                <p>Господь: Тогда мысль, которая еще не возникла, обречена ли она по своей природе
                    на прекращение?</p>
                <p>Субхути: Нет, о Господи, ибо то, что еще не возникло, не может быть
                    прекращено.</p>
                <p>Господь: Но если какая-нибудь дхарма уже прекращена по своей собственной природе,
                    то может ли она (опять) прекратиться?</p>
                <p>Субхути: Не может, о Господи.</p>
                <p>Господь: Может ли прекратиться истинная природа всех дхарм?</p>
                <p>Субхути: Не может, о Господи.</p>
                <p>Господь: Есть ли мысль в Таковости?</p>
                <p>Субхути: Нет, о Господи.</p>
                <p>Господь: Есть ли мысль то же, что и Таковость?</p>
                <p>Субхути: Нет, о Господи.</p>
                <p>Господь: Есть ли мысль что-либо иное, чем Таковость?</p>
                <p>Субхути: Нет, о Господи.</p>
                <p>Господь: Бодхисаттва, движущийся в Таковости, идет ли он в ней вглубь?</p>
                <p>Субхути: Нигде он не движется, ибо нет в нем мысли о том, что он совершает, ни
                    где он проходит.</p>
                <p>Господь: Но где-то должен он быть, когда он — в Запредельном Знании?</p>
                <p>Субхути: Тогда он — в Высшей Реальности.</p>
                <p>Господь: В чем? Отмечена ли она, обозначена?</p>
                <p>Субхути: Нет там никакого знака, о Господи<a href="#citation-12" id="ref-12"
                                                                class="citation-mark"><sup>12</sup></a>.
                </p>
                <p class="mt-6"><em>VII.</em> Шарипутра: Никаких усилий не совершает Бодхисаттва, чтобы в данном
                    своем рождении достичь состояния покидания, оставления всех знаков. В том-то и
                    состоит Великое Умение Пользования Средствами, что и зная знаки и понимая их
                    смыслы, Бодхисаттва всецело предан Беззнаковости, т.е. такой дхарме, где не
                    возникает и никогда не возникал знак<a href="#citation-13" id="ref-13"
                                                           class="citation-mark"><sup>13</sup></a>.
                    Так в Запредельном Знании культивирует он три выхода в Освобождение: Пустоту,
                    Беззнаковость и Бесстрастие. Не так ли? Думает об этом Бодхисаттва и днем и
                    ночью во сне. Ибо не говорил ли нам Господь, что сон и бодрствование по существу
                    одно и то же? И если кто совершает во сне действие, благое или дурное, то не
                    добавятся ли его последствия к кармической сумме совершающего?</p>
                <p>Субхути: Поскольку Господь сказал, что в конечном счете все дхармы подобны сну,
                    то никакое действие, совершенное во сне, ничего не изменит в «кармическом
                    балансе» совершающего. Однако же, с эмпирической точки зрения, здесь будут
                    кармические последствия, если, проснувшись, он подумает об увиденном сне, в
                    каком он, скажем, убил кого-то, и сознательно производит мысль о желании
                    убить.</p>
                <p>Шарипутра: Но тогда и в случае Будды, каждая мысль, будь то наяву или во сне,
                    тоже может возыметь кармические последствия?</p>
                <p>Субхути: Никак не может, о Шарипутра, ибо Татхагата уже покинул все рефлексии и
                    отказался от всякого различения мыслей (и дхарм).</p>
                <p class="mt-6"><em>VIII.</em> Субхути: Ни действие, ни мысль не могут возникнуть в пространстве
                    сами по себе, ни пространство не может их произвести из себя самого. Действие
                    или мысль могут возникнуть, только если у них будет объективная опора, т.е.
                    опора в том или ином объекте. Так, опорой всех интеллектуальных действий
                    являются объекты ума, т.е. дхарма-объекты, каковыми являются видимые, слышимые,
                    мыслимые и т.д. вещи. Таким же образом, волевые импульсы<a href="#citation-14"
                                                                               id="ref-14"
                                                                               class="citation-mark"><sup>14</sup></a>
                    возникают с опорой на объекты.</p>
                <p>Шарипутра: Господь говорил, что все объективные опоры являются отдельными, никак
                    не соотнесенными ни с каким субъектом. Как же тогда может возникнуть волевой
                    импульс, имеющий только объективную опору?</p>
                <p>Субхути: Волевой импульс возникает с объективной опорой на то, что на самом деле
                    (и сейчас) не существует, с опорой на знак чего-то. Таким же образом санскары,
                    которые проявляются в волевых импульсах, обусловлены неведением<a
                        href="#citation-15" id="ref-15" class="citation-mark"><sup>15</sup></a>.
                    Таким же самым образом (возникают) и другие факторы Взаимообусловленного
                    Возникновения, вплоть до старости и смерти, обусловленных рождением.</p>
                <p class="mt-6"><em>IX.</em> Субхути: Но как же тогда мысль, сама подобная иллюзии, мыслит Полное
                    Пробуждение?</p>
                <p>Господь: Видишь ли ты в мысли, подобной иллюзии, отдельную вещь, реальную, о
                    Субхути?</p>
                <p>Субхути: Нет, о Господи.</p>
                <p>Господь: Видишь ли ты иллюзию как отдельную реальную вещь?</p>
                <p>Субхути: Нет, о Господи.</p>
                <p>Господь: Тогда, может быть, ты видишь... такую дхарму, которая знает Полное
                    Пробуждение, которая сама есть не что иное, чем мысль об иллюзии?</p>
                <p>Субхути: Нет, о Господи, не вижу. На какую дхарму мог бы я указать и сказать,
                    «вот она, она есть» или «вот она, ее нет»? Ведь дхарме, которая абсолютно
                    отделена (от чего угодно другого), невозможно приписать «она есть» или «ее нет»,
                    существование или несуществование. Абсолютно отделенная дхарма не знает Полного
                    Пробуждения, ибо она не имеет существования. Как же тогда Бодхисаттва,
                    пребывающий в абсолютно отделенном Запредельном Знании, может знать Полное
                    Пробуждение, разве можно абсолютно отделенное познать через абсолютно
                    отделенное?</p>
                <p>Господь: Именно так, о Субхути, именно потому, что Запредельное Знание является
                    абсолютно отделенным, через него и познается абсолютно отделенное Полное
                    Пробуждение. Но если у Бодхисаттвы возникнет мысль, «Запредельное Знание
                    абсолютно отделено», тогда это уже и не Запредельное Знание. Таким образом,
                    совершенно ясно, что именно через Запредельное Знание Бодхисаттва знает Полное
                    Пробуждение. Отделенное не познается через отделенное. Тем не менее Бодхисаттва
                    знает Полное Пробуждение, которое он не мог бы знать иначе, чем через
                    Запредельное Знание.</p>
                <p class="mt-6"><em>X.</em> И вот наступил момент, когда Господь вернул в себя обратно свою
                    чудодейственную магическую силу<a href="#citation-16" id="ref-16"
                                                      class="citation-mark"><sup>16</sup></a>. И
                    тотчас же исчезли произведенные ею Татхагата Акшобхья<a href="#citation-17"
                                                                            id="ref-17"
                                                                            class="citation-mark"><sup>17</sup></a>
                    и другие Бодхисаттвы, Ученики и Будды этого Поля Будды<a href="#citation-18"
                                                                             id="ref-18"
                                                                             class="citation-mark"><sup>18</sup></a>.
                    Все они исчезли, перестали быть видимыми для собравшихся вокруг Господа. Сказал
                    тогда Господь Ананде: «Смотри, Ананда, подобно тому, как сейчас исчезли
                    произведенные (моей чудодейственной силой) видения, так же и дхармы не видимы
                    не-дхармами. Так же и дхармы не видят и не знают не-дхарм. Ибо все дхармы по
                    самой своей природе невидимы, непознаваемы и неспособны к какому-либо действию.
                    Все дхармы не-активны, а оттого и непостижимы, так же абсолютно пассивны, как
                    пространство. Все дхармы немыслимы и непредставимы, как непредставимы иллюзорные
                    существа<a href="#citation-19" id="ref-19"
                               class="citation-mark"><sup>19</sup></a>. Дхармы невозможно найти,
                    обнаружить, ибо они находятся не только в состоянии бездействия, но и в
                    состоянии несуществования<a href="#citation-20" id="ref-20"
                                                class="citation-mark"><sup>20</sup></a>».</p>
                <p class="mt-6"><em>XI.</em> Господь: И еще, Субхути, Бодхисаттва, приближающийся к Запредельному
                    Знанию, должен знать такие семь вещей:</p>
                <p>(1) Приближающемуся к Запредельному Знанию не следует привязываться к дхармам,
                    приникать к ним, быть связанным с ними.</p>
                <p>(2) Приближающемуся к Запредельному Знанию не следует заниматься различениями
                    дхарм, их чертами и особенностями.</p>
                <p>(3) Надо помнить, что все дхармы не имеют происхождения, источника.</p>
                <p>(4) Твердо знать, что все дхармы неизменяемы и самотождественны.</p>
                <p>(5) Помнить, что все разговоры о дхармах не имеют к самим дхармам никакого
                    отношения. Все это — только слова и чисто условное выражение «того, что само не
                    есть дхармы». Все дхармы — за пределами слов и языка.</p>
                <p>(6) Знать, что, дхармы не рождаются и не исчезают. Их нерождение — абсолютно.</p>
                <p>(7) Знать, что поскольку Таковость везде одна, все дхармы уже достигли
                    Нирваны.</p>
                <p class="mt-6"><em>XII.</em> Тогда Дхармодгата сказал: «Ясно, что Татхагаты ниоткуда не приходят
                    и никуда не уходят. Ибо Таковость недвижна, а Татхагата есть Таковость.
                    Невозникновение не приходит и не уходит, а Татхагата есть невозникновение. Может
                    ли кто помыслить о пределе реального? Да ведь Татхагата является таким пределом.
                    Им же являются Пустота, Беззнаковость и полная остановка в сфере пространства<a
                        href="#citation-21" id="ref-21" class="citation-mark"><sup>21</sup></a>. Но
                    Татхагата никак не вне этих дхарм, он в них. Таковость этих дхарм и Таковость
                    Татхагаты — это та же одна Таковость, не две, не три. Таковость не бывает всякой
                    или разной, в ней нет делений и различий». </p>
            </blockquote>
            <h3 class="text-xl text-center font-semibold text-zinc-700 my-4">Запредельное знание как
                путь Бодхисаттв и как особая (отдельная) философия</h3>
            <p><em>(0)</em> Не исключено, что условно хронологически (т.е. в отношении внутренней
                хронологии школ и текстов Большой Колесницы) Запредельное Знание было наиболее
                ранней школой Большой Колесницы и что тексты этой школы были самыми древними
                письменными текстами буддизма вообще. Восьмитысячник, фрагменты из которого мы
                только что прочли, представляет собой компактное, хотя и не очень систематическое
                изложение учения о Запредельном Знании. Полная версия этого изложения представлена
                «Стотысячником», более краткие версии содержатся в «Алмазной Сутре» и «Сердцевинной
                Сутре» (также переведенных на английский Эдвардом Конзе).</p>
            <p>Запредельное Знание не противопоставлено никакому другому знанию. Оно запредельно в
                буквальном смысле, как «ушедшее (Skr. ita) по ту сторону (Skr. param) высшего
                знания». Или можно сказать: оно находится в ином месте, чем то, где находится высшее
                знание, либо, что еще точнее, само оно является этим иным местом. Строго
                буддистически рассуждая, Запредельное Знание вообще никак не соотносится со знанием
                вообще (из чего следует, что оно не является эпистемологической категорией). В этом
                оно отличается от Мысли Пробуждения, к которой — как мы об этом уже знаем из
                предыдущего семинара — в конечном счете сводится любая мысль и которой в конечном
                счете является мысль вообще.</p>
            <p>Я думаю, что введение Запредельного Знания в качестве онтологической сущности,
                дополнительной к Мысли о Пробуждении, означало радикальную смену аспекта в
                философствовании буддизма Большой Колесницы. Пространственный характер (или даже
                «природа») Запредельного Знания имеет своим онтологическим (а не логическим,
                эпистемологическим) следствием, во-первых, не-процессуальность знания (чем ставится
                под вопрос классическая буддийская концепция Пути) и, во-вторых, топологическое
                смещение знания в отношении к условному субъекту Запредельного Знания, т.е. к
                Бодхисаттве. На бездне примеров из Сутр Запредельного Знания мы видим, что оно не
                апроприируется Бодхисаттвой как свое. Не оно в Бодхисаттве или у Бодхисаттвы, а
                Бодхисаттва в нем «пребывает», «передвигается», либо «достигает» его как
                определенное место. Но перейдем к нашему тексту как к тексту инструкции в
                Запредельном Знании, тексту обучения ему.</p>
            <p>Когда Эдвард Конзе в предисловии к своему переводу «Восьмитысячника» говорит, что
                этот текст был написан для его повторения и запоминания наизусть тогдашними
                читателями, а его перевод — для чтения нынешними читателями, то этим он уже наметил
                три уровня прагматики «Восьмитысячника», т.е. три уровня объективного отношения
                этого текста и его читателей (или слушателей). А именно: (1) уровень тех, кто
                подобно мне и вам может только читать или слушать этот текст; (2) уровень тех, кто
                знает и практикует буддизм Большой Колесницы и для кого йогическое выучивание
                наизусть и повторение этого текста являются одной из сторон их религиозной практики
                и йогического совершенствования; (3) уровень Бодхисаттв, описанных в тексте как
                инструкторы и инструктируемые в Запредельном Знании. Попытаемся теперь двигаться, в
                нашем понимании прагматики Восьмитысячника, от третьего уровня, точнее, от
                объективного отношения между инструктирующими и инструктируемыми Бодхисаттвами.</p>
            <p>Это, однако, станет возможным только при условии, что мы искусственно сконструируем
                какой-то другой, трансцендентальный уровень прагматики нашего текста. Такой уровень,
                с точки зрения которого будет вообще невозможна ни чистая прагматика, ни чистая
                теория, уровень, на котором все сплавляется в едином Запредельном Знании. Это —
                уровень Будды, Господа. Заметим, что во фрагменте (0) Будда призывает Субхути
                обучать Бодхисаттв, как тем следует продвигаться в Запредельном Знании, а не сам их
                обучает. Этим предполагается отсутствие иерархии обучения Запредельному Знанию, но
                зато вводится другая иерархия, иерархия сверхъестественной силы или мощи.
                Оказывается, что одно дело — это собственная сверхъестественная сила Субхути,
                посредством которой он излагает Запредельное Знание, а совсем другое дело — это
                магическая мощь Будды, которой Господь создает всю ситуацию диалога в целом, включая
                ее участников, ландшафт и все такое прочее. Более того, только этой мощью
                присутствующие Бодхисаттвы слушают Субхути и задают ему вопросы. Тогда, я думаю,
                будет иметь смысл предположение, что субъект Запредельного Знания — это субъект той
                или иной сверхъестественной силы или мощи. Впоследствии эти силы и мощи нашли свою
                мифологическую манифестацию в виде особых «умственных тел», созданных Бодхисаттвами
                в состоянии трансцендентального самадхи и особенно в виде «Божественных супруг»
                Бодхисаттв, шакти, сыгравших весьма важную роль в буддийском тантризме.</p>
            <p><em>(1)</em> Но что такое Бодхисаттва? На этот вопрос, дебютный в почти любой
                философской дискуссии в ранней махаяне, абхидхармистским ответом было бы:
                Бодхисаттва — это дхарма, одна из дхарм, точнее, дхарма, обозначенная словом
                «бодхисаттва». Но здесь-то и начинается эпистемологическая критика этого понятия с
                точки зрения уже давно сформулированной в Абхидхарме узко эмпирицистской позиции:
                «Какая такая дхарма, — говорит Субхути, — не вижу я такой дхармы, Бодхисаттва — это
                не более чем название, слово, звукосочетание, а не то, что этим словом обозначается,
                то, что невидимо, неслышано, непредставляемо, невоспринимаемо и т.д.» Я думаю, что
                этот дуализм, «дхарма/ее условное обозначение», является основной чертой
                эпистемологии Запредельного Знания. Но дальше, в том же фрагменте, Субхути
                спрашивает с деланным недоумением еще «не исправившегося» философского реалиста: «Но
                если так, то кого же тогда я буду наставлять в Запредельном Знании, если и последнее
                есть не более чем название — никого буду наставлять ни в чем?» Но каков бы ни был
                ответ, он уже не поставит в тупик Бодхисаттву, утвердившегося в Запредельном Знании,
                который сам исходит из дуализма «невидимая, немыслимая и т.д. дхарма/видимое,
                слышимое, мыслимое и т.д. слово, ее обозначающее». Такой Бодхисаттва не озаботится
                проблемой, есть ли дхарма «Бодхисаттва» или нет. Тем не менее из «Восьмитысячника»
                все-таки следует, что (вне зависимости, есть он или его нет) он — дхарма, которая
                есть или которой нет.</p>
            <p><em>(2)</em> Итак, переход от идеи Бодхисаттвы ко второй важнейшей идее буддизма
                Большой Колесницы — Мысли Пробуждения производится посредством редукции последней к
                мысли (образом, по сути дела аналогичным тому, каким в предыдущем семинаре мысль
                была редуцирована к Мысли Пробуждения). Однако в сутре делается ход напрямик к
                онтологии Запредельного Знания, вводится понятие (состояния) не-мысли, которая не
                может ни существовать (возникать), ни не-существовать. Таким образом, проблема
                существования сводится (почти так же, как в классической Абхидхарме) к существованию
                или несуществованию мысли. Последнее, я думаю, почти не встречается в истории
                древней философии (ведь и Парменид говорил, что мышление есть мышление о бытии, но
                никогда бы не сказал, что бытие есть мышление о бытии). Ведь в большинстве известных
                философских учений существование или не-существование приписывается объектам мысли,
                а не самой мысли. Исключением является, пожалуй, только буддийская школа
                виджпянавада-йогачара (в которой мысли как раз и приписывается не-существование). В
                «Восьмитысячнике» это именно немысль полагается таким состоянием, в котором нет ни
                «есть», ни «нет» в отношении чего бы то ни было. Я думаю, что введение не-мысли
                здесь не обычный для Запредельного Знания прием, а, скорее, предел редукции, более
                чем на две тысячи лет предваривший идею Эдмунда Гуссерля о чистом сознании как
                пределе феноменологической редукции.</p>
            <p><em>(3)</em> Третья, основная идея Махаяны, нашедшая свое переистолкование, в
                «Восьмитысячнике», — это «Таковость». Как и при рассмотрении Пустоты, первым
                вопросом будет: «Чья Таковость или Таковость чего?» Только ответив на эти вопросы,
                мы будем иметь право спросить: «Что такое Таковость?» Тогда ответом на первый вопрос
                будет: «Таковость Татхагаты и таковость Субхути (тоже потенциального Татхагаты)».
                Ответить на второй будет гораздо труднее потому, что ко времени создания
                Восьмитысячника (сколь бы гипотетичной ни была его датировка) Таковость уже давно
                приобрела квазионтологический статус. В переистолковании Таковости в философии
                Запредельного Знания не ставится под вопрос ее онтологический статус, но явно
                наблюдается тенденция к релятивизации этого статуса. Попробуем теперь рассмотреть
                несколько наиболее интересных подобных случаев в «Восьмитысячнике».</p>
            <p>а) Чья бы ни была Таковость — это Таковость Будды-Татхагаты, и (как и его Таковость)
                он не появляется и не приходит.</p>
            <p>Поэтому рожденные по образу Таковости Татхагаты (а таким был рожден и Субхути) не
                являются рожденными.</p>
            <p>b) Таковость Татхагаты и Таковость всех дхарм — это одна и та же Таковость и вот в
                каком смысле: и Татхагата и все дхармы являются нерожденными (здесь можно видеть
                тенденцию к отождествлению дхарм вообще с живыми существами), несотворенными и
                беззнаковыми. Последнее означает принципиальную неотмеченность Таковости, которая не
                даст отличить Таковость от не-Таковости. Таким образом, Таковость и не-Таковость —
                не две различные дхармы, а одна, каковой являются Таковости Будды и Субхути. Значит,
                Таковость не-дуальна. Поэтому, в частности, когда нам кажется, что Будда и Субхути —
                два разных существа, то это — иллюзия, произведенная магической мощью Будды, в то
                время как на самом деле у обоих одна и та же Таковость.</p>
            <p>c) В «Восьмитысячнике» вводится особое и весьма сложное понятие упорядоченности или
                гармонизации (Skr. vynha), исходящих из Таковости Господа. Все факты и события
                вселенной, будь то «рождение» Субхути или Полное Пробуждение Будды, происходят
                только в соответствии с уже имеющим место универсальным устройством, в котором
                манифестируется Таковость Татхагаты. Не будет, мне кажется, слишком рискованной
                гипотезой предположить, что в школе Запредельного Знания возник целый «пучок» особых
                онтологических понятий, явно производных от понятия Татхагаты, — таких как «Поле
                Будды», «Устроение» (кстати, ближе всего по смыслу к понятию космоса в
                досократической греческой философии) и «Гармонизация Поля», в которых можно видеть
                своего рода иную, «мистическую» космологию буддизма, уже в значительной степени
                отличную от дхьянической космологии, прямо идущей от непосредственного йогического
                опыта созерцателей. Наше краткое рассуждение о Татхагате и Таковости в
                «Восьмитысячнике» заключу предположением о том, что в этой «извечной» онтологической
                сущности мастера Запредельного Знания видели выход из философского (и
                мифологического!) дуализма.</p>
            <p><em>(4)</em> Само собой разумеется, что эпистемологически Пустота остается главной (а
                часто и единственной) позицией и исходной точкой зрения для Бодхисаттвы — что бы он
                ни познавал, рассматривал или обсуждал. Но вместе с тем она является, так сказать, и
                универсальной отсылкой. Ведь о чем бы Субхути ни рассуждал, он «отсылает» себя и
                собеседника к Пустоте. Фактически мы можем считать «Восьмитысячник» первым, пусть
                несистематическим и фрагментарным, изложением учения о Пустотности, позднее
                систематически разработанного в школе Мадхъямика, где оно и получило свое название
                «теория пустоты» (Skr. sunyavada).</p>
            <p>Мы знаем, что онтологически все дхармы пусты, т.е. лишены каких-либо признаков,
                атрибутов или имеющихся в них объектов, а понятие дхармы оказывается лишенным
                какого-либо мыслимого позитивного содержания. Таков негативный аспект онтологии
                дхарм. Но — и это важнейший момент в учении о Пустоте — нет одной, какой-то обшей
                для всего Пустоты. Пустоты как универсального атрибута. Пустота всегда — конкретная
                пустота определенного «чего-то». Тогда сказать «все есть пустота» будет таким же
                философским вульгаризмом, как сказать «пустота — это все». Но сказать, что «все
                дхармы пусты», будет философски вполне корректно, ибо «все дхармы» — это, с точки
                зрения Пустоты, один определенный объект. Отсюда и следует множественность пустот,
                так сказать.</p>
            <p>В аспекте позитивной онтологии Пустота является пределом всего, что есть, и всего,
                чего нет, и таким образом она — то же самое, что Таковость и Нирвана. Последним
                только подчеркивается то обстоятельство, что Пустота в философии Большой Колесницы
                была введена как буддологическая категория, а никак не категория натурфилософии или
                эмпирицистской философии. Рассматриваемая вне буддологического контекста, Пустота
                неизбежно окажется тривиализированной и фальсифицированной. А в то же время она
                является предметом Запредельного Знания, подобно тому как дхарма является предметом
                философии Абхидхармы.</p>
            <p><em>(5)</em> Теперь вернемся к понятию Бодхисаттвы. Последний приобретает в
                «Восьмитысячнике» черты, которыми он не наделялся в других сутрах, например в
                «Ланкаватаре». Главная из этих черт состоит в том, что, хотя он и ведет нормальную
                жизнь домохозяина, пребывающего в чувственной сфере существования (Skr. kamadhatu),
                он при этом неустанно прокладывает себе путь через сферу Запредельного Знания. Что
                же он тогда за личность, этот Бодхисаттва? Такой, казалось бы, элементарный вопрос
                уже повергает нас в такие дебри философских тонкостей и буддологических сложностей,
                из которых так просто не выпутаешься. Уже само слово «личность» требует особого
                разъяснения, хотя бы краткого.</p>
            <p>Личность (Skr. pudgala, purusa) — это живое существо (Skr. sattva), которое либо уже
                стало Бодхисаттвой, либо его «прошлое» было «обратным образом» трансформировано
                магической мощью Татхагаты или сверхъестественной силой Запредельного Знания и
                превращено в основу бодхисаттства к «настоящему» времени. Но что значит «было» в
                этом определении, означает ли это, что все прежние влияния и помрачения уже изжиты
                или исчерпаны? Вопрос приводит нас к второй черте Бодхисаттвы в «Восьмитысячнике»:
                Бодхисаттва — это созерцатель, который в силу вступления в дхьяны и пребывания в них
                полностью нейтрализовал свою карму, лишил ее силы давать кармический эффект и тем
                самым обрел «сейчас» способность сам определять свои будущие рождения.</p>
            <p>И наконец, третьей чертой Бодхисаттвы является необратимость его пробужденности:
                Бодхисаттва, продвигающийся в Запредельном Знании к Полному Пробуждению, не может ни
                повернуть вспять, ни изменить направления и курса своего продвижения. Ничто не может
                помешать ему в его продвижении — ни космические события, ни сложности и трудности
                его мирской жизни. Ибо теперь сила Запредельного Знания будет сама высшим
                объективным фактором.</p>
            <p><em>(6)</em> Однако, как явствует уже из нашего вводного фрагмента, сначала мы
                рассматриваем Бодхисаттву как дхарму, как особую, но все же лишь одну из мириада
                вещей, состояний и существ. Дхарму, которая (как в принципе и всякая другая) не
                отождествима, не воспринимаема, не мыслима, словом, которой нет. Личность — это
                вторая сторона Бодхисаттвы, рассмотрение которой возвращает нас к теме буддийской
                антропологии. Ибо к «человеку-аскету» и «человеку-созерцателю» сутры Запредельного
                Знания добавляют «человека-Бодхисаттву». Хотя здесь не обойтись без оговорки, что
                строго буддологически, Бодхисаттва может быть не только человеком, но богом, богиней
                или даже духом.</p>
            <p>Поэтому будет правильнее сказать «личность может быть Бодхисаттвой», чем —
                «Бодхисаттва это личность», ибо последнее превратило бы бодхисаттство в одно из
                «человеческих условий», лишив его трансцендентности. Значит, личность — это еще не
                Бодхисаттва, не Благородная Личность (Skr. aryapudgala).</p>
            <p>Вот почему школе Запредельного Знания пришлось включить в свою политетическую
                квазиантропологическую классификацию существ, саттв и такой таксон, как «человек»
                или «человекоподобное существо» (Skr. purusa). Тогда операциональным определением
                «личности» будет: живое существо, мыслимое в его возможности быть (или стать)
                Боддхисатвой. Главное слово здесь — «мыслимое». Получается, что личность — это модус
                мышления, а не бытия или существования.</p>
            <p><em>(7)</em> Мысль Пробуждения, первое основное понятие философии Большой Колесницы,
                анализируется в «Восьмитысячнике» в порядке ответа на вопрос: как «начинающий»
                Бодхисаттва идет от своей (первой) Мысли Пробуждения к достижению Полного
                Пробуждения? Напомним, что любое «как?» в буддийском философском контексте будет
                иметь своим ответом: «Согласно порядку Взаимообусловленного Возникновения, этому
                закону универсальной причинности, вездесущему и неизменному». Однако мы уже знаем из
                семинара IV, что классическая формула «после чего-то значит вследствие чего-то»
                (Lat. post hoc is propter hoc) так же неприменима к Взаимообусловленному
                Возникновению, как и обратная ей формула «вследствие чего-то значим после чего-то».
                Здесь причинность абсолютна, но время, отделяющее следствие от причины, релятивно
                настоящему моменту, в который все это и происходит одновременно. Что же касается
                времени процесса возникновения Мысли Пробуждения, то оно ставится в
                «Восьмитысячнике» под сомнение как, впрочем, и сам процесс. Так, когда Субхути
                говорит: «Глубоко, трудно для понимания Взаимообусловленное Возникновение», то уже
                это предполагает релятивизацию Взаимообусловленного Возникновения посредством
                полного отбрасывания времени. Возникновение Мысли Пробуждения — это мгновенное
                событие, которое соотносится в обратном порядке с Запредельным Знанием, ибо
                последнее как бы «всегда есть» в настоящем, для которого Мысль Пробуждения остается
                фактом прошлого, будущего или какого угодно другого времени.</p>
            <p><em>(8)</em> Понятие «объективной опоры» (Skr. asraya) обретает в «Восьмитысячнике»
                общий философский смысл. Я думаю, что Эдвард Конзе в своем переводе этой сутры
                употребляет слово «объективный» в качестве понятия, обозначающего отноше ние данного
                феномена, дхармы к другой дхарме. Без последней дхармы первая не могла бы
                возникнуть, но она всегда предполагается уже возникшей, когда Субхути говорит, что
                «пространство не производит мысли или действия из самого себя, без объективной
                опоры». Это может быть понято в том смысле, что пространство, будучи необусловленной
                дхармой, ни из чего само не возникает и не обуславливает ничего, что не является
                пространством. Пространство никоим образом не относится ни к какой мыслимой дхарме
                и, таким образом, не имеет объективной опоры и не может служить таковой. Но все
                обусловленные дхармы возникают, только если имеют объективную опору. Так, все
                действия интеллекта совершаются с объективной опорой на органы чувств.</p>
            <p>В то же время объективная опора описывается в сутре обязательно как дхарма отдельная,
                изолированная от дхармы, которая на нее опирается. Т.е., философски выражаясь,
                отношение этих дхарм не является отношением присущности. В этом смысле объективная
                опора напоминает знак в его отношении к обозначаемому, в трактовке современной
                лингвистики. Но одновременно можно было бы рассматривать отношение объективной опоры
                к тому, что на нее опирается, как своего рода чистое (бессодержательное) отношение,
                т.е. актуально не существующее. Заметим при этом, что «объективно опирающееся» и
                «обусловленное» (в смысле Взаимообусловленного Возникновения) — это две совершенно
                разные вещи (дхармы), хотя отношение первой ко второй представляется отношением
                дополнительности. В общем, я полагаю, что введение понятия объективной опоры можно
                рассматривать как попытку создать какую-то другую, отличную от абхидхармистской
                теорию мысли. исходящую в своей аксиоматике из Запредельного Знания.</p>
            <p><em>(9)</em> Я предполагаю, что первым шагом в этой попытке явилось обращение к
                рефлексивному опыту, отсылки к которому мы видим во фрагменте (VII), где речь идет о
                мышлении во сне. Здесь на вопрос о том, может ли дурная мысль (Skr. akusala-citta),
                возникшая во сне, иметь дурные кармические последствия, дается феноменально
                интересный философски ответ: да, может, но только в случае, если, проснувшись,
                человек вспомнит эту мысль и осознает ее как дурную. Таким образом здесь
                утверждается, что только вторичная, т.е. отрефлексированная мысль может иметь
                кармический эффект. Эффект же неотрефлексированной мысли обычно остается кармически
                неопределенным (Skr. avakrta). Таким образом, рефлексия становится сама кармическим
                фактором, не менее важным, чем качество поступка, мысли или речи. Более того, она
                выступает как особое состояние сознания, в отношении к которому сон является
                промежуточным состояния сознания. А мы уже знаем, что во сне, как и в других
                промежуточных состояниях (включая и трансцендентальное состояние самадхи)
                кармический эффект обычно ослаблен или приостановлен.</p>
            <p>Роль рефлексии особенно подчеркивается, когда речь идет о Бодхисаттвах, пролагающих
                себе путь в Запредельное Знание. Здесь это роль опять же (как и в примере со сном)
                отрицательная: Бодхисаттва не должен себя осознавать ни продвигающимся в
                Запредельном Знании, ни даже вообще Бодхисаттвой. Но почему же? Да потому, что в
                таком осознании его мысль оказывается направленной на самого себя как субъекта
                мышления, т.е. на «субъекта», на «мыслящего», на свое Пробуждение. А в конечном
                счете опять же на «Я». Здесь совершенно очевидно, как и в примере со сном, что этим
                осознанием Бодхисаттва неминуемо притягивает к себе кармический эффект своих мыслей
                и тем самым вновь ввергает себя в круг смертей и рождений.</p>
            <p><em>(10)</em> Я думаю, что в теории мышления, намеченной в «Восьмитысячнике», снова
                становится важным, если не решающим, принцип изолированности отдельной мысли, о
                котором мы знаем из предшествующих семинаров. Однако в сутрах Запредельного Знания
                этот принцип усиливается до такой степени, что это наводит на следующую мысль:
                каждые две отдельные мысли (или не-мысли, если уж на то пошло) возникают в двух
                отдельных мирах, никоим образом не соотносимых друг с другом. Интересно, что одним
                из возможных эпистемологических выводов из этого «абсолютного» разделения будет
                отделение знания от объекта знания. Но как одно отдельное от всего может знать
                (другое) отделенное от всего? Вопрос уже предполагает какую-то парадоксальную
                негативную эпистемологию. Утверждать, что все феномены, дхармы, не познаваемы, будет
                тривиальностью, которую великие учителя Запредельного Знания никогда себе не
                позволяли. Другое дело — переформулировать принцип изоляции следующим образом. Все
                феномены, дхармы, понимаемые как изолированные друг от друга мысли, являются
                непознаваемыми и не-познающими, ибо здесь нет ни не-изолированного знающего, ни
                не-изолированного объекта знания. Однако такое высказывание будет иметь смысл только
                с точки зрения Запредельного Знания, которое само ушло столь далеко за пределы
                знания вообще, что не может быть объектом никакой мысли, изолированной или
                не-изолированной.</p>
