                <p class="mb-3" id="citation-10"><sup>10</sup> Полное, Абсолютное Пробуждение (Skr.
                    samyaksambodhi) имеет здесь еще и более технический смысл последней стадии
                    Пробуждения.<a href="#ref-10" class="text-blue-500 hover:underline">↩</a></p>
                <p class="mb-3" id="citation-11"><sup>11</sup> «Сколь трудно для понимания
                    Взаимообусловленное Возникновение» — прямая отсылка к абхидхармистским аксиомам,
                    но с типичным для школы Запредельного Знания намерением их переистолкования.<a
                        href="#ref-11" class="text-blue-500 hover:underline">↩</a></p>
                <p class="mb-3" id="citation-12"><sup>12</sup> «Нет там никакого знака» — знак здесь
                    служит общим признаком нормального, не-бодхисаттвического восприятия вещей
                    (дхарм) и фактов.<a href="#ref-12" class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="mb-3" id="citation-13"><sup>13</sup> «Беззнаковость» (Skr. alaksanata)
                    здесь означает весьма высокую ступень в движении к Запредельному Знанию.<a
                        href="#ref-13" class="text-blue-500 hover:underline">↩</a></p>
                <p class="mb-3" id="citation-14"><sup>14</sup> «Волевые импульсы» — можно переводить
                    как «волевое действие» (Skr. chandas). Имеется в виду активное действие,
                    направленное на определенный объект.<a href="#ref-14"
                                                           class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="mb-3" id="citation-15"><sup>15</sup> Т.е. санскары, обусловленные
                    неведением, — опять отсылка к Взаимообусловленному Возникновению.<a
                        href="#ref-15" class="text-blue-500 hover:underline">↩</a></p>
                <p class="mb-3" id="citation-16"><sup>16</sup> См. прим. 264<a href="#ref-16"
                                                                               class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="mb-3" id="citation-17"><sup>17</sup> Акшобхья — один из пяти Татхагат
                    Сферы Трансформации (см. XIII). Он пребывает в своем Поле Будды (Skr.
                    buddha-ksetra), в котором все вещи, существа и события созданы его магической
                    силой. Таким образом, строго буддологически говоря, Поле Будды — это сфера
                    действия магической силы данного Будды.<a href="#ref-17"
                                                              class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="mb-3" id="citation-18"><sup>18</sup> Поле Будды — в котором происходит
                    этот диалог.<a href="#ref-18" class="text-blue-500 hover:underline">↩</a></p>
                <p class="mb-3" id="citation-19"><sup>19</sup> «Иллюзорные люди» — это человеческие
                    существа, сотворенные из ума или сознания великих йогов, Бодхисаттв и Татхагат
                    (см. в XII).<a href="#ref-19" class="text-blue-500 hover:underline">↩</a></p>
                <p class="mb-3" id="citation-20"><sup>20</sup> «Состояние не-существования» — это
                    полное отсутствие разума и сознания в живом существе.<a href="#ref-20"
                                                                            class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="mb-3" id="citation-21"><sup>21</sup> «Остановка в сфере пространства»
                    может означать (это не более чем еще одна буддологическая гипотеза) затухание
                    всех флюктуации, вызванных находящимся в пространстве сознанием.<a
                        href="#ref-21" class="text-blue-500 hover:underline">↩</a></p>
