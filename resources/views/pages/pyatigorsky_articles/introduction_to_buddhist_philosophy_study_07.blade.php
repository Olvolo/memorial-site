            <h3 class="text-xl text-center font-semibold text-zinc-700 my-4">Что с чем беседует? Дуализм ума и «Я» и дуализм двух умов: начало буддийской философии мышления</h3>
            <p>В этой беседе Талапутта разговаривает со своим умом в первом лице
                единственного числа. Его «Я» (кстати, часто, если не почти всегда,
                опускаемое в пали и санскрите) фигурирует только как подразумеваемое
                обозначение субъекта глагольного действия в предложении и, таким
                образом, не может быть заменено на «он» или «то». Обращаясь к своему
                уму, он, естественно, употребляет второе лице, а когда говорит о «нем»,
                то - третье. О «себе» Талапутта говорит либо в первом лице, либо в
                третьем. О «Я» Талапутты мы можем мыслить только в его чисто
                эмпирическом, дескриптивном значении, вне какой-либо связи с
                трансцендентальным «Я», атманом древней индийской религии, мифологии и
                философии. Таковы же и употребления слов «мой», «мои» и так далее.
                Поэтому в речи Талапутты «мой» в выражении «мой ум» имеет точно такой же
                смысл, как в выражениях «мой нос» или «мой зуб». Теперь мы знаем, что
                обозначают слова «Я» и «ум». Но что такое «ум» и что такое «Я»? Это
                совсем другое дело (напоминаю, «что такое я?» и «что такое Я?» - это
                два совершенно разных вопроса). Наш текст отвечает:</p>

            <p>(1) ум - это то, что думает (мыслит, полагает, желает и т.д.); в
                этом именно смысле ум считается шестым органом чувства (а также пятым
                агрегатом, скандхой индивидуального существования, о чем было сказано в
                I семинаре);</p>

            <p>(2) «Я» (но только в дескриптивном смысле) - то я, что мыслю (но не
                кто мыслит и не который мыслит, ибо первое лицо здесь обязательно).</p>

            <p>Ум не есть «Я», поскольку он лишь одна нить в ткани индивидуального
                существования, лишь одна составляющая последнего. При этом заметим, что
                из ответа на второй вопрос никак не следует и что «Я» есть мой ум, ибо
                если бы это было так, то ум, мышление, сознание никогда бы не стали
                главным предметом всего буддийского философствования. Картина
                упрощается. Талапутта, не являющийся одним только умом, разговаривает со
                своим умом, никак полностью не отождествимым с ним самим. Более того, ум
                и «Я» Талапутты часто оказываются противопоставлены друг другу в этой
                слишком упрощенной схеме, словом, все слишком просто, чтобы быть
                правдой. Необходим переход на новый виток философствования. Зададим еще
                один, третий вопрос, на который у текста нет ответа: что такое
                Талапутта? Но ответ на этот вопрос будет возможным, только если мы
                сначала спросим наш текст: Талапутта когда? Ибо только через Талапутту
                выходим мы на время. Талапутта здесь - это условное обозначение, имя
                времени изменений в «Я» и уме. По тексту выходит, что в прошлом
                Талапутты, вплоть до настоящего момента, его ум, склонный к аскетизму и
                созерцанию, дхьяне, полностью подчинил себе «Я», верно ему следующее и
                неуклонно совершенствующееся в йоге и аскетизме. В «настоящем» времени
                Талапутты его ум безнадежно деградировал и «Я» (но только в смысле
                ответа на второй вопрос) сбросило с себя иго ума и полностью его себе
                подчинило. Наконец, «будущее» Талапутты - это «мое» архатство, в
                каковом не будет ни времени, ни имени «Талапутта», ни ума или сознания,
                а для «брошенного» ума это будет провалом в бездну сансары. Иначе
                говоря, тогда ум и «Я» не будут связаны друг с другом ни положительно,
                ни отрицательно.</p>

            <p>В этом тексте «Талапутта» - всегда в подразумеваемом «третьем» месте,
                ибо это не то «Я», что обозначаю себя «Талапутта», а то, чем обозначил
                «меня» совсем другой монах, знаменитый комментатор Дхаммапала, который
                полтысячелетия спустя, комментируя «мой» рассказ, поведанный мною Будде
                на пороге «моего» архатства, напишет (к тому времени устная традиция
                давно сменилась письменной на Цейлоне), что это Талапутта говорил то,
                что «Я» говорю. Так в будущем описании прошлое «Я» превратилось в
                персонаж мифа об уме и мышлении.</p>

            <p>Теперь, подытоживая сказанное об отношении (как логическом, так и
                феноменологическом) «Я» и ума во времени Талапутты, отметим, что они
                противопоставлены, когда отличаются друг от друга или не равны друг
                другу в аскетизме и йоге. Так, скажем, в случае, когда Старейшина
                говорит: «Я владею умом, полностью контролирую его в созерцании и в
                сосредоточении сознания, т.е. в дхъяне и самадхи, и ты достигнешь
                полного освобождения от всех становлений», - то здесь подразумевается,
                что это Талапутта в настоящем времени, который йогически культивирует
                сейчас свой ум, но что это ум, который освободится от будущих рождений.
                Из этого может следовать и «мое» освобождение, как бы одновременное с
                освобождением ума. Но скорее из этого будет следовать, что все будущие
                становления, существования, рождения - это становления, существования
                и рождения ума (сознания). Ума как того, что мыслит.</p>

            <p>Однако все, что сейчас говорится об уме, прошлом или будущем, говорится
                в состоянии ума, трансцендентально себя (или что угодно другое)
                созерцающего, о чем свидетельствует и бесформенность «Я»
                (соответствующая третьей стадии дхьяны). Так мы переходим от дуализма
                «Я» и ума к дуализму просто ума и ума, уже трансформированного йогой
                созерцания. Один ум - это ум, «что мыслит» (желает, намеревается и так
                далее). Другой - это ум, «что созерцает». Тогда, соответственно, одно
                «я» - это «я», «что мыслю», а другое - это «я», «что созерцаю».
                Здесь есть еще один интересный момент. Ум сам по себе, как пятый агрегат
                индивидуального существования, бесформен. Поэтому «Я», как обладающий
                также и телом, т.е. формой, противопоставлен уму как тело противостоит
                бестелесному. Но раз достигнув в трансцендентальном созерцании состояния
                бесформенности, я по этому признаку, по крайней мере, становлюсь как бы
                «равным» уму. Тогда формулировка дуализма «Я» и ума и дуализма ума
                нормального и ума, трансформированного трансцендентальным созерцанием,
                окажется примерно следующей. В своем нормальном состоянии «Я» - это
                тело и ум вместе, а ум - это только ум. Тогда как в состоянии
                трансцендентального созерцания «Я» - это йогически трансформированный
                ум и ничего больше (ибо трансцендентальное созерцание может быть только
                самим собой и ни с чем не сочетается).</p>

            <p>Таким образом, в конечном счете (хотя «счет» всегда далек от конца)
                Беседа Старейшины Талапутты с Умом предлагает нам две дуалистические
                концепции: концепцию двух умов (нормального и трансформированного
                созерцанием) и концепцию Двух «Я» (телесного и бестелесного). Я думаю,
                что именно от этого дуализма ума ведет свое происхождение буддийская
                философия сознания (или мышления). Ведь только созерцающим умом возможно
                мыслить о «простом» уме, не говоря уже об «уме созерцающем». «Я»
                оказывалось как бы местом совпадения обоих умов и одновременно
                единственным местом (или позицией), где было бы возможно не только их
                отделение друг от друга, но и их выделение из всего целого, которое мы
                можем условно назвать «психосоматическим комплексом». Это весьма ясно
                показано в тех эпизодах текста VII, в которых Старейшина то обращается с
                ним как с тем же самым умом, то как с другим умом, то вообще не как с
                умом. Последний случай особенно интересен. Ведь когда в VII (2)
                Талапутта упрекает свой ум в том, что тот снова и снова его губит,
                являясь ему то в одной личине, то в другой, и играет с ним как с
                безумным (без-умным!), то это можно истолковать в смысле какого-то
                не-существования, отсутствия ума, подобным тому, которое может являться
                либо случаем психопатологии, либо особым йогическим состоянием (в роде
                того, о котором мы упоминали в предыдущем семинаре). В то же время можно
                предположить, что в этом случае мы имеем дело с идеей какого-то другого,
                «третьего» ума, объективированного как «вообще не ум», как некое
                фантомное существо, играющее с каким-то фантомным (то есть, отделенным
                от ума, не имеющим «своего» ума) существом. Словом, здесь можно
                предположить чисто иллюзионистскую версию концепции ума.</p>

            <p><em>(Вообще этот текст производит странное впечатление, напоминая своим
                    несколько экстатическим тоном средневековые бенгальские тантристские
                    гимны, дохи. Кстати, последние также нередко содержат обращение к
                    себе.)</em></p>

