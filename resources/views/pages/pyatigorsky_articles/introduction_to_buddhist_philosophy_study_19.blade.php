                    первую <em>скандху</em>, форму. Его знание — Высшее Знание Дхармичности. Его
                    место — в
                    центре космоса, его символ — жемчуг. <strong>(3)</strong> <em>Ратнасамбхава</em>
                    — это дхьяни-будда,
                    олицетворяющий космический баланс энергий. Его знание есть Высшее Знание Раности
                    Всего. Оттого он и связан со <em>скандхой</em> чувств, эмоций. Его место — Юг,
                    его символ
                    — драгоценность. <strong>(4)</strong> <em>Амитабха</em> — иногда рассматривается
                    как высшая манифестация
                    личностного аспекта Тела Дхармы, как воплощенное в образе созерцания буддство.
                    Его знание — Высшее Знание Абсолютного Успокоения. Он — космическое выражение
                    второй <em>скандхи</em>, восприятия. Его сторона — Запад, символ — лотос.
                    <strong>(5)</strong> <em>Амогхасиддхи</em>
                    манифестирует творческую активность буддства, его знание — Высшее Знание
                    Действия, а его <em>скандха</em> — <em>самскары</em>. Его сторона — Север,
                    символ — действие
                    (<em>карма</em>). Добавим еще, что «семья» каждого из пяти дхьяни-будд включает
                    в себя
                    его супругу (которая есть <span class="underline">активное</span> воплощение его
                    Знания), а также связанного с
                    ним <em>дхьяни-бодхисаттву</em> с супругой и множество других вторичных и
                    третичных тел
                    созерцания.<a href="#ref-25" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-26"><sup>26</sup> Иначе говоря, мысль Татхагаты
                    Вайрочаны полностью
                    отсечена от своего психо-ментального субстрата и функционирует в пространстве
                    чистого сознания.<a href="#ref-26" class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-27"><sup>27</sup> «Они чисты в своей природе» — то есть
                    по <em>свабхаве</em>, которая и есть Пустота.<a href="#ref-27"
                                                                    class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-28"><sup>28</sup> Это значит, что только посредством
