                    со Способом (санскр. <em>upāya</em>), то есть его самого с его йогической
                    помощницей,
                    <em>садхака</em> может во время совершения ритуального йогического секса обрести
                    Мысль
                    Пробуждения.<a href="#ref-28" class="text-blue-500 hover:underline">↩</a></p>

