            <h2 class="text-2xl font-bold text-center text-zinc-700 mb-8">Семинар девятнадцатый</h2>
            <h3 class="text-xl text-center font-semibold text-zinc-700 mb-4">Текст XIX. Буддийский
                Тантризм:<br>начало, фрагменты философии Ваджраяны
            </h3>

            <h4 class="text-lg font-semibold text-zinc-700 mt-6 mb-4">A. Происхождение<a href="#citation-1" id="ref-1"
                                                                                         class="citation-mark"><sup>1</sup></a></h4>

            <p><em>I.</em> Двумя (основными) школами Большой Колесницы являются школы Запредельного
                Знания и школа мантр<a href="#citation-2" id="ref-2"
                                       class="citation-mark"><sup>2</sup></a>. Согласно школе
                Запредельного Знания, все начиналось так. В продолжении первой неисчислимой кальпы<a
                    href="#citation-3" id="ref-3" class="citation-mark"><sup>3</sup></a> Будда
                неустанно накапливал заслуги и (последовательно) проходил стадии Пути Бодхисаттвы.
                Тогда между первой и седьмой стадией<a href="#citation-4" id="ref-4"
                                                       class="citation-mark"><sup>4</sup></a>
                бодхисаттства он завершил вторую кальпу. Затем между восьмой и десятой стадиями он
                завершил третью. Во время его последнего перерождения в небесном мире Акаништхи<a
                    href="#citation-5" id="ref-5" class="citation-mark"><sup>5</sup></a> все Будды
                десяти направлений вселенной дали ему посвящение Великого Сияющего Света<a
                    href="#citation-6" id="ref-6" class="citation-mark"><sup>6</sup></a>. По
                завершении им десятой стадии бодхисаттства, когда он обретался в подобном Ваджре
                состоянии самадхи, в его континууме мысли возник первый момент мысли<a
                    href="#citation-7" id="ref-7" class="citation-mark"><sup>7</sup></a> вместе со
                Знанием о конце его континуума. Когда же возник второй момент мысли, он достиг Тела
                Дхармы и Тела Преображения и стал манифестированным Полным и Совершенным Буддой.</p>

            <p><em>II.</em> Относительно Тела Преображения известны пять вещей. Первая, что это Тело
                идет в небесный мир Акаништхи и ни в какое другое место. Вторая, что оно учит Дхарме
                только Большой Колесницы и никакой другой Дхарме. Третья, что оно появляется
                украшенным всеми знаками Совершенства Будды. Четвертая, что его свита состоит только
                из Бодхисаттв десятой стадии, а не из обыкновенных людей. И наконец, пятая, о
                времени: Тело Преображения, пока сансара полностью не иссякнет, не учит методу
                перехода в Нирвану<a href="#citation-8" id="ref-8"
                                     class="citation-mark"><sup>8</sup></a>.</p>

            <p><em>III.</em> Но где же находится этот небесный мир Акаништхи, да будет нам дозволено
                спросить? — Акаништха расположена на самом краю обиталища Чистых Богов. За ней лежит
                Поле Будды, называемое гхана-вьюха. В сутре сказано:</p>

            <blockquote class="my-6 pl-4 border-l-4 border-gray-300">
                <p>«Там, в возвышенных обителях Чистых Богов<br>
                    Он обитает, Совершенно Пробужденный.<br>
                    Но, отбросив эти обители, как мираж, марево<a href="#citation-9" id="ref-9"
                                                                  class="citation-mark"><sup>9</sup></a>,<br>
                    Он становится<a href="#citation-10" id="ref-10"
                                    class="citation-mark"><sup>10</sup></a> Полностью и Совершенно
                    Пробужденным Буддой».</p>
            </blockquote>

            <p>Так Тело Преображения остается в небесном мире Акаништхи, а Сотворенное Тело Будды
                учит Пути и Методу в мире людей.</p>

            <p><em>IV.</em> Но в Поле Будды Победителя Будда Шакьямуни (когда он родился принцем
                Сиддхартой в Джамбудвипе, в Индии) пребывал во дворце своего отца и радовал свою
                мать. В это же самое время существовали мириады сансар, мириады Индий, мириады
                принцев по имени Сиддхарта были рождены в мириадах царских дворцов в мириадах
                городов по имени Капилавасту. И в том же самом Поле Будды мириады Будд Шакьямуни
                мириады раз поворачивали Колесо Дхармы. И так будет всегда, пока не иссякнет
                полностью сансара.</p>

            <p><em>V.</em> Согласно учению Колесницы Мантр, наш Учитель, Господь, уже обладал
                знаками Бодхисаттвы десятой стадии, когда он был рожден как принц Сиддхарта, и в
                течение времени, когда он предавался суровой аскезе как аскет Гаутама, до его
                прихода к берегу реки Ниранджана. После шести лет аскезы он обрел уравновешенность
                ума, достигаемую в четвертой дхьяне, и достиг самадхи Чистого Пространства<a
                    href="#citation-11" id="ref-11" class="citation-mark"><sup>11</sup></a>. Тогда
                все Будды десяти направлений пробудили его от этого самадхи, одновременно щелкнув
                пальцами, и сказали ему, что он не сможет стать Буддой только посредством этого
                самадхи. Они направили его Умственное Тело (Тело Самадхи)<a href="#citation-12"
                                                                            id="ref-12"
                                                                            class="citation-mark"><sup>12</sup></a>
                в небесный мир Акаништхи в то время, как его Тело Созревания<a href="#citation-13"
                                                                               id="ref-13"
                                                                               class="citation-mark"><sup>13</sup></a>
                оставалось на берегу реки Ниранджаны.</p>

            <h4 class="text-lg font-semibold text-zinc-700 mt-6 mb-4">B. Философские фрагменты<a
                    href="#citation-14" id="ref-14" class="citation-mark"><sup>14</sup></a></h4>

            <p><em>I.</em> «Пустота абсолютно тверда, субстанциальна<a href="#citation-15"
                                                                       id="ref-15"
                                                                       class="citation-mark"><sup>15</sup></a>,
                непроницаема, несжигаема, неуничтожима. Такая пустота носит имя Ваджра<a
                    href="#citation-16" id="ref-16" class="citation-mark"><sup>16</sup></a>. Все
                дхармы своей первичной вечной природой подобны ваджре. Я по своей природе
                сопричастен Ваджра-подобному Знанию».</p>

            <p><em>II.</em> «Что же тогда есть Существо Ваджры, Ваджрасаттва? «Ваджра» в имени этого
                существа означает Пустоту, «Существо» означает Чистое Знание. Их абсолютное
                тождество — это их собственная природа, свабхава Ваджрасаттвы. В Ваджрасаттве все
                формы и манифестации всех вещей сплавлены в одно и неузнаваемы в их различиях.
                Объективность Ваджрасаттвы — в Пустоте, а субъективность — в существе, в саттве, но
                они есть одно<a href="#citation-17" id="ref-17" class="citation-mark"><sup>17</sup></a>.
                ...И хотя Ваджрасаттва свободен от бытия и не-бытия, он при этом наделен
                способностью принимать все формы, виды и образы. Он — Господь, ибо владеет
                свабхавами всех существ, саттв. Запредельное Знание — его единственный глаз. Он —
                чистое воплощение Знания, он всепроникающий, вездесущий, неизменяемый. Он —
                тончайшее семя<a href="#citation-18" id="ref-18" class="citation-mark"><sup>18</sup></a>
                всех живых существ. Во всех живых существах он принимает форму потока сознания,
                континуума мысли. Он — держатель мысли<a href="#citation-19" id="ref-19"
                                                         class="citation-mark"><sup>19</sup></a>,
                единственный действователь, единственный разрушитель, манифестация бесконечного
                блаженства».</p>

            <p><em>III.</em> «Преклоняюсь пред тобой, о Зародыш Пустоты<a href="#citation-20"
                                                                          id="ref-20"
                                                                          class="citation-mark"><sup>20</sup></a>,<br>
                О Ты, свободный от всех идей и понятий ума<a href="#citation-21" id="ref-21"
                                                             class="citation-mark"><sup>21</sup></a>,<br>
                Ты, великий победитель неведения,<br>
                Ты, создатель знания, воплощенное знание.<br>
                Ты, рожденный из «не-Я» всех дхарм, о Ваджрасаттва!<br>
                Ты Мысль Пробуждения, Ты источник всех Бодхисаттв!<a href="#citation-22" id="ref-22"
                                                                     class="citation-mark"><sup>22</sup></a>»
            </p>

            <p><em>IV.</em> «Практикующий йог, садхака, постигающий Ваджрасаттву, сам становится
                Ваджрасаттвой, поскольку постигает, что все, что есть, — это не что иное, но Пустота
                в ее первозданной природной чистоте. Садхака также называется Великим Существом,
                Махасаттвой, поскольку он полон блаженством бесконечной мудрости, праджни. Еще он
                зовется Самайя-Саттва, Существо (Великого) Ритуала, ибо он постоянно поглощен
                исполнением обетов и совершением обрядов. Ум садхаки пробужден, вся его психика
                просветлена и прозрачна, в этом смысле он является Бодхисаттвой и Буддой-Татхагатой.
                Садхака пусть предается созерцанию себя как космического буддства, которое он должен
                непрерывно почитать. Ибо постижение себя самого во всех вещах есть единственная
                абсолютная самореализация<a href="#citation-23" id="ref-23"
                                            class="citation-mark"><sup>23</sup></a> — это и есть
                тайна всех йог<a href="#citation-24" id="ref-24" class="citation-mark"><sup>24</sup></a>».
            </p>

            <p><em>V.</em> «Собрались раз Татхагаты<a href="#citation-25" id="ref-25"
                                                      class="citation-mark"><sup>25</sup></a>,
                приблизились к Господу, Татхагате Вайрочане, чтобы он им объяснил сущность Мысли
                Пробуждения. Тогда Вайрочана погрузился в созерцание особого рода и сказал: «Когда
                бытие воспринимается с точки зрения небытия, тогда в сознании более не остается
                никаких выдуманных интеллектуальных концептов и построений... и бытие...
                прекращается». Он воскликнул: «Моя мысль свободна от всех идеи, связанных с бытием и
                не-бытием, она не опирается более ни на агрегаты, скандхи, ни на сферы органов
                чувств<a href="#citation-26" id="ref-26" class="citation-mark"><sup>26</sup></a>, ни
                на элементы, ни на восприятия. Моя мысль ни из чего не возникает, не имеет
                происхождения и пуста в своей природе, свабхаве». Тут воскликнул Татхагата Акшобхья:
                «Все — бессущностно, как небо, нет никаких дхарм, никакой дхармы, только одна эта
                неизменная, неизменяемая Мысль Пробуждения!» Затем Татхагата Ратна-Самбхава сказал:
                «Все дхармы лишены рождения и возникновения. Оттого нет ни бытия, ни идеи бытия». О
                бытии вещей говорится как о бытии чистого пространства. Тут наконец воскликнул пятый
                Татхагата, Амогхасиддхи, пребывающий в глубоком трансцендентальном созерцании: «Все
                дхармы как мгновенные всплески, они чисты в своей природе<a href="#citation-27"
                                                                            id="ref-27"
                                                                            class="citation-mark"><sup>27</sup></a>».
            </p>

            <p><em>VI.</em> «Состояние ума практикующего йога, садхаки, таково. Он чувствует
                абсолютную равность, «ту же самость» всех существ, саттв; он преисполнен милости и
                сострадания, которые и являются основным средством, способом в достижении Полного
                Совершенного Пробуждения; он сам и есть это средство. Но его Высшее Знание Пустоты
                манифестируется в великой женщине, йогине, также называемой Праджня. Только в полном
                сочетании с ней обретается Мысль Пробуждения<a href="#citation-28" id="ref-28"
                                                               class="citation-mark"><sup>28</sup></a>.
                Отношение Пустоты к Состраданию подобно отношению любящих в естественной любви. Так
                богиня Праджня всегда есть объект знания, знаемое. Отсюда природа Праджни есть
                не-бытие, а природа Способа или Сострадания есть бытие. Вся же истина — в их
                супружеском соединении<a href="#citation-29" id="ref-29" class="citation-mark"><sup>29</sup></a>».
            </p>

            <p>«Пока в потоке сознания перемешаны мыслительные конструкции, весь ум остается
                замутненным. Ведь даже то, что приносит чистое блаженство и светлое счастье
                успокоившемуся сердцу, — есть лишь игра ума. Даже йогическое бесстрастие — что уж
                там, как бесстрастие, так и страсть также должны быть вычищены из ума. Ибо они —
                основные причины бытия. Нирваны нет нигде, кроме трансцендентального<a
                    href="#citation-30" id="ref-30" class="citation-mark"><sup>30</sup></a>
                существования «Я»<a href="#citation-31" id="ref-31"
                                    class="citation-mark"><sup>31</sup></a>.</p>

            <p><em>VII.</em> «Так объяснял Господь свою свабхаву Бодхисаттве Ваджрагарбхе:</p>

            <blockquote class="my-6 pl-4 border-l-4 border-gray-300">
                <p>Бытие я есть, не-бытие я есть, я,<br>
                    Будда, обладающий совершенным знанием всех вещей.<br>
                    Глупые не понимают меня, обитающего<br>
                    В лоне моей Праджни.<br><br>
                    Я — проповедник, я — дхарма, которую проповедую.<br>
                    Я — весь мир и все вещи в нем.<br>
                    Моя собственная природа, свабхава — в высшем<br>
                    блаженстве семени в лоне Праджни.<br><br>
                    Только в божественной Тайне (ритуала соития)<br>
                    Возможно свершение Наивысшего»<a href="#citation-32" id="ref-32"
                                                     class="citation-mark"><sup>32</sup></a>;</p>
            </blockquote>

            <h3 class="text-xl text-center font-semibold text-zinc-700 my-6">Что это такое?
                Постскриптум к буддийской философии или историческая форма буддизма. Инверсивная
                история. Множественность времени. Гностицизм и теизм буддийского тантризма.
                Восстановленное «Я» как основа тантристской антропологии. Эсхатология — «пока
                вселенная не иссякнет»</h3>

            <p>По первому впечатлению от фрагментов, приведенных в этом семинаре, Колесница Мантр
                или Колесница Ваджры, Ваджраяна — это религия. Примем это как гипотезу и попробуем
                произвести самый сжатый феноменологический анализ этой религии на основании тех
                существенных различений, которые в сумме составляют содержание самого универсального
                понятия и вместе с тем самого универсального термина описания Ваджраяны как религии,
                термина и понятия тантры (Skr. tantra), что буквально этимологически означает
                «ткань», «прядь», «то, что тянется». Вот семь основных смыслов, в которых термин
                «Тантра» употребляется в Ваджраяне.</p>

            <p><em>(0.1.1)</em> Тантра как священный текст, произнесение которого приписывается Телу
                Трансформации того или иного Будды или Бодхисаттвы. Иногда такой текст также
                содержит описание обстоятельств, в которых он создавался. Тантра в этом смысле
                называется именем Будды или Бодхисаттвы, ее произнесшего или в ней описанного
                (Калачакра-Тантра, Гухьясамаджа-Тантра и так далее). Священность этого текста
                определяется прежде всего тем, что он сам служит объектом почитания в качестве
                священного предмета в ритуале (Skr. puja) вне зависимости от того, насколько
                понимается его содержание и, вообще, насколько он воспринимается как текст (а не,
                скажем, как вещь). Среди таких текстов выделяются первичные или «корневые» (Skr.
                mula-tantra), и расширенные, или производные.</p>

            <p><em>(0.1.2)</em> Тантра как визуально-эйдетический символ данного Будды или
                Бодхисаттвы, как его синтетический «образ образов», зафиксированный в иконографии.
            </p>

            <p><em>(0.1.3)</em> Тантра как аудиально-эйдетический символ данного Будды или
                Бодхисаттвы, как синтетический звуковой «образ образа», ни один элемент которого,
                взятый в отдельности, не несет в себе никакой семантики. Такое чисто символическое
                звуко-слово-сочетание называется мантра (отсюда и название «Колесница Мантр»,
                мантраяна).</p>

            <p><em>(0.1.4)</em> Тантра как комплекс ритуальных и медитативных действий, где
                собственно ритуал сливается с медитацией (в формах дхьяны, самадхи и бхаваны) и с
                произнесением мантр в единой интенциональности, направленной на Будду или
                Бодхисаттву данной тантры, гуру или Учителя, созерцаемого и почитаемого
                учеником-садхакой как Будда или Бодхисаттва, и на самого садхаку, ставшего в
                созерцании созерцаемым им его «личным божеством» (Skr. ista-devata, по-тибетски
                yi-dam).</p>

            <p><em>(0.1.5)</em> Тантра как комплекс инициационных обрядов, посредством которых
                новичок снискивает у учителя разрешение и способность читать текст тантры, правильно
                произносить мантры, а также знать способы и порядок созерцательных и ритуальных
                процессов и процедур. Инициационные обряды обладают особой сверхъестественной силой,
                которая переходит на посвященного садхаку.</p>

            <p><em>(0.1.6)</em> Тантра как традиция. Однако сам феномен традиции в Ваджраяне имеет
                два временных измерения. Во-первых, чисто мифологическое: тантра, как она
                наблюдается в данный момент во всех вышеперечисленных смыслах, выводится из некоего
                идеального, обычно до-временного, первоначального состояния, двумя основными
                элементами которого являются данный Будда или Бодхисаттва и священный текст. От
                этого первоначального состояния начинается передача священного текста, ритуала и
                способов созерцания от учителя к ученику, от ученика как учителя к следующему
                ученику и так далее, передача, единственным средством, точнее, механизмом которой
                является обряд инициации. При этом первым учителем и инициатором выступает сам
                данный Будда или Бодхисаттва. Второе временное измерение я условно называю
                инверсивным. В этом измерении первоначальное (мифологическое) состояние тантры
                выводится обратным образом из ее наличного, сиюмоментного состояния, тремя основными
                элементами которого являются данный Будда или Бодхисаттва, учитель и практикующий
                йог, садхака. Прошлая история тантры, вся ее традиция посвящения существует только
                как уже актуализированная в сиюмоментном состоянии, которое само случается как бы в
                «нулевом» времени.
            </p>
            <p><em>(0.1.7)</em> И наконец, каждая тантра в последнем, часто дескриптивном смысле
                выступает как отдельный культ. Скорее, может быть, даже как микрокульт, если
                полагать Ваджраяну в целом как макрокульт. Минимум субъектов культа — это один
                посвященный йог-ученик, садхака (или двое, в случае сексуальной тантры). Максимум не
                определен, возможен случай посвящения тысяч и тысяч в одну тантру. При этом отметим
                тот факт, что одна личность может иметь посвящения во многие различные тантры.
                Говоря строго феноменологически, одна тантра отличается от другой только абсолютно
                индивидуальным именем Будды или Бодхисаттвы, а в некоторых случаях — именем гуру,
                учителя, отождествляемого с данным Буддой или Бодхисаттвой. Общее количество тантр
                неизвестно. Если исходить из опубликованных списков имен тантристских Будд и
                Бодхисаттв, а также из монастырских иконографических регистров и каталогов, число
                тантр как отдельных культов на середину XX века никак не могло быть меньше двадцати
                тысяч.
            </p>
            <p><em>(0.2)</em>
                Переходя к конкретному содержанию наших фрагментов, я сначала делаю одну
                существенную методологическую оговорку. Моя позиция внешнего наблюдателя Ваджраяны
                обуславливает объективизм моего общего подхода к философии буддийского тантризма,
                объективизм, иногда натянутый и всегда искусственный. Так, говоря об «истории», как
                она трактуется во фрагменте (А.1), можно только чисто объективистски установить, что
                она редуцируется к происхождению, генезису мышления об истории, точнее, даже к
                выдумыванию истории, истории в том смысле, в котором мы ее понимаем здесь и сейчас,
                то есть в смысле заведомо субъективном. В нашем объективистском подходе история —
                это один из случаев другого мышления человека о себе самом во времени (как он его
                себе представляет), мышления, образ или тип которого может быть лишь условно
                соотнесен с образом или типом нашего мышления. Тогда «объективность» и
                «субъективность» явятся терминами, обозначающими не отношение мышления к его
                объекту, а отношение одного мышления к другому.</p>
            <p><em>(0.3)</em>
                Но специфика тантристской концепции генезиса, происхождения состоит в том, что она
                не о том, когда что-либо (или все) произошло, а о том, где что-либо впервые
                случилось. Ибо время здесь оказывается вторичным и производным от места события.
                Так, стадии в достижении бодхисаттства измеряются в кальпах, каждая из которых
                насчитывает миллиарды астрономических лет. Но пребывание Будды в трансцендентальном
                самадхи исчисляется в микрокосмическом времени индивидуального континуума мысли,
                времени, измеряемого в единицах отдельных мгновенных дхарм-мыслей. А состояние
                самадхи служит местом возникновения «первой» мысли Будды.</p>
            <p><em>(1.1)</em>
                Как явствует из фрагмента (А.2), местом происхождения Ваджраяны — или буддизма в
                целом, или, если на то пошло, всего космоса — является Тело Преображения Будды,
                которое соотносится с десятой стадией бодхисаттства и с трансцендентальным Самадхи
                Чистого Пространства, то есть пространства без времени. Тело, пребывающее в небесном
                мире Акаништхи. За последним — ну, скажем, ради полноты тантристской космологии —
                расположено Поле Будды, называемое «сверхтяжелое» (Skr. ghana), неумопостигаемое и
                не существующее во времени.</p>
            <p><em>(1.2)</em>
                Весь космос Тела Преображения Будды произведен из самого себя и обладает
                сверхъестественной способностью производить из себя же любые вещи и любые живые
                существа. Это происходит посредством трансформации ума, пребывающего в
                трансцендентальных дхьянах или самадхи, в любой созерцаемый этим умом предмет или в
                любое существо. Более того, только в космосе Тела Преображения Будды создаются все
                другие космосы и вообще все пространство. Классическая буддийская концепция трех тел
                Будды оказалась радикально переосмысленной в Ваджраяне следующим образом.</p>
            <p><em>(1.3.1)</em>
                Тело Дхармы, Тело Преображения и Тело Сотворения возможны, только если их
                рассматривать с точки зрения Тела Преображения, которое постулируется в Ваджраяне
                как универсальное место порождения всего сущего (включая себя самое), как
                онтологический фокус всего мышления и всего мыслимого. </p>
            <p><em>(1.3.2)</em>
                Как время, так и пространство существуют только относительно Тела Преображения. Или
                если речь идет о микрокосмосе созерцателя, то время и пространство существуют только
                в отношении конкретного места и момента созерцания им Тела Преображения Будды или
                Тел Трансформации Будды или Бодхисаттвы данной тантры. Отсюда следует идея
                множественности времен и пространств, в которых любое данное событие случается,
                случилось или могло бы случиться. Тогда и «нормальное» линейное время жизни йога
                будет, в его созерцании, сведено к одному из таких «частных» времен космоса Тела
                Преображения. Но самым интересным философским следствием этой
                временно-пространственной множественности является принципиальная невозможность
                уникальности какого бы то ни было события, факта или феномена. Это относится даже к
                таким важнейшим мировым событиям, как рождение или Нирвана Будды.</p>
            <p>
                Более того, как об этом говорится во фрагменте (A), Будда обрел свое Тело Дхармы,
                находясь в своем Теле Преображения. Свое «физическое тело» он создал, только
                спустившись из небесного мира Акаништхи, которая является Местом Тела Преображения,
                после чего он перешел в Нирвану. Не будет преувеличением заключить, что Тело Дхармы
                и Сотворенное Тело Будды являются в Ваджраяне не более чем производными от Тела
                Преображения.
            </p>
            <p><em>(2.0)</em>
                Теперь попытаемся философски осмыслить вторую исключительно важную и по сю пору
                остающуюся загадочной идею Ваджраяны, идею Ваджры. Я называю ее загадочной из-за
                крайней трудности ее «извлечения» из ритуального контекста буддийского тантризма для
                того, чтобы добраться до метафизической сущности этого по определению символического
                понятия. Именно метафизической в буквальном смысле слова, ибо как символ Ваджра —
                вещь и священнейший предмет тантристского ритуала, вещь вещей, «квазифизические»
                свойства которой, такие как сверхтяжесть, сверхтвердость, всепроникаемость и
                абсолютная непроницаемость, потенциально присутствуют во всех вещах и существах
                космоса Ваджрасаттвы. Концепцию Ваджры можно себе представить как ядро какой-то нам
                еще не известной тантристской прото-философии. Ваджра во фрагменте (B) — это
                универсальный источник вещности, бытийности всех вещей и фактичности всех фактов.
                Заметим, что в Ваджраяне, как и в виджнянаваде и отчасти в сарвастиваде, каждый
                феномен существует одновременно в двух аспектах, модальном и абсолютном. Однако в
                Ваджраяне абсолютный аспект, концептуализированный в идее Ваджра, решительно
                превалирует. Фактически все вещи, оказывающиеся в пространстве тантристского
                трансцендентального созерцания (включая и созерцателя), являются «ваджровыми»
                (позволяю себе употребить выдуманное ad hoc прилагательное). Ваджра, таким образом,
                обозначает и особый класс идеальных сущностей (идеальных, то есть «трансцендентально
                созерцаемых»), из которых наиболее значимыми философски являются три.</p>
            <p><em>(2.1)</em>
                Первой сущностью является Пустота, онтологическая основа, абсолютная объективность
                космоса Ваджры. Поскольку все дхармы пусты по своей собственной природе, свабхаве,
                они имеют природу Ваджры. Однако «Я», условно обозначающее тантристского йога,
                садхаку, также имеет природу Ваджры, ибо он объективно — а не только субъективно, в
                силу того, что он созерцает себя как Пустоту (и говорит, «Я — пустота») — есть
                Ваджра, ибо обладает подобным Ваджре Знанием Пустоты, то есть Праджней, которая
                является субъективным аналогом объективности Пустоты.</p>
            <p><em>(2.2)</em>
                Второй сущностью является особое существо, существо всех существ, называемое
                Существо Ваджры, Ваджрасаттва. В этом имени, обозначающем двойственность его
                природы, Ваджра обозначает абсолютную объективность Пустоты, а саттва — его
                абсолютное Знание Пустоты, которое, условно говоря, субъективно как его знание, хотя
                оно есть Запредельное Знание. Собственная природа, свабхава Ваджрасаттвы заключается
                в единстве, абсолютной тождественности субъективного и объективного аспектов его
                бытия. Ваджрасаттва выступает как единственный источник и первопричина всех живых
                существ всех уровней сознания. Эта черта Ваджрасаттвы возвращает нас к вопросу
                реонтологизации. Ведь сам он является и суммой всех форм мышления и сознания в живых
                существах, и в то же время основной формой бытия мышления и сознания в космосе, то
                есть потоком или континуумом мысли. Но поскольку бытие и не-бытие — это лишь
                конкретные конструкции непробужденного ума, то Ваджрасаттва находится за пределами
                мыслимого в терминах бытия и не-бытия. Все это, разумеется, ассоциируется в нашем
                уме с идеей теистического бога или божества. Но не будем забывать, что историческим
                контекстом, в котором создавалась Ваджраяна, была (пока еще чисто индийская) религия
                буддизма Большой Колесницы. Кроме того, формирование буддийского тантризма
                происходило под сильнейшим влиянием философских школ, Запредельного Знания,
                мадхъямики и виджнянавады. Поэтому я, как объективистский внешний наблюдатель,
                рассматриваю тантристскую концепцию Ваджрасаттвы как случай объективного
                ограниченного теизма, то есть случай, когда личные черты и особенности божества
                нейтрализуются, а иногда и вытесняются объективностью его чисто трансцендентальных
                особенностей и качеств. В случае Ваджраяны — «ваджра-качеств». Сам я склонен думать,
                что Ваджрасаттва — «относительно личностей», не «сверх-личностен», ибо его
                личностность ограничена «сверху» Телом Дхармы, абсолютно безличностным и
                не-манифестированным, а «снизу» — его собственным умственным Телом Трансформации,
                манифестированным в Теле Преображения. Так что, в общем, я считаю методологически
                неправильным говорить о концепции Ваджрасаттвы в понятиях теизма (так же как и
                «монотеизма», «политеизма» и так далее).</p>
            <p><em>(2.3)</em>
                И наконец, третьей сущностью Ваджраяны является «Ваджровое Я». В тантристской
                концепции «Я» можно видеть не отклонение, а чуть ли не отбрасывание основных
                постулатов мадхъямики и виджнянавады, к которым Ваджраяна безусловно восходит в
                своих философских основаниях. В то же время тантристскую концепцию «Я» было бы
                крайне интересно рассматривать как своего рода «запоздалый рефлекс» на классическую
                брахманистскую концепцию атмана-брахмана. Замечательно, что развитие этого
                «рефлекса» четко наблюдается от раннесредневековых школ буддийского тантризма через
                так называемую «новую Тантру» в Тибете, вплоть до новейших тибетских тантристских
                движений XIX—XX веков. Но спросим, о чьем «Я» идет речь? Мы знаем, что конечной
                целью каждого практикующего тантристского йога-созерцателя является достижение Мысли
                Пробуждения, а Мысль Пробуждения достигается йогом-созерцателем, садхакой, только
                когда он превращается в Будду или Бодхисаттву, обитающего в сфере Тела.</p>
            <p>
                Преображения. Но такое превращение реализуется только посредством созерцания
                садхакой самого себя как «своего» Будды или Бодхисаттвы. Это превращение — фокус
                всей тантристской созерцательной и ритуальной интеллектуальной деятельности садхаки.
                Тогда естественным ответом на вопрос, о чьем «Я» идет речь, будет: «Я» садхаки. Ведь
                в конечном счете его созерцание — это самосозерцание, его почитание — самопочитание,
                а его трансформация в Будду или в Бодхисаттву — самотрансформация.
            </p>
            <p>
                Однако здесь мы подошли к интереснейшему моменту философии Ваджраяны. Это — момент,
                скажем так, покидания равно брахманистского постулата о «Я» и буддийского постулата
                о «Не-Я» и перехода от атмана, «Я», к личности, или, говоря другими словами,
                перехода от эгологии к персонологии.
            </p>
            <p>
                В основе тантристской персонологии лежит концепция особого «Я», так сказать, «Ваджра
                Я» садхаки. Садхака есть личность, персона оттого, что это его «Я» — одно с «Ваджра
                Я» созерцаемых им Будд и Бодхисаттв. Можно даже сказать, что Пять Трансцендентальных
                Татхагат буддийского тантризма являются «ваджровыми Я» созерцателей-садхак, теми
                «Я», которые проецируются созерцателями в сферу Тела Преображения Будды. Но ведь эта
                концепция, рассматриваемая в индийской философской ретроспективе, почти равнозначна
                столь невероятному для буддизма признанию трансцендентного «Я», атмана Упанишад и
                Веданты, не так ли? Но в том-то и дело, что сама Ваджраяна есть явление философски
                невероятное в истории не только буддизма, но и индийской философской мысли в целом.
            </p>
            <p>
                Если рассматривать Ваджраяну как религиозную (культовую, ритуалистическую)
                философию, то приходится согласиться с тем, что от относительного персонализма
                ваджрового индивидуального «Я» созерцателя, абсолютно тождественного Ваджра Я
                Ваджрасаттвы, только один шаг до чисто теистической концепции Бога в поздних
                брахманистских религиозных философиях.
            </p>
            <p><em>(3)</em>
                Эта «двусоставная» личность ваджраянистской персонологии, Ваджравое существо,
                имеющее своим трансцендентальным аналогом также «двусоставного» бога, Татхагату
                Ваджрасаттву, обладает своим собственным четко установленным этосом — образом
                поведения, речи и мышления. Разумеется, что поведение садхаки — по преимуществу
                ритуально-созерцательное, йогическое, речь — мантрическая, его мышление лучше всего
                охарактеризовать как особую форму религиозного дуализма. Говоря о тантристском
                этосе, можно добавить, что он полностью нейтрализует все социальные, политические и
                экономические различия и статусы. Один садхака отличается от другого только по
                тантре и степени посвящения.</p>
            <p>
                Вообще тенденция к дуализму несомненно является одной из важнейших черт буддийского
                религиозно-философского мировоззрения. Эта тенденция оказалась значительно
                ослабленной в философском монизме мадхьямики и квазипсихологическом плюрализме
                виджнянавады, но предельно усилилась в Ваджраяне. В последней мы одновременно
                фактически живем в двух противопоставленных друг другу мирах: один мир — это мир
                обыкновенных мирских существ, которые не могут, по причине не-энергетичности (речь
                идет о сверхъестественной йогической энергии) их сознаний, трансформировать себя в
                ваджровые существа космоса Тела Преображения Татхагаты. Другой мир — это мир
                посвященных созерцателей — садхак, ваджровых существ, уже превратившихся сейчас (то
                есть в завершающее мгновение тантристского трансцендентального созерцания, а не в
                неопределенном будущем) в Тела Трансформации Будд и Бодхисаттв. Именно в таких
                умственных Телах Трансформации (о которых подробно говорилось в семинаре о
                Ланкаватаре-Сутре) — или, точнее будет сказать, будучи этими Телами Трансформации, —
                они обретают Мысль Пробуждения в данном их человеческом рождении.
            </p>
            <p><em>(4)</em>Так к трем уже известным нам типам человека в буддийской философии, то
                есть homo asceticus — человек-аскет, homo meditator—человек-созерцатель и homo
                bodhisattva—человек-бодхисаттва, добавляется четвертый, то есть homo tantricus —
                тантрический человек. Возможен вопрос: отменяет ли четвертый тип то, что мы
                несколько метафорически называем «человеческой природой» и «условиями человеческого
                существования»? Ответ: да, отменяет, если не целиком, то, во всяком случае, в
                гораздо большей степени, чем другие три типа.
            </p>
