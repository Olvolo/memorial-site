            <h2 class="text-2xl font-bold text-center text-zinc-700 mb-8">Семинар девятнадцатый</h2>
            <h3 class="text-xl text-center font-semibold text-zinc-700 mb-4">Текст XIX. Буддийский
                Тантризм:<br>начало, фрагменты философии <em>Ваджраяны</em>
            </h3>

            <h4 class="text-lg font-semibold text-zinc-700 mt-6 mb-4"><span
                    class="underline">A</span>. Происхождение<a href="#citation-1" id="ref-1"
                                                                class="citation-mark"><sup>1</sup></a>
            </h4>

            <p><em>I.</em> Двумя (основными) школами Большой Колесницы являются школы Запредельного
                Знания и школа мантр<a href="#citation-2" id="ref-2"
                                       class="citation-mark"><sup>2</sup></a>. Согласно школе
                Запредельного Знания, все начиналось так. В продолжении первой неисчислимой кальпы<a
                    href="#citation-3" id="ref-3" class="citation-mark"><sup>3</sup></a> Будда
                неустанно накапливал заслуги и (последовательно) проходил стадии Пути Бодхисаттвы.
                Тогда между первой и седьмой стадией<a href="#citation-4" id="ref-4"
                                                       class="citation-mark"><sup>4</sup></a>
                бодхисаттства он завершил вторую кальпу. Затем между восьмой и десятой стадиями он
                завершил третью. Во время его последнего перерождения в небесном мире Акаништхи<a
                    href="#citation-5" id="ref-5" class="citation-mark"><sup>5</sup></a> все Будды
                десяти направлений вселенной дали ему посвящение Великого Сияющего Света<a
                    href="#citation-6" id="ref-6" class="citation-mark"><sup>6</sup></a>. По
                завершении им десятой стадии бодхисаттства, когда он обретался в подобном Ваджре
                состоянии самадхи, в его континууме мысли возник первый момент мысли<a
                    href="#citation-7" id="ref-7" class="citation-mark"><sup>7</sup></a> вместе со
                Знанием о конце его континуума. Когда же возник второй момент мысли, он достиг Тела
                Дхармы и Тела Преображения и стал манифестированным Полным и Совершенным Буддой.</p>

            <p><em>II.</em> Относительно Тела Преображения известны пять вещей. Первая, что это Тело
                идет в небесный мир Акаништхи и ни в какое другое место. Вторая, что оно учит Дхарме
                только Большой Колесницы и никакой другой Дхарме. Третья, что оно появляется
                украшенным всеми знаками Совершенства Будды. Четвертая, что его свита состоит только
                из Бодхисаттв десятой стадии, а не из обыкновенных людей. И наконец, пятая, о
                времени: Тело Преображения, пока сансара полностью не иссякнет, не учит методу
                перехода в Нирвану<a href="#citation-8" id="ref-8"
                                     class="citation-mark"><sup>8</sup></a>.</p>

            <p><em>III.</em> Но где же находится этот небесный мир Акаништхи, да будет нам дозволено
                спросить? — Акаништха расположена на самом краю обиталища Чистых Богов. За ней лежит
                Поле Будды, называемое гхана-вьюха. В сутре сказано:</p>

            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg"> Там, в
                возвышенных обителях Чистых Богов<br>Он обитает, Совершенно Пробужденный.<br>Но,
                отбросив эти обители, как мираж, марево<a href="#citation-9" id="ref-9"
                                                          class="citation-mark"><sup>9</sup></a>,<br>Он
                становится<a href="#citation-10" id="ref-10" class="citation-mark"><sup>10</sup></a>
                Полностью и Совершенно Пробужденным Буддой.
            </blockquote>

            <p>Так Тело Преображения остается в небесном мире Акаништхи, а Сотворенное Тело Будды
                учит Пути и Методу в мире людей.</p>

            <p><em>IV.</em> Но в Поле Будды Победителя Будда Шакьямуни (когда он родился принцем
                Сиддхартой в Джамбудвипе, в Индии) пребывал во дворце своего отца и радовал свою
                мать. В это же самое время существовали мириады сансар, мириады Индий, мириады
                принцев по имени Сиддхарта были рождены в мириадах царских дворцов в мириадах
                городов по имени Капилавасту. И в том же самом Поле Будды мириады Будд Шакьямуни
                мириады раз поворачивали Колесо Дхармы. И так будет всегда, пока не иссякнет
                полностью сансара.</p>

            <p><em>V.</em> Согласно учению Колесницы Мантр, наш Учитель, Господь, уже обладал
                знаками Бодхисаттвы десятой стадии, когда он был рожден как принц Сиддхарта, и в
                течение времени, когда он предавался суровой аскезе как аскет Гаутама, до его
