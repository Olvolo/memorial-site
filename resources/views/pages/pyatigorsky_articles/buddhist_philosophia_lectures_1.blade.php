            <h3 class="text-center font-bold mt-6">Краткое введение к рассказу о буддийской философии: текст, дхарма и личность</h3>

            <p>Это небольшое введение — для понимания того, о чем пойдет речь дальше.
                Значит, для читающего, чтобы легче было понимать, и для меня, чтобы
                легче было излагать свое собственное понимание. Начиная эти лекции, я не
                только не рассчитываю на их понимание читающим, но и вполне осознаю тот
                факт, что многое из того, что буду излагать, сам категорически понять не
                в состоянии. Но поскольку вниманию читающего предлагается материал уже
                известный, то есть некоторым образом познанный как <em>объект знания</em>,
                условно называемый «буддийская философия», то нам с вами предоставляется
                возможность (свобода?) его понимания или непонимания.</p>

            <p>Сначала о названии лекций. Слово «философия» здесь не только условно, но
                и обусловлено в своем значении и употреблении контекстом европейской
                историко-философской традиции Просвещения (XVII-XX вв. н. э.). И когда
                я называю этим словом нечто совершенно чуждое этой традиции, я обязан
                обратить ваше внимание на наиболее характерные моменты этой чуждости.
                Самым важным из этих моментов является принципиальное различие в том,
                что я позволил бы себе, опять же условно, назвать «единицей
                философствования». В европейской историко-философской академической
                традиции такой единицей безусловно является ИДЕЯ (<em>положение, постулат,
                    аксиома</em> и т. д). Идеи служат для нее не только составными элементами
                описываемой философской системы, или кирпичиками, из которых
                складывается здание философского учения, но — и это гораздо важнее —
                они образуют само <em>пространство</em> философствования. Посредством
                установленных <em>правил вывода</em> идеи генерируют идеи в этом пространстве,
                которое становится пространством классификаций и иерархических схем этих
                идей. <em>Философский текст</em> мыслится как текст определенной идеи, как ее
                конкретный материальный <em>locus</em>, как инструмент ее чувственной
                манифестации и коммуникации. Замечательно, что сам феномен «изма» в
                нашей историко—философской традиции (так же как и в бытовом
                словоупотреблении) предполагает именно определенность и, так сказать,
                единичность идеи, при неопределенности и множественности текстов. Текст
                здесь вторичен и производен по определению. Более того, он принципиально
                релятивен в отношении идеи. Я думаю, что эта ситуация - заметьте,
                сейчас я говорю только об определенной и узко историко-философской точке
                зрения — имела и одно весьма серьезное философское следствие, а именно
                что идея о чем-то (или чего-то) предполагает, что это что-то само не
                есть идея. (С другой стороны, эта ситуация привела к таким наивным
                семиотическим метафорам, как «текст идеи», «текст мышления», «текст
                сознания» и т. д.). Таково положение вещей в отношении идей и текстов в
                европейской историко-философской традиции Просвещения.</p>

            <p>В том, что я условно называю буддийской философией, единицей
                философствования — при всех оговорках насчет применения к ней этого
                термина — является, конечно, не идея, а <em>текст</em>. Текст, как он
                существовал в <em>устной</em> традиции, закончившейся в письменной фиксации в
                Палийском каноне и других канонах. Текст, как он запоминался, сохранялся
                в памяти, вспоминался, воспроизводился и воспринимался, передавался от
                учителей к ученикам, от поколения к поколению, от места к месту, позднее
                - из одного языка в другой. Текст, включающий в себя и способы своего
                запоминания и транслирования, а позднее и письменного фиксирования.
                Текст, который даже и в позднейших своих письменных формах постоянно
                несет и неизменно воспроизводит «прото-формы» своего неписьменного
                существования.</p>

            <p>Текстом как единицей буддийской философии может быть любой реальный —
                то есть формально фигурирующий как отдельный, отделимый или
                выделенный, — текст буддийской традиции или одной из буддийских
                традиций. Такой текстовой единицей может быть отдельная сутра (сyтpa —
                название начального жанра устных буддийских текстов), «Корзина Сутр»
                Палийского канона, отдельная фраза или гатха (стих) этой сутры, вся
                «Корзина Сутр», весь Палийский канон, отдельная <em>матрика</em> (предельно
                сконденсированный элемент содержания) из «Корзины Абхидхармы», отдельное
                слово или даже звук. Но таким текстом-единицей никак не может явиться ни
                буддизм, ни какая бы то ни было идея, которую мы, следуя нашей традиции
                описания (и восприятия) любой философии как идеи (или идей), могли бы
                отождествить <em>с</em> буддийской философией.</p>

            <p>Вообще, то, что мы называем «буддизм», это не что иное, как
                <em>пространство текстов</em>, пространство, где нет точки, где бы не было
                текстов, где «межтекстовые промежутки», так сказать, принципиально
                невозможны. В каждый данный момент каждый текст в этом пространстве
                дискретен и конечен, но только в отношении других текстов, а не в
                отношении того, что текстом не является, например идеи. В этом
                пространстве тексты генерируют тексты как путем деления или, наоборот,
                укрупнения, так и путем последовательного или параллельного
                комментирования. Идея здесь всегда будет чем-то вторичным, производным
                по отношению к данному тексту (или элементом текста, который в момент
                фиксирования идеи как не-текста — тоже текст). Релятивность идеи в
                отношении текста подчеркивается еще и неопределенностью ее места в
                религиозной аксиологии буддизма. Тому, что в нашей философской традиции
                называется «идея», примерно соответствуют три буддийских термина:
                <em>drsti</em> («взгляд», «точка зрения»), <em>samjna</em> («идея», «восприятие») и
                <em>vikalpa</em> («умственная конструкция»). Первое понятие предполагает, что
                идея может быть истинной <em>(samma)</em> или неистинной <em>(asamma)</em>. Второе
                почти всегда отрицательно, то есть идея здесь будет идеей о чем-то
                неистинном или несуществующем. Третье — негативно по определению как
                противопоставленное высшему знанию <em>(prajna)</em> или высшей истине
                <em>(paramartha satya)</em>.</p>

            <p>Но главное отличие буддийского понимания идеи от нашего, также,
                безусловно, связанное с <em>абсолютностью текста</em>, я вижу в том, что идея
                в этом понимании всегда гораздо более относилась к <em>интерпретации</em>
                сказанного, чем к самому сказанному, становилась термином интерпретации
                текста, переставая, таким образом, быть элементом этого текста, а иногда
                при этом и вытесняясь за пределы пространства буддийских текстов вообще.
                Именно в этом я вижу основную причину непонимания буддийской философии
                многими современными исследователями буддизма, особенно когда они
                стараются объяснить или (хуже того!) разрешить «противоречия идей в
                буддизме», не осознавая, что это не более чем противоречие их
                собственного понимания философского текста как «текста идей» буддийскому
                пониманию текста.</p>

            <p>В применении к буддийским текстам такие наши понятия, как «контекст»,
                «подтекст», «внутренняя реконструкция», также меняют свой смысл.
                Контекст здесь не может быть не-текстовым, он всегда — другие тексты,
                принадлежащие тому же текстовому пространству. Строго говоря, если брать
                любую текстовую единицу буддийской философии, то в ней невозможно
                установить никакой глубины, кроме глубины, состоящей из других текстов.
                Любая, с нашей точки зрения «внетекстовая», действительность здесь
                всегда уже будет текстом. Да, впрочем, она никогда и не была ничем иным,
                с буддийской точки зрения.</p>

            <p>К интереснейшим философским последствиям этой первой и главной «черты
                чуждости» буддийской философии («текст», а не «идея» как единица
                философствования) я буду возвращаться во всех моих лекциях, а сейчас
                попытаюсь показать, как она реализуется в употреблении самого основного
                слова в этой философии. (При всей условности применения «философии» как
                термина нашей историко-философской традиции, я буду его применять,
                исходя из предположения, что мы знаем (!), что такое философия, но еще
                не знаем, что такое буддийская философия!) Это слово — ДХАРМА. Для
                начала я его ввожу именно как «слово», ибо как идея, концепция
                («центральная концепция буддизма» по Ф.И. Щербатскому) <em>дхарма</em> будет
                мыслиться только post factum (где factum — факт восприятия текста, a
                post — указание на уже свершившееся «обратное» переосмысление этого
                слова как идеи или концепции).</p>

            <p>Так что же такое дхарма? Или, скажем, так: что же такое то, что
                обозначено словом «дхарма» (хотя я знаю, что это разные вопросы)? О
                дхарме говорится в девяти приведенных ниже текстах, относящихся к
                различным эпохам, школам и разновидностям буддизма (в круглых скобках
                дается сквозная нумерация текстов, римскими цифрами пронумерованы
                лекции, а арабскими источники).</p>

            <p>I. 1 (1). «Изначально из разума <em>дхармы</em>, разум в них главное, разумом
                они проникнуты».</p>

            <p>(2). «Здесь ненависть ненавистью не успокаивается, но только
                не-ненавистью достигается успокоение. Такова древняя <em>дхарма</em>».</p>

            <p>(3). «Не следуй низкой <em>дхарме</em>, не будь небрежен, не придерживайся
                ложных взглядов, не ищи поддержки в этом мире»<a href="#citation-1" id="ref-1" class="citation-mark"><sup>1</sup></a>.</p>

            <p>2 (4). <em>«Первый Поворот Колеса Дхармы</em> (то есть первая проповедь Будды
                после достижения им Полного и Совершенного Пробуждения). Есть две
                крайности, о аскеты, которых должно избегать тем, кто отринул мир. Первая
                — в погружении в страсти и чувственные удовольствия, что низко,
                вульгарно и не ведет к добру. Вторая — жестокий аскетизм и
                самоистощение, что низко, вульгарно и не ведет к добру. Следуйте, о
                аскеты, Благородному Восьмеричному Срединному Пути»<a href="#citation-2" id="ref-2" class="citation-mark"><sup>2</sup></a>.</p>

            <p>3 (5). «Что думаете вы, аскеты, это <em>(дхарма)</em> постоянно или
                непостоянно? — Непостоянно, о Господи. — Но если это непостоянно, то
                страдание оно или удовольствие? — Страдание, о Господи. — Но если
                это непостоянно и страдание, то может ли оно быть "Я", быть "мной" или
                "моим"? — Не может, о Господи»<a href="#citation-3" id="ref-3" class="citation-mark"><sup>3</sup></a>.</p>

            <p>4 (6). «О Ананда, те аскеты, что живут и сейчас как острова, где остров
                — дхарма, где единственное прибежище — <em>дхарма</em>, и так же будут жить
                после меня, те будут благороднейшими из аскетов»<a href="#citation-4" id="ref-4" class="citation-mark"><sup>4</sup></a>.</p>

            <p>5 (7). «Все <em>дхармы</em> непостоянны, а оттого и не есть "Я". Все <em>дхармы</em>
                непостоянны, а оттого и страдание»<a href="#citation-5" id="ref-5" class="citation-mark"><sup>5</sup></a>.</p>

            <p>6 (8). «О Субхути, Бодхисаттва не думает ни о <em>дхарме</em>, ни о
                <em>не-дхарме</em>, ни о той, ни о другой нет у него идеи <em>(samjna)</em>. <em>Оттого</em>
                учит <em>Татхагата</em> («Так Ушедший»), говоря со скрытым смыслом, те, кто
                знают (это) изложение <em>дхармы</em>, (знают), что она как плот, что бросают
                по достижении другого берега, и они покинут все <em>дхармы</em> и, уж конечно,
                <em>не-дхармы</em>».</p>

            <p>(9). «И если кто-нибудь возьмет только четыре «строки из этого изложения
                дхармы, объяснит их, покажет, разъяснит подробно другим, то его Благие
                Заслуги будут неисчислимы, неизмеримы и немыслимы»<a href="#citation-6" id="ref-6" class="citation-mark"><sup>6</sup></a>.</p>

            <p>Итак, отвечаю на вопрос «что есть дхарма?» буквально по двум последним
                (восьмому и девятому) текстам. Здесь говорится (Буддой), что <em>Дхарма</em>
                (Учение) Будды <em>(buddhadharma)</em> <strong>должна</strong> быть оставлена Пробужденным
                существом, как плот по достижении другого берега (то есть Пробуждения),
                как должны быть оставлены все (другие) дхармы, тем более не-дхармы. Мы
                могли бы, в конце концов, воспринять сказанное Буддой как инструкцию по
                обращению со словом «дхарма» ну, в порядке, скажем, медитативного
                упражнения. Но это не так или, по крайней мере, далеко не только так.
                Ведь именно в девятом тексте утверждается, что при всем отрицании того,
                о чем там говорится, само изложение <em>(раrуaуа)</em> этой дхармы, то есть
                текста, и есть то, что остается как высочайшая и единственная заслуга
                <em>(punya)</em>. Иначе говоря, дхарма здесь <em>аннулирует</em> себя как идею
                (содержание), как то, о <em>чем</em> идет речь, абсолютно утверждая себя как
                само говорение об этом, то есть текст. Значит, можно ответить так: слово
                «дхарма» означает текст о дхарме (как об учении или чем угодно). И в
                этом первом смысле дхарма как <em>идея</em> (в нашем смысле) — пуста.</p>

            <p>Вторым шагом в нашем понимании дхармы в смысле текста как единицы
                буддийской философии будет переход к тому, о <em>чем</em> она как текст, то
                есть к тому, что есть дхарма как <em>условное</em> содержание текста (это
                одновременно и второй ответ на вопрос о дхарме как о слове). Условное,
                потому что здесь значение дхармы будет варьироваться по нескольким
                несводимым друг к другу параметрам. Итак, во-первых, это Учение Будды
                (<em>buddhadharma</em> — заметим, что это выражение может иметь и совсем иной
                смысл, а именно специальные качества (дхармы), присущие только буддам)
                фактически — текст, слова, им сказанные (<em>buddhavacana</em>). То есть
                любой канонический или параканонический (скажем, тантристский) текст
                будет, так или иначе, Дхармой Будды по своему содержанию. Но
                одновременно это и Учение, которое <em>всегда есть</em> и которое каждый данный
                Будда актуализирует в проповеди и изложении своего учения, поворачивая
                Колесо Дхармы, как в текстах (4) и (9). Тогда становится понятным
                сказанное в тексте (6) об аскетах, которые и после ухода Будды имеют
                своим последним прибежищем Дхарму (в дальнейшем Дхарма как учение будет
                писаться с большой буквы).</p>

            <p>Во-вторых, это любое учение (религия, философия, этика), правило,
                предписание, норма, наконец, которые могут быть мудрыми или неразумными,
                возвышенными или вульгарными, как в текстах (2) и (3).</p>

            <p>В-третьих, — но здесь мы оказываемся в специфически философской
                ситуации, почти безнадежной, однако прямо противоположной ситуации
                «философского тупика». Если раньше, когда говорилось о дхарме как
                тексте, она могла быть тем или другим, или не тем и не другим, или,
                наконец, ничем, то теперь речь идет не о ней, а практически о <em>чём
                    угодно</em>, в отношении к чему она будет <em>универсальным предикатом</em>. И
                единицей философии здесь будет текст (дхарма), где говорится о том или
                ином <em>объекте</em> как о дхарме или дхармах. И говорение об одном <em>объекте
                    как</em> о дхарме (а не о дхарме как о данном объекте) будет одним текстом,
                а о другом — другим. И таким объектом, разумеется, может быть все, что
                угодно, дхарма тоже, но уже в ином своем качестве, так сказать в
                качестве, которое никак ее не определяет, то есть не отделяет от (или не
                выделяет из) других объектов, ибо последние, <em>как дхармы</em>, по
                определению будут обладать тем же качеством. Это — умственность,
                ментальность дхарм, о которой говорится в тексте (1). Этот текст не
                комментируется (знаменитый комментарий, приписываемый Буддхагхоше,
                иллюстрирует, но никак не объясняет первую строку из Дхаммапады).
                Возможны, однако, «перевернутые» парафразы этой строки, такие, например
                как «(всё), что мыслит, мыслится и мыслимо, — это и есть (то, что
                называется) дхарма», которые нередки в позднейшем буддийском
                комментировании. Но это «есть» в нашей парафразе не имеет в виду
                никакого <em>бытия</em>. Текст (1) не постулирует существования дхарм, он сам
                — дхарма, так же как его восприятие и мыслимое содержание. Поэтому
                говорить о дхармах как «конечных сущностях», или «кирпичиках бытия» (по
                Ф.И. Щербатскому), неправильно философски, хотя бы потому, что нет в
                буддийской философии такой <em>дхармы</em>, как «сущность» или «бытие». Но
                можно говорить о <em>феноменах</em> (в терминологическом смысле этого слова)
                <em>как</em> дхармах, которые можно себе представить как конечные элементы
                любого мыслимого содержания. Таким образом, дхармы постоянно «выполняют» двойную работу: с одной стороны, они индуцируют содержание
                текста, а с другой — являются факторами порождения текста (последнее
                выражение принадлежит Линнарту Мяллю из Тарту, которому я за него очень
                благодарен).</p>

            <p>Тексты (5) и (7) являются примерами такого порождения. В первом из них
                говорится о непостоянстве <em>всех</em> дхарм, которое не может быть ничем
                иным, нежели страданием, откуда делается вывод о невозможности «Я» (или
                об отсутствии такой дхармы, как «Я»). Во втором как страдание, так и «Я»
                выводятся из непостоянства. Мы можем, опять же, считать текст (7)
                обратной парафразой текста (5), но это едва ли существенно, поскольку
                оба эти текста всегда находятся в контекстах других текстов, где
                говорится о страдании (как в Четырех Истинах о Страдании из Первой
                Проповеди), о непостоянстве, о «не-Я» как трех основных признаках
                феноменального мира и т. д.</p>

            <p>В заключение сказанного о тексте как единице буддийской философии
                («текст вместо идеи») и о дхарме как специфически буддийском способе
                генерации текстов и содержаний я позволю себе добавить два соображения
                общефилософского характера. Первое, подчеркивая, что дхарма не является
                идеей в нашем понимании этого слова, мы понимаем, что всякая идея есть
                дхарма (то есть одна из дхарм ранних списков дхарм, позднее
                превратившихся в текст Абхидхармы). Второе, приведенные нами тексты
                звучат вполне постулятивно, но они никак не постулаты в нашем понимании.
                Ибо последнее предполагает (со всеми возможными оговорками) реальность
                того, <em>о чём</em> этот постулат. (Так, постулаты общей теории
                относительности исходят из абсолютной реальности гравитации, а постулат
                Дэвида Юма о врожденной религиозности человека, так называемая
                «натуральная религия», исходит из реальности идей «Я» и «Бога» в
                человеке и т. д). Но поскольку в дхармических текстах «реальность»
                отсутствует (мы можем сказать, «мыслимость чего-то как» или «что-то, как
                оно есть, когда», но никак не «есть само по себе»), то речь об этих
                «постулатах» может идти скорее как о своего рода <em>позициях</em>, с точки
                зрения которых возможны различные высказывания об одних и тех же
                объектах (так называемые «дхармические позиции»). Я думаю, что при всей
                невыраженности методологии в буддийском философствовании (скажем, по
                крайней мере до II-III вв. н. э.) именно эта дхармическая позиция
                выполняла методологические функции. Хотя и это — с весьма серьезными
                оговорками.</p>

            <p>Теперь я перехожу ко второй черте чуждости буддийской философии нашему
                привычному «просвещенческому» историко-философскому подходу. Это о
                личности "исторического" основателя буддизма Будды Шакьямуни («Учителя
                из Шакьев»), жившего и учившего своих учеников аскетов и мирских
                приверженцев в долине Ганга, по всей вероятности, в V веке до н. э.
                Называя его личностью, я не оговорился. Я не забыл, что, как уже было им
                самим сказано, нет такой дхармы, как «Я». Личность в буддизме — никак
                не «Я», на что, кроме всего прочего, указывает и особое слово, её
                обозначающее, — <em>pudgala</em> (на пали <em>puggala</em>).</p>

            <p>В учении Будды то, что называется «личностью» <em>(реrsona, prosopon)</em>,
                фигурирует в текстах только в отношении к тексту учения. Или, опять же
                «обратно перефразируя» это высказывание, личность — это либо тот, кто
                этот (или любой другой, в пространстве буддийских текстов) текст
                <em>сказал</em>, либо тот, кому он его сказал, либо тот, кто его скажет или
                может сказать (запомнить, передать, объяснить, позднее — записать), то
                есть личность - это всегда что-то <em>вторично соотнесенное с текстом</em>,
                эпифеномен текста, но никак не его автор в абсолютном значении этого
                слова... Ибо этот текст (Дхарма) существует, «есть»: Будда-человек
                релятивен Учению.</p>

            <p>Чтобы понять это, нам будет необходимо обратиться к нескольким терминам,
                философский смысл которых раскрывается именно в отношении к «личности»
                Будды. Будда-человек обладает человеческим телом (<em>kaya</em>), но с
                «добавкой» тридцати двух признаков (<em>laksana</em>) «сверхчеловека»
                (<em>mahapurusa</em>). Он также обладает психикой, разумом (<em>manas</em>), но опять
                же с «добавкой» абсолютной и полной Пробужденности (<em>samyaksambodhi</em>) и
                десяти особых сверхъестественных способностей (<em>abhijna</em>). Это нередкий
                в буддизме пример двойственности феноменов, где приписываемое объекту
                <em>другое</em> релятивизирует объект. Так, <em>тело</em> Будды-человека
                (<em>nirmana-kaya</em>), что буквально означает «сконструированное (или даже
                "искусственное") тело», само может быть либо <em>подлинным (samma)</em>, либо,
                опять же, <em>искусственным</em> или <em>специально сделанным (nimmitta)</em>. Великий комментатор Буддхагхоша объясняет это (в комментарии на первую
                книгу Абхидхармы) необходимостью для Будды одновременно проповедовать
                Дхарму в нескольких местах («и только знающие Дхарму знают, подлинный
                или искусственный Будда тот, которого они сейчас видят и слышат»). Таким
                образом, уже не только тело Будды, его нирманакайя, релятивно Учению
                (Дхарме), но и его личность, «личность благородного» (<em>arya pudgalа</em>),
                существует только как воспроизводящая Дхарму, которая и является
                объективностью личности, так сказать.</p>

            <p>Я думаю, что именно эта дхармическая объективность личности Будды столь
                мешает не только нашему современному, но и любому другому, не
                буддийскому пониманию буддизма. По этому поводу Дэвид Снелгров весьма
                остроумно заметил (в книге «Образ Будды»), что центральноазиатские
                кочевники, слушая первых буддийских миссионеров, с недоумением
                спрашивали, <em>что</em> такое Будда, потому, что его живописный (и
                скульптурный) образ был образом Дхармы. Я бы уточнил: образом личности,
                производной от Дхармы.</p>

            <p>Личность в европейской историко-философской традиции Просвещения —
                абсолютна. Абсолютна не только в своих эллинистических и христианских
                теологических истоках, но и в позднейших и современных философских
                переосмыслениях. Она как <em>идея</em> может по-разному осмысляться, но
                остается при этом константой философского (и научного) мышления,
                независимой от позиции осмысливающего (ведь и осмысливающий как личность
                — абсолютен). В буддийской философии как личность, так и идея —
                позиционны. А что — непозиционно? Я думаю, что здесь этот вопрос
                неправомерен. Речь может идти либо о разных позициях, либо о позициях
                разных уровней. Дхармическая (текстовая) позиция необыкновенно важна
                философски, но и она не единственная и не конечная. Ведь всякая
                конечность тоже крайность, которую следует избегать на Пути, с чего и
                начинается Первая Проповедь Будды.</p>

            <p>В последующих лекциях я попытаюсь объяснить различные моменты и темы
                (тексты!) буддийского философствования опять же, прежде всего, как
                позиции. Это трудно, но возможно.</p>

