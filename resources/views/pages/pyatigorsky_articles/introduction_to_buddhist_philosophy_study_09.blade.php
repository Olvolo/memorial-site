                    здесь имеются в виду факторы, обуславливающие возникновение и прекращение
                    всякого тела, такие, скажем, как неведение, алчба, действие и питание. Более
                    конкретно факторами прекращения будут являться, во-первых, остановка действия
                    всех факторов возникновения и, во-вторых, мгновенное распадение всех
                    материальных структур тела.<a href="#ref-9"
                                                  class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-10"><sup>10</sup> В целом то, что здесь созерцается как
                    тело, относится к форме или первому агрегату индивидуального существования.<a
                        href="#ref-10" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-11"><sup>11</sup> «Чувство», или «эмоция», — это
                    переживание контакта с объектом как приятного, неприятного или ни приятного, ни
                    неприятного. Феноменологически чувство здесь обратно редуцируется к этим трем
                    свойствам, как основным, имманентным свойствам самих объектов.<a href="#ref-11"
                                                                                     class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-12"><sup>12</sup> Важно иметь в виду, что «ум» и
                    «объекты ума» являются двумя различными объектами созерцания. Ибо «ум»
                    рассматривается и в его способности производить изменения в себе самом. Так,
                    например, он может сам себя созерцать. «Объекты ума» (за исключением «ума» как
                    «объекта ума») являются пассивными объектами созерцания. Опять же — ум мыслит,
                    объекты ума мыслятся и мыслимы.<a href="#ref-12"
                                                      class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-13"><sup>13</sup> «Освобожденный ум» — технический
                    термин буддийской йоги, означающий ум, временно освобожденный созерцанием от
                    скверн, замутнений и «притоков».<a href="#ref-13"
                                                       class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-14"><sup>14</sup> «Пять препятствий» (пали <em>pañcanīvaraṇa</em>)
                    являются по преимуществу основными факторами, мешающими обратному вспоминанию,
                    созерцанию и сосредоточению сознания, самадхи. «Внутренними» называются
                    препятствия, причиняемые самим умом, а не органами чувств и их объектами. Так,
                    например, неправильное направление ума, неспособность к сосредоточению и
                    неуравновешенность являются такого рода препятствиями.<a href="#ref-14"
                                                                             class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="mb-3" id="citation-15"><sup>15</sup> Пять скандх по определению
                    обусловлены привязанностью и оттого называются «скандхи привязанности».<a
                        href="#ref-15" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-16"><sup>16</sup> «Внутренние основы» — это шесть
                    чувственных восприятий, «внешние» — это объекты органов чувств.<a
                        href="#ref-16" class="text-blue-500 hover:underline">↩</a></p>

                <p class="mb-3" id="citation-17"><sup>17</sup> «Оковы» здесь — метафора
                    зависимости от чувственных раздражителей и от рефлексов на раздражения.<a
                        href="#ref-17" class="text-blue-500 hover:underline">↩</a></p>

                <p class="my-2" id="citation-18"><sup>18</sup> «Член» (пали и санскр. <em>anga</em>) — это часть или элемент
                    целого, возникающий либо одновременно с целым, либо после него.
                    <a href="#ref-18" class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="my-2" id="citation-19"><sup>19</sup> «Исследование состояний» (пали <em>dhammavicaya</em>) — это
                    детальное исследование всех дхарм в обратном вспоминании созерцателя.
                    <a href="#ref-19" class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="my-2" id="citation-20"><sup>20</sup> «Энергия» (пали <em>viriya</em>, санскр. <em>virya</em>) означает
                    интенсивность и эффективность действия или усилия.
                    <a href="#ref-20" class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="my-2" id="citation-21"><sup>21</sup> «Созерцание» (пали <em>jhana</em>, санскр. <em>dhyana</em>)
                    употребляется здесь как в общем смысле, означая буддийскую йогу в целом, так и в
                    смысле особых состояний сознания, производимых созерцанием.
                    <a href="#ref-21" class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="my-2" id="citation-22"><sup>22</sup> «Конечное знание», точнее — «признание» (пали <em>añña</em>,
                    санскр. <em>ajñā</em>) — знание архатом своего архатства.
                    <a href="#ref-22" class="text-blue-500 hover:underline">↩</a>
                </p>

                <p class="my-2" id="citation-23"><sup>23</sup> «Невозвращение» (пали и санскр. <em>anāgāmita</em>) — состояние
                    йога, достигшего Пробуждения, которому предстоит рождение в Высшем мире, где он
                    достигнет буддства, уже больше не проходя через человеческую жизнь.
                    <a href="#ref-23" class="text-blue-500 hover:underline">↩</a>
                </p>
