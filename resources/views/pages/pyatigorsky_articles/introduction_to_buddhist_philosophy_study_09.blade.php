            <h2 class="text-2xl font-bold text-center text-zinc-700 mb-8">Семинар девятый</h2>
            <h3 class="text-xl text-center font-semibold text-zinc-700 mb-4">Текст IX. Установление
                обратного вспоминания<a href="#citation-1" id="ref-1"
                                        class="citation-mark"><sup>1</sup></a></h3>

            <blockquote class="my-6 pl-4 border-l-4 border-gray-300">
                <p><em>0.</em> Так я слышал. В то время Господь жил в стране Куру, в городе
                    Каммасадхамма. Там он раз обратился к монахам с такими словами: «О монахи, это
                    — прямой путь, ведущий к полному очищению живых существ от всех скверн, к
                    преодолению тоски и грусти, боли и стенаний, путь к прекращению страдания и
                    достижению Нирваны. Это — путь установления четырех оснований обратного
                    вспоминания. Что за четыре? Вот они, о монахи. Первое, это созерцание<a
                        href="#citation-2" id="ref-2" class="citation-mark"><sup>2</sup></a> тела
                    как тела; исполненный энергии, четко осознающий все, что происходит внутри и
                    снаружи, погруженный в обратное вспоминание монах, ни о чем не пекущийся, ничем
                    не озабоченный, ничего не ждущий от этого мира, созерцает свое тело как тело...
                    Потом он переходит к созерцанию своих чувств как чувств, ума как ума, объектов
                    мысли как объектов мысли<a href="#citation-3" id="ref-3"
                                               class="citation-mark"><sup>3</sup></a> и т.д.</p>

                <p><em>1.</em> Но как, о монахи, пребывает монах в созерцании своего тела как тела?
                    В лес идет он, заходит в пустую хижину или устраивается у подножия большого
                    дерева. Там он садится, скрестив ноги и выпрямив туловище, и устанавливает перед
                    собой обратное вспоминание<a href="#citation-4" id="ref-4"
                                                 class="citation-mark"><sup>4</sup></a>. Все время
                    его устанавливая, он делает вдох и выдох. Делая долгий вдох, он осознает<a
                        href="#citation-5" id="ref-5" class="citation-mark"><sup>5</sup></a>: «Я
                    делаю долгий вдох», а делая долгий выдох, осознает: «Я делаю долгий выдох». Так
                    он упражняет себя, осознавая: «Я буду делать вдох и выдох, испытывая<a
                        href="#citation-6" id="ref-6" class="citation-mark"><sup>6</sup></a> целиком
                    свое тело как дыхание». Подобно умелому гончару, вращающему свой круг, он
                    осознает: «Вот короткий вдох, вот долгий выдох»... и так упражняет себя,
                    осознавая: «Я делаю короткий вдох, я делаю долгий выдох... этим я успокою
                    телесные самскары<a href="#citation-7" id="ref-7"
                                        class="citation-mark"><sup>7</sup></a>».</p>

                <p>Таким образом пребывает он, созерцая тело как тело, созерцая его изнутри<a
                        href="#citation-8" id="ref-8" class="citation-mark"><sup>8</sup></a>,
                    снаружи, изнутри и снаружи вместе, созерцая его в возникновении, в прекращении и
                    в его возникновении и прекращении<a href="#citation-9" id="ref-9"
                                                        class="citation-mark"><sup>9</sup></a>
                    вместе. Либо же, путем обратного вспоминания, что «вот — тело», он просто
                    утверждается в осознании тела, которое необходимо для знания того, что есть и
                    как оно есть.</p>

                <p>Так он пребывает ни от чего не зависимый, ни к чему не привязанный в созерцании
                    тела как тела.</p>

                <p><em>2.</em> Теперь об осознании, о монахи. Когда монах ходит, он осознает: «Я
                    хожу», когда стоит, он осознает: «Я стою»; когда сидит, лежит и т.д., то всякий
                    раз точно осознает положение своего тела... Ибо монах — это тот, кто полностью
                    осознает все, что он делает: как он смотрит и слушает, как ходит и стоит, как
                    расправляет свои члены, как носит свое одеяние, как держит чашу для сбора
                    подаяний, как ест и пьет, как мочится и испражняется, как засыпает, спит и
                    просыпается, как говорит и молчит.</p>

                <p>Вновь и вновь наблюдает монах свое тело, от подошв до макушки и с кончиков волос
                    на теле дальше внутрь этого мешка, набитого нечистотами. Он наблюдает,
                    осознавая: «Тело — это волосы, кожа, мускулы, зубы, кости, костный мозг,
                    почки, сердце, печень, селезенка, кишки... желчь, слизь, пот, слюна, слезы,
                    моча...» Также наблюдает он свое тело состоящим из четырех элементов: земли,
                    воды, огня и воздуха<a href="#citation-10" id="ref-10"
                                           class="citation-mark"><sup>10</sup></a>». А если случится
                    монаху увидеть мертвое тело, выброшенное на площадку для сожжения трупов, тело,
                    уже вспухшее, испускающее зловоние, то монах, сравнивая этот разлагающийся труп
                    со своим телом, осознает: «Мое тело такое же, оно — этот труп».</p>

                <p>Вот как монах созерцает свое тело как тело.</p>

                <p><em>3.</em> Теперь, о монахи, спросим, как пребывает монах в созерцании своих
                    чувств как чувств<a href="#citation-11" id="ref-11" class="citation-mark"><sup>11</sup></a>?
                    Так вот, когда он испытывает приятное чувство, то осознает: «Это мне приятно»,
                    когда неприятное, то осознает: «А это — неприятно». Когда испытывает не
                    приятное и не неприятное чувство, то осознает: «Это — не приятно и не
                    неприятно». Таким же образом он осознает, что испытывает чувства земные или
                    неземные, т.е. связанные или не связанные с органами восприятия.</p>

                <p><em>4.</em> А как же, о монахи, созерцает он ум как ум<a href="#citation-12"
                                                                            id="ref-12"
                                                                            class="citation-mark"><sup>12</sup></a>?
                    А так. Созерцая ум, он осознает его либо как испорченный похотью, либо как не
                    испорченный похотью; либо как извращенный ненавистью и невежеством, либо как ими
                    не затронутый; либо как возвышенный, либо как не возвышенный. Так же осознает он
                    ум как сосредоточенный в созерцании или как не сосредоточенный в созерцании. И
                    наконец, монах, созерцающий ум, осознает его либо как освобожденный, либо как не
                    освобожденный<a href="#citation-13" id="ref-13"
                                    class="citation-mark"><sup>13</sup></a>.</p>

                <p><em>5.</em> Как же тогда, о монахи, созерцает монах объекты ума как объекты ума?
                    Прежде всего, он созерцает объекты ума в связи с пятью главными умственными
                    препятствиями<a href="#citation-14" id="ref-14"
                                    class="citation-mark"><sup>14</sup></a>, это: чувственные
                    желания, злая воля, лень, пассивность, беспокойство, угрызения совести и
                    сомнения. Он точно осознает, которое из этих препятствий есть в нем сейчас, в
                    момент осознания, которое из них в нем отсутствует, которое только еще возникает
                    и которое уже прекратилось. И он точно осознает, которое из них им покидается,
                    отбрасывается, чтобы в будущем не было более его возникновения и т.д. Так он
                    созерцает объекты ума в связи с пятью препятствиями. Но еще созерцает он объекты
                    ума в связи с пятью агрегатами индивидуального существования, скандхами,
                    привязанными к становлению и существованию<a href="#citation-15" id="ref-15"
                                                                 class="citation-mark"><sup>15</sup></a>.
                    Он осознает так: «Вот форма, таково ее возникновение, таково ее исчезновение;
                    вот восприятие, вот самскары, вот сознание в его возникновении и прекращении...
                    и т.д.». Так монах созерцает пять агрегатов, скандх, в их связи с объектами ума.
                </p>

                <p><em>6.</em> Наконец, о монахи, вот как монах созерцает объекты ума в их связи с
                    внутренними и внешними основами<a href="#citation-16" id="ref-16"
                                                      class="citation-mark"><sup>16</sup></a>
                    чувственного восприятия. Он осознает формы и осознает оковы<a
                        href="#citation-17" id="ref-17" class="citation-mark"><sup>17</sup></a>,
                    возникающие из форм (так же как из бесформенного), те оковы, что возникли, те,
                    что еще возникнут, и те, что отброшены, разорваны, чтобы более никогда не
                    возникнуть. Еще, о монахи, он созерцает объекты ума в связи с семью членами
                    Пробуждения<a href="#citation-18" id="ref-18"
                                  class="citation-mark"><sup>18</sup></a>. Так он осознает их,
                    начиная с обратного вспоминания: «Есть во мне обратное вспоминание, нет во мне
                    обратного вспоминания». Таким же образом он осознает другие шесть членов
                    Пробуждения — исследование дхарм<a href="#citation-19" id="ref-19"
                                                       class="citation-mark"><sup>19</sup></a>,
                    энергию<a href="#citation-20" id="ref-20"
                              class="citation-mark"><sup>20</sup></a>, созерцание<a
                        href="#citation-21" id="ref-21" class="citation-mark"><sup>21</sup></a>,
                    дхьяну, сосредоточение сознания, самадхи, полную равность ко всему и
                    успокоенность. Тогда, о монахи, он будет созерцать объекты ума в связи с
                    Четырьмя Благородными Истинами. Каждую из них он будет вновь и вновь (т.е. в
                    связи с каждым другим объектом ума) осознавать как она есть: «Это страдание, это
                    — возникновение страдания, это — прекращение страдания и т.д.».</p>

                <p>О монахи, коль разовьет в себе монах эти основы обратного вспоминания и будет ему
                    предаваться в течение семи лет, то плодом этого ему будет либо конечное
                    признание<a href="#citation-22" id="ref-22" class="citation-mark"><sup>22</sup></a>
                    его архатства, либо — если еще остались в нем следы привязанностей —
                    невозвращение<a href="#citation-23" id="ref-23"
                                    class="citation-mark"><sup>23</sup></a>.</p>

                <p>Так сказал Господь. И возрадовались монахи его словам.</p>
            </blockquote>

            <h3 class="text-xl text-center font-semibold text-zinc-700 my-4">ОБРАТНОЕ ВСПОМИНАНИЕ И "КОГДА" БУДДИЙСКОЙ ЙОГИ.<br>ВРЕМЯ СОЗЕРЦАНИЯ</h3>

            <p><em>0.</em> Принцип установления обратного вспоминания очень прост: Созерцатель
                созерцает объект <strong>A</strong> и, созерцая этот объект, осознает себя самого как <strong>A</strong>, так
                устанавливается обратное вспоминание. Но что такое обратное вспоминание в этом
                тексте, обозначенное словом <em>смрити</em>? Вопрос ставит нас лицом к лицу с центральной
                дилеммой понимания буддийской философской (и йогической) терминологии: или мы будем
                исходить из того, что в самих истоках (буддийских или не-буддийских) так называемого
                «раннего исторического буддизма» содержались какие-то еще не получившие наименования
                идеи, ждавшие, пока ранние буддийские учителя их назовут и определят, или мы примем,
                что в тех же истоках уже имелись слова, которые позднее были использованы
                буддийскими учителями для обозначения специфически буддийских идей, до возникновения
                буддизма не существовавших. Я в этих семинарах в общем придерживаюсь второй
                гипотезы. Но эти наименования не только обозначали специфически буддийские идеи и
                вещи, но сами становились этими идеями и вещами, приобретая тем самым особый
                <span class="underline">символический</span> смысл. Смысл, не обязательно вытекающий из прежнего или буквального
                значения этих слов. Заметим в этой связи, что <em>смрити</em> (на пали — сати)
                является одним из самых трудно переводимых буддийских терминов. Хотя его
                санскритская этимология вызывает сильные ассоциации с такими значениями, как
                «память» и «вспоминание», его концептуальное содержание гораздо глубже и богаче всей
                суммы значений, в которых <em>смрити</em> употреблялось в текстах обеих колесниц.</p>

            <p><em>1.</em> Исходя из истолкования понятия <em>смрити</em> великим буддийским комментатором
                Буддхагхошей, я бы предложил следующее операциональное определение: <span class="underline"><em>смрити</em> — это
                такое вспоминание, во время которого вспоминающий присутствует в каждом
                (вспомненном) объекте своей прошлой мысли</span>. Но это определение опять (уже в который
                раз) сталкивает нас с проблемой времени. Но <span class="underline">какого именно</span> времени, точнее, времени
                <span class="underline">чего</span>? Отвечаю, опять же следуя за Буддхагхошей. «Во время» здесь — это не время
                вообще, а настоящее время обратного вспоминания. Такое время сводится к <span class="underline">данному мгновению</span> обратного вспоминания и условно может рассматриваться как <span class="underline">время созерцания</span>
                вообще. Дальше, «вспоминающий» в нашем определении — это сознание вспоминающего,
                т.е. то, что <span class="underline">сознает свои объекты</span>. «Прошлая» мысль здесь является прошлой только в
                силу своего противопоставления настоящему моменту обратного вспоминания. Сейчас
                весьма заманчивой была бы гипотеза, что само событие прошлой мысли генерируется
                событием обратного вспоминания. Но опять же, <span class="underline">когда</span> генерируется? В промежутке между
                временем <span class="underline">прошлой</span> мысли и настоящим временем вспоминания. Этот промежуток в принципе
                <span class="underline">не определен</span> в терминах внешнего времени, ибо оба события могут либо совпадать во
                внешнем времени, либо одно может следовать за другим (даже первое за вторым!). И
                наконец, «вспоминающий присутствует... в каждом объекте своей прошлой мысли... »
                можно было бы понять следующим образом. Поскольку каждое событие возникновения мысли
                состоит из самой мысли вместе с ее объектом или объектами и из мысли о <span class="underline">мыслящем</span>, и
                поскольку обратное вспоминание, <em>смрити</em> замыкается по определению на вспоминающем,
                т.е. на мыслящем, то этот вспоминающий находится в каждом объекте, который он
                <span class="underline">вспоминает</span>, а тем самым и <span class="underline">порождает</span> (вместе со своей мыслью). Соответственно, время
                его «присутствия» в объекте прошлой мысли и есть йогическое время или время
                созерцания, совпадающее со временем прошлого объекта мысли. Предположим, что,
                во-первых, идея <span class="underline">внешнего времени</span> или <span class="underline">времени вообще</span>, могла возникнуть на основании
                <span class="underline">времени созерцания</span>, а последнее могло возникнуть как вывод из осознания обратного
                вспоминания.</p>

            <p>Время вообще или внешнее время (хотя, строго говоря, это совсем не одно и то же) —
                это проекция времени мышления или психического времени, которое само устанавливается
                (как и сами мышление и психика) только на основании опыта созерцания вообще, а в
                частности — опыта обратного вспоминания. Таким образом, время вообще не есть время
                <span class="underline">бытия</span> чего-либо и само по себе, буддистически говоря, <span class="underline">не есть</span>.</p>

            <p><em>2.</em> Обратное вспоминание, <em>смрити</em> — это не спонтанное, а искусственное,
                сознательно выработанное состояние сознания, в котором вся умственная активность
                йога сосредоточена в ретроспективном усилии зафиксировать мысль на объекте
                предшествующей мысли. В этом состоянии все функции и модальности ума редуцированы к
                выполнению этой задачи. Именно поэтому обратное вспоминание, в отличие от всех
                других буддийских и не-буддийских йогических практик, является прежде всего йогой
                осознавания, в которой ум и сознание превращаются в чистое <span class="underline">осознавание</span>. А объекты
                ума и сознания превращаются в чистое <span class="underline">осознаваемое</span>.</p>

            <p><em>3.</em> Теперь попытаемся, исходя из нашего рабочего определения обратного
                вспоминания, вывести хотя бы приблизительно феноменологию этого состояния сознания.
            </p>

            <p><em>(1)</em> Ни само обратное вспоминание, <em>смрити</em>, ни условия и факторы его
                возникновения, развития и культивирования не принадлежат нормально и естественно
                функционирующему человеческому уму. Факторы обратного вспоминания, такие как
                «созерцание», «особое созерцание» и, прежде всего, «осознавание», образуют <span class="underline">сложное целое</span> <em>смрити</em>, включающее в себя и мыслительные объекты обратного вспоминания. В
                данном тексте — это тело, чувства, объекты ума и ум, но в принципе это любые вещи,
                события и факты в отдельности и все они, вместе взятые.</p>

            <p><em>(2)</em> Как определенное йогически индуцированное состояние сознания, обратное
                вспоминание является <span class="underline">конечным</span> результатом порождающих его факторов и условий. Оно
                немыслимо ни как существующее до момента этого результата, ни как продолжающееся
                <span class="underline">после</span> того, как действие порождающих его факторов в этом результате завершилось. Как
                результат, оно является <span class="underline">мгновенным</span> состоянием сознания и в этом смысле может
                рассматриваться не как сложное целое, а, напротив, как своего рода элементарный,
                единичный акт, действие которого направлено на <span class="underline">настоящее</span> объектов вспоминания.
                Последние всегда оказываются как бы «уже подготовленными» созерцанием и (само)
                осознаванием — вместе с уже установленным порядком, в котором они будут «обратно
                вспоминаться». Однако самым интересным в этих объектах является то, что <span class="underline">их настоящее
                слито с настоящим временем, в котором они, так сказать, «обратно» вспоминаются,
                созерцаются и осознаются</span>.</p>
                <p>Более того, настоящее объектов обратного вспоминания не
                только синхронизировано с «единственным» мгновенным настоящим его самого, но первое
                как бы вытекает, следует из второго. Можно даже сказать, что «как они есть» эти
                объекты «есть» только тогда, когда они обратно вспоминаются. Опять же прошлые и
                будущие объекты обратного вспоминания — это только объекты <span class="underline">будущего</span> обратного
                вспоминания.</p>

            <p>Обратное вспоминание не является состоянием или феноменом человеческой психологии
                вообще. Ведь какую бы «науку психологию» мы ни взяли — прошлую, настоящую или
                будущую — мысль, ум и сознание рассматриваются в них как по преимуществу
                темпоральные, временные феномены, т.е. мыслимые только во <span class="underline">внешнем</span> времени.
                Макрокосмически, как «живое целое», наблюдаемое в данный момент, ум (или сознание)
                полагается конечным результатом человеческой (или биологической) эволюции, а
                микрокосмически — как результат индивидуального онтогенеза. Все ментальные функции
                и процессы происходят во времени и по определению <span class="underline">конечны</span>. Сознание без времени для
                любой психологии — полный абсурд.</p>

            <p><em>(3)</em> И наконец, возвращаясь к нашему замечанию об обратном вспоминании,
                <em>смрити</em> как <span class="underline">символе</span>, мы оказываемся перед одним из самых странных феноменов всей
                буддийской йоги. В словах «когда созерцающий устанавливает перед собой <em>смрити</em>» речь
                идет не об объекте обратного вспоминания, а о самом обратном вспоминании, <em>смрити</em>, и
                это предполагает, что последнее является <span class="underline">эйдетическим образом</span>, на котором
                фокусируется сознание созерцающего. Иначе говоря, <em>смрити</em> объективируется, обратное
                вспоминание становится объектом обратного вспоминания; оно таким образом становится
                универсальным способом превращения всех объектов, которые уже созерцались и
                осознавались «как они есть», в один мгновенный факт <span class="underline">абсолютного осознания</span>. Отметим
                при этом, что осознание само не является особым, специфическим элементом буддийской
                йоги, но превращается в таковой только в обратном вспоминании, <em>смрити</em>. «Обратное
                вспоминание», <em>смрити</em>, «особое созерцание» (пали <em>anupassana</em>) и
                «осознавание» (пали <em>sampajanna</em>) составляют первую триаду буддийской йоги
                (о второй триаде будет говориться в следующем семинаре).</p>

            <p><em>(4)</em> Если мы попробуем отвлечься от содержания понятия обратного вспоминания
                и представим себе <em>смрити</em> как единый комплекс мышления и поведения
                созерцателя, то этот комплекс, как в своей структуре, так и в порядке <span class="underline">процесса</span>
                своего выполнения, окажется весьма похожим на древнеиндийское жертвоприношение
                (санскр. <em>yajña</em>). Именно в <em>смрити</em> этос ведийского ритуала
                (санскр. <em>ṛta</em>) нашёл своё точное воспроизведение. <em>«Этос»</em> (англ.
                <em>ethos</em>) — это единое целое, включающее в себя как временной процесс, так и
                структуру того или иного феномена человеческого поведения; это единое целое
                представляется и описывается в виде пространственной конфигурации составляющих его
                элементов. С точки зрения этоса, обратное вспоминание может считаться центральным
                ритуалом буддийской йоги, а сама буддийская йога может рассматриваться как
                удивительный случай <span class="underline">ритуализированного сознания</span> (скорее, я полагаю, нежели как
                случай «ментального ритуала»).</p>

            <p><em>(5)</em> Эпистемологически, в своем отношении к знанию, обратное вспоминание
                четко соответствует формуле: знать <strong>A</strong> — это знать <strong>A</strong> <span class="underline">как</span> <strong>B</strong>. Именно по этой формуле
                строится текст IX. Отметим три наиболее типичных случая конкретизации этой формулы в
                нашем тексте.</p>

            <p><span class="underline">Первый случай</span>: знать <strong>A</strong> — это знать, что «<strong>A</strong> есть (или нет)». Пример из
                текста IX: «Устанавливается обратное вспоминание на основе осознания того, что
                «имеется тело». Это — как бы минимум знания, необходимый для обратного
                вспоминания.</p>

            <p><span class="underline">Второй случай</span>: знать — это знать <strong>A</strong> как <strong>A</strong>, т.е. знать <strong>А</strong> как оно есть.
                Пример из текста IX: «Так пребывает он, созерцая тело как тело».</p>

            <p><span class="underline">Третий случай</span>: знать — это знать <strong>A</strong> как <strong>B</strong>. Пример из текста IX: «Так
                созерцатель познает себя как...». Монах, ходя, осознает себя ходящим, сидя, сидящим,
                едя, едящим... и т.д.</p>

            <p>«Как» в этих примерах служит своего рода универсальным эпистемологическим
                модификатором, ибо это слово не только «превращает» объект <strong>A</strong> в объект <strong>B</strong> (или опять
                же в <strong>А</strong>, но уже по-другому познанное), но — и это самое главное — оно превращает
                знание об <strong>A</strong> в созерцание, обратное вспоминание и любой другой йогический условный
                эквивалент знания об <strong>A</strong>. Таким образом, объект знания становится объектом созерцания.
                Таким же образом, переходя от «<strong>A</strong> <span class="underline">как</span> <strong>A</strong>» к «<strong>A</strong> как оно есть», мы превращаем <strong>A</strong> из
                нормально, обычным образом постигаемого объекта в <span class="underline">другое</span> <strong>A</strong>, в объект постигаемый
                только в обратном вспоминании, <em>смрити</em>. Так знание становится производным от
                созерцания, а в данном случае — от обратного вспоминания. В тексте IX, который я
                считаю одним из самых эпистемологически-содержательных текстов Палийского Канона,
                — объекты четырех классов, «тела», «чувств», «ума» и «объектов ума» представлены
                <span class="underline">как</span> уже прошедшие через йогическую трансформацию обратного вспоминания и, таким
                образом, осознаваемые <span class="underline">как они есть</span>, т.е. полностью очищенные от всех временных
                условий и условий, в каких они были восприняты и осознаны. Иначе говоря, эти объекты
                представлены в их <span class="underline">чистой объектности</span>.</p>

            <p>Однако для полноты этой чистой объектности, или (как ее называли мастера буддийской
                йоги) «таковости» объекта созерцания, было необходимо ввести два дополнительных
                аспекта в эпистемологию созерцания. Первый аспект, который я называю
                «квазипространственным», мы обнаруживаем в том, в частности, что созерцание объекта
                может быть либо внутренним, либо внешним (либо обоими вместе). «Внутренне» означает,
                что созерцаемый объект принадлежит телу или уму (сознанию) созерцающего. В то время
                как «внешне» предполагает, что созерцается тело или ум <span class="underline">другого</span> человека или живого
                существа. Второй аспект, «квазивременной», предполагает, что объект созерцается либо
                в его возникновении, либо в его исчезновении, либо в них обоих вместе. По существу,
                это не более чем вывод из одной из основных аксиом буддийской философии, а именно,
                что <span class="underline">каждый объект в каждый момент находится либо в состоянии возникновения, либо в
                состоянии исчезновения</span>. Таким объектом является и вселенная в целом.</p>

            <p>Первый дополнительный аспект можно рассматривать как один из случаев применения
                древнего буддийского постулата о том, что ум, мысль и сознание являются <span class="underline">объектами особого рода</span>. Так, например, критерий <span class="underline">умственности</span> в применении к уму или сознанию
                созерцателя будет совсем иным, нежели в применении к уму или сознанию созерцаемого
                им другого человека (подробно об этом ниже, в XVIII). В нашем тексте эти два
                дополнительных аспекта являются, по существу, двумя возможными позициями в
                созерцании одного и того же объекта.</p>

            <p>Обратное вспоминание, созерцание и осознавание образуют единую <span class="underline">структуру йоги</span>,
                которая охватывает в своих операциях все мыслимые микрокосмические, макрокосмические
                и космические объекты, а также и объекты трансцендентальные, такие, скажем, как
                Четыре Благородные Истины и т.д. Не говоря уже о том, что, как это ясно видно в
                тексте IX, йогические операции сами являются объектами йоги. Созерцание в первую
                очередь созерцает само себя, а обратное вспоминание — «обратно вспоминает» само
                себя. Этим, между прочим, объясняется и то обстоятельство, что один и тот же
                феномен, одна и та же дхарма оказывается в самых различных местах йогического
                контекста разных текстов, а иногда даже и одного и того же текста.</p>

            <p><em>(6)</em> И наконец, перейдем к общим соображениям насчет <span class="underline">понятия</span> обратного
                вспоминания, <em>смрити</em>. Прежде всего, это понятие отражает в себе идею ранней
                буддийской йоги о том, что весь психосоматический комплекс субъективности индивида
                — т.е. все, обозначаемое как «мой ум», «мое тело», «мои чувства» и т.д., —
                является комплексом объектов <span class="underline">внешних</span> тому же комплексу созерцающего. Отсюда следует,
                что фокус всей проблемы объекта в том, что для созерцателя не существует «готовых»
                объектов самих по себе. Их сначала надо «приготовить», т.е провести через процесс
                йогической объективации. Возможно, именно в связи с этой идеей впервые в истории
                философии и возникло понятие «чистого объекта». Но только в обратном вспоминании
                объективация может быть <span class="underline">фактически</span> осуществлена. В тексте IX объективация
                описывается как трехступенчатый процесс:</p>

            <p>I. На первой ступени тело созерцающего монаха созерцается <span class="underline">как тело</span> (т.е. здесь
                неважно, <span class="underline">чье</span> это тело, оно может оказаться и ничьим) или, скажем точнее, как <span class="underline">вещь</span>,
                <span class="underline">объект</span>.</p>

            <p>II. На второй ступени тело созерцается как отвратительный, мерзкий объект, например,
                как разлагающийся труп (или скелет, кусок гнилого мяса и т.д.).</p>

            <p>III. На третьей ступени тело созерцается как нейтральный безразличный объект (скажем,
                как мешок с бобами или рисом).</p>

            <p>Можно было бы предположить, что, как результат объективации, идея чистого объекта
                оказалась противопоставленной идее объекта как объекта эмоций, который по
                определению приятен, неприятен или не приятен и не неприятен. В этой связи возможно
                также предположение, что в обратном вспоминании предлагается совершенно особый
                способ редукции, каковым объективация, собственно, и является. Тогда
                историко-философски будет вполне возможной и гипотеза о том, что чистый,
                трансцендентальный объект, «выработанный в обратном вспоминании», должен был бы
                заполнить «философский вакуум», образовавшийся в результате элиминации «Я», <em>атмана</em> в
                буддийском философствовании, элиминации, как бы расчистившей «поле» для йогических
                игр с мыслью и сознанием.</p>

            <p>Теперь последнее соображение насчет обратного вспоминания как йогического понятия. В
                обратном вспоминании самоосознание созерцателя является в конечном счете мгновенным
                рефлексом сознания на себя самое. Тогда созерцатель редуцируется к (своему)
                сознанию. Но только через мгновенный акт обратного вспоминания он как бы
                «возвращается» к сознанию (т.е. к «себе»), бывшему до этого акта. Это и делает
                обратное вспоминание «йогическим двойником» сознания, что в общем напоминает Высшее
                Знание <em>Атмана</em> в <em>Упанишадах</em> в его отношении к <em>атману</em>. Это еще более подчеркивает роль
                и значение обратного вспоминания как удивительного и в своем роде единственного
                феномена в теории и практике буддийской йоги.</p>

