                    интерпретации или переживания сознанием индивидуальных психических механизмов.
                    Или, употребляя другое эквивалентное этому выражение: сознание может
                    «захватываться» этими механизмами. Феноменологически же проявление сознания
                    можно интерпретировать как <em>восполнение</em> знания о психике. Объект и
                    субъект тогда будут существовать лишь как разные случаи интерпретации сознанием
                    этих психических механизмов.</p>

                <p><strong>Александр Пятигорский:</strong> А можем ли мы считать оппозицию «объект —
                    субъект» «структурой сознания»?</p>

                <p><strong>Мераб Мамардашвили:</strong> Безусловно. С этой точки зрения сама
                    оппозиция «объект — субъект» может быть нами разъяснена как одна из «структур
                    сознания». И в смысле утверждения о том, что объект и субъект существуют лишь
                    как интерпретации сознанием психических механизмов, может быть рассмотрена не
                    только проблема сознания, но и проблема бессознательного... И тогда
                    бессознательное будет выступать как <em>исполнение сознания</em> в другом (в
                    данном случае психофизиологическом) материале. И в связи с этим опять
                    возвращаюсь к тому, о чём уже говорили: сознание есть такой текст, который
                    возникает актом чтения этого текста, который сам себя обозначает, который
                    отсылает к самому себе. Эта самоотсылка снова становится текстом до
                    бесконечности. И отсюда — переход к «структуре сознания».</p>

                <p>«Структура сознания» — то содержательное, устойчивое расположение «места
                    сознания», которое обнаруживается в связи с «состояниями сознания», <em>с точки
                        зрения сферы сознания</em>. То есть если мы взглянем на «состояние сознания»
                    со стороны «сферы сознания», то мы в «состояниях сознания» можем увидеть,
                    вычленить, выявить «структуры сознания». К этим «структурам сознания» применимо
                    всё то, что ты выше говорил, что они могут быть, могут не быть и так далее.
                    «Структуры сознания» дискретны в пространстве и недискретны во времени, в
                    отличие от декартовой топологии пространства. «Структура сознания» есть
                    фактически внеличностное, квазипредметное состояние бытия. Я уже перехожу на тот
                    уровень анализа, который у нас до сих пор отсутствовал, а именно,
                    сопоставительного анализа «структуры сознания» с бытием. Фактически «структура
                    сознания» есть некоторое «заделывание дыр бытия», «дыр», оставляемых
                    причинностно-следственными агрегатами. В этой квазипредметно структурированной
                    «дыре» (которая другой структуры не имеет, потому что она дыра) есть целостные
                    «структуры сознания».</p>

                <p><strong>Александр Пятигорский:</strong> Это образ?</p>

                <p><strong>Мераб Мамардашвили:</strong> Не совсем. Мне здесь важно подчеркнуть
                    следующую мысль, касающуюся того, как живут и существуют «структуры сознания», —
                    мысль, касающаяся способа бытия, жизни «структуры сознания». «Структура
                    сознания» рассматривается нами как нечто такое, к чему не применимо понятие
                    возникновения, уничтожения, — «структуры сознания» не возникают и не
                    уничтожаются; её может не быть в том или другом месте, или вообще может не быть
                    той или другой «структуры сознания». Это так. Но если она есть, то мы не можем
                    уже говорить о том, что она возникла, или о том, что она уничтожилась. Мы можем
                    говорить, что <em>сознание ушло</em> из какой-то «структуры сознания», покинуло
                    эту «структуру сознания», и может быть, мы <em>это</em> сознание засечем потом в
                    какой-нибудь другой «структуре сознания», но мы ничего не говорим о судьбе
                    предшествующей ей или другой «структуры сознания», из которой ушло сознание или
                    которая была покинута сознанием.</p>

                <p>Кстати говоря, раз мы строим метатеорию сознания с учётом условий
                    дополнительности наблюдения, то мы здесь должны говорить лишь <em>о новом</em>
                    сознательном опыте, а не о рождении и уничтожении «структуры сознания».
                    Фактически говоря, мы «структуру сознания» должны рассматривать в виде некой
                    исконно-заданной и, может быть, даже ограниченной конечным по своему
                    классификационному ряду материалом, который мы «берём взаймы» и здесь
                    разрабатываем под «мы» я имею в виду технический механизм нашей работы). Мы
                    богаты чем-то взятым взаймы. Скажем, на этом взятом взаймы мы строим конструкцию
                    «Я». На этом взятом взаймы мы строим мифологию и так далее. Так вот, сознание
                    может <em>покидать</em> мифологическую систему, научную систему или даже
                    языковую систему.</p>

                <p>И, продолжая это рассуждение, можно будет говорить о следующем, чтобы пояснить
                    такой метатеоретический, методологический характер самого этого понятия
                    «структуры сознания», — например, сопоставив «структуру сознания» с
                    конструкциями типа «Я». Мне кажется, в этом типе имеются конструкции, которые,
                    пожалуй, с точки зрения метода более приближаются к идеологическим конструкциям.
                    Они, если их рассматривать в их отношении к «структуре сознания», находятся как
                    бы на одном уровне, равноправны между собой, и потому схожи со «структурами
                    сознания». Они все в отношении к «структуре сознания» являются <em>псевдоструктурами</em>,
                    являются производными, вторичными явлениями. Так, если идти от «Я» или
                    идеологических конструкций к этим «структурам сознания», то они являются
                    конечными, неразложимыми феноменами, конечными пунктами отсылки. А если идти к
                    «структурам сознания» от нашего символического аппарата, то эти структуры
                    являются квазипредметными образованиями, представляющими собой элемент нашего
                    обобщённого детерминистского объективного описания, которое даёт нам предметы и
                    содержание отсчёта.</p>

                <p>Я подчёркиваю: <em>содержание отсчёта</em>, чтобы напомнить, что само «состояние
                    сознания» как таковое вводилось нами как нечто бессодержательное, в отличие от
                    «структуры сознания». Так вот, это обобщённое объективное описание даёт нам
                    содержательные предметы, идеальные объекты мотивации, счёт вторичных процессов,
                    то есть саму работу, всегда совершающуюся во вторичных процессах, все
                    развёртывание, которое индивидуальный психический механизм совершает с
                    материалом сознания, когда этот механизм в нём находится.</p>
