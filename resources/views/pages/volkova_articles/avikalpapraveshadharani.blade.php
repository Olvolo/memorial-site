            <p>Центральной темой дхарани является термин avikalpa (тиб. rnam-par-mi-rtog-pa) —
                <em>свободный от конструирующего мышления, несозидание мыслей, неконцептность</em>.
                Четыре
                раза употреблен другой термин – nirvikalpa (тиб. rnam-par-rtog-med-pa) —
                <em>бесконцептный, свободный от мысленного процесса вообще</em>. Причем <em>нирвикальпа</em>
                употребляется в контексте, не позволяющем выяснить принципиальное отличие этого
                термина от <em>авикальпы</em>, и может показаться, что употребление термина <em>нирвикальпа</em>
                не
                имеет в дхарани самостоятельной смысловой нагрузки. К сожалению, мы не имеем
                возможности сверить санскритский текст рукописи О. Ф. Волковой с оригиналом ЛО
                ИВАНА, дабы исключить возможные ошибки. В переводе во всех четырех случаях
                употребления термина <em>нирвикальпа</em> он приведен в квадратных скобках.</p>
            <p>На значимость смыслового различия этих терминов наше внимание направляет другой факт.
                Слушающие проповедь Бхагавана образуют собрание, построенное по принципу мандалы:
                бодхисаттвы-махасаттвы<a href="#citation-6" id="ref-6"
                                         class="citation-mark"><sup>6</sup></a>, практикующие все
                парамиты одновременно, именуемые авикальповыми, составляют внутренний круг, а
                «просто» бодхисаттвы, именуемые нирвикальповыми – внешний (см. прим. № 2 к тексту).
                Отсюда необходимость определить семантическое различие между терминами
                <em>авикальпа</em> и
                <em>нирвикальпа</em>.</p>
            <p>Состояния сознания, к которым приложимы эти термины, образуют совершенно разные и,
                главное, не сообщающиеся метрики. Нирвикальпа – это погашение все усложняющихся и
                утончающихся мыслительных построений до состояния полного немыслия, поэтому –
                <em>безмыслие, бесконцептность</em>; в конечном счете, в нирвикальпе мыслить просто
                невозможно, там мыслительного процесса нет по определению. Это, действительно,
                термин, определяющий процесс последовательного <em>погашения</em> мысли вплоть до
                полного ее
                исчезновения, как нирвана архата, и признак нирвикальпы не гарантирует обретение
                мудрости.</p>
            <p>Авикальпа предполагает совершенно другую ситуацию – мыслительный процесс
                наличествует, но конструктивность его бесплодна, ибо индивид индифферентен к
                конструкциям мысли и ничего не выбирает из них, не противопоставляет их друг другу и
                из поставляемого мыслью материала сознательно ничего не конструирует; это и есть
                авикальпа – <em>неконцептность</em>, то есть состояние, в котором образы, категории
                и понятия
                мыслительного процесса не превращаются в теорию, не выстраиваются в иерархию, не
                сравниваются. Авикальпа не исключает оценочного и выборочного мышления, она означает
                уровень сознания, когда оно лишено <em>всеобщей подозрительности</em> (термин Б. Д.
                Дандарона). Признаком авикальпы, согласно дхарани, обладают бодхисаттвы-махасаттвы в
                отличие от «просто» бодхисаттв, обладающих лишь признаком нирвикальпы. Бодхисаттвы,
                обладающие признаком нирвикальпы, вполне понятны обычному, дискурсивному уму, в то
                время как бодхисаттвы-махасаттвы для профанического ума непостижимы и воспринимаются
                как Высшие существа. Неконструированием, авикальпностью, по отношению ко все
                усложняющимся категориям, согласно дхарани, обретается состояние блаженства
                неконцептности (avikalpasukham).</p>
            <p>Переход от измерения нирвикальпового состояния к авикальповому, скорее, невозможен,
                чем возможен, ведь «нирвикальпики» как бы постепенно выключаются из сферы
                проявленности и стоят в преддверии хинаянистической нирваны. Вот это «в преддверии»
                и дает последний шанс к переходу на путь авикальпы, но только скачком, ибо сфера
                авикальпы – совсем другое измерение сознания. К нему тоже приходят скачком, но из
                других состояний в результате последовательного снятия концептности (викальпы) с
                высших понятий буддизма, вплоть до таких, как Всезнание всех аспектов (реальности)
                (sarvakarajñata)<a href="#citation-7" id="ref-7" class="citation-mark"><sup>7</sup></a>.
            </p>
            <p>В публикации в части перевода О. Ф. Волковой для термина <em>авикальпа</em> сохранен
                предложенный ею следующий ряд: авикальпа (без перевода), безвикальпность (частичный
                перевод) и бесконцептность (полный перевод, использован в конце текста).</p>
            <p>К дхарани существует комментарий Камалашилы<a href="#citation-8" id="ref-8"
                                                             class="citation-mark"><sup>8</sup></a>,
                приверженца подшколы <em>мадхьямика-йогачара</em><a href="#citation-9" id="ref-9"
                                                                    class="citation-mark"><sup>9</sup></a>.
            </p>
            <p>Хотя Камалашила в известной своей работе «Пурвабхаванакрама» упоминает АПД, тем не
                менее, он завершает свое сочинение конечной стадией постижения, венчая постепенный
                путь совершенствования бодхисаттвы десятой ступенью – «Облаком Учения», на которой
                обретается Всеведение в отношении всех познаваемых вещей (сарвакараджнята), или
                Всеведение Будды, чем процесс расширительного познавания завершается; система
                оказывается замкнутой.</p>
            <p>В АПД обретение так называемого «знания всех аспектов» рассматривается как
                викальповый признак, то есть как обладающее конструированием, и посему
                отбрасывается: «кто поступает, думая, всеаспектное знание достигнуто, тот живет в
                концепте». Система открыта, что вполне в духе релятивистской философии Нагарджуны и
                современной «философии незамкнутости». «Опираясь на бесконцептную сферу,
                бодхисаттва-махасаттва видит..., что все дхармы подобны иллюзии, миражу, сновидению,
                призраку, эху, отражению луны в воде, чудесному творению». Такое видение
                распространено в дхарани и на Всеведение Будды.</p>
            <p>Интригующим для исследования представляется описание собрания бодхисаттв и монахов,
                составивших аудиторию слушателей Будды при проповеди дхарани. Математизированность
                структуры перечисленных персонажей легко позволяет увидеть в ней конструкцию
                мандалы, что характерно для махаянских сутр вообще. Это убедительно доказывает, что
                тантрийские мандалы, широко распространенные в литературе и изобразительном
                искусстве буддизма, не стоит считать заимствованием из индуизма, их матричные
                прототипы в явном виде наличествуют в поздних сутрах, к каковой относят эту дхарани
                буддийские авторитеты. Думаем, что «мандалический» тип мышления вообще свойствен
                ранним формам религии, а тем более древнеиндийской.</p>
            <p>Проблематика АПД, рассмотренная
                даже в таком кратком исследовании, убеждает, что большинство главных атрибутов
                махаяны и ее теоретических положений не заимствованы, не инкорпорированы извне, а
                есть развитие в нужное время и в соответствующем месте идей, содержащихся в словах
                Будды – сутрах, дхарани. Это относится и к тантрийскому учению о махамудре, и к
                таким положениям дзогчена, как мгновенное и спонтанное самоосвобождение.</p>
            <p>Характерно, что в знаменитом столкновении разнородных направлений буддизма, известном
                как диспут в Самье, Камалашила использовал для аргументации АПД, в которой
