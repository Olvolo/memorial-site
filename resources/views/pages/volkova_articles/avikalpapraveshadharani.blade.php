            <p>В 1903 г. Цанид-Хамбо Агван Доржиев, верховный лама бурятских и калмыцких буддистов,
                привез в Санкт-Петербург подарок Далай-ламы XIII Тубтена Гьяцо (1876–1933),
                адресованный академику С. Ф. Ольденбургу.</p>
            <p>Это – небольшое и неполное собрание буддийских текстов, написанное на серой
                непальской бумаге правильным и четким непальским алфавитом, практически без ошибок.
                Краткое описание рукописи содержится в каталоге, составленном Николаем Дмитриевичем
                Мироновым<a href="#citation-2" id="ref-2" class="citation-mark"><sup>2</sup></a>.
                Рукопись содержит четыре текста. Первый – «Авикальпаправеша-дхарани» (далее АПД) на
                трех листах, в самом тексте отсутствует колофон и название дхарани. Второй – отрывок
                сутры на двенадцати листах без начала и конца, в котором идет речь о монашеских
                самварах (обетах) и упоминается «Пратимокша-сутра». Третий – один лист с колофоном
                «Буддхаватамсака-сутры». Четвертый – полностью сохранившиеся восемь листов Третьей
                «Бхаванакрамы» Камалашилы<a href="#citation-3" id="ref-3"
                                            class="citation-mark"><sup>3</sup></a>.</p>
            <p>Графологический анализ Н. Д. Миронова позволяет отнести всю рукопись к XII в. н.
                э.</p>
            <p>Русский востоковед-буддолог Е. Е. Обермиллер исследовал «Бхаванакраму» из указанной
                О. Ф. Волковой рукописи и опубликовал результаты в «Journal of the Greater India
                Society», vol. II, № 1. В 1963 г. трудами Б. И. Панкратова и Э. Н. Темкина было
                издано факсимиле сочинения Камалашилы с русским предисловием Е. Е. Обермиллера<a
                    href="#citation-4" id="ref-4" class="citation-mark"><sup>4</sup></a>.</p>
            <p>Сочетание в одном блоке текста АПД и Третьей «Бхаванакрамы», по-видимому, неслучайно
                и являет собой отголосок полемики в монастыре Самье. Диспут между сторонниками
                «мгновенного» и «постепенного» пути продолжался вплоть до XII–XIII вв., пока,
                наконец, не оформились традиционные школы тибетского буддизма. Заметим, что АПД
                является одним из базовых текстов в системе махамудры и дзогчена.</p>
            <p>В 1975 г. Октябрина Федоровна ВОЛКОВА (1926–1988), работая над переводом
                «Ланкаватара-сутры», заинтересовалась АПД в качестве вспомогательного текста для
                понимания ряда ключевых терминов, используемых в проповедях Третьего Поворота Колеса
                Учения, в частности – <em>авикальпа, нирвикальпа, викальпа</em>. Она выполнила
                перевод
                дхарани, но, к сожалению, работа не была завершена.</p>
            <p>В январе 1997 г. А. И. Бреславец предоставил в редакцию журнала машинопись перевода
                О. Ф. Волковой, санскритский текст и отрывки тибетской версии. На санскрите
                сохранился в машинописном варианте практически весь текст, за исключением названия,
                колофона и одного отрывка в конце сутры. Утраченные части санскритского оригинала
                были восстановлены О. Ф. Волковой по тибетской версии. Она восстановила 37
                утраченных на санскрите частей текста и, видимо, полностью перевела его. К
                сожалению, эта работа Волковой сохранилась не в полном объеме – отсутствуют
                начальная и конечные части перевода. Кроме того, не сохранились примечания, которые
                существовали, как явствует из рукописи, по крайней мере, к первой части
                перевода.</p>
            <p>Перевод утерянных отрывков был сделан Г. В. Алоянц в основном по санскритскому
                тексту с использованием тибетских фрагментов, сохранившихся в работе О. Ф.
                Волковой.</p>
            <p>В рукописи О. Ф. Волковой отсутствует название, однако, в начале сутры один из
                персонажей обращается к Будде с просьбой: «Да соблаговолит Бхагаван объяснить
                дхарани, называемую “Вхождение в безвикальпность”»<a href="#citation-5" id="ref-5"
                                                                     class="citation-mark"><sup>5</sup></a>.
            </p>

            <p>Данный текст включен в раздел «Сутры», ибо содержит слова Будды, имеет формальные
                признаки сутры (зачин, диалоговый текст, нравственный заряд, концовка), содержит
                философские и логические построения (чего нет в «чистых» дхарани). Что касается
                текста типа «дхарани», то он может быть либо самостоятельным, либо включенным в
                сутру, представляя собой вполне завершенную смысловую ее часть. Кроме того, это
                может быть набор лингвистически ничего не значащих фонем, что сближает его с
                мантрой. Так же, как мантра, дхарани является текстом, предназначенным для начитки
                определенное количество раз. Все эти признаки есть в исследуемом тексте, и это
                объясняет, почему он назван «дхарани». О том, что это именно дхарани, то есть
                своеобразный текстовый метод, есть много указаний и в самом тексте, и в его
                структуре: «Выслушав, познают ее, будут повторять..., досконально запомнят, будут
                разъяснять другим».</p>
            <p>Таким образом, являясь по форме сутрой, это произведение
                принадлежит одновременно к разряду дхарани и подразумевает особый подход слушателя и
                особую работу того, кто пользуется текстом как методом: текст нужно ежедневно по
                нескольку раз повторять наизусть, либо прочитывать. Таковы формальные признаки этого
                сочинения.</p>
            <p>Что касается содержания, то, согласно Будону Ринпоче (1290–1364),
                «Авикальпаправеша-дхарани» относится к Татхагатагарбха-сутрам в числе десяти сутр
                так называемого «конечного смысла» (nitartha), являющиеся сутрами Последнего
                Поворота Колеса Учения, произнесенных Буддой в Вайшали для собрания бодхисаттв,
                стремящихся к высшей цели. Но, согласно школе гелуг в лице Кедуба Гэлэга Пэлсанпо
                (1342–1412), сутры Последнего Поворота относятся к сутрам условного значения
                (neyartha), к кругу идей школы читтаматра. К сутрам конечного смысла Кедуб относит
                сутры произнесенные Буддой во время Второго Поворота Колеса Учения, то есть
                праджняпарамитские тексты, тексты школы мадхьямика.</p>
