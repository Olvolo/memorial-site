                определения в таковости, викальповый признак определения в высшей реальности<a
                    href="#citation-13" id="ref-13" class="citation-mark"><sup>13</sup></a>,
                викальповый признак определения в дхармовой сфере и в высшей истине<a
                    href="#citation-14" id="ref-14" class="citation-mark"><sup>14</sup></a>. С точки
