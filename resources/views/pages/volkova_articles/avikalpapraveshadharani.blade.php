                правильного поведения, викальповый признак использования терпимости, викальповый
                признак использования энергии, викальповый признак использования медитации и
                викальповый признак использования праджни. Другими словами, их испытывают с точки
                зрения их природы, с точки зрения качеств и
                их сокровенной сущности.</p>
            <p>Эти викальповые признаки, используемые как противоядия,
                также отбрасываются путем несосредоточения внимания.</p>
            <p>У того, кто их отбрасывает, появляются и возникают путем проецирования другие
                викальповые признаки относительно использования применения сущностей, а именно:
