            <p>Эти викальповые признаки он также отбрасывает путем необращения внимания<a
                    href="#citation-15" id="ref-15" class="citation-mark"><sup>15</sup></a>.</p>
            <p>Таким образом, бодхисаттва-махасаттва, отбрасывая необращением внимания викальповые
                признаки всех аспектов, ревностно подвизается в бесконцептной сфере, и хотя пока что
                не входит в контакт с бесконцептной сферой, но пребывает в «самадхи соответствия
                контакта с бесконцептной сферой».</p>
            <p>Следуя постоянно правильному усилию, созерцанию,
                повторению, правильному напряжению внимания, благодаря ненакоплению и спонтанности,
                он достигает бесконцептной сферы и последовательно очищается.</p>
            <p>Почему же, о сыновья благородного рода, бесконцептная сфера называется бесконцептной?
                Потому что в ней преодолены все концепты исследования, все концепты наставлений и
                примеров, все концепты относительно сил – индрий, все концепты относительно
                объектных сфер, все концепты относительного знания; потому что нет места всем
                препонам, моральным и относительно объектов познания. Что такое неконцепт? Это – вне
                формы, вне наставления, вне проявления, вне относительного знания, вне места.</p>
            <p>Опираясь на бесконцептную сферу, бодхисаттва-махасаттва видит все дхармы, подобные
                небесному своду, знанием, бесконцептным и неразличительным относительно объектов
                познания. Он видит бесконцептным знанием, что все дхармы подобны иллюзии, миражу,
                сновидению, призраку, эху, отражению луны в воде, чудесному творению.</p>
            <p>Затем он обретает огромность владения состоянием великого блаженства, обретает
                огромность богатства великого сознания, обретает огромность великого знания и
                мудрости (праджни), и огромность владения состоянием великого наставления. Он всегда
                в силах принести любую пользу всем живым существам. (Всё это) благодаря
                непрекращающемуся безусильному исполнению дела Будды.</p>
            <p>О, сыновья благородного рода, например, если бы под твердой и монолитной каменной
                горой был клад разнообразных драгоценностей, великолепных разнообразных камней,
                исполняющих желания, а именно серебра, золота или изумрудов. И пришел бы некий
                человек, стремящийся к великому сокровищу. Знающий об этом великом сокровище человек
                так бы сказал ему: «Эй, человек! Под каменной монолитной скалой есть великий клад
                драгоценностей – (вот) под ней клад великих драгоценных камней, исполняющих желание.
                Ты сначала отрой естественный камень. Когда ты его будешь отрывать, перед тобой
                возникнет камень, имеющий видимость серебра. Но и тогда ты не можешь иметь еще
                представления о великом кладе. Зная это, ты должен рыть (дальше). Когда ты будешь
                рыть дальше, перед тобой возникнет камень, имеющий видимость золота. Но и тогда ты
                не можешь иметь представления о великом кладе. Зная и это, нужно рыть (дальше).
                Когда ты будешь его отрывать, перед тобой возникнут камни, имеющие видимость
                различных драгоценностей. Но и тогда ты не можешь иметь представления об этом
                великом кладе. Признав и это, ты должен рыть дальше. Таким образом ты, человече,
                усердно стараясь, не накапливай вырытое и без накопления и совсем без усилий увидишь
                великий клад волшебного камня – чинтамани. И заполучив этот великий клад, ты станешь
                богатым (состоятельным), имущим, зажиточным и сможешь принести пользу себе и
                ближним. Это – сравнение, о сыновья благородного рода. Что же касается понимания его
                смысла,
                то монолитная каменная скала – это название моральной скверны всех аспектов
                обусловленных состояний бытия и наличия (привязанности к) двойственности. Великий
                клад волшебного камня – чинтамани внизу, это название бесконцептной сферы. Жаждущий
                клада великого волшебного камня – чинтамани – это название бодхисаттвы-махасаттвы.
                Знающий о великом кладе – это название татхагаты, архата, совершенного будды.
                Естественная каменная скала – это название концептов относительно природы. Рытье –
                это название необращения внимания. Камень, подобный серебру, – это название
                концептов относительно концептуальных характеристик противоядий. Камень, кажущийся
                золотом, – это название для концептов шуньяты и т.п. Камни, кажущиеся разнообразными
                драгоценностями, – это название для концептов [дхармы] получения. Обретение клада
                великого волшебного камня – чинтамани – это название для вхождения в бесконцептную
                сферу. Так, о сыновья благородного рода, в соответствии с приведенным сравнением
                должно понимать вхождение в бесконцептную сферу.</p>
            <p>О сыновья благородного рода, каким же образом бодхисаттва-махасаттва размышляет о
                вышеобъясненных концептах и затем входит в бесконцептную сферу? В этом мире, о
                сыновья благородного рода, бодхисаттва-махасаттва, если возникают
                концепты относительно природы чувственного, рассматривает их так.</p>
            <p>Кто действует, думая, это – моя рупа, тот действует в концепте. Кто живет, думая, это
                – рупа других, тот живет в концепте. Кто живет, думая, это – рупа, тот живет в
                концепте. Рупа возникает, прекращается, загрязняется, очищается – так думающий живет
                в концепте. Рупы нет – так думающий живет в концепте.</p>
            <p>Кто живет, думая, рупы нет даже с точки зрения своебытия, причины, следствия,
                объектности, обладания функции, тот живет в концепции. Кто живет, думая, рупа – это
                только ограниченное понятие, тот живет в концепте. Кто живет, думая, как рупы нет,
                так нет и относительного понятия рупы, – тот живет в концепте.Хотя, о сыновья
                благородного рода, бодхисаттва-махасаттва не воспринимает рупу, даже
                видимость рупы не воспринимает, но целиком и полностью он не утрачивает виджняпти<a
                    href="#citation-16" id="ref-16" class="citation-mark"><sup>16</sup></a>. И ту
                виджняпти в силу отсутствия не рассматривает, и вне виджняпти отсутствие не
                рассматривает. И отсутствие этой виджняпти, заключающейся в видимости рупы благодаря
                той виджняпти с точки зрения единства [с виджняпти ] не рассматривает; с точки
                зрения отдельности не
                рассматривает. Ни отсутствие виджняпти с точки зрения присутствия не рассматривает;
                ни с точки зрения отсутствия не рассматривает. О сыновья благородного рода, кто не
                создает концептов всеми этими всеаспектными концептами, тот даже не рассматривает:
                это – бесконцептная сфера. Это, о сыновья благородного рода, метод вхождения в
                бесконцептную сферу. Таким
                образом, бодхисаттва-махасаттва утверждается в бесконцептной сфере.</p>

            <p>[Так же<a href="#citation-17" id="ref-17" class="citation-mark"><sup>17</sup></a> с
                ощущением, с распознаванием, с двигателями, с сознанием<a href="#citation-18"
                                                                          id="ref-18"
                                                                          class="citation-mark"><sup>18</sup></a>.
                Так же с парамитой даяния, с парамитой нравственности, с парамитой усердия, с
                парамитой сосредоточения, с парамитой мудрости<a href="#citation-19" id="ref-19"
                                                                 class="citation-mark"><sup>19</sup></a>.
                Так же с пустотой и прочим<a href="#citation-20" id="ref-20"
                                             class="citation-mark"><sup>20</sup></a>
                и, наконец, это нужно проделать со знанием всех аспектов<a href="#citation-21"
                                                                           id="ref-21"
                                                                           class="citation-mark"><sup>21</sup></a>.
                Здесь<a href="#citation-22" id="ref-22" class="citation-mark"><sup>22</sup></a>, о
                сыновья благородного рода, бодхисаттва-махасаттвы, если возникают концепты
                относительно описания знания всех аспектов, рассматривают их так: «Кто поступает,
                думая, “это – мое знание всех аспектов”, тот живет в концепте; кто поступает, думая,
                “это – знание всех аспектов другими”, тот живет в концепте; кто поступает, думая,
                “это – знание всех аспектов”, тот живет в концепте; кто поступает, думая,
                “всеаспектное знание достигнуто”, тот живет в концепте; кто поступает, думая,
                “всеаспектное знание (существует) для отбрасывания аффективных и познавательных
                покровов (препятствий)<a href="#citation-23" id="ref-23" class="citation-mark"><sup>23</sup></a>”,
                тот живет в концепте. Кто поступает, думая, “знание всех аспектов
                возникает-прекращается, загрязняется-очищается”, тот живет в концепте. “Нет
                всеаспектного знания”, кто так думает, живет в концепте. Кто поступает, думая, “нет
                всеаспектного знания с точки зрения своебытия, причины, следствия, объектности,
                обладания функции, склонности”, тот живет в концепте. Кто поступает, думая, “как нет
                всеаспектного знания, так нет и относительного понятия<a href="#citation-24"
                                                                         id="ref-24"
                                                                         class="citation-mark"><sup>24</sup></a>
                всеаспектного знания”, тот живет
                в концепте<a href="#citation-25" id="ref-25" class="citation-mark"><sup>25</sup></a>.
            </p>
            <p>Хотя, о сыновья благородного рода, бодхисаттва-махасаттва как всеаспектное знание не
                воспринимает, точно так же и относительное понятие всеаспектного знания не
                воспринимает, но целиком и полностью при этом своего сознавания не губит.</p>
            <p>И, помимо сознавания, никакой другой дхармы не воспринимает. И ту (дхарму) сознавания
                в силу
                (ее) отсутствия не рассматривает и, помимо сознавания, отсутствие не рассматривает.
                И отсутствие (благодаря тому) сознаванию не рассматривает ни с точки зрения единства
                с ним (с сознаванием), ни с точки зрения отдельности от него. Ни отсутствие
                сознавания как присутствие не рассматривает, ни как отсутствие не рассматривает.</p>
            <p>О сыновья благородного рода, кто не создает концептов всеми этими всеаспектными
                концептами<a href="#citation-26" id="ref-26" class="citation-mark"><sup>26</sup></a>,
                тот даже и не рассуждает так: “это – неконцептная сфера”. Таков способ вхождения в
                неконцептную сферу”».</p>
            <p>Таким образом бодхисаттва-махасаттва утверждается в
                неконцептной сфере.</p>
            <p>И тогда Бхагаван сказал такую гатху:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg italic">
                <p>
                    Установившись с упованием на неконцептность в этой благой дхарме,
                </p>
                <p>
                    Бодхисаттва, преодолев труднопроходимость неконцептности,
                </p>
                <p>
                    Последовательно<a href="#citation-27" id="ref-27"
                                      class="citation-mark"><sup>27</sup></a>
                    достигает неконцептности.
                </p>
                <p>
                    От этого великий покой и наивысшая бесстрастность (обретаются, и)
                </p>
                <p>
                    Бодхисаттва достигает блаженства неконцептности<a href="#citation-28"
                                                                      id="ref-28"
                                                                      class="citation-mark"><sup>28</sup></a>.
                </p>
            </blockquote>

            <p>Когда Бхагаван произнес эти слова, возрадовались бодхисаттва Авикальпапрабхаса,
                бодхисаттвы-махасаттвы всего собрания, все присутствующие боги, люди, асуры,
                гандхарвы – весь мир возликовал от сказанного Блаженным].</p>

