                            – gzung-'dzin,
                        </li>
                        <li>препятствование и осуществление,</li>
                        <li>очищенное и неочищенное,</li>
                        <li>внешнее и внутреннее</li>
                        <li>отбрасывать и находить,</li>
                        <li>принимать и оставлять.</li>
