                (учение)». По этому поводу читаем (Махамудра, л.28):
            </p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <p>
                    Воззрений реалистических и идеалистических, высокомерных и
                    других существует великое множество, но они заменяются в
                    тантризме средним воззрением (dbu-ma).
                </p>
