                </p>
                <p class="my-2" id="citation-6"><sup>6 </sup>Парадоксальность и драматизм
                    ситуации обнаруживаются в осознавании индивидом, воспитанным
                    в русле западной культуры, того факта, что речь здесь идет
                    действительно об универсальной методологии, некоем, как
                    выражались в средние века, философском камне, позволяющем
                    постичь как относительную, так и абсолютную истину. Отсюда и
                    драматизм ситуации, фокусирующийся в жгучий вопрос: а как у
                    «нас»?<a href="#ref-6" class="text-blue-500 hover:underline">↩</a>
