                Абхидхарма (с.7):</p>
            <blockquote
                class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <p class="mb-4">
                    Высшая мысль, познающая основу сущности вещей, это
                    психические процессы, которые вызывают движение в объекте
