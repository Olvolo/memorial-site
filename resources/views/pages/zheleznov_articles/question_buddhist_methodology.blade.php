                    <ul class="list-disc pl-5 space-y-1 ml-8">
                        <li>вечность и оторванность (непостоянство),</li>
                        <li>нечто и ничто,</li>
                        <li>широкое и узкое,</li>
                        <li>хорошее и плохое,</li>
                        <li>большое и малое,</li>
                        <li>высокое и низкое,</li>
                        <li>признание действительным того, что непосредственно
