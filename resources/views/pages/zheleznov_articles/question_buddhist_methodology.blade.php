                    современный оккультизм. Несколько отдельно стоят такие
                    источники, как греческая натурфилософия, логика и др. Все
                    они возникли и развивались в культурной среде, связанной с
                    народами, проживающими в обширном регионе Средиземного моря,
                    особенно в ее восточной части. Все это – источники науки и
