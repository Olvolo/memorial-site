                <h4 class="text-center text-gray-800 font-bold text-xl
                sm:text-xl md:text-xl my-4">ПРИМЕЧАНИЯ</h4>

                <p class="my-2" id="citation-1"><sup>1 </sup>Розенберг, следуя Васубандху,
                    скандху определяет так: «Если объединить в одну группу все
                    дхармы чувственные (рупа), т.е. прошедшие, настоящие и
                    будущие, притом всех разновидностей, то получится группа
                    чувственного (рупа-скандха). Таким же образом получается
                    группа сознания (виджняна-скандха) и др., т.е. группа
                    ощущений (ведана-скандха), группа процессов различения
                    (санджня-скандха) и группа вообще процессов
                    (санскара-скандха)» (1, 133). Здесь сразу заметим, что
                    перевод О. Розенбергом термина <em>виджняна-скандха</em> не
                    совсем точно отражает смысл этого выражения. <em>Виджняна</em> на
                    тибетский переводится как rnam-shes, что означает
                    <em>способность (абсолютно) познать</em>. К тому же
                    сознание — sems (<em>санскр</em>. citta) как отдельный
                    элемент есть <em>виджняна-скандха</em>, поэтому
                    в дальнейшем будем употреблять выражение <em>скандха
                        способности познать</em> или просто
                    <em>виджняна-скандха</em>.<a href="#ref-1" class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="my-2" id="citation-2"><sup>2 </sup>Оговоримся сразу, что под словом
                    <em>всех</em> подразумеваются элементы, обозначаемые
                    термином<em>самскрита</em>, т.е. дхармы, подверженные бытию.
                    Слово <em>все</em>здесь употреблено исключительно для того,
                    чтобы только подчеркнуть то обстоятельство, что в данной работе рассматриваются те
                    элементы, которые должны успокоиться, перевернуться в
                    трансцендентальную область. В действительности, <em>все элементы</em>
                    (sarvadharmah) включают в себя, кроме пяти скандх, также и
                    постоянные элементы (nitya).<a href="#ref-2" class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="my-2" id="citation-3"><sup>3 </sup>Клеши, начиная с неведения
                    (ma-rig-pa), или моральные осквернения, буддисты считают
                    главной причиной существования индивида в сансаре. Вся
                    система тантризма построена с точки зрения того, каким
                    образом подавлять клеши.<a href="#ref-3" class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="my-2" id="citation-4"><sup>4 </sup>По тантрийской системе авандуди
                    проходит от макушки головы до конца полового органа. Первый
                    узел находится под корой головного мозга, второй — на уровне
                    горла, третий — на уровне сердца, четвертый — в области
                    пупка, пятый — в конце полового органа.<a href="#ref-4" class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="my-2" id="citation-5"><sup>5 </sup>Изначальную нирвану тантристы
                    персонифицируют Адибуддой — Ваджрадхара, Ваджрасаттва,
                    Самантабхадра. Он обладает Пятью Мудростями (пять
                    дхьянибудд) и ему присущи Три Тела: Тело Абсолюта, или
                    сущность нирваны — дхармакая (chos-sku), Тело собственного
                    блаженства — самбхогакая (longs-spyod-sku) и Тело Явленное —
                    нирманакая (sprul-sku). Второе Тело и Третье исполняют
                    коммуникативную роль между Абсолютом и совершенствующимся.<a href="#ref-5" class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="my-2" id="citation-6"><sup>6 </sup>Для удобства анализа порядка
                    перечисления скандх мы переходим от порядка, рассмотренного
                    в данном отрывке, к исходному порядку Абхидхармы.<a href="#ref-6" class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="my-2" id="citation-7"><sup>7 </sup>В центре мандалы расположен
                    дворец с четырьмя входами. У входов стоят Ямантаки,
                    охраняющие дворец от проникновения в него внешних врагов.<a href="#ref-7" class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="my-2" id="citation-8"><sup>8 </sup>Буддисты трёх колесниц
                    постоянно утверждают, что все мары есть субъективное
                    явление, возникающее от различных комбинаций клеш и скандх
                    индивидуума с его же сознанием, подчёркивая, что это ни в
