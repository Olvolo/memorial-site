            <p>
                Здесь за основу принципа перечисления скандх положена
                непосредственная их связь с нервными узлами на авандуди<a
                    href="#citation-4" id="ref-4"
                    class="citation-mark"><sup>4</sup></a>
                индивида, а сами узлы перечислены, начиная с нижнего, вверх.
                Сейчас мы оставляем в стороне вопрос, почему в одних случаях
                дается основной порядок описания узлов, а именно сверху вниз:
            </p>

            <ul class="list-none pl-5 space-y-1 ml-8">
                <li class="my-2">1) макушка, чакра великого блаженства (spyi-bo bde-chen-gyi-’khor-lo) — рупа-скандха;</li>
                <li class="my-2">2) горло, самбхогачакра (mgrin longs-spyod ’khor-lo) — санджня-скандха;</li>
                <li class="my-2">3) сердце, дхармачакра (snying-kha chos-kyi-’khor-lo) — виджняна-скандха;</li>
                <li class="my-2">4) пуп, нирманачакра (lte-ba sprul-ba’i ’khor-lo) — ведана-скандха; тайное место, сукхаракшачакра (gsangs gnas bde-skyong ’khor-lo) — самскара-скандха,</li>
            </ul>
               <p>
                а в других – порядок
                дополнительный, как в приведенном примере, где осуществлена
                перестановка природы среднего и верхнего узлов, — этот вопрос
                самостоятельный и еще более сложный. Помимо указания на
                существование порядка перечисления скандх на авандуди в
                приведенном отрывке для нас важны следующие два момента.</p>
            <p>
                Первый — то, что каждая скандха в тантрийской практике
                соответствует одной клеше, входящей в данную скандху.
                Соответственно, те совершенствующиеся, которые обращаются
                непосредственно к системе Ваджрасаттвы<a href="#citation-5"
                                                         id="ref-5"
                                                         class="citation-mark"><sup>5</sup></a>,
                на первой стадии
                созерцания занимаются исключительно подавлением этих клеш, как
                бы атакой клеши в лоб. Это и понятно: с точки зрения созерцания
                сосредоточиться на пяти элементах гораздо легче, чем сразу на
                всех. Вопрос же о том, к каким осложнениям уже в ином плане
                ведет такая замена, мы оставляем ввиду его самостоятельности.
                Укажем, что в более сложных методах созерцания также делается
                упор на подавление клеш, но с одновременным указанием, что таким
                образом очищаются скандхи, или же непосредственно тантрийской
                практикой переворачивают скандхи в трансцендентальную область,
                отрезая этим самым возможность реализоваться клешам.
            </p>
            <p>
                Второй – то, что после уничтожения клеш очищенные скандхи
                переворачиваются в трансцендентальную область, где они
                обнаруживаются как Пять Мудростей — персонификации пяти
                дхьянибудд<a href="#citation-6" id="ref-6"
                             class="citation-mark"><sup>6</sup></a>.
            </p>
            <ol class="list-decimal list-inside ml-2 pl-2">
                <li class="my-2">Рупа-скандха переворачивается в Зерцалоподобную мудрость
                    (санскр. adarsajnana, тиб. me-long-lta-bu’i ye-shes). Скандха
                    формы возникает первой у индивида, и неведение, базируясь на
                    ней, создает представление об индивидуальном Я
                    (gang-zag-gi-bdag), или эго, которое не может по общему закону,
                    находясь в своей собственной «системе» (природе), познать себя.
                    Поэтому индивид (его индивидуальное Я) видит Вайрочану после
                    указания со стороны, как бы в зеркале, представленном
                    тантрийским учителем, и потому эта мудрость называется
                    Зерцалоподобной.</li>
                <li class="my-2">Ведана-скандха переворачивается в Мудрость Равного содержания
                    (санскр. samantajnana, тиб. mnyam-pa-nyid-kyi ye-shes). Эта
                    скандха направлена на приобретение индивидом утилитарных
                    ценностей, которые по природе есть шуньята (пустота), и между
                    которыми нет никакой разницы; поэтому эта Мудрость называется
                    Мудростью Равного Содержания. Атрибутом ее у Ратнасамбхавы
                    является волшебный талисман Чинтамани, производящий любую
                    ценность.</li>
                <li class="my-2">Санджня-скандха переворачивается в Мудрость, познающую
                    каждую вещь в отдельности. Эта скандха тесно связана с
                    предыдущей: чтобы обладать богатством, нужно различать его
                    ценность. Обе они направлены на внешний мир. После уничтожения
                    Амитабхой страсти, стремления к различению вещей в сансаре
                    (длинная вещь или короткая, красивая или некрасивая, приятная
                    или неприятная, и т.д.) индивид постигает идеал различительной
                    способности, начинает четко различать абсолютно любую вещь
                    отдельно от любой другой, то есть действительно познает
                    абсолютно каждую вещь в отдельности. Это качество
                    различительной мудрости, чистое, без примеси какого-либо
                    пристрастия к самому объекту познания символизируется лотосом,
                    символом чистоты и непорочности, атрибутом Будды Амитабхи.</li>
                <li class="my-2">Самскара-скандха переворачивается в Действенную (Творящую)
                    Мудрость (санскр. kriyanusthanajnana, тиб. bya-ba-sgrub-ba’i
                    уе-shes). Эта скандха в отличие от предыдущих, способных
                    находиться в потенциальном состоянии, пребывает в постоянном
                    движении, то есть клеша зависть (или самомнение), видя мнимые
                    преимущества чужого индивидуального Я, заставляет себя
                    направлять усилия по тому же пути, чем развертывает все в новых
                    аспектах свою карму и создаст причины и условия для все новых и
                    новых перерождений. Амогхасиддхи, уничтожая зависть, как бы
                    отсекает своим мечом плохую карму и переворачивает саму скандху
                    в Творящую (благоприятную) карму мудрость.</li>
                <li class="my-2">Виджняна-скандха переворачивается в Мудрость дхармового
                    пространства (санскр. dharmadhatujnana, тиб. chos-kyi-dbyings-kyi уе-shes). Эта скандха характеризуется тем,
                    что сознание повсюду в сансаре испытывает муки страдания,
                    вследствие чего Я индивида гневается. Когда Акшобхья уничтожает
                    гнев, скандха переворачивается в Неколебимую мудрость, подобную
                    непоколебимому дхармовому пространству; символом этой мудрости
                    служит неразрушимый ваджр — атрибут Акшобхьи. В результате все
                    клеши подавляются дхьянибуддами, причем все одновременно, и
                    индивид покидает сансару.</li>
            </ol>

            <p>
                Вернемся к примерам, взятым из сутр. Этот порядок перечисления
                скандх мы наблюдаем во многих работах, но прежде всего нас
                интересует «Абхидхармакоша» Васубандху, ибо она подводит итог
                всем хинаянистическим трудам, разбирающим учение о дхармах и по
                традициии никоим образом не связана с тантрами.
            </p>
            <p>
                Устойчивое повторение такого порядка перечисления скандх
                наводит на мысль, что этот порядок есть одновременно и схема
                чего-то иного, а не просто схема группировки дхарм.
                Действительно, такая схема существует в мандалах — магических
                кругах пяти дхьянибудд, где они могут быть представлены в
                спокойных, гневных и иных формах.</p>
            <p>
                Обратимся, например, к мандале идама Ваджрабхайравы [6]. Во
                внутреннем круге располагаются гневные дхьянибудды в виде
                Ямантак:</p>
            <ol class="list-decimal list-outside ml-8 pl-2">                <li class="my-2">
                    Вайрочана в виде Невежественного Ямантаки
                    (gTi-mug-gshin-rje’i-gshed) подавляет клешу неведения
                    (avidya, ma-rig-pa) или ее разновидность – невежество (moha,
                    gti-mug);
                </li>
                <li class="my-2">
                    Ратнасамбхава в виде Жадного Ямантаки
                    (Ser-sna-gshin-rje’i-gshed) подавляет клеши гордость (mana,
                    nga-rgyal) и жадность (malsarya, ser-sna);
                </li>
                <li class="my-2">
                    Амитабха в виде Страстного Ямантаки
                    (’Dod-chags-gshin-rje’i-gshed) подавляет клешу страсти
                    (raga, ’dod-chags);
                </li>
                <li class="my-2">
                    Амогхасиддхи в виде Завистливого Ямантаки
                    (Phrag-dog-gshin-rje’i-gshed) подавляет клешу зависти
                    (irsya, phrag-dog).
                </li>
            </ol>

            <p>
                Сам Ваджрабхайрава, находящийся в центре, подавляет гнев
                (krodha, zhe-sdang или dvesa, khong-khro), по схеме авандуди
                также располагающийся в центре по отношению к другим клешам.
            </p>
            <p>
                Основываясь на первом моменте из приведенного отрывка «Хандо
                Нянжуд», подставляем в схему мандалы на место клеш
                соответствующие скандхи и получаем исходный порядок перечисления
                скандх в Абхидхарме.
            </p>
            <p>
                Здесь мы вторично подчеркиваем то положение, что в ваджраяне для
                достижения пяти трансцендентальных мудростей первостепенное
                значение имеет процесс уничтожения клеш, тогда как в хинаяне
                после подавления клеш могут оставаться скандхи с «незанятыми»
                местами клеш, и индивид (архат) даже после очищения скандх не
                приобретает мудрости.
            </p>
            <p>
                Но в ваджраяне наряду с уничтожением клеш не отказываются совсем
                от отдельного очищения скандх. Об этом свидетельствует внешнее
                окружение внутреннего круга мандалы Ваджрабхайравы<a
                    href="#citation-7" id="ref-7"
                    class="citation-mark"><sup>7</sup></a>, в котором
                Ямантаки выступают против мар – персонификаций всего того, что
                держит индивида в сансаре.
            </p>
            <ul class="list-none pl-5 space-y-1 ml-4">
                <li class="my-2">У входа на востоке, с которого начиналась расстановка дхьянибудд во внутреннем круге, находится Ямантака с ваджрным молотком (Tho-ba-gshin-rje'i-gshed). Он побеждает Мару скандх (Phyung-po’i-bdud). Далее, по часовой стрелке,</li>
                <li class="my-2">у южного входа располагается Ямантака с дубиной (dByung-pa-gshin-rje’i-gshed) из человеческого костяка, он побеждает Мару клеш (Nyon-mongs-pa’i-bdud).</li>
                <li class="my-2">У западного входа стоит Ямантака с лотосом (Padma-gshin-rje’i-gshed), он побеждает Мару Хозяина смерти (’Chi-bdag-gi-dbdud).</li>
                <li class="my-2">У северного входа — Ямантака с мечом (Ral-gri-gshin-rje’i- gshed), который побеждает Мару Сына Небожителя (lHa’i-bu’i-bdud).</li>
            </ul>
            <p>
                Краткое толкование этих мар можно найти в сочинении тибетского
                ученого Гунчена Жамьяна Шабды (XVIII в.): «Отдельно взятая
                скандха (как таковая), любые ее тонкие составляющие являются
                особенностью (признаком) Мары скандх. Сами клеши и их
                пристрастия являются признаком Мары клеш. Поступки индивида вне
                зависимости от моральных установок, клеши и их пристрастия, все
                то, что так или иначе ведет индивидуума к смерти, является
                признаком Мары смерти. Препятствия, возникающие при стремлении
                освободиться от первых трех мар, являются особенностью Мары
                Сына Небожителя» [5, 109а].
            </p>
            <p>
                Здесь мы вторично сталкиваемся со скандхами, теперь
                персонифицированными в виде Мары — своего рода антипода Будды.
                Введение мар, воплощения всего того, что держит индивидуума в
                сансаре и мешает ему совершенствоваться (от скандх и клеш, как
                субъективной стороны мар, вплоть до смерти, и помех внешнего
                мира, препятствующих спасению, как объективной стороны мар<a
                    href="#citation-8" id="ref-8"
                    class="citation-mark"><sup>8</sup></a>),
                позволяет не только понять, с чем имеют дело тантристы во время
                созерцания, но объясняет, почему дхьянибудды принимают гневный,
                грозный вид.
            </p>
            <p>
                В связи с этим, видимо, можно говорить о том, что если
                дхьянибудды принимают гневный вид для подавления мар, то
                возглавляет их в этом случае, как правило, Акшобхья
                (Mi-bskyod-pa), обладающий Мудростью дхармового пространства и
                занимающий то место у созерцающего, где располагается гнев.
            </p>
            <p>
                Таким образом, исходя из анализа принципа, по которому
                перечисляются скандхи в Абхидхарме, можно утверждать, что тантры
                возникли не только одновременно с учением о дхармах, но и
                явились цементирующим началом для него, хотя прямых указаний на
                такую связь на поверхности Абхидхармы мы не находим.
            </p>
            <p>
                Поскольку зарождение Абхидхармы произошло, как считает
                буддологическая традиция, в хинаяне, постольку правомочно
                утверждать о существовании некоей программности в учении о
                дхармах во времена оформления хинаяны как целого. Программность
                эта заключается в том, что в самом учении хинаяны в скрытом виде
                заложены указания на существование высших путей
                совершенствования, а следовательно, на возможность перехода в
                одной жизни от хинаяны через махаяну в тантризм, примером чему
                является известная эволюция Васубандху, а также система обучения
                тибетских монастырей, созданная Цзонхавой так, что индивидуум,
                приступающий к совершенствованию, сначала становится монахом,
                потом, по мере совершенствования, махаянистом и, наконец,
                тантристом. В сочинениях Цзонхавы, особенно в «Ламриме»,
                постоянно подчеркивается непротиворечивость учения хинаяны и
                ваджраяны.
            </p>
