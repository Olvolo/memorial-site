                    [качества]. Важность этого "определителя" (названия) связана
                    с вышеописанными тремя аспектами thugs rje (об этом будет
                    сказано в связи с сравнением Махамудры и Дзогчена).<a href="#ref-44" class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="my-2" id="citation-45"><sup>45 </sup>Другие многочисленные
                    предметы, привязанные к хрусталю и различным изделиям из
                    хрусталя, желающий может найти там же (л. 21-27).<a href="#ref-45" class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="my-2" id="citation-46"><sup>46 </sup>Нацог-Рандол описывает встречу
                    Манчжушримитры с Гарабдорже так: «В то время в западной
                    Индии, в двухэтажном городе сын полноценного учителя брамина
                    и матери, которую звали "ясносветлой красавицей", был
                    великим учителем по имени "Друг Манчжушри" ('Jam dral bshes
                    gnyen). Он был знатоком пяти отраслей знания и создал учение
                    на основе познания секрета кладбища (тантрийское учение).
                    Мудрый Манчжушри дал ему предписание: "О, сын генезиса,
                    Бодхисаттва! Если хочешь завершить достижение результата
                    (плода), то иди в прохладную рощу великого кладбища".
                    Услышав этот призыв, он сразу явился туда. [Там он]
                    встретился с великим Учителем, Весьмарадостным Ваджром,
                    последовал за ним и получил от него все учение полностью.
                    Будучи тантрийским учителем, прибыл он на исток реки в виде
                    концентрации света» [Нацог-Рандол, л. 166].<a href="#ref-46" class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="my-2" id="citation-47"><sup>47 </sup>Непалец по имени "Нерожденное
                    Великое Блаженство" (известен также под именем bla ma bal po
                    a su) был первым (из известных трех) учеником индийского
                    учителя Ваджрапани, положившего начало преподаванию "Песен"
                    в Тибете. Другим учеником Ваджрапани был Рэчунг (ras chung),
                    любимый ученик Миларэйпы. От третьего ученика Ваджрапани -
                    Mnga' ris pa - линия преемственности приводит нас к еще
                    одному комментатору "Песен" - Кармапринлэйпе (Karma phrin
                    las pa), современнику VII Кармапы.<a href="#ref-47" class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="my-2" id="citation-48"><sup>48 </sup>Приведем для краткости лишь те
                    песни, что связаны непосредственно с группой "небесные
                    облака и дождь (сострадания)":
                    [5]. "Так же, как облако (rtsal), которое поднимается из
                    моря, / Впитывая дождь (gdangs), обнимает землю, / Так и
                    море, подобное небу (gshis, gshis ma bcos pa, см. выше), /
                    He уменьшается и не увеличивается (т.е. не улучшается (bcos
                    su med), не ухудшается (bslad du med)" [Гюнтер 1969, p. 64].
                    (Соответствия трех rnang образам в "Доха" даны мной. - А.Ж.)
                    [11]. "Подобно тому, как морская вода становится / Пресной,
                    когда ее пьют облака, / Так и непоколебимый ум, который
                    трудится для других, / Превращает яд предметов чувств в
                    нектар" [Там же, с. 65]. Ср.: "или сильный яд, попавший под
                    власть мантры или не попавший, или камфора (ga bur), или
                    (ср. следующую доху, № 12. - А.Ж.) похожа на сверкание
                    молний" [Нацог-Рандол, л. 256]. [12]. "...Хотя вы боитесь удара грома из тучи, хлеб
                    созревает, когда она льет дождь" [Там же].<a href="#ref-48" class="text-blue-500 hover:underline">↩</a>
                </p>
