            <p>Настоящая работа не является собственно историей буддизма в
                Бурятии (или шире в России); для этого, видимо, надо объединить
                усилия многих авторов, специализирующихся в различных областях
                буддизма и, естественно, не ориентирующихся на две-три статьи
                аналогичного сборника. Не ставится в этой работе и задача
                ограничиться только обзором и оценкой буддологической
                историографии в России, так как даже полная кропотливая работа
                по историографии для целостного представления об истории и
                развитии буддизма в Бурятии должна быть дополнена хотя бы
                описанием письменных первоисточников на тибетском и монгольском
                языках (в свое время этим описанием систематически занимался
                небольшой коллектив сотрудников БФ СО АН СССР, но ныне
                систематическая работа с первоисточниками приостановлена), а
                также материалом по культовым сооружениям, монастырской жизни
                лам и т.д. (сейчас такой материал приходится в основном
                реконструировать).
            </p>

            <p>В связи с этим здесь будет намечена некая историческая канва по
                бурятскому буддизму с освещением отдельных вопросов тантризма и
                его взаимоотношений с другими колесницами. Возможно, такая канва
                станет опорной для собственно исторической работы по буддизму в
                Бурятии.
                Для понимания пути проникновения и развития буддизма в Бурятии
                очень важно иметь представления об истории буддизма в Индии (на
                родине), в Тибете и Монголии, а также в Китае. Работы по истории
                буддизма в этих странах на русском языке позволяют, даже
                неспециалисту, составить достаточно определенное мнение об этом
                вопросе, поэтому, прежде чем приступить к предмету настоящей
                статьи, напомним вкратце основные вехи на пути проникновения
                буддизма в Бурятию.
            </p>

            <p>На своей родине буддизм возник и развивался в пестрой среде
                религиозных, философских систем, поэтому среди некоторых учений
                можно найти, казалось бы, много общего с буддийским учением.
                Примеры толкований таких понятий, как перерождение, карма, йога,
                самадхи, при желании можно выстроить в длинный ряд. Однако лишь
                буддизм сумел найти ключи к умам и сердцам далеко за пределами
                Индии, тогда как все другие религиозные учения остались только
                индийскими, не выходящими за пределы сугубо индийской культуры
                ("увлечение" кришнаизмом в Европе и Америке не может быть
                предметом нашего внимания, как не может попасть в поле внимания
                желающего приобрести изделие из хрусталя осколок стекла).
            </p>

            <p>Рассматривая развитие буддизма в Индии, некоторые авторы выделяют
                три периода. Первый: с бенаресской проповеди Будды Шакьямуни до
                первого буддийского собора в Раджагрихе - период
                непосредственного контакта-влияния учительства Будды (523-477
                гг. до н.э.)<a href="#citation-1" id="ref-1"
                               class="citation-mark"><sup>1</sup></a>. Второй: преобладание шраваков и
                пратьекабудд -
                период архатства - с первого буддийского собора до учения
                Махаяны (или, иначе, период Хинаяны - малого пути, малой
                колесницы - до I в. н.э.). Третий: период Махаяны, или великого
                пути, великой колесницы (I-XII вв. н.э.).
            </p>

            <p>С точки зрения вращения колеса учения (по Будону) такое деление
                на периоды (по времени и содержанию), соответствует разделению
                учения на три Писания:
            </p>
            <ul class="text-left italic list-none my-4 pl-5 space-y-1 ml-8">
                <li>раннее (bka' dang po) - Хинаяна;</li>
                <li>промежуточное (bka' bar ba) - Мадхъямика и Праджня-парамита;
                </li>
                <li>позднейшее (bka' tham) - Йогачар.</li>
            </ul>
            <p>
                Какой бы принцип, однако, ни был положен в основу классификации
                периодов развития буддизма в Индии, нельзя не обратить внимания
                на как бы самостоятельное, независимое друг от друга<a href="#citation-2" id="ref-2" class="citation-mark"><sup>2</sup></a> развитие
                колесниц, что отразилось даже на географии их распространения.
                По мере роста числа последователей Будды в разных местах Индии
                сначала небольшие группы, а затем и целые общины нищенствующих
                монахов меняют бродячий и полубродячий образ жизни на жизнь с
                относительными удобствами в небольших благоустроенных общинных
                жилищах. Постепенно эти жилища становятся многочисленнее и
                многолюднее - возникают настоящие монастыри с общемонастырскими
                и местными уставами. В свою очередь этот первоначальный тип
                монастыря со строгим, замкнутым стилем внутренней жизни был
                вытеснен новым типом многолюдного монастыря -
                монастырем-университетом. Теперь наряду с монахами-отшельниками
                здесь можно встретить преподавателей, писателей, ученых.
                Преподается полный набор отраслей знаний того времени;
                проводятся на регулярной основе диспуты - как учебные,
                тренировочные, так и между главами буддийских школ, а также
                между буддистами и не буддистами.
                Вот этот самый интересный<a href="#citation-3" id="ref-3"
                                            class="citation-mark"><sup>3</sup></a> период развития индийского буддизма и
                явился "трамплином" для распространения его в Тибете, хотя здесь
                также можно выделить свои качественно разнящиеся периоды и
                говорить об универсализме ранних форм тибетского буддизма можно
                лишь с оговоркой. Оговорка эта в основном делается в связи с
                разными подходами к переводам на тибетский с санскрита.
            </p>
            <p>
                В XI в. великий переводчик и создатель тибетской эристики
                Ринчен-санбо, ученик знаменитого Атиши, проводит большую работу
                по организации школы переводчиков, вследствие чего появилась
                масса стандартизированных переводов остававшейся не переведенной
                к тому времени литературы, а также была перепроверена (и
                "исправлена") часть уже переведенной на тибетский. Все эти труды
                были сведены в два корпуса - Ганчжур и Данчжур<a
                    href="#citation-4" id="ref-4" class="citation-mark"><sup>4</sup></a>. Все работы,
                относимые к ранней традиции перевода (не подвергнутые
                корректировке), не были включены в Канон. Вся эта литература
                принадлежит последователям традиции Нингма, в меньшей степени -
                Дзогчен<a href="#citation-5" id="ref-5" class="citation-mark"><sup>5</sup></a>.
            </p>
            <p>
                Помимо различения этих двух периодов для понимания истории
                буддизма в монголоязычных странах важно иметь представление о
                школах и сектах, которые возникли и развивались в русле новой
                переводческой традиции.
            </p>
            <p>
                В настоящее время различают четыре школы-направления тибетского
                буддизма. Каждому направлению присущи свои отличительные
                особенности как философского содержания, так и монастырской
                жизни, а также созерцательной практики.
            </p>
            <p>
                Самая ранняя школа Тибета, и это отражено в ее названии -
                Нингмапа, имеет почти все те основные признаки, которыми
                характеризуется первоначальный тибетский буддизм: не имеет
                сектантских различий<a href="#citation-6" id="ref-6"
                                       class="citation-mark"><sup>6</sup></a>; монастыри не получили широкого
                распространения и были исключительно отшельнического<a
                    href="#citation-7" id="ref-7" class="citation-mark"><sup>7</sup></a> типа (в
                форме одиночных келий и маленьких монастырей). Эти формы
                характерны для всего периода: от начала проникновения (по
                тибетской традиции, от времени царствования Лхатотори-нянцзана -
                II в.) и до эпохи первых религиозных царей включительно. В
                правление одного из них (Тисрон-дэвцзана) в середине VIII в. из
                Индии был приглашен в помощь другому индийскому
                проповеднику-философу Шантиракшите знаменитый практик-тантрист
                Падмасамбхава.
            </p>
            <p>
                Подчинив своей йогической силой местных жрецов бон - противников
                утверждения буддизма в Тибете, Падмасамбхава не только создал
                условия для дальнейшего упрочения буддизма, но и дал
                направление, характерное для Нингмапы. Речь идет о более чем ста
                сочинениях Падмасамбхавы, спрятанных им в различных местах
                Тибета. Большинство этих произведений (уже после возникновения
                других школ) в XII в. были обнаружены тертонами (открывателями
                кладов) и сведены в 61 том, что вместе с житием Падмасамбхавы
                составило своеобразный канон Нингмапы, не признаваемый новыми
                школами Тибета.
            </p>
            <p>
                В X-XI вв., в период возрождения<a href="#citation-8" id="ref-8"
                class="citation-mark"><sup>8</sup></a> буддизма, Тибет наводняют
                бежавшие от исламских завоевателей буддийские монахи из Кашмира
                и Восточного Туркестана. Кроме них было много и специально
                приглашенных ученых - из Кашмира, Непала и Индии. В это же время
                в Кашмире обучалось много тибетцев, будущих переводчиков с
                санскрита.
            </p>
            <p>
                В середине XI в. в Тибет прибывает Атиша (982 - 1054 гг.),
                известнейший пандит из Бенгалии. Вместе со своими ближайшими
                сподвижниками Брондонбой (1004 - 1065 гг.) и Бодобой (1027 -1105
                гг.) он закладывает основы школы Кадампа, а с помощью другого
                сподвижника - Лоцзавы Ринчен-санпо (958-1055) создает, как уже
                сказано выше, школу переводчиков буддийских текстов с санскрита.
                В стенах монастырей Кадампы некоторое время поддерживался
                индийский дух: строго соблюдалась монашеская дисциплина
                независимо от направления колесниц спасения, а сами колесницы
                рассматривались в рамках экаяны ("единой колесницы") не как
                исключающие друг друга, а, скорее, как дополняющие, переходящие
                одна в другую (от шравакаяны до ваджраяны).
                Такая чисто индийская форма секты не могла долго сохраняться, и
                во второй половине XI в. в Тибете появляются новые направления с
                сильно "отибеченной" формой.

            </p>
            <p>
                Основатель школы Каржудпа Марба-лодзава (1012- 1097 гг.) трижды
                наведывался в Индию, где встречался с великими пандитами
                Наланды, а главное - с махасидхами Наропой, Кукурипой и
                Майтрипой, от которых воспринял различные доктрины материнской и
                отцовской тантры, а также наставления в Махамудре. Отличительным
                признаком Карджудпы является ее главное учение "шесть йог
                (доктрин) Наропы"<a href="#citation-9" id="ref-9"
                class="citation-mark"><sup>9</sup></a>. Со временем внутри Каржудпы выделились
                подсекты: Кармапа, Бригунба, Брукпа, Даглумба, которые имели
                свои небольшие монастыри. Эти секты, как и Нингмапа, с самого
                начала повернулись спиной к монастырям школьного типа, в силу
                чего не "афишировалась" принадлежность их к виджнянавадинской
                традиции.
            </p>
            <p>Школа Сакьяпа возникла в одно время с Каржудпой (свое название
                она получила от монастыря Сакья). У истоков этой школы стоит
                знаменитый Брогми-лоцзава (993- 1074 гг.), у которого в
                молодости учился санскриту Марба-лоцзава.</p>
            <p>
                Брогми-лоцзава передал доктрину лам-брэ (путь - результат)<a
                href="#citation-10" id="ref-10" class="citation-mark"><sup>10</sup></a> Кон Гончогчжалцану (1034-1102 гг.),
                основавшему монастырь Сакья в
                1073 г. В XIII в. эта секта уже имела школьный тип монастырей
                шадда на манер индийских монастырей-университетов. В философии
                сакьяпинцы придерживаются ярко выраженной линии виджнянавады. Со
                временем многочисленные сакьяпинские монастыри теряют дацаны
                шадда, а многие отшельнические монастыри поглощаются школой
                Нингмапа.
                Про четвертую школу - Гелукпа, оформившуюся в XV в., можно
                сказать, что она была преемницей школы Кадампа, обновленной,
                отибеченной ее формой. Основатель Гелукпы Цзонхава (1357-1419
                гг.) требовал, как и Атиша, строгого соблюдения монашеской
                дисциплины (безбрачия и сухого закона для служителей
                монастырей). Краткое (стихотворное) изложение сущности сутр и
                тантр Атишей - "Светильник на пути бодхисаттвы" у Цзонхавы
                получает развитие в двух объемных работах: "Ламрим ченмо" и
                "Агрим", в которых Цзонхава подробнейшим образом обосновывает и
                излагает всю совокупность путей, известных по сутрам и тантрам.
                Все свои работы Цзонхава создал на основе строгой
                Мадхьямики-Прасангики, которая осталась философской основой
                обучения в монастырях Гелукпы и по сей день. В настоящее время
                только в школе Гелукпа широко представлены монастыри обоих типов
                - отшельнического и школьного, причем в последних получили
                большое распространение дацаны (факультеты) цаннид -
                философские. Постепенно Гелукпа развивает институт далай-лам
                (светских и духовных правителей последователей этой школы, а
                начиная с V Далай-ламы и всего Тибета, ставшего с этого времени
                теократическим государством).
            </p>
            <p>
                Говоря о буддизме в Бурятии, надо иметь в виду, что на
                территории, где проживают буряты, это не первая форма буддизма.
                Известно, что эта территория входила в Тюркский каганат,
                образовавшийся в VI в. и затем быстро распавшийся на Северный и
                Южный. В Северном каганате (сосредоточившемся в районе озера
                Байкал) среди тюрков преобладали монахи из Турфана и северной
                части реки Тарим, которые придерживались махаянской формы
                буддизма. Уйгуры, завоевавшие Северный каганат, правили в
                Монголии и сопредельных районах с середины VIII до середины IX
                в. На уйгурский, как и на старый тюркский, были переведены
                некоторые буддийские тексты индийскими, согдийскими и китайскими
                монахами-махаянистами. От этой первой волны распространения
                буддизма в Забайкалье ни письменных источников, ни каких-либо
                других известных памятников не сохранилось (точнее сказать, они
                не известны), и хотя именно от уйгурского письма возникла
                монгольская письменность, это произошло во время второй волны
                распространения, причем незадолго до ее появления уже была
                сделана попытка выработать монгольское "квадратное письмо" по
                образцу тибетского. Авторами обеих систем письменности были
                сакьяские ламы. Во время движения второй волны буддизм получил
                распространение в среде знати, особенно при дворе хана Хубилая,
                который установил самые тесные отношения со школой Сакьяпа (хотя
                при ставке хана можно было встретить представителей школ
                Нингмапа и Каржудпа).
                Настоящее же распространение буддизма в Монголии началось
                одновременно с усилением школы Гелукпа при III-м Далай-ламе
                Соднам-чжамцо, которого в 1678 г. Алтан-хан Тумэтский пригласил
                к себе в Южную Монголию, где он первый получил титул Далай-ламы.
                С этого времени Гелукпа, а вместе с ней и весь тибетский буддизм
                делают настолько большие успехи, что уже в начале следующего
                века в Монголии МЫ обнаруживаем массу монастырей, в которых
                литературно-переводческая работа духовенства завершается
                изданием на родном языке почти всей священной литературы
                буддизма.
            </p>
            <p>
                Таким образом, к началу XVIII в., когда было зафиксировано
                официально<a href="#citation-11" id="ref-11"
                class="citation-mark"><sup>11</sup></a> распространение буддизма
                среди бурят, создались
                внешние и внутренние условия для его усвоения и развития. В
                соседней Монголии насчитывались десятки монастырей (особенно
                много было их в Халхе - Северной Монголии, т.е. непосредственно
                по соседству с бурятами), которые отличались от тибетских
                многочисленностью своего духовенства<a href="#citation-12"
                                                       id="ref-12"
                                                       class="citation-mark"><sup>12</sup></a>
                и нравами менее
                затейливыми, чем в тибетских монастырях. Подавляющим числом
                монастырей владела школа Гелукпа<a href="#citation-13"
                                                   id="ref-13"
                                                   class="citation-mark"><sup>13</sup></a>.
                Теснейшее переплетение
                политики монгольских ханов<a href="#citation-14" id="ref-14"
                                             class="citation-mark"><sup>14</sup></a>
                и высших иерархов Гелукпы привело к
                тому, что в Монголии (Халхе) в подавляющем числе монастырей
                господствовало именно это направление. Помимо желтошапочных
                (гелукпинских) монастырей (числом около 700) в Монголии было
                несколько монастырей школы Нингмапа, тогда как школы Сакьяпа и
                Каржудпа не были признаны официально, и эти направления
                существовали вне монастырской формы.
            </p>
            <p>Заканчивая беглый обзор пути проникновения буддизма в Бурятию,
                нельзя не упомянуть хотя бы вскользь о самой системе монастырей.
                В Тибете мы обнаруживаем монастыри различных типов. В основном
                они подразделяются на две группы: отшельнического типа (их было
                не менее тысячи) и школьного (не менее трехсот). Монастыри
                отшельнического типа, появившиеся в Тибете первыми, в Монголии
                не получили распространения<a href="#citation-15" id="ref-15"
                                              class="citation-mark"><sup>15</sup></a>.
                Определенную роль в этом сыграли
                реформа всей монастырской жизни, проведенная Цзонхавой, а также
                создание в XV в. в монастырях-университетах системы школьного
                изучения тантрийской символики учениками и последователями
                Цзонхавы Хайдубом Шэрап Сенге и Гунга Дондубом<a
                    href="#citation-16" id="ref-16" class="citation-mark"><sup>16</sup></a>. Что же
                касается монастырей школьного (университетского) типа, его
                школьнического аспекта (т.е. изучения догматики буддизма), то
                здесь мы видим устоявшуюся на протяжении нескольких столетий
                структуру воспроизведения энциклопедии буддизма или известной
                системы пяти отраслей буддийского знания<a href="#citation-17"
                                                           id="ref-17"
                                                           class="citation-mark"><sup>17</sup></a>.
                Постепенно в стенах
                больших монастырей сложилась специализация факультетов -
                дацанов<a href="#citation-18" id="ref-18" class="citation-mark"><sup>18</sup></a>. С небольшими вариациями, зависящими от
                размеров
                монастыря, традиций различных школ, выделяются следующие дацаны:
                по отрасли теологии, по ее разделу - философии утвердился
                Цанид-дацан (или шойра)<a href="#citation-19" id="ref-19"
                                          class="citation-mark"><sup>19</sup></a>
                с пятью отделами (курсами). Пять этих
                курсов в совокупности составляют 12-13 классов<a
                    href="#citation-20" id="ref-20" class="citation-mark"><sup>20</sup></a>. К Цанид-дацану
                тесно примыкает Ламрим-дацан, но встречается он, в отличие от
                Цанид-дацана, редко. Обучение на этом факультете длится 3-4
                года, и обязательно после окончания курса цанид-дацана. </p>
            <p>На основе второй части первой отрасли с XV в. получили широкое
                распространение Акпа-дацаны, обязательные во всех крупнейших
                монастырях. Здесь изучается тантрийская символика идамов,
                наиболее популярных в среде гелукпинцев: Сандуй, Демчог,
                Жигжед<a href="#citation-21" id="ref-21" class="citation-mark"><sup>21</sup></a>. К Акпа-дацану близко примыкают
                Дуйнхор-дацан, где
                помимо символики изучается санскрит и астрономия, и Жедор-дацан,
                встречающийся у гелукпинцев весьма редко<a
                    href="#citation-22" id="ref-22" class="citation-mark"><sup>22</sup></a>.</p>
            <p>В большинстве крупных монастырей распространены факультеты
                медицины (Манба-дацаны).
                Подводя итог беглому обзору пути буддизма в Бурятию<a
                    href="#citation-23" id="ref-23" class="citation-mark"><sup>23</sup></a>, следует
                отметить, что кроме таких же пересечений с шаманизмом, как в
                Тибете и Монголии, в самой Бурятии буддизм встретился с другой
                мировой религией - христианством (православием). И если во
                взаимодействии с национальными верованиями местного населения
                буддизм выработал универсальную систему вписывания-вложения
                местных культов в свою структуру<a href="#citation-24"
                                                   id="ref-24"
                                                   class="citation-mark"><sup>24</sup></a>,
                то в Бурятии (а она к этому
                времени вошла в состав России<a href="#citation-25" id="ref-25"
                                                class="citation-mark"><sup>25</sup></a>)
                буддизму при соприкосновении с
                православием пришлось опираться на косвенный опыт
                взаимоотношений с христианством, другими мировыми религиями и
                крупными конфессиями. Такой косвенный опыт сложился из контактов
                в странах юго-восточной Азии с индуизмом, куда он проник в
                незначительной степени и с которым у буддизма был самый крупный
                опыт взаимоотношений "у себя дома". В той же юго-восточной Азии,
                Китае и на Дальнем Востоке косвенный опыт пополнился в контактах
                с конфуцианством и даосизмом. Здесь же (в незначительной
                степени) состоялся контакт с христианством и исламом -
                настоящими мировыми религиями, тогда как конфуцианство и даосизм
                за пределами Китая не смогли утвердиться как мировые религии и
                сохранились в основном в среде хуацяо - этнических китайцев.
                Более полный опыт контактов с христианством и исламом у буддизма
                был на родине, в самой Индии и в странах, расположенных по
                "шелковому пути". Опираясь на этот косвенный и прямой (с
                христианством вообще<a href="#citation-26" id="ref-26"
                                       class="citation-mark"><sup>26</sup></a>)
                опыт взаимоотношений буддизм сумел
                преодолеть гонения и весьма энергичное сопротивление<a
                    href="#citation-27" id="ref-27" class="citation-mark"><sup>27</sup></a>
                православного миссионерства и утвердиться среди бурят
                Забайкалья<a href="#citation-28" id="ref-28"
                             class="citation-mark"><sup>28</sup></a>.
            </p>
            <p>
                При анализе библиографий работ по истории буддизма в Бурятии
                нельзя не обратить внимания на то, что, из бурятской
                историографии и других местных оригинальных источников авторы
                избирают в основном летописи (общего характера) и архивные
                материалы<a href="#citation-29" id="ref-29"
                            class="citation-mark"><sup>29</sup></a>, реже
                привлекают
                различные надписи, каменные рельефы
                в местах поклонения верующих, а также устную традицию. Особый
                интерес на фоне таких первоисточников вызывают исторические
                хроники, составленные в среде ламаистского духовенства. Одна из
                них была обнаружена в рукописном отделе БИОН и была переведена
                на русский язык (с тибетского) Б.Д. Дандароном. Рукопись
                охватывает события с 1648 по 1852 г. Она подтверждает, что в
                начале распространения буддизма среди бурят существенную роль
                играли монголы: "Тогда они (хоринские буряты) поклонялись
                различным духам - онго-нам (ong kvong) ("онгод" дословно -
                "шаманские идолы". - Прим. Перев.). Именно в это время
                здравствовал Джибцзан-Лобсан-Дамби-Джалцан (первый ургинский
                хутухта Лубсан-Дамба-Джалцан (1636-1723 гг.), известный под
                прозвищем Ундэр-гэгэн. - Прим. перев.).
                В связи с большой смутой, посеянной в 1676 г., из Урги прибыли
                сюда 150 тибетско-монгольских гэлунов, которым народ начал
                поклоняться как представителям драгоценной и высокой религии.
                Ими же был создан Цонгольский дацан..."<a href="#citation-30"
                                                          id="ref-30"
                                                          class="citation-mark"><sup>30</sup></a>
                [Дандарон .
                Из истории..., с. 3]. Далее сказано: "Для распространения
                религиозного учения лама Чойджи Агван-Пунцок, тибетец из Джонан
                (со neing) основал [должность] главы желтой религии (ser mo ba
                kun kyi mgo mdzad pas - тибетское выражение, означающее "глава
                желтой религии". - Прим. перев.) в монастыре Цонгол
                брай-бун<a href="#citation-31" id="ref-31"
                           class="citation-mark"><sup>31</sup></a> в
                стране русских. С того времени до настоящих пор эту должность
                продолжают занимать пандиты Хамбы" [Там же, с. 4]<a
                    href="#citation-32" id="ref-32" class="citation-mark"><sup>32</sup></a> . Монголов
                же мы видим среди первых настоятелей других монастырей: ширетуй
                (настоятель) Анинского дацана (имя его автор хроники не
                сообщает; известен только его ученик Лобсан-Дандар, бывший
                недолгое время также настоятелем этого дацана и которого за
                появление в нетрезвом виде в конце сагаловки (15-го числа белого
                месяца) в дацане (прибыв в дацан для чтения молебна "нгоба"
                (bsngo ba), совершить службу не смог) отстранили от должности и
                назначили "ламу Пунцок (Phun tshogs - о нем не упоминается в
                хоринских летописях. - Прим. перев.), который стал настоятелем
                Кудунского и Анинского дацанов" [Там же, с. 4].
            </p>
            <p>
                Другими монголами-настоятелями были Лама Дондуб - «известный по
                прозвищу "монгольский высокий лама" (hor ung dur bla mar grags
                pa - первый ширетуй Кижингинского дацана, он известен как первый
                распространитель буддизма среди хоринцев. - Прим. перев.)» [Там
                же, с. 2]; Жамьян-Сунраб, «"лама из Халхи" ('Jam dbyangs gsung
                rab-hal hai - 3-й настоятель Кижингинского дацана. - Прим.
                перев.)» [Там же]; лама Лобсан-Шираб в Кудунском монастыре [Там
                же] и другие. "Духовное освящение" (вновь отстроенного после
                пожара) Кудунского дацана сделали монгольские ученые ламы во
                главе с Ендон-гэлоном" (hot mer gan bla та Yon lan dge slong - о
                нем не упоминается в хоринских летописях. - Прим. перев.) [Там
                же, с. 3].
            </p>
            <p>
                Со временем, по мере увеличения числа высокообразованных и
                высоконравственных лам среди бурят и в особенности после
                официального обретения независимости от монгольской церкви,
                настоятелями монастырей (и вообще "высшими" ламами) становились
                выходцы из местных лам: "В те далекие времена, кроме молебствий,
                читаемых в период больших праздников, еще не проводилось
                ежедневное чтение проповедей и слушание их в соответствии со
                способностями воспринимающего разума. Однако, благодаря
                авторитету высших лам, рядовые ламы могли постепенно стать
                образованными. Правда, появилось несметное количество лам:
                ученые, познавшие степени обычных путей спасения, великие йогины
                (отшельники), практикующие тантрийские пути; ученые, познавшие
                основу цанида (mtshan nyid; первый философский факультет в
                Бурятии был организован при Цугольском дацане в 1845 году. -
                Прим. перев.) и овладевшие методами обдумывания и слушания
                проповедей; и ламы, познавшие медицину, искусства и астрономию"
                [Там же, с. 5].
                Одним из признаков высокого развития буддийской культуры явилось
                местное издание литературы. Отдельные работы, напечатанные с
                ксилографических досок, издавались многими бурятскими
                монастырями. Примерно в 1860 г. в Агинском монастыре
                устанавливается печатный станок для ксилографического печатания
                буддийских текстов на тибетском и монгольском языках, после чего
                монастырь становится издательским центром Бурятии. Широко
                известен каталог книг, напечатанных в этом монастыре.
            </p>
            <p>
                Другим признаком (еще более) высокого развития стало издание
                самостоятельных работ. В статье Б. Д. Дандарона "Aga monastery
                (Aginski Datsan)" мы читаем: "Оно (издательство) также
                напечатало оригинальные работы бурятских ученых-монахов. Эта
                литература публиковалась на двух языках - тибетском и
                монгольском... Известно, что некоторые ламы монастыря написали
                (rtsom) самостоятельные работы на тибетском языке: работы по
                системе Кала-чакра Данзана Жарбаева; учебник для монастырского
                философского факультета Соднома Чжамсо (который сам учился на
                философском отделении Агинского дацана и получил степень
                рабчамбы в монгольском монастыре Бадгар) под названием "Mtshod
                bya don gyi dge 'dun nyi shu'i rnam bzag" ("Повествование о
                двадцати ламах, следовавших по пути мудрости"), которое
                использовалось как руководство по классу Праджня-парамита;
                учебник по философии Мадхьямики; работа о понятии Шуньята в
                философских системах Мадхьямика и Йогачара.
            </p>
            <p>
                В 1867 году ширетуй Тугулдуров написал на тибетском языке книгу
                по астрономии и основал астрономический факультет.
            </p>
            <p>
                Среди авторов и переводчиков, которые писали на монгольском
                языке, первое место принадлежат ширетую Лобсан Дорже Данжинову.
                Он был известен как переводчик и издатель книг на бурятском
                языке, расчитанных на самый широкий круг читателей. Он
                опубликовал биографии высокочтимых в Монголии буддийских
                деятелей, популярные книги по этике, монгольские буквари,
                грамматики, собрания джатак, песни Миларайпы и был одним из
                авторов цикла книг по этике, изданного в 1892 г.
                Как переводчик и издатель широкой известностью пользовался
                Дылгыров; особенно известны его переводы руководств по
                философии, таких как "Chu'i bstan bcos", "Shing gi bstan bcos",
                и комментарий на "Ламрин-чен-мо" [Дандарон, 1961, с. 249-250].
            </p>
            <p>
                Опуская период с середины 30-х годов, когда было разрушено
                подавляющее большинство из более чем сорока дацанов, и до
                открытия двух небольших дацанов в 1948 г. (Иволгинский под
                Улан-Удэ и Агинский в Агинском н.о. Читинской области), можно
                сказать, что это частичное восстановление (структурное и
                функциональное) монастырей было неполным.
            </p>
            <p>
                Можно долго перечислять, что не было восстановлено в монастырях
                (не было ни одного факультета, ни типографии, ни одной
                мастерской и т.д.). До 1970 года<a href="#citation-33"
                                                   id="ref-33"
                                                   class="citation-mark"><sup>33</sup></a>
                не было
                возможности готовить
                смену уходящему поколению лам. Проще сказать, что вся дацанская
                деятельность была ограничена ежегодными календарными
                хуралами<a href="#citation-34" id="ref-34"
                           class="citation-mark"><sup>34</sup></a>
                и более мелкими ритуальными службами (малые хуралы), а также
                выполнением различных просьб мирян.
            </p>
            <p>
                В настоящее время в Бурятии, Калмыкии, Туве открываются новые
                дацаны, восстанавливаются (ремонтируются) сохранившиеся кое-где
                старые; во многих крупных городах России, Украины, Прибалтики
                образовалось множество буддийских групп и общин, как
                зарегистрированных официально под покровительством ЦДУБ
                (Центральное духовное управление буддистов) или других центров,
                так и существующих стихийно.
            </p>
            <p>
                В этой пестрой среде не сформировавшихся полностью,
                рассыпающихся и вновь возникающих в других комбинациях
                группировок можно обнаружить интерес ко всем направлениям
                буддизма. Естественно, на этом фоне не мог не возникнуть вопрос
                о соотношении различных школ и вытекающий отсюда отдельно вопрос
                о так называемом высшем учении.
            </p>
            <p>
                Не избежала проявления такого внимания и сложная форма буддизма
                в Бурятии. Утверждается широко распространенная точка зрения на
                эту форму как исключительно гелукпинское направление в его
                консервативном монастырском варианте и поэтому уступающее другим
                направлениям (таким, например, как Каржудпа-Кармапа, Нингмапа).
                Несомненное влияние на такой расклад интереса оказал наплыв в
                70-90-е годы англоязычной литературы. Авторы этих произведений -
                в подавляющем большинстве эмигрировавшие тибетцы, осевшие не
                только в Индии, но и в некоторых странах Западной Европы и
                Северной Америки.
            </p>
            <p>
                Работы этих авторов, представителей практически всех направлений
                тибетского буддизма, переведенные (но не интерпретированные, как
                это было ранее, известными европейскими буддологами) по горячим
                следам на европейские языки (на русский, как уже упоминалось
                выше, переводы осуществлялись с английского), внесли безусловно
                свежую струю. Прежние буддологические работы естественным
                образом уступают произведениям тибетцев как по простоте
                изложения, так и по конкретике, по привлечению массы неизвестных
                (в работах буддологов) до сих пор деятелей, создающих совершенно
                новый стиль изложения, близкий к разговорному языку и
                сохраняющий в какой-то степени первоначальный аромат, столь
                ценимый современным читателем.
                Но на этом фоне новой переводной литературы помимо простительных
                оплошностей, а порой и явных ляпсусов (сделанных, очевидно,
                небрежными переводчиками) начинают проглядывать, наряду со
                старыми укоренившимися ошибками переводчиков<a
                    href="#citation-35" id="ref-35" class="citation-mark"><sup>35</sup></a>, новые. Причем
                большинство ошибок, как ни странно, связано не столько с
                переводом (ведь зачастую работы тибетских авторов записываются
                сразу на европейских языках), сколько с установкой автора на
                западную аудиторию. По своему характеру эти ошибки можно свести
                в две группы. Одна группа формируется ожиданием публики
                необычного, некой экзотики, и в силу сложившегося стиля жизни
                (западного общества) подача этого материала непременно должна
                проходить наподобие рекламы высочайшего класса. С другой
                стороны, явно желание автора подчеркнуть особенности своей школы
                (традиции), которое легко соединяется с упомянутым ожиданием
                публики, что и приводит к "объявлению" так называемого "высшего
                направления" (учения), со всеми сопутствующими передержками.
                Вторая группа ошибок также связана с ориентацией на уровень
                культуры западного общества, у которого многие естественные
                проявления сознания вытеснены (или ослаблены) механическим
                симбиозом с миром машин, машинной технологией и т.д., и в
                результате, например, ясное слово thugs rje - "сострадание"
                превращается на западно-европейских языках в "энергию" (хотя до
                сих пор было известно только о возможности такого превращения
                материальной массы).
                Более или менее последовательный анализ работ такого рода
                представляется приемлемым провести на примере сочинений широко
                известного представителя школы Дзогчен Намхэй Норбу<a
                    href="#citation-36" id="ref-36" class="citation-mark"><sup>36</sup></a>. Если
                оставить в стороне его ранние работы (например, "Беседы в
                Конвее"), где часто чисто механически повторяется утверждение об
                уникальности школы Дзогчен, о том, что это высшее (или наиболее
                глубокое) учение, и остановиться (если так можно его определить)
                на среднем периоде творчества Намхэя Норбу, то можно заметить
                исчезновение этих механических повторов, а главное - усиление
                стремления обосновать утверждения об уникальности и высшей
                природе учения Дзогчен.
                Такое обоснование подается на фоне очень схематичного изложения
                всего учения Дзогчен. Выдвигается совокупность признаков,
                присущих якобы исключительно этому направлению, а именно: (1)
                практика Дзогчен "работает" на уровне ума, а не речи или тела,
                как в других направлениях; (2) основой для вхождения в Дзогчен
                является "введение", а не трансформация и очищение, как,
                например, в тантре. Из этого выводятся другие отличия: то, что
                путь Дзогчена в отличие от путей других направлений - это не
                постепенный путь; то, что на этом пути практикующие Дзогчен не
                нуждаются в каких-либо правилах и методах, а специальным методом
                для них является самоосвобождение (недвойственное
                самосозерцание). Конечный результат, утверждается также,
                совершенно отличается от реализаций, достигаемых на путях
                усвоения Сутр и Тантр. При этом смешиваются (видимо, опять же в
                силу желания убедить все школы буддизма, что Дзогчен не просто
                "высокое учение, и даже самое высшее", но и что "эти школы
                должны признать,... что Дзогчен даже слишком высок, он за
                пределами способности обычных индивидуумов..." [Норбу, 1982, с.
                79], а также, "что это сверхтайное учение" [Норбу, "Беседы..."
                с. 156]) характеристики пяти высших возможностей, необходимых
                для практики Дзогчена. Н. Норбу справедливо указывает, что этой
                же точки зрения придерживаются Тантры и тут же добавляется, что
                "это точка зрения... даже уровня Сутр" [Там же, с. 28].
            </p>
            <p>
                Речь идет o dbang po lnga (пять индрий-способностей), которые,
                как известно, входят в 37 направлений бодхисаттовской мысли, и
                правомернее было бы говорить, что эта точка зрения возникла в
                среде махаянистов несколько ранее середины I в. н.э. (времени
                появления учения Дзогчен). Следовательно, можно утверждать и
                обратное: этой точки зрения (т.е. трактуя пять
                индрий-способностей как высшие возможности) придерживается даже
                Дзогчен.
            </p>
            <p>
                Но суть не в этой подмене. Приступая к описанию пяти
                характеристик, Н. Норбу сталкивается сразу с наиболее
                парадоксальной характеристикой - dad (вера), которую он
                трактует, как участие, желание<a href="#citation-37"
                                                 id="ref-37"
                                                 class="citation-mark"><sup>37</sup></a>.
                Действительно,
                парадоксальность этой характеристики вытекает из емкости веры.
                Возможно, "участие", как его описывает Н. Норбу, и входит в
                веру, но вера никоим образом не сводится ни к участию, ни тем
                более к желанию, ни к ним обоим вместе; в веру входят и другие
                характеристики из этого пятиричного набора, а именно: четвертая
                - самадхи (ting nge 'dzin) и пятая - интуиция (shes rab), и в
                этом парадоксальность веры.
            </p>
            <p>
                Далее, говоря о второй характеристике - "старании", Н. Норбу
                указывает, что (1) надо быть последовательным в своем участии и
                (2) "не колебаться в собственном обязательстве" [Норбу, 1982, с.
                80]. Совершенно справедливы слова о последовательности и
                непоколебимости. Несостоятельность трактовки первого элемента
                как "участия" станет еще яснее, если проанализировать заявления
                Н. Норбу об обетах и обязательствах. Что здесь имеется в виду?
                Н. Норбу говорит, что существуют два взгляда на путь Дзогчена, а
                именно: путь в тантризме в лице ануйоги и атийоги (Дзогчен) и
                путь (за рамками тантризма) собственно Дзогчена. В тантризме, в
                методе следования "пути трансформации есть и признание
                совершённых ошибок" [Беседы..., с. 260], в момент которого и
                берутся обеты и обязательства. Так вот, в собственно Дзогчене,
                утверждает автор, нет ни обещаний, ни обетов - ничего. Осознание
                же того, что нет обетов, обязательных правил, является в свою
                очередь разновидностью обязательства, которое постоянно
                необходимо выполнять, т.е. это нечто редуцированное, вторичное.
            </p>
            <p>
                Действительно, такой контекст в тантризме существует (ошибки -
                обеты), но он есть и в "собственно" Дзогчене. Так, Нацог-Рандол,
                известный мастер Махамудры и Дзогчена, закончив перечисление
                своих учителей по линии Дзогчен, говорит: "Таким образом, эта
                драгоценная традиция идет без перерыва от самого Реального
                Будды, и поэтому благословенное наставление не прерывалось.
                Поскольку тут свидетелями (непрерывности) являются традиция
                (rgyud), посвящение (lung) и наставление (man ngag), постольку
                она не причастна грехам (грязи) самовольного, собственного
                конструирования. В ней чиста клятва (dam tshig) (курсив мой. -
                А.Ж.), традиция без перерыва, поэтому ей не присущи грехи тех,
                которые из-за нарушения обетов попали под влияние мары"
                [Нацог-Рандол, л. 38]. Но такой же контекст мы обнаруживаем в
                работах по Махамудре: "Убеждение, освобожденное от правильности
                и неправильности, не ограниченное пределами изучения и освоения,
                это - наставление Учителя. <em>Безошибочное созерцание без
                    ослабления. Путь без страстей, неподверженный ошибкам,
                    клятва,
                    не допускающая отклонения</em>, связывают плод, желаемый
                небожителями
                (богами). Например, как основной состав лекарства,
                пропорционально сконцентрированный, дает лекарство высшего
                качества" (курсив мой. - А. Ж.) [Б. Д. Дандарон, 1995, с. 23].
            </p>
            <p>
                Как уже заметил читатель, тексты и Дзогчена и Махамудры говорят
                скорее о том, что если есть (дана) клятва, то нет и ошибок, а не
                наоборот: признаются ошибки, а затем берутся обеты и
                обязательства. Получается как в анекдоте: я хозяин своего слова:
                сам дал клятву (dam tshig), и сам взял (забрал назад) обет (dam
                tshig). В чем же дело? Ответ находим у своих учителей. Мастер
                Махамудры Годцанпа (rGod tshang pa) пандит говорит: "Все области
                сосуда Виная собраны в учениях о высшем нравственном обете, а
                сам нравственный обет собран в истинной вере<a
                    href="#citation-38" id="ref-38" class="citation-mark"><sup>38</sup></a>. В период
                "проявления истинной веры и чтения молитвы Учителю перестают
                проявляться гнев и по причине этого очищаются признаки гнева.
                Таким же образом очищаются, подавляются все виды клеш"
                [Махамудра..., с. 11]. Далее, выделив пять особенностей
                Махамудры, Годцанпа говорит о третьей: "особенность [ее] в том,
                чтобы не принимать за неправильность соблюдение клятвенных
                обетов И постоянно не (?) прерывать неправильность неотступным
                сознанием" [Там же ].
            </p>
            <p>
                И здесь мы подходим к центральной точке наставлений Н. Норбу
                <a href="#citation-39" id="ref-39" class="citation-mark"><sup>39</sup></a>.
            </p>
            <p>
                Если наложить ее на упрощенную схему тантрийской системы (три
                внешние или нижние тантры: крийя, чарья (упайя) и йогатантра;
                три внутренние или высшие: в школе Нингмапа они называются
                махайога, ануйога, атийога; в трех других новых школах махайоге
                соответствует ануттарайогатантра, а ануйоге и атийоге - жедрим и
                зогрим, т.е. степени развития и завершения ануттарайогатантры,
                хотя Н. Норбу это соответствие не отмечает), то кратко это можно
                описать так: принцип знания ануйоги основан на самосовершенстве
                (lhun sgrub) и в своей высшей реализации - атийоге ануйога
                достигает Дзогчена. Обеим йогам присущ непостепенный путь, тогда
                как махайога и ануттара в целом практикуют постепенный путь, а
                кульминация реализации называется Махамудрой, совпадающая с
                Дзогченом. Утверждается, что в Махамудре, как и в тантризме, в
                целом нет ничего, что не развивалось бы через путь
                трансформации. И лишь собственно Дзогчен (не относимый к
                тантризму, который работает на уровне речи или так называемой
                энергии<a href="#citation-40" id="ref-40" class="citation-mark"><sup>40</sup></a>) в силу того, что в него заложен принцип
                самоосвобождения, действует на уровне ума, а следовательно, это
                самый быстрый (прямой) путь к реализации. Здесь основополагающим
                является понимание изначального состояния личности, в которое
                непосредственно вводится ученик учителем Дзогчена одним из трех
                способов (прямое введение, символическая и устная передача). Это
                изначальное состояние (основа) характеризуется как обладающее
                сущностью (ngo bo), природой (rang bzhin) и энергией (thugs
                rje)<a href="#citation-41" id="ref-41" class="citation-mark"><sup>41</sup></a>, проявляющейся тремя способами, для
                которых существуют
                такие символы: 1) ролпа (rol pa), что иллюстрируется хрустальным
                шаром, внутри которого можно увидеть изображения внешних
                объектов; 2) данг (gdangs), что иллюстрирует прозрачный
                хрустальный шар, помещенный на какой-либо цветной фон, цвет
                которого принимает и шар; 3) цал (rtsal), - что символизирует
                прозрачный кристалл, который, когда на него падает солнечный
                свет, отражает его и преломляет в виде радуги. Перечисляет эти
                способы проявления "энергии" личности Н. Норбу двояко (цал,
                ролпа, данг или данг, ролпа, цал), но это безразличие к порядку
                перечисления допустимо, если не делать попытки перевести три
                термина и не представлять их как разновидности "энергии".
            </p>
            <p>
                Нацог-Рандол в "Карнатантре" приводит следующий порядок этих
                трех моментов (трех аспектов) в "правилах развития", в
                "радостях<a href="#citation-42" id="ref-42"
                            class="citation-mark"><sup>42</sup></a> сострадания": rtsal - "интенсификация"
                (или
                "динамика существования"); rgyan - "украшение"<a
                    href="#citation-43"
                    id="ref-43" class="citation-mark"><sup>43</sup></a> (или его
                собственная волна, мотив - gdangs); rol pa - "радость"
                [Нацог-Рандол, л. 25].
            </p>
            <p>
                В изложении "способа становления сущности-основания" [Там же, л.
                20] и "правила развития ... сострадания" [Там же, л. 25 ]
                Нацог-Рандол для характеристики rtsal, rgyan (gdangs) и rol pa
                использует несколько десятков иллюстрирующих примеров (mtshon
                deb)<a href="#citation-44" id="ref-44" class="citation-mark"><sup>44</sup></a>.
                Для нас интересны две группы сравнений. Одна из них
                концентрируется вокруг слова shel (хрусталь): man shel (белый
                хрусталь), shel gon (хрустальный шар), shel pa'i me long
                (хрустальное зеркало). Другая группа - пустое небо, небесные
                облака и дождь. В "Тантре проповеди" (bShad rgyud) сказано: "То,
                что дхармакае присуща мудрость с ясностью внутри, похоже на тот
                хрусталь (shel), внутренность которого как бы освещена пятью
                светами... Примеры реальности, содержания и сострадания таковы:
                они похожи на ... небесные облака и на дождь, или похожи на
                белый хрусталь (man shel)". Небесные облака и их дождь означают,
                что сострадающий разум похож на пустое небо, отсюда радость (rol
                pa) основания, скопившегося как облако, похожа на дождь.
                Хрусталь - это пример того, что все [что сказано выше]
                неразделимо и сконцентрировано в одну радость" [Там же, л.
                21]<a href="#citation-45" id="ref-45" class="citation-mark"><sup>45</sup></a>.
            </p>
            <p>
                И тут мы подошли к самой сути интересующего нас вопроса - как
                обстоит дело с этим материалом в тантризме, и в частности в
                Махамудре. Обратимся к одному из первых (а возможно, и первому,
                если не считать самого Шакья-Муни) учителей Махамудры - Сарахе.
                Так же как Манчжушримитра<a href="#citation-46" id="ref-46"
                                            class="citation-mark"><sup>46</sup></a>
                изложил (и тем самым сделал
                возможным изучение и более широкую практику реализации) в своих
                работах учение Дзогчен Гараба Дорже, Нагарджуна, любимый и
                известнейший ученик Сарахи, сохранил для нас квинтэссенцию
                учения Махамудры в стихотворной форме (так называемых Доха -
                песнях), спетых ранее народу, царице и царю.
            </p>
            <p>
                Комментатор Доха Жемед Дечен (sKyes med bde chen)<a
                    href="#citation-47" id="ref-47" class="citation-mark"><sup>47</sup></a> в своей
                работе «Светильник, освещающий смысл "Песни о человеческой
                деятельности - сокровище Доха"» сразу вводит нас в суть
                занимающего нас вопроса: "Хвала превосходному Манджушри
                Кумарабхута! Я приветствую Учителей прошлого, которые открыли
                истинную трансцендентную имманентность (lhun grub) трех тел
                (дхармакаи, самбхогакаи, нирманакаи) в трех аспектах (gshis,
                gdangs, rtsal), свободную от трех определений, в царство
                Махамудры, которое есть немышление, сфера высшего блаженства,
                свободная от сведения к одному значению" [Цит. по: Гюнтер, 1969,
                р. 76-77]).
            </p>
            <p>
                Далее Жемед Дечен говорит: "Никто не смеет полностью записывать
                то, что преподано изустно, поэтому я записываю только то, что
                преподает и разрешает записывать мой Учитель" [Там же, р. 791.
                Поэтому, констатировав полную идентичность объяснения трех тел
                (chos sku, longs sku, sprul sku) тремя аспектами (rnam pa gsum):
                gshi, gdangs, rtsal и иллюстрацией их "Дохами"<a
                    href="#citation-48" id="ref-48" class="citation-mark"><sup>48</sup></a>, укажем на
                некоторые другие параллели, а по существу на полную идентичность
                Махамудры и Дзогчена.
                Прежде всего речь пойдет о якобы исключительной принадлежности
                способа "посвящения" в Дзогчен - о прямом введении, о
                специальном методе - самоосвобождении (rang grol).
                В сочинении "Phyag rgya chen po lhan cig skyes sbyor zhes pa'i
                don 'chad" вслед за Сарахой читаем: "Махамудра не показывается
                Учителем, не осваивается учеником (blа mas bstan du med slob mas
                go ru med pa)", не обладает видом (nyams) и чувством вкушания
                (myong ba), не загораживается уверенностью (nges shes), не
                разделена по убеждениям, по сосредоточению и путям
                совершенствования. Не подвластна ни основанию (gzhi), ни путям
                (lam), ни результатам (bras). ... Она прочно привязана к
                элементам Сансары и Нирваны. Прочно привязана к путям (grol
                lam), преодолевает раскрытие самого себя. Ни разу не делает
                попытку исправить (bcos) что-либо по природе и по признакам.
                Познавая самое себя в собственном пространственном искривлении,
                она уходит из Сансары (rang grol - самоосвобождается. - А. Ж.),
                и поэтому ее назвали Махамудрой" [Махамудра, л. 5].
            </p>
            <p>
                Спрашивается, если Махамудра не показывается Учителем и не
                осваивается учеником, то как возможно вхождение-введение в
                Махамудру? В сочинении "Zhes sogs" говорится по этому поводу:
                "Через глубокую и истинную веру в Дхармакаю нужно осуществить
                соединение моего сознания (sems) с сознанием Учителя (thugs) и,
                добиваясь тождества (dbyer med) этих двух сознаний, необходимо
                освободить сущность своего сознания от рождения и препятствий"
                [Махамудра, л. 9]. Такая вера в Дхармакаю есть концентрация
                девяти аспектов mos pa - веры: трех сосудов (или трех учений -
                bslob pa gsum), трех низших (og ma gsum) тантр, а также
                ануттарайогатантры, жедрима и зогрима, каждого по отдельности
                входящего в подлинную веру (mos pa). Эта концентрация есть "так
                называемое восточное направление, идеальное совпадение ваджрных
                концов, находится в пространстве, которое недоступно для других"
                [Нацог-Рандол, л. 8]. Этим девяти аспектам (или девяти разделам
                ганджура - gsung gi sde dgu) соответствуют девять ступеней (rim
                dgu) в Нингмапе. Такая концентрация есть вера (dad pa)<a
                    href="#citation-49" id="ref-49" class="citation-mark"><sup>49</sup></a>.
            </p>
            <p>
                "Прямое введение" и "самоосвобождение" действительно связаны
                напрямую. И так же как "прямое введение" обнаруживается в
                Махамудре (соединение двух сознаний - "моего" и Учителя), точно
                так же обнаруживается "самоосвобождение" в самой проповеди о
                названиях Махамудры. Дагпо-ринпоче (Dvag po rin po che) прямо
                вводит этот специальный метод в определение Махамудры (phyag
                rgya chen po): "...рука (phyag)-мудра означает шуньяту видимой
                Сансары и Нирваны, она не отличима от них ввиду бессмертности,
                [необходимо] познавать ее как не превосходящую их, поэтому она
                мудра. Обычная видимость не превосходит своего первоначального
                содержания, поэтому она называется печатью (rgya). Сущность ее
                <em>самоосвобождается</em> (ran grol; курсив мой. - А. Ж.), и
                таковой она познается; поэтому ее называют великой - маха"
                [Махамудра, л. 23].
            </p>
            <p>
                И, наконец, о "постепенности", по Норбу, пути Махамудры. Если
                следовать Н. Норбу, то становится очевидным, что,
                "самоосвобождение" на пути Махамудры является непостепенным. Это
                подтверждают подразделения Махамудры на: (I) lta ba - убеждение
                (мировоззрение), (2) sgom pa - созерцание, (3) nyams myong -
                анализ, (4) spyod pa - совершенствующее деяние, 5) bras bu -
                плод; они же пять особенностей Махамудры. Эти пять
                подразделений-особенностей есть печать мандала Учителя. Они
                соотносятся с пятью дьяни-буддами (пятью rigs): Акшобхьей,
                Вайрочаной, Ратнасамбхавой, Амитабхой и Амогхасиддхи. И сам
                вопрос разрешается скорее не в плане наличия или отсутствия
                изначальной свободы - ye(nas) grol или изначального
                самосовершенства (lhun grub), а в плане естественных
                способностей ученика. Ведь совокупность пяти способностей (dbang
                po lnga), необходимых для практики Дзогчена, также отмечена
                печатью мандала, где Акшобхья при перечислении передвигается с
                первого упоминания на последнее, оставаясь при этом в центре
                мандала. Другое дело, когда мы определяем характер пути, глядя
                через призму классического деления на три типа личности: низший
                (skyes bu chung ngu), средний (skyes bu 'bring pa) и высший
                (skyes bu chen po). По-другому эти типы личностей
                характеризуются как ученики слабых (rtul), средних ('bring) и
                высших (rnon) способностей, иногда сводимых к двум - слабым и
                высшим. И такое подразделение можно обнаружить на любой из rim
                dgu - девяти подразделов буддийского учения. Так, с этой точки
                зрения различают две разновидности архатов, т.е. архатов,
                изживших свои заблуждения путем постепенного метода (обладающих
                слабой способностью - dbang rtul) и архатов, освободившихся
                одновременно от всех заблуждений (т.е. обладающих высшей
                способностью). Опираясь на такое деление по способностям,
                Шан-ринпоче говорит: "Индивиды со слабыми (tha ma) способностями
                стараются совершенствоваться по коренному пути, средние ('bring)
                осуществляют систему методов действия праны по
                нервно-психическим каналам. Индивиды с высшими (rab)
                способностями созерцают вместерожденное единство и Махамудру"
                [Махамудра, л. 29 ].
            </p>
            <p>
                Итак, завершая анализ того, что есть высшее учение, мы можем
                утверждать: "Обычно существуют различные названия, как-то:
                махамудра, мадхьямика, великое завершение (rdzogs chen),
                праджняпарамита, [но] на деле они имеют одну и ту же сущность"
                [Там же, л. 1 ], или, как говорил Сараха:
            </p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <div class="flex justify-center">
                    <ul class="list-none pl-5 space-y-1 ml-8">
                        <li class="my-2">Так же, как вода, возмущенная ветром,</li>
                        <li class="my-2">Превращается в волны и валы,</li>
                        <li class="my-2">Так и о Сарахе думает по-разному царь,</li>
                        <li class="my-2">Хотя один человек Сараха.</li>
                    </ul>
                </div>
            </blockquote>
            <p class="text-right italic text-gray-600 mt-4">
                [Гюнтер, 1969, р. 63].
            </p>

