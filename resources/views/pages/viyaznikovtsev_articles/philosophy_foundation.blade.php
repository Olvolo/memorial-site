            <p>Проблема передачи индо-буддийской традиции семиотическими средствами иных культур
                успешно разрешалась в течение всего периода распространения буддийской Дхармы на
                азиатском материке. В результате в различных странах Азии сформировались
                оригинальные формы и традиции буддизма, сохраняющие (хотя и в разных видах) суть и
                дух первоначальных учений. В наше время (прежде всего благодаря усилиям тибетской
                диаспоры) буддизм всё глубже проникает в духовную жизнь Запада, поэтому всё более
                интересной становится тема диалога этих двух культур, в котором совершается
                осознание буддийских идей как основы компаративного мышления, снимающего различия
                временных и культурных контекстов. Эта «компаративная философия представляет собой
                нечто большее, чем сравнение разных философских традиций..., она ...идентифицирует
                себя как особый род интеллектуальной деятельности, отвечающей современному уровню
                взаимопонимания "восточных" и "западных" философов» [Лысенко, с. 146]. Поэтому в
                данной статье мы попытаемся осветить некоторые аспекты возможного диалога западных
                философских идей и идей позднего буддизма.</p>
            <p>Принципиальной особенностью буддийской философской традиции, начиная с феноменологии
                Абхидхармы, является рассмотрение сознания как единственной реальности, имеющей для
                человека абсолютную сотериологическую ценность<a href="#citation-1" id="ref-1"
                                                                 class="citation-mark"><sup>1</sup></a>.
                Один из учителей Ваджраяны,
                принадлежавший к школе Кагью<a href="#citation-2" id="ref-2"
                                               class="citation-mark"><sup>2</sup></a>, Ритод-ванчуг
                (ri-khrod dbang-phyug) сформулировал её
                идеи предельно точно и лаконично: «Все познаваемое (букв.: все дхармы, chos-rnams
                thams-cad) есть мое сознание. Кроме самого сознания, не существует никаких иных
                составляющих его познаваемых элементов» [Phyag-rgya, 5]. Иначе говоря, ничто
                сотериологически значимое не выходит за пределы сознания. Именно поэтому, употребляя
                традиционную метафору, можно сказать, что состояние Будды раскрывается изнутри,
                подобно цветку лотоса, и (согласно буддийской точке зрения) ошибочно его искать вне
                пределов собственного сознания<a href="#citation-3" id="ref-3"
                                                 class="citation-mark"><sup>3</sup></a>. Существует
                известная формула, иногда приписываемая
                Мачиг Лабдон, основательнице системы чод<a href="#citation-4" id="ref-4"
                                                           class="citation-mark"><sup>4</sup></a>:
                «Мое собственное сознание (rang-sems) и
                есть Будда» [Ibid., 6].</p>
            <p>Однако ясное и непосредственное в<em>и</em>дение (rang-gsal) этой
                реальности обретается в особом медитативном переживании, исполненном блаженства
                (rang-bde) и именуемом на санскрите свасамведа (rang-rig), которое можно описать как
                внеконцептуальное состояние сознания, вернувшегося к самому себе [Tsepak Rigzin, р.
                392]. По-видимому, одним из философских оснований подобного сотериологического
                подхода служил феноменологический анализ Абхидхармы<a href="#citation-5" id="ref-5"
                                                                      class="citation-mark"><sup>5</sup></a>,
                редуцировавший любую наивную
                онтологизацию «субъективных» и «объективных» элементов переживающего мир и самого
                себя сознания (snang-ba-la dngos-por 'dzin ум, воспринимающий феномены как нечто
                реальное). В тибетской литературе (см., например [Phyag-rgya]) существуют подробные
                описания
                пути, ведущего к реализации вышеописанного состояния сознания, характеризуемого
                тремя вместерожденными элементами (гang-rig, rang-gsal, rang-bde). Обычно этот путь
                подразделялся на три взаимосвязанные фазы: воззрение (Ita-ba), созерцание (sgom-pa)
                и поведение (spyod-pa), о которых один из адептов Ваджраяны Джеринпочешан (zhan)
                писал: «Не видеть зримое это воззрение; не воспринимать как реальносущее все
                увиденное или пришедшее на ум это созерцание; переживание единства блаженства и
                пустоты есть переживание признаков практики; своевременность деяния есть [способ]
                поведения» [Phyag-rgya, 5].</p>
            <p>В этом контексте невозможно не вспомнить философские дискурсы М. Мамардашвили, для
                которого сознание всегда было «предельным понятием философии как таковой, о чём бы
                она ни была» [Мамардашвили, 1990а, с. 3], а анализ сознания был и «орудием
                философствования и тем, с чем оно работает» [Мамардашвили, 1996, с. 6].</p>
            <p>Другая, не менее важная, особенность буддийской философии сознания заключается в том,
                что ей принадлежит несомненный приоритет если не открытия, то, по крайней мере,
                чёткого философского и метафорического описания двух «вместе рожденных», но не
                сводимых друг к другу уровней сознания, которые в контексте традиции дзогчен
                обозначались как мысль (sems) и природа сознания (sems-nyid), a Шри Ауробиндо
                называл ментальным и супраментальным (blo-'das [Klong-chen, 3])<a href="#citation-6"
                                                                                  id="ref-6"
                                                                                  class="citation-mark"><sup>6</sup></a>.
                Различение ординарных и неординарных уровней сознания непосредственно связано с
                практическим осуществлением высшей метафизической ценности, находящейся за пределами
                ума и реализуемой посредством различных медитативных методов. В высшем смысле, как
                пишет Намхай Норбу, «созерцание (rig-ра) запредельно уму» [The Dzogchen ritual, p.
                7].</p>
            <p>Уже сарвастивадины считали, что дхармы (элементы потока сознания) существуют в двух
                планах: как истинная сущность элемента (дхарма-свабхава) и как его моментальное
                проявление (<em>дхарма-лакшана</em>). Как отмечает Ф. Щербатской, истинная сущность
                элементов
                это не неизменная вечная субстанция, она есть «потенциальность» проявления, которая
                существует всегда, поэтому с её точки зрения время не существует реально
                [Шербатской, с. 145]. Благодаря лакшане (mtshan-ma признак, проявление) дхармы
                различимы (самджня, du-shes) и анализируемы, благодаря же дхарма-свабхаве они могут
                быть нирванизируемы, но их собственная природа постигается другой познавательной
                способностью, праджней (shes-rab).</p>
            <p>Если обратиться к «Абхидхарме», то дискретность, мгновенность проявления дхарм
                понималась в ней как тождественная наличию признака, качества (лакшана). «Элементы,
                на которые разлагается поток сознания, писал О. Розенберг, не являются дхармами, а
                "проявлениями дхарм" (<em>дхарма-лакшана</em>)» [Розенберг, с. 99]. Словом, уже в
                «Абхидхарме» можно обнаружить чёткое выражение принципа взаимодополнительности
                (вместерожденности, lhan-cig skyes-pa) дискретных моментов проявления дхарм и так
                называемой их «нерожденности» (skyes-pa-med-ра, <em>анутпада</em>) или, иначе
                говоря,
                неотъемлемости их фундаментальной природы, виртуальными состояниями которой они
                являются. В реальности все дхармы неотделимы от своей истинной природы
                (chos-kyi-rang-bzhin), однако познающий в акте дихотомирующего ментального познания,
                наивно онтологизируя, разделяет их, наделяя независимой сущностью, тем самым как бы
                давая им «родиться». В результате дхарма в качестве дхарма-лакшаны становится
                предметом ума, интенциональным объектом, который может быть схвачен
                (<em>грихнати</em>,
                dzin-pa) субъективным умом (<em>мано-виджняна</em>). Как следствие отсюда возникает
                наивно-реалистическое видение мира, которое воспринимает только «объективный» мир,
                совершенно забывая о способе данности его через сознание. Эта точка зрения в
                буддизме именуется: признание реальности индивидуального Я (gang-zag-gi-bdag
                rang-mtshan-'dzin-pa) и Я дхарм (chos-kyi-bdag- dzin). Истинная же природа дхарм,
                лишенная признаков (<em>лакшана</em>, mtshan-ma), оказывается недоступной для
                дискурсивной
                мысли, которая, будучи всегда интенциональной, может «схватить», познать лишь
                объект, обладающий таковыми.</p>
            <p>Однако в рамках этого подхода оказывается возможным и
                иной способ видения мира или точка зрения «таковости» (<em>татхата</em>), которая
                характеризуется отсутствием интенции познания того, чем предмет (<em>артха</em>)
                является «в
                себе», независимо от его данности через сознание. Эта точка зрения традиционного
                метафорически уподобляется отношению зеркала<a href="#citation-7" id="ref-7"
                                                               class="citation-mark"><sup>7</sup></a>
                (символ истинной природы дхарм) к
                отражающимся в нём предметам (<em>дхарма-лакшана</em>). Оставаясь в пределах
                интеллектуального анализа, такое воззрение направляет интенциональность интеллекта
                на него самого, однако, не обнаруживая признаков, которых истинная природа сознания
                лишена, вынуждено констатировать, что она есть ничто (пустота), т. е. недоступна
                интеллектуальной способности различающего познания (самджня).</p>
            <p>Итак, истинная природа человека (rang-bzhin) это чистое (<em>amala</em>) сознание.
                Она
                недвойственна (gnyis-su-med) и поэтому не может сказать: «Я». Подлинным
                самовыражением её может быть только молчание супраментальное созерцается без
                включения функции языка. В связи с тем, что это изначальное состояние сознания
                недоступно дискурсивной мысли, движущейся в рамках субъект-объектной дихотомии, для
                такой мысли оно есть шунья пустота, ничто. Подобно тому, как чистое небо с точки
                зрения облаков, его заволакивающих, есть ничто, т. е. их отсутствие, в себе же и для
                себя оно есть нечто положительно сущее.</p>
            <p>Здесь мысль сталкивается с проблемой трансценденции интеллектуального. Для буддийской
                философской традиции было очевидно, что проблема трансценденции (<em>ати-крама</em>)
                это
                предельная философская проблема, которую можно сформулировать на ментальном уровне
                сознания, но разрешить лишь при переходе на супраментальный уровень, безусловно
                предполагающий практику йоги. Методы такого рода практики и культивировались в
                рамках традиций ваджраяны и дзогчена.</p>
            <p>Тибетские переводчики (лоцзавы), вносившие в перевод терминов свой глубокий
                созерцательный опыт, для целого ряда дихотомий, связанных с ментальным и
                супраментальным и равнозначных по смыслу, давали структурно тождественный перевод,
                зафиксированный на уровне словообразования. Так, фундаментальное разделение потока
                сознания на сансару и нирвану в рамках абхидхармического анализа превращается в
                дхармы (chos) и дхармату (chos-nyid, дхармовость); в традиции читтаматры и дзогчена
                в мысль (sems) и природу мысли (sems-nyid); в традиции ваджраяны и дзогчена в
                конкретную субъективность личности (bdag) и природу субъективности (bdag-nyid). В
                самом абстрактном виде эта дихотомия выглядит как интенциональный указатель
                предметности (de-kho-na) и природа интенциональности сознания (de-kho-na-nyid,
                <em>таттва</em>), или дхарма (chos) и её источник (дхармадайо, chos-byung)<a
                    href="#citation-8" id="ref-8" class="citation-mark"><sup>8</sup></a>, а в
                контексте
                Гуру-йоги (см. ниже) как bla-ma относительный аспект природы Учителя и bla-ma-nyid
                абсолютность его природы.</p>
            <p>В традиции западноевропейской мысли проблема раздвоения сансары и нирвана
                превращается в проблему непрерывности, континуальности сознания и дискретности его
                ментальной составляющей. Однако история употребления термина «континуальность»
                уходит в глубокое прошлое буддийской традиции. Так, рассматривая проблему
                континуальности в рамках философии ваджраяны, иначе называемой тантраяна, следует
                выяснить прежде, что означает один из основополагающих её терминов — <em>тантра</em>.
                Намхай
                Норбу определяет этот термин так: «Само существование ума в изначальном состоянии,
                которое непрерывно (не может быть прервано), называется <em>тантра</em>» [You..., p.
                51].</p>
            <p>Гухьясамаджа-тантра, один из древнейших письменных источников буддийского тантризма,
                объясняет это слово следующим образом: «Тантра это прабандха» [Guhya-samaja Tantra,
                p. 158], что означает: тантра это континуальность потока изначального чистого
                (ka-dag) и недвойственного сознания (rang-rgyun, <em>сантана</em>), которое
                онтологически представляет собой истинную природу каждого живого существа. Эта
                непрерывность
                присутствия истинного самобытия (татхагатагарбха) инвариантна любой мыслимой
                дихотомии. Она проходит как через истину живого знания (rig-pa), так и через
                онтологическую ошибку (khrul-pa), вызванную мертвящей тенденцией неведения
                (та-rig-pa); как через нирвану, так и через сансару, являясь основой их тождества.
                Она проходит через жизнь, смерть и промежуточное состояние (bar-do), через покой и
                движение, шуньяту и явление, релаксацию и напряжение.</p>
            <p> Ясное внутреннее осознавание света (rang-gsal) непрерывного присутствия самобытия
                (свабхава) есть состояние
                просветленного Будды, неосознавание (ma-rig-pa) этого есть существование на уровне
                сансарического существа. Как пишет великий учитель Нацог-рандол, «те, кто не узнали
                своего лица и положили в основу истинность существования всего (Я и объектов) через
                вместерождённое неведение, заблудились, сделавшись живыми существами. А те, кто
                узнали [лицо] через познание самих себя, стали Буддами» [Sna-tshogs, 1.3A].</p>
            <p>Буддийский тантризм, таким образом, есть метод реализации тождества сансары и
                нирваны, вытекающий из континуальности и неотъемлемости (mi-'phrog-pa) самобытия
                истиной природы сознания человека (rang-bzhin) и проясняющий её изначальную чистоту
                (ka-dag) и неизменность (<em>ваджр</em>, rdo-rje).</p>
            <p> Его Святейшество Далай-Лама XIV так
                описывает состояние высшей реализации в ваджраяне: «... поскольку он удерживает
                ваджр в потоке собственного бытия (континуума), его зовут Ваджрадхара» [Tantra, p.
                231.]</p>
            <p>Герменевтика ваджра, этого фундаментального буддийского символа, также иллюстрирует
                идею континуальности сознания в тождестве сансары и нирваны, так как в центре ваджра
                находится бинду (thig-le)<a href="#citation-9" id="ref-9"
                                            class="citation-mark"><sup>9</sup></a> – символ потенции
                развития в двух зеркально-симметричных направлениях сансары и нирваны на едином
                континуальном
                стержне. Таким образом, тхигле в центре ваджра – это континуальная потенция
                осознания
                состояния тантры или, что то же самое, состояние дзогчен или состояния ваджра. Это
                состояние запредельно для дихотомичной дискретной мысли. Его гностическим аспектом
                является зерцалоподобная способность истинного знания. Зеркальная симметрия ваджра
                её символическое выражение.</p>
            <p>Один из современных исследователей этой проблемы А. Брушлинский связывает
                непрерывность человеческой психики с её процессуальностью и «изначальной
                включенностью в непрерывное и динамическое взаимодействие с миром» [Брушлинский, с.
                75] в отличие от принципиальной дискретности функционирования любой машины и
                выделяет очень важный момент в проблеме континуальности сознания: «психическое
                функционирует непрерывно, недизъюнктивно... [и] эта непрерывность обеспечивается
                органическим единством осознанного и неосознанного» [Там же]. Осцилляция осознания и
                забвения континуальной составляющей потока сознания, по-видимому, связана с
                проблемой разделения сансары и нирваны. По выражению Шри Ауробиндо «то, что мы
                называем <em>несознанием</em>, есть просто иное знание» [Сатпрем, с. 611].</p>
            <p>«Признак [махамудры]<a href="#citation-10" id="ref-10"
                                      class="citation-mark"><sup>10</sup></a> есть признак
                (континуального. А.В.) пребывания дхармакаи, которая раздваивается на познающего и
                непознающего её. От этого признака
                единое раздвоилось на сансару и нирвану. Те, которые познали сущность дхармакаи,
                остались в нирване, а те, которые из-за присутствия авидьи (ma-rig-pa) не сумели
                познать её, ушли в сансару» [Дандарон, с. 15].</p>
            <p>Следует заметить, что осознание вышеописанного может быть, как проблеск, мгновенным и
                только его присутствие континуальным. Отсюда вытекает духовно-праксеологический
                аспект проблемы континуальности сознания, на который указывает современный тибетский
                учитель Ургьен Ринпоче: «Жизненно важным моментом в нашей практике является
                узнавание, осознание природы сознания (rig-pa), но после осознания её... главное
                остаться в этом состоянии, удержаться от потери осознания его континуальности» [The
                mirror..., p. 8].</p>
            <p>В. В. Налимов, изучавший континуальность человеческого мышления на основе
                вероятностной модели языка, пришел к выводу, что механизм континуальной составляющей
                мышления носит, во-первых, аналоговый характер, а во-вторых, представляет собой не
                столько «мозговой», сколько «общесоматический» процесс. Таким образом, «человек в
                каком-то глубоком смысле мыслит всем своим телом» [Налимов, 1978, с. 84]. Аналоговый
                характер континуальности человеческого мышления связан, по-видимому, с тем, что оно
                опирается на непрерывные процессы тонкой психофизической циркуляции (мысль-прана,
                rlung-sems).</p>
            <p>Как известно, <em>нади</em> (rtsa), <em>чакры</em> (khor-lo), <em>праны</em> (rlung),
                <em>бинду</em>
                (thig-le) это элементы структурной картографии пространства внутреннего опыта,
                измерением которого является единство мысли и энергии (rlung-sems) (см., например,
                [Guenther, 1963; Пупышев, 1992, с. 86]). Практически все религиозные системы Индии и
                Центральной Азии культивировали интегральное психосоматическое переживание
                (ting-'dzin, <em>самадхи</em>), использующее в качестве инструмента и опоры
                возможности динамической структуры тонкой психофизиологии человеческого тела, как
                доминантный способ познания.</p>
            <p>Духовно-практически эта тонкая инструментальность человеческого
                тела использовались в различных системах так называемой Янтра-йоги (phrul-'khor).
                Само слово «янтра» означает «механизм», т. е. практики этой системы используют
                механизм естественной корреляции и взаимозависимости трёх фундаментальных уровней
                человеческого существования телесного (связанного с движением), энергетического
                (связанного с дыханием) и ментального (связанного с визуализацией). По свидетельству
                Намхая Норбу, различные системы янтр в древности тайно практиковались всеми
                традициями аннутарайоги, однако до наших дней, по всей видимости, дошли только лишь
                некоторые из традиций янтры, в частности культивировавшаяся по линии дзогчена
                янтра-йога, именуемая «Соединение солнца и луны» (Nyi-zla-kha-sbyor) [Norbu,
                1982].</p>
            <p>Эта уникальная тибетская система психофизиологического тренинга стала известна на
                Западе благодаря устной проповеди, а также написанному по-тибетски расширенному
                комментарию на коренной текст Вайрочаны (Phrul-khor-nyi-zla-kha-sbyor-bzhugs-so),
                сделанному Норбу Ринпоче.</p>
            <p>Известный тибетский переводчик и один из ближайших
                учеников Гуру Падмасамбхавы Вайрочана был автором первого письменного изложения
                принципов этой системы (см. Ibid, p. 1-31]. Тибетские кальки названия «Янтра-йога»
                раскрывают некоторые принципиальные смысловые аспекты этой практики. Так, выражение
                phrul-'khor-gi rnal-'byor означает следующее: rnal спокойное, уравновешенное,
                естественное состояние; 'byor — достигать, обретать; 'phrul — механизм и 'khor —
                колесо,
                на санскрите — чакра, что намекает на «тонкую» психофизиологию человеческого тела,
                именуемую в буддийских текстах по йоге — «ваджрным телом» (rdo-rje'i-lus).</p>
            <p>Сопоставив все значения, получим в итоге: «достижение уравновешенного состояния,
                [используя] механизм <em>ваджрного тела</em>». Таков внутренний смысл практики
                янтра-йоги. Значения других тибетских синонимом рассматриваемого термина:
                'phrul-sbyor, или «механизм соединения» [принцип Солнца и Луны]; 'phrul-'khor
                bskor-ba' или «механизм вращения чакр [ваджрного тела]» (см.: [Суматиратна, 1959, с.
                161]).</p>
            <p>Структурные элементы ваджрного тела, или наборы пяти чакр, пяти пран-ветров, пяти
                элементов-махабхутов, представляют собой пять фундаментальных уровней
                жизнедеятельности организма человека, которые в итоге сводятся к трём основным:
                телу, речи и мысли. Янтра-йога имеет прямое отношение к культуре этих трёх аспектов
                человеческой жизни. Культура тела связана с принятием определенных позиций в
                сочетании с движениями, регулирующими правильность, точнее заданность определённых
                типов дыхания. Культура речи, поскольку она связана с дыханием и произнесением
                звуков, достигается посредством дыхательной практики и чтения мантр, а культура ума
                формируется упражнениями по созерцательному сосредоточению, или медитации. Таким
                образом, управляя механизмом ваджрного тела с помощью методов янтра-йоги,
                практикующий может привести систему собственных тела, речи и ума к изначальному
                состоянию покоя (rnal-dbab) и уравновешенности, что является необходимым
                предварительным условием для более глубоких практик.</p>
            <p>По свидетельству Намхая Норбу, практика янтра-йоги представляет собой одну из
                эффективнейших предварительных, или
                вторичных, практик, используемых и поныне в дзогчен-общинах. Собственно
                дзогченовские, или первичные, практики представляют собой различные методы,
                действующие главным образом на уровне ума, точнее, на уровне его трансценденции, или
                реализации его истинной природы (sems-nyid). Один из величайших авторитетов традиции
                дзогчен — Лонченпа<a href="#citation-11" id="ref-11"
                                     class="citation-mark"><sup>11</sup></a> так описывает различие
                ментального и супраментального: «Каким бы ни являлось ментальное (sems) оно всегда
                есть феномен сансары... Именно из-за
                наличия ума (sems) живые существа в омрачении [рождаются] в Шести сферах (сансары).
                Когда же собственное исконное сознание [дхармакая] освобождается от обусловленности.
                А.В. ментальным, тогда [такое существо] именуют Буддою... поскольку она [дхармакая]
                свободна от любых ментальных представлений, её природой является пустота и ясность,
                подобные небу» [Buddha, 1989, p. 212].</p>
            <p>Переводя термин «дхармакая» (chos-sku) как «исконное со-знание», мы используем
                герменевтику слова «сознание», предложенную М. К. Мамардашвили, а именно: «состояние
                знания». В результате такого истолкования проясняется единство онтологического
                (состояние) и эпистемологического, а точнее гностического, в феноменологии
                буддийской мысли. Символически это единство также можно обнаружить в структуре
                ваджра, который представляет собой онтологический аспект Абсолютной реальности, но
                имплицитно, в зеркальной симметрии своей формы, содержит аспект познания (гнозиса),
                универсальным символом которого для большинства культур древности было зеркало
                (me-long).</p>
            <p>С сотериологической точки зрения традиция ваджраяны и дзогчена развивала прежде всего
                интуитивный анализ (по типу феноменологического) и непосредственный опыт
                (nyams-myong) виртуальных психосоматических состояний сознания, через переживание
                которого неминуемо проходит психика человека, вставшего на путь совершенствования
                йогического типа. Этот путь (lam) в результате должен привести практикующего к
                окончательному и непрерывающемуся пребыванию на уровне собственного измерения
                супраментального пространства сознания.</p>
            <p>При этом реальная практика созерцания, медитации, может «осциллировать» в пределах
                всеохватной фундаментальной дихотомии
                сансары и нирваны, которая либо преодолевается на вершине переживания их тождества
