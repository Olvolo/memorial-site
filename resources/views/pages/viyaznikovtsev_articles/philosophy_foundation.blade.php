            <p>По-видимому, этот список может быть продолжен, но главное здесь это момент соединения
                обоих аспектов, который обозначается терминами бодхичитта
                (byang-chub-sems)<a href="#citation-12" id="ref-12"
                                    class="citation-mark"><sup>12</sup></a>, джняна<a
                    href="#citation-13" id="ref-13" class="citation-mark"><sup>13</sup></a>
                (ye-shes), биджа (мистический слог мантры, результат соединения гласных и
                согласных),
                центральный путь прохождения внутренней энергии авадхути (как результат соединения
                левого канала -<em>лаланы</em> и правого -<em>расаны</em>). Фундаментальной целью
                всей духовной практики подобного рода является
                осознание себя на супраментальном уровне, где все вышеперечисленные диады узнаются в
                их тождестве и нераздельности (dbyer-med). Именно ментальность (sems) в результате
                онтологической ошибки ('khrul-pa)<a href="#citation-14" id="ref-14"
                                                    class="citation-mark"><sup>14</sup></a>
                отчуждает онтологический и гностический аспекты этого в сущности (ngo-bo)
                недуального процесса познания, так что все эти вместерожденные элементы иллюзорно
                распадаются на серию диадических соответствий.</p>
            <p>Состояние знания (со-знания), переживаемое на уровне супраментального
                онтогнозиса, где, по образному выражению из старых переводов (rnying-ma) узнается
                лик собственного сознания (sems nyid rang-zhal)<a href="#citation-15" id="ref-15"
                                                                  class="citation-mark"><sup>15</sup></a>
                [Lamp, 1989], именуется джняна<a href="#citation-16" id="ref-16"
                                                 class="citation-mark"><sup>16</sup></a>
                (ye-shes). На уровне тонкой психосоматики йогических методов
                (например, янтра-йоги) это переживание джняны, или (что, то же самое) бодхичитты,
                соответствует движению всей внутренней энергии тела (sems-rlung-thig-le) в
                центральном канале авадхути (Ava-dhuti)<a href="#citation-17" id="ref-17"
                                                          class="citation-mark"><sup>17</sup></a>.
            </p>
            <p>В заключение остановимся на центральном, ключевом методе, посредством которого в
                традициях ваджраяны и дзогчена достигалась реализация состояния будды, тождественная
                вышеописанному вхождению в состояние онтогнозиса или джняны. Одна из самых кратких
                формул, выражающих суть этого метода, называемого Гуру-йога (bla-ma'i rnal-'byor),
                принадлежит монгольскому махасиддху<a href="#citation-18" id="ref-18"
                                                      class="citation-mark"><sup>18</sup></a>
                (grub-chen) Додмарандолу
                (Gdod-ma-rang-grol), современнику и ученику одного из наиболее уважаемых авторитетов
                традиции дзогчен Жигмеду Лингпы (Jigs-med-rling-pa, 18v.). По-видимому, именно
                Додмарандол был первым распространителем учения дзогчен и связанного с ним варианта
                Чод в Монголии. Слова этой формулы таковы: «Осознаю неразрывное единство с сознанием
                Учителя» (Bla-ma rang-sems 'du bral-med) [Gdod-ma-rang-grol].</p>
            <p>Иначе говоря, это процесс актуализации потенциального единства внутреннего состояния
                ученика с внутренним состоянием Учителя как мгновенного присутствия (rig-ра) на
                супраментальном уровне неделимой дхармакаи (chos-sku). Инициацией этого процесса
                служит культивируемая в традиции дзогчен так называемая прямая передача<a
                    href="#citation-19" id="ref-19" class="citation-mark"><sup>19</sup></a>
                знания (ngo-rang-thog-tu-sprad) [Norbu, 1990, р. 94]. Эта уникальная особенность
                учения дзогчен осуществляется B процессе особого ритуала (rig-pa'i rtsal-dbang),
                эквивалентного четырём основным посвящениям (dbang) на пути ваджраяны и
                пробуждающего в участниках чувство присутствия высшего сознания (rig-ра), которое
                спонтанно рассеивает сомнения относительно пути.</p>
            <p>Принципиальным условием вышеописанной коммуникации, связанным с решением проблемы
                интерсубъективности сознания, является всеобщее потенциальное присутствие (rig-ра)
                неделимого, открытого и чистого (ka-dag) «шуньевого» сознания, обладающего природой
                (rang-bzhin) ясности и блаженства (gsal-bde). Открытость подобного
                рода, в которой реализуется «локальная данность целого»<a href="#citation-20"
                                                                          id="ref-20"
                                                                          class="citation-mark"><sup>20</sup></a>,
                обеспечивается остановкой (mi-rtog-pa) механизма самоотождествления сознания с любым
                из элементов
                (<em>дхарма</em>, chos) или группой элементов (<em>скандха</em>, phung-po),
                составляющих его содержание, что ведёт к прекращению восприятия себя и мира сквозь
                понятийный фильтр субъективных представлений.</p>
            <p>Таким образом, открытость, или шуньяту, можно
                интерпретировать как несводимость сознания ни к одному из его возможных состояний.
                Иными словами, предельная природа чистого сознания (<em>amala-vijñana</em>)<a
                    href="#citation-21" id="ref-21" class="citation-mark"><sup>21</sup></a>
                может быть определена только апофатически, посредством процедуры отрицания. С другой
                стороны, катафатически самобытие изначальной природы сознания представляет собой
                такой высочайший уровень чистоты (ka-dag) и ясности (gsal), что только в его свете
                Cod) становится возможным свободное от ошибочных идентификаций (khrul-pa)
                видение-осознавание всех проявлений сознания (snang) как игры (rol-ра) или украшения
                состояния<a href="#citation-22" id="ref-22" class="citation-mark"><sup>22</sup></a>.
                С учётом всего вышесказанного известная буддийская формула о
                тождестве ясного света Cod-gsal) и пустотности (stong-nyid), по существу, выглядит
                как указание на два аспекта предельного уровня реальности. Это гностический аспект,
                связанный с апофатическим её описанием, и онтологический связанный с её
                непосредственным переживанием<a href="#citation-23" id="ref-23"
                                                class="citation-mark"><sup>23</sup></a>.</p>
            <p>Иначе говоря, постижение Шуньяты (stong-nyid) открывает возможность экзистенциального
                контакта с этой вечно ускользающей из объятий ума, необъективируемой природой
                чистого сознания (amala-vijñana). В этом смысле шуньята указывает на неисчерпаемость
                высшей реальности сознания, которая является тайной (<em>guhya</em>, gsang-ba).</p>
            <p>На уровне тайны реальность в своих проявлениях виртуально спонтанна (lhum-grub), т.
                е. не
                связана с причинностью и пребывает вне пределов трёх периодов времени (прошлого,
                настоящего и будущего). Коммуникация C этим источником тайны, реализуемая
                посредством Гуру-йоги, вырастает в последовательность, непрерывность линии передачи
                Учения и составляет ещё одно значение слова <em>тантра</em> (rgyud). Наш
                современник,
                тибетский учитель Тхинлей Норбу так комментирует эту тему: «Сущность линии передачи
                такова, что она имеет нерушимую и естественную связь с непрерывной, чистой,
                космической (<em>букв</em>.: природной) энергией. Если мы отделяем эту полученную от
                природы
                энергию от её тайного источника (дхармакан. А.В.), она становится омраченной и
                нечистой, и тогда линия передачи прерывается [Norbu, 1985, p. 51].</p>
            <p>Функцию сохранения этой линии преемственности вплоть до наших дней выполняют Учители
                Держатели знания (rig-'dzin), Учители, которых тибетская традиция именует Лама-дампа
                (bla-ma dam-pa), или Высшие Учители. Постичь, увидеть пространство нементального
                можно лишь чистым оком исконного сознания (rig-ра), которое открывает истинный Гуру
                (bla-ma dam-pa) посредством прямой его передачи.</p>

