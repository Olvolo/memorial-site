            обозначают термином <i>Гухья</i> – Тайна.</p><br>

        <p class="text-justify">8. Нетройственность – это вместерожденность трёх: пустоты (<i>шуньята</i>), проявленности (<i>Ясный Свет</i>)
            и сознания. Таково выражение антропного принципа языком буддизма. Символом и воплощением идеи
            неуничтожимости сознания является в буддизме <i>Ваджрасаттва</i>. <i>Ваджрасаттва</i> – ярчайшая печать
            Бытия в этот раз. Поэтому он есть воплощение <i>махамудры</i><a href="#citation-15" id="ref-15"
                                                                            class="citation-mark"><sup> 15</sup></a>.
            <i>Ваджрасаттва</i> – матрица, форма высшей жизни в этом конкретном фантоме <i>акаши</i>. Здесь и сейчас <i>акаша</i>
            может предстать как жизнь исключительно в виде и наподобие <i>Ваджрасаттвы</i>.</p>

        <p class="text-justify"><i>Дхарма</i>, воспринятая как учение, не поможет, пока не будет понято, что <i>дхарма</i> и есть сама
            реальность. Тогда станет ясно, что речь идёт не о следовании рецептам конкретной личности по имени
            Шакьямуни, а о законе внутри каждого из нас – Будда ты сам! Такой Будда, конечно же, называется не
            Шакьямуни, имя его – <i>Ваджрасаттва</i>.</p>

        <p class="text-justify">Три субъекта Нетройственности: 1. <i>Ваджрасаттва</i>, <i>ваджр</i>, Я.; 2. <i>Вайрочана</i><a
                href="#citation-16" id="ref-16" class="citation-mark"><sup> 16</sup></a>, зеркало, Оно.; 3. <i>Самантабхадра</i>,
            кольцо, Мы.</p>

        <p class="text-justify">Утверждения Нетройственности:</p>
        <p class="text-justify">Сознания нет снаружи (Бога нет),</p>
        <p class="text-justify">Сознания нет внутри (души нет),</p>
        <p class="text-justify">Сознание между (сознание есть).</p>

        <p class="text-justify">Сознание – это интерференция волн возбуждения <i>акаши</i>, это зеркальная сфера, принимающая каждый раз
            определённую форму. Эти формы квантуются. Сознание – это пространственная голограмма, это своеобразная грань
            ясносветно-шуньевой осцилляции, грань, порождающая эффект зеркала – способности отражать. И грань эта не в
            глубине микромира, не в бездне космоса, а здесь, сопряжённая и соизмеримая с нашим телом. Наше тело это и
            есть примерный «размер» «узла» индивидуального сознания на общем
            размытом фоне полевого сознания.</p><br>

        <p class="text-justify">9. Избыточность. Допущение: резерв мозга в сотни раз превышает оперативную необходимость, – не есть
            свидетельство «нейтральной мутации» (Н. Моисеев), но лишь след невидимого биологического Зеркала Вселенной
            (автозеркалирование – механизм Мудрости <i>Вайрочаны</i>). Этот резерв соответствует Простору непознанного
            (целостности <i>акаши</i> – сплошной и гладкой). Жизнь человека в пространстве понятного, разумного только и
            возможна благодаря этому избыточному Простору интеллектуального резерва (неисчерпаемость <i>шуньяты</i>,
            или, говоря словами Б. Д. Дандарона, «шуньята полна запредельных ценностей»). Избыточность – необходимый
            гарант устойчивости психики человека.</p><br>

