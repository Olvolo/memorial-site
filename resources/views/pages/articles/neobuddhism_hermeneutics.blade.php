            восхитительную открытую беспредельность Вселенной, буддисты
            обозначают термином <em>Гухья</em> – Тайна.</p>

        <p><strong>8.</strong> Нетройственность – это вместерожденность
            трёх: пустоты (<em>шуньята</em>), проявленности (<em>Ясный Свет</em>)
            и сознания. Таково выражение антропного принципа языком буддизма. Символом и воплощением идеи
            неуничтожимости сознания является в буддизме <em>Ваджрасаттва</em>.
            <em>Ваджрасаттва</em> – ярчайшая печать
            Бытия в этот раз. Поэтому он есть воплощение <em>махамудры</em><a href="#citation-15" id="ref-15" class="citation-mark"><sup> 15</sup></a>.
            <em>Ваджрасаттва</em> – матрица, форма высшей жизни в этом конкретном
            фантоме <em>акаши</em>. Здесь и сейчас <em>акаша</em>
            может предстать как жизнь исключительно в виде и наподобие <em>Ваджрасаттвы</em>.
        </p>

        <p><em>Дхарма</em>, воспринятая как учение, не
            поможет, пока не будет понято, что <em>дхарма</em> и есть сама
            реальность. Тогда станет ясно, что речь идёт не о следовании
            рецептам конкретной личности по имени
            Шакьямуни, а о законе внутри каждого из нас – Будда ты сам! Такой
            Будда, конечно же, называется не
            Шакьямуни, имя его – <em>Ваджрасаттва</em>.</p>

        <p>Три субъекта Нетройственности: 1. <em>Ваджрасаттва</em>,<em>ваджр</em>, Я.; 2. <em>Вайрочана</em><a href="#citation-16" id="ref-16" class="citation-mark"><sup>16</sup></a>, зеркало, Оно.; 3. <em>Самантабхадра</em>, кольцо, Мы.</p>

        <p>Утверждения Нетройственности:</p>
        <p>Сознания нет снаружи (Бога нет),</p>
        <p>Сознания нет внутри (души нет),</p>
        <p>Сознание между (сознание есть).</p>

        <p>Сознание – это интерференция волн возбуждения
            <em>акаши</em>, это зеркальная сфера, принимающая каждый раз
            определённую форму. Эти формы квантуются. Сознание – это
            пространственная голограмма, это своеобразная грань
            ясносветно-шуньевой осцилляции, грань, порождающая эффект зеркала –
            способности отражать. И грань эта не в
            глубине микромира, не в бездне космоса, а здесь, сопряжённая и
            соизмеримая с нашим телом. Наше тело это и
            есть примерный «размер» «узла» индивидуального сознания на общем
            размытом фоне полевого сознания.</p>

        <p><strong>9.</strong> Избыточность. Допущение: резерв мозга в сотни
            раз превышает оперативную необходимость, – не есть
            свидетельство «нейтральной мутации» (Н. Моисеев), но лишь след
            невидимого биологического Зеркала Вселенной
            (автозеркалирование – механизм Мудрости <em>Вайрочаны</em>). Этот
            резерв соответствует Простору непознанного
            (целостности <em>акаши</em> – сплошной и гладкой). Жизнь человека в
            пространстве понятного, разумного только и
            возможна благодаря этому избыточному Простору интеллектуального
            резерва (неисчерпаемость <em>шуньяты</em>,
            или, говоря словами Б. Д. Дандарона, «шуньята полна запредельных
            ценностей»). Избыточность – необходимый
            гарант устойчивости психики человека.</p>

        <p><strong>10.</strong> Ум (а также Тело и Речь) творит реальность. Всё продуманное влияет на ход событий и воплощается. Буддийская триада Тело, Речь, Ум – полнота существования сознательного существа. При излишнем доминировании одной из составляющих увеличивается страдание. При отсутствии одного из тел – ущербность бытия. Идеал буддизма – Трикая.</p><br>
