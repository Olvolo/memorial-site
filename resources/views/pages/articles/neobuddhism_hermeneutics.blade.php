        <p>Каждое мгновение постоянно раскрывающаяся в <i>виджняну дхармакая</i> может обнаружиться <i>Ваджрасаттвой</i> (тантрийский серфинг, Бегущий по Волне), но наверняка оборачивается просто жизнью и притом, конечно, сознательной (солитон жизни).</p>

        <p>4. Нет неживого, есть лишь менее живое. Максималистский, мировоззренческий тезис, крайне необходимый в эпоху нарастания экологического кризиса. Это вариант формулировки антропного принципа, но с акцентированием принципа «Вселенная есть целое» – достаточное основание диктата экологии в становящемся мире.</p>

        <p>5. Необходимость Учителя в буддизме абсолютна. Связка Учитель-ученик выражает суть высшего жертвоприношения: <i>дхармакая</i> не может не саморазмножествляться. Не узнать Целое в единичном, значит не узнать его никогда; не увидеть Абсолют в одном, значит не увидеть его вообще. Человечество есть множественный блик Одного, буддисты его называют по-разному – Учитель, Будда, <i>Ваджрасаттва</i><a href="#citation-9" class="citation-mark"><sup>9</sup></a>, <i>Вишварупа</i><a href="#citation-10" class="citation-mark"><sup>10</sup></a>.</p>

