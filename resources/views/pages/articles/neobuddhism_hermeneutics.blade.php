        <p>Коль где-то есть сознание – смерти нет.</p><br>

        <p><em> --- Резюме --- </em></p>
        <p>Заметка обозначила некоторые позиции
            перенормировки базовых буддийских терминов языком современной
            философии
            физики и психологии. Буддизм, прочтённый таким образом, естественно
            становится одним из ведущих
            мировоззрений современности, открытых в будущее. Так он выходит из
            монашеской резервации и, избегая
            ограниченности и фанатизма дхарма-центров, становится достоянием
            <em>мирской сангхи</em> – светских
            последователей
            Учения, а возможно и мира. Именно такой буддизм Б. Д. Дандарон
            называл <em>необуддизмом</em>.</p>
