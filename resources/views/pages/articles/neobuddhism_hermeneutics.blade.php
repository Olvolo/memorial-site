        <p>0. Конфессии не умирают, но программа их исчерпана. Дальнейшее их существование инерционно.</p>

        <p>00. Философия настоящего и будущего – не религия и непривычное для XIX – XX в. научное видение. Новый этап – асимптотическое сближение науки и религии, наука с элементами религиозной философии и практики.</p>

        <p>000. Метод <i>перенормировки</i> – один из инструментов создания новой картины мира. Старые категории и понятия, названные иначе, создают новое поле значений, органично включающее религиозный и научный опыт человечества.</p>

        <p>Далее – элементы подобной перенормировки в связи с буддизмом.</p>

        <p>1. Вселенная есть целое. Неограниченный монизм. Ничто не происходит во Вселенной независимо. Всё взаимосвязано. Нет действий, не имеющих последствий и не распространяющихся абсолютно на всё. Действия отдельной личности, а не только всего человечества влияют на Вселенную, как на единое целое. При этом имеется в виду не только материальная деятельность человека, но троякий аспект деяний: деяния телом, речью и мыслью. Взаимовлияние происходит не по известным на данный день законам Общей теории относительности, а мгновенно и безусильно.</p>

        <p>Всесвязность всего в буддизме обозначена термином <i>бодхичитта</i><a href="#citation-2" class="citation-mark"><sup>2</sup></a> – всё чувствует всё. Именно <i>бодхичитта</i> есть субстрат целостности мира как множества, есть механизм высшей жертвы буддийского целого – <i>дхармакаи</i><a href="#citation-3" class="citation-mark"><sup>3</sup></a>.</p>

        <p>Нравственный аспект этого тезиса в буддизме может звучать так: всех живых воспринимаю, как свою мать; в тантре несколько иначе и острее – все живые существа есть мои дети.</p>

        <p>2. Нет единичного сознания. Сознание проявляется как гроздь, гнездо, семья, как множество. Единичного сознания просто не бывает. Мы и Я равнозначны и вместерождённы.</p>

        <p>Я, начавшее теснить древнее Мы в эпоху Ренессанса, сейчас начинает ещё не очень заметно признавать сущностную и ценностную основу Мы. На данном этапе появляются первые признаки возможной гармонизации Целого – Мы и единичного – Я; переживается возможность понимания и практики вместерождённости Мы и Я. В разных странах, у разных народов и в разных культурах это происходит по-своему. Запад только начал ощущать утраченное Мы. Соединенные Штаты тоже обретают Мы, но в гипертрофированной и уродливой форме превосходства так называемых американских ценностей над всеми другими. Россия, качнувшись к Я, имеет возможность обрести сравнительно быстро гармонию Я личности и Мы народа. Мы древности – Я ренессанса – Мы русских – технотронное Мы современного Запада – Мы китайцев.</p>

        <p>Соотношение Я и Мы отражено в буддийских концепциях <i>татхагатагарбхи</i><a href="#citation-4" class="citation-mark"><sup>4</sup></a> махаянских сутр, <i>Гухьягарбхи</i><a href="#citation-5" class="citation-mark"><sup>5</sup></a>, <i>махайоги</i><a href="#citation-6" class="citation-mark"><sup>6</sup></a> школы <i>ньингма</i>, <i>алая-виджняны</i><a href="#citation-7" class="citation-mark"><sup>7</sup></a> школы <i>йогачаров</i>.</p>

        <p>3. Бытие абсолютно вероятно и его Реальность есть Множество. Небытия нет. <i>Дхармакая</i> небытийна по определению, это лишь точка пространственного перегиба, предел отскока, мгновенное изменение пространственной метрики, сингулярность, размножествляющаяся через <i>акашу</i><a href="#citation-8" class="citation-mark"><sup>8</sup></a>, потенция напряженной <i>шуньяты</i>. Размножествление <i>дхармакаи</i> есть сущность <i>бодхичитты</i>, высшее жертвоприношение. Это – абсолютное основание нравственности, Высшее сострадание.</p>

        <blockquote class="quote pl-6 border-l-4 border-gray-300 my-4">
