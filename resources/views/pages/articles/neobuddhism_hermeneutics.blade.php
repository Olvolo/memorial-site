        <p class="text-justify">Всё пусто, но действовать уместно.</p>
        <p class="text-justify">Без формы жизни нет, и нет сознания.</p>
        <p class="text-justify">Познанье, радость сопричастья – вот высшее.</p>
        <p class="text-justify">Коль где-то есть сознание – смерти нет.</p><br>

        <p class="text-justify"><em> --- Резюме --- </em></p>
        <p class="text-justify">Заметка обозначила некоторые позиции перенормировки базовых буддийских терминов языком современной философии
            физики и психологии. Буддизм, прочтённый таким образом, естественно становится одним из ведущих
            мировоззрений современности, открытых в будущее. Так он выходит из монашеской резервации и, избегая
            ограниченности и фанатизма дхарма-центров, становится достоянием <em>мирской сангхи</em> – светских
            последователей
            Учения, а возможно и мира. Именно такой буддизм Б. Д. Дандарон называл <em>необуддизмом</em>.</p>

