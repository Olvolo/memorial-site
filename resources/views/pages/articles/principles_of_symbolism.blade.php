        <p>Но столь абстрактный и аскетический символизм не мог долго
            просуществовать в чистом виде в такой богатой традициями живописи и
            пластики стране, как Индия. К I в. н.э. под влиянием античного
            искусства появляются первые антропоморфные изображения Будды в
            художественных центрах Гандхары и Матхуры. Северная ветвь буддизма —
            Махаяна, выдвинув идеал общего спасения, создает культ Будды как
            Спасителя. Возникает учение о Трёх Телах Будды. Махаяна ставит
            человека среди существ на первое место по возможности реализации
            нирваны — религиозной максимы буддийского учения о спасении.
            Антропоцентризмом дополняется антропоморфизм, эталоном становится
            совершенный человек — Будда. Весь ряд символов приравнивается к
