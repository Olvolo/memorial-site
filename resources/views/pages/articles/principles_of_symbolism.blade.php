        <p>Мандала как наиболее абстрактный образ стала заменителем и
            эквивалентом всего ряда буддийской символики.</p>

        <p>Внутреннее строение каждого символа Ваджраяны обычно сохраняет общую
            числовую структуру: обычно это число 5 или 3. Подобное повторение
            числовой структуры наблюдается в следующих числовых рядах: число 5 —
            пять элементов зародышевого слога ХУМ, пять центров авандуди, пять
            цветов радуги вокруг нирванистических существ, пять частей ступы,
            пять рогов ваджра и т.д.; число 3 — божество типа яб-юм на лотосе,
            навершие ступы — сваямбху, состоящее из луны, солнца и всплеска
            нада; а также – шуньята, Ясный Свет, джнянасаттва (знающий
            наблюдатель); дхармакая, самбхогакая, нирманакая; Мысль, Речь, Тело
            Будды; видение, созерцание, поведение.</p>

        <p>Каждый из буддийских символов универсален. Универсализм обеспечивает
            их взаимозаменяемость: ступа, мудра, статуя, слог — все они
            приравниваемы к мандале; и далее, как следствие, — автономный
            универсализм, когда, например, и божество, и атрибут (Ваджрапани и
            ваджр), каждый, есть достаточный и полный символ операциональности
            монизма Ваджраяны: символ как иллюстрация снятия проблемы
            двойственности.</p><i></i>

        <p>Постоянство и числовую повторяемость общей структуры буддийских
            символов на разных уровнях, преломленных в художественных образах,
            предлагается назвать <i>принципом семени</i>, или принципом
            <i>зародышевого символизма</i>. Способы воплощения этого
            принципа: <i>умозрительный</i> —
            принцип недвойственности, деяния Просветленных; <i>объемный</i> — ступа,
            статуя, курильница, ваджр; <i>плоскостной</i> — икона, мандала, янтра;
            <i>автономный</i> — сваямбха, ушниша, ваджр.</p>

        <p>Таким образом, в искусстве северного буддизма, представленном в
            основном школами Ваджраяны, синтезировавшей главные аспекты
            буддийской философии и религии, постепенно сложился своеобразный
            универсальный метаязык, удобно поддающийся семиотическому и
            психологическому анализу и позволяющий её последователям общаться,
            минуя обычный уровень словесных описаний.</p>
