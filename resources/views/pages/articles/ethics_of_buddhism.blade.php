            <h3 class="text-2xl font-bold text-center mb-4">
                Теория дхарм – психологическое основание
                буддийской этики
            </h3>
            <p>Анализируя личность, буддийские практики и философы выделили по
                одним системам 100, по другим 75 не сводимых друг на друга
                элементов психофизиологического континуума личности. В центр
                личности они поставили сознание - виджняну. Это элемент, вокруг
                которого группируются по закону кармы все остальные дхармы,
                образуя неповторимое кружево индивидуальности. Само сознание
                лишено содержания, это как бы зеркало, отражающее меняющийся
                вихрь комбинации элементов жизни. Сознание всегда
                самосовершенно, чисто и свободно, обладает "изначальной
                мудростью". Человеку же свойственно отражения принимать за
                реально существующее, забывая про самосовершенное зеркало. Игра
                отражений и принимается за условное Я. Отсюда спутанность и
                обусловленность обыденной жизни.</p>
            <p>Вторая группа элементов (дхарм) – это элементы, формирующие нашу
                форму, видимое, пространственное, цветовое и др.</p>
            <p>Третья группа – это элементы, отвечающие за нашу способность
                дифференцировать, сравнивать и различать. На этом зиждется
                способность умственного и творческого труда.</p>
            <p>Четвёртая группа дхарм – это чувственные элементы, элементы
                приятного, неприятного и безразличного.</p>
            <p>Пятая группа – элементы-двигатели, заставляющие вступать во
                взаимосвязь все ранее перечисленные дхармы.</p>
            <p>В этическом смысле дхармы делятся на три типа: благоприятные для
                процесса освобождения личности от страданий, неблагоприятные и
                нейтральные.</p>
            <p>Подавление неблагоприятных дхарм на основе активизации
                благоприятных составляет практическую основу буддийской
                психотерапии. Важно отметить, что анализ личности производится
                не изолированно от окружающего, объективного мира.
                Анализируется, например, не человек отдельно и не солнце как
                явление природы, а целостный процесс, который можно выразить
                словами: «человек, видящий солнце».</p>
        </section>

        <!-- Бодхисаттвы – этический идеал -->
        <section id="bodhisattva" class="mb-8">
            <h3 class="text-2xl font-bold text-center mb-4">
                Бодхисаттвы – этический идеал буддийской личности Махаяны</h3>
            <p>
                Буддисты Хинаяны занимаются личным спасением и не интересуются
                другими личностями. Они опирались на слова Будды: «Будь сам себе
                светильник». Никто не мог помочь на пути спасения, лишь
                вдохновлял пример Будды. Махаяна выдвинула концепцию
                всесвязности всего сонма сознаний, идею неполноценности
                индивидуального спасения. Бодхисаттвы – это те, кто прежде
                спасает других, забывая о собственном благе. Ими движет чувство
                великого сострадания. Известна знаменитая и вдохновенная клятва
                бодхисаттвы: «Да освобожу я всех живых существ! Да достигну
                состояния просветлённости! Пусть родится истинная
                бодхисаттвовская мысль!» Бодхисаттва – это тот, кто постиг
                просветленную, т.е. бодхисаттвовскую мысль. Содержание этой
                мысли – беспримерная, равная для всех любовь к живым существам,
                сочетающаяся с самоотверженной деятельностью на общее благо.
                Гносеологические принципы буддизма – это вместерожденность
                этической чистоты и способности к познанию. Только нравственно
                чистый, а в идеале бодхисаттва, способен скачкообразно, т.е.
                интуитивно, продвинуться в познавательном процессе.
            </p>
            <p>Идея бодхисаттвовской мысли как принцип всесвязности всего живого
                во вселенском масштабе, предвосхищает идею ноосферы
                Вернадского.</p>
            <p>В Махаяне путь стремящегося к состоянию Будды, представленный в
                Хинаяне восьмеричным путём, развит в десять <i>бхуми</i>, или
                ступеней, которые должен пройти бодхисаттва.</p>
            <p>Ступень <i>радости</i> (прамудита) – появление мысли бодхи;
                именно здесь принимается решение о спасении других.</p>
            <p>Ступень <i>чистоты</i> (вимала) – здесь практикуется
                нравственность и начинает проявляться мудрость.</p>
            <p>Ступень <i>мягкости и терпения</i> (прабхакари) – это различные
                созерцания, уничтожающие гнев, зависть, невежество и развивающие
                веру, сострадание, милосердие, бескорыстие.</p>
            <p>Ступень <i>лучезарности</i> (бодхипакша дхарма) – совершение
                добрых дел и воспитание добродетелей ради уничтожения следов
                эгоизма.</p>
            <p>Ступень <i>непобедимости</i> (судуржая) – изучение 4-х истин на
                более глубоком уровне.</p>
            <p>Ступень <i>обращения</i> (абхимукти) – принципы зависимого
                происхождения (пратитьясамутпада) и несубстанциональности; здесь
                в основном работает интуиция (праджня).</p>
            <p>Ступень <i>далёкого захождения</i> (дурамгама) – ещё есть страсть
                и желание стать Буддой и намерение спасти человечество.</p>
            <p>Ступень <i>неколебимости</i> (ачала) – освобождение от страсти к
                частному, культивируется высшая добродетель, заключающаяся в
                том, что вещи видятся как таковые, имеющие источником
                необусловленность абсолютного (татхата).</p>
            <p>Ступень <i>творящих добро</i> (адхумати) – нет двойственности,
                нет эгоизма, бодхисаттва стремится другим сообщить учение
                (дхарму), действия бескорыстны и совершаются без желаний. Иногда
                это проповедь через самопожертвование.</p>
            <p>Ступень <i>облако дхарм</i> (дхармамегха) – становление
                татхагатой (тем, кто ничего не меняет, кто понял, что мир и так
                совершенен, что совершенен он сам); жизнь в полном согласии с
                дхармой.</p>
            <p>Буддизм Махаяны содержит две ступени, более высокие, чем
                состояние хинаянского отшельника, святого (архата) – это
                состояние бодхисаттвы и состояние Будды.</p>
            <p>Основы нравственной жизни в Махаяне – это система шести
                трансцендентальных добродетелей – шести парамит:</p>
            <p><i>Дана-парамита</i> – подаяние: проповедь учения, отдача вещей
                (еды), проявление неустрашимости Будды – дача посвящения.</p>
            <p><i>Шила-парамита</i> – нравственность: прекращение преступных
                деяний,
                накопление добрых дел, совершение добрых дел, совершение блага
                для живых существ.</p>
            <p><i>Кшанти-парамита</i> – терпение: не таить зла, принять
                страдание,
                признать истинным буддийское учение. Очищает от гнева.</p>
            <p><i>Вирья-парамита</i> – усердие: панцирное, наивысшее,
                осуществление
                блага для живых существ. Очищает от лени и пассивности.</p>
            <p><i>Дхьяна-парамита</i> – созерцание: успокоение тела и мысли,
                истинное
                воспитание добрых качеств, осуществление блага для живых.
                Очищает от неустойчивости, сомнений.</p>
            <p><i>Праджня-парамита</i> – интуиция (мудрость): познание
                относительной
                истины, познание абсолютной истины, познание сущности принесения
                блага живым. Очищает от сомнительного и неустойчивого
                понимания. </p>
            <p>Секрет Махаяны заключён в парадоксальной установке – всё пусто,
                шуньево, но тем не менее уместно действовать: можно достичь
                состояния покоя ума – шаматхи, но мысли всё же являются. Поэтому
                буддисты считают, что нирвана не угасание, а интенсивная
                деятельность на благо других и в радость себе.</p>

        </section>

