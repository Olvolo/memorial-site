        <section id="theoretical-basis" class="mb-8">
            <h3 class="text-2xl font-bold text-center mb-4">
                Теоретические основы буддийской этики</h3>
            <h3 class="text-2xl font-bold text-center mb-4">
                Теория отсутствия индивидуального Я</h3>
            <p>
                Именно признание реальностью некоей личностной
                константы, простонародно именуемой душой, личностью,
                Я, буддизм полагает причиной противоречий, ссор,
                притязаний и конфликтов, сопутствующих жизненному
                пути человека. Буддисты утверждают, что строгий
                логический анализ, психологическое самонаблюдение,
                попытка найти, к примеру, субстрат мыслительной
                деятельности приводят нас к признанию отсутствия
                достаточных оснований для утверждения как о
                реальности индивидуального Я, так и о реальности
                бытия материальных объектов мира. Эта расступаемость
                исследуемого, будь то внешнее или внутреннее, при
                попытке найти неделимые, конкретные элементы бытия,
                буддисты и называют отсутствием Я. Иначе это
                называется в буддийских текстах «шуньей», пустотой.
            </p>
            <p>
                Но если нет Я, то некому и притязать на Моё, нет того,
                кто стремится, нет желания, нет субъекта страдания,
                следовательно, нет и самого страдания.
            </p>
            <h3 class="text-2xl font-bold text-center mb-4">
                Теория перерождений</h3>
            <p>
                Эта теория воспринята буддизмом из общепринятой
                индийской культурой положения о продолжении нашей
                жизни после смерти. Единичная жизнь – это лишь элемент
                в длинной цепи многочисленных проявлений одного и того
                же потока сознания. Каждый из нас – наследник прошлых
                рождений и предшественник будущих. Но поскольку жизнь
                есть страдание, то цель – прекратить эту мучительную
                круговерть жизней, так называемую сансару, и достичь
                прекращения страданий в успокоенности феноменального,
                в нирване.
            </p>
            <h3 class="text-2xl font-bold text-center mb-4">
                Закон кармы</h3>
            <p>
                Теория перерождения смыкается с законом морального
                воздаяния, именуемого кармой. Это – своеобразный закон
                сохранения в сфере этики, закон причинно-следственных
                связей. Мучения или неудовлетворённости этой жизни не
                есть только результат деяний этой жизни, но частично
                восходят к наследию прошлого; дурное и хорошее не
                исчезают бесследно со смертью, а формируют признаки
                нового рождения, новой индивидуальности. Также
                поступки данного рождения имеют следствием не только
                эту жизнь, но и всю дальнейшую цепь рождений. Такая
                установка позволяет буддизму объяснить любые невзгоды,
                вплоть до проблемы так называемых "невинно убиенных
                младенцев", о коих туманно рассуждает христианство.
            </p>
            <h3 class="text-2xl font-bold text-center mb-4">
                Отсутствие иерархии в этических действиях на уровне
                тела, речи и мысли</h3>
            <p>
                Поступки, совершаемые на уровне тела, и их последствия
                легко дифференцируются. Их последствия очевидны. Более
                завуалированы деяния речи и скрытыми являются наши
                мысли. Хотя источником двух первых является мысль,
                обычно воздаяние настигает нас, как правило, на уровне
                тела и речи. Буддизм считает, что неправедная,
                преступная мысль, не воплощённая в речи или теле,
                столь же опасна и чревата последствиями, как и деяния
                двух других. Отсюда понятен принцип дисциплины ума –
                йога мысли. Анализу мыслительного процесса буддисты
                уделяли первостепенное значение и не только в области
                теории, но и в области практики, развив сложнейшие
                методы психофизической подготовки личности на пути
                самосовершенствования.
            </p>
            <h3 class="text-2xl font-bold text-center mb-4">
                Принцип не причинения вреда всему живому</h3>
            <p>
                Этот принцип принят всеми религиозными направлениями
                Индии. Буддизм впитал его вместе с идеями закона
                кармы. Живое существо признаётся величайшей ценностью,
                а человеческая его форма – наиредчайшей возможностью
                для скачка в развитии, для преодоления ограниченности
                пространственно-временного существования и для
                преодоления двойственности нашего мышления.
                Самосознание считается неуничтожимым, а человеческое
                воплощение его – редким и перспективным. Отсюда
                принцип «непричинения вреда», вплоть до неуничтожения
                насекомых и "невидимых тварей". Этот принцип буддизма,
                его миролюбие, стал составной частью известных пяти
                принципов международных отношений, принятых на
                Бандунской конференции 1956 года, так называемых
                "Панча Шила".
            </p>
            <h3 class="text-3xl font-bold text-center mb-4">
                Теория непостоянства</h3>
            <p>
                Знаменитое утверждение античности – «Нельзя дважды
                войти в одну и ту же реку» – буддизм ещё ранее
                сформулировал на ином основании. Нe только река будет
                другой, главное, во второй раз в реку войдёт уже
                другой человек. Непостоянство личности наряду с
                теорией отсутствия индивидуального Я также стало
                основанием для разрушения эгоцентрических позиций
                обыденности.
            </p>
