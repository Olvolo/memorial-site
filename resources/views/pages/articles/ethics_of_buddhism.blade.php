        <!-- Дзогчен -->
        <section id="dzogchen" class="mb-8">
            <h3 class="text-2xl font-bold text-center mb-4">
                Дзогчен</h3>
            <p> Это оригинальное учение об изначальном совершенстве всего
                сущего. Возникло оно в буддизме не ранее I в. н.э. Происходит из
                северо-западной Индии, из района северного Пакистана, восходит к
                идеям раннего зороастризма о Свете. Дзогчен практиковали все
                видные буддисты Тибета, считая его высшим из всех учений, так
                называемой Девятой Колесницей. Идеи дзогчена столь оригинальны,
                что ортодоксы порой отказывали ему в буддистичности. Поэтому
                многие известные ламы, такие как знаменитый Пятый Далай- лама
                (ХVII в.), практиковали дзогчен тайно. Ныне дзогчен моден и на
                Западе, и в России.</p>
            <p>Говорят, что в дзогчене нет правил, нет обетов и обязательств.
                Это учение выходит за их уровень. Правила – это способ
                обусловить человека, если он обладает знанием. Обладая знанием,
                человек полностью принимает ответственность на себя. Поэтому в
                дзогчене, говоря об обете, говорят о четырёх принципах. Эти
                принципы не предполагают никаких действий, ибо принцип здесь
                знание, а знание выходит за пределы обещаний. Эти четыре
                принципа хотя и называют обетами, но это четыре аспекта
                знания.</p>
            <p><i>Принцип «ничто»</i>. Утверждение "ничего нет" – само по
                себе есть знание, а не понятие. Все лишено конечной сущности.
                Анализ Я и анализ природы вещей приводит к пустоте, к «ничто».
            </p>
            <p><i>Принцип присутствия</i> – всё присутствует, и вы присутствуете
                при этом присутствии. По природе все пусто, ничего нет. По сути
                же
                все – это ясность, чистая, ничем не замутненная проявленность.
                Ничего нет, например мысли, но мысли всё же продолжаются.
                Продолжает происходить событие, например, мысль, которую вы
                осознаёте. Это и есть ясность.</p>
            <p><i>Принцип уникальности</i>, единственности. Речь идёт об
                индивидууме, о его уникальном кармическом видении. В центре
                ситуации, всей вселенной, находитесь вы сами, как нечто
                совершенно
                неповторимое.</p>
            <p><i>Принцип самосовершенства</i>. Состояние человека, его
                уникальное состояние совершенно с самого начала, совершенно по
                своей сути, ничего не надо исправлять. Обычно учитель передает
                состояние <i>переживания состояния Будды</i> ученику, и ученик
                старается
                жить в этом состоянии. Но как это сделать – это и есть эти
                четыре принципа дзогчена. Пытаться жить в раз переданном
                состоянии присутствия – это и есть единственное обещание в
                дзогчене. </p>
            <p>Фраза «в дзогчене нет обетов и обещаний» предполагает, что вместо
                них есть постоянное осознание. Нет обетов, ну, кажется, и слава
                Богу, так проще. Но здесь задача потруднее. Вы не можете
                ответственность переложить на какие-то правила. Все сто
                процентов ответственности на вас самих. Одно дело, если вам
                говорят: «переходи улицу вот так-то», другое дело, если вам
                скажут: «имея дело с чем-либо, делайте всё, что в ваших
                силах».</p>
            <p>В дзогчене: не обязанность, или необходимость, а –
                ответственность. Главное, это находиться в состоянии т.н.
                «присутствия», ощущения совершенного и лишенного содержания
                осознавания – знаменитого зеркала дзогчена. Любые нравственные
                установки полезны, но если при проведении их в жизнь нет чувства
                "дна бытия", безличностного переживания присутствия, то нет
                гарантии от ошибок, от обусловленности ситуацией и заданностью
                кармическим видением.</p>

