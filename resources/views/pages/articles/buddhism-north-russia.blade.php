            <h2 class="text-center text-lg sm:text-xl font-semibold mt-8 mb-4">
                Идеал махаяны
            </h2>
            <p>
                Буддизм исповедует принцип «непричинения вреда всему живому».
                Живое существо признается величайшей ценностью, а человеческая
                форма его — наиредчайшей возможностью для скачка в развитии, для
                преодоления ограниченности пространственно-временного
                существования, преодоления двойственности нашего мышления. Этот
                принцип в буддизме махаяны дополнен идеей всесвязности всего
                живого и идеей неполноценности индивидуального спасения. Идеал
                махаяны — это бодхисаттва — просветленное существо, тот, кто
                прежде спасает других, забывая о собственном благе. Бодхисаттвой
                движет чувство великого сострадания. Известна вдохновенная
                клятва бодхисаттвы: «Да освобожу я всех живых существ!»
                Бодхисаттва — это тот, кто постиг бодхичитту — мысль, полную
                беспримерной, равной для всех любви, мысль великого сострадания.
            </p>
            <p>
                Но бодхичитта также открывает горизонт нового видения. Только
                этически чистое существо способно успешно продвигаться в
                познании, буддизм настаивает на вместерожденности нравственности
                и мудрости. Нет области современной космологии, физики,
                социологии и тем более психологии, новейшие идеи которой не
                имели бы аналогов и, более того, уже готовых практических
                соответствий в буддизме. Не то, чтобы ответы существовали в
                окончательном виде — сами установки буддизма помогают угадывать
                ответ, дают возможность чувствовать себя жителем не только
                Земли, но и Вселенной.
            </p>
            <p>
                Двадцать лет буддизм в европейской части СССР развивался
                неофициально и внецерковно, новаторски и максималистски.
                Легендарный период развития буддизма на севере России кончился,
                начался исторический период. Каковы же будут формы существования
                этого буддизма?
            </p>
