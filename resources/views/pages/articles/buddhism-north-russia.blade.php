            <p>
                В XVIII веке в России появились буддисты, и не просто появились
                — буддизм процветал, росло количество монастырей. К началу ХХ
                века в Бурятии насчитывалось 46 монастырей и 15 тысяч лам, в
                Калмыкии — 105 небольших храмов и 5 тысяч лам, в Туве 33 храма и
                около 4 тысяч лам.
            </p>
            <p>
                Такое счастливое обстоятельство позволило русским ученым
                приобщиться к живой буддийской традиции. Еще во времена Пушкина
                в рамках Российской Академии наук Яков Шмидт подготовил первый
                тибетско-русский словарь. Тибетская грамматика Шмидта вышла в
                Петербурге в 1839 году. Это создало прочную базу для изучения
                буддизма, Россия подарила миру таких ученых, как В. П. Васильев,
                И. П. Минаев, Ф. И. Щербатской, чьи работы произвели переворот в
                мировой буддологии. Нельзя не упомянуть С. Ф. Ольденбурга, О. О.
                Розенберга, Б. Я. Владимирцова, Е. Е. Обермиллера, ставших
                гордостью буддологии и тибетологии. Огромен вклад и Н. К.
                Рериха. Великий художник интуитивно предугадал будущий духовный
                союз и дружбу между Россией и Индией.
            </p>
            <p>
                Этот духовный поиск шел не только с Запада на Восток. Помимо
                естественного распространения буддизма, в конце ХIХ века в
                Бурятии наметились реформаторские движения. Одно из них имеет
                прямое отношение к регистрации буддийской общины в Ленинграде.
            </p>
            <p>
                Возглавил это движение известный бурятский лама Лубсан Сандан
                Цыденов (1842 – 1920(?)). Именно с ним была связана известная
                история, которая произошла во время торжественного приема в
                честь восшествия на престол императора Николая II. Николаевский
                зал Зимнего дворца был полон. В числе почетных гостей были
                представители бурятской делегации: Хамбо-лама Юролтуев и Сандан
                Цыденов. Вошел Николай II — все преклонили колена. В зале
                осталось две стоящих фигуры: царь и лама... Бурятской делегации
                пришлось уплатить штраф. А Сандану Цыденову — объяснить, почему
                он так поступил. «Духовный царь не кланяется царю мирскому», —
                сказал он. Сандан Цыденов носил очень редкий титул Дхарма-Раджи
                — «Царя Учения».
            </p>
