            <p>
                Расстанемся на время с Лубсан Санданом. В начале нашего века
                известный буддийский деятель Агван Доржиев стал инициатором
                создания буддийского храма в Петербурге. Доржиев был не только
                монахом, но и политическим деятелем. Стремясь к процветанию
                своей Родины, он искал пути к достижению этой цели в укреплении
                связей с Тибетом, Монголией, Китаем. Агван Доржиев был
                наставником XIII Далай-ламы, который и благословил его на
                создание храма. Идея получила всемирную поддержку (достаточно
                упомянуть такие имена, как Радлов, Щербатской, Ольденбург,
                Рерих).
            </p>
            <p>
                10 августа 1915 года храм был освящен и открыт. Его назвали
                «Источник Святого Учения (буддизма) Владыки-Отшельника (Будды),
                сострадающего ко всем», а более кратко — «Источник Учения Будды,
                сострадающего всему живому». Храм был посвящен Калачакре
                («Колесу времени»). В его штате было двадцать лам. Настоятелем
                стал бурятский лама из Цугольского дацана Ганжирва Гэгэн.
            </p>
            <p>
                В 1919 году храм разграбили… А к 1932 году храм уже практически
                не функционировал, хотя официально не был закрыт. К 1937 году
                все служившие там ламы подверглись репрессиям. Не избежал общей
                участи и 90-летний Агван Доржиев.
