                взгляд на природу, увязать свои научные знания с извечными
                этическими проблемами. Есть уже в общине «перебежчики» из
                неформальных восточных групп, включая кришнаитов. Планируется,
                что в состав Буддийского общества войдут общины различных школ —
                дзэн, ньингмапа, каржудпа и др.
