                Во время своего визита он освятил вновь открытую общину. Посетив
                храм, он совершил внутри него ритуал очищения пространства и
                пожелал, чтобы здесь процветал буддизм.
            </p>
            <p>
                Буддийское общество Ленинграда считается открытым — в него
