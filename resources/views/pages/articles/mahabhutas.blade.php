        <p class="text-justify mb-4">
            Не углубляясь в проблему пространства, отметим, что нами уже
            выявлено в буддийской теории три типа
            пространства: <em>акаша</em>, <em>пространство-сосуд</em> и <em>чувственное
                пространство</em>.
        </p>
        <p class="text-justify mb-4">
            Вернемся к процессу дифференциации дхармакаи. Махабхуты,
            реализованные от Пяти Светов, называются
            <em>внешними</em>, от бинду – <em>внутренними</em>. Разобраться во
            всех разновидностях типов махабхути помогает глава книги
            Нацог-Рандола, описывающая их свойства и дающая подробную классификацию:
        </p>
        <blockquote class="border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            <p class="my-4">
                «Если проповедовать по отраслям учение о махабхути, то имеются семь:
            </p>
            <ol class="list-decimal list-outside ml-8 mb-4 space-y-1 pl-5">
                <li class="my-2">реальность;</li>
                <li class="my-2">подлинные слова;</li>
                <li class="my-2">различие;</li>
                <li class="my-2">иллюстрирующие примеры;</li>
                <li class="my-2">свойства;</li>
                <li class="my-2">закон роста;</li>
                <li class="my-2">закон увядания.</li>
            </ol>

            <ul class="list-none pl-5 space-y-1 my-4 ml-2">
                <li class="my-2">Реальность махабхути суть то, что беспрерывно проявляется динамика появления сансары и нирваны от основ существования.</li>
                <li class="my-2">Реальности чистого (махабхути) проявляются в непрерывных актуальностях самих Пяти Мудростей.</li>
                <li class="my-2">Реальности осадочных (махабхути) проявляются в виде земли, воды, огня, воздуха и неба (эфира).</li>
                <li class="my-2">Подлинные слова определяются так: поскольку беспрерывно появляется радость (творчество) от актуальности, это и есть махабхути.</li>
                <li class="my-2">От внутреннего очищения собственного проявления мудрости появляется чистота (махабхути), но неожиданное появление (махабхути) через заблуждение пристрастия есть осадочные махабхуты.</li>
            </ul>

            <p class="my-2">Если различить их, то их четыре:</p>

            <ul class="list-disc pl-5 space-y-1 ml-8">
                <li class="my-2">чистые (прозрачные, <em>dwangs ma</em>);</li>
                <li class="my-2">осадочные (<em>snyigs ma</em>);</li>
                <li class="my-2">осадок чистоты (<em>dwangs ma'i snyigs pa</em>);</li>
                <li class="my-2">чистота осадка (<em>snyigs ma'i dwangs ma</em>).</li>
            </ul>
            <p class="my-4">
                Чистые, или прозрачные, суть трансцендентно-имманентные Пять
                Великих Махабхути: они суть
            </p>
            <ul class="list-none pl-5 space-y-1 my-4 ml-8">
                <li class="my-2">не твердая земля,</li>
                <li class="my-2">не мокрая вода,</li>
                <li class="my-2">не горячий огонь,</li>
                <li class="my-2">воздух без колебаний и</li>
                <li class="my-2">эфир (небо) с нераспространяющейся актуальностью.</li>
            </ul>
            <ul class="list-none pl-5 space-y-1 my-4 ml-2">
                <li class="my-2">Деятельности, совершающие кармовые результаты, становятся источником появления божественного тела мудрости, поэтому это есть тайные махабхуты абсолютно очищенной мудрости.</li>
                <li class="my-2">Осадки в большинстве находятся вместе с умом и познанием (<em>rig shes</em>).</li>
                <li class="my-2">Твердая земля, мокрая вода, горячий огонь, колеблющийся воздух, разлитое небо и внутренние махабхуты становятся опорами тела.</li>
                <li class="my-2">Осадок чистоты в виде мяса и кости есть земля. Кровь, жидкость в суставах (<em>chu ser</em>) и сперма есть вода. Теплота есть огонь, дыхание – воздух, внутреннее пустое пространство есть махабхути небо (пространство).</li>
                <li class="my-2">В чистоту (прозрачность) осадка входят осадки внешних махабхути, формы, которые проявляются в пяти элементах – земле, воде, огне, воздухе и небе (пространстве).</li>
                <li class="my-2">Прозрачности же их являются пятью цветами радуги. Собрав все вместе, можно дать такой олицетворяющий пример <em>чистоты осадка:</em> видимость <em>солнечного света</em> или <em>от хрусталя исходит свет</em>.  <em>(5, л. 366)</em></li>
            </ul>

            <p class="my-4">О <em>внутренних</em> свойствах (махабхути). Если различить, то
                внутренних свойств два: свойство Пяти Великих
                Махабхути мудрости заключается в том, что, имея пять цветов
                разума своей актуальностью, держит тело (будды)
                и сущность просветления и становится основой непоколебимого.
                Если различить, то</p>
            <ul class="list-none pl-5 space-y-1 ml-8 mb-4">
                <li class="my-2">не будучи твердым – крепкий,</li>
                <li class="my-2">не будучи мокрым – собирающий,</li>
                <li class="my-2">не имея колебания – движется,</li>
                <li class="my-2">не будучи горячим – может сжечь и имеет нераспространяющуюся актуальность.</li>
            </ul>
            <p class="text-justify mb-4">
                Великие Махабхути Пяти Мудростей суть:
            </p>
            <ul class="list-none pl-5 space-y-1 ml-8 mb-4">
                <li class="my-2">Земля – Будда-Лочана (<em>Sans rgyas spyan</em>)</li>
                <li class="my-2">Вода – Мамаки</li>
                <li class="my-2">Огонь – Пандари</li>
                <li class="my-2">Воздух – Тара (<em>sGrol ma</em>)</li>
                <li class="my-2">Небо – Дхату-Швари (<em>dByings phyog ma</em>)</li>
            </ul>
            <p class="text-justify mb-4">
                Эти пять матерей сидели в очищенном вместилище вместе со всеми
                буддами, входящими в пять генезисов. От их
                лучезарной формы непрерывной актуальности проявились пять
                осадков, они суть формы непрерывной актуальности
                пяти матерей, и они выросли там от основы. В пространстве,
                составленном из абсолютного очищения пяти
                матерей, вмещаются все дхармы <em>светлой сансары</em>, или <em>сансары</em>
                и <em>нирваны</em>. Поэтому
                всю <em>светлую сансару</em> называют вместилищем матери – <em>бхага</em> (<em>bha ga</em>).
            </p>
            <p class="text-justify mb-4">
                Свойство пяти осадочных махабхути суть то, что они проявляются
                как опора для выхода единичного, отличного от
                света, формы-изображения. Если различить другие дхармы,
                находящиеся в пространстве махабхути, то они
                следующие: твердый, крепкий, жидкий, горячий, колеблющийся и
                просторный, почему и открывает отверстие.
                Махабхути сосуда и содержания (<em>snod bcud</em>) становятся
                как махабхути сна.
            </p>
            <p class="text-justify mb-4">
                Если взять пять чистых, то там, где сущность пяти светов
                осваивает сердце, там же они и теряются в основе
                своей, это похоже на свет внутри хрусталя. От (этого света) свет
                актуальности формы-изображения приходит в
                движение при помощи ветра мудрости (<em>ye shes rlung</em>) и,
                таким образом, вырастает сам. Если сказать
                порядок выхода осадка, то от света-формы-изображения выросли
                пять осадков:
            </p>
            <ul class="list-disc pl-5 space-y-1 ml-8 mb-4">
                <li class="my-2">От белого света нетвердой земли вышла твердая земля <em>(5, л. 37а)</em></li>
                <li class="my-2">От желтого света немокрой воды вышла мокрая вода</li>
                <li class="my-2">От красного света негорячего огня вышел горячий огонь</li>
                <li class="my-2">От зеленого света неподвижного воздуха вышел движущийся воздух</li>
                <li class="my-2">От синего света неразлитого (нерасходящегося) неба выросло
                    расходящееся и открывающееся отверстие неба</li>
            </ul>
            <p class="text-justify mb-4">
                Способы освобождения заключаются в том, что освобождаются
                (махабхути) от того пространства, от которого
                вышли. Это потому, что махабхути являются необходимой основой,
                фундаментом (всего объективного). Здесь
                освобождаются пять махабхути от пространства формы-изображения.
                В тот период, когда просветляется светлая
                сансара (<em>snang srid</em>, вместилище), тогда угасают
                ошибочные проявления. Пять светов
                формы-изображения, исконно блестящих (чистых), входят и
                растворяются каждый в своем пространстве, через это
                Ясный Свет, просветляясь, проявляется. Это есть время, когда
                разум занял свое место и показал в
                трансцендентно-имманентном дупле драгоценности собственную
                основу и фундамент. Если исследовать в этом
                направлении, то великие махабхуты – победоносные – присущи
                каждому живому по своей сущности. Поскольку в них
                концентрирован свет-направление, то от них возникли четыре
                промежуточных света-направления: и верхние, и
                нижние (возникновение четырехмерного пространства). От цвета не
                реального, но особенного, проявляется
                отлично от других основа размышления и так же она изменяется.
                “То, что пыль (мельчайшие частицы) не имеет
                движения, есть самая вершина всех пяти махабхути”. Так сказано.
            </p>
            <p class="text-justify mb-4">
                В “Созидающем Всё” <em>(Kun byed)</em> проповедано. Расширенное
                пояснение к этому дано в “Освещении Всего”:
            </p>
            <blockquote
                class="border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
                «Я покажу реальность подлинных слов, различие махабхути вместе с
                примерами. Но ты задержи их в памяти! О
                реальности нужно рассказать с точки зрения различий, но она есть
                чистота и осадок махабхути. Реальности
                чистого есть пять мудростей. Реальность осадка суть (махабхути)
                земля, вода, огонь, воздух и неба – эти
                пять. Подлинные слова махабхути выявляются как динамика
                существования разума, от динамики существования
                возникают сансара и нирвана, поэтому и есть махабхути. Если
                различить, то получится тетралемма <em>(mu bzhi):</em> чистота (ясность), осадок, осадок чистоты,
                чистота осадка — эти четыре. О, Бодхисаттва,
                воспринимай хорошо! Чистота махабхути суть пять мудростей, а
                осадок суть земля, вода, огонь и воздух. Осадок
                чистоты суть кости, мясо и прочее. Чистота осадка есть радуга на
                небе. Примером служат лучезарность
                солнечного света или подобно тому, как от хрусталя исходит луч».
            </blockquote>
            <p class="text-justify mb-4">
                Так сказано.
            </p>
            <blockquote
                class="border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
                «Слушай, держатель ваджра! Свойство земли есть твердость,
                свойство воды – мокрота, свойство огня –
                горячесть, свойство воздуха – колебание, свойство неба –
                просторность. Они производят действия на основе
                этих же свойств. От чистого элемента земля выходит белый свет, а
                от него проявляется осадочная земля. Так,
                от чистых махабхути выходят четыре вида светов; они же
                проявляются в виде четырех осадков. Свойства чистых
                махабхути суть (земля), будучи нетвердой, – крепкая; (вода),
                будучи немокрой, – собирающая; (воздух), не
                имея
                колебаний, движется; (огонь), будучи негорячим, может жечь;
                (небо) не распространяется, но имеет
                актуальность. Сущности дхармы и дхармакаи неизменны: элемент
                земля не твердый, но крепкий. Сущности дхармы и
                дхармакаи не имеют различия – они очищены, прозрачны и не имеют
                грязи: будучи не мокрая, но собирающая, вода
                есть неразличимость сущности дхармы и дхармакаи. Воздух без
                колебаний, но безостановочный. То, что воздух,
                не имея колебаний, движется, означает: приходит туда, куда не
                шел. Поскольку нет двойственности между
                сущностями дхармы и дхармакаи, то мудрости исходят от
                пространства собственной реальности. Поскольку не
                концентрированы абсолютные сомнения, проявляющиеся для других,
                то существует не горячий, но обжигающий
                огонь. То, что просветлилось, хотя не показывается другим,
                означает, что распространяющаяся мудрость вместе
                с актуальностью имеет непрерывную актуальность, не
                распространяющуюся по всему небу. Пять махабхути осадка
                спасаются в пяти светах. Пять светов растворяются во всех порах
                чистого. Хотя так они не определяются, но
                все это делают по порядку».
            </blockquote>
            <p class="text-justify mb-4">
                Так проповедано.
            </p>
            <p class="text-justify mb-4">
                Здесь от пяти махабхути чистоты вырастают их непрерывные
                актуальности, и последние заблудились, превращаясь
                в
                осадки (которые дали развитие мира в течение трех мировых
                периодов – кальп) (<em>5, л. 36а-38а</em>).
            </p>
        </blockquote>

        <p class="text-justify mb-4">
            Буддизм не проводит анализа объективного или субъективного по
            отдельности, он анализирует мир в целом. Так,
            опора тела состоит из осадочных, т.е. внешних и внутренних
            махабхути; таким образом, обе линии дифференциации
            дхармакаи в аспекте сосуда и содержания разделены лишь в теории. В
            реальности они неразрывны.
        </p>
        <p class="text-justify mb-4">
            Цитированная выше глава Нацог-Рандола о махабхути подробно
            объясняет, как “ошиблись” свет и бинду; это есть
            классификация махабхути по четырем типам:
        </p>
        <ol class="list-upper-roman list-inside pl-8 text-gray-700">
            <li class="my-2"><strong>Чистые махабхути</strong> – трансцендентно-имманентные
                Пять Великих Махабхути:
                <ol class="list-decimal list-inside pl-6 space-y-1">
                    <li class="my-2">Нетвердая земля</li>
                    <li class="my-2">Немокрая вода</li>
                    <li class="my-2">Негорячий огонь</li>
                    <li class="my-2">Воздух без колебаний</li>
                    <li class="my-2">Небо без распространенности</li>
                </ol>
                <p class="my-2">Это есть <em>тайные махабхути</em> абсолютно очищенной мудрости.</p>
            </li>
            <li class="my-2"><strong>Осадочные махабхути</strong> (или внешние):
                <ol class="list-decimal list-inside pl-6 space-y-1">
                    <li class="my-2">Твердая земля</li>
                    <li class="my-2">Мокрая вода</li>
                    <li class="my-2">Горячий огонь</li>
                    <li class="my-2">Колеблющийся воздух</li>
                    <li class="my-2">Распахнутое небо</li>
                </ol>
            </li>
            <li class="my-2"><strong>Осадок чистоты</strong> (внутренние махабхути):
                <ol class="list-decimal list-inside pl-6 space-y-1">
                    <li class="my-2">Осадок чистоты в виде мяса и костей есть земля</li>
                    <li class="my-2">Осадок чистоты в виде крови и жидкости в суставах есть вода</li>
                    <li class="my-2">Осадок чистоты в виде теплоты есть огонь</li>
                    <li class="my-2">Осадок чистоты в виде дыхания есть воздух</li>
                    <li class="my-2">Осадок чистоты в виде внутреннего пустого пространства есть небо</li>
                </ol>
                <p class="my-2">Осадочные махабхути и осадок чистоты (собственно внешние и внутренние махабхути) становятся опорами тела.</p>
            </li>
            <li class="my-2"><strong>Чистота осадка</strong>. В чистоту осадка входят остатки
                внешних махабхути, формы которых проявляются в пяти элементах – земле, воде, огне,
                воздухе и пространстве: это и есть прозрачность осадка – пять цветов радуги.
            </li>
        </ol>
