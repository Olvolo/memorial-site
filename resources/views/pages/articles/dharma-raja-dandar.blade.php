                мог явить только лагерь того времени. Они занимались буддийской
                философией и йогой, но главное – осмыслением своей собственной
                жизни и своего положения в смысле буддийской философии и йоги. В
                течение нескольких лет Дандарон руководил этими занятиями... Это
                явилось пробой собственных сил, в которой можно видеть
                истинность практического буддизма... Вспоминая лагерь, Дандарон
                как-то однажды сказал: “Буддисту полезно родиться в России”. И
