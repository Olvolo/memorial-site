                поженились. Будучи известным перерожденцем (верующие называли
                его Бидьядара), Дандарон испытывал непрестанное давление со
                стороны местных властей, и вполне естественным стал его отъезд с
                женой в Ленинград, где он поступил учиться в институт
                авиаприборостроения. Параллельно он посещал лекции А.И.
                Вострикова, продолжив занятия тибетским языком.
            </p>
            <p id="ref-12">
