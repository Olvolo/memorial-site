            </p>
            <p id="ref-16">
                Еще в 1939 году по дороге в Бурятию, в поезде, не доезжая
                Иркутска, умерла жена Дандарона. Чудом не потерялся маленький
                сын Леонид, взятый на воспитание родственниками из Иркутска.
                Старшая дочь, очевидно, под другой фамилией затерялась в
                детдомах, тщетно ее разыскивал отец после освобождения.
            </p>
            <p id="ref-17">
                В лагерях Дандарон приобрел первых учеников, среди них были два
                немца и поляк Кокошка. В конце войны их интернировали на Запад.
                Перед выходом из лагеря Кокошка сделал ложный перелом руки и,
                спрятав под гипс листы рукописи Дандарона “Необуддизм”, вывез ее
                на Запад. Эту работу Дандарон подписал одним из своих имен –
                Зидабазар, что является бурятизированной формой от санскритского
                Читтаваджра. Это – второе имя, которое он получил от своего
                учителя Лубсан-Сандана.
            </p>
            <p id="ref-18">
                В одном из просветов между отсидками Дандарон вместе с ламой
                Падма-Доржи пишет письмо Сталину с ходатайством о возрождении
                буддийских монастырей в Бурятии. (К этому времени уже почти 10
                лет, как буддизм практически не существовал. Все 46 дацанов
                Бурятии, а также дацаны Тувы и Калмыкии были закрыты,
                большинство из них стерто с лица земли. 25 тысяч лам из
                названных республик сосланы, и большинство из них погибло. В
                Бурятию из 15 тысяч лам после войны вернулось немногим более
                двухсот.) На письмо последовала быстрая реакция: в 1946 г.
                возрождается Агинский дацан и строится новый в 40 километрах от
                Улан-Удэ – Иволгинский.
            </p>
            <p id="ref-19">
                В 1956 г., через 19 лет после ареста, последовала реабилитация,
                полностью аннулировавшая все нелепые обвинения. Дандарон
                поступает на работу младшим научным сотрудником в Бурятский
                Институт Общественных Наук. Кончились странствия по кругам
                лагерного ада, начались странствия в море буддийских текстов – в
                хранилище тибетских и монгольских рукописей и ксилографов.
                Началась внешне спокойная жизнь, но давление со стороны местных
                властей продолжалось. Девятнадцать лет лагерей не сломили
                независимый характер Дандарона, наоборот, наступил период
                расцвета сил и творчества.
            </p>
            <p id="ref-20">
                Что очень характерно для отношения к Дандарону местных бурятских
                властей, так это то, что они ненавидели его именно за его
                принадлежность к буддийской духовной традиции – религии, ими
                гонимой и отвергнутой. Что касается другой стороны его проповеди
                – проповедуемого им философского универсализма буддизма, то эта
                сторона была им просто недоступна, ибо они уже отступились от
                традиции. И Дандарон как бы служил им живым напоминанием об их
                отступничестве от прежней культуры и о невозможности реального
                принятия ими никакой другой, хотя он сам, будучи человеком
                глубоко позитивного склада, никогда не имел в виду упрекать их
                за это<sup><a href="#citation-19" class="text-blue-500 hover:underline">19</a></sup>.
            </p>
            <p id="ref-21">
                Через год после освобождения Дандарона, в 1957 году,
                возвращается на Родину после странствий по Индии, Центральной
                Азии, Китаю, Европе и Америке Юрий Николаевич Рерих, сын
                художника и ученый-филолог и буддолог с мировым именем. А.М.
                Пятигорский вспоминает:<br>
                <span class="italic">Одним из первых вопросов, обращенных ко мне, в то время его подчиненному по службе и ученику, был: “Вы знаете Дандарона?” Я сказал, что не знаю. Юрий Николаевич улыбнулся и сказал: “А я знаю”. И вдруг снова началась буддология... Стали переводиться и издаваться буддийские тексты, появились серьезные новые статьи, трактующие самые сложные вопросы религии, метафизики и психологии буддизма</span><sup><a href="#citation-20" class="text-blue-500 hover:underline"> 20</a></sup>.
            </p>
            <p id="ref-22">
                В 1959 году Бидия Дандарович Дандарон и Юрий Николаевич Рерих
                встречаются в Улан-Удэ. Это была не просто встреча двух ученых.
                Встретились духовные подвижники, представители встречных потоков
                религиозного и научного творчества. Они обсуждали проблемы
                развития буддологии и тибетологии, и не только это. Была
                составлена программа исследований, названы имена буддийских
                ученых, чьи труды надлежало переводить в первую очередь. Юрий
                Николаевич не прожил на Родине и трех лет, в 1960 году он
                умирает еще не старым от разрыва сердца. Один странник ушел,
                другой же странник продолжал работать.
            </p>
            <p id="ref-23">
                В сотрудничестве с ламами-консультантами Ж.Ж. Цыбеновым, Л.Я.
                Ямпиловым и ламой Гэмпилом Дандарон разбирает и систематизирует
                тибетский фонд БИОНа – один из богатейших подобных фондов не
                только в СССР, но и в мире. Появляются первые публикации,
                посвященные описанию собраний фонда и отдельным авторам. В
                начале 60-х идет кропотливая работа по подготовке тибетского
                словаря, увенчанная в 1963 г. изданием совместно с Б.В.
                Семичовым и Ю.М. Парфионовичем “Краткого тибетско-русского
                словаря” объемом в 21000 слов. Этот словарь в 70-х годах был
                основным пособием для развивающегося буддийского движения среди
                европейцев.
            </p>
            <p id="ref-24">
                Летом 1965 года в Улан-Удэ к Дандарону приезжает его первый
                европейский ученик, с которого началось распространение буддизма
                в нашей стране как религиозного движения. К этому времени в
                Ленинграде, странах Балтии и в Москве независимо друг от друга
                некоторые искатели духовности пришли к буддизму. У каждого был
                свой путь – либо путь профессионального филолога и востоковеда,
                либо путь долгих поисков через христианство, индуизм, суфизм,
                через изучение античной, египетской и европейской литературы
                религиозно-философского контекста. Характерно, что у каждого
                была с самого начала поиска заметная поляризация в сторону
                Индии. Велико было влияние русского востоковедения и миссии
                Рерихов. Уже были проштудированы труды Ф.И. Щербатского и О.О.
                Розенберга, стали понятными и близкими идеи мадхьямиков и
                виджнянавадинов. Необходимость встречи с Учителем прояснялась
                неотвратимостью в канве порой трагического течения жизни. Поиск
                Учителя приводил к мысли о пути в Индию. И вот спонтанно, но
                вполне закономерно реализовалась вместерожденность учительства и
                ученичества. Учитель и ученик встретились. Бидия Дандарон начал
                проповедь на Запад, первым стал передавать духовную традицию
                буддизма представителям иной культуры, иного этноса, в
                пространство гигантской страны.
            </p>
            <p id="ref-25">
                Когда Дандарон начал передачу учения на Запад, не все ламы
                восприняли его деяние. “Зачем им, западным людям, буддизм, у них
                есть свой Бог?” – говорили некоторые из них. И все же ведущие
                ламы поддержали Дандарона – среди них были лама-философ
                габжа-лама Готавон, настоятель Иволгинского дацана Цыбен
                Цыбенов, тувинский лама Гендун-Цырен, лама-врач Дашиев, оказывал
                поддержку и хамбо-лама Гомбоев. Ныне, продолжая начатое
                Дандароном, передает учение на Запад девяносточетырехлетний
                святой лама Дхарма-Доди.
            </p>
            <p id="ref-26">
                Первому всегда трудно, особенно в делах духовных; требуется
                нечеловеческое мужество – “неустрашимость Будды”. Принято
                говорить о традиции – передаче учения по цепочке учитель –
                ученик, восходящей к Будде Шакьямуни. Для Дандарона одна из
                таких линий преемственности проходит через Джаягсы Гэгэна к
                современнику Будды архату Ваджракхае. Но Дандарон – это не одна,
                это все традиции: “Я объединяю все школы – и гелуг, и каржуд, и
                сакья, и ньингма”, – таковы его слова, такова его уникальность
                для нашей страны. Среди учеников Дандарона принято говорить не о
                традиции, а о полноте учения, проповеданного Учителем. В истории
                Тибета таковыми учителями были Джово Атиша (982–1054) и Цзонхава
                (1357–1419). Сопоставление Дандарона с великими учителями
                прошлого не удивительно, ибо преодолеть сопротивление советской
                сансары и запустить вращение Колеса Благого Учения в
                пространстве жизни такой громадной и необычной страны под силу
                человеку могущественному, и человеку ли.
            </p>
            <p id="ref-27">
                Проповедь Дандарона в России и странах Балтии сопamieтся с
                деятельностью Падмасамбхавы<sup><a href="#citation-21" class="text-blue-500
                hover:underline"> 21</a></sup>
                в Тибете, чьи способности сиддха помогли преодолеть
                сопротивление светских и религиозных противников Дхармы<sup><a href="#citation-22" class="text-blue-500 hover:underline"> 22</a></sup>.
            </p>
            <p id="ref-28">
                Дандарон и Падмасамбхава, эти имена связывает уже новейшее
                предание. Еще во времена постройки знаменитой ступы Бодхнатх,
                что в Непале, согласно легенде, рассказанной Падмасамбхавой
                тибетскому царю Тисрон Децану<sup><a href="#citation-23" class="text-blue-500
                hover:underline"> 23</a></sup>,
                оба они в те далекие времена были участниками этого
                строительства. По окончании строительства во время ритуала
                Ганапуджи – жертвоприношения Древу Собрания всех Будд и святых –
                получили возможность взмолиться о будущем. Один из строителей
                пожелал родиться благим царем, покровительствующим буддизму, –
                это сам Тисрон Децан, другой пожелал родиться тантрийским
                йогином, дабы охранять Учение, – это был Падмасамбхава.
                Взмолился и Ворон, также помогавший в строительстве; он пожелал
                всегда рождаться устранителем препятствий при распространении
                буддизма. Этим Вороном, согласно кругу кудунских<sup><a href="#citation-24" class="text-blue-500 hover:underline"> 24</a></sup>
                религиозных преданий, и был в прошлом Бидия Дандарон. Таков еще
                один штрих к внешнему уровню его жизнеописания.
            </p>
