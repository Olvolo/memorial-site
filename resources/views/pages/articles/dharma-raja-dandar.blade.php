                испытаний эпопея тюремных скитаний. Полтора года он провел во
                внутренней тюрьме на ул. Воинова – шли допросы. Политзаключенным
                давали книги из тюремной библиотеки, укомплектованной из
                конфискованных книг; выбор чтения был обширен. Именно тогда он
                прочел “Закат Европы” Шпенглера – книгу, которую ценил как опыт
                изложения кармы народов и цивилизаций.
            </p>
