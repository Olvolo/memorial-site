            <p id="ref-38">
                Постепенно расширялся круг учеников, росла известность Дандарона
                не только как ученого-буддолога, но и как мастера проповеди, как
                Учителя. Единственный тулку, то есть перерожденец, был в Бурятии
                под пристальным наблюдением властей. В августе 1972 г.
                затевается “Дело Дандарона”, его обвиняют в создании религиозной
                группы и арестовывают вместе с четырьмя учениками, у остальных
                берут подписку о невыезде из Улан-Удэ. В Москве допрашивают
                известных востоковедов О.Ф. Волкову и А.М. Пятигорского –
                учеников Дандарона, разыскивают в Эстонии Л.Э. Мялля. Но “дела”
                с всесоюзным размахом не получилось, пришлось извиняться перед
                О.Ф. Волковой<sup><a href="#citation-26"
                                     class="text-blue-500 hover:underline">
                        26</a></sup>
                и А.М. Пятигорским, вернуть незаконно отобранные материалы. Но в
                Бурятии антибуддийский шабаш продолжался. Дабы не допустить
                арестованных учеников до суда, их после десятиминутного
                амбулаторного “обследования” объявляют страдающими психическими
                заболеваниями. Судят одного Б.Д. Дандарона. Защищавшая его
                адвокат Н.Я. Немеринская и адвокат, защищавший учеников, Н.Я.
                Герасименко и в суде, и в частных беседах настаивают на полной
                невиновности Дандарона, выражают неоднократные протесты
                произволу следствия и противозаконным действиям прокурора
                Байбородина. Но неправый суд свершился: пять лет лагеря общего
                режима – таков приговор.
            </p>
            <p id="ref-39">
                И снова каторга, на этот раз в поселке Выдрино, что на южном
                берегу Байкала, в продуваемом ветрами сыром и гиблом месте. Ему
                шел 58-й год, но он продолжал бороться: отказывался от
                непосильной работы, неоднократно попадая в штрафной изолятор,
                вел себя гордо и независимо. Продолжалось творчество и
                проповедь. Он написал серию писем ученикам, в которых продолжил
                синтез буддизма с идеями современной картины мира, создал новый
                труд – описание кармы современного мира, судьбы Союза, других
                народов с точки зрения Ваджрабхайраватантры – это так называемая
                “Черная тетрадь”, к сожалению, конфискованная при очередном
                лагерном обыске.
            </p>
            <p id="ref-40">
                Еще до ареста Учитель не раз говорил об уходе: “Дакини зовут”.
                Ученики просили не уходить. Но в июне 1972 г. после того, как в
                Москве в квартире О.Ф. Волковой к нему приходил Учитель
                Лубсан-Сандан, Дандарон бросил фразу: “Очевидно, придется в
                тюрьму садиться”. Таково было начало Ваджрной Проповеди, ученики
                почувствовали это на расстоянии и, не сговариваясь, приехали из
                разных концов страны в Бурятию. В этот последний период Дандарон
                учил их быстрым методам реализации недвойственности, через
                ситуацию неопределенности, через веселие и бесстрашие, через
                импровизацию и умение созерцать в любой жизненной ситуации. И
                вот сам он в зоне – в гневном мандала. В октябре 1974 г. Учитель
                не раз уходил в самадхи. При этом он предупреждал сидевших с
                ним, чтобы в это время его не трогали, и зэки к этому привыкли.
                Но вот однажды он объявил о семидневном “уходе”: “не будет
                дыхания и сердце не будет биться”. И он действительно ушел. Это
                было 26 октября 1974 года. Закончился очередной этап жизни
                Изначального Учителя. Ученый и проповедник, основатель
                современного буддизма на западе России покинул это рождение, но
                не это время.
            </p>
            <p class="text-right italic mt-4">
                В.М. Монтлевич, 1992
            </p>
