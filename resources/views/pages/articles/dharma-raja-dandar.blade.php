            <p id="ref-37">
                Основатели балагатского движения с самого начала проявляли
                интерес к старой тибетской школе ньингма и к дзогчену. Особый
                интерес был обращен к работам Нацог-Рандола (Лончен Рабжампа,
                1308–1363). Отец Дандарона Агван-Силнам в начале века перевел с
                тибетского на монгольский язык из книги “Забмо Янтиг” итоговое
                сочинение Нацог-Рандола «Карнатантра, опора великого Учения,
                называемая “Зерцало механизма глубокого содержания”». Дандарон
                начал переводить эту книгу на бурятский язык еще в лагерный
                период между отсидками, затем вернулся к переводу после 1956 г.
                Наконец, в 1971 г. приступил к переводу “Карнатантры” на русский
                язык. Перевод предназначался для учеников, Дандарон назвал его
                “Основы философии ваджраяны”. Эта книга стала соединительным
                звеном в изучении тантры и дзогчена. В дальнейшем она стала
                опорой для восприятия учений дзогчена, проповедуемых мастерами
                этой школы – выходцами из Тибета. К сожалению, во время обысков
                1972 г. рукопись “Карнатантры” исчезла в подвалах Бурятской
                Прокуратуры.
            </p>
