            <p id="ref-29">
                Есть два порядка проникновения учения в сансару – кармический и
                риддхический. Кармический порядок – это долгий путь созревания
                человечества до уровня восприятия учения и затем распространение
                новых идей через цепь передачи знания от учителя к ученику. И
                второй порядок – риддхический – это внедрение учения проповедью
                и могуществом Совершенных, через рождение и воплощение
                Просветленных и Великих Подвижников как прошлого, так и впервые
                явленных (риддхи – это “сверхъестественные” силы Будды).
            </p>
            <p id="ref-30">
                “Дандарон приходил лишь один раз”, – резко произнес недавно один
                из его учеников. Да, Тело Явленное, нирманакая всегда уникально,
                но Изначальный Учитель пребывает всегда, дело учеников – не
                упускать контакт, осознавать присутствие, а если надо – узнать.
                Свет учения пробился к нам во мрак атеизма и в эпоху падения
                нравов. Нити традиций рвались в тюрьмах и лагерях. После войны
                только два монастыря жили замкнутой жизнью в пределах Бурятии. И
                вдруг – вспышка Проповеди и выход учения на простор новых
                культур и этносов. Такое кажется чудом. Но в этом – соединение
                обоих порядков вхождения буддизма – кармического и
                риддхического. “Подпиткой сверху” иногда называют последователи
                Дандарона “внезапное” появление Учителя – согласно и вопреки
                традициям и кальпам, в единстве Естественной Свободы и
                Изначального Совершенства.
            </p>
            <p id="ref-31">
                Какой же тип буддизма проповедовал Дандарон? Уровень проповеди
                должен быть адекватен уровню слушающих, здесь тот же принцип,
                что в знаменитом “когда готов ученик, готов и Учитель”.
                Европейцы 60-х востребовали максимального, так и просили, но не
                посвящения искали, а Учителя. Достойные получили правомочности
                высоких уровней, открывшие возможность практик преображения сил
                и сознания. Этот рискованный, но быстрый путь реализации не
                случайно стал опытом духовной жизни европейцев-буддистов. Что
                характерно для распространения буддизма в новые пределы, так это
                то, что на острие его всегда идут тантрийские практики. Недаром
                Дандарон однажды воскликнул: “Тантра – на Запад!”
                Максималистские методы тантры помогли ученикам выжить в 70-е
                годы после ухода Учителя, начать период развития в 80-е и
                создать на западе России и в странах Балтии буддийскую ситуацию
                достаточной интенсивности, чтобы в 1989 г. буддийские общины
                обрели официальное признание.
            </p>
            <p id="ref-32">
                Победе Дхармы способствовала трудоемкая и неутомимая работа.
                Дандарон успел описать и частично издать сочинения “Семи
                Великих” – видных буддийских ученых из Тибета, в основном из
                района Амдо, – распространявших учение на север: в Монголию и
                Бурятию. Это – знаменитая “Стрела из Амдо”. Вот эти славные
                имена: Гунчен-Жамьян Шадпа Агван-Цзондуй (1649–1723) –
                воплощение Манджугхоши, настоятель монастыря Лавран; Второй
                Панчень-лама Лобсан-Ешей (1663–1737); Сумба-Ханбо Ешей-Балжор
                (1704–1788) – ученый и историк; Джанжа Рольби-Дорже (1717–1786)
                – пекинский хутухта, выдающийся буддийский просветитель,
                издатель буддийского терминологического словаря “Источник
                Мудрецов”; Лондол-лама Агван-Лобсан (1719–1796); Чахар
                Лобсан-Сультим и Дандар-лхарамба (1759–?).
            </p>
            <p id="ref-33">
                Методично и детально занимается Дандарон и объяснением основных
                положений теории и практики буддийской медитации и созерцания.
                Известность Дандарона росла. Ученики ехали к нему из Ленинграда,
                Москвы, Балтии, Украины и самой Бурятии.
            </p>
            <p id="ref-34">
                Он любил говорить, что это не они идут к нему в Улан-Удэ, а
                буддизм идет на Запад. И он говорил еще одну вещь, которая на
                первый взгляд кажется излишней, но на самом деле в высшей
                степени создана самим буддийским духом релятивизма. Дандарон
                утверждал, что у буддизма нет места, как нет и времени, эпохи, и
                что буддизм странствует, не зная народов, стран, климата,
                ренессансов и декадансов, общин и социальных групп. Это не
                значит, что буддизм отрицает все это, Буддизм ничего не
                отрицает. Это значит, что буддизм не знает этого, что это – не
                его дело<sup><a href="#citation-25"
                                class="text-blue-500 hover:underline">
                        25</a></sup>.
            </p>
            <p id="ref-35">
                Дандарон учил не только традиционным методам тантры. Он всегда
                настаивал на общебуддийском образовании, на обязательном
                освоении восточных языков, особенно на изучении тибетского. Сам
                он создал необходимый корпус статей, осветивших основополагающие
                проблемы буддийской философии. Вот основные из них:<br>
                Теория шуньи у мадхьямиков<br>
                51 психический элемент виджнянавадинов<br>
                Постоянные элементы в буддийской философии<br>
                Общая схема совершенствования по путям мантраяны<br>
                Буддийская теория индивидуального Я<br>
                Элементы зависимого происхождения по тибетским источникам<br>
                Махамудра как объединяющий принцип буддийского тантризма
            </p>
            <p id="ref-36">
                И все это за неполные четыре года – с 1968-го по 1972-й.
                Некоторые из названных статей опубликованы. Удалось издать и
                начало перевода “Источника Мудрецов” – главы “Мадхьямика” и
                “Парамита”. В рукописи остался перевод главы “Абхидхарма”.
                Неизданным до сих пор, но распространяемым в самиздате,
                пребывает труд Дандарона “Мысли буддиста”, содержащий изложение
                философии, этики и теории путей и на долгие годы ставший
                ценнейшим пособием для всех входящих и уже идущих по пути.
                Статьи писались в процессе общения с учениками. В беседах с ними
                Бидия Дандарович оттачивал творческий перевод терминов тантры,
                постепенно складывался язык школы Дандарона. Это была школа
                Дхармы, дисциплины ума, расширения общебуддийского кругозора. В
                контакте с ним преодолевалась присущая всем двойственность,
                пробуждалась спящая интуиция – через парадокс, абсурдность
                ситуации, импульс риска, через необходимость постоянно принимать
                решения.
            </p>
