                попал в привычные для себя климатические условия и, будучи
                молодым и здоровым, выдержал все тяготы многолетней сталинской
                каторги. Годы заключений свели его с образованнейшими людьми.
                Среди них были советские немцы – историки и философы.
                Образовался даже неокантианский кружок. В результате этого
                общения он написал цикл лекций об истории античной и европейской
                философии “Взаимоотношение материи и духа”. Тогда же он создает
                труд по эстетике на 174-х страницах общей тетради по две строчки
                в каждой клетке. Среди заключенных были и ламы. Общаясь с ними,
                Бидия Дандарон продолжил духовное буддийское образование и
                совершенствование в восточных языках.
            </p>
