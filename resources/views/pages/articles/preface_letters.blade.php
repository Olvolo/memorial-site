            </p>
            <p>
                Дандарон и Падмасамбхава — эти имена связывает уже новейшее предание. Еще во времена
                постройки знаменитой ступы Бодхнатх, что в Непале, согласно легенде, рассказанной
                Падмасамбхавой тибетскому царю Тисрондэцану, оба они в те далекие времена были
                участниками этого строительства. По окончании строительства во время ритуала
                жертвоприношения Древу Собрания всех Будд и святых — получили возможность родиться
                благим царем, покровительствующим буддизму, — это сам Тисрондэцан; другой пожелал
                родиться тантрийским йогином, дабы охранять Учение, — это был Падмасамбхава.
                Взмолился и Ворон, также помогавший в строительстве; он пожелал всегда рождаться
                устранителем препятствий при распространении буддизма. Этим Вороном, согласно кругу
                кудунских<a href="#citation-6" id="ref-6" class="citation-mark"><sup>6</sup></a>
                религиозных преданий, и был в прошлом Бидия Дандарон. Таков еще один штрих
                к внешнему уровню его жизнеописания.
            </p>
            <p>
                Есть два порядка проникновения Учения в сансару — кармический и риддхический.
                Кармический порядок — это долгий путь созревания человечества до уровня восприятия
                Учения и затем распространение новых идей через цепь передачи знания от Учителя к
                ученику. И второй порядок — риддхический — это внедрение Учения проповедью и
                могуществом Совершенных, через рождение и воплощение Просветленных и Великих
                Подвижников как прошлого, так и впервые явленных (<em>риддхи</em> —
                сверхъестественные силы Будды).
            </p>
            <p>
                "Дандарон приходил лишь один раз", — резко произнес недавно один из его учеников.
                Да, Тело Явленное, <em>нирманакая</em>, всегда уникально, но Изначальный Учитель
                пребывает
                всегда, дело учеников — не упускать контакт, осознавать присутствие, а если надо —
                <em>узнать</em>. Свет Учения пробился к нам во мрак атеизма и в эпоху падения
                нравов. Нити
                традиций рвались в тюрьмах и лагерях. После войны только два монастыря жили
                замкнутой жизнью в пределах Бурятии. И вдруг — вспышка Проповеди и выход Учения на
                простор новых культур и этносов. Такое кажется чудом. Но в этом — соединение обоих
                порядков вхождения буддизма — кармического и риддхического. "Подпиткой сверху"
                иногда называют последователи Дандарона "внезапное" появление Учителя — согласно и
                вопреки традициям и кальпам, в единстве Естественной Свободы и Изначального
                Совершенства.
            </p>
            <p>
                Какой же тип буддизма проповедовал Дандарон? Уровень проповеди должен быть адекватен
                уровню слушающих — здесь тот же принцип, что в знаменитом "когда готов ученик, готов
                и Учитель". Европейцы 60-х годов востребовали максимального, так и просили, но не
                посвящения искали, а Учителя. Достойные получили правомочности высоких уровней,
                открывшие возможность практик преображения сил и сознания. Этот рискованный, но
                быстрый путь реализации не случайно стал опытом духовной жизни европейцев-буддистов.
                Что характерно для распространения буддизма в новые пределы, так это то, что на
                острие его всегда идут тантрийские практики. Недаром Дандарон однажды воскликнул:
                "Тантра на Запад!" Максималистские методы тантры помогли ученикам выжить в 70-е
                годы после ухода Учителя, начать период развития в 80-е и создать на западе России и
                в странах Балтии буддийскую ситуацию достаточной интенсивности, чтобы в 1989 г.
                буддийские общины обрели официальное признание.
            </p>
            <p>
                Победе Дхармы способствовала трудоемкая и неутомимая работа. Дандарон успел описать
                и частично издать сочинения "Семи Великих" — видных буддийских ученых из Тибета, в
                основном из района Амдо, — распространявших Учение на север: в Монголию и Бурятию.
                Это — знаменитая "Стрела из Амдо". Вот эти славные имена: Гунчен Жамьян Шадпа Агван
                Цзондуй (1649-1723) — воплощение Манджугхоши, настоятель монастыря Лавран; Второй
                Панчен-лама Лобсан Ешей (1663-1737); Сумба-хамбо Ешей Балжор (1704-1788) — ученый и
                историк; Джанжа Ролби Дорже (1717-1786) — пекинский хутукта, выдающийся буддийский
                просветитель, издатель буддийского терминологического словаря "Источник Мудрецов";
                Лондол-лама Агван Лобсан (1719-1796); Чахар Лобсан Сультим и Дандар-лхарамба
                (1759-?).
            </p>
            <p>
                Методично и детально занимается Дандарон и объяснением основных положений теории и
                практики буддийской медитации и созерцания. Известность Дандарона росла. Ученики
                ехали к нему из Ленинграда, Москвы, Прибалтики, Украины и самой Бурятии. "Он любил
                говорить, что это не они идут к нему в Улан-Удэ, а буддизм идет на Запад. И он
                говорил еще одну вещь, которая на первый взгляд кажется излишней, но на самом деле в
                высшей степени создана самим буддийским духом релятивизма. Дандарон утверждал, что у
                буддизма нет места, как нет и времени, эпохи, и что буддизм странствует, не зная
                народов, стран, климата, ренессансов и декадансов, общин и социальных групп. Это не
                значит, что буддизм отрицает все это. Буддизм ничего не отрицает. Это значит, что
                буддизм не знает этого, что это — не его дело"<a href="#citation-7" id="ref-7"
                                                                 class="citation-mark"><sup>7</sup></a>.
            </p>
            <p>
                Дандарон учил не только традиционным методам тантры. Он всегда настаивал на
                общебуддийском образовании, на обязательном освоении восточных языков, особенно на
                изучении тибетского. Сам он создал необходимый корпус статей, осветивших
                основополагающие проблемы буддийской философии. Вот основные из них: "Теория шуньи у
                мадхьямиков", "51 психический элемент виджнянавадинов", "Постоянные элементы в
                буддийской философии", "Общая схема совершенствования по пути мантраяны",
                "Буддийская теория индивидуального <em>Я</em>", "Элементы зависимого происхождения
                по
                тибетским источникам", "Махамудра как объединяющий принцип буддийского тантризма". И
                все это за неполные четыре года — с 1968-го по 1972-й. Некоторые из названных статей
                опубликованы. Удалось издать и начало перевода "Источника Мудрецов" — главы
                "Мадхьямика" и "Парамита". Труд Дандарона "Мысли буддиста", содержащий изложение
                философии, этики и теории Путей, на долгие годы стал ценнейшим пособием для всех
                входящих и уже идущих по Пути. Статьи писались в процессе общения с учениками. В
                беседах с ними Бидия Дандарович оттачивал творческий перевод терминов тантры,
                постепенно складывался язык школы Дандарона. Это была школа Дхармы, дисциплины ума,
                расширения общебуддийского кругозора. В контакте с ним преодолевалась присущая всем
                двойственность, пробуждалась спящая интуиция — через парадокс, абсурдность ситуации,
                импульс риска, через необходимость постоянно принимать решения.
            </p>
            <p>
                Основатели балагатского движения<a href="#citation-8" id="ref-8"
                                                   class="citation-mark"><sup>8</sup></a>
                с самого начала проявляли интерес к старой
                тибетской школе ньингма и к дзогчену. Особый интерес был обращен к работам Нацог
                Рандола (Лончен Рабжампа, 1308-1363). Отец Дандарона Агван Силнам Тузол Доржи в
                начале века перевел с тибетского на монгольский язык из книги "Забмо Янтиг" итоговое
                сочинение Нацог Рандола "Карнатантра, опора великого Учения, называемая "Зерцало
                механизма глубокого содержания". Дандарон начал переводить эту книгу на бурятский
                язык еще в лагерный период между отсидками, затем вернулся к переводу после 1956 г.
                Наконец, в 1971 г. приступил к переводу "Карнатантры" на русский язык. Перевод
                предназначался для учеников. Дандарон назвал его так: "Основы философии ваджраяны".
                Эта книга явилась соединительным звеном в изучении тантры и дзогчена. В дальнейшем
                она стала опорой для восприятия учений дзогчена, проповедуемых мастерами этой школы
                — выходцами из Тибета. К сожалению, во время обысков 1972 г. рукопись "Карнатантры"
                исчезла в подвалах Бурятской прокуратуры.
            </p>
            <p>
                Постепенно расширялся круг учеников, росла известность Дандарона не только как
                ученого-буддолога, но и как мастера проповеди, как Учителя. Единственный тулку, то
                есть перерожденец, был в Бурятии под пристальным наблюдением властей. В августе 1972
                г. затевается "дело Дандарона", его обвиняют в создании религиозной группы и
                арестовывают вместе с четырьмя учениками, у остальных берут подписку о невыезде из
                Улан-Удэ. В Москве допрашивают известных востоковедов О. Ф. Волкову и А. М.
                Пятигорского — учеников Дандарона, разыскивают в Эстонии Л. Э. Мялля. Но "дела" с
                всесоюзным размахом не получилось, пришлось извиняться перед О. Ф. Волковой и А. М.
                Пятигорским, вернуть незаконно отобранные материалы.
            </p>
            <p>
                Но в Бурятии антибуддийский шабаш продолжался. Дабы не допустить арестованных
                учеников до суда, их после десятиминутного амбулаторного "обследования" объявляют
                страдающими психическими заболеваниями. Судят одного Б. Д. Дандарона. Защищавшая его
                адвокат Н. Я. Немеринская и адвокат, защищавший учеников, Н. Я. Герасименко и в суде
                и в частных беседах настаивают на полной невиновности Дандарона, выражают
                неоднократные протесты по поводу произвола следствия и противозаконных действий
                прокурора Байбородина. Но неправый суд свершился: пять лет лагеря общего режима —
                таков приговор.
            </p>
            <p>
                И снова каторга, на этот раз в поселке Выдрино, что на южном берегу Байкала, в
                продуваемом ветрами, сыром и гиблом месте. Учителю шел 58-й год, но он продолжал
                бороться: отказывался от непосильной работы, неоднократно попадая в штрафной
                изолятор, вел себя гордо и независимо. Продолжалось творчество и проповедь. Он
                написал серию писем ученикам, в которых продолжил синтез буддизма с идеями
                современной картины мира, создал новый труд — описание кармы современного мира,
                судьбы Союза, других народов с точки зрения Ваджрабхайраватантры — это так
                называемая <em>Черная Тетрадь</em>, к сожалению, конфискованная при очередном
                лагерном обыске.<a href="#citation-9" id="ref-9" class="citation-mark"><sup>9</sup></a>

            </p>
            <p>
                Еще до ареста Учитель не раз говорил об уходе: "Дакини зовут". Ученики просили не
                уходить. Но в июне 1972 г., после того как в Москве в квартире О. Ф. Волковой к нему
                приходил Учитель Лубсан Сандан, Дандарон бросил фразу: "Очевидно, придется в тюрьму
                садиться". Таково было начало Ваджрной Проповеди, ученики почувствовали это на
                расстоянии и не сговариваясь приехали с разных концов страны в Бурятию. В этот
                последний период Дандарон учил их быстрым методам реализации недвойственности, через
                ситуацию неопределенности, через веселье и бесстрашие, через импровизацию и умение
                созерцать в любой момент жизни. И вот сам он в зоне — в гневном мандале. В октябре
                1974 г. Учитель не раз уходил в самадхи. При этом он предупреждал сидевших с ним,
                чтобы в это время его не трогали, и зэки к этому привыкли. Но вот однажды он объявил
                о семидневном "уходе": "Не будет дыхания и сердце не будет биться". И он
                действительно ушел. Это было 26 октября 1974 года. Закончился очередной этап жизни
                Изначального Учителя. Ученый и проповедник, основатель современного буддизма на
                западе России покинул это рождение, но не это время.
            </p>
