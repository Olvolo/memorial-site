                Они виделись недолго - месяц в октябре 1956-го и две недели в январе 1957 года. Но
                впечатления от этих встреч были столь велики, что породили у Бидии Дандаровича
                страстное стремление общаться постоянно. Он реализовал это через письма, которые
                писал иногда по два на день. Ковригина жила в Литве, в Тракае и в Вильнюсе, работая
                там экскурсоводом. Всего за три года переписки он написал Наталии Ковригиной 99
                писем, из которых 70 были написаны за первые шесть месяцев. После переезда Дандарона
                в Бурятию переписка стала ослабевать. Когда же ему стало известно о замужестве
                Ковригиной, она и вовсе прекратилась. Последнее письмо было им написано 15 июля 1959
                года и отправлено из Улан-Удэ.
            </p>
            <p>
                Все эти письма сохранились. Через двадцать лет после ухода Дандарона с ними
                посчастливилось ознакомиться и нам. К сожалению, судьба писем Наталии Юрьевны к
                Дандарону не выяснена. И точно известно от самой Н. Ю. Ковригиной-Климанскене, что
                письма Дандарона к В. Э. Сеземану были сожжены ее матерью.
            </p>
            <p>
                Образ жизни Дандарона в этот период был подвижническим, он торопился наверстать
                упущенное. Дни проводил в библиотеке, вечером до поздней ночи читал или писал письма
                Наталии Юрьевне. Он как бы запасался знаниями впрок. Знания и любовь поглощали его
                полностью, о чем он пишет так: "Наука и Наташа; потом, видимо, появится процесс
                совершенствования - сосредоточение. Не хожу ни в кино, никуда" (письмо N 14). В этот
                период накопления материала к созданию новой буддийской теории Дандарон нуждался в
                оппонировании. Но его не было, не было ни в лице коллег, ни в лице противников; но
                была Наталия Юрьевна, и весь заряд интеллектуального созидательного запала обрушился
                на нее: "Не писать мне трудно, ибо голова заполнена идеями, которые необходимо
                изложить на бумаге, но зато здесь (при этом <em>ред</em>.) требуется колоссальное
                время для
                доставания еды и примитивного одеяния. Если окажусь на воле (Дандарон еще не был
                реабилитирован <em>ред</em>.), я, несомненно, буду писать; если не издадут в СССР, а
                это
                будет именно так, то буду рукописи отправлять за границу. Для всего этого не
                требуется мне ни Ленинград, ни Москва. Тогда я буду настоящим отшельником
                (анахоретом)" (письмо N 61).
            </p>
            <p>
                По своему характеру Дандарон был, безусловно, максималистом; и это не взгляд со
                стороны, это - самооценка. "У меня с детства был решительный характер. Чтобы
                добиться поставленной цели, я не останавливался ни перед чем" (письмо N 23). "Ты
                права, я должен идти к цели, к йогической практике; если не смогу сделать этого, то
                не хочу бесцельно жить, как живут или жили два с половиной миллиарда людей. Если я
                буду ученым-философом, учение которого будет известно всему миру и принесет много
                пользы, и то этого недостаточно, чтобы жить" (письмо N 15).
            </p>
            <p>
                Хотя идеи необуддизма были в общих чертах развиты в работе, что вывез на Запад
                Кокошка, Дандарон продолжает новое переосмысление древнего учения: "Несмотря,
                однако, на то, что основа мира представляет собой нечто непонятное, ум человека не
                может успокоиться на таком признании и делает попытки осмыслить эту основу,
                представить ее совершенно определенным, наглядным образом. Но на самом деле это
                представление не осуществимо: наглядное представление бога приводит к противоречиям.
                Однако основатели теории (системы) необуддизма вынуждены строить свою метафизическую
                теорию, которая может объяснить происхождение мира и совершенство атмана, не впадая
                в противоречие с современной наукой... Это - моя "система", она может выдержать
                испытание в том случае, если способна будет объяснить все явления феноменального и
                духовного мира без противоречий. Пока все это (в основном) находится в голове. Тебе
                буду излагать в схематической форме" (письмо N 14).
            </p>
            <p>
                Прошло два месяца, как они познакомились, два месяца Дандарон почти через день
                излагает свои взгляды на буддизм в письмах к Наталии Юрьевне. И вот, как бы
                оглядываясь назад, он пишет: "Эта переписка дает материал не только одной тебе, она
                возбуждает меня к творческой деятельности, и поэтому я пишу, мы вместе создаем новую
                систему". Здесь в оценке Дандарона Наталия Юрьевна предстает как муза его
                философского творчества. Он так и пишет: "Ты меня вдохновляешь и даешь импульс
                творчеству. Из-за тебя я ночами сижу и копаюсь в материалах, которых нет ни на одном
                из европейских языков. Ты подумай, может, оценишь нашу переписку через много лет"
                (письмо N 24).
            </p>
            <p>
                В этом же письме он дает предполагаемую схему изложения необуддизма. "Мне кажется,
                лучше излагать нашу систему в таком плане:
            </p>
            <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
                <li>Индивидуальное <em>Я;</em></li>
                <li>Психология;</li>
                <li>Учение о зависимом происхождении;</li>
                <li>Этика;</li>
                <li>Карма и новое рождение;</li>
                <li>Еще раз о нирване;</li>
                <li>Отношение к Богу;</li>
                <li>Практическая религия;</li>
                <li>Теория познания;</li>
                <li>Пути совершенствования у йогачаров.</li>
            </ol>
            <p>
                Я полагаю, что если излагать в таком плане, то войдет все - как сансара, так и
                нирвана" (письмо N 24).
            </p>
            <p>
                Удивительно, но почти все из перечисленных тем Дандарон выполнил за предоставленные
                ему судьбой шестнадцать лет жизни на воле, достаточно сравнить этот план со списком
                опубликованных и неопубликованных его работ.
            </p>
            <p>
                Обстоятельства жизни в Москве тем временем складываются неблагоприятно. Нет прописки
                и, следовательно, нет возможности устроиться на работу ни в Москве, ни в Ленинграде,
                где о нем хлопочет профессор Алексеев и где получено принципиальное согласие на его
                прием в ЛО ИВАН от И. А. Орбели. Столица же готовится к Международному студенческому
                фестивалю, ужесточается паспортный режим, и Дандарон это сразу почувствовал. 17
                апреля 1957 года он пишет Ковригиной: "Я живу в Москве на птичьих правах. С 13
                апреля не захожу к М.(арии) А.(лексеевне Свирской), ночую, где придется. Но
                постоянно держу с ними телефонную связь. Что мне нужно - книги, письма и одежду -
                мне выносит В.(ладимир) П.(авлович) в условленное место. Днем сижу в библиотеке им.
                Ленина. Иногда приходится спать на улице, и, как назло, в Москве в эти дни стоит
                очень холодная погода. По ночам по всем улицам шныряют патрули (милиция), одним
                словом, невесело; невыносимо хочется спать и отдохнуть с вытянутыми ногами. Я ведь
                от сансары не жду ничего хорошего... Передо мною стоит вопрос, где переждать
                окончательный ответ из Ленинграда. Есть четыре таких места: 1) поехать в Ленинград и
                ждать там; 2) ехать в Нахабино (в 40 км от Москвы) к Познякову, и там переждать; 3)
                поехать в Вильнюс - к В.(асилию) Э.(мильевичу Сеземану) и к тебе; 4) поехать домой,
                то есть на родину, к матери" (письмо N 58). Мы знаем, что осуществился четвертый
                вариант, и 2 мая Дандарон выехал в Бурятию, с которой и была далее связана его
                личная жизнь и работа. Но это будет впереди, пока же он продолжал читать и писать.
            </p>
            <p>
                Дандарон просматривал литературу не только на русском языке, он неплохо знал
                немецкий, так что спокойно мог читать в подлиннике и Хайдеггера, и Фейхтвангера. К
                последнему он обратился из-за своего постоянного интереса к парапсихологии: "Наконец
                я нашел книгу Фейхтвангера, где пишется о парапсихологии. Это целый роман,
                называется он "Die Brüder Lautensack"... Я сейчас читаю сам роман, но идет медленно,
                так как он написан на немецком языке" (письмо N 45). И еще на эту же тему:
                "Парапсихология в СССР не признана, потому что она угрожает ударить по самому корню
                марксизма. Наши биологи и психологи еще не нашли марксистского толкования
                парапсихологии. Науку ничем не остановишь, она так или иначе, рано или поздно
                проникнет и к нам. Надо стараться стать пионером этой науки у нас. Самоотверженно
                настаивать и доказывать полезность этой науки через печать, как делал Тимирязев с
                дарвинизмом. Нужно довести до сознания людей, чтобы хотя бы перестали мешать
                производить парапсихологические опыты. Нужно читать журналы Парапсихологического
                института в Касабланке (Северная Африка), журналы есть в библиотеке им. Ленина и в
                Публичной библиотеке в Ленинграде. Изучать, изучать" (письмо N 41).
            </p>
            <p>
                Разнообразие интересов Дандарона, широта охвата исследуемого материала и постановка
                к разрешению не просто узких проблем, а задач вселенских, вечных поражают. Вот
                несколько цитат из писем.
            </p>
            <p>
                "Мне страшно хочется исследовать психологию школ <em>санкхья</em> и <em>йога</em> и
                сопоставить ее с
                европейскими школами, начиная с Платона, Плотина, Гегеля, Фрейда до школы
                парапсихологов..." (N 87).
            </p>
            <p>
                "Смутное понятие божества или предположение его существования приводит нас к
                установлению первого понятия субстанции, к определению ее, как чего-то, что способно
                существовать в себе и служить объектом, носителем других реальностей. Однако
                основание, необходимое для объяснения мира, рассматриваемого сколько с динамической,
                столько же и со статической точки зрения, есть бог, сущность которого - нирвана" (N
                16).
            </p>
            <p>
                "Таким образом, разумный закон, который создал природу вещей, Будда называет
                сознанием", - резюмирует Дандарон в письме N 29 диалог Будды и Ананды о сознании из
                "Маханидана-сутры".
            </p>
            <p>
                Мысль Дандарона не останавливалась даже перед самыми фундаментальными проблемами,
                такими, например, как вопрос о непрерывно длящемся процессе творения, совершающемся,
                по его мнению, через совершенствующееся движение индивидуального сознания -
                <em>виджняны</em>, которую он в письмах называет <em>атманом</em>, подразумевая его
                индивидуальное
                несовершенство. "Так как основным условием и первопричиной всякого движения и
                развития является этот же атман, то всякий процесс развития в природе (в материи) и
                в мире духа обусловливается противоречиями. Отсюда вывод: в природе самого духа (в
                самом атмане) был необходимо заложен диалектический закон развития. Благодаря тому,
                что совершенствующийся процесс развития ведется разумной волей, направление этого
                процесса определяется не только причиной, толкающей как бы сзади, но и тем конечным
                результатом, которым завершается процесс. Нирвана как бы манит и завлекает к себе
                атман как цель. В этом заключается участие Абсолюта в мировом процессе. Значит,
                решающее значение для течения процесса имеет не только прошлое, итог которого дает
                настоящее, но будущее состояние атмана" (N 17). Этот отрывок чрезвычайно важен для
                понимания утверждения буддийской тантры о возможности спасения в течение одной жизни
                и более того - в каждое мгновение оной, ибо процесс разделения сансары и нирваны не
                в отдаленном прошлом, он с неумолимой периодичностью и немыслимой частотой
                репродуцируется постоянно, а значит, и шанс к реализации не в далеком будущем, а
                здесь, сейчас, постоянно.
            </p>
            <p>
                Интересно мнение Дандарона об эсхатологической проблеме в христианстве: "В одном из
                писем ты спрашивала, как я отношусь ко второму пришествию Христа? Я отвечаю, что
                отношусь равнодушно. Во всяком случае, не боюсь этого прихода, ибо если он - Бог (в
                этом я не сомневаюсь), то он не будет организовывать эту комедию - Страшный суд. Ибо
                это противоречит его предикату - Всеблагости. Зачем он будет посылать людей
                (несовершенных) на вечный огонь, тогда как человеческий суд и то имеет
                снисхождение?" (N 28).
            </p>
            <p>
                Характерно отношение Дандарона к тем людям, которые за долгие годы его лагерных
                скитаний причинили ему зло, тем более к тем, кто был повинен в его аресте:
                "Клеветник, провокатор, который посадил меня и других двадцать лет назад, теперь
                работает в Институте востоковедения Академии Наук. Он монгол, летом в 1956 г. ездил
                в Индию во главе советских буддистов на праздник 2500-летия со дня рождения Будды.
                Алексеев (профессор Ленинградского университета - <em>ред</em>.) уговаривал меня
                целый час,
                чтобы я не трогал при людях этого негодяя. Но когда он увидел, что я с ним
                разговариваю, как ни в чем не бывало, он сказал: "Великое дело - буддизм, если он
                может укротить такого человека, как ты" (N 55).
            </p>
            <p>
                Понять это алексеевское "укротить" можно по следующему эпизоду из лагерной жизни
                Дандарона: "В 1949 году в Тайшетской пересылке один немец (майор танковых войск)
                посмеялся с добрым намерением, сказав: "Вот советская власть не брезгует ничем,
                никем, даже такими полуобезьянами, как он; всех сажает в тюрьму" (при этом показал
                на меня пальцем), что было после этого? Я совершенно потерял облик человека,
                остервенел, действительно превратился в животное. За немца заступились другие немцы
                (двадцать с лишним человек). В это время во мне проснулся демон, сатана: я, будучи
                один, обратил в бегство всех. Только за одну такую фразу этому майору пришлось
                пробыть в больнице около месяца, мне же — в карцере десять суток. Не карцер меня
                наказывал, а угрызения совести; потом пришлось носить свою пайку (хлеба) в больницу
                этому немцу каждый день. Плакал и мучился. Вот как бывает" (№ 23).
            </p>
            <p>
                Более полугода, проведенные Дандароном в Москве с осени 1956 года по май 1957-го,
                хотя и короткий срок по сравнению со всей жизнью, но для него был, очевидно,
                многозначительным. И дело не в том, что он страстно полюбил и всей силой души и
                убеждения стремился к сотрудничеству с любимым человеком на уровне необычайном,
                духовном, и не в том, что, попав в общество людей, если не понимавших его до конца,
                то угадавших в нем человека неординарного и старавшихся помочь ему в его занятиях,
                для него эти несколько месяцев были пиком научной творческой деятельности,
                определившим его дальнейший исследовательский поиск, утолившим за короткий срок
                накопившуюся за долгие годы неутоляемую жажду знания. "Изучать, изучать", — так он и
                пишет в одном из писем. За эти месяцы он проделал неимоверный труд, что под силу
                другим за целые годы, сформировал общую схему исследования буддизма. За короткий
                срок он проштудировал практически всю историю философии Запада от античности до
                современности; восполнил пробелы в изучении буддизма, перечитав ранее недоступную
                ему литературу по буддологии. Медленно и мучительно он приспосабливается к обычной
                жизни, вживается в обычный ритм ее течения. Для таких, как он, травмированных адом
                лагерей, это было непросто, и не всем это удавалось, тем более, когда человек
                стремился к полноценной творческой деятельности. Вот его собственные мысли о таком
                "переходном" периоде, переходном не только для отдельных личностей, но и для всей
                страны: "Ты, как умная девушка, должна ближе присмотреться к психологическим
                последствиям, вытекающим из охватывающего все углы нашей страны перелома в эпоху
                культа личности. Такая кошмарная эпоха, как 1937-й год и послевоенные годы,
                представляют очень благоприятную почву для повышенной нервозности. С одной стороны,
                распадается нормальная форма человеческой жизни, она заменяется ужасными темницами и
                лагерями, разрушаются верования и чувства; с другой — возникает настоятельная
                необходимость приспособления к новым условиям "жизни". Такие ужасы переходных эпох,
                которые постоянно повторяются в сансаре, представляют очень благоприятную почву для
                нервного заболевания. При таких условиях психика людей расшатывается и
                взвинчивается. Нервы натягиваются, звучат болезненно и надломленно, содрогаются от
                каждого ничтожного соприкосновения..." (№ 43).
            </p>
            <p>Можно представить, прочтя этот отрывок, как Дандарон, прекрасно всё понимая, шел
                навстречу жизни, ни от чего не отказываясь, а наоборот, требуя от жизни
                сверхвозможного — знания, любви, йогической практики, созидательной интеллектуальной
                деятельности. Но на этом этапе в любви жизнь ему отказала. Наталия Юрьевна не стала
                его женой, как он хотел. Но она стала поистине музой его духовного подъема,
                вдохновительницей многих его поисков. Мы не знаем ее писем к нему, но о них косвенно
                можно судить по его ответам, можно понять, что своими вопросами она часто, может
                быть, не желая этого, заставляла его перерывать горы литературы, чтобы ответить ей,
                а порой и себе на те вопросы, что она ставила. В основном, судя по его ответам, это
                были вопросы морали и природы Бога. В ее лице он нашел такого оппонента, которого,
                пожалуй, не смог бы обрести и в научном коллективе, тем более, что в этом
                удивительном философском и любовном диалоге не было никаких цензурных сдерживающих
                факторов. Такой диалог не мог состояться прилюдно, да, пожалуй, и при личном
                контакте, глаза в глаза. Это особый жанр, когда близкий и любимый собеседник все же
                абстрагирован, его нет рядом, он не может мгновенно согласиться с тобой или дать
                знак согласия. Пожалуй, именно такой диалог особенно благоприятен для раскрытия, для
                развития нити сложных философских построений.</p>
            <p>
                Характерная черта писем Дандарона — обилие цитат. Он приводит целые абзацы из
                сочинений С. Радхакришнана, А. Шопенгауэра, И. Канта, Л. Фейхтвангера и др. Такой
                стиль пересказа чужих мыслей в канве собственного авторского повествования может
                показаться странным для европейского читателя, даже для любителя чтения мемуаров и
                переписок. Но для человека восточной культуры в этом нет ничего странного. Ведь
                восточная культура более, чем западная, строится на принципе традиции, исключительно
                на принципе традиции, то есть на восприемстве прошлого во всей его целостности с
                прибавлением небольших авторских добавок к уже созданному и освященному. Так что
                Дандарон, как человек, хотя и оказавшийся на Западе в чудовищной ситуации лагерной
                жизни, по сути остался человеком восточной культуры. Он ткет основание какой-либо
                проблемы из высказываний именитых предшественников и только в конце увенчивает ее
                собственной мыслью. Так построены многие известные сочинения буддийских авторов
                прошлого, достаточно заглянуть в знаменитый "Агрим" Цзонхавы, сплошь состоящий из
                цитат индийских и тибетских авторов.
            </p>
            <p>
                Жизнь в Москве становилась бесперспективной: невозможно прописаться, не берут на
                работу, отношения с Наталией Юрьевной мучительны — у нее появляется жених. 2 мая
                1957 года Бидия Дандарович выезжает в Улан-Удэ, 7-го он уже на родине — в Кижинге.
                Казалось бы, должно наступить успокоение, защищенность, отдохновение. Но он
                испытывает иное: "Настроение довольно тяжелое, в душе моей, по-моему, нет ничего,
                кроме тебя. Меня очень угнетает то, что я уехал от тебя так далеко, Но здесь,
                кажется, я найду в обильном количестве ту литературу, которая нам нужна. Мне
                сообщили, что в библиотеке моего отца, то есть в моей библиотеке, имеются книги
                Рамачараки на русском языке" (№ 65). Но постепенно он втягивается в привычную
                исследовательскую работу, читает запоем, начинает переводы. "Мой приезд на родину,
                видимо, тоже связан с совершенствующим движением, ибо здесь нашел такую литературу
                на тибетском и монгольском языках, которую я не нашел нигде. Но эта литература мне
                нужна именно в эту стадию моего развития" (№ 68), — сколь буддийское самоощущение
                заключено в этом высказывании, в нем — самовосприятие себя как постоянно меняющейся
                и развивающейся личности, как реализатора буддийского Пути.
            </p>
            <p>
                Как бы ни захватывала его работа над текстами, чувство не утихало, и мнилась
                грядущая перспектива: "Моя хорошая, добрая, не ищи больше ягод в лесу, мы пойдем
                рука об руку к фруктам нирваны" (№ 73).
            </p>
            <p>
                6 сентября 1957 года Дандарон приезжает в Москву. Следует его первая встреча с Юрием
                Николаевичем Рерихом: "Здесь встретил одного крупного ученого-тибетолога с
                европейским именем, некоего Рериха. Он эмигрант, последние годы жил в Индии и
                Тибете. По договору с нашим правительством он приехал в Москву. Дали ему
                персональный оклад в четыре тысячи рублей и квартиру. Меня с ним познакомил проф.
                Черемисов (тибетолог). Он (Рерих), оказывается, составляет тибетско-русский словарь
                и очень любезно пригласил меня участвовать в его составлении.
            </p>
            <p>
                Кроме того, он говорит, что в Москве при Институте китаеведения организуется группа
                по изучению Тибета. Руководителем (как он говорит: президентом) будет он, Рерих.
                Говорит, что будет ставить вопрос обо мне.
            </p>
            <p>
                "По внешности он похож на джентльмена, насколько он джентльмен в деле, это мы
                увидим.
                Пока все это похоже на дым" (№ 78). Ю. Н. Рерих выполнил, как мы знаем, свои планы —
                словарь уже после его смерти (1960 г.) увидел свет.
            </p>
            <p>
                Поездка в Москву была последней попыткой устроиться на работу в Институте
                востоковедения, но и на этот раз она не удалась: штатной единицы не нашлось ни в
                Ленинграде, ни в Москве. "В общем, впереди туман, никакого просвета: как в жизни,
                так и в любви" (№ 78). Тем не менее, ему в Москве в этот приезд удается даже
                подработать: "Сейчас в Москве я веду одну литературную работу, связанную с
                300-летием "добровольного" присоединения бурят-монголов к русскому народу... Пока
                работаю в библиотеке им. Ленина, копаюсь в архивах XVII и XVIII веков" (№ 79).
            </p>
            <p>
                9 октября Дандарон уезжает в Улан-Удэ с намерением устроиться на работу по своей
                буддологической профессии. 1 ноября он устраивается на работу в филиале АН в
                Институте культуры в секторе тибетологии в качестве внештатного научного сотрудника.
                Переписка с Ковригиной продолжается, но не так часто, как раньше. Некоторая надежда
                появилась у него, когда узнал о разрыве Наташи с Яном, ее женихом.
            </p>
            <p>
                Лето 1958 года Дандарон проводит в трудной и не очень интересной для него
                диалектологической полевой экспедиции. Осенью участвует в экспедиции по сбору
                лекарственных трав в Саянских горах. И вот, наконец, долгожданное событие: "С
                сегодняшнего дня я принят в штат научных сотрудников Института культуры и
                перебираюсь на новую квартиру" (№ 91 от 6 октября 1958 г.).
            </p>
            <p>
                За зиму и весну 59-го года Дандарон отсылает в Вильнюс семь писем и 15 июля 1959
                года пишет последнее, т.к. узнал о замужестве Наталии Юрьевны, она становится
                Климанскене. Он же полностью погружается в научную работу, начались странствия в
                море буддийских текстов — в хранилище тибетских и монгольских рукописей и
                ксилографов. Началась внешне спокойная, но насыщенная творчеством жизнь.
            </p>
            <p>
                Девятнадцать лет лагерей не сломили независимый характер Дандарона, наоборот,
                наступил период расцвета сил и творчества. "Что очень характерно для отношения к
                Дандарону местных бурятских властей, так это то, что они ненавидели его именно за
                его принадлежность к буддийской духовной традиции-религии, ими гонимой и
                отвергнутой. Что касается другой стороны его проповеди — проповедуемого им
                философского универсализма буддизма, то эта сторона была им просто недоступна, ибо
                они уже отступились от традиции. И Дандарон как бы служил им живым напоминанием об
                их отступничестве от прежней культуры и невозможности реального принятия ими никакой
                другой, хотя он сам, будучи человеком глубоко позитивного склада, никогда не имел в
                виду упрекать их за это"<a href="#citation-4" id="ref-4" class="citation-mark"><sup>4</sup></a>
                .
            </p>
            <p>
                Через год после освобождения Дандарона, в 1957 г., возвращается на Родину после
                странствий по Индии, Центральной Азии, Китаю, Европе и Америке Юрий Николаевич
                Рерих, сын художника и ученый-филолог, буддолог с мировым именем. "Одним из первых
                вопросов, обращенных ко мне, в то время его подчиненному по службе и ученику, был, —
                вспоминает А. М. Пятигорский, — "Вы знаете Дандарона?" Я ответил, что не знаю. Юрий
                Николаевич улыбнулся и сказал: "А я знаю". И вдруг снова началась буддология...
                Стали переводиться и издаваться буддийские тексты, появились серьезные новые статьи,
                трактующие самые сложные вопросы религии, метафизики и психологии буддизма"<a
                    href="#citation-5" id="ref-5" class="citation-mark"><sup>5</sup></a>.
            </p>
            <p>
                В 1959 г. Бидия Дандарович Дандарон и Юрий Николаевич Рерих снова встречаются в
                Улан-Удэ. Это была не просто встреча двух ученых. Встретились духовные подвижники,
                представители встречных потоков религиозного и научного творчества. Они обсуждали
                проблемы развития буддологии и тибетологии, и не только это. Была составлена
                программа исследований, названы имена буддийских ученых, чьи труды надлежало
                переводить в первую очередь. Юрий Николаевич не прожил на Родине и трех лет, в 1960
                г. он умирает еще нестарым от разрыва сердца. Один странник ушел, другой же странник
                продолжал работать.
            </p>
            <p>
                В сотрудничестве с ламами-консультантами Ж. Ж. Цыбеновым, Л. Я. Ямпиловым и ламой
                Гэмпилом Дандарон разбирает и систематизирует тибетский фонд БИОНа — один из
                богатейших подобных фондов не только в СССР, но и в мире. Появляются первые
                публикации, посвященные описанию собраний фонда и отдельным авторам. В начале 60-х
                идет кропотливая работа по подготовке тибетского словаря. Этот словарь в 70-х годах
                был основным пособием для развивающегося буддийского движения среди европейцев.
            </p>
            <p>
                Летом 1965 г. в Улан-Удэ к Дандарону приезжает его первый европейский ученик, с
                которого началось распространение буддизма в нашей стране как религиозного движения.
                К этому времени в Ленинграде, странах Балтии и в Москве независимо друг от друга
                некоторые искатели духовности пришли к буддизму. У каждого был свой путь — либо путь
                профессионального филолога и востоковеда, либо путь долгих поисков через
                христианство, индуизм, суфизм, через изучение античной, египетской и европейской
                литературы религиозно-философского содержания. Характерно, что у каждого была с
                самого начала поиска заметная поляризация в сторону Индии. Велико было влияние
                русского востоковедения и миссии Рерихов. Уже были проштудированы труды Ф. И.
                Щербатского и О. О. Розенберга, стали понятными и близкими идеи мадхъямиков и
                виджнянавадинов. Необходимость встречи с Учителем прояснялась неотвратимостью в
                канве порой трагического течения жизни. Поиск Учителя приводил к мысли о пути в
                Индию. И вот спонтанно, но вполне закономерно реализовалась вместерожденность
                учительства и ученичества. Учитель и ученик встретились. Бидия Дандарон начал
                проповедь на Запад, первым стал передавать духовную традицию буддизма представителям
                иной культуры, иного этноса, в пространство гигантской страны.
            </p>
            <p>
                Когда Дандарон начал передачу Учения на Запад, не все ламы восприняли его деяние.
                "Зачем им, западным людям, буддизм, у них есть свой Бог?" — говорили некоторые из
                них. И все же ведущие ламы поддерживали Дандарона; среди них были лама-философ
                габжа-лама Гатавон, настоятель Иволгинского дацана Цыбен Цыбенов, тувинский лама
                Гендун Цыбен, лама-врач Дашиев, оказывал поддержку и хамбо-лама Гомбоев. Продолжил
                начатую Дандароном передачу на Запад Учения и лама Дхарма-Доди, недавно ушедший
                (1994 г.).
            </p>
            <p>
                Первому всегда трудно, особенно в делах духовных; требуется нечеловеческое мужество
                — "неустрашимость Будды". Принято говорить о традиции-передаче Учения по цепочке
                Учитель-ученик, восходящей к Будде Шакьямуни. Для Дандарона одна из таких линий
                преемственности проходит через Джаягсы-гэгэна к современнику Будды архату
                Ваджракхае. Но Дандарон — это не одна, это все традиции: "Я объединяю все школы — и
                гелуг, и каржуд, и сакья, и ньингма", — таковы его слова, такова его уникальность
                для нашей страны. Среди учеников Дандарона принято говорить не о традиции, а о
                полноте Учения, проповеданного Учителем. В истории Тибета таковыми Учителями были
                Джово Атиша (982-1054) и Цзонхава (1357-1419). Сопоставление Дандарона с великими
                Учителями прошлого не удивительно, ибо преодолеть сопротивление советской сансары и
                запустить Колесо Благого Учения в пространстве жизни такой громадной и необычной
                страны под силу человеку могущественному и человеку ли. Проповедь Дандарона в России
                и странах Балтии сопоставима с деятельностью Падмасамбхавы в Тибете, чьи способности
                сиддха помогли преодолеть сопротивление светских и религиозных противников Дхармы.
            </p>
            <p>
                Первому всегда трудно, особенно в делах духовных; требуется нечеловеческое мужество
                — "неустрашимость Будды". Принято говорить о традиции-передаче Учения по цепочке
