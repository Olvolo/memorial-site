            <h2 class="text-xl text-center font-semibold mb-4">Акаша – свойство ума
                с признаком пространственности</h2>
            <p>Всегда можно поставить мысленный эксперимент:
                представить весь мир целиком в обозримых, например для современной
                науки, границах. Совершив такое представление, легко обнаруживается
                возможность постановки вопроса: а что там, за гранью только что
                представленного? Сам вопрос придаёт этому “за гранью” некоторую
                степень реальности, тем более, что мы этот вопрос задаём всегда, как
                бы ощущая нечто за пределом представимого. Совершается это
                переживание “за гранью” с помощью ума.</p>

            <p>Свойство ума заглядывать за грань ранее
                предельно представленного, обладающее очевидным признаком
                пространственности, мы называем <em>акашей</em>.</p>

            <p>Здесь <em>акаша</em>, означающая на санскрите
                буквально “не препятствие”, предстаёт в своём прямом значении как
                отсутствие препятствующей границы для деятельности ума.</p>

            <p>В классификации <em>Абхидхармы</em> <em>акаша</em>
                относится к группе дхарм, не подверженных бытию, к <em>асанскрита-дхармам</em>,
                то есть не подверженным четырём процессам: рождению, пребыванию,
                изменению и исчезновению. В отличие от <em>санскрита-дхарм</em>,
                считающихся непостоянными, или <em>анитья</em>, <em>акаша</em> считается
                постоянной, длящейся, или <em>нитья</em>. Тибетцы переводят
                <em>акашу</em> термином “<em>намха</em>” (<em>nam mkh'a</em>) – небо, как
                символ беспрепятственности. Абхидхармистское толкование <em>акаши</em>
                выглядит как результат длительных схоластических споров, как продукт
                изощрённого интеллектуального построения.</p>

            <p>В традиции <em>ньингмапы</em> термин <em>акаша</em>
                не является центральным, его место занимает термин “<em>лонг</em>”
                (<em>klong</em>). Значение “<em>лонг</em>” трояко: ум, центр,
                пространство. Два из них – ум и пространство постоянно используются
                в нерасчленимой связке – таков постоянный контекст, например,
                сочинений <em>Лончена Рабжама</em>. Когда же используются все три
                значения сразу, <em>лонг</em> на русский удобно переводить словом
                <em>умосредоточие</em>,
                соединившим в себе все три значения:
                умо-средо(центр)-точие(пространство). В таком значении <em>лонг</em>
                выступает как орган восприятия <em>акаши</em>.</p>

            <h2 class="text-xl text-center font-semibold my-6">Акаша – это тело,
                осязаемый объект ума.<br>Ум самоосязает себя в акаше</h2>
            <p><em>Акаша</em> – это не пространство в прямом
                смысле этого слова, но тело, и не обычное тело, а тело ума.
                <em>Акаша</em> – живая нерасчлёненная, целостная ткань жизни.</p>
            <p>В буддизме принято считать ум шестым органом
                чувств. Если перечислить органы чувств в их “естественном”, телесном
                порядке – осязание, вкус, обоняние, слух, зрение и ум, то заметно
                возрастание степени дальнодействия от прямого тактильного контакта в
                осязании, через зрение, воспринимающего свет далёких звёзд, до ума,
                “ощущающего” мыслью Вселенную.</p>
            <p>Поскольку первые пять органов чувств есть
                модификации осязания и все контактны, то ум следует считать тоже
                разновидностью осязания. Дальнодействие этого осязания ума поистине
                беспредельно – до мыслимых границ мира.</p>
            <p>Так же, как первоосязание, обычное тактильное
                чувство может, осязая себя, знать о себе, так и ум знает о себе,
                чувствует себя посредством прикосновения самим собой к себе (мыслью
                к <em>акаше</em>). Это есть хитрословное определение <em>акаши</em> как
                тела ума.</p>
            <p>Но ум способен осязать, чувствовать далее
                представимого рассудком, обнаруживая нечто, не имеющее границ
                принципиально, не имеющее формы, всегда длящееся и не подверженное
                волнению – <em>акашу</em>.</p>
            <p>В таком контексте <em>акаша</em> предстаёт как
                особый скрытый объект ума. Повторим: восприятие <em>акаши</em> есть
                акт самоосязания ума, сходный с актом самоосознания.</p>

            <blockquote class="quote pl-6 border-l-4 border-gray-300 my-4">
                “Путь жизни, который НЕ осознаёт нерождённого, не является
                источником <em>Победителя</em>, – говорю я”. (<em>Лончен Рабжам</em>,
                <em>"Корабль из драгоценных камней"</em>).
            </blockquote>

            <p><em>Акаша</em> постигается умом как
                непредставимое. Ум же действует так, что, познавая-ощущая, всё время
                сам продуцирует ещё не познанное. В этом смысле реализуется
                открытость познания, его распахнутость, что роднит его с
                пространством. Возможность умственного постижения <em>акаши</em>,
                дхармы, не подверженной бытию, свидетельствует, что природа самого
                ума сходственна <em>акаше</em>, иначе такое постижение было бы
                невозможно. О Проявленности же можно говорить как о волнах на
                поверхности ума-<em>акаши</em>:</p>

            <div class="flex justify-center">
                <ul class="text-left list-none">
                    <li>Настал блаженственный Момент Тайной жертвы,</li>
                    <li>Встречи Двух на гребне Стоячей волны вздыбившейся <em>Акаши</em>.</li>
                </ul>
            </div>

                <p class="my-2 text-right italic">"Нетройственность"</p>

            <p>Распахнутость ума, распахнутость пространства и
                распахнутость Просветления – одного рода. В тибетском термине <em>Sangs
                    rgyas</em>, являющимся переводом с санскритского <em>Buddha</em> –
                Будда, составляющее <em>rgyas</em> означает распахнуть,
                распространить. Дословно тибетский термин <em>Sangs-rgyas</em>
                переводится как “распространяющееся Просветление”, что вполне
                соответствует санскритскому <em>Buddha</em> – Пробуждающийся, что
                означает длящееся и обращённое на себя действие.</p>

            <h2 class="text-xl text-center font-semibold my-6">Акаша – первый и
                главный махабхути</h2>
            <p>Не относится <em>акаша</em>, согласно <em>Васубандху</em>, к четырём великим элементам.
                Но относится к ним пространство, согласно <em>Нацог Рандолу</em>,
                участвуя в пертурбациях четырёх уровней. (См. “<em>Махабхути в тантре
                    и Абхидхарме</em>”; журнал “<em>Гаруда</em>” № 3, 1994 г. и № 1,
                1995 г.). Пять <em>махабхут</em> <em>Нацог Рандола</em> согласованы с
                тантрийской традицией Пяти мудростей. Пространство как пятый <em>махабхута</em>
                <em>Нацог Рандол</em> называет небом (<em>nam-mkh'a</em>), что
                соответствует словарному <em>акаша</em>. Но невозможно с позиций <em>Абхидхармы</em>
                представить <em>акашу</em>, участвующую в четвероякой динамике <em>махабхут</em>
                – от осадочного уровня до уровня чистых <em>махабхут</em>. <em>Нацог
                    Рандол</em> тем не менее делает это, включая <em>акашу</em> в схему
                дифференциации <em>дхармакаи</em>. Это вполне согласуется с
                представлениями “<em>Нетройственности</em>” о “наблюдателе”: нет
                смысла говорить о четырёх великих <em>махабхутах</em> и о дхармах
                вообще, если нет наблюдателя, нет говорящего, думающего и, наконец,
                осязающего – пустых миров не бывает. (Но бессмыслен ли
                <em>мандала</em> без центра. Ведь это возможно попробовать. Что за
                удивительный <em>идам</em> будет при этом!).
            </p>
            <p>Наблюдатель и “привносит” в акт мгновенной вспышки дхарм <em>акашу</em>
                как пятый<em>махабхути</em>. <em>Акаша</em> занимает особое место среди
                <em>махабхути</em>, она всегда длится. Её символическое место в <em>мандале</em> – центр.
            </p>
            <p>Так же, как ум есть Царь органов чувств –
                вершина осязания, так и <em>акаша</em> есть Владыка всех
                <em>махабхут</em> – первый и главный из них.</p>

            <h2 class="text-xl text-center font-semibold my-6">Акаша – это целое</h2>
            <p>Когда речь идёт о <em>виджняне</em>, об <em>алая-виджняне</em>,
                о <em>мано-виджняне</em>, то имеется в виду единичная дхарма, потенция
                целого, его часть, с потенцией <em>нирваны</em> и Будды. <em>Акаша</em>
                – исключительно целое, хотя и можно оперировать её частями.
                <em>Акаша</em> – единая живая клетка бытия, всегда длящаяся и
                безграничная.</p>

            <h2 class="text-xl text-center font-semibold my-6">Акаша и Ригпа</h2>
            <p>Присутствие, или <em>Ригпа</em>, обнаруживает себя
                в созерцании как факт наличия нерасчлёненной реальности бытия.</p>

            <blockquote class="quote pl-6 border-l-4 border-gray-300 my-4">
                “Это великое полное тождество – чистый факт существования, где ум и
                то, что появляется, изначально чисты. Это само есть глубокое
                ощущение непознаваемого, фундаментального измерения реальности”.
                (<em>Лончен Рабжам</em>,<br><em>“Корабль из драгоценных камней”</em>. –
                См.: журнал "<em>Гаруда</em>", 1995, № 1, с. 8.).
            </blockquote>

            <p>Динамика <em>Ригпа</em> часто бывает геометрична:
                нечто вроде мультфильма в режиме компьютерной графики. Так же как
                законы такой графики непроизвольны, так же непроизвольна и динамика
                самообнаружения Присутствия. “Наблюдение” за Присутствием возможно
                по причине того, что это равносильно наблюдению за <em>акашей</em>:
                “Знайте, что состояние Чистого и Полного Присутствия есть широкое
                пространство без центра или границ”. Постигший Присутствие и
                продливший его, испытывает то же чувство, что и узнавший,
                почувствовавший <em>акашу</em>. <em>Акаша</em>, не имеющая свойств,
                длящаяся, целая, неволнующаяся, похожа по своим свойствам на <em>Ригпа</em>.
            </p>

            <p><em>Акаша</em> безлична, <em>Ригпа</em> всегда
                откровение субъекта. В момент переживания <em>Ригпа</em> субъект
                теряет свою единичность, обретая целостность, и видит как бы
                фрагмент <em>акаши</em>. Угадывание, чувствование
                <em>дзогченовского</em> Присутствия – это ни что иное как обнаружение
                <em>акаши</em>.</p>

            <h2 class="text-xl text-center font-semibold my-6">Лонг – инструмент
                познания акаши в стиле ньингмапы</h2>
            <p>Если рассудок как функция мозга анализирует
                варианты обычного пространства – лотосового (эвклидова метрика) и
                пространства-сосуда (геометрия Лобачевского – Римана), то
                <em>акаша</em> обнаруживается исключительно умом.</p>
            <p>Его месторасположение – сердце, про которое так
                и говорят: <em>умосредоточие</em>, или по-тибетски “<em>лонг</em>” (<em>klong</em>).
                Отсюда <em>Лонченпа</em>-Великий <em>лонг</em>.</p>
            <p><em>Лонг</em> чувствует <em>акашу</em>, ибо встроен
                в неё, но так, что сам является не частью её, а целостностью.
                “Раздвигание” границ осуществляется <em>лонгом</em>, при этом просто
                увеличивается высвеченный объём <em>акаши</em>, естественно неимеющей
                объёма. Но иногда говорят совсем иначе и очень заманчиво и опасно: “<em>Лонг</em>
                парит пространством”, то есть <em>акашей</em>, уже впавшей в метрику.
                Или не впавшей ещё, об этом стоит подумать, это надо почувствовать.
                Опыт с <em>Ригпа</em> подсказывает, что уже с метрикой.</p>
            <p>Благодаря <em>лонгу</em> голый факт Присутствия,
                обнаруженный сознанием, обретает чувственные характеристики
                протяжённости и формы. <em>Лонг</em> осязает <em>акашу</em>, как живую
                ткань, о которой человек лишь догадывается, воспринимая её как бы на
                ощупь. Ум и <em>лонг</em> – это два термина для обозначения одного и
                того же. Значение “ум” придано <em>лонгу</em> традицией <em>Нетройственности</em>,
                в которой принято просто употреблять обычное русское с загадочной,
                но глубочайшей этимологией слово “ум”. Ни в коем случае не надо
                путать Ум с разумом и рассудком, о которых действительно можно
                говорить как о функциях только мозга. Считает и анализирует человек
                мозгом, но думает всем телом, в центре которого не мозг, а Сердце –
                истинное <em>Умо-средо-точие</em>, источник и орган восприятия <em>акаши</em>.
            </p>
            <p>Знающий <em>Нетройственность</em>, обнаруживая <em>акашу</em>,
                обретает неисчезающее чувство её Присутствия; другая сторона этого –
                сам такой ощущает себя <em>Ваджрасаттвой</em> (<em>Ваджр</em> – явление,
                <em>саттва</em> – его продолженность).</p>
            <p>Итак, <em>лонг</em> – это <em>умо-средо-точие</em>,
                все три неслиянно-нераздельны, это лишь похоже на <em>Нетройственность</em>,
                где ум – сфера, средо – центр, точие – пространство (Свет Ясный). На
                самом деле сочетание трёх: ясного света, <em>акаши</em> и ума, это не
                <em>Нетройственность</em>, а Тождественность.</p>
            <p>Пространство и все его виды – частный случай<em>акаши</em>, впавшей в метрику.</p>

            <h2 class="text-xl text-center font-semibold my-6">Акаша – один из
                членов троякого Тождества:<br>Ясный Свет (Амрита) – Акаша – Ум</h2>
            <p><em>Акаша</em> – потенциально живое, способное
                себя осознавать в любой степени осознания путём спонтанного
                выделения в себе ограниченных, замкнутых неоднородностей.
                <em>Акаша</em>, не имея ни границ, ни формы, может их обретать,
                представая как один из видов чувственного пространства. <em>Акаша</em>
                – это одновременно и свойство ума и его объект. <em>Акаша</em> – это
                гигантское целое, необходимо осознающее себя. Здесь важно наречие
                “необходимо”, его можно усилить до “неизбежно”, “абсолютно
                вероятно”, ибо бессмысленно говорить об <em>акаше</em> (<em>шуньяте</em>,
                это не синоним, а другой уровень рассмотрения), о ясносветной
                Проявленности и об Уме, или Сознании, по-раздельности. Они либо есть
                все три сразу, либо нет ни одного из них. А поскольку небытие
                невероятно, Бытие всегда есть, значит всегда есть Наблюдатель
                (сознание, ум), Наблюдаемое и Внутреннее переживание (исключительно
                шуньевое или акашиевое). В такой конструкции так и хочется найти
                центр, сместить его внутрь. Порочный соблазн, причина всех горестей
                Боготворчества и Сверхчеловечества.</p>
            <p>Сознание ни снаружи, ни внутри, оно – между, оно – пространственно!</p>

            <h2 class="text-xl text-center font-semibold my-6">Акаша – не
                шуньята,<br>но категория сходного уровня рассмотрения</h2>
            <p><em>Акаша</em> не <em>шуньята</em>, как могут
                подумать многие в силу непроявленности <em>акаши</em> в сфере пяти
                органов чувств. Обе они не имеют формы. Но <em>шуньята</em> – это
                принцип несубстанциональности (<em>акаша</em> – это “положительная
                субстанция”), расступаемости материального (<em>рупово-формного</em>),
                это принцип относительности ценностей феноменального. <em>Шуньята</em>
                – это прежде всего принцип, некое усмотрение.</p>

            <p><em>Акаша</em> обладает несравненно большей
                способностью к уловлению органами чувств, ибо угадывается самой
                тонкой и дальнодействующей разновидностью осязания – умом; то есть
                <em>акаша</em>, хотя и не принадлежит к <em>санскрита-дхармам</em>
                (подверженным бытию), всё же есть дхарма. Принцип же шуньёвости
                применим и к <em>акаше</em>: она тоже пуста, шуньева, ибо невозможно
                найти субстрат, её составляющий. В оппозиции <em>шуньята</em> – Ясный
                Свет, <em>акаша</em>, безусловно, тяготеет к сфере Ясного Света, <em>Акаша</em>
                – это ум-пространство, средоточие которого в глубине сердца каждого
                (пространственно переменная величина, постоянно перемещающаяся), а
                периферия которого – везде, поэтому и говорят: <em>умосредоточие</em>.
            </p>

            <p><em>Акаша</em> – эта та <em>шуньята</em>, которая
                есть. Философская <em>шуньята</em> небытийна.</p>

