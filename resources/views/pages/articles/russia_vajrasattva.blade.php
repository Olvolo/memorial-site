                    Мазда</em>, что может свидетельствовать о тесных связях буддизма
                и маздаизма в виде его позднейшей трансформации в виде
                <em>бона</em>.<br> Древнеиранский маздаизм, гонимый на родине
                Ксерксом, иудаистским лобби и победившим зороастризмом, при миграции
                на восток в среде народов, в том числе находящихся под сильным
                влиянием древнеиндийской культуры, постепенно к началу нашей эры
                превратился в <em>бон</em> (<em>Шан-Шунг</em>, Согдиана, Восточный
                Туркестан). В пограничной зоне разнородных культурно-религиозных
                традиций, в знаменитой <em>Уддияне</em> (район реки Сват и Гильгита)
                <em>бон</em> неоднократно пересекался с буддизмом. Среди буддийских
                имён <em>бонские</em> жрецы встретили полную кальку имени верховного
                божества <em>Ахура Мазды</em> (букв. Всеблагой), это было имя одного
                из восьми <em>бодхисаттв</em> – сынов Будды – <em>Самантабхадры</em>.
                Так постепенно буддийский <em>бодхисаттва Самантабхадра</em> стал
                обретать черты высшего божества <em>бонского</em> пантеона под
                тибетским именем <em>Кунтузанпо</em>. В старой тибетской школе – <em>ньингма</em>,
                максимально повлиявшей на модернизированный <em>бон</em>, <em>Самантабхадра</em>
                также почитается Изначальным буддой, <em>Адибуддой</em> в школах <em>гелуг</em>
                и <em>кармапа</em> является <em>Ваджрадхара</em>.<a href="#ref-8"
