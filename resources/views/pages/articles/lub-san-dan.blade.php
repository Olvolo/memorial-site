                учредительным собранием теократического государства, созданного
                С. Цыденовым, провозгласившего себя царём-деспотом, назначен
                наследником на его престол; причём был подвергнут семёновской
                властью к арестам три раза совместно с Цыденовым и умер в начале
                1920 г. после освобождения из читинской тюрьмы".
            </p>
            <p>
                (Тело Агвана Силнам Тузол Доржи на телеге привёз на Суархэ сам
                Лубсан Сандан. На Суархэ была построена ступа. Частично была
                разрушена. Восстановлена в 1992 году; ред.).
            </p>

            <h3 class="text-lg sm:text-xl font-semibold mt-4 mb-2 text-gray-700">
                Ритуал Дамжана</h3>
            <p>
                Агади-лама (Лубсан Сандан) был позван провести ритуал Дамжана —
                покровителя кузнецов.
            </p>
            <p>
                Старик, который позвал его, повесил на главное место икону с
                изображением Дамжана, поставил три балина и всё хорошо украсил.
                Когда вошёл к нему Учитель Лубсан и увидел всё так почтительно
                приготовленное, он смял руками балины, превратив их в
                бесформенные комки теста, а икону Дамжана снял с алтаря, свернул
                трубкой и засунул в кольца сундука, сказав: "Для простого
                кузнеца этого вполне достаточно".
            </p>
            <p>
                Старик был ужасно всем этим напуган.
            </p>

            <h3 class="text-lg sm:text-xl font-semibold mt-4 mb-2 text-gray-700">
                Лубсан Доржи и шаман</h3>
            <p>
                Эта история об ученике Агвана Силнам Тузол Доржи. Звали ученика
                Лубсан Доржи. Он созерцал безрогого чёрного Ямандагу — хозяина
                кладбищ и жил на Суархэ. Однажды он поехал на праздник, к утру
                должен был по приказу Учителя вернуться. Но на празднике он
                напился и стал показывать сиддхи — дул на листья крапивы, и те
                вяли. Потом совсем напился, и его повезли на телеге. По дороге
                его встретили и сказали, что зовёт Учитель. Он тут же передал
                своё опьянение одному старику, и тот упал сразу пьяным, а Лубсан
                Доржи трезвым поспешил на "Поляну" (Суархэ). В своём доме он
                обнаружил сброшенный алтарь и жертвенник (это сделал сам
                Учитель). Лубсан Доржи бросился к Учителю, а тот его избил
                палкой до крови и прогнал. Все другие ученики видели это, многие
                жалели его и даже плакали. Лубсан Доржи поселился выше, на
                Аршане, и его братья носили ему еду. Тогда Учитель и их избил.
                Лубсан Доржи переехал ещё дальше, к своему ученику, — и тот был
                избит. Он поселился у Бату-художника, тоже ученика Агвана Силнам
                Тузол Доржи, и Бату-художник был избит. Тогда он уехал к своим
                родным в Читинскую область. Там и созерцал дальше.
            </p>
            <p>
                Однажды от шамана передал кто-то ему мешочек-пузырёк с топлёным
                маслом, и пока он с ним ехал, мешочек этот за пазухой всё время
                дёргался. Он его взял в руки, и в руках тоже было трудно
                удержать. Даже в зубах пытался зажать он непослушный мешочек.
                Наконец, Лубсан Доржи сразу по приезде домой сделал светильник,
                заправив его маслом из этого мешочка, светильник загорелся
                исправно, и только тогда прекратилось наваждение. Лубсан Доржи
                понял тогда, что к нему привязался женский шаманский дух. Чтобы
                избавиться от него, совершил Лубсан Доржи ритуал необычайной
                эффективности — дордуг Сэндэмы. Как раз в это время шаман вселил
                в себя в очередной раз этого духа и вдруг страшно испугался и
                закричал: "Я боюсь Лубсана Доржи!" И выпустил духа, в воздухе
                запахло горелым, кожей и костью. Соученики Лубсана Доржи потом
                смеялись над ним, мол, против какой-то незначительной
                ведьмы-духа он применил такой мощный метод, способный отбросить
                целое сонмище самых сильных вредоносных духов или мар.
            </p>
            <p>
                Лубсан Доржи закончил, наконец, полностью положенное количество
                созерцаний и увидел в тот же миг, что его дом со стороны Учителя
                окружает радуга; и он всё понял, что случилось с ним за
                последнее время. Сразу поехал к Учителю, и тот его обнял.
            </p>

            <h3 class="text-lg sm:text-xl font-semibold mt-4 mb-2 text-gray-700">
                Дворец-мандал</h3>
            <p>
                В период созерцания на Суархэ ученики и лучшие художники из
                бурят построили Лубсану Сандану жинхор Ямандаги. Это был
                деревянный дворец, прекрасно разрисованный, украшенный
                драгоценностями. Размер дворца был таков, что в него можно было
                вполне войти. Лубсан Сандан, когда дворец был готов, зашёл в
                него, сел в центре и провёл несколько часов в созерцании. Затем
                он вышел, а дворец с жинхором был по его приказу сожжён.
            </p>

            <h3 class="text-lg sm:text-xl font-semibold mt-4 mb-2 text-gray-700">
                Выход из созерцания</h3>
            <p>
                Среди последних рассказов о Лубсане Сандане есть один о его
                выходе из Ямандагинского созерцания. Когда Бурятия, а точнее
                хори-буряты, были между Семёновым и "Большим Красным", Семёнов
                требовал призыва молодых бурят в своё войско. Но Лубсан Сандан,
                чтобы спасти от призыва молодых людей, стал среди стариков
                распространять слух, что такой-то, мол, объявил себя царём,
                считает бурят своими подданными и запрещает им вступать в войско
                атамана Семёнова. В ответ на это на Суархэ пришёл отряд во главе
                с генерал-майором Корвин-Петровским. Лубсан Сандан был
                совершенно один, он вышел из дома и сказал: "А! Так и должно
                быть. Закончив созерцание, я должен был покинуть уединение из-за
                прихода войск". "Ты почему мутишь народ?" — спросили его. — "Да,
                это я не даю вам людей. Не дам народ вам". — "Тогда мы тебя
                арестуем". — "Пожалуйста", — ответил Лубсан Сандан, и его
                посадили в телегу и увезли. Это было в 1920 году.
            </p>
            <p>
                В тюрьме, куда был препровождён Лубсан Сандан, он не раз
                доставлял охране изрядные хлопоты. Сколько бы его ни запирали в
                камере, он неумолимо и непредсказуемо появлялся на крыше тюрьмы,
                никакие запоры не помогали. И однажды исчез из тюрьмы совсем.
                Снова попал в тюрьму уже при советской власти.
            </p>

            <h3 class="text-lg sm:text-xl font-semibold mt-4 mb-2 text-gray-700">
                Рассказ Цыгана</h3>
            <p>
                Последним, кто видел Лубсана Сандана, был Цыган. Цыган потом и
                описал эпизод о последней встрече. Цыган знавал Лубсана Сандана
                по тюрьме. Когда же он его видел последний раз на Улан-Удэнском
                вокзале, Лубсан Сандан преобразился, ему на вид было около
                сорока лет. Он сел в поезд, отходящий на Запад. Цыгану сказал,
                что уезжает в Италию. Лубсан Сандан достиг сиддхи бессмертия и
                не собирался покидать сансару.
            </p>
            <p>
                Предполагалось, что в будущем он снимет силу заклятия и укротит
                Роланда Кижингинской долины, который сокрыт под землёй, под
                невысоким обо, в двух километрах за Ушхайтой, слева от дороги,
                если ехать из Кижинги в Леоновку. По другой версии — обиталище
                Роланда — в районе Эдэрмыга.
            </p>

            <h3 class="text-lg sm:text-xl font-semibold mt-4 mb-4
            text-gray-700">
                Дашицыреновы</h3>
            <div class="flex justify-center my-6">
                <figure class="rounded-lg shadow-md overflow-hidden max-w-[400px] text-center">
                    <img src="{{ asset('images/Anchen.jpg') }}"
                         alt="Анчен Дашицыренов"
                         class="w-full object-cover">
                    <figcaption class="p-4 italic
                    text-gray-600">Анчен Дашицыренов</figcaption>
                </figure>
            </div>
            <p>
                Много было последователей Лубсана Сандана и среди мирян, в
                особенности среди жителей Кижингинской долины. Между
                сторонниками Лубсана Сандана и хошунами возникла вражда. Среди
                мирян-балагатов выдвинулся Аюшей Дашицыренов, чаще звали его
                просто Даша. Однажды балагаты, заранее узнав путь следования
                отряда хошунов, сделали вооружённую засаду в лесу по дороге из
                Улзыту к Суархэ. Но Даша выступил перед земляками со страстной
                речью и удержал их от убийственного плана. "Наше дело — двигать
                колесо Дхармы!" — сказал он, кровопролитие было предупреждено.
                Позже, повзрослев, многие из тех, молодых тогда, оценили
                мудрость и дальновидность Даши. Избежали крови, а значит,
                избежали в дальнейшем мести детей и родни кижингинских родов
                друг на друга. Дашу Дашицыренова называли "главой балагатов",
                имея в виду его авторитет среди светских последователей Лубсана
                Сандана. Он был отцом Анчена Дашицыренова.
            </p>
            <p>
                Анчен Дашицыренов, глава славной семьи Дашицыреновых, хорошо был
                известен и землякам, и многим буддистам-европейцам. Для
                последних он был не просто старейшим из учеников Б. Дандарона,
                но воплощением народной бурятской культуры, а в силу своих
                удивительных человеческих свойств — символом буддийской идеи
                взаимопомощи между людьми. Его так и называли часто за глаза —
                бодхисаттва.
            </p>
            <p>
                Эти краткие строки — посмертная дань старшему другу и духовному
                собрату, так неожиданно покинувшему всех в январе 1992 года.
