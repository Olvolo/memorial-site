                Сандан, кожа красная, замёрз. Объясняет: "Мы, люди, в доме
                живём, а животные — на улице. Я решил на себе испытать, как они
                в такой холод живут, что чувствуют". Так просто и буднично
                объяснил Лубсан Сандан, очевидно, непростое своё деяние.
            </p>

            <h3 class="text-lg sm:text-xl font-semibold mt-4 mb-2 text-gray-700">
                Встреча</h3>
            <p>
                Первая встреча Лубсана Сандана и Джаягсы-гэгэна произошла в
                Кижингинском дацане. Когда туда приехал Джаягсы-гэгэн, все к
                нему шли, а Лубсан Сандан не пришёл. Они начали в пределах
                дацана переписываться. Пришёл к Сандану в это время Агади-лама,
                брат его матери. (Позже самого Лубсана тоже называли почтительно
                Агади-лама.) И стал корить Лубсана: "Ты почему не идёшь? Иди!"
                Лубсан сделал вид, что идёт и стал одеваться. Агади пошёл к
                выходу, Лубсан за ним, но в последний момент, когда Агади
                переступил внешний порог, Лубсан Сандан вдруг запер дверь, а сам
                остался внутри дома.
            </p>
            <div class="flex justify-center my-6">
                <figure class="rounded-lg shadow-md overflow-hidden max-w-[400px] text-center">
                    <img src="{{ asset('images/Jayag_lama_400.jpg') }}"
                         alt="Джаягсы-гэгэн"
                         class="w-full object-cover">
                    <figcaption class="p-4 italic text-gray-600">Джаягсы-гэгэн</figcaption>
                </figure>
            </div>
            <p>
                Через трое суток после переписывания Лубсан Сандан всё же пришёл
                в дацан. Джаягсы-гэгэн встал ему навстречу, и они на равных
                поклонились друг другу.
            </p>

            <h3 class="text-lg sm:text-xl font-semibold mt-4 mb-2 text-gray-700">
                Реформа</h3>
            <p>
                (Действующие лица и краткое содержание)
            </p>
            <p>
                Именно в этот первый период зародилась идея реформы буддизма. В
                обсуждении принял участие целый круг лиц, в основном ламы
                Кижингинского и Чесанского дацанов (из бурят), и два высоких
                тибетских гостя — Джаягсы-гэгэн и Акпа-гэгэн, его друг и
                соратник. Инициатором реформы был Лубсан Сандан. Это ему
                принадлежит знаменитая фраза, которую он бросил на предложение
                стать хамбо-ламой: "Дацан — это сансара".
            </p>
            <p>
                Эрдэни Ламхе, настоятель Чесанского дацана, был связан с
                Джаягсы-гэгэном ещё до раскола. У него были два известных
                впоследствии ученика, бывшие друзья детства, — Санданэй-лама и
                Гурусорже.
            </p>
            <p>
                Гурусорже — один из самых активных противников реформаторского
                движения Лубсана Сандана и Джаягсы-гэгэна. Противники реформ
                остались жить в дацановских домах и стали называться хошунами.
            </p>
            <p>
                Последователи Лубсана Сандана стали называться балагатами
                (откочевавшие).
            </p>
            <p>
                Санданэй-лама, друг детства Гурусорже, дамдинъянсановский йогин
                (главный идам Акпы-гэгэна), в момент разобщения решительно
                принял сторону Джаягсы-гэгэна, а в дальнейшем — балагатов.
            </p>
            <div class="flex justify-center my-6">
                <figure class="rounded-lg shadow-md overflow-hidden max-w-[400px] text-center">
                    <img src="{{ asset('images/Akpa-gegen.jpg') }}"
                         alt="Акпа-гэгэн"
                         class="w-full object-cover">
                    <figcaption class="p-4 italic text-gray-600">Акпа-гэгэн</figcaption>
                </figure>
            </div>
            <p>
                Энергия к разделению уже была велика, когда в Чесанский дацан
                приехали Джаягсы-гэгэн и Акпа-гэгэн. Остановились они в доме у
                Гурусорже, каждый в отдельном помещении. В эту ночь в дацан
                прибыла неизвестная, необычной красоты, женщина. Утром в дацане
                стало известно, что Джаягсы-гэгэн был у неё ночью, а Акпа-гэгэн
                не был. Поскольку оба были гэлонгами, то часть лам во главе с
                Гурусорже осудила Джаягсы-гэгэна, другая же часть лам отнеслась
                к этому без осуждения. Оба же гэгэна относились всегда друг к
                другу дружески, сотрудничали и относились с пониманием к
                особенностям пути другого. Вскоре после этого сторонники
                балагатского движения покинули Чесанский и Кижингинский дацаны и
                сосредоточились в Шулутайском дацане, к востоку от Кижинги.
                Шулутайский дацан стал главным дацаном балагатов.
            </p>
            <p>
                В дальнейшем Лубсан Сандан основал на правом берегу Кудуна, на
                склоне Кудунского хребта, против села Усть-Орот, обитель для
                созерцания, где и провёл около тридцати лет в окружении самых
                близких учеников. Опыт непосредственного ведения ученика
                Учителем, акцент на созерцательной практике, уединённость жизни
                в сочетании с активностью по отношению к сансаре, освоение
                максималистских практик стали нормой жизни новой общины. Все эти
                годы соратником Лубсана Сандана был Агван Силнам Тузол Доржи, в
                миру — Доржи Бадмаев.
            </p>

            <h3 class="text-lg text-center sm:text-xl font-semibold mt-4 mb-2
            text-gray-700">
