            <p>
                "Бадмаев Д. родился в 18.. году в улусе Средняя Кижинга
                Хоринского ведомства и происходил из семьи середняка бурята
                Галзутского рода Хоринского ведомства Бадмы (Доржи) Балданова
                (Жамаева), проходил духовное образование в Кижинском дацане, где
                получил звание гэбши, причём он считался одним из лучших
                философов. Будучи в Монголии более года, занимался изучением
                таинственной мистики. По приезде из Монголии он ведёт жизнь
                степного ламы, отступая от правил дулби-виная, ибо он считал
                себя последователем мистического учения.
