        <h1 class="text-2xl font-bold text-center mb-8">БОНСКИЕ УЧИТЕЛЯ В ДАЦАНЕ</h1>

        <p class="text-justify">В августе в Санкт-Петербургском буддийском Храме, захваченном в 1988 г. вооруженным путем антибуддистами, по их приглашению предполагаются занятия современного бонского ламы Тэнзина Вангьяла. Тэнзин Вангьял, будучи также знатоком буддизма, принадлежит всецело традиции учения бон, как он себя сам и представляет. Уважая тибетскую культуру и историю в целом, заметим, что ни в Тибете, ни в эмигрантских кругах в буддийских храмах бонцы не правят свои ритуалы. Такое было лишь однажды в истории Тибета в правление гонителя дхармы царя Ландармы в IX в., когда буддийские монастыри разрушались, а лам убивали или заставляли отрекаться от своей веры.</p>

        <p class="text-justify">Бон и буддизм совершенно разные религии. Вот цитата из недавно изданной книги Б.И. Кузнецова <i>“Бон и маздаизм”</i>:</p>

        <blockquote class="quote pl-6 border-l-4 border-gray-300 my-4">
            “В связи с последней проблемой нельзя не упомянуть замечания Ю.Н. Рериха на эту тему (из разговора с А.Н. Зелинским) незадолго до его скоропостижной кончины в 1960 г. на вопрос, в чем состоит главное различие между буддизмом и боном, Рерих сказал, что когда им этот же вопрос был задан одному бонскому ламе в Тибете, тот ответил: «Как Вы, изучивший доктрину Будды, можете интересоваться боном и спрашивать об этом? Эти учения несовместимы друг с другом»». Здесь уместно подчеркнуть, что две великие концепции буддизма: учение о пустоте, или ранний его вариант – <i>анатмавада</i>, и нравственная максима Махаяны – учение о <i>бодхичитте</i>, были столь авторитетны и цивилизационно универсальны, что даже те учения, суть которых никак не содержала идеи отсутствия души и не была пронизана идеей вселенского сострадания, вынуждены были ассимилировать эти две возвышенные концепции. Так было с боном. Тибетские буддисты, выдержавшие многовековое соперничество с боном, и, зная его не со стороны, а из гущи народного восприятия, тонко чувствовали его нравственный и интеллектуальный тонус. Они прекрасно знали: теория <i>шуньяты</i> и <i>бодхичитта</i> ничего общего с боном не имеет. И лама, беседовавший с Ю.Н. Рерихом, мог бы добавить: разница между боном и буддизмом такова – буддизм есть религия спасения, бон – учение силы и самоосвобождения. Буддист спасает и слабого, бонцу до этого нет дела. И речь не идет об отсутствии сострадательных мотивов в боне, все дело в акцентах, что и придает каждому учению свойственный только ему оттенок”.
        </blockquote>

        <p class="text-justify">Чтобы нагляднее понять разницу между боном и буддизмом, достаточно вспомнить известный этап жизни великого буддийского йогина и поэта Миларэпы: в юности он, мстя недругам матери, преспокойно, используя народную бонскую магию, убил несколько человек, и не тронул ни травинки после своего обращения в буддизм.</p>

        <p class="text-justify">О современных же событиях в Санкт-Петербургском буддийском Храме, где воинственные антибуддисты над алтарем, где стоит статуя Будды, соорудили непотребный для буддийского Храма жертвенник, можно сказать – воистину наступили темные времена и свершается пророчество Великого Падмасамбхавы:</p>

        <blockquote class="quote pl-6 border-l-4 border-gray-300 my-4">
            “К концу эры, когда продолжительность жизни людей уменьшится до 60-70 лет и эгоизм людей возрастет, … отрыжка бонского мага зазвучит в уединенном жилище йогина …”.
        </blockquote>

        <p class="text-justify">Странно, нелепо и кощунственно, что подобное может произойти в просвещенном Санкт-Петербурге в наши дни.</p>

        <p class="text-justify">Никоим образом не желая оскорбить молодого тибетского ученого, мы посылаем ему перевод этого письма и более подробную информацию о судебно-уголовном юридическом статусе лиц, пригласивших его в Санкт-Петербургский дацан.</p>
