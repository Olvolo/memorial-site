            <p>
                Одним из помощников Агвана Доржиева в двадцатые-тридцатые годы
                был Галсан Хайдуб-лама. Агван Доржиев имел аудиенцию у Ленина,
                на которой присутствовал Сталин. Агвана Доржиева на аудиенции
                сопровождал Галсан Хайдуб-лама. Во время беседы Агван Доржиев
                старался убедить Ленина, что социальная позиция буддизма и
                основы его нравственности сходны с идеями коммунизма, а посему
                не стоит подвергать религиозные институты буддизма репрессиям.
            </p>
            <p>
                Позже, в Ленинграде, Галсан Хайдуб-лама виделся в дацане с
                молодым Дандароном, посетившем Агвана Доржиева. Это было в 1933
                г.
            </p>
            <p>
                Галсан Хайдуб-лама участвовал в знаменитом параде, когда войска
                прямо с Красной площади шли на фронт, он воевал и по его словам
                имел звание героя Советского Союза. После войны он был, как
                герой, на приеме у Сталина и ему удалось напомнить в беседе о
                том, что в том памятном параде войска благословлял православный
                священник. Упомянул Галсан Хайдуб-лама и о давней встрече у
                Ленина и о бедственном положении буддизма в Бурятии. Сталин был
                лаконичен – он потребовал предоставить ходатайство об открытии
                буддийских монастырей с шестнадцатью подписями.
