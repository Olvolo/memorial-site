                Алексеев (мой духовный собрат), приехав в Иволгинский дацан, и
                посетив болеющего ширетуя Цыдена Ламхе (Цыден Цыденов), получили
                от него совет посетить дом № 17 в дацане, где проживал лама
                Дарма Доди. Идя снежной тропой к домику ламы, мы удивлялись и
                его имени (ведь это одно из имен Дандарона в прошлых рождениях)
                и числу семнадцать (относится к числовой метрике Ваджрасаттвы).
                Последовавшая встреча удивила и алтарем с признаками гневных
                ритуалов (на алтаре лежала пурба), и немногословностью, и
                суровой скромностью. Не задав нам ни одного вопроса, он, тем
                не менее, разрешил наши проблемы.
            </p>
            <p>
                С 1976 г. Дарма Доди становится настоятелем Иволгинского дацана.
                Именно он проводил ритуал освящения главного храма дацана после
                пожара. В 1982 г. он отходит от официальных дел, но не
                оставляет духовного подвижничества: передает традицию на Запад
                – европейцам. Множится круг учеников, и кто сейчас перечислит
                города и веси России, где бы ни обитали почитатели скромного
                старого бурятского ламы – Учителя Дарма Доди.
            </p>
            <p>
                Придет время, и его ученики поведают более подробно о жизни
                Учителя, о делах необычайных и замечательных – от проявления
                сиддх до дара предвидения, и, может быть, правы те, кто вполне в
                духе буддизма считал, что уже многие годы Дарма Доди на благо
                учеников проявлял Иллюзорное Тело, сам же уже давно оставив
                сансарное существование.
            </p>
            <p>
                С уходом Дарма Доди ушел один из последних Великих Лам Бурятии,
                через которых драгоценная линия Традиции не прерываясь дошла до
                нас из дали священных стран и веков.
            </p>
            <p>
                В заключение несколько об ином. В юности Дандарон интересовался
                панмонголизмом, встречался с Ц. Жамцарано, писал небольшой труд
                об истории Монголии, сердцем переживал разделенность и судьбу
                бурятского и монгольского народов, воспринимая их в
                нераздельности. Этот интерес и стал в дальнейшем причиной его
                первого ареста в 1937 г. Прошли годы и энергия его интеллекта,
                мощь характера выразилась на ином поприще – поприще возрождения
                Дхармы, проповеди буддизма европейцам. Не политический, а
                духовный образ жизни стал его стезей, и, как оказалось, такой
                путь является более эффективным в созидании национальной
                культуры, в укреплении исторической памяти и самосознания
                народа, чем путь политической борьбы. Трудно представить
                современную Бурятию без дацанов, лам, без буддизма. Они
                составляют скрепляющую и основополагающую роль в жизни
                современного бурятского общества. Недавно ушедший бурятский
                ученый Бато Мункин прекрасно сказал: «Не мечем Атиллы, а
                проповедью Дхармы завоевал Европу Дандарон». Эти слова во всей
                полноте мы относим к старому поколению лам, к священным именам
                наших Учителей.
            </p>
