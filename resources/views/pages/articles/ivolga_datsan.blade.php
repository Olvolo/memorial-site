                утвердительный ответ, выстрелом убил ламу. Вышел из дома и
                только тогда был схвачен. Ни о следствии, ни о суде, ни о судьбе
                этого человека никто ничего не услышал. Так погиб, очевидно, по
                заказу, Гэндун Цырен.
