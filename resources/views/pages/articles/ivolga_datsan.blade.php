                рассказа, например, упоминание о сакральном для буддизма числе
                «шестнадцать». Но, тем не менее, это более подробное
                свидетельство.
            </p>
            <p>
                А далее события были сколь удивительными, столь и прозаическими,
                что кажется, так оно и было.
            </p>
            <p>
                Галсан Хайдуб вернулся из Москвы в Улан-Удэ и стал искать людей,
                которые согласились бы подписать обращение к Сталину (скорее
