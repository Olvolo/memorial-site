            <p>
                Одним из последних лам довоенного поколения был Дарма Доди. Он
                был ключевой фигурой для европейцев в дацане после ухода Б. Д.
                Дандарона. Дарма-Доди родился в 1900 году. В восемь лет его
                послали жить к родному дяде в Эгэтуйский дацан, где он начал
                изучение тибетского и монгольского языков. Там же он был
                определен в статусе хуварака. В пятнадцать лет он уже изучает
                цанид и известную ритуальную систему чод. В тридцатые годы,
                продолжая монашескую жизнь, приступает к изучению тантрийских
                систем, в частности, тантры Ваджрабхайравы, линию
                преемственности которой он возводил к Лубсану Данде
                (Богдо-ламе).
                Волна репрессий не миновала Дарма Доди; в 1937 г. он был
                арестован в Новоселенгинске, и начались смертельно тяжкие годы
                ГУЛАГа: Колыма – прииск Сусыман, прииск Каменский. Освобожден
                Дарма Доди был в 1946 г. С 1948 г. снова возвращается к
                официальной духовной деятельности, с 1964 г. – в Иволгинском
                дацане. Таковы скупые сведения, которые сообщил сам Дарма Доди
                в июне 1989 года.
            </p>
            <p>
