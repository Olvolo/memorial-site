        <p class="text-justify">Личная смертность на фоне неуничтожимого Поля сознания Совершенных, но не слиянных с
            Единым, <i>шуньята</i>, пугающая неточно понятой пустотностью, ясносветная забава жизненного творчества в
            бездне напряжённого “небытия” и бесподобная мелодия <i>бодхичитты</i> от запредельного звука светолучения до
            тончайших мелодий песен любви и колыбельных – вот “оружие” пробудившегося гражданина-воина на беспредельном
            невероятном и неожиданном поле России. Это поле часто было полем брани и смерти. Сейчас в опасности его
            сердце, дом его духа, обиталище интуиции рода; во вне – это гибель природы, экологическая катастрофа. Мы
            плохо знаем историю своей планеты, следы на её лике читаем как попало, а они говорят о многочисленных
            катастрофах, не уступающих нынешней. Причины их до сих пор остаются втайне. Но ведь должен был человек
            выдвинуть ответ неумолимому и, очевидно, периодическому хаосу, дабы жизнь прорастала сквозь эпохи распада и
            в конце концов смогла научиться избегать их катастрофический характер!</p>

        <p class="text-justify">Такой ответ не только должен быть, он – был: “Приди, приди, <i>Ваджраветали</i>!” –
            слышится в мантрической песне <i>Ваджрабхайраватантры</i>, в этом уникальном экологическом гимне, ибо это –
            призыв к Ваджрному мертвецу женского рода восстать из псевдонебытия к жизни, это страстный одухотворяющий
            призыв к Вселенной, как к живой царственной невесте личного творческого духа. В этом призыве ваджрная, а
            значит, постоянная и неразрушимая возможность совершения йогического акта осознания, оживотворения “мёртвой
            природы” – Великого Кладбища Мира, ибо воистину всё вокруг, вплоть до тончайшей лучистости света, –
            “останки” живого. Идея целостности, идея Единого, снимает тайну савана – загадочного одеяния тантр от
            шёлковых одежд бодхисаттв до пепла на обнажённых телах йогинь, и под ним – Живой Труп – <i>Ваджраветали</i>
            – Интуиция Мира, наречённая жена практикующего.</p>

        <p class="text-justify">И опять как всегда – <strong>«В сердце солнца не задержится луч!»</strong>; странствие продолжается, и
            линия горизонта – в сердце у каждого.</p>

