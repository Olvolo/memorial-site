                все есть не что иное, как ум, зрящий собственное явление.</p>

            <p class="text-center font-bold text-gray-800 my-4">* * *</p>

            <p class="text-justify">Тиртики рассматривают с точек зрения
                вечности и
                пресечения. Девять колесниц рассматривают сообразно своим
                воззрениям. Все по-разному видят и по-разному объясняют и,
                цепляясь
                за различия, впадают в привязанность и ошибки.</p>
            <p class="text-justify">Когда все явления, осознаваемые умом,
                возникают,
                но ты не цепляешься за эти зримые явления, ты – Будда. Явление
                само
                по себе еще не ошибка, цепляться за него – вот в чем ошибка.
                Когда
                распознал в своем уме эти овеществляющие мысли, это и есть
                самоосвобождение.</p>

            <div class="flex justify-center my-4">
                <ul class="text-left list-none italic">
                    <li>Что бы ни являлось – это лишь проявления ума.</li>
                    <li>Является неодушевленный мир-сосуд – это ум.</li>
                    <li>Является сок шести классов живых существ – это ум.</li>
                    <li>Является рай и блаженство богов и людей – это ум.</li>
                    <li>Являются страдания трех дурных участей – это ум.</li>
                    <li>Являются неведение и яд пяти клеш – это ум.</li>
                    <li>Является Ригпа Саморожденной Мудрости – это ум.</li>
                    <li>Являются благие мысли и нирвана – это ум.</li>
                    <li>Являются препятствия, чинимые Марой и духами, – это ум.</li>
                    <li>Является Идам и обретаются сиддхи и другие блага – это ум.</li>
                    <li>Пребываешь в однонаправленной концентрации без мыслей – это ум.</li>
                    <li>Являются различные характеристики вещей – это ум.</li>
                    <li>Является безатрибутная немножественность – это ум.</li>
                    <li>Является недвойственность одного и многого – это ум.</li>
                    <li>Является «есть», «нет», «нереальное» – это ум.</li>
                </ul>
            </div>

            <p class="text-justify">Нет явлений, кроме как возникающих из ума.
                Нет явлений, которым природа ума воспрепятствовала бы возникнуть.
                Как вода в океане собирается в волны, так и эти явления
                возникают из ума и, недвойственные уму, освобождаются в уме же.</p>
            <p class="text-justify">Какие имена ни давай, какие ярлыки ни
                цепляй, не существует ничего, единственно кроме ума. Но это
                единство не имеет ни основы, ни корня.</p>
            <p class="text-justify">На него не может быть одностороннего
                воззрения.</p>

            <div class="flex justify-center my-4">
                <ul class="text-left list-none italic">
                    <li>Его нельзя рассматривать как сущее,</li>
                    <li>ибо оно нигде не осуществляется.</li>
                    <li>Его нельзя рассматривать как пустое,</li>
                    <li>ибо оно осознается и сверкает ясностью.</li>
                    <li>Его нельзя рассматривать как и то, и другое,</li>
                    <li>ибо ясность и пустота нераздельны.</li>
                    <li>Сиюминутное самоосознание отчетливо ясно.</li>
                </ul>
            </div>

            <p class="text-justify">События происходят, но нет осознания
                действующего лица (совершающего их). Хотя и нет у вещей
                само-бытия,
                но они действительно воспринимаются в опыте. Если так
                практиковать – достигнешь освобождения.</p>
            <p class="text-justify">Познание происходит с непосредственной
                опорой на органы чувств без вмешательства интеллекта.</p>

            <p class="text-center font-bold text-gray-800 my-4">* * *</p>

            <p class="text-justify">Кунжут и молоко – оба являются причиной
                масла,
                но без выдавливания кунжута или пахтания молока масло не
                получишь.
                Так и все живые существа, хотя все поистине обладают потенциалом
                буддства, но если не будут практиковать, то буддства не
                достигнут.</p>
            <p class="text-justify">Если будет практиковать, то даже
                пастух-кочевник
                достигнет освобождения. Хоть и не слыхал наставлений, но
                чувствует
                себя как дома в этом очевидном состоянии. Когда сам отведаешь
                сахару, нет нужды, чтоб кто-то описывал тебе его вкус.</p>
            <p class="text-justify">Даже пандита может ошибаться, если у него
                нет
                этого прямого опыта. Хоть и поднаторел в толковании девяти
                колесниц,
                но это как распускать слухи о далеких местах, где никогда не
                был.
                Состояния будды даже на миг не коснется.</p>
            <p class="text-justify">Когда понимаешь это, то и благо, и грехи
                освобождаются в естественное состояние. Когда не понимаешь
                этого, то
                хоть благо творишь, хоть грешишь – лишь копишь карму, скитаясь
                меж
                раем и адом. Но как только понял, что твой ум – это пустота и
                мудрость, не созреет больше ни хорошего, ни дурного. Как в небе
                не
                может забить родник, так в шунье не может реализоваться ни
                хорошее,
                ни дурное.</p>
            <p class="text-justify">Таким образом, естественное осознание видит
                очевидное в его неприкрытости. Это самоосвобождение через
                обнажающее
                внимание весьма глубоко, потому освойтесь с этим естественным
                состоянием.</p>

            <p class="text-center font-semibold text-gray-800 mt-4">ЗАБ ДЖА!<a
                    href="#citation-6" class="citation-mark" id="ref-6"><sup>
                        6</sup></a></p><br>

            <p class="text-center font-semibold text-gray-800 mt-4">* * *</p>

            <p class="text-center">Э МА!</p>
            <p class="text-justify">Закончен текст «Самоосвобождение через
                обнажающее внимание – Прямое Введение в Ригпа».</p><br>

            <p class="text-justify">На пользу живых существ грядущих поколений,
                коим выпадет жить в нечистую эпоху кали-юги, я составил все эти
                тантры, агамы и упадеши, по необходимости, в краткой форме.
                Хотя я и составил их сейчас, но укрыл в тайных кладах, как
                драгоценные сокровища – терма. Пусть будут найдены, когда
                приспеет их время.</p>

            <p class="text-center font-semibold text-gray-800 mt-4">
                Самая ДЖА! ДЖА! ДЖА!</p>

            <p class="text-center font-bold text-gray-800 my-4">* * *</p>


