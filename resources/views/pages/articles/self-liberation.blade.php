            <p class="text-justify">Цикл текстов «Карлин шитро» посвящен
                сокровенному духовному действу, известному в дзогчене как
                Прямое Введение в Ригпа. Природа ума, Прямое Введение и Ригпа —
                ключевые термины атийоги, но сколько бы книг ни было,
                написанных компетентными авторами, значение этих терминов
                остается ускользающим для внимания не только искушенного
                читателя, но порой и для практика дзогчена. Предлагаемый перевод
                сочинения Падмасамбхавы дает возможность проверить каждому свое
                понимание этих понятий, углубить их, а может быть, и пережить,
                сверяясь с мыслью и духовным импульсом Великого Мастера из
                Уддияны.</p>
            <p class="text-justify">Тибетский термин дзогчен (rdzog-pa chen-po)
                и его санскритский эквивалент mahasandhi — Великое
                Совершенство, или Великая Завершенность, описывают сокровенное
                и изначальное состояние каждой личности, не зависящее от
                времени и пространства. В подобном состоянии заключена наша
                истинная природа, лишенная ошибок и являющаяся потенцией Будды;
                синоним изначального состояния — это природа ума (sems-nyid),
                которую не нужно путать с умом (sems). Разницу между природой
                ума и умом дзогченовские авторы объясняют с помощью метафоры
                зеркала, где зеркало с его способностью отражать сравнивается с
                природой ума (или умом-как-таковым), а отражения в зеркале — с
                умом как процессом мышления. Ум (и его содержание — мысли) не
                знают о себе. Знание о себе, или осознание, в его элементарном
                виде, реализуемое способностью к отражению (ум-как-таковой),
                лишенное какого бы то ни было содержания, кроме осознания
                простого «есть», это и будет ригпа (тиб. rig-pa; санскр. vidya)
                — знание, видение. Видеть и знать на этом воистину самом
                глубоком, элементарном уровне осознания означает одно и то же.
                Эта изначальная осознанность мастерами дзогчена считается
                свойством природы ума и сравнивается с зеркалом, при этом
                ничего не говорится о происхождении его отражательной
                способности. Отметим лишь, что зеркало можно представить как
                пространственный фантом разрыва ратны — неоднородности акаши, и
                последующего за этим сложного взаимодействия махабхут. Зеркало
                плоско в идеальном случае, и его отражательная способность или
                первичная осознанность чиста и ничем не искажена, в нем неоткуда
                взяться мыслям, в нем нечему отражаться. Это — случай
                дхармакаи, он чист и пуст, все есть шуньята (субъектом такого
                события является Адибудда Самантабхадра — Всегда Благой). Но
                реальность всегда вероятностна и содержит флуктуации, случайные
                и неисчислимые неточности, иными словами Зеркало, как говорят
                оптики, «ведет», в нем неумолимо появляются неоднородности, и
                Плоское Зеркало превращается в Зеркало кривое. Собственно, с
                этого момента начинается Бытие, ибо в Зеркале появляются
                изображения, мир обретает размер, а Зеркало — радиус кривизны,
                и на голый факт осознания (ригпа) накладывается содержание, и
                сансара неумолимо «начинается».</p>
            <p class="text-justify">Индивидуум забывает о возможности Плоского
                Зеркала, он не ощущает за многообразием и игрой образов
                первичный факт обычной осознанности — ригпа, впадает в неведение
                — ma-rig-pa (санскр. avidya), буквально, не видит того феномена
                способности отражать, благодаря чему сам существует как
                сознательное существо. Вернуть такое ощущение, ощущение ригпа,
                можно — это и есть содержание Прямого Введения, удержание же его
                реализует состояние, которое западные исследователи дзогчена
                называют «присутствием» (англ, presence). Добавим лишь, что
                такая модель Зеркала не статична, она пульсирует около особой,
                сингулярной, дхармакаевой точки — Плоского Зеркала (в
                тантрийской символике — это центр, пуп ваджра).</p>
            <p class="text-justify">В названии текста присутствует термин Прямое
                Введение (ngo-sprod). Термин этот появился в русскоязычных
                текстах как калька английского direct introduction. Однако
                английский глагол to introduce означает также «представлять» (в
                смысле знакомить) и более буквальным переводом будет
                «непосредственное знакомство». Здесь уместно будет обратиться к
                этимологии исходного тибетского термина, чтобы полнее раскрыть
                смысл этого одного из самых важных понятий учения Великого
                Совершенства.</p>
            <p class="text-justify">Первая часть, ngo, буквально означает
                «лицо». Но в тибетском языке это слово означает не просто лицо,
                не внешний вид (zhal — собственно лицо как часть тела), а,
                скорее, идентичность объекта, его сущность, или, если речь идет
                о человеке, состояние души. Можно сравнить его с употреблением
                слова «лицо» в русском обороте «показать свое истинное лицо».
                Глагол sprod означает обучать, знакомить и имеет парный
                непереходный глагол «phrad», означающий «знать, быть знакомым
                (с чем-то)». Таким образом, весь термин можно перевести как
                «показать (ученику) его истинное лицо», или «познакомить
                (ученика) с его истинной сущностью». Падмасамбхава пишет в
                публикуемом тексте о восьмидесяти четырех тысячах наставлений
                Будды: «Ни о чем ином не проповедуют они, кроме как об узнавании
                собственного лица».</p>
            <p class="text-justify">Текст изобилует множеством общебуддийских
                терминов — анатман, дхармата, мадхьямика. и др., а также
                классическими терминами дзогчена — изначальное состояние,
                ум-как-таковой, самосовершенство. Используемые автором термины
                особо важны для понимания именно этого текста; вот они, начиная
                с уже упомянуто ngo-sprod: </p>
            <ul class="my-6">
                <li><strong>ngo-sprod </strong> — прямое введение — познакомить
                    (показать) ученика с его (ученика) собственным лицом (сущностью,
                    идентичностью). Как результат успешного прямого введения (и
                    практики Трех Заветов Ганраба Дорже), ученик...
                </li>
                <li><strong>ngo-shes</strong> — знает свое лицо, свою истинную природу.
                </li>
                <li><strong>gCer-mthong</strong> — обнажающее внимание. Взгляд,
                    срывающий пелену иллюзий и вскрывающий/обнажающий суть вещей.
                    Отбрасывая иллюзии, приходим к…
                </li>
                <li><strong>mngon-sum</strong> — ...очевидному...</li>
                <li><strong>da-lta’i shes-pa</strong> — сиюминутному осознанию
                    (уму). Это
                    «теперь-осознание» видит обнаженным очевидное естественное
                    состояние — свое собственное лицо. Зачем говорить, что не
                    видишь этой очевидности?! И ничего иного не найти. Сколько
                    ни ищи, найдешь лишь ум, который...
                </li>
                <li><strong>mthong-snang</strong> — зрит свое же собственное
                    явление самому себе же.
                </li>
            </ul>

            <p class="text-justify">Текст Падмасамбхавы не только теория. Его
                чтение, заучивание, повторение должны произвести действо,
                означенное в названии — «Прямое Введение в Ригпа»; действие это
                предлагается совершить «через обнажающее внимание»
                (gcer-mthong). Вниманием обнажается ригпа — способность к
                отражению — обычное осознание. Обнаружив, его надо удержать —
                это и есть «присутствие», то есть удержание ригпа, пребывание в
                состоянии непокидания момента «обычного осознания» (tha-mal-gyi
                shes-pa). «Простое внимание, усиленное тантрийской практикой,
                есть надежный путь к нирване». Последняя формулировка
                принадлежит Б.Д. Дандарону. И это не просто трудно, это не
                просто много, это — все, в этом — состояние будды, ибо в ригпа —
                фундаментальном свойстве знать о себе в минимальном объеме
                «есть», в свойстве осознания, где все чисто (пусто) и ясно
                (прозрачно), не искажено, нет неведения, там потенция, зародыш
                будды (сугатагарбха). Если ухватить эти три в единстве: ясность,
                осознание и пустоту, то реализация совершена</p>
            <blockquote class="text-justify">— «Если нераздельность ясности,
                осознания и пустоты самосовершенна, зачем говорить, что, практикуя,
                не обрел сиддхи?!"
            </blockquote>
            <p class="text-justify">Из свойств ригпа Падмасамбхава выделяет
                непрерывность, то свойство ригпа, которое позволяет в явном
                восприятии, осознании через внимание удержать факт первичной
                отражательной способности нашего естества, то есть быть в
                состоянии присутствия.</p>
            <p class="text-justify">Практика такого обнажающего внимания есть
                практика созерцания (не путать с медитацией), когда индивид
                соединяет напряжение внутреннего видения с тем, что именно
                мерцает, взблескивает в глубине удивительного феномена
                осознания, в той глубине, где и совершается поразительный
                процесс элементарной отражательной способности живого существа.
                В западных книгах по буддизму термины «созерцание» и
                «медитация» путают, либо не видят в них разницы. Четкую
                границу между двумя процессами проводит только Д. Рейнольдс:
                «Перевод dgongs-pa как ‘медитация’ не достигает цели. Медитация
                (bsgom-pa) подразумевает медитацию над чем-то, будь то образ
                божества или содержание размышления типа того, что природа всех
                вещей пуста. Медитация двойственна по своей природе, она
                существует во времени и обусловлена, ибо включает в себя работу
                ума. Но изначальное состояние индивидуума (dgongs-pa) вне
                времени и обусловленности деятельностью ума. Поэтому правильнее
                говорить о ‘самоосвобождении в Изначальном состоянии’ (dgongs-pa
                rang-grol). Быть в этом состоянии можно только посредством
                созерцания, поэтому когда слово dgongs-pa является глаголом, его
                нужно переводить как ‘созерцание’. Мы должны четко разделять
                понятия ‘созерцание’ и ‘медитация’. ‘Созерцание’ — это
                вхождение и пребывание в состоянии чистого присутствия, или
                внутреннего осознания (ригпа), в то время как медитация
                подразумевает активность ума и возведение ментальных
                конструкций. Войдя же в состояние созерцания, мы позволяем любым
                возникающим мыслям самоосвобождаться в их собственном измерении
                (rang-sar-grol). В этом заключается метод дзогчена» (J.
                Reynolds. Self-Liberation Trough Seeing With Naked Awareness.
                Station Hill Press, 1989, p. 35).</p>
            <p class="text-justify">Падмасамбхава несколько раз в этом тексте
                подчеркивает, что все школы буддизма и даже тиртики говорят об
                одном и том же, что главнейшие понятия разных школ — это
                синонимы: природа ума, атман, анатман, виджняна,
                праджняпарамита, сугатагарбха, махамудра, единственное тигле,
                дхармадхату, алая. Сам же он предлагает остановиться на
                привычном понятии «обычное осознание», чем выводит свой взгляд
                из принадлежности какой бы то ни было школы вообще. Разрушая
                догматические конструкции, прививая бесконцептный взгляд на мир
                своим последователям, Падмасамбхава поставил себя вне школ, по
                крайней мере в свое время, тогда он для всех был как бы чужим.
                Тексты же его сохранили лишь ближайшие ученики. Востребованными
                они стали позже, воскреснув для жизни уже в качестве терма и
                поставив их автора в основание старейшей буддийской школы
                ньингма. Но случилось это только через шестьсот лет, в эпоху
                появления первых тертонов.</p>
            <p class="text-justify">Первый перевод этого текста в Европе сделал
                Эванс Вэнс. Этот перевод вошел как основная часть в изданную им
                «Тибетскую Книгу Великого Освобождения». Автор перевода вообще
                не знал тибетского языка и в основном редактировал английский
                перевод, сделанный другими. При этом, поскольку о дзогчене в
                Европе не было ничего известно, многие приняли ошибочное и
                искаженное истолкование этого учения Эвансом Вэнсом.</p>
            <p class="text-justify">Во многом ситуацию с этим важным текстом
                исправила блестящая работа современного знатока дзогчена Д.
                Рейнольдса. Он издал перевод этой работы Падмасамбхавы c
                комментарием к нему (см. выше), при этом он пользовался
                консультациями ламы Торчина из Конпо и профессора Намхая Норбу
                Ринпоче — ведущего учителя дзогчена в наши дни. Перевод В.Е.
                Ушакова, сделанный непосредственно с тибетского языка, раза в
                четыре меньше по объему, чем перевод Д. Рейнольдса, он лаконичен
                и практически не содержит слов-вставок и слов-пояснений
                переводчика. </p>
            <p class="text-justify">Можно предположить, как возник текст. В
                течение всего наставления Падмасамбхава с разных позиций, с
                повторами, прибегая к сравнениям, используя синонимы, настойчиво
                объясняет, что же такое ригпа. Создается впечатление, что он
                неоднократно объяснял смысл ригпа своим ученикам, а они не
                понимали, либо понимали не так, как он хотел, что вызывало в нем
                заметный след разочарования: «да никак не поймем», «вообще не
                понимают, либо превратно понимают, либо однобоко понимают». И
                Падмасамбхава снова и снова пускался в объяснения, применяя и
                отбрасывая сложные санскритизмы типа анатман, виджняна,
                махамудра, праджняпарамита и настойчиво завершая утверждением,
                что проще всего и яснее всего определять ригпа, как обычное
                осознание.</p>
            <p class="text-justify">В конце концов, Учитель записал серию
                многочисленных ответов и определений на провоцирующие и
                настойчивые вопросы учеников. Эта запись и составила настоящий
                текст.</p>
            <p class="text-justify">Не обойти особым вниманием чрезвычайно
                важных строк Падмасамбхавы: «Естественное ригпа, естественное
                осознание, естественная ясность возникает сверкая. Это и есть
                бодхичитта». Это редкое определение бодхичитты и своеобразное
                свидетельство в пользу антропного принципа — нет бытия без
                возможности самоотражения (ригпа), нет бытия без
                наблюдателя.</p>
            <p class="text-justify">В одной фразе Падмасамбхава не избегает
                оттенка противопоставления Будды и Ваджрасаттвы — это может
                быть свидетельством самостоятельности дзогчена по отношению к
                учению Шакьямуни и свидетельством об исключительно дзогченовском
                генезисе Ваджрасаттвы?!</p>
            <p class="text-justify">Заканчивается текст именем составителя —
                Падмасамбхавы — и знаменательным пожеланием: «Пусть не кончится
                (ma-rdzogs), пока сансара не опустеет». Это «не кончится», или
                «не завершится», то есть останется открытым в саморазвитии,
                означает, что Падмасамбхава не присваивает ни себе, ни
                человеческой природе вообще конечное знание, предлагая понимать
                все сказанное и практически пережитое как творческий и
                нескончаемый поток непредсказуемого развития человека и его
                познавательной устремленности в постоянном самоосвобождающем
                процессе самоосознания.</p>
            <p class="text-right italic my-4">В.М. Монтлевич</p>
        </div>

