                Ежедневно положено повторять стослоговую мантру не менее двадцати одного раза.
                Оптимальный ритм, реализующий парамиту усердие, это 100 раз в день. Если ситуация
                неблагоприятна для созерцания, то рекомендуется повторять краткую мантру – ОМ
                ВАДЖРАСАТТВА ХУM, польза будет та же, что и от повторения стослоговой мантры. Но
                Учителя всегда подчеркивают, что повторение мантры с созерцанием во много раз
                эффективнее, чем просто повторение мантры. Согласно Е.А. Даргьею, Е. Рабдену и
                Гэгэну Кьенце неспешное повторение мантры, её «умное бормотание» может
                сопровождаться одним из трёх видов представлений.
                1) Нектар и свет, исходящий из Ваджрасаттвы на вашей макушки, входит в вас и падает
                на живые существа, которые вы поместили предварительно в своем сердце, и устраняет
                все ваши и их клеши. Клеши, как грязная вода, удаляется из пор и отверстий нижней
                части тела. («Грязная вода» не может просто так исчезнуть или испоганить некий
                «нижний» мир. Она как и всё состоит из махабхутов, чьё конечное превращение – Чистые
                махабхуты или Пять Мудростей и, наконец, Ясный Свет. Подобное «безотходное
                доусвоение» наших клеш осуществляют особые существа, для которых грязная вода клеш
                привычная пища, которую они могут «переварит» – поглощая осадочные махабхуты они
                сразу превращают их в Свет. Подобный процесс созерцается в некоторых методах
                Ваджрасаттвы. – В.М.). Когда все клеши удалены, все живые и вы представляете себя
                как чистейший сверкающий кристалл. 2) Нектар и свет удаляют клеши как пену и гной,
                выходящие изо рта, глаз и ушей. И снова вы становитесь подобными кристаллу. 3) Свет
                и нектар разрушают клеши подобно тому, как свет рассеивает тьму.
