                желания, мы, лишь заметив это по указанию Учителя, усиливаем резонанс уровней нашего
                существа, и, пережив целостность, можем достичь Ясности Видения Всего. Истинная и
                безличная причина вращения вообще и мантры в частности, а значит и живого — это
                асимметрия «кванта» пространства, символически описываемая в текстах как «дупло
                драгоценности». Именно порядок выхода махабхутов из дупла драгоценности
                (воздух-огонь-земля-вода) образует вращение, в правом или левом варианте, чем
                реализуется Тело, Речь и Ум Совершенных. Вращением мантры свет обращается в нектар,
                нектар — это «свет в сосуде». Вращение насильственно собирает свет в фигуру ратны
                (сосуд), вселенский лотос света «закрывается» в бутон. Пойманный свет нектаром
                стекает в йогина и является очищающим источником (бинду) потока (прана) колебаний
                (нади), то есть формирует собой центральный канал — авадхути (санскр. avani —
                поток, dhūti — колебание).<a href="#ref-19"
                                              class="text-blue-500 hover:underline">↩</a></p>

