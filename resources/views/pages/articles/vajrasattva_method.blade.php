                истинный, то есть обладающий знанием саттва, <em>джнянасаттва</em> (тиб. ye-shes
                sems-dpa', Ваджрасаттва I). Йогин, превращающийся и затем пребывающий в состоянии
                Ваджрасаттвы, это <em>самаясаттва</em> (<em>тиб.</em> dam-tshig sems-dpa'), саттва
