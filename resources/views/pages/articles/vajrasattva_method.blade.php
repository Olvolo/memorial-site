            <p class="mb-4" id="citation-13"><sup>13</sup> «...пригласил джняну, подобного самому
                (Ваджрасаттве)..». Джняна (<em>тиб.</em> ye-shes), букв. «изначальное знание»,
                неличностное знание, коим обладает тот, кто не имеет индивидуального «я», то есть
                Будда и в частности Самантабхадра. Виджняна — сознание, индивидуальная
                сопричастность джняне. Её персонификацией является Ваджрасаттва. Между джняной и
                виджняной должен находится механизм их соединения (приставка «со» в слове
                «сознание»), — это эффект автозеркалирования. Его персонификацией является
