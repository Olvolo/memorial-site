        <h1 class="text-2xl font-bold text-center mb-8">ПРЕДИСЛОВИЕ К
            КНИГЕ<br>«БУДОН
            РИНЧЕНДУБ. ИСТОРИЯ БУДДИЗМА»<br>БИОГРАФИЯ БУДОНА РИНЧЕНДУБА<br>(1290
            – 1364)</h1>

        <p>Знаменитый ученый Будон Ринчендуб (Kun-mkhyen Bu-ston Rin-chen-grub),
            историк, переводчик, кодификатор и редактор канона буддийских
            текстов – Ганжура и Данжура – почитается всеми школами и традициями
            буддизма в Тибете. В нем признавали перерождение кашмирского
            энциклопедиста пандита Шакьяшрибхадры (1127 – 1225)<a
                href="#citation-1" class="citation-mark"><sup>1</sup></a>,
            составителя каталогов, неутомимого искателя оригинальных
            санскритских подлинников многих сочинений, неустанного примирителя
            межшкольных разногласий. Будон был тем человеком, через которого
            нравственные идеи буддизма, идеи бодхичитты упрочились в Тибете. В
            большинстве тантрийских линий преемственности новых школ он является
            важнейшим звеном и почитается как видьядхара – держатель знания.</p>

        <p>С ранних лет Будон проявил удивительные способности и устремления,
            кои поощрялись родителями. Его отец, ньингмапинский лама Жалцан
            Балсанпо (rGyal-mtshan dpal bzang-po), был незаурядным философом и
            тантрийским практиком. Мать Соднамбум (bSod-nams ’bum) также была
            духовным практиком из тантрийской семьи. Будон системно изучил
            Винаю, Абхидхарму, Праджняпарамиту и шастры, получив посвящения по
            всем названным дисциплинам. Кроме того, он был посвящен во все
            тантры новой традиции. В особенности отмечают, что две традиции
            Калачакратантры проходят именно через него. Будон и его учитель
            Долбупа из знаменитой школы <i>джонанпа</i> (Jo-nang-pa)<a
                href="#citation-2" class="citation-mark"><sup>2</sup></a>
            почитаются как два самых выдающихся учителя тантры Калачакры в
            Тибете.</p>

        <p>В 1319 или в 1320 г. Будон стал настоятелем монастыря Шалу, что в
            Цанге (gTsang), и руководил им тридцать семь лет. Там он собрал,
            отредактировал и систематизировал переводы буддийских книг в два
            раздела: Ганжур – собрание текстов самого Будды Шакьямуни и Данжур –
            комментарии к этим сочинениям последующих авторов-буддистов. В
            Данжур вошли также тексты по грамматике, медицине, искусствам и по
            другим темам. Он составил гарчаг, то есть оглавление, ко всем
            собранным и систематизированным текстам<a href="#citation-3"
                                                      class="citation-mark"><sup>3</sup></a>.
        </p>

        <p>Предшественниками Будона по собиранию книг были ученые-буддисты при
            царе Содналеге, сыне Тисрондэцена (804 – 817). При нем начался
            процесс собирания и систематизации буддийского священного Писания,
            которое позже в законченном виде станет известным как Ганжур. При
            Содналеге переводчики Каба Палцег (VIII – IX вв.), Шойжи Ньинбо,
            Девендра и Балжи Лхунпо тщательно перебрали богатое книгохранилище
            монастыря Пантан Гамэд (’Phang thang ka med) и составили каталог,
            известный как «Гарчаг Пантанма». Такую же работу провел Каба Палцег
            в монастыре Донтан Дангар (sTong thang ldan dkar) и составил «Гарчаг
            Дангарма». Два эти гарчага считаются первыми систематизированными
            списками буддийской литературы Тибета<a href="#citation-4"
                                                    class="citation-mark"><sup>4</sup></a>.
            В IX в. при царе Ландарме наступила пауза в развитии буддизма, ибо
            царь запретил распространение учения, принял сторону бона и разрушал
            буддийские монастыри. Переводческая и систематизаторская
            деятельность возобновилась в XI в. трудами переводчика Ринчена Занпо
            (958 – 1055). Ринчен Занпо, получивший образование в Кашмире,
            работал по собиранию канона в монастыре Тхолинг в Нгари. В это же
            время в Тибет прибыл проповедник – ученый Атиша (982 – 1054),
            преподававший до этого в монастыре Викрамашила. Он стал основателем
            школы кадампа – влиятельной школы новой традиции. Со времени Атиши в
            течение трехсот лет пересматривались старые переводы, делались
            новые, составлялись гарчаги собраний буддийских текстов, и, наконец,
            многовековой труд тибетских ученых завершил Будон Ринчендуб.</p>

        <p>К этому времени наилучшее собрание текстов было сосредоточено в
            монастыре Нартанг. Нартанг в то время был славен не только
            библиотекой. Здесь работал коллектив переводчиков, печатались и
            копировались тексты. В XIV в. собрание книг Нартанга было
            каталогизировано и значительно расширено неутомимой деятельностью
            страстного библиофила, переводчика и систематизатора Чомдана Ригпаи
            Ралди. Тексты из Нартанга распространялись в копиях по всему
            Тибету<a href="#citation-5" class="citation-mark"><sup>5</sup></a>.
            Работал там и Будон, проведя в общей сложности наездами в Нартанге
            около десяти лет.</p>

        <p>Для составления Канона Будон перевез из Нартанга в Шалу тексты
            Трипитаки, учел тексты монастыря Шалу и составил два комплекта
            текстов: Ганжур и Данжур. Ганжур состоит из 101 тома, в разных
            изданиях количество томов незначительно меняется, Данжур – из 225
            томов. Таким образом, пятисотлетний труд собирания, переводов,
            комментирования и классификации текстов буддийского Писания на
            тибетском языке был завершен.</p>

        <p>Наш соотечественник Гомбожап Цыбиков во время
            экспедиции-паломничества в Тибет в октябре 1900 года побывал в
            монастыре Шалу и сообщил удивительное: «Верстах в пятнадцати выше
            Шигацзе, при устье левобережной пади, находится знаменитый в
            древности монастырь Шалу, в котором сохраняются, между прочим,
            типографские доски знаменитого писателя XIV в.
            Будон-тамчжад-чэнбы»<a href="#citation-6"
                                   class="citation-mark"><sup>6</sup></a>.</p>

        <p>Работая над составлением буддийского Канона, Будон систематизировал
            один из сложных и запутанных корпусов текстов – раздел буддийских
            тантр. Он составил каталог тантр, указал количество глав и, главное,
            провел оригинальную классификацию текстов. Он дал четвероякое
            разделение новых тантр, впервые предложенное учеником Атиши
            Бромтонпой (992 – 1074): это – криятантра, чарьятантра, йогатантра,
            ануттарайогатантра. Так называемые старые тантры, к которым
            сохранились к этому времени их санскритские оригиналы, всего 25
            сочинений, вошли в небольшой отдел «старых тантр» (rNying-rgyud)
            Ганжура.</p>

        <p>Принцип включения в создающийся канон только тех текстов, к которым
            сохранились их санскритские оригиналы, породил азартный поиск
            древних книг приверженцами того или другого учения или теми, кто
            понимал уникальность, духовную и интеллектуальную ценность
            затерявшихся сокровищ Писания.</p>

        <p>Характерна в этом отношении история включения в Ганжур старейшей из
            тантр – «Шри-Гухьягарбха-таттва-винишчаи» (dPal gSang-ba’i snying-po
            de-kno-na-nyid rnam-par nges-pa)<a href="#citation-7"
                                               class="citation-mark"><sup>7</sup></a>.
            Ко времени собирания текстов Канона санскритский оригинал тантры был
            утерян, мало того, представители новой традиции отказывали
            «Гухьягарбхе…» в подлинности, считая ее компиляцией тибетских
            авторов VIII в. С невключением этой тантры, на которой во многом
            основано все здание ньингмапинских практик и знаменитый цикл мирных
            и гневных божеств «Шитро», не могли согласиться сторонники старой
            традиции. Санскритский оригинал тантры специально искал во время
            своего пребывания в Тибете (1204 – 1213) кашмирский ученый
            Шакьяшрибхадра. Он нашел его в монастыре Самье. Санскритский
            оригинал позднее попал в руки Датону Зижиду (rTa-ston gZi-brjid, XII
            – XIII в.), «который подарил его Шаган–лоцзаве. Тот послал этот
            текст Чомдану Ригпаи Ралди (bCom-ldan Ral-gri)»<a href="#citation-8"
                                                              class="citation-mark"><sup>8</sup></a>.
            Так старейшая тантра была включена в Канон.</p>

        <p>Но не все старые тантры попали в Канон, ибо сам Будон довольно
            сдержанно относился к включению их в Ганжур. В переводе «Голубых
            Анналов» Гой-лоцзавы Шоннупэла (1392 – 1481) в главе о старых
            тантрах Ю. Н. Рерих пишет: «Многие тибетские ученые ставили под
            вопрос подлинность тантр ньингма. Поэтому Будон не включил их в свой
            “Каталог” и утверждал в “Шойжунге” [“История буддизма”]: “Что
            касается древних переводов тантр ньингма, то лоцзава Ринчен Занпо,
            Лха-лама Ешей Од, Пхобран Жибаод и другие придерживались мнения, что
            они (тантры ньингма) не представляют собой чистые тантры. Мой
            учитель лоцзава Нима Жалцан и Риграл [Чомдан Ригпаи Ралди. – Прим.
            ред.], а также другие считали их настоящими тантрами, потому что в
            Самье они нашли индийские подлинные тексты, а части
            «Ваджракилатантры» были обнаружены в Непале. По моему мнению, лучше
            оставить их, не высказывая суждения (об их подлинности)…”»<a
                href="#citation-9" class="citation-mark"><sup>9</sup></a>. Этот
            отрывок интересен тем, что Будон называет имена своих учителей и
            предшественников по переводческой традиции.</p>

        <p>В 1322 г., пребывая в монастыре Шалу, Будон завершил ныне всемирно
            известную «Историю буддизма»<a href="#citation-10"
                                           class="citation-mark"><sup>10</sup></a>,
            и хотя позже были составлены такие фундаментальные труды, как
            «Голубые Анналы» Гой-лоцзавы Шоннупэла и «История буддизма в Индии»
            Таранатхи (1575 – 1634), работа Будона остается наиболее полным из
            ранних исторических сочинений тибетских авторов. В отличие от ученых
            более позднего времени, при Будоне еще были в сохранности многие
            санскритские оригиналы позже безвозвратно исчезнувшие. Собственно
            отдельные главы «Истории…», посвященные обзору буддийских текстов,
            стали основой для дальнейшей работы по составлению списков Ганжура.
        </p>

        <p>«История…» Будона состоит из четырех частей: «Обзор буддийских
            учений», «Возникновение буддизма и его развитие в Индии», «История
            буддизма в Тибете», «Систематический каталог литературы,
            переведенный на тибетский язык» с важной главой «История
            возникновения и развития йогатантры, “Гухьясамаджатантры”,
            “Чакрасамваратантры”». Работа Будона была первой в Тибете,
            основательно излагающей историю буддизма в Индии. Причем Будон
            описывает не историю страны, она лишь служит ему фоном для
            подробного описания истории буддизма. Излагая истории буддийских
            школ, Будон вынужден был во множестве перечислять и буддийские
            сочинения с кратким их изложением. Это сделало его труд неоценимым
            источником не только для историков, но для религиоведов и философов.
            Труд Будона всегда был славен на его родине, ссылки на него можно
            найти почти в каждом историческом произведении Тибета.</p>

        <p>Одно из важных достоинств сочинения Будона – установление точных
            датировок. Многие исследования последующих тибетских и современных
            европейских историков опираются на хронологические ряды Будона. Он
            установил несколько опорных дат, в частности, год рождения царя
            Сронцзангамбо (617) и год свержения Ландармы – царя-гонителя
            буддизма (814). Хронологические выкладки Будона за редким
            исключением стали опорными для всей последующей тибетской
            историографии. Он проделал скурпулезнейшее исследование и сравнение
            хронологических рядов, составленных предшественниками: Атишей (982 –
            1054), кашмирцем Шакьяшрибхадрой, первым сакьясцем Дагпой Жалцаном
            (1172 – 1216), Сакья-пандитой Гунгой Жалцаном (1182 – 1251) и
            другими.</p>

        <p>В европейской буддологической литературе первым отметил «Историю
            буддизма» венгерский тибетолог А. Чома де Кёрёши<a
                href="#citation-11" class="citation-mark"><sup>11</sup></a>.
            Российский буддолог В. П. Васильев в переводе «Истории буддизма в
            Индии» Таранатхи неоднократно ссылается на труд Будона<a
                href="#citation-12" class="citation-mark"><sup>12</sup></a>.
            Отдельные части «Истории» переводил академик Ф. И. Щербатской<a
                href="#citation-13" class="citation-mark"><sup>13</sup></a>.
            Щербатской перевел также заметки Будона по Абхидхарме школы
            сарвастивада<a href="#citation-14"
                           class="citation-mark"><sup>14</sup></a>. Более всех
            из старой буддологической школы России уделил внимание Будону А. И.
            Востриков в своей уникальной работе «Тибетская историческая
            литература»<a href="#citation-15"
                          class="citation-mark"><sup>15</sup></a>. И, конечно
            же, неоценима роль Е. Е. Обермиллера, переводчика «Истории…»,
            введшего в современную науку имя знаменитого буддийского историка.
        </p>

        <p>Кроме «Истории буддизма» Будон написал множество сочинений на самые
            разные темы философской и религиозной проблематики буддизма. Все они
            были изданы в виде собрания сочинений в 30 томах, которое
            сохранилось лишь частично. Отметим четыре работы Будона, упоминаемые
            А. И. Востриковым. К классу сочинений шойжунг (chos-’byung) он
            относит четыре следующие сочинения: «Одно из них излагает историю
            возникновения системы йогатантры…, другое посвящено истории
            возникновения и распространения мистического учения Гухьясамаджи…,
            третье произведение посвящено истории системы Самвары и четвертое
            сочинение останавливается на истории знаменитой астрологической
            системы Калачакра»<a href="#citation-16" class="citation-mark"><sup>16</sup></a>.
        </p>

        <p>Несколько замечаний о принадлежности Будона к определенному течению
            тибетского буддизма. Он был связан со многими традициями. Сумба
            Хамбо (1704 – 1788) в историческом сочинении «Пагсам-чжонсан» пишет
            об одном ученом ламе: «Он, по-видимому, есть Шойку Одзер, или Дагмэд
            Дорже, учитель Паг Ода, который был, в свою очередь, учителем
            Будона». Этот Шойку Одзер был никем иным, как Шойжи Одзером (1214 –
            1294) – известным ламой, проповедавшим в Монголии и
            усовершенствовавшим монгольский алфавит<a href="#citation-17"
                                                      class="citation-mark"><sup>17</sup></a>.
            Среди учителей Будона называют ламу Кунгу Лодой Жалцана (1312 –
            1375). Кроме того, Будон принадлежал к линии преемственности,
            восходящей к известному комментатору Праджняпарамиты и
            «Абхисамаяланкары» Шонну Сультиму (известному как Карчун Рингмо –
            sKar-chun-ring-mo). К этой же традиции принадлежал и знаменитый
            автор «Голубых Анналов» Гой-лоцзава Шоннупэл. Был связан Будон и с
            традицией школы сакьяпа. Вообще же его принято причислять к школе
            кадампа, предшественнице школы гелуг<a href="#citation-18"
                                                   class="citation-mark"><sup>18</sup></a>.
            Среди учеников Будона, унаследовавших его интерес к истории и
            хронологии, известен Юнтон Доржебал (1284 – 1365), принявший участие
            в нескончаемых уточнениях времени рождения и нирваны Будды. Юнтонпа
            принадлежал линии махайоги школы ньингмапа и был знатоком
            «Гухьягарбхатантры» и дзогчена<a href="#citation-19"
                                             class="citation-mark"><sup>19</sup></a>.
        </p>

        <p>«История буддизма» стала еще при жизни автора популярным сочинением в
            ареале стран северного буддизма. Труд Будона был высоко оценен в
            соседнем Китае, всегда славившемся высоким уровнем исторической
            науки. Послы из Пекина посетили ученого и предложили ему приехать в
            Пекин. Но Будон, сославшись на неблагоприятное время, отказал
            им.</p>

        <p>Конечно же, Будон был не только историком, прежде всего он, как и
            большинство ученых лам, был буддистом-практиком, в частности,
            практиком системы Калачакра. У него была небольшая группа
            последователей. Они составили собою традицию, которую в честь
            учителя назвали бупа, или шалупа, по имени монастыря, где долгие
            годы пребывал Будон<a href="#citation-20"
                                  class="citation-mark"><sup>20</sup></a>.</p>

