            <p>
                Ценнейшим вкладом в нее был подготовленный Дандароном «Краткий
                тибетско-русский словарь» (издан в 1963 г. совместно с Б. В.
                Семичевым, под редакцией Ю. М. Парфионовича), ставший на долгие
                годы непременным спутником европейских буддистов. Он также
                перевел и издал знаменитый «Источник мудрецов» — буддийский
                терминологический словарь, подготовленный буддийским
                просветителем XVIII века Джанжа Рольби Дорже (1717-1786).
            </p>
            <p>
                Особый период в жизни Дандарона, который и назвали его биографы
                духовным подвигом, начался в 1965 году, когда к нему в Улан-Удэ
                приехал его первый европейский ученик. С этого времени можно
                говорить о возникновении буддизма как религиозного движения на
                западе России. К Дандарону идут ученики из Ленинграда, Москвы,
                Тарту, Вильнюса, Каунаса, Кишинева. Дандарон говорил: «Это не
                они едут ко мне в Улан-Удэ, это буддизм идет на Запад!»
            </p>
            <p>
                Передача духовной традиции в пространство иных культур, других
                народов — в особенности в тех случаях, когда это происходит
                впервые, — требует особого, духовного мужества — «неустрашимости
                Будды». Не сразу и не все ламы поддержали духовное новаторство
                Дандарона. «Зачем Западу буддизм? Там есть свой Бог»,— говорили
                ему многие из них. Но не все. Его проповедь буддизма для
                западных искателей восточной духовности поддержали лама-философ
                Готавон, настоятель Иволгинского дацана Цыбен Цыбенов, тувинский
                лама Гендун Цырен, лама-врач Дашиев. С удивительным пониманием и
                глубоким осознанием важности и необычности происходящего
                воспринимали приход к буддизму представителей новых народов
                верующие буряты — гостеприимные жители Кижингинской долины и
                Улан-Удэ.
            </p>
            <p>
                Дандарон обучил своих новых учеников не только практике тантр,
                но и основам буддийской философии. Он пишет ряд блестящих
                статей, которые стали основой для изучения теоретического
                буддизма. В его книге «Мысли буддиста» (1970) — содержащей всего
                лишь 235 машинописных страниц, изложена буддийская философия,
                этика и теория путей — замечательное пособие для начинающих
                буддистов-практиков.
            </p>
            <p>
                Не случайно Дандарон передавал ученикам основы именно
                тантрийских практик — методы трансформации психики и сознания,
                раскрытия естественной природы личности. Это самый верный путь
                передачи буддийской традиции представителям других культур,
                воспитанных в иных традициях. Импульс проповеди Дандарона быстро
                нашел отклик -- спонтанно реализовалась вместерождённость
                Учительства и ученичества. И уже окруженный учениками Дандарон
                подтверждает: «Тантра — на Запад!». Времени ему было отпущено
                мало, и как бы зная об этом, он находит оптимальные формы
                передачи духовного знания - через абсурд, ситуационную
                неопределенность, через методы быстрого вскрытия интуиции и
                реализации недвойственности, через игру, смех и гнев. Это была
                его Махамудра<a href="#citation-1" id="ref-1"
                                class="citation-mark"><sup> 1</sup></a>.
            </p>
            <p>
                Росла известность Дандарона как ученого и как учителя, ширился
                круг учеников. Человек независимый, погруженный в духовное
                творчество, он, естественно, находился под неустанным присмотром
                властей. «Что характерно для отношения к Дандарону местных
                бурятских властей, так это то, что они ненавидели его именно за
                его принадлежность к буддийской духовной традиции — религии, ими
                гонимой и отвергнутой. Что касается другой стороны его
                проповеди: проповедуемого им философского универсализма
                буддизма, — то эта сторона была им просто недоступна, ибо они
                уже отступились от традиции. И Дандарон как бы служил им живым
                напоминанием об их отступничестве от прежней культуры и о
                невозможности реального принятия ими никакой другой, хотя он
                сам, будучи человеком глубоко позитивного склада, никогда не
                имел в виду упрекать их за это», — так вспоминает об учителе
                А.М. Пятигорский в своём эссе «Уход Дандарона».
            </p>
            <p>
                В августе 1972 года в Бурятии начинается — не могло не начаться
                — так называемое «дело Дандарона». Вместе с ним арестовывают
                четырех его учеников, с остальных берут подписку о невыезде из
                Улан-Удэ. Дандарона обвиняют в организации религиозной группы.
                «Дело» было затеяно во всероссийском масштабе — обыски проходили
                в Mocкве и Ленинграде, в Прибалтике. В Москве изымают буддийские
                книги у известной и всеми почитаемой О. Ф. Волковой (1926-1988)
                — санскритолога и буддолога, допрашивают А. М. Пятигорского, в
                Тарту безуспешно пытаются допросить буддолога Л. Мялля.
                Допрашивают учеников в Ленинграде, пытаются привлечь к
                ответственности за явно дружеское и откровенно сочувственное
                отношение к Дандарону бессменного преподавателя тибетского языка
                на восточном факультете Ленинградского университета Б. И.
                Кузнецова. Однако на западе России антибуддийскую кампанию
                свернули. О. Ф. Волковой принесены извинения, возвращены книги.
                В Бурятии же все продолжалось. Чтобы не допустить четырех
                арестованных учеников до суда, их после десятиминутного
                амбулаторного обследования объявляют душевнобольными — все та же
                печально знаменитая «вялотекущая шизофрения». Судят одного Бидию
                Дандарона. Безуспешно пытаются доказать суду полную невиновность
                Дандарона — адвокаты Н. Я. Немеринская и Н. Я. Герасименко. Суд
                соглашается с прокурором Байбородиным и выносит приговор — пять
                лет лагерей общего режима. Дандарону уже 58 лет. Позади
                девятнадцать лет сталинских лагерей. И снова лагерь — в сыром и
                гиблом месте, в продуваемом ветрами поселке Выдрино на южном
                берегу Байкала.
            </p>
            <p>
                Не потерявший достоинства в сталинских лагерях, Дандарон и в
                брежневском ведет себя независимо. Здесь, в «гневной мандала» —
                в ограде из колючей проволоки, под прицелом часовых на вышках,
                продолжает борьбу. В письмах ученикам он развивает идею синтеза
                буддизма с современными положениями физики и философии. Пишет
                новый труд — о карме народов, судьбе Союза с позиций теории
                тантры. Но эта, так называемая «Черная тетрадь» исчезает при
                очередном лагерном обыске. Некоторое время положение Дандарона
                облегчает подполковник Колбасьев, однако с его уходом из лагеря
                ситуация резко ухудшилась. За малейшую провинность — штрафной
                изолятор, ледяное помещение, с пристегивающейся на день койкой,
                жалкая пайка хлеба на сутки и легкое нижнее белье.
            </p>
            <p>
                26 октября 1974 года Дандарон уходит из этого рождения и,
                возможно, из этого места<a href="#citation-2" id="ref-2"
                                           class="citation-mark"><sup>
                        2</sup></a>.
            </p>
            <p>
                Ученики называли его Изначальным Учителем. Он говорил: «Есть
                много традиций (буддизма - В.М.): цзонхавинская, Ньйнгма, Каржуд
                и другие. Моя традиция их всех объединяет». Поэтому правильнее
                говорить не о традиции, а о «полноте» учения, проповеданного
                Дандароном. Он создал — сам и с помощью учеников — буддийскую
                ситуацию на Западе России, в Эстонии, Литве и позже в Латвии,
                где духовная почва уже была подготовлена для этого в довоенные
                годы деятельностью Общества Рериха и легендарного Карла
                Теннисона. Появление и официальное признание (в 1989 г.)
                буддийских общин в Ленинграде, Москве, Эстонии и Риге — плод
                духовного подвига Дандарона. Но его помыслы шли дальше — он
                видел в буддизме, внешкольном и внемонастырском, возможность
                нового синтеза всех человеческих знаний; возможность духовного
                эволюционного скачка, хотя и допускал угрозу гибели в случае
                кармической необходимости.
            </p>
            <p>
                Сегодняшний день, день тревог и надежд, как никогда ранее
                востребовал духовного. Единство нравственности и высокого
                интеллектуального напряжения буддизма спонтанно вскрывает — и на
                Западе, и в нашей стране — скрытую природу многих людей,
                обнаруживая в них носителей древнего учения Будды.
            </p>
            <p class="text-right italic mt-4">
                В. Монтлевич
            </p>
