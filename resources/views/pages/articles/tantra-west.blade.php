            </p>
            <p>
                В младенце Дандароне, появившемся на свет 13 декабря 1914 года в
                семье ламы-философа и поэта Агван Силнам Тузол Доржи, признали
                новое рождение тибетского хамбо-ламы из Гумбума Джаягсы Гэгэна.
                Духовный вождь буддистов Бурятии, «Царь Учения» Лубсан Сандан
                дает новорожденному имя Ригзин, что на санскрите звучит как
                Видьядхара и отразилось в светском имени Дандарона — Бидия.
                Вскоре из Тибета в Бурятию прибыла делегация лам, чтобы забрать
                мальчика на воспитание, но Лубсан Сандан сказал: «Он нужен
                здесь». И Дандарон остался.
            </p>
            <p>
                В июле 1921 года Лубсан Сандан передает Бидии Дандарону свое
                духовное могущество и титул Царя Учения — Дхармараджи. Ритуал
                духовных посвящений и провозглашения нового Дхармараджи прошел
                при большом стечении верующих в специально построенном для этого
                небольшом храме. Церемонию довелось наблюдать российскому
                буддологу Б. И. Панкратову (1892 - ?). Среди других подробностей
                события он выделяет почетный трон из шестнадцати плоских
                квадратных подушек, какого он никогда до тех пор не видел, хотя
                посетил почти все буддийские страны.
            </p>
            <p>
                Вскоре исчезает (а по одной из версий уезжает на Запад) Лубсан
                Сандан. К тому времени уже умер отец Дандарона, и мальчик
                остался на воспитании матери Балжимы (1870-1973). Она была не
                только хранительницей родовых традиций, но и — как ученица
                Лубсан Сандана — буддийской подвижницей. Многие годы посвятила
                созерцательной практике.
            </p>
            <p>
                С раннего детства ламы обучали Дандарона тибетскому и
                монгольскому языкам, приобщали к буддизму. Но он учился и в
                общеобразовательной школе, где одним из его учителей был Хоца
                Намсараев, будущий бурятский писатель.
            </p>
            <p>
                Популярность Дандарона среди верующих была велика, и с началом
                широкой антирелигиозной кампании для него наступает тяжелое
                время, когда он испытывает постоянное давление и угрозы от
                местных властей. Видимо, это и побудило его покинуть Бурятию. Он
                становится студентом Ленинградского института
                авиаприборостроения. И в то же самое время слушает лекции на
                восточном факультете университета, не оставляет занятий языками.
            </p>
            <p>
                Образование на воле завершить не удалось, 22-летнего Бидию
                Дандарона арестовывают. Обвинение бессмысленно, но именно по
                таким обвинениям приговаривали тысячи людей к высшей мере. Таков
                был приговор и Дандарону. Но он не был исполнен, немедленную
                смерть заменили 25 годами лагерей.
            </p>
            <p>Начались странствия по кругам советской каторги. Годы,
                проведенные в заключении, Дандарон назвал своими университетами.
                Ибо и в тех, казалось бы, невыносимых условиях продолжалась
                творческая жизнь, продолжалось познание. Он встретил там
                образованнейших людей — в том числе российских немцев —
                историков и философов. Возник даже неокантианский кружок. Плодом
                этого общения стали его труды «Взаимоотношение материи и духа» и
                «Эстетика». Продолжилось и буддийское образование — с помощью
                оказавшихся в лагерях лам. Учась, Дандарон учил и сам. В лагерях
                раскрылся его талант проповедника, и у него появились не только
                собеседники, но и последователи. «...Постепенно из них сложился
                своего рода буддийский круг такой пестроты, которую мог явить
                только лагерь того времени. Они занимались буддийской философией
                и йогой, но главное — осмыслением своей собственной жизни и
                своего положения в смысле буддийской философии и йоги. В течение
                нескольких лет Дандарон руководил этими занятиями. Это явилось
                пробой собственных сил, произведенной людьми над самими собой, в
                которой можно видеть истинность практического буддизма...»,—
                пишет востоковед А. М. Пятигорский, которому Дандарон
                рассказывал о своих лагерных годах.</p>
            <p>В числе лагерных учеников Дандарона был поляк Кокошка. Когда в
                конце войны его препроводили на Запад, он, сделав себе ложный
                перелом руки, под гипсом вынес из зоны листы рукописи учителя
                «Необуддизм».</p>
            <p>Во время одного из просветов между отсидками вместе с ламой Падма
                Доржи Дандарон пишет письмо Сталину с ходатайством о возрождении
                буддийских монастырей в Бурятии. И вскоре, в 1946 году два
                монастыря (дацана) действительно были открыты — в Aгe и около
                Улан-Удэ. «Я возродил дацановскую форму буддизма на два
                поколения»,— так потом говорил он об этом событии.</p>
