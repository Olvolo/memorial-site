            <p>
                На публикацию в нашем журнале статьи В. М. Монтлевича «Буддизм
                на севере России» (№2, 1990) откликнулось не менее ста
                читателей. В числе других нам написал председатель Совета
                Ленинградского общества буддистов Ц. Ч. Чимитов. По его мнению,
                Монтлевич не имел права писать об истории Ленинградского
                буддийского храма, поскольку он не входит в Совет общества.
                Действительно, Монтлевич связан не с буддийской школой гелугпа,
                которая преобладает сейчас в СССР и которую представляет
                Чимитов, а с другой, более древней традицией — тантризмом.
            </p>
            <p>
                В 20-30-е годы именно это направление во многом определяло жизнь
                буддизма. В 30-е годы все ламы всех направлений буддизма были
                репрессированы. Против тантрического буддизма выдвигались
                наиболее чудовищные обвинения, основанные на превратном
                толковании эротических образов искусства тантры (подобно тому,
                как в Римской империи христианское таинство причащения Крови и
                Плоти Христовой навлекало на христиан обвинения в ритуальном
                умерщвлении людей).
            </p>
            <p>
                Продолжая разговор, начатый год назад, мы обращаемся к имени
                ламы Б. Д. Дандарона, который исповедовал тантрический буддизм.
                Дандарон был духовным преемником Лубсан Сандан Цыденова, который
                действительно возглавил созданное в Бурятии в 1919 году
                «теократическое государство», что, видимо, и стало причиной его
                гибели в советских лагерях. Лишь в 1989 году в печати появились
                материалы, которые доказывают несостоятельность обвинений
                «теократов» в связях с атаманом Г. М. Семеновым. Напротив,
                теократическое государство сорвало призыв бурят-буддистов в
                белогвардейскую армию, так как убийство противоречит главной
                заповеди буддизма. Это вызвало репрессии Семенова против
                «теократов» (пытки, заключение в тюрьму), о чем многие потом
                постарались забыть. Но в Кижинге, на родине Сандан Цыденова,
                память о нем живет и сейчас — память об учителе веры и жизни.
            </p>
            <p>
                Лама Б. Д. Дандарон в 1937 году был приговорен к смертной казни
