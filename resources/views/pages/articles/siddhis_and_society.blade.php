        <h1 class="text-2xl font-bold text-center">СИДДХИ И ОБЩЕСТВО</h1>
        <p>Тантра полна презрения к концептуальной мысли. В её канонических
            текстах мало почтения к логике, зато часто присутствует парадокс. В
            песнях сиддхов именно через парадокс разрешается тупик двойственной
            мысли или тягота нравственного сомнения: “Если все вещи — это
            абсолютная реальность, шуньята, фантом пространственно-временного
            континуума, чем же тогда для Просветленного является материальная
            форма?” Ответ: <span class="quote">"Шуньята — это форма, и форма
                — это шуньята".
                <a href="#citation-1" id="ref-1" class="citation-mark"><sup>1</sup></a>
            </span>
        </p>

        <p>При анализе любого утверждения, как и при любом анализе, приходится
            исходить из принципа относительного существования, то есть с позиций
            двойственности, с позиций пусть и условного, но существования
            индивидуального Я. Единое при этом буддисты рассматривают как
            нераздельность двух: пустотности — шуньяты и проявленности —
            неисчислимой игры Ясного Света, где шуньята рассматривается как
            <i>вместилище — мир сосуда</i>, а Ясный Свет — как <i>содержимое —
                мир сока, совокупность всех живых существ</i>. Тогда понятно,
            почему шуньята рассматривается как форма, которая вовсе не обязана
            исчезнуть при
            умозрении внешнего мира, как игры пространства-времени. </p>
        <p><span class="quote">«Сказанное не противоречит постоянно подчеркиваемой предпосылке
            Махаяны о том, что нет запредельного сущего или не-сущего вне
            чувственного мира обычного сознания. Утверждение о варианте
            восприятия всего, как игры пространства-времени, вовсе не
            подразумевает упразднение работы самого органа зрения. Скорее это
            предполагает, что сугубо понятийное, умственное пространство-время
            воспринимающего совпадает с внутренним пространством-временем
            воспринимаемого. При этом все явленное уподобляется прозрачной
           оболочке яйца, а еще лучше — голограмме, в которой Я и
                <i>другое</i> оказываются полностью объединенными».<a
                    href="#citation-2"
                    class="citation-mark" id="ref-2"><sup>2</sup></a></span>
        </p>
        <p>Такой подход ведет к великому таинству махамудры, таинству
            <i>двух-в-одном</i> (yoganaddha), при котором и двойственность, и
            недвойственность втягиваются в нарастающий процесс так называемого
            парного вхождения в единство пустотности и блаженства — шуньяты и
            сострадания (<i>тиб</i>.: bde stong dbyer med kyi zung 'jug),
            достигая
            непресекающейся вершины переживания. Феномен такого переживания лишь
            в некоторой степени символически передают так называемые парные
            образы тантрийских танок. </p>
        <p>Самый внешний, социальный, уровень приложимости принципа махамудры,
            как достижения союза между Я и <i>другим</i>, способствует
            устранению
            барьера меж собою и другими членами общества. Социальность может
            внезапно раскрыться как сопереживание сиддха всем чувствующим
            существам. Но, как правило, до этого <i>внезапно</i> их мало заботят
            проблемы общества, тем более, если конечная стадия осуществления —
            дзогчен (в данном случае термин <i>дзогчен</i> не вполне совпадает с
            обозначением известного специального учения) является сахаджаяной —
            Колесницей вместерожденности. </p>
        <p>Здесь йогин не уничтожает неведение, не подавляет скверны (<i>тиб</i>.:
            nуоn
            mongs, <i>клеша</i>), не отказывается ни от чего сансарного, но,
            используя
            их, опираясь на них, реализует их имманентную сущность как
            трансцендентную дхармакаю, то есть, используя особые методы,
            превращает имманентные несовершенства в трансцендентные ценности:
            <span class="quote">«Никто не может достичь совершенства посредством процессов трудных и
            болезненных, но может легко преуспеть посредством удовлетворения
                всех желаний”</span> — говорит Такошествующий в
            “Гухьясамаджатантре”»<a href="#citation-3"
                                    class="citation-mark" id="ref-3"><sup>3</sup></a>
            . </p>
        <p>Такая практика действительно сопровождается весьма активным
            включением йогина в жизнь, сопровождается порой весьма интенсивным
            общением, а порой и эксцессами и уж во всяком случае не предполагает
            изоляционизма или бегства в тихую обитель. Бхагаван сказал: <span
                class="quote">«Еда и
            питье должны быть только тогда, когда приходят. Не следует избегать
            вещей, интересуясь, подходят они или не подходят. Не должно
            исполнять специальных омовений или очищений, также не должно
            избегать городских дел. Мудрец не произносит мантр и не посвящает
            себя медитации; он не избегает сна и не удерживает своих чувств. Ему
            следует есть всякое мясо и общаться с людьми всех каст. Он
            поддерживает компанию всех женщин. Ум его совершенно свободен от
            беспокойства. Он не должен иметь любви к друзьям и не должен
            ненавидеть любых недругов. Он не должен приветствовать тех богов,
            что сделаны из дерева, камня и глины. Ведь сам йогин всегда един с
                формой его собственного божества»</span><a href="#citation-4"
                                                           class="citation-mark" id="ref-4"><sup>4</sup></a>.
        </p>
        <p>В этой же тантре есть и более веские примеры весьма специфической
            “вписанности” сиддхов в современное им общество: <span
                class="quote">«Перед собравшимся
            народом или придя в дом грешника или преступника, в выпившем
            состоянии кричать, петь песни и буянить, наконец, затеять драку и
            отдубасить хозяина. После совершения подобного безмерно увеличится
            собрание добродетелей, а сам поступок станет необходимым спутником
            самадхи, подобно тому, как огонь в лесу разгорается в великий
                пожар».</span></p>
        <p>Такое поведение, согласное с наставлениями соответствующих тантр,
            только формально может показаться социально задействованным. Йогин в
            метрике собственного сознания в период этой экстремальной
            завершающей стадии практики действует исключительно субъективистски,
            проецируя внутреннее видение на внешнюю ситуацию, испытывая личное
            мужество, проверяя на прочность собственную психику. Только для
            самых близких сподвижников, посвященных в происходящее или
            догадывающихся об оном, контакт с сиддхом не приносит смущения и
            нравственных потрясений. Для всех остальных, <i>усмиряемых</i>,
            общение с
            ним вряд ли может доставить удовлетворение. Все могут восторгаться
            талантами сиддха, будь то стихосложение, пение, танцы, остроумие,
            физическая сила, воинское мужество или проявление собственно <i>сиддх</i>
            —
            так называемых сверхсил, но, тем не менее, предпочитают держать
            дистанцию. Вокруг сиддха образуется некое поле, войти в которое
            могут лишь очень близкие к нему люди — ученики, родственники,
            любимые женщины, все остальные наблюдают за происходящим как бы на
            расстоянии. </p>
        <p>Вне зависимости от личных качеств сиддха и типа практики его влияние
            на общество всегда благотворно или, по-буддийски, полно сострадания
            ко всем живым существам. Сиддха воистину исполнен сострадания, что
            означает буквально «страдание вместе», чем естественно и как бы не
            специально исполняет обет бодхисаттвы — «Да спасу я всех живых!». На
            конечной стадии практики сиддха спонтанно реализует начальные
            установки ума, так называемые <i>Четыре безмерных</i>: любовь,
            радость
            события, милосердие, равное отношение. Несмотря на порой
            экстравагантное поведение и шокирующие поступки, жизнеописания
            великих йогинов Индии полны примерами замечательных проявлений
            общечеловеческих чувств — от страстной любви к женщине до
            привязанности к беспомощному животному. Завершающий этап практики
            сиддхов — это махамудра, реализация единства метода (<i>тиб</i>.:
            thabs,
            upaya) и интуиции, познающей шуньяту (<i>тиб</i>.:shes rab, prajna),
            единства мужского и женского, наставления учителя и творческого
            порыва ученика, единства безличного знания и индивидуального
            осознания, единства йогина и дакини, блаженства и пустотности. Здесь
            метод практики синонимичен состраданию во всех его аспектах, но
            более в смысле спонтанного и безличностного реагирования любого
            сознания на спроецированную им самим вкупе со всеми остальными
            условно внешнюю реальность. Это довольно простая для понимания вещь:
            любое наше реагирование есть аспект бодхичиттовости, хотим мы этого
            или нет. Непрерывный процесс <i>разделения</i> дхармакаи на
            множественность
            сознаний, забывающих о своем истинном единстве, а потому и
            мучающихся в разделённости, есть спонтанно и никем не установленный
            процесс бодхичитты — в данном аспекте как процесс вселенского
            сострадания. Его не придумал человек, не установил Бог, это просто
            происходит само по себе, это просто <i>так</i> , в этом — знаменитая
            <i>таковость</i> буддизма, именно потому татхагата — <i>Такошествующий</i>,
            или
            <i>Так пришедший</i>. Поведение сиддхов как реализовавших практиков,
            не
            укладывающееся порой в общепринятые правила современного для них
            общества, как раз и свидетельствуют об их <i>тáковом</i> состоянии
            сознания, они ничего не исправляют и как бы ни во что не
            вмешиваются, но порой — наоборот, как говорится, лезут во все самые
            рискованные ситуации, доставляя немало хлопот современникам. <i>Тáковое</i>
            поведение сиддхов — лишь аспект общего потока сострадания,
            исходящего от дхармакаи. Минимум субъективности он имеет в Адибудде
            (Самантабхадре, Манджушри, Вайрочане, Ваджрадхаре), мгновенно
            постигшем <i>свое собственное лицо</i>. В конкретном учителе эта
            своеобразная рука помощи достигает каждого ищущего. Учитель должен
            восприниматься учеником как нирманакая, то есть как существо, не
            имеющее скандх, а значит, и кармы. Подобное видение не приобретается
            через анализ, оно трансцендентно и приходит как бы со стороны, от
            учителя, через посвящение и дарование метода, почитаемого как
            <i>абсолютное</i>. Понять подобное можно лишь через уже наличное,
            центростремительное и имманентное каждой личности — через интуицию,
            познающую шуньяту. Через йогический опыт можно достичь
            недвойственности трансцен-дентного сострадания и имманентной
            интуиции, при этом и обретается истинное благословение дакини —
            Мудрость недвойственности блаженства и шуньяты. </p>
        <p>Такое состояние не есть нечто новое, не есть некое изобретение, оно
            соприсущно любому существу, обладающему сознанием, до поры в силу
            неведения даже не догадывающемуся об этом. Но сиддха в процессе
            практики начинает осознавать это состояние, осознает его вполне и,
            наконец, становится им самим до такой степени, что поистине не имеет
            индивидуального Я, внутри себя как бы перестает быть личностью,
            ничего не знает в любой данный момент, но вдруг знает всё при
            внешнем вопрошании, как бы до востребования, и воспринимается
            окружающими как <i>достигший состояния Просветленности</i>, как
            Будда. </p>
        <p>В тексте Абхаядатты «Жития 84 сиддхов», в его тибетской версии
            (перевод Мондуба Шейраба, XII век), подобное событие фиксируется
            словами “mkha' spyod du gshegs so” , что переводится как «ушел в
            практику неба» (Дхелипа), «ушел в Ясный Свет», «взошел в завершение
            в небо даков» (Лилапа, Сараха и др.). В. Васильев использует
            выражение «ушел - исчез в радужном теле»<a href="#citation-5"
                                                       class="citation-mark" id="ref-5"><sup>5</sup></a>.
            Иногда история сиддхов
            заканчивается несколько иным выражением: “nam mkha' longs spyod du
            gshegs” — «ушел в наслаждение неба». По-русски все это можно было бы
            определить расхожим «отправился к небожителям», если бы подобному
            “путешествию” не предшествовали слова: «в этом же теле». Сиддхи не
            просто умирали, они проделывали это порой очень эффектно, при
            большом стечении народа и часто совместно со своей женой-йогиней.
            Такой уход в Тантре именуется обретением сиддхи махамудры, а в
            дзогчене — обретением Тела Света, когда все махабхути в результате
            практики растворяются до своей наитончайшей сущности — мудрости
            татхагат. Такое состояние соответствует четвёртой стадии реализации
            Ясного Света (don gyi 'od gsal mkha' spyod). </p>

