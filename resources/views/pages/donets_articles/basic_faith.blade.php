            <h4 class="text-center text-gray-800 font-bold text-xl my-4">РЕЗЮМЕ</h4>
            <p>Анализ тибетоязычных источников и религиозного опыта буддистов Центральной Азии
                приводит к заключению о наличии реализации в данной традиции буддизма специфического
                феномена базовой веры, который имеет сложную структуру, содержит когнитивные,
                эмоциональные и волевые моменты, интегрирует различные виды веры и характеризуется
                тотальностью, определяя магистральное русло течения духовной жизни человека.</p>
            <h4 class="text-center text-gray-800 font-bold text-xl my-4">SUMMARY</h4>
            <p>The analysis of the Tibetan texts and the religious experience of the Buddhists in
                Central Asia leads to the conclusion that in this tradition there is a realization
                of the specific phenomenon of the basic faith which has complex structure. It
                contains cognitive, emotional and volition aspects, integrates different types of
                the faith and is characterized by the totality defining the main course of the
                person’s spirit life.</p>
