                        т. д. приводят к тому, что перевод является в известной мере комментарием, а
                        близкое следование тексту вызывает некоторую корявость. При комментировании
                        смысла и сглаживании корявости формы мы становимся в известной мере его
                        соавторами. Излишняя активность в этом направлении может привести даже к
                        изменению качественно-энергетических характеристик текста при переводе.
                        Поэтому нужно учиться соблюдать меру в проявлении своего творчества.</p>
