                    погруженности: мы как бы погружаемся в некое море неопределенности, где неясными
                    тенями блуждают смыслы, и пытаемся светом своего ума осветить их и сделать ясно
                    видимыми. Это состояние неизбежно и необходимо, но длительное пребывание в нём
