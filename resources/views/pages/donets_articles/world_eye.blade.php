                    времени всплывать на поверхность, чтобы сориентироваться. Поскольку это
                    состояние вызывается установкой на перевод (как перевести?), то поплавком,
                    позволяющим нам всплыть, является мысль: «Интересно, а о чем здесь идёт речь?»
