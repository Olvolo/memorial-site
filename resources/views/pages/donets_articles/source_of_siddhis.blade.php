            </ol>

            <h4 class="text-base font-semibold text-blue-900 mt-4 mb-2">2.3.1.1. Описание коренных
                падений в системах Ануттара-Йога-Тантры, за исключением Калачакры</h4>
            <p>Состоит из двух частей:</p>
            <ol class="list-decimal ml-6">
                <li>Описание общих членов коренных падений.</li>
                <li>Описание необщих членов коренных падений.</li>
            </ol>

            <h4 class="text-base font-semibold text-blue-900 mt-4 mb-2">2.3.1.1.1. Описание общих
                членов коренных падений</h4>
            <p>Для того чтобы при наличии деяний, подпадающих под рубрику коренного падения, в итоге
                совершенно родилось коренное падение, необходимо наличие в полноте членов большого
                «полного попадания в ловушку» для тринадцати коренных падений, кроме пятого
                коренного падения. Для пятого коренного падения, состоящего в отражении мысли,
                желающей достичь Просветления ради блага всех существ, нет нужды в наличии членов
                «полного попадания в ловушку». Из-за простого отвержения мысли, желающей
                Просветления для блага всех существ, бросаются сразу оба вида обетов — обеты
                Мантраяны и обеты Бодхисаттвы. При этом обеты Мантраяны и обеты Бодхисаттвы являются
                обетами Тантры Бодхисаттв. Простое отрицание бодхисаттвовской мысли, желающей
                Просветления для блага всех существ, не будет нарушением только бодхисаттвовских
                обетов.</p>
            <p>Имеется четыре элемента великого «полного попадания в ловушку», являющихся составными
                членами тринадцати коренных падений:</p>
            <ol class="list-decimal ml-6">
                <li>Совершить проступок, относящийся к разряду какого бы то ни было коренного
                    падения, и снова рождать желание опять совершать его, не отвращать желание
                    совершить проступок снова — один элемент.
                </li>
                <li>Смотреть на содеянный проступок как на достоинство, не рассматривать как зло —
                    один элемент.
                </li>
                <li>Испытывать радость и удовольствие в отношении содеянного проступка, радоваться
                    осуществлению того проступка — один элемент.
                </li>
                <li>Отсутствие даже в малейшей степени двух: совести, когда, смотря на тот
                    проступок, думают: «Это не является тем, что должно много совершаться», и,
                    сделав это основанием со своей стороны, избегают; и стыда, когда думают: «Если
                    другие узнают, что я совершил этот проступок, то будут порицать. Поэтому не
                    годится мне делать его», — и, сделав это основанием со стороны других, избегают
                    совершать его. Отсутствие совести и стыда — один элемент.
                </li>
            </ol>
            <p>Эти четыре элемента, вызывающие состояние, подобное полному поражению, и являются
                великим «полным попаданием в ловушку».</p>
            <p>Поэтому, если после рождения состояния ума, осуществляющего какой-либо грех коренного
                падения, осуществляется собственно основа падения (т. е. совершается деяние падения)
                и сразу же затем имеются в полноте эти четыре элемента, то, поскольку имеются в
                полноте члены великого «полного попадания в ловушку», будет коренное падение.</p>
            <p>Если при видении наличия у себя проступка немного родился хотя бы один стыд или
                совесть, или немного возникло одно рассматривание того проступка как зла, или
                немного родился один ум, отвращающий желание совершать проступок, или немного
                имеется одно отворачивание от ума, радующегося тому проступку, то, поскольку члены
                великого «полного попадания в ловушку» не имеются в полноте, коренного падения не
                будет.</p>
            <p>Также, если из этих четырёх элементов великого «полного попадания в ловушку» нет
                второго, то, хотя бы и были первый, третий и четвёртый, будет малое «полное
                попадание в ловушку». А если имеется в наличии второй, то, хотя бы и не было трёх
                остальных, будет среднее «полное попадание в ловушку».</p>

            <h4 class="text-base font-semibold text-blue-900 mt-4 mb-2">2.3.1.1.2. Описание необщих
                членов коренных падений</h4>
            <p>Имеется четырнадцать подразделов — от описания членов первого коренного падения до
                описания членов четырнадцатого коренного падения.</p>

            <h5 class="text-base font-semibold text-blue-900 mt-4 mb-2">1. Пренебрежение к
                Ваджарному
                Учителю</h5>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Сказано: «Поскольку Держатель Ваджра</p>
                <p>Сказал, что сиддхи следуют за Учителем,</p>
                <p>Поскольку пренебрежительное отношение к Нему</p>
                <p>Указывается как первое коренное падение»<a href="#citation-1" id="ref-1"
                                                              class="citation-mark"><sup>1</sup></a>.
                </p>
            </blockquote>
            <p>Держателем Ваджра — Ваджрадхарой сказано, что реализация у ученика любых из двух
                видов сиддх следует за радованием Ваджарного Учителя.</p>
            <p>При наличии в полноте всех членов великого «полного попадания в ловушку»,
                одновременно с завершением сбрасывания умом того груза восприятия Наставника,
                сделавшего для меня любое из трёх: изложение — разъяснение наставлений-упадеша по
                любой из четырёх систем Тантры, дачу абхишеки, изложение-разъяснение Тантры, — в
                качестве Учителя, воспринимая Его как высшее поле для роста моих заслуг, достойного
                почитания тремя дверями, будет это коренное падение.</p>
            <p>Если имевшее место пренебрежение-презрение не подходит под мерку коренного падения,
                или хотя имевшее место пренебрежение-презрение и подходит под мерку коренного
                падения, но не в полном составе члены большого «полного попадания в ловушку», то
                будет падение с неполными членами. Сущность падения проявилась как содеянный
                проступок, а разряд проступка устанавливается как разряд поражения. Оценивая этим,
                признаём его падением с неполными членами, относящимся к низшим коренным
                падениям.</p>
            <p>Поскольку неправильное поведение по отношению к объясняющему нам Учение будет
                препятствовать нашему совершенствованию, то имеется эта заповедь-обетование<a
                    href="#citation-2" id="ref-2" class="citation-mark"><sup>2</sup></a>.</p>

            <h5 class="text-base font-semibold text-blue-900 mt-4 mb-2">2. Отход от проповеди
                Сугаты</h5>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Сказано: «Указывается, что отход от проповеди Сугаты</p>
                <p>Является вторым коренным падением».</p>
            </blockquote>
            <p>Это пренебрежительное отношение к проповеди Сугаты Будды, которая, как указывается,
                берётся здесь в значении каких бы то ни было заповедей-обетований, относящихся к
                трём видам обетов — Хинаяны, Махаяны и Ваджраяны.</p>
            <p>Когда, зная о тех, что имеются, каких бы то ни было заповеданных Буддой
                заповедях-обетованиях, относящихся к трём видам обетов, в отношении отхода от
                которых сам обещал, с относящимся пренебрежительно умом, думающим: «Ничего не будет
                из-за того, что отошёл именно от этого обета», взял и целиком отошёл от тех обетов и
                вошёл в осуществление тех падений, при наличии в полноте членов большого «полного
                попадания в ловушку», будет коренное падение.</p>
            <p>Так, например, в отношении заповеди-обетования, касающегося совершения падения,
                состоящего в соприкосновении с огнём: когда обладающий обетами Мантраяны монах, зная
                о том обетовании-заповеди, из пренебрежения, думая: «Хотя бы и отошёл именно от той
                заповеди, ничего не будет», завершил вхождение в осуществление падения, состоящего в
                соприкосновении с огнём, совершится это коренное падение.</p>
            <p>Поскольку грех, состоящий в том, что, пренебрежительно отнёсшись к проповеданному
                лично Буддой Учению, отходят от правил обучения, являющихся созерцанием практической
                части того Учения, будет препятствовать собственному совершенствованию, то в
                отношении этого коренного падения имеется заповедь-обетование.</p>

            <h5 class="text-base font-semibold text-blue-900 mt-4 mb-2">3. Говорение плохого о
                ваджарном товарище</h5>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Сказано: «Рассердившись на ваджарного товарища,</p>
                <p>Говорить плохое — является третьим».</p>
            </blockquote>
            <p>Если вследствие того, что какому бы то ни было обладающему обетами Мантраяны
                ваджарному товарищу, получившему у одного и того же Учителя какую бы то ни было
                абхишеку четырёх систем Тантры прежде, после или вместе с тобой, зная о том, говорил
                о его недостатках — проступках и т. д., и те порочащие слова были поняты оппонентом
                (т. е. тем товарищем), тогда, при наличии в полноте членов большого «полного
                попадания в ловушку», будет коренное падение.</p>
            <p>Однако, когда у него нет внутренней абхишеки в Йога-Тантре, он будет просто товарищем
                по Учению и не будет ваджарным товарищем, каковым становятся благодаря внутренней
                абхишеки в ту систему. Поэтому тогда не возникает собственно ситуация совершения
                деяния, соответствующего именно этому коренному падению.</p>
            <p>Поскольку грех говорения плохого, рассердившись на истинного товарища по реализации
                Учения, будет препятствовать совершенствованию, то имеется заповедь-обетование в
                отношении этого коренного падения.</p>

            <h5 class="text-base font-semibold text-blue-900 mt-4 mb-2">4. Отвержение любви к живым
                существам</h5>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Сказано: «Победителем сказано, что отвержение любви к живым существам</p>
                <p>Является четвёртым».</p>
            </blockquote>
            <p>Когда, думая о каких бы то ни было существах — вплоть до единого — породил мысль:
                «Если бы этот отделился от всех блаженств!» — тогда, при наличии в полноте членов
                большого «полного попадания в ловушку», будет коренное падение.</p>
            <p>Поскольку грех неправильного поведения в отношении существ, ради которых порождается
                мысль о Просветлении, будет препятствовать совершенствованию, то в отношении этого
                коренного падения имеется заповедь-обетование.</p>

            <h5 class="text-base font-semibold text-blue-900 mt-4 mb-2">5. Отвержение мысли о
                Просветлении</h5>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Сказано: «Отвержение той мысли о Просветлении —</p>
                <p>Корне Учения — является пятым».</p>
            </blockquote>
            <p>Поскольку это коренное падение появится из-за простого отвержения мысли, желающей:
                «Достигну положения Будды ради всех существ, имеющихся в своих родах»<a
                    href="#citation-3" id="ref-3" class="citation-mark"><sup>3</sup></a>, и рождения
                ума, думающего: «Я не способен реализовать положение Будды ради неисчислимых
                существ», то нет необходимости в наличии в полноте всех членов «полного попадания в
                ловушку» и т. д.</p>
            <p>Поскольку грех отвержения собственной сущности порождения мысли о Просветлении ради
                блага всех существ будет препятствовать совершенствованию, то в отношении этого
                коренного падения имеется заповедь-обетование.</p>

            <h5 class="text-base font-semibold text-blue-900 mt-4 mb-2">6. Порицание учения своей
                или
                чужой школы</h5>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Сказано: «Порицание учения своей или чужой философской школы-секты</p>
                <p>Является шестым».</p>
            </blockquote>
            <p>Когда, имея в виду относящуюся к Сутре или Мантраяне проповедь Будды, подумал: «Это —
                не является проповедью Будды», — и искренне произнёс слова отрицания, тогда, при
                наличии в полноте членов большого «полного попадания в ловушку», будет коренное
                падение.</p>
            <p>Поскольку грех отвержения-отрицания Учения, состоящий в клевете на проповедь Будды:
                «Не является проповедью Будды», будет препятствовать совершенствованию, то в
                отношении этого коренного падения имеется заповедь-обетование.</p>

            <h5 class="text-base font-semibold text-blue-900 mt-4 mb-2">7. Разглашение сокровенного
                несозревшему</h5>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Сказано: «Разглашать сокровенное</p>
                <p>Совершенно несозревшему существу — является седьмым».</p>
            </blockquote>
            <p>Если любому индивиду, которого не сделали созревшим для слушания сокровенного
                посредством абхишеки, что делает это возможным, или который, хотя и получил
                абхишеку, но, если слышит то сокровенное, порождает неверие, не имея большой нужды в
                изложении сокровенного ради обуздания-воспитания другого, излагал устно сокровенное
                содержание, относящееся к ступеням зарождения и завершения, и т. д., и это было
                понято оппонентом (т. е. тем, кому говорил), тогда, при наличии в полноте членов
                большого «полного попадания в ловушку», будет коренное падение.</p>
            <p>Поскольку грех объяснения Учения Проповеди Будды тому, кто не годится для получения
                объяснения, будет препятствовать совершенствованию, то в отношении этого коренного
                падения имеется заповедь-обетование.</p>

            <h5 class="text-base font-semibold text-blue-900 mt-4 mb-2">8. Пренебрежение к
                скандхам</h5>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Сказано: «Пренебрежительно относиться к той</p>
                <p>Сущности Пяти Будд — скандхам является восьмым».</p>
            </blockquote>
            <p>Желая, чтобы те пять собственных скандх, обладающих сущностью Пяти Родов Сугат (т. е.
                Дхьяни-Будд), пришли в упадок, пренебрежительно, презрительно относиться к ним,
                мучить аскезой и т. д., или, думая о скандхах как о нечистом, мучительном и т. д.,
                считать невозможным созерцать те скандхи как Будду, или порождать желание убить
                себя, — когда появилось любое из этого, при наличии в полноте членов большого
                «полного попадания в ловушку», будет коренное падение.</p>
            <p>Поскольку пренебрежительное отношение к скандхам — основе, которую следует созерцать
                в качестве бога, будет препятствовать совершенствованию, то в отношении этого
                коренного падения имеется заповедь-обетование.</p>

            <h5 class="text-base font-semibold text-blue-900 mt-4 mb-2">9. Сомнение в чистоте
                дхарм</h5>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Сказано: «Питать сомнение в отношении чистых по природе дхарм —</p>
                <p>Является девятым».</p>
            </blockquote>
            <p>Когда, питая сомнения в отношении чистой по природе дхарматы, отделённой от всякой
                активности-проявленности, и желая отвернуть вообще несуществование сущности дхарм,
                которое является смыслом праджня-парамиты, отверг несуществование сущности дхарм
                вообще и любую теорию несуществования сущности дхарм, относящуюся к области взглядов
                виджнянавадинов и мадхьямиков, тогда, при наличии в полноте членов большого «полного
                попадания в ловушку», будет коренное падение.</p>
            <p>Поскольку грех критики идеи праджня-парамиты и отрицание с самого начала шуньяты
                будет препятствовать совершенствованию, то в отношении этого коренного падения
                имеется заповедь-обетование.</p>

            <h5 class="text-base font-semibold text-blue-900 mt-4 mb-2">10. Проявление симпатии к
                злобному</h5>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Сказано: «То постоянное проявление симпатии</p>
                <p>К злобному — считается десятым».</p>
            </blockquote>
            <p>Если по отношению к заслуживающему сурового отношения злобному индивиду, совершающего
                поступки типа порицания Учителя, Трёх Драгоценностей и т. д., и уничтожения Религии
                Будды, зная о том, дружил с ним и проявлял симпатию телом или речью, то, при наличии
                в полноте членов большого «полного попадания в ловушку», будет это коренное
                падение.</p>
            <p>Поскольку дружба с имеющими ложные взгляды товарищем будет препятствовать
                совершенствованию, то в отношении этого коренного падения имеется
                заповедь-обетование.</p>

            <h5 class="text-base font-semibold text-blue-900 mt-4 mb-2">11. Признание лишённых
                названий дхарм за имеющие названия</h5>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Сказано: «Признавать лишённые названий и т. д. дхармы</p>
                <p>За те, которые имеют названия и т. д. — одиннадцатое».</p>
            </blockquote>
            <p>Если йог, обретя признающий несуществование сущности дхарм взгляд любой школы —
                виджнянавадинов, или мадхьямиков-сватантриков, или мадхьямиков-прасангиков,
                считающих свой взгляд окончательным, провёл сутки, не созерцая его или не сохраняя,
                то, при наличии в полноте членов большого «полного попадания в ловушку», будет это
                коренное падение.</p>
            <p>Поскольку грех, состоящий в том, что, хотя и постиг шуньяту, но бросаешь практику её
                в уме, будет препятствовать совершенствованию, то в отношении этого коренного
                падения имеется заповедь-обетование.</p>

            <h5 class="text-base font-semibold text-blue-900 mt-4 mb-2">12. Вызывание потери веры у
                существа</h5>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Сказано: «Вызывание потери веры</p>
                <p>У существа — двенадцатое».</p>
            </blockquote>
            <p>Когда, вследствие того, что, желая, чтобы существо, обладающее счастливой судьбой
                быть сосудом, подходящим для практики буддийского пути, потеряло верующее почитание,
                проявляемое им по отношению к какому бы то ни было наставлению Высшей Колесницы,
                изыскал и применил некий способ вызывания потери такого почтения, и у того существа
                пропало почтение, тогда, при наличии в полноте членов большого «полного попадания в
                ловушку», будет коренное падение.</p>
            <p>Поскольку грех вызывания потери веры у существа, являющегося подходящим сосудом,
                будет препятствовать совершенствованию, то в отношении этого коренного падения
                имеется заповедь-обетование.</p>

            <h5 class="text-base font-semibold text-blue-900 mt-4 mb-2">13. Неполагание на связанные
                с
                обетом предметы</h5>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Сказано: «Не полагаться на какие бы то ни было получаемые</p>
                <p>Связанные с обетом предметы — тринадцатое».</p>
            </blockquote>
            <p>Если мясо, вино, чеснок, лук и другие связанные с обетом предметы, раздаваемые во
                время регулярного совершения ритуала согхор-круга собрания, или праздника Героев,
                или праздника Героинь, посчитал нечистым или, питая сомнения, не принял, или во
                время согхор-круга собрания женщина-видья пожелала и предложила: «Давай займёмся
                сексуальной практикой», а получивший способность полагаться на видью (т. е.
                практиковать с женщиной) йог, не имея других препятствий-запретов, не согласился,
                или, пренебрежительно относясь к ваджре, колокольчику, мудре-жесту и т. д., не
                полагается на них и отвергает, — если совершается любое из этих деяний, то, при
                наличии в полноте членов большого «полного попадания в ловушку», будет коренное
                падение Ануттара-Йога-Тантры.</p>
            <p>А если пренебрежительно относишься к ваджре, колокольчику, мудре-жесту рук и т. д. и,
                думая: «Можно обойтись одним созерцанием. А от этих что толку?», не полагаешься и
                отвергаешь, то будет коренное падение Йога-Тантры при наличии в полноте членов
                большого «полного попадания в ловушку».</p>
            <p>Поскольку грех, состоящий в том, что не опираешься на обеты — основу пути, будет
                препятствовать, то в отношении этого коренного падения имеется
                заповедь-обетование.</p>

            <h5 class="text-base font-semibold text-blue-900 mt-4 mb-2">14. Порицание женщины,
                имеющей
                природу праджни</h5>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Сказано: «Порицать женщину, имеющую природу праджни —</p>
                <p>Четырнадцатое».</p>
            </blockquote>
            <p>Если по отношению к какой бы то ни было — вплоть до единой — женщине, имеющей природу
                праджни и являющейся причиной появления мудрости великого блаженства — Ваджраварахи
                и других божеств, имеющих женский облик, или обычной женщине, с намерением опорочить
                совершил опорочивание, выражающееся в словах порицания женского тела-опоры вообще,
                причём, если той порицаемой женщиной является Ваджраварахи и т. д., то при простом
                порицании, а если ею является обычная женщина, то при понимании смысла порицающих
                слов оппонентом (т. е. той женщиной), будет, при наличии в полноте членов большого
                «полного попадания в ловушку», это коренное падение.</p>
            <p>Поскольку грех порицания женщины, являющейся товарищем-помощником при практике пути,
                будет препятствовать, то в отношении этого коренного падения имеется
                заповедь-обетование.</p>
            <p>Однако, поскольку в настоящее время из этих падений имеют тенденцию чаще возникать
                третье, шестое, тринадцатое и четырнадцатое, то им следует уделять больше
                внимания.</p>

            <h4 class="text-base font-semibold text-blue-900 mt-4 mb-2">2.3.1.2. Коренные падения в
                системе Калачакры</h4>
            <p>Их тоже четырнадцать:</p>
            <ol class="list-decimal ml-6">
                <li>Когда, зная, что если сам входишь в осуществление неблагого деяния, то Учитель
                    не обрадуется, не для блага другого существа вошёл в осуществление какого бы то
                    ни было из десяти деяний в присутствии Учителя и по этой причине великий высший
                    Учитель обеспокоился-опечалился, тогда будет коренное падение.
                </li>
                <li>Когда тот мой высший Учитель прикажет: «Не делай дурного», а я, не для блага
                    других, а для собственного блага нарушил приказ Учителя и, нисколько не подумав
                    о приказе, совершил в тайне от Учителя проступок, тогда будет коренное падение.
                </li>
                <li>Если по отношению к обладающему обетом Мантраяны ваджарному товарищу, у которого
                    тот же Учитель, что и у меня, зная о том, не для блага других, а из чувства
                    гнева говорил о недостатках того товарища и смысл того был понят оппонентом,
                    тогда будет коренное падение.
                </li>
                <li>Если с момента, когда отверг и оставил любовь, желающую всем существам до
                    единого встретиться с блаженством, и до момента, когда снова родилась любовь,
                    прошли одни сутки, тогда будет коренное падение.
                </li>
                <li>Если, зная, что собственная сперма — та семенная жидкость имеет природу
                    неизменного блаженства, используется для практики тайной абхишеки, блага других,
                    изготовления пилюль и т. д., истощаешь её каким бы то ни было способом,
                    используя её только для достижения приходящего блаженства, то, когда испустил
                    семенную жидкость, тогда будет коренное падение.
                </li>
                <li>Когда, приняв истину Мантраяны за высшую, а Парамитаяну — за низшую, не для
                    блага других, с чувством антипатии к Парамитаяне произнёс слова, порицающие её,
                    тогда будет коренное падение.
                </li>
                <li>Если тому, кто не сделался созревшим для практики благодаря абхишеке, или, хотя
                    и сделался созревшим, но не верит при открытии ему сокровенного, зная о том, не
                    для блага других, открыл тайное, объясняя, не утаивая тайное место, и смысл того
                    был понят оппонентом, тогда будет коренное падение.
                </li>
                <li>Когда, зная свои скандхи как Победителя (Будду), Богиню, Саттву и т. д., не для
                    блага других, не для обуздания других, с намерением уменьшить блаженство скандх
                    посредством их подавления-угнетения, причиняешь телу страдание посредством жары
                    и холода, лишения еды и питья, отсечения членов тела и т. д., и рождаешь мысль
                    убить себя, тогда, при осуществлении любого из этого, будет коренное падение.
                </li>
                <li>Если с чувством неверия в Учение шуньяты-пустоты, не ради обуздания-воспитания
                    других, раскритиковав и отвергнув идею пустоты-шуньяты, принял идею реальности
                    феноменальной явленности, тогда будет коренное падение.
                </li>
                <li>Когда, не храня с верой обеты-дамсиг и т. д., делая вид, что ревностно хранишь
                    их, в тайне совершаешь неблагое деяние, то во время совершения того обмана будет
                    коренное падение.
                </li>
                <li>Из-за того, что, когда дают неизменное блаженство, не для обуздания-воспитания
                    других, питая сомнения в отношении неизменного блаженства, не принял его, будет
                    коренное падение.
                </li>
                <li>Когда, не для обуздания-воспитания других, а из чувства зависти к тому йогу,
                    который обладает качествами, присущими тем, кто практикует йогу, говорил о его
                    недостатках и благодаря тому опровергнул мысль оппонента (смутил его ум), тогда
                    будет коренное падение.
                </li>
                <li>Из-за простого непринятия во время совершения ритуала «согхор» — круга собрания
                    и других ритуалов, выполнение которых обусловлено обетом, связанных с
                    выполнением обетов еды, питья и совокупления с женщиной, неприятия, совершаемого
                    не для защиты других и для обуздания-воспитания других, будет коренное падение.
                </li>
                <li>Из-за того, что по отношению к женщинам вплоть до единой, не ради
                    обуздания-воспитания других, говорил о недостатках, что выразилось в словах
                    порицания-пренебрежения, и смысл тех слов был понят той женщиной-оппонентом,
                    будет коренное падение.
                </li>
            </ol>
            <p>Таким образом, семь из четырнадцати коренных падений в системе Калачакры подобны
                коренным падениям в других системах Ануттара-Йога-Тантры, а семь — не похожи.</p>
            <p>Семь непохожих:</p>
            <ol class="list-decimal ml-6">
                <li>Первое общее для Мантраяны коренное падение установлено в отношении
                    пренебрежения-порицания Ваджарного Учителя, а первое коренное падение в системе
                    Калачакры устанавливается в отношении беспокойства высшего Учителя.
                </li>
                <li>Второе общее коренное падение установлено в отношении отхода какого бы то ни
                    было обетования трёх обетов из-за пренебрежительного отношения к проповеди
                    Наставника, а второе коренное падение в системе устанавливается в отношении
                    нарушения указания, данного тебе для чего-то Учителем.
                </li>
                <li>Пятое общее установлено в отношении отрицания-оставления мысли, желающей достичь
                    Просветления ради спасения всех существ, а пятое в системе Калачакры
                    устанавливается в отношении отрицания правильного использования для
                    совершенствования семенной жидкости-спермы.
                </li>
                <li>Шестое общее установлено в отношении отвержения Учения при отрицательном
                    высказывании о проповеди: «Не является проповедью Будды», а шестое в системе
                    Калачакры устанавливается в отношении простого пренебрежения-порицания при
                    принятии истины Мантраяны за высшую и истины Парамитаяны — за низшую, а
                    отвержение Учения не является необходимым элементом.
                </li>
                <li>Десятое общее установлено в отношении проявления любви-симпатии к злобному
                    человеку, а десятое в системе Калачакры устанавливается в отношении совершения
                    обмана.
                </li>
                <li>Одиннадцатое общее установлено в отношении временного отхода от тренировки во
                    взгляде, постигающем пустоту-шуньяту, а одиннадцатое в системе Калачакры
                    устанавливается в отношении непринятия неизменного блаженства.
                </li>
                <li>Двенадцатое общее установлено в отношении уничтожения религиозного
                    порыва-стремления у верующего, а двенадцатое в системе Калачакры устанавливается
                    в отношении говорения о недостатках йога, обладающего чертами, свойственными
                    практикующему йогу.
                </li>
            </ol>
            <p>Для возникновения коренного падения в системе Калачакры — для всех коренных падений,
                кроме пятого, относящегося к извержению спермы, — тоже необходимо наличие в полноте
                членов «полного попадания в ловушку».</p>
            <p>Для не получившего абхишеку Калачакры, но получившего другую абхишеку
                Ануттара-Йога-Тантры обладателя обетов Мантраяны указанные в системе Калачакры
                коренные падения не будут коренными падениями, и указанные в системе Калачакры
                коренные падения, несходные с общими коренными падениями Мантраяны, являются
                необщими-особыми коренными падениями только для системы Калачакры.</p>
            <p>Для обладателя обетов Мантраяны, получившего абхишеку только Калачакры, и коренные
                падения в системе Калачакры будут коренными падениями, и общие коренные падения
                Мантраяны тоже будут коренными падениями. Общие коренные и грубые падения Мантраяны
                будут общими коренными и грубыми падениями в системе Калачакры и
                Ануттара-Йога-Тантры вообще.</p>
            <p>Однако, как же будет для обладателя обета Мантраяны, получившего абхишеки по двум
                системам — Калачакры и, например, Гухьясамаджи? Если тот индивид сначала получил
                обеты Мантраяны во время совершения ритуала абхишеки, например, Гухьясамаджи, то
                необщие — особые коренные падения по системе Калачакры не будут для него коренными
                падениями. Если сначала получил обеты Мантраяны во время совершения ритуала абхишеки
                Калачакры, то коренные падения, указанные в системе Калачакры, будут для него
                коренными падениями.</p>
            <p>Тем не менее, говорят, этот вопрос ещё нужно исследовать.</p>

            <h4 class="text-base font-semibold text-blue-900 mt-4 mb-2">2.3.2. Описание грубых
                падений</h4>
            <p>Состоит из трёх частей:</p>
            <ol class="list-decimal ml-6">
                <li>Описание грубых падений, общих для Ануттара-Йога-Тантры.</li>
                <li>Описание обетов по системе Калачакры.</li>
                <li>Описание дополнительных обетов — в отношении еды и т. д.</li>
            </ol>

            <h4 class="text-base font-semibold text-blue-900 mt-4 mb-2">2.3.2.1. Общие для
                Ануттара-Йога-Тантры грубые падения</h4>
            <p>Вообще-то грубых падений имеется бессчётно. Но если излагать согласно указанному
                Учителем Нагарджуной, подразделяя грубые падения на восемь разрядов, то
                получаем:</p>
            <ol class="list-decimal ml-6">
                <li><strong>Брать силой праджню<a href="#citation-4" id="ref-4"
                                                  class="citation-mark"><sup>4</sup></a>.</strong>
                    Если видью (т. е. женщину), совершенно не
                    очистившуюся — не натренированную посредством должного обучения и т. д. Мантре и
                    Тантре после того, как она сделалась созревшей благодаря абхишеке, установилась
                    в обетах дамсиг и домпа, своей силой берёшь как видью и вступаешь в интимные
                    отношения, будет грубое падение.
                </li>
                <li><strong>Брать силой её нектар.</strong> Хотя бы видья и обладала признаками (т.
                    е. получила абхишеку и т. д.), но если берёшь силой нектар видьи-бодхичитту (т.
                    е. женский половой элемент), не выполняя это в соответствии с изложенным в
                    Тантре методом практики интимных отношений с видьей, будет грубое падение.
                </li>
                <li><strong>Не утаивать от того, кто не является сосудом.</strong> Если будешь
                    показывать, не скрывая, изображения божеств, книги, шесть украшений, атрибуты
                    (ваджр, колокол и т. д.) и прочее, показывать выполнение мудр-жестов и т. д.
                    тому, кто не является сосудом, так как не получил абхишеку или, хотя получил
                    абхишеку, но не верит, будет грубое падение.
                </li>
                <li><strong>Спорить на собрании.</strong> Во время «согхора» — круга собрания или
                    праздника Героев, Героинь, не пребывая в практике гордости (сознавания, что ты
                    бог), любви к существам и накопления собрания заслуг хозяина даров (т. е.
                    подносящего жертвы), устраивание взаимного спора на словах, а телом — даже
                    малейший хлопок в ладони<a href="#citation-5" id="ref-5"
                                               class="citation-mark"><sup>5</sup></a>, будет грубым
                    падением.
                </li>
                <li><strong>Объяснять верующему другое Учение.</strong> Если объясняешь не то
                    учение, о котором спросил верующий человек, — например, спрашивают об абсолютном
                    учении, а объясняешь относительное учение, — будет грубое падение.
                </li>
                <li><strong>Находиться семь суток среди шраваков-хинаянистов.</strong> Поскольку
                    говорится о позволительности пребывания шесть суток среди последователей
                    Колесницы Шраваков, практикующих Сокровенную Мантраяну, без особой
                    необходимости, состоящей в спасении от препятствий своей жизни и т. д.,
                    выполнении приказа царя и т. д., то, если пребываешь семь суток, будет грубое
                    падение.
                </li>
                <li><strong>Ложно гордиться йогой.</strong> Если пребываешь в гордости познания
                    истины и показываешь это другим, когда знаешь только метод реализации Божества,
                    и, не зная йоги, открыто гордишься йогой, то будет грубое падение.
                </li>
                <li><strong>Объяснить Учение неверующему<a href="#citation-6" id="ref-6"
                                                           class="citation-mark"><sup>6</sup></a>.</strong>
                    Если объяснять сокровенное Учение
                    Мантраяны тому, кто не созрел благодаря абхишеке или, хотя и созрел, но, не
                    веруя в Мантраяну, почитает внешнее Учение, будет грубое падение.
                </li>
            </ol>
            <p>Это падение не тождественно с коренным падением, состоящим в открытии сокровенного.
                Для возникновения коренного падения необходимо наличие индивида, рождающего неверие
                при слушании сокровенного Учения. Для этого же необходимо наличие индивида, не
                рождающего как веру, так и неверие при слушании сокровенного Учения.</p>
            <p>Также, кроме тех восьми грубых падений, есть ещё два, указанные Лакшинкарой:</p>
            <ul class="list-none italic pl-5 space-y-1 ml-8">
                <li class="my-2">"Не совершив реализацию-ньен, входить в деяние мандала.</li>
                <li class="my-2">Отходить без необходимости от заповедей двух обетов".</li>
            </ul>
            <p>
                Дача
                абхишеки другому, совершение ритуалов освящения, 100 балинов и т. д., не
                проделав как положено определённый цикл реализации-ньен с каким бы то ни было
                числом прочитанных мантр, временем пребывания в уединении и признаками его
                завершения, без выполнения жертвы сжиганием — жинсрека, йоги, бога и т. д.,
                считается грубым падением. Отход без
                необходимости от заповедей двух обетов Бодхисаттв и Пратимокши тоже считается
                грубым падением. Так, например, монах — последователь Держателя Ваджра (т. е.
                тантрист), хотя во время совершения жертвы сжиганием — жинсрека и касается огня,
                носит белую одежду, украшается гирляндой и т. д., но помнит, что это только на
                время, и не делает этого постоянно.
            </p>

            <h4 class="text-base font-semibold text-blue-900 mt-4 mb-2">2.3.2.2. Двадцать пять
                обетов, указанных в системе Калачакры</h4>
            <p>Состоит из пяти групп по пять: пять грехов, пять второстепенных грехов, пять убийств,
                пять ненавистей, пять желаний.</p>
            <ol class="list-decimal ml-6">
                <li><strong>Пять грехов:</strong>
                    <ol class="list-decimal ml-6">
                        <li>Пресекать жизнь.</li>
                        <li>Говорить ложь.</li>
                        <li>Брать неданное.</li>
                        <li>Прелюбодействовать.</li>
                        <li>Употреблять спиртное.</li>
                    </ol>
                </li>
                <li><strong>Пять второстепенных грехов:</strong>
                    <ol class="list-decimal ml-6">
                        <li>Играть в азартные игры (делать ставки, держать пари), кости и шахматы.
                        </li>
                        <li>Слушать истории, вызывающие сомнения в их достоверности.</li>
                        <li>Пустая болтовня — говорить, что взбредёт на ум.</li>
                        <li>Делать жертвы духам предков — для родителей.</li>
                        <li>Делать жертву крови, убивая скот.</li>
                    </ol>
                </li>
                <li><strong>Пять убийств:</strong>
                    <ol class="list-decimal ml-6">
                        <li>Убийство коровы.</li>
                        <li>Убийство ребёнка.</li>
                        <li>Убийство женщины.</li>
                        <li>Убийство мужчины.</li>
                        <li>Разрушение опоры Тела, Речи и Ума<a href="#citation-7" id="ref-7"
                                                                class="citation-mark"><sup>7</sup></a>.
                        </li>
                    </ol>
                </li>
                <li><strong>Пять ненавистей:<a href="#citation-8" id="ref-8"
                                               class="citation-mark"><sup>8</sup></a></strong>
                    <ol class="list-decimal ml-6">
                        <li>Не верить в Учение и Будду.</li>
                        <li>Гневаться на друга.</li>
                        <li>Гневаться на господина (правителя).</li>
                        <li>Гневаться на монаха.</li>
                        <li>Обманывать индивида, доверившегося тебе.</li>
                    </ol>
                </li>
                <li><strong>Пять желаний:</strong>
                    <ol class="list-decimal ml-6">
                        <li>Желать формы-цвета.</li>
                        <li>Желать звука.</li>
                        <li>Желать запаха.</li>
                        <li>Желать вкусового ощущения.</li>
                        <li>Желать осязательного ощущения.</li>
                    </ol>
                </li>
            </ol>

            <h4 class="text-base font-semibold text-blue-900 mt-4 mb-2">2.3.2.3. Дополнительное
                описание обетов в отношении еды и т. д.</h4>
            <p>Вообще, со времени получения абхишеки Ануттара-Йога-Тантры следует хранить обет в
                отношении еды, держать обетованные предметы и т. д.</p>
            <p>Итак, во время принятия пищи и напитков надо делать согласно изложенному в Тантре:
                бросив в еду и напиток нектарную пилюлю, сделанную и приготовленную из закваски и т.
                д. пяти нектаров высшего существа, и благословив как пять мяс и пять нектаров,
                жертвовать Богу, употреблять самому и т. д. Если не делаешь так, то, по крайней
                мере, нужно употреблять, очистив рот водой, в которую была брошена нектарная пилюля,
                и родив верой — представлением ту еду и напиток как пять мяса и пять нектаров<a
                    href="#citation-9" id="ref-9" class="citation-mark"><sup>9</sup></a>.</p>
            <p>Соответственно, получившие абхишеку Ануттара-Йога-Тантры должны держать двенадцать
                предметов:</p>
            <ol class="list-decimal ml-6">
                <li>Ваджр и колокол.</li>
                <li>Барабанчик — жандэу (сделанный из черепов).</li>
                <li>Габалу.</li>
                <li>Хадангу.</li>
                <li>Украшения из костей.</li>
                <li>Восемь украшений из драгоценностей.</li>
                <li>Ушнишу и корону.</li>
                <li>Ложку и черпак (для жинсрека).</li>
                <li>Кувшин.</li>
                <li>Чётки.</li>
                <li>Сидение из трупа и юбку из шкуры тигра.</li>
                <li>Одеяние для верхней части тела и для нижней части тела.</li>
            </ol>
            <p>Если имеешь эти предметы, то надо держать их реально. Когда же не имеешь их или
                имеется препятствие со стороны объекта — места и времени, то, говорят, надо держать
                в качестве обетованных предметов их нарисованные изображения<a href="#citation-10"
                                                                               id="ref-10"
                                                                               class="citation-mark"><sup>10</sup></a>.
            </p>
            <p>Также, если получена абхишека Чакрасамвары — Дэмчога, то сверх тех обетов надо
                хранить также четыре необщих обета Материнской Тантры Чакрасамвары:</p>
            <ol class="list-decimal ml-6">
                <li>Пребывать в левом делании всего — делании левой рукой и т. д. при приготовлении
                    жертвы, поднесении жертвы Божеству, употреблении еды и напитков и т. д.
                </li>
                <li>Вообще — каждый день подносить жертву согшод, а особенно — совершать большую
                    жертву в десятый день первой половины месяца.
                </li>
                <li>В течение каждых суток шесть раз подносить хвалу из восьми стихотворных строчек,
                    осуществив нераздельность Учителя с Идамом.
                </li>
                <li>Когда видишь какую бы то ни было женщину — старую или молодую, нужно произнести
                    указанные в Тантре стихи:
                    <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                        <p>Я — сын, ты — мать.</p>
                        <p>До возникновения Просветления</p>
                        <p>Поддерживай меня молоком, появившимся</p>
                        <p>Из груди семи членов Просветления.</p>
                    </blockquote>
                    и сделать три обхода влево (т. е. против часовой стрелки).
                </li>
            </ol>
            <p>Однако, хотя бы и не делал обход реально из-за особенностей объекта — места и
                времени, говорят, надо произнести стихи и делать обход мысленно.</p>
            <p>Это необщие обеты Материнской Тантры. Этим также указывается, что нужно хранить
                необщие обеты — дамсиг и домпа, особые у каждого Идама и Хранителя Учения —
                Дхармапалы.</p>
            <p>Также Панчен Балдан Ешей говорит, что к ним нужно обязательно добавить десять
                обетований, указанных Хайдуб Же.</p>
            <p>Эти десять обетований таковы:</p>
            <ol class="list-decimal ml-6">
                <li>За исключением защиты другого и т. д. и большого удивления, не вести шумный
                    разговор (уважать мнение другого и т. д.).
                </li>
                <li>Не говорить о недостатках других.</li>
                <li>Не говорить другим грубые слова.</li>
                <li>Не быть привязанным к родственникам и подношениям.</li>
                <li>Во время усердных занятий практикой добра не следует терять тело, поглощать
                    слишком большое количество еды и т. д.
                </li>
                <li>Давать вещи другим.</li>
                <li>Не сердиться на вредящего тебе и какую бы то ни было близкому тебе или
                    дальнему.
                </li>
                <li>Не держать для собственного блага вещи, за исключением тринадцати своих
                    предметов (ваджра и т. д.), и при проповеди Учения и т. д. задействовать мысль о
                    Просветлении<a href="#citation-11" id="ref-11"
                                   class="citation-mark"><sup>11</sup></a>.
                </li>
                <li>Не питать зависти к совершенству<a href="#citation-12" id="ref-12"
                                                       class="citation-mark"><sup>12</sup></a>
                    других.
                </li>
                <li>Заниматься практикой трёх типов людей — маленького, среднего и великого;
                    практиковать в четыре периода<a href="#citation-13" id="ref-13"
                                                    class="citation-mark"><sup>13</sup></a>
                    зарождение и созерцание двух мыслей о Просветлении
                    посредством маленького ритуала зарождения мысли, желающей Просветления ради
                    спасения всех существ, и мысли вхождения в практику ради этого. В четыре периода
                    делать основательную визуализацию мандалы какого бы то ни было Идама — опоры и
                    опирающихся<a href="#citation-14" id="ref-14"
                                  class="citation-mark"><sup>14</sup></a>.
                </li>
            </ol>

            <h4 class="text-base font-semibold text-blue-900 mt-4 mb-2">2.3.3. Описание метода
                исправления при нарушении обета</h4>
            <p>Поразмыслив над утверждениями о очень большой пользе при сохранении обетов дамсиг и
                домпа и тоже очень большом вреде при несохранении, нужно изучить должным образом
                метод сохранения обетов по каждому из Пяти Родов и общих второстепенных,
                заповеди-обетования в отношении коренных падений, грубых и т. д., и хранить, не щадя
                жизни. Если, хотя и усерден в сохранении, но из-за четырёх причин возникновения
                падения — незнания, непочтительности, легкомыслия и множества клеш — замарался
                коренным падением, грубым и прочими падениями, то нужно действовать согласно методу
                исправления, особого для каждого вида падений.</p>

            <h5 class="text-sm font-semibold text-blue-900 mt-4 mb-2">Исправление коренного
                падения</h5>
            <p>Сначала сделать из того, чем располагаешь, Учителю и ваджарным товарищам
                предварительное подношение, согхара и праздника. Затем, родив раскаяние в возникшем
                ранее коренном падении и приняв твёрдое решение не совершать его впредь, поклониться
                три раза, сесть на низшем месте<a href="#citation-15" id="ref-15"
                                                  class="citation-mark"><sup>15</sup></a> со
                сложенными ладонями, поджав колени к груди, и
                обратиться с просьбой:</p>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Прошу уделить внимание признанию в возникновении такого-то и такого-то коренного
                    падения.</p>
            </blockquote>
            <p>Затем произнести три раза:</p>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Учитель, молю внять! У меня, именуемого... (назвать своё тайное имя) Ваджара,
                    возник такой-то и такой-то грех (произнести название коренного падения, которое
                    возникло), нарушение винаи-дисциплины Ваджраяны из основы поражения, как это
                    полностью называется. Объявляю их Учителю, признаюсь, не утаиваю.<br>
                    Благодаря раскаянию и признанию я буду пребывать в блаженстве. А если не
                    признаюсь, не раскаюсь, то не буду пребывать в блаженстве.</p>
            </blockquote>
            <p>В конце Объект<a href="#citation-16" id="ref-16" class="citation-mark"><sup>16</sup></a>:
            </p>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Ты видишь грех в грехе?</p>
            </blockquote>
            <p>На это ответить:</p>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Вижу.</p>
            </blockquote>
            <p>И снова задают вопрос:</p>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Будешь держать обет впредь?</p>
            </blockquote>
            <p>На это отвечать:</p>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Буду хранить как следует, в соответствии с Учением, в соответствии с Винаей.</p>
            </blockquote>
            <p>После того, как это проделано три раза, Объект говорит:</p>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Есть метод.</p>
            </blockquote>
            <p>Совершающий покаяние говорит один раз:</p>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Хорошо.</p>
            </blockquote>
            <p>Затем Учитель должен полностью дать абхишеку.</p>
            <p>Так поступают. А когда нет возможности сделать так из-за того, что нет своего
                Ваджарного Учителя, или хотя и есть, но очень далеко и путешествие затруднительно и
                т. д., то надо совершать исправление коренного падения так, как указано прежде, в
                собрании обладателей обетов Мантра(-яны) — своих ваджарных товарищей и т. д., или
                можно также с одним.</p>
            <p>Итак, сделав сначала предваряющее поднесение Праздника (Героев, Героинь), затем тот
                человек (кающийся) кланяется земно, если другие учащиеся старше, чем он сам; если
                моложе, то просто выказывает уважение как обычно (кланяется неземно). Всё остальное
                — как указано прежде.</p>
            <p>Неодинаковое: вместо «Учитель, молю внять!» надо говорить:</p>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Видьядхара (т. е. тантрист) или достопочтенный (в случае монаха), молю внять!</p>
            </blockquote>
            <p>Затем вместе с товарищами или можно также одному самому надо войти самому (в мандал)
                и получить (абхишеку).</p>

            <h5 class="text-base font-semibold text-blue-900 mt-4 mb-2">Исправление грубого
                падения</h5>
            <p>Индивид, у которого возникло грубое падение, должен устроить Праздник Героев, если
                располагает необходимым для этого.</p>
            <p>Затем следует покаяться — лучше всего перед своим Учителем, если Он есть, а если нет,
                то перед обладающим обетом Мантра(-яны), сделав его объектом, перед которым
                каешься.</p>
            <p>А всё остальное — как указано в разделе «Исправление коренного падения».</p>
            <p>Неодинаковое: надо говорить:</p>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>Прошу уделить внимание признанию в таком-то и таком-то грубом падении.</p>
            </blockquote>
            <p>И:</p>
            <blockquote class="italic pl-4 border-l-4 border-gray-300 my-4">
                <p>…возник такой-то и такой-то грех (произнести название грубого падения, которое
                    возникло) из основы грубого (падения), как это полностью называется.</p>
            </blockquote>
            <p>После слов «Есть метод» и «Хорошо» тот объект, перед которым совершается покаяние,
                произносит семь раз стобуквенную (мантру Ваджрасаттвы) над пригоршней воды и, дунув
                (на неё), даёт кающемуся (в) падении. Тот ученик должен умыться этой водой.</p>
            <p>А также, во время повторения стобуквенной (мантры) там, где имеется «ме»<a
                    href="#citation-17" id="ref-17" class="citation-mark"><sup>17</sup></a> (в
                выражении) «сарва сиддхи ме», надо произносить имя оппонента (т. е. того, для кого
                читают — кающегося).</p>
            <p>Говорят, что (совершаемое) подобным образом исправление падения, делая Учителя или
                другого подходящего индивида объектом, перед которым каются, имеет много нужного:
                легко вызывает рождение стыда и совести, производит подразделение на серьёзные и
                второстепенные заповеди-обетования, уменьшает вероятность совершения проступка в
                будущем и т. д. Поэтому оно имеет огромное значение.</p>
            <p>А также, говорят об очищении от грехов и падений посредством созерцания двух стадий
                (пути Ваджраяны), повторения мантр, поднесения балина, поднесения мандала, покаяния
                перед опорой (иконой, ступой, книгой), спасения жизни живого существа, чтения
                проповеданного Буддой, делания фигурок «саадза»<a href="#citation-18" id="ref-18"
                                                                  class="citation-mark"><sup>18</sup></a>,
                сжигания в огне (сделанной из
                кунжута) фигуры скорпиона, представляя при этом, что все грехи входят в эту фигуру,
                чтения «Трискандха (-Сутры)» и т. д.<a href="#citation-19" id="ref-19"
                                                       class="citation-mark"><sup>19</sup></a></p>
            <p>Особенно же, если повторять ежедневно без перерывов по двадцать один раз стобуквенную
                (мантру Ваджрасаттвы) в соответствии с методом (его созерцания), то это пресекает
                умножение падений<a href="#citation-20" id="ref-20"
                                    class="citation-mark"><sup>20</sup></a>. А если повторил одну
                сотню тысяч раз эту мантру, то, говорят,
                падения очищаются целиком и полностью.</p>
            <p>Эти "Правила обучения (в) Мантра(-яне)" подношу как радующую жертву мудрецам,
                принявшим в сердце смысл сказанного в "Объяснении постижения":</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <p>"Из двух видов (Колесниц) — глубокой Ваджраяны и Парамитаяны — для движения к
                    (положению) Будды в этой (жизни) Сокровенная Мантра(-яна) является значительно
                    более важной, чем Парамита(-яна)".</p>
            </blockquote>
            <p>(Это) всюду известно, как Солнце и Луна. Если есть такой обладатель мудрого разума,
                который, признавая эти слова за слова истины, не занимается поисками: "Какова та
                глубокая Колесница", а берет на себя груз следования за мудрыми, то это, увы,
                великая редкость, поскольку другие подвижники отвергают этот наивысший путь, который
                очень трудно встретить.</p>

            <p>Поэтому, войдя в ту высокую Колесницу Победителя, глубокую, являющуюся сокровищницей
                двух (видов) сиддх — Ваджарную Колесницу, редкую даже среди Будд, нужно всеми силами
                долго заниматься (ее практикой).</p>

            <p>Заслуги, полученные благодаря усердному сочинению этого текста, отдаю на великое
                Просветление.</p>

            <p>Эту работу сочинил монах по имени Лобсан-Прекрасный Ум для себя и подобных ему
                обладателей очень малой силы ума, немного ограничив объем и не приводя слова
                проповеди Будды с целью облегчения понимания. Если обладающие большой силой ума не
                удовлетворятся этим, то должны изучить дополнительно большие тексты-оригиналы.</p>

            <p>Благодаря заслуге от печатания "Правил обучения (в) Мантра(-яне)" — "Источник сиддх",
                пусть все равные небу живые существа будут поддержаны Учителем Ваджрадхарой<a
                    href="#citation-21" id="ref-21" class="citation-mark"><sup>21</sup></a> и дойдут
                до
                конца йогической практики двух стадий глубокого пути!</p>

