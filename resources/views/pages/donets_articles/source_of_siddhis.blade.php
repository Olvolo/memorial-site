            <ol class="list-decimal ml-6">
                <li>Уяснение опоры — того, у кого рождается падение.</li>
                <li>Изложение классификации падений.</li>
                <li>Установление природы падения.</li>
            </ol>

            <h3 class="text-lg font-semibold text-blue-900 mt-4 mb-2">2.1. Уяснение опоры — того, у
                кого рождается падение</h3>
            <p>Индивид, являющийся опорой не общего падения Мантраяны, должен обладать двумя
                особенностями: получив не общие с Махаяной обеты Мантраяны, не бросать их и
                пребывать в здравом уме.</p>
            <p>Таким образом, если не обладаешь обетом Мантраяны, то, хотя бы и пребывал в основе
                падения (т. е. совершил деяние, считающееся падением), возникает только проступок
                содеянного неосознания греха, а падение, состоящее в нарушении обещания, не
                возникает.</p>
            <p>Если из-за сумасшествия и т. д. не пребываешь в ясной памяти и спокойствии души, то
                падения не возникает, поскольку, когда не способен к реализации, не можешь выполнить
                обещание.</p>

            <h3 class="text-lg font-semibold text-blue-900 mt-4 mb-2">2.2. Изложение классификации
                падений</h3>
            <p>Классов или разрядов падений того, кто обладает обетом Мантраяны, два: коренные
                падения и грубые падения.</p>
            <p>Однако некоторые проступки, состоящие в нарушении обетов по каждому из Пяти Родов, и
                некоторые проступки, состоящие в нарушении второстепенных обетов, хотя и не
                называются, но фактически имеются в двух классах падений.</p>
            <p>Это подобно тому, как, хотя падения монаха и перечисляются в пяти классах падений,
                проступков, объединённых основой (т. е. принципом падения), — бессчётно.</p>

            <h3 class="text-lg font-semibold text-blue-900 mt-4 mb-2">2.3. Установление природы
                падения</h3>
