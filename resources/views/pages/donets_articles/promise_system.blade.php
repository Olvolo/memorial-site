                следующим образом. Причиной всего хорошего и плохого в мире является ум
                (<em>sems</em>), поскольку именно он порождает деяния, совершение которых приводит к
                образованию и накоплению кармы (<em>las</em>), а уж она-то и творит непосредственно
                все сущее. Ум в его обычном состоянии очень плохо поддается контролю, вследствие
                чего уподобляется буддистами дикому быку. Поэтому человек, желающий себе блага –
                получения хорошего рождения, освобождения из круговорота феноменального
                существования (сансары) и даже обретения положения Будды, должен приложить все
                усилия, чтобы сделать контролируемым и послушным свой ум<a href="#citation-3"
