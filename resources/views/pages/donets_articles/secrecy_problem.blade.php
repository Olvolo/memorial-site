        <div class="text-justify text-base sm:text-lg leading-relaxed space-y-4">
            <p>В буддизме обычно выделяют два главных течения (санскр. <em>яна</em>, буквально –
                «колесница») – Хинаяну («малую/низшую колесницу») и Махаяну («большую/великую
