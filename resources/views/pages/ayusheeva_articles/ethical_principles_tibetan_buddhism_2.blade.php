            <p>Причинами страдания являются клеши<a href="#citation-7" id="ref-7" class="citation-mark"><sup>7</sup></a>
                и карма<a href="#citation-8" id="ref-8" class="citation-mark"><sup>8</sup></a>
                (деяния). Эти два понятия
                взаимообусловлены и существуют в единстве: основные группы клеш – неведение, жажда
                (страсть) и привязанность – вызывают деяния, которые, в свою очередь, определяют
                карму. Главной из перечисленных причин является неведение. Избавление от неведения
                ведет к прекращению страдания, что достигается, согласно буддийской доктрине, через
                Путь, ведущий к освобождению.</p>
