            <h3 class="text-xl font-bold text-center text-zinc-700 mb-6">Концепция кармы</h3>
            <p>В контексте рассмотрения учения о четырех благородных истинах особое внимание
                уделяется буддийской концепции кармы. Специальная актуализация внимания на этом
                понятии в нашем исследовании обусловлена тем, что в буддийской философии эта
                категория несет основную нравственную нагрузку. Признание существования закона кармы
                – закона причины и следствия и реинкарнации является основой буддийской этики,
                базисом для практики нравственности. Важно понимать, говоря о понятии кармы, что оно
                демонстрирует буддийское учение в его практической направленности. Здесь
                нравственность выступает на первое место, потому что освободиться от тягот
                существования, которые сопровождают нас в сансаре, можно только исправляя карму в
                условиях нашего повседневного существования<a href="#citation-9" id="ref-9" class="citation-mark"><sup>9</sup></a>.
            </p>
            <p>Как было сказано выше, клеши способствуют накоплению кармы, обусловливающей
                реинкарнацию и создающей само феноменальное бытие (сансару), которое характеризуется
                неустойчивостью, изменчивостью, преходящестью и неотъемлемой мучительностью,
                способствующими возникновению и интенсифицирующими проявления перечисленных
                негативных феноменов. Таким образом, по мнению буддистов, клеши являются прямыми и
                косвенными причинами всего отрицательного в нашей жизни вообще, а в частности –
                фундаментальной неустойчивости и глубинной неудовлетворенности.</p>
            <p>По существу, неблагие кармы создаются в силу желания и ненависти. Корень желания и
                ненависти – неведение.</p>
            <p>Существует два вида неведения: неведение, которое не позволяет нам постичь ясную
                природу собственного ума, и неведение, порожденное построениями ума, из-за которого
                происходит различение объектов по качеству и т.д.</p>
            <p>Эти два вида неведения, в сущности, коренятся в изначальном неведении, которое цепко
                держится за представление о «Я» и «Мое», единственную реальность земных благ и
                наслаждений, и по этой причине оно является источником проявления эгоизма: желания
                наслаждений и нежелания страданий. Более активным и изощренным в искажении
                восприятия действительности является неведение, порожденное деятельностью ума. Оно
                все внешнее делит на приносящее пользу и не приносящее пользу, которое в свою
                очередь может быть вредящим и нейтральным. Отсюда вытекают понятия «Мое», отсутствия
                у «Меня» того, что имеет «Другой». Это вызывает у человека стремление удержать и
                сохранить «Свое» и обладать вещами, ему не принадлежащими, которое проявляется в
                виде алчности, а в отношении вредящего – стремление уничтожить его, проявляющееся в
                виде агрессии (гнева). Нейтральное отношение к явлениям проявляется через эмоции
                безразличия и равнодушия. Из алчности, агрессии, основой которых является неведение,
                разворачиваются гордость, зависть и другие клеши. Возникшие на основе неведения
                ложные взгляды усиливают само неведение и порождают клеши.</p>
            <p>Таким образом, пока человек не поймет природу явлений, он будет полагать, что они
                существуют сами по себе, а это ведет к порождению желания и ненависти и к накоплению
                кармы. Но как только человек непосредственно постигает истину об отсутствии
                самобытия, он хоть и может накапливать дурную карму, но уже не создаст новой кармы,
                приводящей к рождению в сансаре<a href="#citation-10" id="ref-10" class="citation-mark"><sup>10</sup></a>.</p>

