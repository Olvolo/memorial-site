            <h2 class="text-2xl font-bold text-center text-zinc-700 mb-8">Тантрийские обеты, Кунлон
                и светская этика</h2>
            <p>Второе направление в системе обетования устанавливается в связи с наличием в буддизме
                разных путей совершенствования. Обычно выделяют путь достижения освобождения ради
                самого себя – Хинаяну (Малую Колесницу), которую подразделяют на Шравакаяну
                (Колесницу Слушателей) и Пратьекабуддаяну (Колесницу Самопросветляющихся), а также
                путь обретения положения Будды ради спасения всех существ – Махаяну (Великую
                Колесницу), которую делят на Парамитаяну (Колесницу Совершенств), опирающуюся на
                метод практики шести или десяти парамит (совершенств), и Ваджраяну (Алмазную
                Колесницу), или Тантру, делающую упор на использование особенных методов (созерцание
                Идамов-Божеств и др.). В связи с этим тибетские буддисты выделяют три типа
                нравственности: 1) нравственность личного освобождения достигается главным образом
                путем воздержания от поступков и высказываний, которые могут причинить вред; 2)
                нравственность заботы о других живых существах, называемая нравственностью
                Бодхисаттв, достигается главным образом путем недопущения эгоистичности ума; 3) в
                основе нравственности тантры лежат особые методы, дающие возможность представить
                себе полностью развитые тело и ум, что позволяет успешно помогать другим живым
                существам. Эта нравственность открывает путь к воздержанию, благодаря которому
                индивид оказывается в состоянии превзойти свое ограниченное телесное и умственное
                восприятие и увидеть себя преисполненным сияния мудрости и сострадания<a
                    href="#citation-33" id="ref-33" class="citation-mark"><sup>33</sup></a>.</p>
            <p>
                В тибетском буддизме, как принято считать, нет следующих по пути Хинаяны. Однако
                есть такие буддисты, которые в соответствии с теорией известного индийского
                миссионера Атиши (982-1054) и Цонкапы о трех типах личности занимаются практикой
                ступеней «низшей» и «средней» личности для того, чтобы впоследствии достичь ступени
                «высшей» личности, став махаянистом. Для этой категории лиц принятие специальных
                обетов является необязательным, хотя желательно использование обетования для
                культивирования присущих им установок на достижение истинно высокого (хорошего
                рождения) и подлинно хорошего (освобождения для себя лично).
            </p>
            <p>Все махаянисты принимают общие для них обеты, а именно: фундаментальный для
                Бодхисаттвы (обычного святого последователя Махаяны) обет достижения положения Будды
                ради спасения всех существ, а также обеты не совершать восемнадцать падений и сорок
                шесть грехов<a href="#citation-34" id="ref-34"
                               class="citation-mark"><sup>34</sup></a>.
            </p>
            <p>
                Обет достигнуть состояния Будды включает четыре цели:
            <p>
            <ul class="list-none pl-5 space-y-1 ml-8">
                <li class="my-2">1) принести спасение всем существам;</li>
                <li class="my-2">2) искоренить страсти;</li>
                <li class="my-2">3) достичь Истины;</li>
                <li class="my-2">4) наставить других на путь к Будде.</li>
            </ul>
            <p>
                Давший этот обет отказывается от нирваны до тех пор, пока все живые существа не
                будут спасены от круговорота бытия и не достигнут Просветления. Догмат о Вечном
                Проклятии несовместим с этим универсальным альтруизмом, и как говорил христианский
                философ Ориген, «он не совместим со всеохватывающей Любовью Благого Закона»<a
                    href="#citation-35" id="ref-35" class="citation-mark"><sup>35</sup></a>.
            </p>
            <p>В краткой форме махаянского «Прихода к Прибежищу» говорится: «Прибегаю к трем
                Драгоценностям! Да освобожу я всех живых существ! Да будет достигнуто просветление!
                Да зародится истинная просветленная мысль!». В этой формуле заложен весь смысл
                буддийской практики Махаяны. Поэтому индивид, принимая обет бодхисаттвы должен
                направлять свою деятельность на благо всех живых существ без какого бы то ни было
                исключения. Объектом его бодхисаттвовской деятельности становится вся полнота живых
                существ. Поэтому здесь не должно быть близких, чужих и безразличных. В этом смысл
                практики «Четырех безмерных» – безмерной любви, безмерного сострадания, сорадования,
                равностного отношения ко всем. Суть этой практики заключается в молитве: «Все живое
                да вольется в блаженство до самых основ своих! Все живое да избавится от страданий и
                самих основ его! Все живое да не расстанется с блаженством, чуждым мучению! Все
                живое да не имеет мысли вредить чужим, любить близких, но имеет мысли равные для
                всех!» Такая практика блокирует проявление клеш на эмоциональном уровне,
                вырабатывает высшую мотивацию, на базе которой развивается бодхичитта.</p>
            <p>Специфической характеристикой махаянских обетов служит их общая ориентация на
                осуществление блага для других, а максима может быть сформулирована в следующем
                виде: «Приноси пользу всем другим существам». Таким образом, данная координата имеет
                три основные значения, в качестве которых выступают статусы буддиста: (1) не
                ставшего еще на путь Махаяны, (2) ставшего на путь Махаяны в виде Парамитаяны, (3)
                вошедшего в Тантру.</p>
            <p>Тантрист добавляет к бодхисаттвовским обетам еще целый ряд обетов, устанавливаемых в
                соответствии с той конкретной тантрийской системой, в которую он получил
                посвящение.</p>
            <p>Особое значение в тибетском буддизме придается тантрийским обетам, поскольку
                считается, например, что простое соблюдение обетов высшей из тантрийских систем<a
                    href="#citation-36" id="ref-36" class="citation-mark"><sup>36</sup></a>
                (Ануттара-йога-тантры) даже без созерцательной практики может обеспечить обретение
                положения Будды максимум через шестнадцать реинкарнаций. Сущность тантрийских обетов
                – в воздержании от действий, которые помешают добиться успехов в практике, они
                «способствуют устранению обыденной видимости и порождению в себе очищенного
                божественного восприятия мира»<a href="#citation-37" id="ref-37"
                                                 class="citation-mark"><sup>37</sup></a>. Их
                нарушение считается падением
                (коренным или грубым), вызывающим рождение в плохих формах жизни (аду и т.д.) и
                блокирование продвижения по пути совершенствования на очень продолжительное время.
            </p>

            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                Это пример со змеей внутри бамбука. Змее не уползти ни вправо, ни влево; она может
                двигаться только вверх или вниз<a href="#citation-38" id="ref-38"
                                                  class="citation-mark"><sup>38</sup></a>.
            </blockquote>
            <p>Тантрийские обеты получают во время ритуала посвящения (абхишека). Характерной для
                последних лет стала следующая ситуация: с одной стороны, не так уж и много
                буддийских Учителей (гуру), которые могут и имеют право давать высшие посвящения, а
                с другой стороны – многие желают получить подобные посвящения. Поэтому сейчас весьма
                распространенной является практика, когда известный тантрийский Учитель дает в
                разных странах посвящения сразу сотням и тысячам людей, многие из которых имеют
                довольно смутное представление даже о буддизме, не говоря уж о знании обетов. В силу
                этого полученные ритуально обеты не могут соблюдаться должным образом, что, согласно
                буддийскому Учению, должно привести к крайне тяжелым кармическим последствиям. Чтобы
                хоть как-то исправить положение, на западных языках издаются книги, в которых
                описываются тантрийские практики и обеты. Но подобные публикации фактически идут
                вразрез с обетом, согласно которому не следует открывать доступ к тантрийскому
                учению для тех, кто не получил соответствующего посвящения. Кроме того, для очищения
                от неизбежного нарушения обетов тантрийский Учитель должен периодически давать
                повторные посвящения. Создается некоторая тенденция к «оригинальному» способу
                буддийского совершенствования, которое сводится главным образом к тому, чтобы
                регулярно получать разные тантрийские посвящения, так как эти ритуалы обладают
                сильным очистительным действием и наделяют большим благословением. Тантрийские
                Учителя, очевидно, идут на это вполне сознательно, считая, что польза значительно
                перевешивает возможный вред, и ориентируясь на достигаемое посредством посвящений
                постепенное улучшение общей кармы как всех существ вообще, так и непосредственно
                получающих посвящения, делающее их все более и более подготовленными («созревшими»)
                для правильных занятий тантрийской практикой. В современных условиях, когда тайные
                тантрийские учения тибетского буддизма находятся под угрозой исчезновения, гораздо
                важнее предоставить каждому, кто по-настоящему заинтересован, возможность углубить
                свое понимание и заложить фундамент для совершенного постижения. Именно этому служит
                безграничная деятельность современных учителей всех тибетских традиций<a
                    href="#citation-39" id="ref-39" class="citation-mark"><sup>39</sup></a>.</p>
            <p>Несколько особое положение в тибетском буддизме занимает тантрийская система
                Калачакры<a href="#citation-40" id="ref-40" class="citation-mark"><sup>40</sup></a>,
                посвящения в которою еще в давние времена производились периодически и
                давались сразу множеству самых разных людей, включая даже грудных младенцев. Это
                делалось и делается сейчас потому, что подобные ритуалы, по мнению тантристов,
                оказывают весьма благотворное воздействие на окружающий мир, а также, вероятно, для
                того, чтобы возможно большее число людей установило посредством посвящения
                мистическую связь с божеством Калачакрой и благодаря этому смогло родиться в
                мифической стране Шамбале и принять участие в предрекаемой некоторыми авторитетными
                тибетскими святителями шамбалинской войне со злом на стороне правых сил.</p>
            <p>Учение Калачакры принадлежит к категории тантр высшей йоги и является поэтому тайной
                доктриной. В древние времена все тантрические учения практиковались и передавались с
                чрезвычайной осторожностью. Открытая публикация тантрической литературы не
                поощрялась, а доступ к ней был ограничен узким кругом посвященных. Однако за
                последнее столетие многочисленные западные ученые и энтузиасты очень много писали о
                тантрическом буддизме, часто без должного понимания системы в целом. Это вызвало и,
                к сожалению, продолжает вызывать множество ошибочных и искаженных представлений,
                связанных с природой тантрической практики и самого учения тантры.</p>
            <p>Учение тантры высшей йоги считается наиболее могущественным из всех учений Будды,
                поэтому, давая наставления по пути нравственного совершенствования, тибетские
                учителя делают акцент на пути Махаяны, а внутри него – на Ваджраяне. Ее методы
                особенно полезно использовать именно в тех условиях, в которых оказалось ныне
                человеческое сообщество. Глобальность современных проблем соответствуют высшему
                уровню мотивации – преодолению своих проблем и недостатков, чтобы стать помощником
                для других. «В Эпоху Раздора люди редко бывают дружелюбны; скорее они пытаются
                обставить друг друга. Эта фундаментальная страсть к соревнованию и дала название
                этой эпохе. Но именно в этом и кроется причина того, что Ваджраяна идеально
                соответствует нашему времени. Чем сильнее беспокоящие эмоции, тем больше возможность
                узнавания своей изначальной пробужденности. В эру Майтрейи все будут любить друг
                друга, но никто даже не услышит слова «Ваджраяна»; этих учений попросту не будет»<a
                    href="#citation-41" id="ref-41" class="citation-mark"><sup>41</sup></a>.</p>
            <p>Тантра особенно подходит для западного образа мышления: являясь самым быстрым из всех
                путей, она привлекает западного человека, стремящегося к получению мгновенных
                результатов. При использовании техник тантры высшей йоги на основе трех высших
                навыков, сострадательного стремления бодхисаттвы к высшему просветлению и практики
                шести совершенств можно достичь просветления уже в этой короткой жизни. Более того,
                суть тантрийского учения заключается в трансформации сознания, а на Западе хорошо
                понимают принцип трансформации энергии – по крайней мере, на материальном уровне. И,
                наконец: тогда как мощный взрыв желаний считается серьезным препятствием для
                практики большинства духовных учений, в Тантре он играет роль положительную,
                поскольку желание выступает в качестве импульса, дающего интенсивный толчок к
                достижению высочайшей цели<a href="#citation-42" id="ref-42"
                                             class="citation-mark"><sup>42</sup></a>.</p>
            <p>Выделение двух указанных направлений и присущих им значений позволяет легко
                установить положение любого буддиста в системе обетования. Например,
                монах-тантрист<a href="#citation-43" id="ref-43" class="citation-mark"><sup>43</sup></a>
                имеет монашеские, общемахаянские и тантрийские обеты, а послушник-махаянист –
                тридцать шесть обетов послушника и общемахаянские.</p>
            <p>Обычно считается, что основные обеты линии «индивидуального спасения» принимаются
                только один раз и должны соблюдаться до момента смерти, а махаянские могут
                приниматься многократно и должны соблюдаться также и в следующих рождениях.
                Поскольку обеты в выделенных указанных выше группах могут дублироваться или даже
                оказываться несовместимыми, то в тибетской литературе жанра «Три обета» (sdom gsum)
                рассматриваются специально вопросы о взаимоотношениях между обетами, их иерархии и
                т.п. Однако следует признать, что решение этих вопросов тибетскими буддистами
                отличается большим разнообразием<a href="#citation-44" id="ref-44"
                                                   class="citation-mark"><sup>44</sup></a>.</p>
            <p>Само принятие обетов создает определенные заслуги, а совершение или воздержание от
                деяний в качестве соблюдения обетов дает больший эффект, чем до принятия обетов.
                Нарушение же обетов усиливает накопление отрицательного. С другой стороны,
                соблюдение обетов блокирует проявление отрицательной кармы и активизирует проявление
                положительной. Рекомендуется ежедневно обновлять обеты. Тибетские буддисты считают,
                что в момент принятия обета передается сила, которая будет тянуть к Нирване. При
                ежедневном принятии обетов эта сила обновляется и возрастает. Это также дает
                установку, программу нравственного поведения на день, способствует определенному
                напряжению мысли в течение дня. Поэтому в Махаяне ежедневная практика начинается с
                принятия основных обетов буддиста-махаяниста, а именно – прибегания к трем
                драгоценностям и порождения мысли бодхисаттвы, а в Тантре занимаются Шестиразовой
                Гуру-Йогой, в которой принимаются тантрийские обеты. Если все же есть нарушение
                обетов, то необходимо покаяться и принять твердое решение больше не совершать
                их.</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                В небрежности по отношению к своим самайям есть огромная опасность. Однако, если вы
                искренне и с раскаянием восстанавливаете их, твердо решив более их не нарушать,
                тогда нарушение подобно повреждению золотого сосуда – его легко исправить<a
                    href="#citation-45" id="ref-45" class="citation-mark"><sup>45</sup></a>.
            </blockquote>
            <p>Следует отметить то, что присущая человеку эгоцентрическая ориентация на заботу
                только о себе самом, подогреваемая современной широкой пропагандой идеалов
                потребительского общества, привела к формированию и укреплению установки на
                безудержное инкорпорирующее поглощение ценностей самого различного рода, в том числе
                и чисто духовных. Люди хотят только получать, причем, желательно, уже в готовом
                виде, не прилагая к этому никаких особых усилий и ничего не давая взамен. Такая
                идеология и практика исключают принятие обетов, ограничивающих свободу безудержного
                хотения и налагающих определенные обязательства.</p>
            <p>Однако в современном мире есть много людей, которые склонны к соблюдению этики, но
                при этом – в силу распространенности рационализма считают необходимым твердо
                обосновать необходимость принятия тех или иных норм нравственного поведения.
                Исследуя состояние нравственности в настоящее время, Далай-Лама XIV, являющийся
                общепризнанным главой тибетского буддизма, выступил с инициативой создания
                внерелигиозной этики.</p>

            <h3 class="text-xl font-bold text-center text-zinc-700 mb-6">Кунлон и светская
                этика</h3>

            <p>Главным критерием этичности совершенных человеком поступков, как считает Далай-Лама,
                является наличие <em>кунлона</em> или присутствие в сознании мотивации на совершение
                благих дел. Поэтому цель духовной и этической практики заключается в трансформации
                сознания таким образом, чтобы происходило непрерывное совершенствование благого <em>кунлона</em><a
                    href="#citation-46" id="ref-46" class="citation-mark"><sup> 46</sup></a>.</p>
            <p>Термин <em>кунлон</em> занимает важное место в учении тибетских буддистов о
                нравственности и методах ее культивирования. Далай-лама использует это понятие при
                построении своей системы внерелигиозной этики, предлагаемой для современного
                западного менталитета. Для того, чтобы понять содержательную сущность термина <em>кунлон</em>,
                обратимся к трем основным видам сознания, выделяемым в тибетском буддизме, – это
                благое (нравственное), неблагое (безнравственное) и нейтральное. Благое и неблагое
                считаются несовместимыми, поскольку они не могут одновременно определять качество
                сознания одного и того же человека. Когда совершается благое деяние, сознание
                получает качество благого, а при совершении неблагого – качество неблагого. Для
                потока сознания обычного индивида характерно преобладание качества неблагого.
                Поэтому, говорят тибетские буддисты, неблагое является доминирующим качеством его
                сознания. Особенностью подобного доминирования является то, что данное качество
                охватывает и пронизывает все сознание в целом, и при его наличии создается тенденция
                к совершению неблагого и нереализации благого. Аналогичный процесс разворачивается и
                при доминировании в сознании качества благого. Преобладание в сознании определенного
                качества зависит от наличия соответствующей установки или мотивации. Именно такое
                целостное состояние сознания, в котором доминирование конкретного качества
                сочетается с присущей ему установкой, тибетские буддисты и называют
                <em>кунлоном</em>. Из этого следует, что непосредственная цель практики
                нравственности должна состоять в полном избавлении от неблагого <em>кунлона</em> и
                культивировании исключительно благого.</p>
            <p>Достижение этой цели представляет собой довольно трудную задачу, поскольку у человека
                обычно преобладает неблагой <em>кунлон</em>, вследствие чего благие деяния
                совершаются им лишь изредка, что, в свою очередь, препятствует возникновению
                устойчивого благого <em>кунлона</em>.</p>
            <p>В этом отношении религиозные люди находятся в более выгодном положении, чем
                нерелигиозные, поскольку у первых, как полагают тибетские наставники, при занятиях
                типичными видами религиозной практики сам собой возникает благой <em>кунлон</em>.
                Правда, он, если не будет поддерживаться в повседневной жизни совершением благих
                деяний и реализацией благих качеств, вскоре после завершения конкретной практики
                (молитвы, медитации) может смениться неблагим <em>кунлоном</em>. Эта проблема
                сохранения в течение всего дня благого <em>кунлона</em>, возникшего благодаря
                занятиям религиозной практикой, является одной из первых, с которыми приходится
                сталкиваться каждому неофиту. Основная причина заключается в том, что новичок,
                получив с относительной легкостью благой <em>кунлон</em>, обычно не уделяет особого
                внимания активизации связанной с ним установки, реализация которой в повседневной
                жизни сопряжена с достижением высокого уровня самоконтроля и необходимостью
                непрерывного проявления терпения и усердия. Поэтому тибетские наставники рекомендуют
                начинать день с формирования прочного благого <em>кунлона</em>, затем развивать его
                благодаря занятиям серьезной практикой, поддерживать и по возможности даже усиливать
                этот <em>кунлон</em> посредством реализации благого в повседневной жизни, а перед
                сном подвергнуть анализу имевшие место отклонения от культивирования нравственности,
                очиститься от них путем покаяния и укрепить <em>кунлон</em> (точнее – его установку)
                с помощью принятия твердого решения никогда больше не допускать нарушения этики.</p>
            <p>Итак, благой <em>кунлон</em>, с одной стороны, возникает благодаря реализации
                благого, а с другой – сам, при должном развитии, создает тенденцию к свершению
                нравственного поступка. Данную закономерность в культивировании нравственности
                отмечали и западные мыслители, хотя и рассматривали ее несколько по-иному. Так, Н. О.
                Лосский указывает, что совершение нравственного положительного поступка приводит к
                возникновению особого вида счастья, которое может испытываться даже при наличии
                физического страдания. С другой стороны, подобное счастье характеризуется как
                источник добродетели, ибо только счастливые совершают добрые поступки. Таким
                образом,</p>

            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                счастье вовсе не есть средство для достижения блаженства. Счастье есть
                корень и источник добродетели, но такой источник, который сам есть уже не что иное,
                как следствие внутренней добротной сущности лица. Иными словами, счастье и
                нравственно добротная сущность личности суть два неразрывно связанных аспекта одного
                и того же бытия, объективно ценного и субъективно переживаемого как счастливое:
                нравственно достойное бытие есть счастливое бытие и, наоборот, счастливое бытие есть
                нравственно достойное<a href="#citation-47" id="ref-47" class="citation-mark"><sup>47</sup></a>.
            </blockquote>

                <p>Поскольку счастье, о котором говорит Лосский, очевидно, выступает в качестве спутника и следствия благого
                <em>кунлона</em>, то приведенные здесь слова можно расценить как своеобразное
                описание механизма функционирования этого <em>кунлона</em>.</p>
            <p>Хотя тибетские учителя заявляют об определенном преимуществе религиозных людей в
                наработке нравственности, все же они признают, что в духовном отношении все люди,
                верующие или нет, имеют одну и ту же природу. Выстраивая систему внерелигиозной
                этики, предлагаемой для современного западного менталитета, Далай-лама IV
                распространяет понятие <em>кунлона</em> и на нерелигиозных людей, рекомендуя им
                уделять особое внимание развитию благой мотивации.</p>
            <p>Здесь в первую очередь имеется в виду культивирование того, что тибетские буддисты
                называют «четырьмя безмерными», а именно: безмерного добросердечия, безмерного
                сострадания, безмерной радости и безмерной беспристрастности. Реализация отношения
                равного интереса, любви, сорадования и сострадания ко всем живым существам, без
                какого бы то ни было исключения, служит мощным оружием против безнравственности,
                формируя прочный и устойчивый благой <em>кунлон</em>, наделенный сильной установкой
                (направленностью) на совершение морального поступка. Регулярная практика «четырех
                безмерных» приводит к образованию целостного нравственного феномена, который
                включает в себя эти «безмерные» и твердое намерение приносить всем существам пользу
                и счастье. Как считают тибетские буддисты, «поскольку число живых существ
                безгранично как пространство, безграничной должна быть и наша практика этих четырех
                добродетелей»<a href="#citation-48" id="ref-48" class="citation-mark"><sup>48</sup></a>. Сформированный феномен выступает в
                качестве специфического ядра благого <em>кунлона</em>, обеспечивая устойчивость и
                развитие нравственных качеств личности. Кроме того, при постоянной поддержке благого
                <em>кунлона</em> с подобным ядром обнаруживается интересная особенность, состоящая в
                том, что у обладающего им индивида раскрывается своеобразная «этическая» интуиция,
                позволяющая ему ясно представить, какие именно деяния будут правильными
                (нравственными) и поэтому приемлемыми для него в конкретной ситуации, а какие –
                неправильными, стало быть, и ненравственными.</p>
            <p>Сострадание ко всем живым существам как нравственный принцип должен реализоваться в
                социальной деятельности человека. Обычно люди, в большинстве своем, при общении
                следуют принятым в данном сообществе нормам поведения в соответствии с определенным
                ролевым репертуаром. Эти нормы усваиваются в процессе социализации в результате
                применения различных социальных технологий<a href="#citation-49" id="ref-49" class="citation-mark"><sup>49</sup></a>, которые, развертывая свои требования в
                многомерную систему экспектаций и придавая им статус морального долженствования,
                делают систему социальных отношений устойчивой. Однако эти нормы долженствования,
                как отмечал еще П. А. Флоренский, не могут обеспечить стабильность нравственного
                поведения, если они не укоренены в более прочном основании. Ведь подлинная
                нравственность, указывает Ю. М. Федоров, ориентирована на целостного субъекта и
                разворачивается в системе отношений «человек-человек», в то время как нормы
                социального поведения ориентированы на частичного субъекта (выступающего в
                конкретной роли – отца, мужа, сына, начальника и проч.) и разворачиваются в системе
                отношений «человек-общество». Фактически эти нормы представляют собой продукт
                распада системы гуманистических ценностей и насыщаются моральным содержанием только
                при их укоренении в этой сфере. Поэтому система социальных взаимоотношений будет
                сбалансированной и стабильной лишь тогда, когда центр тяжести в универсуме морали
                приходится на систему отношений «человек-человек»<a href="#citation-50" id="ref-50" class="citation-mark"><sup>50</sup></a>.</p>
            <p>Проблема наделения социально-ролевых норм качествами морально насыщенных правил
                поведения неизбежно возникает при решении задач нравственного совершенствования. При
                этом у людей, только принявших какую-либо религию, возможен так называемый
                «программный сбой», когда благой <em>кунлон</em> у них еще должным образом не
                сформирован, а прежние стереотипы поведения не приведены в соответствие с новым
                подходом к жизни. Период переориентации на новые ценности может затянуться на
                неопределенное время, переживаться довольно болезненно и обернуться совершенно
                противоположными результатами. Нравственная дезориентация, вызванная, прежде всего,
                недостаточным уровнем самодисциплины, может привести, в частности, к пренебрежению
                семейными и профессиональными обязанностями, послужить причиной более низкого порога
                ответственности, надежности в сравнении, к примеру, с людьми, не занимающихся
                религиозными практиками и даже не ставящих цели нравственного совершенствования.</p>
            <p>Выработке жизненной ориентации и поведенческого стиля человека, в котором статусные
                нормы и нравственные правила гармонично бы взаимодействовали, служит предложенный
                тибетским буддизмом метод. Так, в Сутре «Вопросов домохозяина Угры» рассказывается о
                том, как Будда при поучении мирян указывал на неправомерность определения индивидом
                себя как человека, сына, отца, министра и т.п., ибо все эти особенности его
                положения в мире и сообществе обусловлены совершенными им в прежних рождениях
                деяниями (кармой) и поэтому не могут быть признаны подлинными и реальными<a href="#citation-51" id="ref-51" class="citation-mark"><sup>51</sup></a>. Этим самым Будда обесценивал и весь комплекс ролей, который человеку
                приходится играть в обществе. В Сутре «Праджня-парамита восьмитысячная» он указывал
                на необходимость относиться ко всем живым существам как к своим ближайшим
                родственникам<a href="#citation-52" id="ref-52" class="citation-mark"><sup>52</sup></a>. При этом на первый план выдвигается в
                качестве базовой роль близкого родственника всех живых существ. Поскольку для роли
                близкого родственника характерно проявление любви, сорадования и сострадания ко всем
                другим живым существам (близким родственникам), намерение приносить им пользу,
                которые, как было установлено ранее, служат специфическим ядром нравственно
                ориентированного сознания, то два эти наставления Будды можно расценить как вполне
                конкретное указание того, как именно следует трансформировать социально-ролевые
                нормы в морально насыщенные правила поведения. При этом обычные социальные нормы не
                отбрасываются, а укореняются в прочном фундаменте нравственности и духовности,
                придавая системе социальных отношений большую сбалансированность и стабильность.</p>
            <p class="text-center font-semibold">***</p>
            <p>Таким образом, наше небольшое исследование позволило установить общий подход
                тибетских буддистов к построению своей системы этики, в основе практической
                реализации которой лежит представление о действии причинно-следственных связей,
                обрисовать роль, принадлежащую системе обетования, привело к уяснению базового для
                практической реализации этой системы понятия <em>кунлона</em> и механизма
                эффективного культивирования нравственности, выявлению специфического ядра
                нравственно ориентированного сознания и оптимального метода. Предложенный метод
                заключается в трансформации социально-ролевых норм в морально насыщенные правила
                должного поведения человека по отношению ко всем другим существам. Это открывает
                некоторые перспективы в деле дальнейшего исследования проблемы культивирования
                нравственности и имеет несомненную практическую значимость, помогая человеку с
                возможно большей отчетливостью и определенностью осмыслить, почему, зачем и как ему
                следует заниматься моральным совершенствованием.</p>
            <p>Предложенные методы нравственного совершенствования личности – реализации «четырех
                безмерных» и трансформации чисто социального в моральное, безусловно, далеко не
                единственные в тибетском буддизме. Отвечая запросам духовного прогресса, принимая во
                внимание мировоззренческие, культурные и социальные особенности, они вместе с тем
                оказываются пригодными и довольно эффективными при культивировании нравственности
                современным западным человеком.</p>

