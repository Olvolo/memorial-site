            <h2 class="text-2xl font-bold text-center text-zinc-700 mb-8">Обеты и монашество</h2>
            <p>В буддизме нравственность в первую очередь реализуется через принятие и соблюдение
                обетов. Буддийская этика немыслима без системы обетования. Даже само становление
                буддистом осуществляется путем ритуального принятия обета прибегания к Трем
                Драгоценностям (Будде, Учению, Общине) вплоть до Просветления<a href="#citation-29" id="ref-29" class="citation-mark"><sup>29</sup></a>.</p>
            <p>Функционирование системы обетования в буддизме опирается на следующие идеи
                нравственного совершенствования личности:</p>
            <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
                <li class="my-2">ритуальное принятие обета наделяет особой силой, благодаря которой
                    резко возрастает способность реализовывать добродетели;
                </li>
                <li class="my-2">совершение благого деяния в соответствии с обетом порождает
                    значительно больше заслуг, чем аналогичное его совершение без предшествующего
                    принятия обета;
                </li>
                <li class="my-2">ритуальное принятие обета очищает от грехов и порождает заслуги;
                </li>
                <li class="my-2">последующее соблюдение обета накапливает собрание заслуг;</li>
                <li class="my-2">наличие обетов чисто психологически содействует воздержанию от
                    дурного и осуществлению хорошего.
                </li>
            </ol>
            <p>Таким образом, система обетования нацелена на интенсивное формирование и реализацию
                нравственного поведения телом, речью и разумом и достижение таким образом
                сотериологических целей. Особенное значение этой системы подчеркивал основатель
                доминирующей в буддизме Центральной Азии «желтошапочной» традиции Гелуг Цонкапа
                (1357–1419), который еще в самом начале своей религиозной деятельности выступил с
                циклом проповедей о необходимости соблюдения во всей чистоте обетов нравственной
                дисциплины (Винаи), являющегося фундаментом самого существования буддизма<a href="#citation-30" id="ref-30" class="citation-mark"><sup>30</sup></a>.</p>
            <h3 class="text-xl font-bold text-center text-zinc-700 mb-6">Индивидуальное спасение и
                обеты</h3>
            <p>В тибетском буддизме обетование разворачивается в двух направлениях. Первое – это
                линия «индивидуального спасения» (пратимокша). Она задается дихотомией «мирское –
                отрешенное от мира» и имеет восемь основных значений, в качестве которых выступают
                статусы индивидов, а именно: 1) мирянин, 2) мирянка, 3) послушник, 4) послушница, 5)
                монах, 6) монахиня, 7) готовящаяся стать монахиней, 8) принявший однодневный
                обет.</p>
            <p>Особенной характеристикой обетования этой линии «индивидуального спасения» считается
                самообуздание путем воздержания от пагубного поведения телом, речью и разумом,
                максимальное проявление подобного самообуздания может быть представлено в следующей
                формулировке: «Не причиняй вреда никому».</p>
            <p>Обязательным для всех буддистов является обет прибегания к Трем Драгоценностям. С
                точки зрения прибежища буддистом является тот, кто принимает Будду, его Учение
                (Дхарму) и духовную общину (Сангху). Многие миряне ограничиваются только этим,
                однако они могут принимать пять обетов мирян – это отказ от убийства, воровства,
                лжи, прелюбодеяния, применения интоксикантов, то есть алкоголя, наркотиков и т.д.
                При этом миряне свободны в выборе: принять обеты в полном составе или только
                некоторые из них. Мирские обеты являются основой нравственной дисциплины, которую
                необходимо соблюдать даже и при принятии бодхисаттвовских и тантрийских обетов.</p>
            <p>В тибетской традиции Винаи послушники должны принимать тридцать шесть обетов, монахи
                – двести пятьдесят три, а монахини – триста пятьдесят семь.</p>
            <p>Таким образом, различию обетов воздержания, сужающих возможности проявления неблагой
                мотивации, соответствует различие дисциплинарных статусов. Такое различие в
                предписаниях основывается на том, что в положении мирянина весьма сложно достичь
                состояния Будды в силу присутствия множества препятствующих тому факторов, потому
                учение тибетского буддизма ориентировалось в основном на монашество. Монастыри
                вместе с проживающими в них монашествующими, которые отрешились от всего мирского и
                целиком поглощены только религиозной деятельностью, стали для тибетского буддизма
                своеобразными опорными пунктами. Статус отрешившегося от мира (правраджита) был
                очень высок, о чем свидетельствует былое наличие в Центральной Азии огромного
                количества монастырей с впечатляющим числом монахов, послушников и проходящих курс
                обучения учеников. Однако в настоящее время нет благоприятных условий для подобного
                процветания монашества в Тибете, Индии и других ареалах традиционного
                распространения буддизма. Поэтому, например, большинство лам (священнослужителей) в
                монастырях-дацанах Бурятии, Тывы и Калмыкии являются мирянами-тантристами, которые
                занимаются преимущественно совершением положенных ритуалов, проведением
                соответствующих молебнов (бурятск. хуралов), а также обслуживанием мирян (гадание,
                обряды, лечение).</p>
            <p>На Западе монашеский образ жизни с его строгой дисциплиной еще менее приемлем.
                Несмотря на это, в европейских странах и Америке все же действуют монастыри основной
                школы монашеского направления в тибетском буддизме – гелугпа, в которых правила
                значительно, в сравнении с тибетскими образцами, смягчены и сокращен курс обучения.
                Подобные изменения объясняются тем, что, попав в другие социокультурные условия,
                школа гелугпа, в которой отношение к Тантре было наиболее строгим, вынуждена была
                пересмотреть свои позиции и приспосабливаться к новым реалиям.</p>
            <p>Таким образом, понимание неизбежности и необходимости адаптации учения с учетом
                особенностей западного социума и специфических черт западного человека привело к
                тому, что современные тибетские наставники, проповедуя на Западе, ориентируются в
                основном на мирян.</p>
            <p>Вместе с тем несправедливо одним только вынужденным пересмотром основ учения в
                условиях западной культуры объяснить вышеназванную тенденцию. Любой человек,
                независимо от касты или убеждений, может практиковать Учение, не принимая ни одного
                обета. И точно так же тот, кто нарушил или отверг какой-либо обет, ни в коем случае
                не изгоняется и не отстраняется от буддийской практики. Уже сам факт того, что были
                выработаны системы обетов и для мирян, должен развеять бытующее среди не тибетцев
                представление о том, что наличие в Тибете вплоть до 1959 года такого большого числа
                монахов означало одновременно и необходимость стать монахом для того, чтобы
                следовать буддийским путем. На самом деле не обязательно даже возлагать на себя
                обеты мирянина, чтобы воплощать в своей жизни Дхарму. Это – проявление той степени
                свободы, которая характерна для буддизма в целом, и такой подход очень подходит для
                западного менталитета<a href="#citation-31" id="ref-31" class="citation-mark"><sup>31</sup></a>.
            </p>
            <p>Что касается принятия монашеских обетов женщинами, то в тибетском буддизме до
                последнего времени не было монахинь, так как еще в давние времена прервалась
                традиция передачи соответствующих обетов. Для проведения ритуала принятия полных
                обетов монахини необходимо присутствие обязательно десяти монахов и десяти монахинь.
                В Тибете монахини не могли принять полные монашеские обеты из-за отсутствия монахинь
                с полным монашеским посвящением. Несмотря на то, что в «Афоризмах о дисциплине»
                перечислены правила для монахинь, на практике они не применяются<a href="#citation-32" id="ref-32" class="citation-mark"><sup>32</sup></a>. В
                настоящее время система передачи полных обетов монахини существует и успешно
                работает в китайской и корейской традициях Махаяны. Желающие стать монахинями
                принимают обеты в Гонконге, на Тайване при финансовой поддержке Кагью Дхарма-центра
                в Гонконге.</p>
