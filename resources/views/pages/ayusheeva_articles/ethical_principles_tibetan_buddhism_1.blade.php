            <p>Особый интерес в этом отношении вызывает довольно интенсивно распространяющийся в
                современном мире и возрождаемый в России тибетский буддизм, в частности, его
                рецепция представителями западной культуры, в процессе которой происходит усвоение и
                культивирование этической концепции буддизма. Центральное значение в этой концепции
                отводится самой личности, при этом личность не исчерпывается тем, как она проявляет
                себя во взаимодействии с окружающим миром. Кроме этой сферы соприкосновения с
                внешней средой, у нее есть своя, только ей принадлежащая и только ей доступная сфера
                личной жизни, где душа остается наедине с собой и где скрыты корни ее нравственного
                достоинства. Буддизм усматривает высшее проявление нравственного бытия в самой
                личности, она – источник и средоточие всех нравственных добродетелей как в
                социальной, так и личной жизни <a href="#citation-3" id="ref-3" class="citation-mark"><sup>3</sup></a>.</p>
            <p>Продвижение тибетского буддизма на Запад сопровождается определенной трансформацией
                его доктрин под менталитет современного представителя западной культуры. Учитывая
                сложившуюся и привычную для мышления западного человека дихотомию «добро – зло»,
                тибетский буддизм из всего философского материала для укоренения нравственного
                поведения на первый план выдвигает представление о нравственных и безнравственных
                поступках, формирующих как личность, так и общество как совокупность личностей.
                Осознание последствий совершения нравственных и безнравственных поступков является
                одной из побуждающих причин принятия западным человеком буддизма как веры и
                специальных законов нравственной дисциплины (обетов). В целях достижения благого
                сознания, или сознания, направленного на благие деяния, тибетские буддисты для
                западного человека разрабатывают систему внерелигиозной этики, предложенную
                Далай-ламой XIV. В связи с этим представляется уместным рассмотреть сущность
                причинно-следственной взаимообусловленности (кармы), законов нравственной дисциплины
                и трансформации моральных норм в нормы социального поведения в условиях современного
                западного общества.</p>
