                человека. На фоне растущего технического прогресса негативные проявления в
                общественной жизни воспринимаются не как сопутствующие, а потому объективные и
                объяснимые, процессы, а как оборотная сторона технократической цивилизации. Вне
                зависимости от того, на каком уровне возникают проблемы: национальном,
                наднациональном или глобальном, групповом, личностном, – их причину невозможно
                свести только к материальному фактору.</p>
            <p>Негативные последствия эксплуататорского освоения природы приобретают
                межгосударственный, глобальный характер; постоянная борьба за господство над
                ресурсами оборачивается угрозой для жизни многих, проявляясь в международном
                терроризме, военных конфликтах и затяжных войнах. Но более сильную реакцию вызывают
