                детской среде. Все эти процессы разворачиваются на фоне постоянного
                пропагандирования культа силы в средствах массовой коммуникации.</p>
            <p>Таким образом, материальное благополучие западной цивилизации оборачивается
                индивидуальной эмоциональной нестабильностью, повышенным уровнем тревожности,
                напряженности и подавленности, неудовлетворенностью жизнью, неуверенностью в
                завтрашнем дне, что не может не вести к личностной изолированности каждого. В
                подобной ситуации высокой степени одиночества и отчужденности, с одной стороны, и
                усиления людской склонности к соперничеству и ревности, с другой, трудно ожидать
