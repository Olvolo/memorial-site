                образ жизни человека и его отношение к миру.
            </p>
            <p>
                Одним из сочинений, излагающих глубинные основы буддийской тантрийской мысли,
                является «Карнатантра» великого тибетского йогина Нацог-Рандола (Лонченпы) [sNa
                tshogs rang grol]. В своей трактовке основ тантрийского мировоззрения будем
                полагаться главным образом на эту работу, прекрасный перевод которой (первой её
                трети) на русский язык успел выполнить в конце своего жизненного пути известный
                бурятский ученый Б. Д. Дандарон.
            </p>
            <p>
                Описывая тантрийскую традицию понимания процесса становления вселенной (сансары –
                феноменального бытия и нирваны – мира безусловной ценности), Нацог-Рандол
                утверждает, что «динамика появления сансары и нирваны беспрерывно проявляется от
                основы существования» [Там же, л. 1] и что до разделения Единого на нирвану и
                сансару, ввиду необходимости совершенствования последней, «объектность своим
                содержанием была абсолютно очищена шуньей (пустотностью – ничто с возможностью
                нечто) в дхармовом пространстве (в пространстве непроявленной актуальности)», и было
                это «во времена давно прошедшие» [там же, л. 1б].
            </p>
            <p>
                Определяя единую основу существования, Нацог-Рандол пишет: «Сансара и нирвана
                возникают от динамики существования разума» [Там же, л. 37а], при этом «реальность
