                традиций буддийских народов мы здесь и остановимся.
            </p>
            <p>
                Вся жизнь народов Центральной Азии, принявших буддизм, проникнута духом тантрийской
                мысли, а поступки отдельных людей окрашены тантрийским ритуалом – в той или иной
                степени. Само слово «ритуал» (тиб. cho ga) при этом понимается как «движение к
