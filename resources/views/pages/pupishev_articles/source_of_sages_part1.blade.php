                <p>
                    <em>Перевод:</em> Тантра означает «длительность». Эта длительность троична:
                    1) основа, 2) состояние «татха» личности, 3) неотъемлемость. Формально личность
                    представляет имманентную причинность; неотъемлемость [состояния] «татха» есть
                    плод. Основу составляет метод.
                </p>
                <p>
                    Согласно 1: VI, 1011, «татха» (ta thā, de bzhin) означает достоверное
                    знание пути Будд (de bzhin nyid, tathatā) и вступление на него.
                </p>
                <p>
                    Rang bzhin — подлинная природа личности, вступившей на путь тантры, или
                    длительность как таковая — rgyun, santāna. Само слово «длительность» означает
                    одушевленность в смысле способности поставить перед собою цель и стремиться к ее
                    достижению (Дхармакирти. <em>Santānāntarasiddhi</em> [4]), и именно в этом
                    смысле личность представляет имманентную причинность. То, что эта причинность
                    неотъемлема от состояния «татха», есть плод, ибо приводит к состоянию
                    Ваджрадхары, или нелокализованной нирване. Основой этого движения является метод
                    [11: 114 – 115].
                </p>
