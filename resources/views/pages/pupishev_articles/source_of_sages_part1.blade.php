                <p class="mb-3" id="citation-7"><sup>7</sup> <em>Ваджр</em> (rdo rje, vajra)
                    означает тантрийский метод (thabs, upāya) (см. 8: л. 2б 3: rdo rje ni thabs
                    dang...). Он назван «ваджром», алмазом, ибо он, как алмаз, неразрушим и
                    существует вечно. Таким образом, название «ваджраяна» подчеркивает, что методы
                    этой колесницы существовали всегда (а не только со времени исторического Будды
                    Шакьямуни) и всегда будут существовать, традиционно-непрерывно передаваясь от
                    Учителя к ученику.<a href="#ref-7" class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="mb-3" id="citation-8"><sup>8</sup> Согласно словарю Суматиратны [1: VII,
                    916], слово «видьядхара» означает прежде всего me tog so can. Здесь me tog = me
                    tog lam, или путь использования тантрийских методов, а so can означает, что эти
                    методы остры, как лезвие, то есть, с одной стороны, опасны для ищущего, а с
                    другой — целебны, подобно скальпелю в руках врача. Само же слово «видьядхара»
                    значит «опора знания» и относится к тем людям, которые обладают совершенным
                    знанием и правомочны давать соответствующие тантрийские посвящения тем, кто для
                    этого подходит, ибо нож в руках хирурга — целебное средство, в руках же
                    человека, обуреваемого низкими желаниями — орудие убийства. В махаяне
                    насчитывается более 20 видов видьядхар.<a href="#ref-8"
                                                              class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="mb-3" id="citation-9"><sup>9</sup> Метод тантры признает чистоту всех
                    дхарм по природе (см. 8: л. 4, л. 16а: «по природе чисты все дхармы, по природе
                    чист «я»). Отсюда — отсутствие помрачения (moha). Причиной скитания в сансаре
                    живых существ считается непризнание (нереализация) этого факта (см. 9: л. 2б):
                    «Те, кто не узнали своего лица и положили в основу истинность существования
                    всего через вместерожденное (вместе с мудростью — В. П.) неведение, заблудились,
                    сделавшись живыми существами. А те, кто узнали [свои лица] через познание самого
                    себя, стали Буддами» (перевод Б. Д. Дандарона).<a href="#ref-9"
                                                                      class="text-blue-500 hover:underline">↩</a>
                </p>
                <p class="mb-3" id="citation-10"><sup>10</sup> Считается, что существует 6000
                    тантрийских систем, и почти в каждой из них существуют свои краткие и в той или
                    иной степени расширенные методы — садханы (sgrub thabs), благодаря чему
                    способность достижения состояния Будды или иных целей, которые ставит перед
                    собой индивид, гарантируется для любого человека, по карме своей пришедшего к
                    тантре, так как учитываются его склонности и его психическое состояние;
                    разумеется, если он эти методы практикует.<a href="#ref-10"
