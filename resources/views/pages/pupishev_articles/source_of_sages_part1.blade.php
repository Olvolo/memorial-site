            </p>
            <p>
                Различные тантрийские тексты переводят уже давно (А. Авалон, Г. В. Гюнтер, А.
                Говинда
                и многие другие), однако, систематического изложения терминологии Тантры, которое с
                достаточной полнотой описывало бы тантрийскую теорию и практику в целом, до сих пор,
                насколько мне известно, не было. Хотя и отдельные области Тантры, и система Тантры в
                целом, описаны в европейской или, точнее говоря, в литературе на европейских языках
                довольно подробно, тантрийская терминология остается все же для читателя практически
                недоступной. Та же терминология, значительная ее часть, которая включена в настоящий
                раздел, никогда не была не только описана, но даже упомянута в литературе на
                европейских языках<a href="#citation-02" id="ref-02"
                                     class="citation-mark"><sup>02</sup></a>
                . Очевидно, такое положение сложилось в основном по той причине,
                что большая часть имеющихся в настоящее время переводов тантрийских текстов являются
                скорее не переводами, а свободными авторскими комментариями переводчиков, причем
                своеобразно трактуемые термины нередко не приводятся ни в тибетском, ни в
                санскритском варианте. Так, Г. В. Гюнтер излагает содержание тантрийских текстов
                терминами современной экзистенциальной философии; А. Говинда ограничивается
                приведением санскритских и тибетских терминов лишь для очень ограниченного числа
                тантрийских "коренных" понятий, переводя или разъясняя содержание текстов
                описательными английскими словами либо фразами; Эвантс-Вентс, публикуя переводы свои
                и лам, нередко комментирует их в духе брахманских и ведантийских школ и т.д. и т.п.
                А между тем, по моему глубокому убеждению, буддийская Тантра представляет явление
                исключительное, неописуемое никакими другими терминами, кроме тантрийских.
                Задачей же переводчика является найти в своем родном языке слова, вполне адекватно
                передающие смысл тантрийских терминов, и притом такие слова, которые не вызывали бы
                никаких небуддийских философских ассоциаций, не привязывали бы понимание читателя к
                какой-либо известной ему философской системе. В качестве примера сходного подхода к
                переводу тантрийских текстов можно указать на работы Ф. Д. Лессинга и А. Ваймана
                [13] и Дж. Хопкинса.
            </p>
            <p class="font-semibold">
                Словами, с помощью которых можно выполнить эту задачу, являются простые русские
                слова.
            </p>
            <p>
                К сожалению, на данном этапе изучения Тантры это не всегда оказывается возможным, и
                нередко в тексте перевода приходится оставлять термины в их санскритской либо
                тибетской форме, либо даже употреблять слова, которые уже применялись как термины в
                какой-либо философской системе. Но в таких случаях я старался в своих комментариях
                дать интерпретацию этих слов по буддийским оригинальным источникам, предлагая текст
                и его перевод. Так, само слово "тантра" осталось непереведенным, поскольку оно
                включает такую гамму понятий, всю сумму которых одним словом современного русского
                языка не передать. При объяснении же этого термина в комментарии по Лобсан Балдан
                Чойпэлу и Суматиратне [1] я употребил слово "длительность", и, чтобы у читателя не
                возникло ассоциаций с философией А. Бергсона<a href="#citation-03" id="ref-03"
                                                               class="citation-mark"><sup>03</sup></a>,
                дал объяснение длительности в соответствии с тем, как она
                трактуется в сочинениях самих буддистов. В своем коренном значении термин "тантра"
                действительно означает длительность, однако переводить этот термин именно этим
                словом было бы, по крайней мере, преждевременно, поскольку эта длительность, кроме
                указанного в комментарии значения, имеет также значение традиционности, то есть
                традиционно непрерывной передачи тантрийских знаний (метода) от учителя к ученику и
                т.д. Для примера возьмем трактат тантриста Дхармакирти <em>Сантaнaнтарасиддхи</em>
                (санскр. Santānāntara siddhi)<a href="#citation-04" id="ref-04"
                                                class="citation-mark"><sup>04</sup></a>. В тибетском
                варианте
                он известен как rGyud gzhan sgrub pa. Казалось бы, судя по тибетскому варианту, в
                оригинале он должен бы быть <em>Тантрaнтарасиддхи</em>. Ф. И. Щербатской даже
                высказывает
                предположение, что rgyud здесь стоит ошибочно вместо rgyun. Однако монгольские
                переводчики дают <em>Сантaнaнтарасиддхи</em> именно под <em>rGyud gzhan sgrub
                    pa</em> (см.
                раздел VII "Источника Мудрецов", л.2). Очевидно, в данном случае тибетские
                переводчики по известным им соображениям сочли необходимым передать значение
                длительности словом rgyud [3].
            </p>
            <p>
                Термин rdo rje [здесь и далее тибетская транслитерация дана по международной системе
                Т. Уайли — <em>прим. ред.</em>], санскр. vajra, также остался непереведенным (как и
                в
                монгольском) и по тем же соображениям. Я даю его в современном произношении «ваджр»
                из соображений удобства.
            </p>
            <p>
                Термин chos, dharma в тех случаях, когда он означает не "Учение" или не только
                "Учение", я также оставляю без перевода в его санскритской форме. И так далее,
                особенно что касается терминов уже привычных русскому уху.
            </p>
            <p>
                Имена тантрийских идамов даются мною в основном в их санскритской форме, поскольку в
                буддологической литературе это стало уже прочно сложившейся традицией.
            </p>
            <p>
                Вообще, тибетские и монгольские переводчики при переводе тантрийских текстов
                сталкивались, по-видимому, с той же задачей, перед какой стоим сейчас мы.
            </p>
            <p>
                Так, известно, что в Тибете было три периода перевода буддийских текстов с санскрита
                на тибетский (несколько иная периодизация дана у Ю. Н. Рериха; см. "Тибетский язык"
                [2, стр. 26], однако она касается развития письменного тибетского языка вообще). На
                первом этапе имена собственные и основные термины в тибетских переводных с санскрита
                сочинениях давались в их санскритской форме в тибетской транслитерации: A mi tha bha
                (совр. ’Od dpag med), dha rma (совр. chos) и т.д. Отметим, что среди последователей
                «старых» школ тексты, изобилующие санскритизмами, весьма популярны до сих пор.
            </p>
            <p>
                На следующем этапе, который длился почти 200 лет, с VII по IX вв, шло как бы
                привыкание ума тибетцев к восприятию всего содержания, вкладываемого в тот или иной
                термин, оставшийся непереведенным.
            </p>
            <p>
                И, наконец, в IX веке в правление царя Ралпачана стала возможной реформа языка,
                когда все санскритские термины, равно как имена собственные, стали передаваться
                тибетскими словами<a href="#citation-05" id="ref-05"
                                     class="citation-mark"><sup>05</sup></a>.
            </p>
            <p>
                В монгольской буддийской переводческой и комментаторской традиции явно
                прослеживается только первый и второй из указанных этапов. Возможно, в силу
                внутренних законов развития и функционирования монгольской речи иной вариант для нее
                и неприемлем. Возможно, этому были и другие причины. Во всяком случае, уже к концу
                первой четверти нашего столетия монголоязычная тантрийская буддийская ученая
                традиция прекратила свое существование за редчайшими исключениями, и в настоящее
                время известна лишь монголоязычная тантрийская буддийская схоластическая традиция.
                Современные монгольские и бурятские ламы, особенно тантрического направления, явно
                предпочитают монгольским текстам тибетские.
            </p>
            <p>
                Тибетские и монгольские ученые в тантрийских текстах находили до шести и более
                заложенных в них смыслов (см. ниже — текст перевода [Четвертое — украшение четырех
                методов — буквальный смысл, общий смысл, скрытый смысл, мистический смысл…])
            </p>
            <p>
                Для теории и практики перевода тантрийских текстов на русский язык существенное
                значение имеют три:
            </p>
            <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
                <li class="my-2">yig don - буквальный смысл,</li>
                <li class="my-2">tshig don - словесный смысл,</li>
                <li class="my-2">nges don - истинный смысл.</li>
            </ol>
            <p>
                При переводе буквальному смыслу соответствует буквальный перевод. Если буквальный
                перевод вполне понятен, и на уровне фразы полностью передает смысловую нагрузку
                оригинала, то этим самым он передает словесный смысл. Истинный смысл постигается
                через практику, и если переводной текст может служить руководством к действию, то
                можно считать, что в этом переводном тексте заложена также возможность реализации
                истинного смысла.
            </p>
            <p>
                С задачей вкладывания в буквальный перевод тройного (четверичного и т.д.) смысла
                тибетские переводчики справились блестяще, поскольку они обладали совершенным
                знанием предмета и, в течение длительного времени обучаясь в Индии, впитывали в себя
                живую традицию.
            </p>
            <p>
                Для нас же, буддологов, трудность состоит прежде всего в том, что живая традиция нам
                практически мало доступна или не доступна вовсе (это, разумеется, не относится к
                ученым буддистам, занимающимся научной буддологией). В буддологическую же литературу
                вкралась масса ошибок и неточностей из-за того, что некоторые авторы ложно считали
                буддийскую тантру производной от индуистских тантр и нередко «домысливали» значение
                малопонятных для них буддийских терминов, опираясь на их значение в более доступных
                и лучше изученных брахманских системах, где эти термины употреблялись в ином
                контексте и в иных значениях, а это затемняло и искажало смысл рассматриваемых
                буддийских текстов [5: 316 – 317]. Так, например, Юм (тиб. yum), в буквальном
                переводе «мать», супруга тантрийского идама, в работах по буддизму сплошь и рядом
                идентифицируется с <em>шакти</em> (санскр. śakti) шиваитских тантр, что справедливо
                возмущает А. Говинду [там же: 319]. Между тем ни в одном из известных мне
                тантрийских текстов оснований для такого сопоставления не имеется. Скорее, Юм —
                нечто противоположное Шакти, так как энергия Шакти направлена на созидание миров, то
                есть страдания, активность Юм — это активность нирванизации, успокоения страдания, а
                сама Юм — не что иное, как антропоморфный символ <em>шуньи</em>, или интуиции, <em>праджни</em>,
                за которой признается определенная запредельная (pha rol tu phyin pa) реальность.
                Само слово «шакти» (тиб. shak ti) в буддийских тантрийских текстах встречается, но
                лишь в одном значении — «пика или копье определенной формы» [1: VII, 1037].
            </p>
            <p>
                Чтобы избежать подобных ошибок, я в своем переводе старался опираться только на
                тибетские тексты, на трактовку того или иного термина самими буддистами и по мере
                возможности не привлекать европейских переводов, даже хороших, так как основой
                настоящей работы является разработка тантрийской терминологии для перевода именно на
                русский язык, и те задачи, которые я перед собою при этом ставлю, европейскими
                переводчиками начали ставиться только в самое последнее время.
            </p>
            <p>
                Для того, чтобы передать дух текста, я предпринял попытку применить опыт тибетских
                переводчиков. И в тех случаях, когда буквальный перевод в простых русских словах был
                понятен и на уровне фразы, то есть отражал и словесный смысл, я давал именно
                буквальный перевод (однако, необходимость в пояснительных словах которые я заключаю
                в обычные скобки, постоянно ощущалась).
            </p>
            <p>
                В некоторых случаях буквальный перевод не нес бы никакой информации для читателя,
                знакомого с переводной и комментаторской литературой по Тантре на европейских языках
                и мало знакомого с оригинальной тибетской тантрийской литературой, ведь, к
                сожалению, печатных работ русских буддологов по Тантре очень мало. В этих случаях я
                давал перевод, соответствующий словесному смыслу, обосновывая достоверность такого
                перевода в комментариях и в комментариях же приводя перевод буквальный, если в этом
                существовала необходимость.
            </p>
            <p>
                Так, буквальный перевод терминов bdag gi gzhi и gzhan gyi gzhi не представляет
                затруднений, но в таком переводе смысл фразы будет не выявлен, и задача переводчика
                останется невыполненной (см. ниже — перевод текста «созерцать божество в себе»,
                прим. 35). То же в случаях, упомянутых в прим. 37, 38, 51. От этого правила я
                отступал, когда сам термин требует вопрошания и последующего разъяснения, либо когда
                разъяснение смысла, заложенного в термине, традиционно существовало лишь в устной
                передаче (<em>карнатантра</em>) — в этих случаях я даю лишь буквальный перевод или
                же оставляю термин непереведенным, как это делали монгольские переводчики. При ином
                подходе был бы нарушен дух текста, что представляется недопустимым.
            </p>
            <p>
                Когда же по мере дальнейшей разработки тантрийской терминологии при буквальном
                переводе на русский язык того или иного термина мы станем учитывать ("доставать из
                памяти") и его словесный смысл, буквальный перевод станет во всех случаях
                правомерен.
            </p>
            <p>
                Перевод некоторых общеизвестных терминов, которые я интерпретирую иначе, чем это
                принято, также требует пояснения.
            </p>
            <p>
                В качестве примера возьмем четыре безмерных:
            </p>
            <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
                <li class="my-2">byams pa - доброта (букв. дружественность, дружелюбие),</li>
                <li class="my-2">snying rje - милосердие,</li>
                <li class="my-2">dga’ ba - радость,</li>
                <li class="my-2">btang snyoms - равное отношение ко всем.</li>
            </ol>
            <p>
                Простые слова, отражающие букву и дух тибетского термина, взяты мною здесь потому,
                что они не вызывают уводящих в сторону ассоциаций, и дело заключается в том, чтобы
                наполнить их соответствующим содержанием. Формула для созерцания, с помощью которой
                эти состояния сознания достигаются, приведена в прим. 34.
            </p>
            <p>
                Термин snying rje, который обычно переводят как «сострадание», здесь передан словом
                «милосердие», и вот почему. Согласно буддийской теории, сострадание (snying brtse
                ba, kṛpā), то есть способность сочувствовать страданию ближнего, является врожденным
                свойством человеческой личности. Со временем оно либо развивается, либо сходит почти
                на нет. На определенной ступени развития сострадание превращается в нечто иное — в
                четыре последовательных активных состояния сознания, направленных на оказание
                действенной помощи живым существам.
            </p>
            <p>
                Хотелось бы обратить внимание читателя еще на один принципиальный момент. До тех
                пор, пока термины <em>шераб</em> (shes rab, prajñā) и <em>еше</em> (ye shes, jñāna)
                будут переводиться на европейские языки одним словом — русским ли «мудрость»,
                английским ли «wisdom», смысл буддийской теории и практики, парамитаяны и тантры
                передан не будет. Для выявления семантики указанных терминов необходимо учитывать
                значение термина thabs, upāya. <em>Упая</em> — это метод, движение мысли по заданной
                программе от состояния запутанности к абсолютной чистоте в потоке сознания. <em>Праджня</em>
                — это интуиция, эмоциональная точность, мечом которой отсекаются заблуждения разума,
                идущего путем метода. Метод — вещь абсолютная, он направлен от Нирваны (Учителя) к
                Сансаре (ученику), и на пути совершенствования является для ученика рядом вех,
                указующим правильное направление для разума ищущего. Интуиция,
                эмоционально-мистическая интуиция, есть движение души затерянного в сансаре индивида
                к духовности Учителя-Будды или к Нирване. Результатом содружества или
                недвойственности метода и интуиции является <em>джняна</em> — мудрость
                недвойственности метода (блаженства) и интуиции (<em>шуньи</em>). Таким образом,
                термин shes rab следует передавать словом «интуиция», и только термин ye shes может
                быть передан словом «мудрость».
            </p>
            <p>
                Свой комментарий я не рискнул бы назвать филологическим, скорее он может быть назван
                терминологическим, так как основными задачами, которые я перед собою ставил при
                составлении комментария в виде примечаний, были обоснование предлагаемого перевода,
                разъяснение словесного смысла буквального перевода, если в этом ощущалась
                необходимость, а также объяснение большей части терминов, оставленных мною в их
                санскритской или тибетской форме. Привлечение во многих местах санскритских
                эквивалентов к приводимым в скобках тибетским терминам служит для облегчения
                восприятия перевода специалистами-буддологами, которые в своей практической
                деятельности чаще пользуются санскритом, нежели тибетским языком.
            </p>
            <p>
                Конечно, предлагаемый перевод далек от совершенства, ибо попытка изложить на одном
                из европейских языков, на русском, смысл санскритской терминологии в системном виде,
                как она дана в краткой энциклопедии "Источник Мудрецов", предпринята впервые в
                истории буддологии; перевод же тантрийских текстов и даже просто понимание их
                требует длительного "привыкания" к их специфике, что создает определенные трудности
                и для читателя, и для переводчика. В связи с этим должен отметить, что выполнение
                данной работы оказалось возможным в значительной мере благодаря тому, что я имел
                возможность пользоваться рукописными работами моего коренного Учителя Б. Д.
                Дандарона, устными разъяснениями лам, в особенности моего Учителя Ц. Гатавона, а
                также помощью друзей.
            </p>

