                <p class="mb-3" id="citation-12"><sup>12</sup> Впервые в нашем тексте мы встретились
                    с термином «тантра» как таковым. Встречавшееся же ранее guhya mantrayāna я
                    переводил как «колесница тайной тантры», во-первых, потому что такой перевод
