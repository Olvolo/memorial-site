                <strong>В. Н.</strong> Еще Платон сделал гениальное прозрение, сказав, что кроме
                мира грубых явлений существует мир идей, и он более реален и ценен. Если добро для
                нас является реальностью, для нас является реальностью жизнь сознания, жизнь тела,
                это только тот храм, в котором живет сознание. Если принять материалистическое
                мировоззрение, то это приведет только к всеобщему смертоубийству, к истреблению рода
                человеческого. Современный потенциал ядерного оружия, по крайней мере, позволяет
                пятьдесят раз уничтожить наш хрупкий мир. И только мир идей является тем двигателем,
                который не позволяет человечеству самоуничтожиться.
            </p>
