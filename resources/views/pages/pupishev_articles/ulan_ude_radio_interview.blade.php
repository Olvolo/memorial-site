            <p>
                <strong>В. Н.</strong> В совершенствующие пути включают три: путь Хинаяны, или узкой
                колесницы, или, иначе говоря, героический путь, когда человек один, героически
                преодолевая все препятствия, стремится к совершенству. На этом пути изучают, прежде
                всего, неизбежность смерти и истину о страдании, о том, что страдание можно
                преодолеть и обрести Нирвану, т. е. поток абсолютно чистых состояний сознания.
