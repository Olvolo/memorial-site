                совершенствования идут сообща. Если первый путь предполагает полный отказ от мира,
                то второй путь доступен и тем, кто живет в миру. Поскольку в миру соблазнов сколько
                угодно, то второй путь, очевидно, сложнее. И третий путь — это Ваджраяна, или
                Тантра. Это путь йогического совершенствования, который предполагает наиболее
                быстрые достижения, реализацию уже в этой жизни, а не в течение нескольких жизней,
                как в предыдущих двух путях. Однако нужно учесть, что это разделение условно.
                Скорее, можно было бы сказать, что это три ступени развития каждого человека. Но
                здесь очень важно то, что, не пройдя первой ступени, т. е. не обуздав себя,
                невозможно идти дальше, и поэтому нам Бидия Дандарович говорил: «Вы никогда не
                должны падать ниже второй парамиты». Вторая парамита — это парамита нравственности,
                которая означает отказ от дурных деяний, освоение благого учения и деяния на благо
                всех живых. Если этого нет, никакое движение невозможно.
            </p>
            <p>
                <strong>С. А.</strong> Как Вы считаете, мы идем путем совершенствования или
