                            <li class="my-4">
                                <p>
                                    <strong><em>I. 1. Дхармакая</em></strong> (Дхарма - здесь
                                    Нирвана,
                                    кая - тело) - космическое тело Будды, Свет (ясный Свет),
                                    являющийся основой
                                    всего, т.е. природа Будды, являющаяся истинной природой всех
                                    живых существ,
                                    которую знают только пробуждённые (просветлённые). Обычным живым
                                    существам
                                    мешает познать свою собственную природу Будды <em>неведение</em>
                                    , поэтому
                                    состояние обычных живых существ сравнимо со сном. Просветлённые
                                    от этого сна
                                    пробуждаются, а потому они Пробуждённые (санскр. Будды).
                                    Сущность Дхармакаи
                                    - ясный свет, реальность её - шунья. Предикат Дхармакаи - это
                                    сострадание.
                                </p>
                                <p>Значение сова "дхарма" - носитель своего признака. Сущность
                                    каждой дхармы - трансцендентна, непознаваема; только "признаки",
                                    или
                                    проявления дхарм образуют поток сознательной жизни; таким
                                    образом, элементы,
                                    на которые разлагается поток сознания, не являются дхармами, а
                                    "проявлениями
                                    дхарм", однако ради краткости часто употребляется термин
                                    "дхарма" вместо
                                    выражения "проявление дхарм": в таком случае "дхарма" можно
                                    передавать прямо
                                    словом "элемент".
                                </p>
                                <p>Среди элементов и их носителей упоминается один,
                                    который в метафизике и в теории спасения занимает первое место,
                                    это -
                                    элемент "успокоения", "покоя", т.е. нирвана..."Так как конечной
                                    целью бытия
                                    является достижение состояния вечного покоя, то этот элемент
                                    среди всех
                                    дхарм является лучшей"..."а поэтому дхарма иногда является
                                    синонимом
                                    "нирваны". "Нирвана" употребляется иногда в смысле "Пустоты" или
                                    "Абсолютного", т.е. истинно реального, в виду чего и это
                                    последнее иногда
                                    называется "дхарма". (О. О. Розенберг "Введение в изучение
                                    Буддизма" издание
                                    Петроградского университета, стр. 85-86).
                                </p>
                            <li class="my-4">
                                <p>
                                    <strong>2.</strong> "Сам Будда уже определил, что мир - это лишь
                                    то, что
                                    представляется как мир в нашем сознании, не вдаваясь в проблему
                                    объективной реальности" (Лама Анагарика Говинда, стр. 148).
                                    Мироздание рассматривается а буддизме в психологическом аспекте,
                                    а потому эмоциональные свойства, такие как сострадание вполне
                                    естественны. Дхармакая и др. в иконографии изображается
                                    антропоморфно, например в виде сидящей со скрещенными ногами
                                    человеческой фигуры синего цвета и т.п. Принятые в иконографии
                                    образы изображают, как правило, не каких-то определённых святых,
                                    не личности, а уровни и аспекты мироздания, зашифрованные в
                                    антропоморфном изображении. Все канонические характеристики
                                    буддийских икон являются символами аспектов реальности.
                                </p>
                                <p>
                                    Каждое живое существо - это "вход во вселенную".
                                </p>
                                <p>
                                    "Различия так называемых материальных миров, переживаемых
                                    сознательными существами, объясняются действием силы
                                    <strong><em>"карма"</em></strong>, располагающей элементы,
                                    комбинация которых составляет данную личность вместе с
                                    переживаемым ею материальным миром; внешний материальный мир,
                                    таким образом, является составным элементом личности". Теория
                                    мгновенности "состоит в предположении, что каждый элемент длится
                                    только один момент: сознательная личность является,
                                    следовательно, цепью беспрестанно сменяющихся мгновенных
                                    комбинаций, состоящих из появляющихся на мгновение единичных
                                    элементов".
                                </p>
                                <p>
                                    "Согласно теории мгновенности, смена в составе содержания
                                    сознания происходит настолько быстро, что самый процесс перехода
                                    к новому содержанию не поддаётся наблюдению. Момент ("кшана"),
                                    не будучи нулём, является столь минимальною частицею времени,
                                    что он непосредственному восприятию не доступен"..."Теория
                                    мгновенности заключается, таким образом, в условном сведении
                                    потока сознания и его содержания на цепь одинаковых бесконечно
                                    малых частиц времени". (О. О. Розенберг "Введение в изучение
                                    Буддизма" стр. 101-102).
                                </p>
                            </li>
                        </ol>
                    </li>
                    <li class="my-4">
                        <p>
                            <strong><em>II.</em></strong> Из сострадания ради помощи живым
                            существам Дхармакая (непроявленное) излучает из себя пять лучей света,
                            или<strong><em>Самбхогакаю</em></strong> - тело блаженства, или тело
                            света Будды.
                        </p>
                        <p>
                            Различают два вида Самбхогакаи - это Сва-Самбхогакая и Пара-Самбхогакая.
                            "Первый тип - это тело чистых форм, Рупакая (Рупа - форма, кая - тело),
                            которое совершенно чисто, универсально, безгранично, обладает истинными
                            атрибутами, через которые выражается неизмеримость достоинств и знания"
                            (Лама Анагарика Говинда стр. 395), накопленных буддами за время
                            бесчисленных кальп. "Оно будет продолжаться до конца времён, постоянно
                            переживая внутри себя блаженство и несомненность Дхармакаи" (там же).
                        </p>
                    </li>
                    <li class="my-4">
                        <p>
                            <strong><em>III.</em></strong> Пара-Самбхогакая - это тело, вызывающее
                            религиозный восторг в других, видимое посвящёнными в созерцании. Такова
                            реализация Дхармакаи внутри человеческого ума. Пять лучей света
                            Пара-Самбхогакаи - это пять мудростей. Они направлены на искоренение
                            пяти клеш, активность которых формирует карму живых существ. эти пять
                            мудростей называют пятью Дхьяни-буддами (Дхьяна - созерцание), т.е.
                            буддами, видимыми только в созерцании с применением особых методов, на
                            реализацию которых даётся посвящение Учителем, т.е. просветлённым
                            существом.
                        </p>
                        <p>
                            Этот уровень Нацог Рандол определяет как "чистоту (сок махабхут - dvangs
                            ma)" (В. П. "Т. М.: яз., т.п." стр. 73).
                        </p>
                    </li>
                    <li class="my-4">
                        <p>
                            <strong><em>IV.</em></strong> Клеша и мудрость - это одна дхарма. Это
                            значит, что у просветлённого существа действуют мудрости, а у обычного
                            сансарного существа они проявляются как клеши, поскольку загрязнены
                            неведением. Клеши - это эмоциональные аффекты неведения. Клеши есть
                            результат появления органического неведения - веры в существование "Я",
                            что влечёт за собой разделение реальности на "Я№ - не-"Я", т.е. с этого
                            момента появляется двойственность восприятия реальности, что и является
                            источником Сансары. (Всё описанное до этого - Нирвана). С этой точки
                            разворачивается время-пространство как параметр сансарного восприятия
                            реальности, отсюда происходит проявление множественности.
                        </p>
                        <p>
                            <strong><em>Органическое неведение</em></strong> - это неузнавание
                            своего собственного лица, своей истинной природы Будды, что ведёт к
                            дезориентации в мире, омрачённости и запутанности. Восприятие себя
                            отдельно от окружающего мира, оппозиция "Я - внешний мир", вызывает
                            реакцию этого "Я" на этот окружающий мир. Реакции эти эмоциональны, и
                            они могут быть трёх видов: притягивание, отталкивание и
                            индифферентность. <em>Притягивание</em> - это положительная реакция "Я"
                            на полезные и привлекательные для него объекты, - это <strong><em>клеша
                                    страсть</em></strong>, т.е. стремление завладеть нужными для "Я"
                            объектами. <strong><em>Отталкивание</em></strong> - это отрицательная
                            реакция "Я" на угрожающие или вредные для него объекты, - это
                            <strong><em>клеша гнев</em></strong>, т.е. стремление избавиться от
                            неприятных объектов. <strong><em>Индифферентность</em></strong> - это
                            безразличие к тому, что не имеет непосредственного отношения к этому
                            "Я", отсюда <strong><em>клеша омрачённость, неведение</em></strong>.
                            Клеши называют эмоциональными аффектами.
                        </p>
                        <p>
                            Эти три основные клеши (неведение, страсть, гнев) можно разложить на
                            пять: неведение, омрачённость, дезориентация в реальности в результате
                            веры в "Я" ведёт к приданию слишком большого значения этому "Я",
                            гипертрофии своего "Я", что есть <strong><em>клеша гордость (и
                                    жадность)</em></strong>.
                        </p>
                        <p>
                            Страсть ведёт к желанию обладать не только своим, но и чужим, что бывает
                            подчас невозможно. Отсюда <strong><em>клеша зависть</em></strong>.
                        </p>
                        <ul class="list-none pl-5 space-y-1 ml-8 my-4">
                            <li class="no-arrow">
                                органическое неведение ("Я" - "не Я")
                                <ul>
                                    <li>
                                        неведение-омрачённость
                                        <ul>
                                            <li>неведение-омраченность +</li>
                                            <li>гордость</li>
                                        </ul>
                                    </li>
                                    <li>
                                        страсть
                                        <ul>
                                            <li>страсть +</li>
                                            <li>зависть</li>
                                        </ul>
                                    </li>
                                    <li>
                                        гнев
                                        <ul>
