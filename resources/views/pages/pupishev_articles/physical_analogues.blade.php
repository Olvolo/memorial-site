                вошли в тибетскую медицину из китайской медицины, однако рассматриваются они всего
                лишь как частный случай проявления махабхут и не носят подобно махабхутам
                универсального характера
            </p>
            <p>
                Областями проявления махабхут называют также пять плотных и шесть полых органов, и
                соотношение здесь следующее:
            </p>
            <ul class="list-none pl-5 space-y-1 ml-8">
                <li>
                    1) <em>земля</em> — селезенка, желудок,
                </li>
                <li>
                    2) <em>вода</em> — почки, мочевой пузырь, семенные пузырьки, матка,
                </li>
                <li>
                    3) <em>огонь</em> — сердце, тонкий кишечник,
                </li>
                <li>
                    4) <em>железо</em> — легкие, толстый кишечник,
                </li>
                <li>
                    5) <em>дерево</em> — печень, желчный пузырь.
                </li>
            </ul>
            <p>
                Функции указанных тканей и органов при тщательном изучении также могут помочь нам в
                исследовании особенностей проявления махабхут в теле человека.
            </p>
            <p>
                6. Махабхуты не могут быть непосредственно зарегистрированы приборами, поскольку
                «то, что мы называем материей, <...> сводится на некоторую <em>часть осязательных
                    явлений</em> <...> Так называемое материальное входит в область чувственного,
                чувственное же вовсе не исчерпывается материальным» [7: 163]. Следовательно, наше
                постижение сущности махабхут может идти по пути исследования всех известных нам
                проявлений махабхут в природе и в теле человека, важнейшие из которых, с точки
                зрения медицины, упомянуты выше.
            </p>
            <p>
                Однако это вовсе не означает, что постичь физический смысл махабхут мы не в
                состоянии. Ведь мыслительные процессы также входят в область <em>чувственного</em>,
                представляя идеальную сторону чувственного, и рядом математических уравнений природа
                махабхут, вероятно, может быть описана. Но при такого рода моделировании обязательно
                следует учитывать, что махабхуты всегда в каждый момент времени «рождаются вместе»,
                хотя, как правило, в каждом исследуемом объекте макро- или микрофизики сила одного
                или двух из них преобладает. Изучение свойств <em>вакуума</em> — свойств как
                материальных, так и идеальных, здесь совершенно неизбежно: именно вакуум,
                <em>шунья</em> (санскр. śūnyatā, тиб. stong pa nyid) является источником,
                порождающим махабхуты.
            </p>
            <p>
                Теперь обратимся к нашей схеме (рис. 2).
            </p>
            <p>
                На первом уровне приводимой схемы <em>Единое</em> (<em>дхармакая</em>) может быть
                сопоставлено с «абсолютным ничто» («абсолютным вакуумом») современной физики в том
                смысле, что оно не является объектом изучения физики вследствие отсутствия
                субъектно-объектных отношений. Категории абсолютного различения и понимания, а также
                сострадания, являющиеся предикатами дхармакаи, объектами изучения физики также не
                являются.
            </p>
            <p>
                <em>Рупакая</em> может быть сопоставлена с первичным торсионным полем в том смысле,
                что активный её аспект <em>дхармадхату</em>(источник <em>дхарм</em>) служит как бы
                мостом между непроявленным (<em>шуньей</em>) и проявленным (<em>сансарой</em>); из
                дхармадхату <em>дхармы</em> (элементы бытия) на мгновение возникают в виде
                <em>вихря</em> и вновь возвращаются в свой источник затухая.
            </p>
            <p>
                <em>Пять чистых махабхут</em> можно сопоставить с физическим вакуумом в том смысле,
                что <em>махабхуты</em> как основа бытия рассматриваются на этом уровне как <em>потенции</em>
                проявления: когда <em>земля, вода, огонь, ветер, пространство</em> уже обрели <em>бытие</em>,
                но еще не актуализировались как фундаментальные свойства мироздания, когда их
                функциональная активность пребывает лишь как некая <em>потенциальность</em> — «огонь
                не горячий, но может сжечь» (Нацог-Рандол [1]).
            </p>
            <p>
                <em>Пять осадочных махабхут</em> рассматриваются как <em>пять фундаментальных
                    свойств бытия</em>, и в этом смысле их можно, учитывая приписываемые махабхутам
                в текстах свойства, соотнести с пятью полями современной физики (попытку соотнесения
                четырех махабхут античной традиции с четырьмя полями предпринял еще в 1974 г.
                известный физик-ядерщик академик М.А. Марков). <em>Сознание</em> же появляется здесь
                потому, что никакое сознание в феноменальном мире не может существовать без своего
                носителя, своего «коня» — махабхуты <em>ветер</em>.
            </p>
            <p>
                <em>Проявленные махабхуты</em> (чистота осадка и осадок чистоты) предстают как
                воспринимаемый нами мир (мир сосуда) и обитающие в нем живые существа (мир сока):
                всё это представляет собой, с одной стороны, дхату, или области проявления махабхут,
                с другой стороны, <em>сознательную сторону</em> бытия, а по сути — <em>шунью</em>,
                отсутствие конкретной сущности, или реальной <em>непустой</em>основы существования у
                чего бы то ни было — и у объектов, и у сознания. Как учил Будда Шакьямуни, вечное —
                это одна противоположность, а невечное — это другая противоположность. То, что лежит
                между этими двумя противоположностями, не подлежит исследованию, неизреченно,
                непроявленно, непостижимо и не имеет длительности. Это и есть Срединный путь,
                который характеризуется подлинным познанием и различением дхарм.
            </p>
