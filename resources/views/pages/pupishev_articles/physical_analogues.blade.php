                            </li>
                        </ul>
                        <p>
                            Если зеркало покрыто слоем грязи,, оно не отражает свет. Смыв с зеркала
                            слой грязи, мы обнаружим, что зеркало отражает свет. По аналогии с этим,
                            смыв грязь клеш со своего ума, мы обнаружим, что природа его - это
                            мудрость, т.е. пять мудростей, или пять лучей света (пяти цветов), что
                            есть реализация Дхармакаи внутри нашего ума. Когда присутствуют клеши,
                            наш ум - в сансаре, когда клеши смыты, реализуется Нирвана. Нирвана -
                            это не какое-то место вне сансары. Это наш ум - он может воспринимать
                            реальность либо как сансару (когда активны клеши), либо как Нирвану
                            (когда клеши смыты). Отсюда говорят о тождестве Сансары и Нирваны,
                            поскольку разница между ними - это лишь разница в восприятии. Клеши
                            (неведение+гордость, страсть+зависть, гнев), действующие на уровне ума,
                            называют "три яда", которые, отравляя наш ум, заставляют нас
                            воспринимать реальность как сансару, где мы страдаем, рождаясь, болея и
                            умирая множество раз.
                        </p>
                        <p>
                            Внешне этот уровень проявляется как пять осадочных махабхут, или <em>осадок</em>
                            (snyigs ma) (В. П. "Т. М...." стр. 73) - это энергетика мироздания, пять
                            его фундаментальных свойств.
                        </p>
                        <p>
                            <strong><em>Тело</em></strong> человека (всех живых существ) формируется
                            как результат его кармы, которая порождается активностью клеш,
                            действующих на уровне ума. Именно клеши, такие как неведение, страсть и
                            т.д. приводят ум живого существа после окончания его предыдущей жизни к
                            поиску возможности родиться, получить тело. Человеческое тело обретают
                            лишь те, кто кармически готов к этому, т.е. в уме такого живого существа
                            отпечатались установки, дающие возможность родиться человеком (см. тему
                            "шесть классов живых существ").
                        </p>
                        <p>
                            Пять мудростей (пять лучей света), а соответственно и пять клеш,
                            соотносятся с пятью великими элементами, или махабхутами.
                        </p>
                        <p>
                            Мудрость зерцалоподобная, представленная Дхьяни-буддой Вайрочаной
                            (белый) - в качестве клеши здесь работает неведение - соответствует
                            <strong><em>пространству</em></strong>. (В некоторых описаниях здесь
                            вода).
                        </p>
                        <p>
                            Мудрость познания каждой вещи в отдельности и всех в совокупности,
                            представленная Дхьяни-буддой Амитабхой (красный) - клеша страсть -
                            соответствует <strong><em>огню</em></strong>.
                        </p>
                        <p>
                            Мудрость дхармового пространства (пространства как шуньи, из которой
                            возникает все дхармы), представленная Дхьяни-буддой Акшобхьей (синий) -
                            клеша гнев - соответствует <strong><em>воде</em></strong> (В некоторых
                            описаниях здесь пространство). </p>
                        <p>
                            Мудрость равного содержания, подтверждающая тождество разума и шуньи,
                            представленная Дхьяни-буддой Ратнасамбхавой (жёлтый) - клеша гордость,
                            жадность - соответствует <strong><em>земле</em></strong>.
                        </p>
                        <p>
                            Мудрость, творящая карму, ведущую к Нирване, представленная
                            Дхьяни-буддой Амогхасиддхи (зелёный) - клеша зависть, ревность -
                            соответствует <strong><em>ветру</em></strong>.
                        </p>
                        <p>
                            Свернув пять клеш в три, получим неведение, страсть, гнев, т.е. три яда,
                            результирующие при формировании комплекса ум-тело в три основы (три
                            виновника, тиб. "ньес-па"): ветер, желчь и слизь.
                        </p>
                        <p>
                            <strong><em>Слизь</em></strong> - это сочетание махабхут земля и вода,
                            следовательно порождается она клешами гордость, жадность и неведение.
                        </p>
                        <p>
                            <strong><em>Желчь</em></strong> - это огонь, порождается гневом.
                        </p>
                        <p>
                            <strong><em>Ветер</em></strong> - это воздух, порождается сочетанием
                            страсть и зависть, ревность.
                        </p>
                        <p></p>
                    </li>
                    <li class="my-4">
                        <p>
                            <strong><em>V.</em></strong> Действующие на уровне сознания три
                            виновника "определяют существование физического мира, форм без материи и
                            мира без форм - мира высших духов" (В. П.), т.е. отсюда идёт развёртка
                            сансарного мира, - мира тонкой и плотной материи.
                        </p>
                        <p>
                            При формировании человеческого тела происходит проявление пяти
                            фундаментальных свойств в живых существах - по Нацог Рандолу это <em>осадок
                                чистоты</em> (dvangs ma'i snyigs ma) (В. П. "Т. М..." стр. 73). На
                            этом уровне в теле человека проявляются пять махабхут тела, пять органов
                            чувств, семь тканей - это субъективный мир - мир живых существ.
                        </p>
                        <p>
                            В объективном мире происходит субстанциональное проявление пяти
                            фундаментальных свойств мироздания - агрегатные состояния вещества, то,
                            что Нацог Рандол называет <em>чистота осадка</em> (snyigs ma'i dvangs
                            ma) (В. П. "Т. М..." стр. 73).
                        </p>
                        <p>
                            Из сострадания к живым существам Будда, просветлённое существо, может
                            пребывать в мире живых существ с целью проповеди. В этом случае он
                            рождается среди людей, но не по карме, а по собственному выбору, т.е. он
                            (она) обретает тело, в котором живые существа могут его видеть и
                            воспринимать. Это называется явленным телом Будды, или Нирманакаей
                        </p>
                        <p>
                            Итак, говорят о ТРИКАЕ (санскр.) - это три тела или три способа бытия
                            Будды (ср. с христианской Троицей) - Дхармакая, Самбхогакая и
                            Нирманакая. Символом Трикаи является колесо: ступица колеса - Дхармакая,
                            обод - Самбхогакая, а спицы - Нирманакая, поскольку на явленном уровне
                            наблюдается множественность.
                        </p>
                        <p>
                            <em>Примечания:</em> Дхармакаю можно сравнивать с вакуумом, только
                            объясняя и то и другое как пустоту, однако Дхармакая - это состояние
                            Будды, т.е. оно персонифицируется, тогда как вакуум, видимо, безличен,
                            он - внешний по отношению к живым существам. Европейская наука склонна
                            рассматривать мироздание, как некий резервуар, хотя и очень большой, где
                            что-то помещается или кто-то обитает. В Буддизме мироздание
                            рассматривается через субъекта, тогда как для европейца - это объект,
                            вместилище субъектов.
                        </p>
