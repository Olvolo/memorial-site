                бесконечное количество других элементов. Благодаря связующим свойствам элемента <em>вода</em>
                электрон удерживается около ядра, вращается вокруг него по определенной орбите, в
                результате чего образуется атом водорода. Само вращение электрона вокруг ядра есть
                проявление свойства элемента <em>воздух</em> (<em>ветер</em>), придающее электрону
                способность к
                движению, возникающему вместе с пространством и временем. Пространственная
                конфигурация элементов сначала образует атомы, затем, согласно
                причинно-следственному закону, атомы образуют молекулы; это уже грубая материя. В
                создании подобной грубой материи участвует элемент <em>земля</em>: элемент не
                твердый, но
                крепкий. От плотности материальных элементов в одном атоме зависит состояние
                вещества: жидкое, газообразное или твердое. Эту степень плотности, или концентрации,
                элементов регулирует элемент <em>земля</em>, созидающий прочность против разрушения
                и повинный в существовании газообразных, жидких и твердых тел.
            </p>
            <p>
                Противоположен элементу <em>земля</em> элемент <em>огонь</em>, который в изначальном
                состоянии не был горячим, но мог жечь, то есть разрушать. Через свойство разрушения
                <em>огонь</em>порождает энтропию. Все, ограниченное пространством и временем,
                возникшее или созданное, необходимо будет разрушено и придет к концу. Это разрушение
                реализуется элементом<em>огонь</em>.
            </p>
            <p>
                Элементы <em>ветер</em> и <em>огонь</em> проявляют силы, противоположные силам
                элементов <em>вода</em> и <em>земля</em>. В результате совокупности действия четырех
                элементов по закону кармы возникают три поля: мезонное (ядерное), электромагнитное и
                гравитационное. Нет в природе такого материального элемента или вещества, которое бы
                не подчинялось действию какого-либо из этих полей. Любое из этих полей создается
                взаимодействием сил двух «противоположных» элементов, например, силами <em>воды</em>
                — соединения и силою <em>ветра</em> — движения. Интенсивность элемента соединения
                (<em>вода</em>) тормозится элементом движения, поэтому электрон не падает на ядро, а
                вращается вокруг него. Участвующий в этом процессе элемент <em>земля</em>придает
                полю, например, мезонному (ядерному), крепость, сопротивление против силы
                разрушения» [8: 65 – 66].
            </p>
            <p>
                Из вышеизложенного вытекают следующие выводы.
            </p>
            <p>
                1. Пять махабхут как «великие проявления» некорректно было бы соотносить с
                какими-либо физическими элементами или полями непосредственно. Махабхуты,
                представляя основу существования и проявляясь в энергетических процессах
                относительного временного-пространственного созидания, становления и
                разрушения-гибели, могут быть выявлены по <em>следам</em>, или по проявлениям их
                активности, как об электроне судят по его<em>треку</em>. Это в полной мере относится
                и к человеческому организму: «Без совершения деяний не появится сознание (речь идет
                о деяниях прошлой жизни — <em>авт</em>.); без (стихии) <em>земли</em> не исполнится;
                без (стихии) <em>воды</em>не сочетается; без (стихии) <em>огня</em> не созреет; без
                (стихии) <em>воздуха</em> не получит развития; и без (стихии)<em>эфира</em> (в нашем
                переводе <em>пространства — авт</em>.) не установится пустых пространств (т. е.
                полостей тела — <em>авт</em>.) [9: 61].
            </p>
            <p>
                2. Следы активности махабхут — это проявление следующих их свойств (мы имеем в виду
                проявление свойств махабхут в живом организме):
            </p>
            <ul class="list-none pl-5 space-y-1 ml-8">
                <li>а) для <em>земли:</em> тяжесть, устойчивость, тупость, мягкость, умащенность,
                    сухость, твердость, связанность, успокаивает ветер (не махабхуту <em>ветер</em>,
                    а <em>ветер</em> как одно из трех начал (тиб. nyes pa) организма, формируемых
                    махабхутами наряду с <em>желчью</em>и <em>слизью</em>);
                </li>
                <li>б) для <em>воды:</em> растворимость, холодность, тяжесть, отупляемость,
                    умащающее свойство, упругость; свойства — увлажняющее, смягчающее, связующее,
                    успокаивает желчь;
                </li>
                <li>в) для <em>огня:</em> согревающий, обостряющий, сушащий, огрубляющий, легкий,
                    подвижный, доводящий до созревания, успокаивает слизь;
                </li>
                <li>г) для <em>ветра:</em> легкий, подвижный, холодный, огрубляющий, поглощающий
                    (тепло), сушащий, плотный, проникающий, колеблющий, успокаивает слизь и желчь
                    [6: 77].
                </li>
            </ul>
            <p>
                Кроме того, махабхуты, опять же попарно, как и в приведенном Б. Д. Дандароном
                описании отражения свойств махабхут в известных физических полях, формируют шесть
                основных вкусов: <em>сладкий — земля + вода; кислый — земля + огонь; соленый — вода
                    + огонь; горький — вода + ветер; острый (жгучий) — огонь + ветер; вяжущий —
                    земля + ветер</em>.
            </p>
            <p>
                Из приведенных выше характеристик (свойств) махабхут явствует, что <em>вода</em> и
                <em>ветер</em> обладают природой <em>холода</em>, <em>огонь</em> обладает природой
                <em>жара</em>, <em>земля</em> обладает <em>уравнивающей</em> природой. Интересно,
                что в книге современных тибетских авторов [4] лекарственное сырье классифицировано
                именно по этим категориям — <em>согревающее, охлаждающее</em> и
                <em>уравнивающее</em>.
            </p>
            <p>
                Первая попытка выявить природу жара и холода в пищевых продуктах была предпринята
                еще в 1980 г., в Новосибирске [10]. Продукты были классифицированы по предложенной
                Г.Л. Ленхобоевым схеме <em>теплое – холодное – нейтральное</em>. Исследовался
                характер поворота плоскости поляризации плоскополяризованного монохроматического
                излучения влево и вправо.
            </p>
            <p>
                При этом оказалось, что «из 34 измеренных продуктов» лишь «в 4-х случаях совпадения
                с традиционным делением не было» [там же: 12]. При этом отмечалось, что «фрукты, не
                растущие в Сибири, были привезены из Средней Азии, и срок их хранения и
                транспортировки установить не удалось... Любые процессы гниения, брожения и т.п.
                неизменно сопровождаются изменением результирующей оптической активности вещества»
                [там же: 14].
            </p>
            <p>
                3. Следы активности махабхут в организме человека наиболее четко представляются в
                <em>пульсации</em>, однако опять же не непосредственно, а через посредство трех
                основных начал (nyes pa), или функциональных регулирующих систем организма:
                систем<em>ветра</em>, <em>желчи</em> и <em>слизи</em>. Здесь <em>ветер</em> как
                функциональная система является производным махабхуты <em>ветер</em> и служит
                «конем» для сознания, отвечая за все процессы, связанные с расходованием всех видов
                воспринятой и накопленной организмом энергии.<em>Желчь</em> является производным
                махабхуты <em>огонь</em> и обеспечивает восприятие и накопление организмом всех
                видов энергии. <em>Слизь</em>является производным махабхут <em>земля</em> и
                <em>вода</em> и отвечает за гомеостаз. Производным махабхуты <em>пространство</em>
                являются «полости» тела — пути циркуляции <em>ветра, желчи, слизи</em> и
                <em>крови</em>.
            </p>
            <p>
                4. Пульсация <em>ветра, желчи</em> и <em>слизи</em> проявляется двояким способом.
                Когда это здоровая пульсация, то это врожденная пульсация, присущая человеку от
                рождения — это пульсация <em>мужская</em>,<em>женская</em> и
                <em>бодхисаттвовская</em>, или <em>нейтральная</em>, характеристики которых хорошо
                известны и многократно описаны [6: 68]. Когда это болезненная пульсация, это
                пульсация расстройств <em>ветра, желчи, слизи</em> или их сочетаний, характеристики
                этих пульсаций также хорошо известны и описаны [11].
            </p>
            <p>
                5. Области проявления махабхут в теле человека носят название <em>дхату</em> (тиб.
                <em>khams</em>), им соответствуют семь уровней организации тканей тела. Соответствие
                пяти махабхут семи тканям, или семи составляющим тела и пяти дхату, может быть
                представлена следующим образом.
            </p>
