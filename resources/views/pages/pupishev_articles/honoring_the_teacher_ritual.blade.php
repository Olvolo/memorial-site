                имеющих веру учеников, и крюком, быстро притягивающим все благое сего мира и
                вышнего".
            </p>
            <p>
                Накопленные благодаря усердию в (практике) этого метода добродетели, белые словно
                луна, сколько бы мы их ни накопили, мы преобразуем в причину обретения всеми живыми
