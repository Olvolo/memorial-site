                в Сансаре, ни в Нирване нет ни единого реально существующего атома, а причины и
                следствия, и (закон) взаимозависимого становления безошибочны, и что эти два
                (положения) не противоречат одно другому, но взаимодополняют друг друга.
            </p>
            <p>
                И просим благословить нас на то, чтобы, благодаря милости Кормчего — держателя
                ваджра — направляясь через океан раздела Тантр, мы больше жизни ценили корень всех
                совершенств — обеты Тантры и общие обеты.
            </p>
            <p>
                Просим благословить на очищение всех загрязнений профанического бытия с применением
                первой ступени йоги превращения рождения, смерти и промежуточного состояния в Три
                Тела Победителя, чтобы всё, что бы ни являлось, вырастало как божественное тело.
            </p>
            <p>
                Просим благословить на подлинное становление на путь парного вхождения Ясного Света
                и Иллюзорного Тела, появляющихся из ногтей пальцев ног Твоих, Владыка, — стопы
