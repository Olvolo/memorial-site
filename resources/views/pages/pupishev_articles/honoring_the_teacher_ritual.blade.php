            </p>
            <p class="italic">
                Совершив это устремление и молитвы, следует понять, что единственным механизмом
                получения непосредственных наставлений является получение их из уст (Учителя). Если
                таким образом каждый день практиковать постоянную йогу, устанавливаясь в практике
                сущности всех сутр и тантр, в завершение (этой практики) корень накопления всего
                благого станет крепким. Сказано:
            </p>
            <p>
                "Святые наставления сутр и тантр — молочное озеро. Когда оно перепахтано мудрым
                Владыкой, держателем Земли, из него (вырастает) Кайлаша (высокая гора) небывалой
