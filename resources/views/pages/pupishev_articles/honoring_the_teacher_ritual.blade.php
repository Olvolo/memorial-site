                дурной кармы (и есть эти грехи), мы смогли принять неблагоприятные условия как путь
                (совершенствования)!
            </p>
            <p>
                Словом, что бы ни являлось как хорошее или дурное, просим благословить нас на то,
                чтобы мы реализацией пяти сил <a href="#citation-68" id="ref-68"
                                                 class="citation-mark">[68]</a>, сущности всей
                Дхармы, сделали своим путём развитие двух мыслей Бодхи <a href="#citation-69"
                                                                          id="ref-69"
                                                                          class="citation-mark">[69]</a>
                и в созерцании своём (опирались бы) только на ум, полный блаженства!
            </p>
            <p>
                Благословите нас на то, чтобы мы обрели величие содержания свобод и совпадений
                благодаря постижению метода четырёх подготовок <a href="#citation-70" id="ref-70"
                                                                  class="citation-mark">[70]</a>,
                благодаря практике того, чем полно сердце, и благодаря практике исполнения обетов
                очищения ума!
            </p>
            <p>
                Просим благословить нас на обретение сути Бодхичитты для спасения живых существ из
                моря бытия с мыслью жалости и сострадания к ним, применяя магию оседлания ветра на
                выдохе и вдохе <a href="#citation-71" id="ref-71" class="citation-mark">[71]</a>.
            </p>
            <p>
                Просим благословить на усердие в исполнении трёх [аспектов парамиты] нравственности
                Высшей Колесницы, которые являют собой единственный путь шествования Победителей
                трёх времён, когда поддерживается нить традиции [исполнения] обетов чистейших
                Победителей и их сыновей.
            </p>
            <p>
                Просим благословить на осуществление парамиты отдачи, чтобы мы могли, собрав
                добродетели, накопленные нами в трёх временах телом и достоянием, осуществить
                исполнение чаяний каждого из живых существ, (руководствуясь) наставлением о
                взращивании мысли, отринувшей привязанности.
            </p>
            <p>
                Просим благословить на осуществление парамиты нравственности, которая собирает
                благие дхармы и осуществляет чаяния живых существ, и чтобы при этом даже ради
                спасения собственной жизни мы не оставляли обетов Пратимокши (обетов Хинаяны),
                Бодхичитты и Тайной Тантры.
            </p>
            <p>
                Просим благословить на осуществление парамиты терпения, чтобы мы могли бестрепетно
                переносить мучения даже в том случае, если все живые существа трёх миров <a
                    href="#citation-72" id="ref-72" class="citation-mark">[72]</a> будут злиться на
                нас, бранить нас, указывать на наши пороки и даже лишать нас жизни, а мы отвечали бы
                им добром.
            </p>
            <p>
                Просим благословить на осуществление парамиты усердия, устремлённой (на достижение)
                высочайшей (мысли) Бодхи, (исходящей) из милосердия: эта (мысль), избавляющая каждое
                из живых существ от мук, да установится в океане кальп, посреди (опаляющего живых
                существ) пламени!
            </p>
            <p>
                Просим благословить на осуществление через самадхи парамиты созерцания, ровно
                устанавливающей мысль в единонаправленности на вытекающем из истинности всех дхарм
                естественном шуньевом состоянии, через отказ от ошибок вялости, необузданности и
                рассеянности мысли.
            </p>
            <p>
                Просим благословить на осуществление парамиты интуиции с помощью йоги пространства,
                направленной на постижение абсолютной (истины) — (йоги), сочетающейся с совершенно
                чистым великим блаженством, привлекаемым интуицией, познающей каждую вещь в
                отдельности как (находящуюся в природе) таковости.
            </p>
            <p>
                Просим благословить на осуществление иллюзорного самадхи методом постижения того,
                что элементы-дхармы вне и внутри нас в действительности и не существуют, хотя и
                проявляются как иллюзия, как сон, как отражение луны на глади озера.
            </p>
            <p>
