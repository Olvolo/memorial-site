                    href="#citation-73" id="ref-73" class="citation-mark">[73]</a>.
            </p>
            <p>
                Если мы не завершим этапы пути к моменту смерти, просим благословить нас на то,
                чтобы мы смогли достичь чистой земли (будд), руководствуясь могучим методом
                Просветления, которым владеет ведущий нас Учитель, либо же руководствуясь
                наставлениями о правильном применении пяти сил <a href="#citation-74" id="ref-74"
                                                                  class="citation-mark">[74]</a>.
            </p>
            <p>
                Словом, просим благословить нас на то, чтобы в этом и во всех рождениях Ты, Владыка,
                руководил нами, чтобы мы стали старшими из Твоих сыновей, держателями всех тайн
                Тела, Речи и Мысли.
            </p>
            <p>
                Даруй самовозникающее благо трансцендентной имманентности, приводящей все
                необходимое и искомое к завершению в местах и при обстоятельствах, где бы Ты,
                Владыка, сам воочию пребывал во главе круга буддистов.
            </p>
            <p class="italic">
                В этом месте (нужно созерцать следующее). Из тел собравшихся в (чистой) земле (см.
                прим. 23) исходят лучи света, ими притягиваются (живые существа и их обители) – мир
                внешних сосудов и соков, которые прибывают, плавятся в свете и входят в живых
                существ сока (в созерцаемых живых существ). Они же (сами) плавятся в свете и входят
                в нас. Жертвенные предметы плавятся в свете и входят в древо созерцания. Древо
                созерцания входит в львиный трон. Затем из сердца Учителя исходят лучи света,
                проницают собравшихся в (чистой) земле, притягивают и притягивают их, а те прибывают
                и, словно пар от дыхания на зеркале, (тают) и входят снаружи по порядку в самое
                сердце коренного Учителя, концентрируясь внутри него. Львиный трон с лотосом
                соединяется с троном Учителя и лотосом. Тогда мы (взываем) к Учителю с сильнейшим
                благоговением, верой и устремлением.
            </p>
            <p>
                В ответ на это моление высочайший Учитель радостно прибывает, чтобы благословить
                нас, на наши макушки. Просим Вас снова прочно поставить свои стопы с источающими
                свет кончиками ногтей на пыльники лотосов наших сердец!
            </p>
            <p class="italic">
                В ответ на эти слова и в первых двух случаях, сам Учитель прибывает на наши макушки,
                из него исходит безмерное количество нектара вместе с лучами света. Они очищают наши
                греховные остатки, а также подлежащие устранению болезни, и мы обретаем
                благословение Тела, Речи и Мысли Учителя. Верим, что умножаются все достоинства
                постижения наставлений, а при произнесении двух вторых строк снова ради
                благословления наших тел, (центров) речи и умов они нисходят по Авадхути и ясно и
                покойно входят в неотделимого от нашего сознания Изначального Владыку в наших
                сердцах. Этим (созерцанием) мы утверждаемся в вере в то, что (Учитель) с радостью
                неколебимо обитает в наших сердцах. Этим методом мы реализуем единую сущность
                Учителя, Идама нашего сознания, и что бы ни являлось нам на нашем пути практики (см.
                прим. 37), (предстаёт как великое незапятнанное) блаженство. Сущность шуньи
                проявляется в сорадости богов и богинь в других (видах) йоги, связанных с парностью
                — в (степенях) рождения и завершения (ануттара-йогатантры), а механизм степеней пути
                <a href="#citation-75" id="ref-75" class="citation-mark">[75]</a> и махаянского
                очищения ума те, кто очищает свой ум совершенным образом, осуществляют усердной
                практикой неотступления (от обетов). В завершение следует читать «Благопожелания»,
                «Бхадрачарью» <a href="#citation-76" id="ref-76" class="citation-mark">[76]</a> и
                другие обширные молитвы. А если сокращённо ( то можно читать следующее):
            </p>
            <p>
                Те благие заслуги, которые мы обрели исполнением (вышеизложенного), мы сделаем
                причиной обращения ко всем без исключения молитвам, созданным всеми Татхагатами и их
