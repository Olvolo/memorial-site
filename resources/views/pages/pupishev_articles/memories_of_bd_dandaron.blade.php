            <p>Однако простить буддийское мироощущение власти ему не могли. Будучи как-то вызван в
                обком, в ответ на упреки в том, что он не скрывает, что он буддист, Бидия Дандарович
                сказал: «Хоть сажайте меня, хоть расстреливайте, но я буддистом родился, буддистом
                живу и буддистом умру».</p>

            <p>В августе 1972 года Бидия Дандарович был арестован. Еще за несколько месяцев до этого
                он как-то сидел в комнате задумчивый, перебирал четки. Неожиданно, обращаясь ко мне
                (больше никого в комнате не было), он произнес: «Видимо, этого не избежать. Ничего
                нельзя сделать. Скоро нас будут арестовывать. Но я думаю, что наши Учителя не
                допустят, чтобы наши ребята сидели по дурдомам. Они выйдут». И всё. Больше к этой
                теме Бидия Дандарович долго не возвращался. В июле 1972 года Учитель начал свою
                последнюю прижизненную проповедь, которая длилась до дня его ухода 26 октября 1974
                года, и набат ее слышен до сих пор.</p>

            <p class="text-center"><strong>* * *</strong></p>

            <p>О суде писать неприятно: следствие велось откровенно тенденциозно, прокуратура была
                вызывающе агрессивна, суд был болен и послушен. Приведу лишь несколько характерных
                эпизодов.</p>

            <p>С участием оперработников был проведен ряд негласных и гласных обысков, причем в
                протоколах гласных обысков без обиняков писали: обыск проводится «с целью отыскания
                и изъятия буддийской литературы и предметов буддийского культа».</p>

            <p>В нарушение закона большинство учеников Бидии Дандаровича были насильно подвергнуты
                психиатрической экспертизе до предъявления обвинения. В результате амбулаторного (!)
                обследования четверо были признаны невменяемыми, естественно, с диагнозом
                «вялотекущая шизофрения». Как это происходило, я знаю по себе. Меня подвергли
                принудительной экспертизе дважды. Первый случай просто комичен, второй трагикомичен
                — от диагноза «вялотекущая шизофрения» меня спасло некоторое знание латыни. А было
                это так.</p>

            <p>Первый раз меня обследовала женщина психиатр в спецприемнике МВД, куда я и мои друзья
                были посажены за наши настойчивые требования взять у нас показания, так как вначале
                следствие допрашивало только тех, кто мог дать, или кого можно было заставить дать
                нужные следствию показания. В ходе «ознакомительной беседы» психиатр на свой
                очередной вопрос «Как вы родились?» получила бесхитростный ответ: «Не помню». — «Как
                так?», — удивилась психиатр, — «Я очень маленький был, и естественно, что я не
                помню, как я рождался. — «Ну, я имею в виду, нормально ли?» — «Поскольку я веду себя
                нормально и разговариваю с Вами нормально, то я думаю, что нормально». Психиатр была
                обескуражена, прервала собеседование и отпустила меня с миром обратно в камеру.</p>

            <p>Второй раз «собеседование» со мной проводил главный психиатр Улан-Удэ. В ходе
                «беседы» он задал мне вопрос о моем отношении к Бидии Дандаровичу, на что получил
                ответ: «Omnia mea mecum porto». На эти слова он очень спокойно заметил: «Я восточных
                языков не понимаю». — «Простите, но вы ведь окончили мединститут?» — «Да». «Значит,
                вы изучали латынь. А я ведь ответил вам на латыни. Но если вы ее забыли, готов для
                вас перевести на невосточный русский язык: «все свое ношу с собой». В том числе и
                мнение свое о людях я держу при себе, и делиться им с Вами у меня нет
                оснований».</p>

            <p>Присутствовавшие врачи с трудом удерживали смех, прыская в ладони. Главный психиатр
                весь побагровел, минуты три, превозмогая себя, сидел молча, затем сквозь зубы
                процедил: «Идите». Свидетелем последовавшего телефонного разговора с прокурором,
                куратором следствия, был В. Ш. Аранов, бывший в это время на допросе у этого
                прокурора.</p>

            <p>— Я же настаивал, чтобы вы еще и этого обязательно признали невменяемым, — говорил в
                трубку прокурор.<br>
                — Я вам и так четверых невменяемых сделал, а этого не могу, не могу, не могу! —
                кричал в ответ главный психиатр.</p>

            <p>На суде обвинение основывалось на данных «научно–атеистической, искусствоведческой»
                экспертизы, подготовленной непосредственным начальником Б. Д. Дандарона<a href="#citation-3" id="ref-3" class="citation-mark"><sup>3</sup></a>,
                неспециалистом по буддизму, и известной ученой-атеисткой<a href="#citation-4" id="ref-4" class="citation-mark"><sup>4</sup></a>, которая не решилась даже
                явиться в суд для защиты своей экспертизы. В тексте «экспертизы» были столь явные
                нелепости, что суд вынужден был отклонить ее. Так, в «экспертизе» утверждалось, что
                «буддизм есть насилие, в нем существуют изуверские секты», авторы «экспертизы»
                позволили себе ряд замечаний, выходящих за рамки их задачи и представляющих просто
                нападки на Б. Д. Дандарона и обвинения его в «преступлениях». Явившийся же на суд
                «эксперт», не будучи способен ответить ни на один из специальных вопросов адвоката
                Н. Я. Немеринской, вообще отказался говорить, ссылаясь на то, что он не компетентен
                в вопросах буддизма.</p>

            <p>Известного эстонского востоковеда Л. Мялля оперработники трижды пытались арестовывать
                уже в суде, чтобы он, как непосредственный свидетель некоторых эпизодов, не смог
                дать показаний, расходящихся с версией следствия. Я смог отбить его (выражаюсь не
                фигурально), за что мне один из оперработников посулил большие неприятности
                (действительно, после этого мне не один раз устраивали провокации).</p>

            <p>Высказывания прокурора во время суда — вершина тогдашнего правосудия. Например: «Они
                все сумасшедшие. Нормальные люди стремятся из Бурятии в Ленинград, а они, наоборот,
                сюда едут жить». Или: «Сослал бы я вас, буддистов, всех на Колыму, посмотрел бы я,
                какими бы вы там буддистами были». Или: «Дандарон дважды судим. То, что его
                реабилитировали, не имеет значения, при Хрущеве реабилитировали кого попало». Или:
                «Верующие в Советском Союзе не имеют права на труд». И тому подобное. Местные и
                несколько центральных газет поместили еще до суда ряд клеветнических материалов.</p>

            <p>В защиту Б. Д. Дандарона выступили А. Д. Сахаров, радио «Свобода» и зарубежные
                печатные издания. Зарубежные организации обратились с просьбой выпустить Б. Д.
                Дандарона до суда под значительный залог. Ответа они не получили.</p>

            <p>Все было напрасно. Исход процесса был предрешен. Бидию Дандаровича приговорили к 5
                годам лагерей, а четверых его учеников, признанных невменяемыми — к высылке в
                психлечебницы Ленинграда, Киева и Каунаса (sic!). Всех остальных, в том числе
                главного свидетеля обвинения, уволили с работы.</p>

            <p>«Меня привезли сюда, чтобы убить», — сказал Бидия Дандарович в лагере. Прожил он там
                два года. И только женщина, одна из его учениц [Г. Д. Мерясова — прим. ред.], даже в
                последние месяцы своей беременности, с риском для жизни проползала под колючей
                проволокой в зону подсобного хозяйства лагеря и через известного ей человека
                неоднократно передавала Учителю чай, табак, еду. Да дважды в год получали свидание
                родственники. Другие связи были нерегулярными — в гневный, ощерившийся колючей
                проволокой и автоматами охраны последний на этой земле приют Учителя попасть было
                непросто.</p>

            <p>Попытку вызволить из лагеря Б. Д. Дандарона предпринял тогдашний Хамбо-лама Гомбоев.
                Он отправился в Москву и обратился в самые высокие инстанции. Но и это не принесло
                результатов. За два года пребывания в лагере Бидия Дандарович успел написать главный
                труд своей жизни — книгу, в которой он изложил высшую философию буддизма. По доносу
                предателя эта книга была изъята и судьба ее неизвестна. Уверениям лагерного
                начальства в том, что в присутствии оперработников из Улан-Удэ она была сожжена,
                как-то не верится — даже у нас рукописи не горят. Возможно, первую часть именно этой
                работы опубликовал В. В. Бараев в альманахе «Буддийский мир» (1994 г.) и то, что
                успел написать Учитель, полностью издал В. М. Монтлевич в 1995 г. под названием
                «Черная Тетрадь. О четырех благородных истинах Будды».</p>

            <p>Не осилил огонь и «Мысли буддиста», которые мы с огромной радостью представляем
                читателю.</p>

            <p>Я думаю, что теперешнее возрождение буддизма стало возможным именно благодаря таким
                подвижникам духа, каким всю свою жизнь был Бидия Дандарович Дандарон. Мы радуемся
                полученным нами правам на свободу мысли и слова. Но ведь свобода мысли не право,
                свобода мысли — это обязанность всякого мыслящего человека. И наша свобода слова
                была завоевана духовным подвигом тех, кто в самые суровые времена не пренебрегал
                обязанностью свободно мыслить и расплачивался за свободу выражать свои мысли годами
                лагерей.</p>

            <p>Преклоняюсь перед их подвигом.</p>

            <p>Пусть та малая толика, которую мы смогли сделать с мыслью об Учителе, издав эту
                книгу, пойдет на благо всех живых<a href="#citation-5" id="ref-5" class="citation-mark"><sup>5</sup></a>.</p>
