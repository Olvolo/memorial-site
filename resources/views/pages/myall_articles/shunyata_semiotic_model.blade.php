        <div class="text-justify text-base sm:text-lg leading-relaxed space-y-4">
            <p>В. П. Васильев писал более ста лет тому назад: «Идея о пустоте есть понятие
                субъективное и вместе трансцендентальное; с одной стороны, это то, что мы отыскиваем
                как нечто самобытное, самостоятельное, прочное, независимое от форм; в этом случае
                она есть и не есть, существует как бытие отрицательное или противоположное всему
