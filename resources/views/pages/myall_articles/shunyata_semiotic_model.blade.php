            <p>Чрезвычайно интересный перечень синонимов встречается в ХІ главе «Аштасахасрики»:</p>
            <p>sarvam hi samskrtam-anityam sarvam bhaya-avagatam duhkham, sarvam traidhātukam śūnyam
                sarvadharmā anātmanāh<a href="#citation-23" id="ref-23" class="citation-mark"><sup>23</sup></a>.
            </p>
            <p>(Ведь все созданное не вечно, все страшное страдание, вся трисферность пустота, все
                дхармы без «я».)</p>
            <p>Поскольку этот отрывок представляет собой явно цитату из какой-то канонической сутры,
                то эти синонимы, выражающие эмоциональную окраску «шуньяты», можно не считать
                специфически праджняпарамитскими. Но в то же время этот отрывок подтверждает
                положение самой «Аштасахасрики» о том, что учение о «шуньяте» может быть
                провозглашено и в иных, непраджняпарамитских сутрах<a href="#citation-24"
                                                                      id="ref-24"
                                                                      class="citation-mark"><sup>24</sup></a>.
            </p>
            <p>Какой перевод, из принятых в буддологической литературе, подходит по материалу
                «Аштасахасрики» более всего к обозначению «шуньяты»? «Пустота», как переводит
                абсолютное большинство исследователей, в том числе и автор монографии по этой теме
                Ф. И. Стренг<a href="#citation-25" id="ref-25"
                               class="citation-mark"><sup>25</sup></a>, «относительность»<a
                    href="#citation-26" id="ref-26" class="citation-mark"><sup>26</sup></a> или
                «нуль»<a href="#citation-27" id="ref-27" class="citation-mark"><sup>27</sup></a>.
            </p>
            <p>Мне представляется, что каждый из этих переводов отражает определенную сторону этого
                понятия. В «Аштасахасрике» встречается форма «шуньята» в ситуации, где она явно
                значит «пустой»<a href="#citation-28" id="ref-28"
                                  class="citation-mark"><sup>28</sup></a>. По следующему примеру
                можно «шуньяту» интерпретировать как относительность»:</p>
            <p>evam yaś-ca abhisambudhyate anuttarām samyaksambodhim, yan-ca-abhisambodhavyam,
                yaś-ca janīyat, yan-ca jnatavyam, sarvā ete dharmāh śūnyāh<a href="#citation-29"
                                                                             id="ref-29"
                                                                             class="citation-mark"><sup>29</sup></a>.
            </p>
            <p>(Тот, кто понимает наивысшее совершенное просветление, и то, что следует понимать,
                тот, кто знает, и то, что следует знать, все эти дхармы «относительны».)</p>
            <p>Все же мне представляется, что перевод «нуль» соответствует более всего таким
                текстовым ситуациям, где встречается слово «шуньята» и разнородности синонимов
                «шуньяты». Обращаем внимание и на то, что многие синонимы представляют собой пары
                оппозиций (анутпада аниродха и т. д.), и «шуньята» означает их нейтрализацию.</p>
            <p>«Шуньята» по «Аштасахасрике» не онтологический факт, не «существующее положение
                вещей». Она также и не концепция какой-либо теории, не идея, которую необходимо
                понимать и постигать. Скорее всего, это спонтанный отказ от всех операций, как
                положительных, так и отрицательных, человека с дхармами и в то же время отказ от
                этого отказа<a href="#citation-30" id="ref-30"
                               class="citation-mark"><sup>30</sup></a>. Возможно, что авторы
                «Аштасахасрике» имели в виду именно такое понимание в следующем отрывке:</p>
            <p>yaś-ca atyanta-vivikto dharmah, na so 'sti-iti vā na-asti-iti vā upaiti<a
                    href="#citation-31" id="ref-31" class="citation-mark"><sup>31</sup></a>.</p>
            <p>(То, что является отдельной дхармой, об этом нельзя сказать ни «есть», ни «не
                есть».)</p>
