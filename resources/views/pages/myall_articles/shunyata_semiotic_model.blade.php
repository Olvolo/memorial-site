                бытию; с другой стороны, это есть то отвлеченное и истинное бытие, которое
                существует во всем, ни в чем не заключаясь и все заключая в себе, хотя и ничего не
                содержа; одним словом, это объект, сходный с субъектом, и объект, который как скоро
                входит в наше мышление уже становится чем-то субъективным; следовательно, то, что не
                вообразимо, что не может быть предметом цели и стремления. Когда ум вознесется до
                некоторой степени абстрактности, в которой не может его сопровождать уже здравый
                критический смысл, он придается какому-то упоению, в котором не может дать себе
                отчета, но которое составляет для него необыкновенную прелесть; он не хочет
                воротиться назад, безотчетно томится в хаосе отвлеченности и говорит самым
                непонятным языком. В таком виде должны мы смотреть на учение о пустоте; как учение
                оно представляется под именем Праджняпарамиты, мудрости, переводящей на тот берег
                Сансары, и в этом смысле само орудие сливается с самим средством и целью.
                Праджняпарамита и учит нас рассуждать об отвлеченной идее и содержит в себе это
                учение; идея о пустоте есть вместе отчет или вывод нашего разума и то, что должно
                составлять отныне его ингредиент и руководить им. Отсюда выходит, что, с одной
                стороны, все предметы, входившие в учение Яны Шраваков (скандхи, 37 членов боди,
                Шраваки, Сротапанны, Анагамины, Арханы, Пратьекабудды), самая Праджняпарамита, не
                существуют, не истинны, пусты, с другой стороны, вследствие этой же пустоты они и
                существуют; одним словом, здесь сливаются все противоречия: положительное и
                отрицательное составляют тождество»<a href="#citation-1" id="ref-1"
                                                      class="citation-mark"><sup>1</sup></a>.</p>
            <p>Как видно, уже Васильев понял, что «пустота», т. е. шуньята занимает важное место в
                теории дхарм махаяны<a href="#citation-2" id="ref-2"
                                       class="citation-mark"><sup>2</sup></a>. Несомненно, она
                центральный термин философии Нагарджуны, которую Ф. И. Щербатской обозначал как
                теорию «сарва-дхарма-шуньята» («пустота всех дхарм»)<a href="#citation-3" id="ref-3"
                                                                       class="citation-mark"><sup>3</sup></a>.
                Философия Нагарджуны возникла вероятно под влиянием праджняпарамитских сутр. Уже
                Чандракирти писал, что учение Нагарджуны прямое продолжение Праджняпарамиты<a
                    href="#citation-4" id="ref-4" class="citation-mark"><sup>4</sup></a>. В наш век
                то же самое утверждали многие буддологи. М. Валлезер писал: "The systematic
                development of the thought of voidness laid down in the Prajnāpāramitā Sūtras is
                brought into junction with the name of a man of whom we cannot even positively say
                that he has really existed, still less that he is the author of the works ascribed
                to him: this name is Nāgārjuna"<a href="#citation-5" id="ref-5"
                                                  class="citation-mark"><sup>5</sup></a>.</p>
            <p>Так же думает и проф. Мурти: "The Madhyamika system is the systematized form of the
                śūnyatā-doctrine of the Prajnāpāramitā treatises: its metaphysics, spiritual path
                (sat-pāramitā-naya) and religious ideal are all present there, though in a loose,
                prolific garb"<a href="#citation-6" id="ref-6"
                                 class="citation-mark"><sup>6</sup></a>.</p>
            <p>Но исследования Р. Робинзона приводят к переоценке этой точки зрения. Оказывается,
                почти все основные термины праджняпарамитских сутр или не встречаются в кариках
                Нагарджуны (праджня, татхата, бхутакоти, адвая, бодхичитта, бхуми), или встречаются
                весьма редко (бодхисаттва)<a href="#citation-7" id="ref-7"
                                             class="citation-mark"><sup>7</sup></a>.</p>
            <p>Только один термин «шуньята» фигурирует часто и в праджняпарамитских сутрах, и в
                кариках Нагарджуны. Но можно ли из этого сделать вывод, что «шуньята» основной
                термин и в праджняпарамитских сутрах? Мне представляется, что такой вывод был бы
                необоснован<a href="#citation-8" id="ref-8" class="citation-mark"><sup>8</sup></a>.
            </p>
            <p>То же самое утверждал в предисловии к своей статье «Философия и религия
                Праджняпарамиты» Д. Т. Судзуки: "The chief defect, however, with the Diamond Cutter
                is that emphasizes the śūnyatā aspect of the Prajnāpāramitā teaching to strongly,
                giving to the general reader the impression that this is the Alpha and Omega of the
                Mahāyāna. (...) The object of this essay is to state that the teaching of the
                Prajnapāramitā consists in defining the essence of Bodhisattvahood"<a
                    href="#citation-9" id="ref-9" class="citation-mark"><sup>9</sup></a>.</p>
            <p>Но тем не менее «шуньята» занимает и в основной сутре цикла Праджняпарамиты в
                «Аштасахасрике Праджняпарамите» столь важное место, что любое исследование этой
                сутры немыслимо без анализа ее, и любое исследование этого понятия немыслимо без
                анализа этого текста. Но, к сожалению, до сих пор материал «Аштасахасрики» был
                исследован слишком односторонне: в «шуньяте» «Аштасахасрики» искали только те ее
                стороны, которые подтверждали бы гипотезу о происхождении учения Нагарджуны от
                учения праджняпарамитских сутр.</p>
            <p>В действительности «Аштасахасрика» представляет для исследования «шуньяты» гораздо
                более разносторонний материал. В «Аштасахасрике» этот термин не единственное слово,
                обозначающее особое отношение бодхисаттвы-махасаттвы к дхармам<a href="#citation-10" id="ref-10"
                                                   class="citation-mark"><sup>10</sup></a>. Наряду с
                «шуньятой» существует множество других терминов. Эти другие термины можно в
                некотором смысле рассматривать как синонимы «шуньяты». Хотя поле их значения не
                совпадает целиком с полем значения «шуньяты», а иногда совпадает лишь частично, они
                представляют ценный материал для анализа как теории шуньяты, так и понятия
                «шуньята».</p>
            <p>Основные синонимы «шуньяты» суть «анимитта» и «апранихита». Вместе с «шуньятой» они
                образуют особый подраздел в некоторых буддийских списках под названием
                «тривимокшамукха» – «три двери освобождения». В «Аштасахасрике» также встречается
                термин «тривимокшамукха» и перечисляются его элементы<a href="#citation-11" id="ref-11" class="citation-mark"><sup>11</sup></a>.
            </p>
            <p>«Анимитта» обычно переводится как «незнаковость», «апранихита» как «непривязанность».
                Имеется один интересный контекст этих терминов:</p>
            <p>kimcā-api śāriputra etesām bodhisattvānām-asti mārgah śūnyatā vā ānimitta-caryā vā
                apranihita-manaskāratā vā...<a href="#citation-12" id="ref-12" class="citation-mark"><sup>12</sup></a>.</p>
            <p>(Хотя эти бодхисаттвы имеют путь шуньяты, незнаковое поведение и непривязанное
                внимание...)</p>
            <p>Приведенный пример позволяет выяснить, как авторы «Праджняпарамиты» разграничили
                сферу употребления этих синонимов: «шуньята» описывает путь, т. е. собственно
                буддийское поведение бодхисаттвы; «анимитта» поведение на каком-то достигнутом
                уровне этого пути; «апранихита» состояние наблюдения. Только на одном примере трудно
                показать, насколько универсальным было это разграничение в буддийской литературе, но
                тем не менее, в «Аштасахасрике» оно имеет определенное значение.
            </p>
