                    «звательный» или «местный» падеж причастия «ушедшая» или «местный» падеж
                    причастия «ушедший», а «ОМ» (санскр. <em>oṃ</em>) и «СВАХА» (санскр.
                    <em>svāhā</em>) — традиционные формы начала и конца
                    мантры: «ОМ» и «СВАХА» — звуки, благословляющие пользу обретения плода
                    повторения (мантры).<a href="#ref-28"
                                           class="text-blue-500 hover:underline">↩</a></p>
                <p>
                    Пять слов <em>gate gate pāragate pārasaṃgate bodhi</em>, помещенных между «ОМ» и
                    «СВАХА» соотносятся, по
                    мнению Э. Конзе (<em>Conze E.</em>, р.106), последовательно с (2) — (6)
                    фрагментами текста (хотя членение текста у Конзе немного отличается от нашего),
                    охватывая, таким образом, все содержание сутры.
                </p>
                <p>
                    Чогьям Трунгпа (тиб. <em>Chos rgyam Drung pa</em>) считает, что суть всего
                    учения в соотнесении с мантрой может быть выражена в рамках фрагмента (2).
                    Комментарий Махаджаны (санскр. <em>Mahājana</em>, тиб. <em>sKyes bu chen po</em>)
                    расширяет ассоциативное поле тантры, объясняя термин «ГАТЕ» как оставление (тиб.
                    <em>spangs pa</em>) пройденного пути (тиб. <em>lam</em>) совершенствования:
                </p>
                <ul class="list-disc pl-5 space-y-1 ml-8">
                    <li class="my-2">ГАТЕ (первое) – «пути накопления» (санскр.
                        <em>sambhāra-mārga</em>, тиб. <em>tshogs lam</em>);
                    </li>
                    <li class="my-2">ГАТЕ (второе) – «пути подготовки» (санскр.
                        <em>prayoga-mārga</em>, тиб. <em>sbyor lam</em>);
                    </li>
                    <li class="my-2">ПАРАГАТЕ – «пути видения» (санскр. <em>darśana-mārga</em>, тиб.
                        <em>mthong lam</em>);
                    </li>
                    <li class="my-2">ПАРАСАМГАТЕ – «пути медитации» (санскр. <em>bhāvanā-mārga</em>,
                        тиб. <em>sgom lam</em>);
                    </li>
                    <li class="my-2">БОДХИ (санскр. <em>bodhi</em>) – «пути не-учения» (санскр. <em>aśaikṣa-mārga</em>,
                        тиб. <em>mi slob lam</em>).
                    </li>
                </ul>
                <p>
                    (Подробнее об описании буддийской системы совершенствования в терминах "пяти
                    путей" см., напр. <em>Abhisamayālaṅkāra</em>, гл. 1).
                </p>
                <p>
                    Существуют и другие истолкования мантры Праджняпарамиты, но все они сходятся в
                    одном: данная мантра кодирует ВЕСЬ путь совершенствования, включая достижение
                    состояния Будды. «Поэтому, — говорит комментарий Ваджрапани (санскр. <em>Vajrapāṇi</em>,
                    тиб. <em>Lag na rdo rje</em>), — эта мантра Праджняпарамиты есть сердце смысла
                    всех тайных мантр (санскр. <em>guhya-mantra</em>, тиб. <em>gsang sngags</em>)».
                </p>
