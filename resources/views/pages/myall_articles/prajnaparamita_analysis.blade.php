                Пустота (санскр. <em>śūnyatā</em>, тиб. <em>stong pa nyid</em>) — ключевой термин
                праджняпарамитской литературы, обозначающий центральную идею доктрины — указание на
                отсутствие абсолютной конечной реальности, которую мы ожидаем найти в результате
                анализа бытия, и, опираясь на которую, можно было бы дать "истинное описание"
                существующего. Всякое описание реальности есть ограничение и искажение. Обозначив
                вещь, мы гипостазируем это обозначение, саму же вещь теряем из виду. Учение о
                пустоте призвано разрушить все обозначения, показав их условность, а также условный,
                временный характер обозначаемого понятия, так как в состоянии просветления
                реальность должна восприниматься непосредственно и целиком, без рамок понятийного
                мышления. Традиционно принято рассматривать понятие пустоты в восемнадцати или
                двадцати аспектах<a href="#citation-30" id="ref-30"
