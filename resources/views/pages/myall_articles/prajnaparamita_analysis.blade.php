                мыслительных конструкций:
            </p>
            <ol class="list-decimal list-outside ml-4 space-y-1 pl-2">
                <li class="my-2">пустота внутреннего (санскр. <em>adhyātma-śūnyatā</em>, тиб. <em>nang
                        stong pa nyid</em>) — отрицание абсолютного
                    статуса субъекта;
                </li>
                <li class="my-2">пустота внешнего (санскр. <em>bahirdhā-śūnyatā</em>, тиб. <em>phyi
                        stong pa nyid</em>) — отрицание абсолютного статуса
                    объекта, то есть независимости внешнего мира;
                </li>
                <li class="my-2">пустота внешне-внутреннего (санскр.
                    <em>adhyātma-bahirdhā-śūnyatā</em>, тиб. <em>nang phyi stong pa nyid</em>) —
                    отрицание
                    возможности найти какую-либо абсолютную основу в самом субъектно-объектном
                    отношении, если такой основы не оказалось ни в одной из сторон, участвующих в
                    отношении;
                </li>
                <li class="my-2">пустота пустоты (санскр. <em>śūnyatā-śūnyatā</em>, тиб. <em>stong
                        pa nyid stong pa nyid</em>) — отрицание абсолютного статуса
                    самой пустоты, которая, как и всё прочее, не может быть сделана опорой для
                    конструирующего мышления, то есть рассматриваться в качестве субстанции.
                </li>
            </ol>
            <p>
                Теория пустоты, таким образом, может здесь пониматься как естественное и
                закономерное развитие древнейшей буддийской доктрины "несубстанциональности" —
                Анатма-вады (санскр. <em>anātmavāda</em>, тиб. <em>bdag med pa'i lta ba</em>).
            </p>
            <p>
                Фрагмент (2), оперируя введенным понятием пустоты, разрушает представление о
                незыблемости, окончательной истинности описания бытия в терминах пяти скандх
                (санскр. <em>pañca-skandha</em>, тиб. <em>phung po lnga</em>).
            </p>
            <p>
                Фрагмент (3) распространяет достигнутый результат на все дхармы (санскр.
                <em>dharma</em>, тиб. <em>chos</em>), последовательно снимая их наиболее
                распространённые характеристики.
            </p>
            <p>
                Фрагмент (4) совершает очередной виток спирали, возвращаясь от общих характеристик
                дхарм к конкретным дхармическим описаниям бытия. В начале в парадоксальной форме
                относительно фрагмента (2) повторяется отрицание представлений о скандхах. Затем
                проводится деструкция описания бытия в терминах двенадцати "источников сознания"
                (аятан, санскр. <em>āyatana</em>, тиб. <em>skye mched</em>), в терминах восемнадцати
                "классов элементов" (дхату, санскр. <em>dhātu</em>, тиб. <em>khams</em>), и,
                наконец,
                отрицается теория взаимозависимого происхождения (санскр. <em>pratītyasamutpāda</em>,
                тиб. <em>rten cing 'brel bar 'byung ba</em>), начинающая причинно-следственную цепь
                со
                звена "неведения" (санскр. <em>avidyā</em>, тиб. <em>ma rig pa</em>) и завершая её
                звеном "пресечения старости и смерти" (санскр. <em>jarāmaraṇa</em>, тиб. <em>rga
                    shi</em>).
            </p>
            <p>
                Фрагмент (5) завершает разрушение здания буддийской философии, отрицая его фундамент
                - Четыре Благородные Истины (санскр. <em>catvāri āryasatyāni</em>, тиб. <em>'phags
                    pa'i bden pa bzhi</em>) — и, заодно, саму идею достижения (нирваны, санскр. <em>nirvāṇa</em>,
                тиб. <em>mya ngan las 'das pa</em>) вместе с её противоположностью - идеей
                недостижения.
            </p>
            <p>
                Фрагмент (6), однако, подводит итог проделанному движению, возвращая всё на свои
                места: бодхисаттва (санскр. <em>bodhisattva</em>, тиб. <em>byang chub sems dpa'</em>)
                в конце концов достигает нирваны, но он вынужден, как и все
                прежние Будды (санскр. <em>buddha</em>, тиб. <em>sangs rgyas</em>), делать это
                методом "недостижения", методом Праджняпарамиты (санскр. <em>prajñāpāramitā</em>,
                тиб. <em>shes rab kyi pha rol tu phyin pa</em>), очищающей
                его сознание от всех мыслительных конструкций, ограничивающих восприятие реальности.
            </p>
            <p>
                Фрагмент (7) кодирует метод Праджняпарамиты в форму мантры (санскр. <em>mantra</em>,
                тиб. <em>sngags</em>), предназначенную для
                интериоризации доктрины в процессе йогической медитации (санскр. <em>dhyāna</em>,
                тиб. <em>bsam gtan</em>).
            </p>
            <p>
                И фрагмент (8) является эпилогом, "счастливым концом" интеллектуальной драмы. В
                кратких версиях текста этот фрагмент опускается.
            </p>
            <p class="text-center font-semibold">***</p>
            <p>
                Таким образом, небольшой текст "Сутры сердца" (санскр. <em>Prajñāpāramitāhṛdaya-sūtra</em>,
                тиб. <em>Shes rab kyi pha rol tu phyin pa'i snying po'i mdo</em>) охватывает
                важнейшие доктрины
                раннебуддийской философии и, обрабатывая их понятием "пустоты", лишает статуса
                догмы, сковывающей мышление и препятствующей постижению истины. Ни один другой
                праджняпарамитский текст не делает такой последовательной переработки буддийского
                теоретического наследия, поэтому можно согласиться с мнением Э. Конзе,
                подчёркивающим особую роль Сутры сердца в опусе литературы Праджняпарамиты, и
                сравнивающим её с той ролью, которую играла излагающая Четыре Благородные Истины
                <em>Сутра поворота колеса учения</em> (санскр. <em>Dharmacakrapravartana-sūtra</em>,
                тиб. <em>Chos kyi 'khor lo bskor ba'i mdo</em>) в раннем буддизме:
            </p>
            <p>
                "<em>Праджняпарамита-хридая-сутра</em>", — писал Э. Конзе, — "это <em>Сутра поворота
                    колеса учения</em>
                нового распространения. Это результат восьмисот лет непрерывной медитации над
                традицией первого поворота колеса учения. В литературе второго поворота колеса
                учения <em>Хридайя</em> была призвана занять такое же центральное и фундаментальное
                положение, какое <em>Сутра поворота колеса учения</em> занимает в писаниях первого
                поворота<a href="#citation-31" id="ref-31" class="citation-mark"><sup>31</sup></a>."
            </p>

