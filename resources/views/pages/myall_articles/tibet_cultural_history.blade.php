        <div class="text-justify text-base sm:text-lg leading-relaxed space-y-4">
            <p>Истоки крупнейших рек Востока находятся в Тибете<a href="#citation-1" id="ref-1"
                                                                  class="citation-mark"><sup>1</sup></a>.
                Тибетские воды орошают поля Южной, Восточной и Юго-Восточной Азии. Хорошо известны и
                другие азбучные истины, например, плотность населения в Тибете очень невелика, а
