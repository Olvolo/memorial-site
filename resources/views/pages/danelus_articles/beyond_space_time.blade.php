            </p>
            <p>
                В середине шестидесятых пришли и первые ученики-европейцы (не считая тех нескольких
                человек, которые сумели получить тантрийские посвящения еще в лагере). Этот период в
                жизни Дандарона был особенно важным - эти семь лет стали и осуществлением его
                стремлений. Тут следует поговорить о двух чрезвычайно важных моментах, определяющих
                успешность передачи традиции - о мотивации и способностях учеников. Если судить по
                санскритским и тибетским источникам, различались три вида мотивации. Первый - это
                стремление обеспечить себе лучшее рождение после смерти (т.е. стремление опять
                родиться человеком). Дальше идет решимость освободиться от цепи рождений и смертей
                (что обычно называется Сансарой). И, наконец, третья установка - достичь
                просветления, чтобы помочь другим. С некоторой долей осторожности можно говорить и о
                еще одной мотивации - стремлении "прыгнуть" (или нырнуть) в Нирвану еще в этой
                жизни. Смею утверждать, что большинство учеников Дандарона имели именно последнюю,
                наивысшую мотивацию. Правда, тут нужна оговорка: как однажды писал Арно Дежарден,
                девяносто девять процентов европейцев не верит в реинкарнацию, и даже есть попытки
                со стороны известных западных буддистов отрицать ее. Как заметил Конзе, есть богатые
                старушки, для которых учение о реинкарнации привлекательно хотя бы потому, что
                позволяет им верить, что они когда-то были египетскими принцессами. Западные
                буддисты чаще всего испытывают радость от того, что они в прошлой жизни обитали в
                Тибете. Забота о будущей жизни не становится составной частью мотивации; это
                позволяет утверждать, что ориентация европейских буддистов почти исключительно
                ценностная и что они упускают очень важный момент ответственности за свои поступки.
            </p>
            <p>
                Никто не может сказать, какими последствиями для Запада грозит это упущение. Прежде
                всего, вероятно, тем, что в своих грехах будем винить близких людей. Ясно только
                одно: романтическая эра мимолетных увлечений проходит, придется учесть всю полноту
                учения. Как ее учел Б. Дандарон в 1972 г., когда, несмотря на жесткий контроль КГБ,
                сумел передать нам учение о шуньяте. Когда я пишу эти слова, думаю о своих ушедших
                братьях<a href="#citation-2" id="ref-2" class="citation-mark"><sup>2</sup></a> (о трех первых европейцах, которые получили все, но воспользоваться вряд ли
                сумели). Кроме мотивации в буддизме еще подчеркиваются способности, три вида которых
                описываются в любом тексте ламрима. Способность в буддизме значит прежде всего
                умение схватить самое главное из уст учителя. Можно слушать проповедь сто раз, можно
                слушать пятьсот раз, и ничего не запомнить (ничего не сделать личной инструкцией
                жизни). Подозреваю, что я тоже многое не услышал, несмотря на то, что я почти неделю
                жил рядом, что Бидия Дандарович (как мы его тогда называли, а вообще следовало
                говорить Видьядхара) давал читать свои переводы. Человеческие существа должны иметь
                чувство юмора, они должны думать о своих будущих жизнях (абсолютно непопулярная тема
                для "эгоизирующих" индивидов). У меня на столе лежит последнее письмо, высланное с
                каторги Выдрино 2 октября 1974 г. Там учитель пишет о смерти сансарного человека (в
                процессе созерцания); там есть очень глубокие мысли насчет практики, равных которым,
                наверное, сейчас в мире нет...
            </p>
            <p>
                Пять лет уже я читаю курс буддийской психологии в Вильнюсском университете. И каждый
                год убеждаюсь, что буддизм недоступен, -- разве только одному из ста тысяч. Нет
                понятия недостаточности жизни. Чувства есть, а понятия нет. А главное, что нет
                установки. Нет решимости жертвовать своей иллюзорной личностью и что-то
                по-настоящему понять.
            </p>
            <p>
                Это относится почти ко всем. Когда сейчас мы читаем философские и психологические
                статьи Дандарона, становится ясно, насколько они были актуальными. Скажем, текст о
                махамудре; вряд ли появилось на русском языке что-нибудь лучшее за последние 25 лет.
                Есть переводы, но нет мыслителей.
            </p>
            <p>
                Смею утверждать, что "Мысли буддиста" станут настольной книгой для каждого, кто
                интересуется буддизмом. По трем причинам: потому, что там есть научные коннотации;
                потому, что там очень хорошо излагается теория шуньяты; и потому, что в этой книге
                очень хорошо написано о практике.
            </p>
            <p>
                Кажется, что язык - основное средство выражения поэтов и мистиков - был и остается
                главным препятствием для передачи смысла. Хотя следует заметить, что буддийская
                система понятий является одной из самых тонких и совершенных инструментов.
                Интересно, что приблизительность западных философских и психологических понятий не
                дает никакого повода считать, что современные исследователи проникают глубже, или
                преодолевают однобокость лучше, или различают тоньше, чем это сумели сделать наши
                очень далекие предки.
            </p>
            <p>
                Кроме плана выражения существует и план понимания (знания, постижения). Когда
                Дандарон начал передавать свои обширные, экзистенциально проверенные знания,
                тантрийский путь был тайной. По его же собственным словам, единственный человек на
                Западе, который что-то в этом понимал, был Герберт В. Гюнтер. Уникальный человек,
                всю жизнь пытающийся найти язык на стыке разных наук (от экзистенциальной философии
                до термодинамики).
            </p>
            <p>
                Есть еще и план передачи. Симптоматично для буддизма то, что почти все Учителя даже
                не собирались кого-то учить. Они старались реализовать свою истинную природу, т.е.
                хотели сначала полностью прозреть сами, чтобы затем по-настоящему помочь другим.
            </p>
            <p>
                У меня почти нет сомнения, что подобная ситуация сложилась в середине шестидесятых
                не только в России (в широком смысле), но и вообще на Западе. Известно, что в 1964
                г. в Улан-Удэ приехал уже известный буддолог (ученик Ю. Н. Рериха) А. М.
                Пятигорский, который хотел стать учеником Б. Д. Дандарона. Но Бидия Дандарович
                рекомендовал ему известного ламу Гатовона; ведь тогда в Бурятии было непонятно,
                зачем русским нужен буддизм, так как у них имеется своя религия. И только после
                длительных просьб он согласился сам стать Учителем, взять на себя ответственность не
                только за отдельных учеников, но и за сангху (буддийскую общину).
            </p>
            <p>
                Общественная карма (понятие, не существующее в традиционном буддизме) привела нас с
                разных концов тогдашнего Союза к Б. Д. Дандарону. Мы что-то знали об оккультизме и о
                мистических традициях, но очень мало о буддийском пути самосовершенствования.
                Учитель знал, что он рискует дождаться санкций КГБ, но он был бесстрашен. Он мог
                спокойно жить и работать над тибетскими текстами, писать статьи, но избрал путь
                сострадания, путь бодхисаттвы. Он мечтал создать в Улан-Удэ научный буддологический
                центр, поэтому побуждал всех своих учеников заниматься наукой, защищать диссертации.
                Слух о настоящем Учителе распространялся быстро, и можно с уверенностью
                предположить, что число его потенциальных учеников удвоилось за те два года
                (1972--1974), что он провел в лагере.
            </p>
            <p>
                Можно указать по крайней мере четыре особенности Дандарона как религиозного
                реформатора. Во-первых, в отличие от "разъезжающих" Учителей буддизма на западе, он
                хорошо знал своих учеников и их возможности. Во-вторых, он предъявлял максимальные
                требования к себе и другим: это и постоянное указание на традицию махасиддхов, и
                передача довольно сложных систем созерцания параллельно со вступительными
                практиками. В-третьих, это его прекрасное знание языка и реалий западной жизни,
                позволяющее найти самый прямой путь к сердцу. И, в-четвертых, попытка синтеза науки
                с духовными знаниями, - преодоление расщепления, произошедшего во времена
                Возрождения.
            </p>

