            </p>
            <p>
                Это вовсе не попытка переоценить роли учеников. Дело в том, что большинство
                буддийских учителей становятся таковыми только после настоятельных просьб желающих
                практиковать; таким образом, известный из гетевского «Фауста» тезис будет звучать
                иначе: «Вначале был вопрос», или приглашение, как в последнее время. Буддизм ведь
                распространяется не внешним путем религиозной пропаганды, а благодаря внутренней
                мотивации тех, кто в нем что-то понимает. Мотивацией, связанной с первой парамитой -
                первым совершенством, которое требует щедрости и отдачи, а высшая отдача -
                поделиться тем, что постиг, что твердо знаешь, с другими. Но как это сделать, с чего
                начать? Такой вопрос себе задавали все те, судьба кого была связана с
                распространением буддизма на западе. Есть аутентичный рассказ об известном тибетском
                учителе Чогьям Трунгпа, который в молодости попал в Англию, учился в Оксфорде и
                жаловался, что, несмотря на его знания и опыт, никому он здесь не нужен. И только
