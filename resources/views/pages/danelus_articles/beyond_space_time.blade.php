                знаний жителям христианского Запада<a href="#citation-1" id="ref-1" class="citation-mark"><sup>1</sup></a>, у которых уже была своя религия (христианство)
                и свои интересы (зачастую не сопоставимые с интересами обычных буддистов).
                Оккультизм, гностицизм, Рерих, Каббала, индуизм часто были отправными точками
                учеников. Б. Д. Дандарон являлся именно той личностью, которая была нужна в этой
                ситуации. У него было и особое преимущество - он прекрасно знал русский язык и мог
                объясняться без помощи переводчика.
            </p>
            <p>
                Надо сказать, что в то время мысли о буддизме были обычно направлены на Гималаи.
                Мало кто мог вообразить, что он доступен и в нашей стране. И что традиция тут ничем
                не хуже, чем в Тибете. Об этой традиции есть смысл говорить и более пространно,
                сначала в кратком изложении биографии Дандарона.
            </p>
            <p>
                Б. Дандарон родился 15 декабря 1914 г. И попал прямо в самую сердцевину нового
                буддийского движения, так как его отец, Лама Агван Силнам Тузол Доржи, был
                сподвижником Лубсана Сандана Цыденова. Именно они и решили уйти из монастыря и
                вернуться к древней индийской традиции махасиддхов. Поселившись на красивой лесной
                поляне (по-бурятски - суархэ), они направили все свои усилия на йогическую практику,
                на созерцание. Их решимость достичь Нирваны в этой жизни была настолько велика, что
                Агван даже ходил в Китай, чтобы достать недостающие тексты для практики. Лубсан
                Сандан признал в Дандароне новое воплощение своего учителя Джаягсы-гэгэна,
