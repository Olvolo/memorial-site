                бурятской духовной традицией. Когда из Гумбума прибыли монахи, чтобы по обычаю
                забрать мальчика и готовить его к дальнейшей жизни в монастыре, его «большой
                учитель» (как Лубсана Сандана называл сам Дандарон) не согласился его отдать. В 1921
                г. в одном из храмов Бидия был публично провозглашен наследником Лубсана Сандана и
                получил от него передачу духовной силы и титула Дхармараджи - Царя учения.
            </p>
