            <p>
                В 1956 г., наконец на свободе, он некоторое время (около семи месяцев) живет в
                Москве у знакомых. Почти все время он проводит в библиотеке, читая и размышляя,
                делая заметки. В это время он знакомится с приемной дочерью своего товарища по
