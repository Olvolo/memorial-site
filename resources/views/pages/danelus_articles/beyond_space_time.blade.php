            </p>
            <p>
                После того как не удается устроиться на работу в Ленинграде, Дандарон возвращается в
                Бурятию и начинает работать в тамошнем Институте культуры (позже -- БИОН), исследует
                фонд тибетских ксилографов и рукописей. Это открывает перед ним возможность
                пользоваться одним из богатейших в мире хранилищ литературы этого рода. Вскоре
                появляются первые научные публикации. Интересно, что это совпадает с возвращением на
                родину известного тибетолога Ю. Н. Рериха, который за три года до своей
                преждевременной смерти сумел дать новый толчок вегетирующим научным исследованиям по
                буддийской литературе и традиции. Приезжал он и в Бурятию, где долго обсуждал с
                Дандароном план будущей совместной работы. К сожалению, после его кончины эта волна
                энтузиазма в Москве скоро угасла, главным образом из-за идеологического давления,
                большинство его учеников ("обитателей" кабинета Рериха) эмигрировали, и практически
                только в Улан-Удэ, хотя и с трудом, но все же удавалось издавать что-то серьезное
