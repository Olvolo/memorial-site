            <h2 class="text-center text-xl font-bold text-gray-800 mt-6 mb-4">На пороге Гражданской
                войны</h2>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Автономисты и антиаймачники</h3>
            <p>Февральская революция 1917 года запустила в Забайкалье процесс самоорганизации бурят
                и движение за автономизацию. Созванный в апреле революционного года Первый
                общебурятский съезд учредил Бурятский национальный комитет, который определил задачу
                реорганизации булучных и волостных управлений и замены их на новые территориальные
                администрации по образу и подобию военно-административных единиц, введённых
                маньчжурами в Монголии ещё в XVII веке — сомонов (стрел), хошунов (знамён) и аймаков
                (дивизий). Основной движущей силой движения за автономизацию бурят стали так
                называемые бурятские буржуазные националисты, наиболее известными представителями
                которых были социал-демократ Элбэг-Доржи Ринчино (1888–1938), общественный деятель
                Михаил Богданов (1878–1919), известные учёные Базар Барадин (1878–1937), Цыбен
                Жамцарано (1881–1942), Даши Сампилон (1891–1938), буддийский лама и дипломат Агван
                Доржиев (1856–1938) и другие<a href="#citation-6" id="ref-6"
                                               class="citation-mark"><sup>6</sup></a>. Эту группу
                нельзя было назвать единой, но в целом её члены сходились в необходимости
                обеспечения бурятам административной и культурной автономии в составе новой России.
                Обособленную от них группу составляли так называемые стародумцы (позднее
                антиаймачники), или сторонники реставрации Устава Михаила Сперанского от 1822 года<a
                    href="#citation-7" id="ref-7" class="citation-mark"><sup>7</sup></a>.
                Стародумцы, в ряды которых входили бывшие чиновники Степных дум<a href="#citation-8"
                                                                                  id="ref-8"
                                                                                  class="citation-mark"><sup>8</sup></a>
                и часть буддийского духовенства, считали отменённую на стыке веков систему
                бурятского самоуправления отвечающей главным потребностям и чаяниям бурят. Лидером
                группы стародумцев был бывший тайша Хоринской Степной думы Эрдэни Вамбоцыренов<a
                    href="#citation-9" id="ref-9" class="citation-mark"><sup>9</sup></a>. К этой же
                группе примыкала часть бурятского буддийского духовенства, которая также решительно
                выступала против каких-либо изменений в многолетней системе бурятского светского и
                духовного самоуправления.</p>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Создание органов бурятской
                автономии</h3>
            <p>Вопрос о реставрации Степных дум был поднят стародумцами во время работы Первого
                общебурятского съезда, который состоялся в Чите в первые месяцы после Февральской
                революции 1917 года. Тогда Э.-Д. Ринчино удалось уговорить стародумцев не включать
                этот вопрос в резолюцию Съезда. Вместо этого Съезд постановил рекомендовать создать
                бурятскую автономию с делением её на аймачные, хошунные и сомонные территориальные
                единицы иерархического подчинения. Высшим управляющим органом автономии стал
                Бурятский национальный комитет. В Хоринском аймаке Бурнацкомом было организовано 11
                хошунов по числу родов, входящих в состав хоринских бурят-монголов<a
                    href="#citation-10" id="ref-10" class="citation-mark"><sup>10</sup></a>. Во
                время обсуждения резолюции Съезда небольшая группа депутатов-стародумцев во главе с
                настоятелем Кудунского (Кижингинского) дацана Гэнином Цыремпиловым и бывшим
                хоринским зайсаном<a href="#citation-11" id="ref-11"
                                     class="citation-mark"><sup>11</sup></a> Бурнобадарой Далаевым
                выразила публичное недовольство использованием военной терминологии в наименовании
                территориальных единиц бурятской автономии. Она показалась им пугающей и
                предвещающей грядущую военизацию бурят-монголов<a href="#citation-12" id="ref-12"
                                                                  class="citation-mark"><sup>12</sup></a>.
                Очевидно, претензии к наименованиям территориальных единиц были лишь внешним
                проявлением недовольства стародумцев ситуацией поворота Бурнацкома в сторону
                автономизации. Тогда на недовольство кижингинцев участники Съезда большого внимания
                не обратили. Многие видные представители стародумцев получили должности в аймачных,
                хошунных и сомонных управлениях, а лидер стародумцев-консерваторов Эрдэни
                Вамбоцыренов вошёл в состав Хоринского аймачного комитета общественной безопасности.
                Компромисс между различными группами бурятских активистов, казалось, был найден.</p>

            <p>Однако вскоре соперничество между автономистами и стародумцами вылилось в тяжёлое
                противостояние. 17 июня 1917 года Временное правительство приняло постановление о
                введении земских учреждений в Сибири. Это означало крах надежд стародумцев, которые
                полагали, что теперь бурят-монголы снова остаются беззащитными перед властью
                русского большинства. Виновными в этом провале были объявлены автономисты, в
                частности Э.-Д. Ринчино, который во время работы Первого съезда убедил депутатов не
                поднимать ходатайства о восстановлении Степных дум. Напряжение ещё более усилилось,
                когда в результате выборов, организованных в Забайкалье 18 января 1918 года, в
                состав земских гласных были включены почти исключительно представители автономистов.
                Стародумцы на выборах не сумели вовремя консолидироваться и выдвинуть своих
                кандидатов, тем более что голосование проводилось по сформированным заранее спискам.
                В среде консервативно настроенных бурят заговорили о засилье представителей
                бурятской национальной интеллигенции в органах власти<a href="#citation-13"
                                                                        id="ref-13"
                                                                        class="citation-mark"><sup>13</sup></a>.
            </p>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">«Улан Туг» и вопрос о военном
                призыве</h3>
            <p>После Октябрьского переворота ситуация в Забайкалье стремительно меняется.
                Верхнеудинский Совет рабочих, солдатских и крестьянских депутатов ещё в
                сентябре–декабре 1917 года накануне взятия в свои руки всей полноты власти
                активизирует работу в бурятских аймаках<a href="#citation-14" id="ref-14"
                                                          class="citation-mark"><sup>14</sup></a>.
                Для этого в бурятских аймаках создаётся организация «Улан Туг» (Красное знамя) под
                руководством Бурнацкома, непосредственно Э.-Д. Ринчино. В момент организации в
                организацию вошло 22 человека, в основном из числа интеллигенции. Цель организации
                заключалась в выдвижении на передний фланг сочувствующих Советской власти и
                обеспечении вступления бурят в создаваемые в Забайкалье отряды Улан Цагда (Красной
                гвардии)<a href="#citation-15" id="ref-15" class="citation-mark"><sup>15</sup></a>.
                Недавно образованные земства спешно преобразовываются в Советы, а буряты
                теряют в них своё представительство. Реальная власть сосредоточивается на местах,
                где прочные позиции удерживают консервативно настроенные активисты-антиаймачники под
                фактическим руководством Эрдэни Вамбоцыренова. Они препятствуют работе просоветских
                активистов и агитируют против вступления бурятской молодёжи в ряды Улан Цагда.
                Консерваторы призывают к нейтралитету в разворачивающейся в Сибири Гражданской
                войне. На аймачных и хошунных съездах члены «Улан Туг» объявляли, что за агитацию
                против призыва и дезертирство на виновных будет возложена ответственность по
                военному времени. Однако фактически эти угрозы усилили оппозицию в среде бурят и
                даже привели к консолидации антиаймачников из разных частей Бурятии. Так, к
                противникам призыва в Хоринском аймаке присоединились жители Агинского аймака,
                Баргузина и ноёхонские станичники. Ситуация противостояния антиаймачников с
                улантугистами и Бурнацкомом в целом продлилась до августа 1918 года, когда Советская
                власть в Забайкалье была низложена и власть перешла в руки казачьего атамана
                Григория Семёнова.</p>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Движение против призыва и
                консолидация антиаймачников</h3>
            <p>Приход к власти Семёнова знаменовал новую волну противостояния антиаймачников со
                сторонниками автономии. Бурнацком, преобразованный в Бурятскую народную думу, во
                главе с Даши Сампилоном, в октябре 1918 года обращается к атаману Семёнову с
                ходатайством об организации автономии для бурят<a href="#citation-16" id="ref-16" class="citation-mark"><sup>16</sup></a>.
                Однако для Семёнова приоритетной задачей являлся призыв бурятской молодёжи в ряды
                собственных вооружённых сил — Цаган Цагда, или Белой Гвардии<a href="#citation-17" id="ref-17" class="citation-mark"><sup>17</sup></a>.
            </p>
            <p>
                Для представителей Бурнардумы вопрос об автономии фактически встал в зависимость от
                успеха кампании по военному призыву. Семёновцы пытаются уверить бурят в том, что
                призыв в Цаган Цагда не является военным призывом, поскольку буряты призываются а)
                на 6 месяцев, что существенно меньше срока для остального населения; б) призывники
                из числа бурят будут нести службу в пределах бурятских аймаков и исключительно для
                охраны внутреннего порядка. Однако эти аргументы на большинство бурят не
                подействовали, и требование о призыве ими было категорически отвергнуто.<a href="#citation-18" id="ref-18" class="citation-mark"><sup>18</sup></a>
                Несмотря на позицию бурят, атаман Семёнов не думал отступаться от идеи их
                привлечения к службе в рядах Цаган Цагда. В среде бурят настроения против призыва
                были также сильны. Они ещё более усилились, когда в январе 1919 года по бурятским
                аймакам прошёл слух о том, что известный буддийский лама-отшельник из Кижинги
                Лубсан-Самдан Цыденов собирается создать теократическое правительство с целью защиты
                бурят-буддистов от насилий Гражданской войны.</p>

            <h2 class="text-center text-xl font-bold text-gray-800 mt-6 mb-4">Сумасшедший
                святой</h2>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Многообещающее начало</h3>
            <p>Лубсан-Самдан Цыденов родился в 1850 году в улусе Кижинга Хоринского ведомства. Ещё в
                детстве Цыденов был отдан в Кижингинский (Кудунский) монастырь, где через некоторое
                время начал обучение на цаннидском (философском) факультете, продолжив его в
                резиденции Бандидо Хамбо-лам<a href="#citation-19" id="ref-19" class="citation-mark"><sup>19</sup></a>
                — Тамчинском дацане. С раннего детства у него проявилась склонность к учёбе,
                благодаря чему он получил монастырскую учёную степень габжи<a href="#citation-20" id="ref-20" class="citation-mark"><sup>20</sup></a>.
                Источник<a href="#citation-21" id="ref-21" class="citation-mark"><sup>21</sup></a>
                сообщает об особой приверженности Цыденова к медитативной практике. Цыденов
                характеризовался современниками как человек замкнутый, склонный к уединению,
                ограниченным контактам с окружающими и воздержанию в обыденной жизни. Однако
                необычайная личная харизма и исключительная учёность подняли авторитет Цыденова в
                среде верующих, что позволило ему принять участие в выборах на должность настоятеля
                Кудунского монастыря. В других источниках сообщается о том, что он, якобы, мечтал о
                своём назначении настоятелем Кудунского монастыря и даже претензиях на престол
                Бандидо Хамбо-ламы, высшей административной должности буддийской церкви Восточной
                Сибири. Тем не менее, тогда в конкурентной борьбе Цыденов проиграл другому
                претенденту, пользовавшемуся, как утверждается, поддержкой действовавшего тогда
                Хамбо-ламы Чойнзона Иролтуева. В качестве некоторой моральной компенсации за это
                поражение Иролтуев включает Цыденова в состав бурятской делегации на коронацию
                Николая Второго в 1896 году. Если верить этим сведениям, включению Цыденова в состав
                делегации содействовали и авторитетные представители хоринской родовой
                аристократии<a href="#citation-22" id="ref-22" class="citation-mark"><sup>22</sup></a>.
            </p>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Скандал на коронации</h3>
            <p>В марте 1896 года делегация на почтовых лошадях отправилась в г. Красноярск, откуда
                по Сибирской железной дороге продолжила путь в Москву<a href="#citation-23" id="ref-23" class="citation-mark"><sup>23</sup></a>.
                Светские и религиозные представители Забайкальских бурят приняли участие в церемонии
                коронации Николая Второго в Успенском соборе Московского Кремля и последовавших за
                ней пышных торжествах в Москве и Санкт-Петербурге. Во время торжественной церемонии
                коронации Цыденов отказался преклонить колени, что не только поставило всю бурятскую
                делегацию в неловкое положение, но и привело к расследованию данного инцидента
                Министерством внутренних дел. Сам Цыденов объяснил свой поступок тем, что ему как
                полному монаху дана привилегия не преклонять колени пред царём, что «неучастие его в
                данном поклонении не является преступным деянием, что поклонение буддийской
                делегации, в частности, хамбо-ламы Иролтуева как <em>гылуна</em> <a href="#citation-24" id="ref-24" class="citation-mark"><sup>24</sup></a>
                и главаря буддийского духовенства Сибири, является отступлением от закона Винаи<a href="#citation-25" id="ref-25" class="citation-mark"><sup>25</sup></a> и служит
                позором»<a href="#citation-26" id="ref-26" class="citation-mark"><sup>26</sup></a>.
                Тем не менее, как и все члены делегации, 20 августа 1896 года Самдан Цыденов был
                награждён серебряной медалью для ношения в петлице на Андреевской ленте<a href="#citation-27" id="ref-27" class="citation-mark"><sup>27</sup></a>.
            </p>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Годы отшельничества</h3>
            <p>Через два года после своего возвращения в Забайкалье Цыденов покидает монастырь и
                уходит в длительный медитативный затвор, поселившись в местности Соорхой в
                окрестностях села Кижинга вместе со своими последователями. В медитативном уединении
                Цыденов предаётся тантрийской практике созерцания идама Ваджрабхайравы (Ямантаки)<a href="#citation-28" id="ref-28" class="citation-mark"><sup>28</sup></a>.
                Многолетнее созерцание усилило его духовный авторитет в глазах простых верующих.
                Слава о Цыденове и его последователях проникает далеко за пределы долины Кудуна. В
                1907 году Цыденов назначается настоятелем Кудунского монастыря, однако даже ради
                этого не прерывает уединённого образа жизни, руководя монастырём посредством
                помощников. Спустя девять лет такой способ управления монастырём стал причиной
                отстранения Цыденова по распоряжению Бандидо Хамбо-ламы Д.-Д. Итигэлова от должности
                настоятеля<a href="#citation-29" id="ref-29" class="citation-mark"><sup>29</sup></a>.
            </p>

            <p>Из свидетельств знавших его людей известен свойственный Цыденову повышенный интерес к
                западным научным достижениям, мировым религиям, помимо буддизма, наконец, к
                государственному устройству стран Европы. Так, в любопытном рассказе российского
                этнографа Моисея Кроля, проводившего свои этнографические изыскания в восточном
                Забайкалье в конце XIX века, рассказывается о его неожиданной встрече с Цыденовым,
                произошедшей в августе 1893 года. Два дня учёному пришлось отвечать на
                многочисленные вопросы «святого ламы» о христианстве, западной философии,
                европейском понимании устройства мира, после чего он записал в своём дневнике:</p>

            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                Кто знает, сколько лет таились в его душе вопросы, которыми он меня засыпал с первой
                же нашей встречи. Утолил ли я хоть сколько-нибудь его душевный голод, я не знаю, но
                он услышал от меня много нового, он ознакомился с мировоззрением, о котором не имел
                даже смутного представления до встречи со мною... Снова он замкнётся в самого себя,
                снова он одним движением руки будет удалять из своей аскетической кельи
                благоговеющих перед ним бурят, но в его голове запечатлелось так много новых мыслей,
                что на их обдумывание и углубление он, быть может, употребит весь остаток своих дней
                в промежутки между чтением тибетских священных книг и исполнением своих обязанностей
                духовного пастыря...<a href="#citation-30" id="ref-30" class="citation-mark"><sup>30</sup></a>
            </blockquote>
            <p class="text-right"></p>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Восторженный монархист?</h3>
            <p>Ещё более уникальное и важное свидетельство о том, какое влияние оказала на
                Лубсан-Самдана Цыденова встреча с Европой, мы видим в уже упоминавшейся нами
                пространной поэме на тибетском и монгольском языках, посвящённой интронизации
                Николая Второго. Поэма написана в очень сложной манере, обильно наполнена
                стихотворными метафорами, отсылками в область классической индийской мифологии,
                религиозно-философской лирикой, пышной патетикой. В поэме автор прямо называет
                российского монарха <em>чакравартином</em> <a href="#citation-31" id="ref-31" class="citation-mark"><sup>31</sup></a>,
                осознавая и подчёркивая при этом его иноконфессиональность. Как правило, этот титул
                использовался в пределах буддийской ойкумены, и правители, называвшиеся таким
                образом, позиционировали себя в качестве буддистов. Использование этого сакрального
                титула по отношению к Николаю представляется нам даже более важным, чем все другие
                метафоры, какие использует автор для обозначения мощи и величия российского
                императора, как индо-буддийских — Тара Чинтаманичакра, Вишну, Индра, Рама, бог
                богов, превосходящий Махешвару, так и монгольских — Установленный силою вечного
                Неба.</p>

            <p>Ещё один важный момент, который необходимо подчеркнуть в этой связи, связан с якобы
                имевшим место отказом Цыденова от поклонения уже коронованному императору Николаю.
                Этот неординарный поступок может показаться нам мало соответствующим тому
                настроению, с которым автор писал восторженный гимн, посвящённый коронованию и
                аудиенции. И действительно, в поэме мы находим практически совершенно иное описание
                происходившего во время приёма:</p>

            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                Словно луна среди звёзд, пленяющий ум с первого взгляда, незабываемый владыка людей
                вышел к нам вместе со своей супругой. [...] И в тот момент, как пространство
                обширного моря покрывается рябью волн, моё тело покрылось златыми одеждами
                поднявшихся волосков, сопровождаемых дрожью. Склонившись в поклоне и трижды
                распростёршись, я преподнёс вещи, символизирующие пожелание долгой и крепкой жизни —
                приветственный шарф и статуэтку божества долгой жизни<a href="#citation-32" id="ref-32" class="citation-mark"><sup>32</sup></a>.
            </blockquote>
            <p class="text-right"></p>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">К идеалам монархии</h3>
            <p>Можно предположить, что Самдан Цыденов действительно мог отказаться от следования
                церемониалу в месте поклонения императору, сославшись на упомянутые в очерке вполне
                резонные аргументы. Это тем более вероятно, если мы примем во внимание известную нам
                принципиальность и неординарность кудунского отшельника. Тем не менее, на страницах
                своего произведения Цыденов трижды простирается перед монаршей особой, декларируя
                тем самым своё отношение к императору как к фигуре сакральной. Поэма, таким образом,
                может отражать некое идеальное инобытие происходившего в реальности, которое не во
                всём могло ему соответствовать. Иными словами, Самдан Цыденов не был готов к
                церемониальному поклонению перед императором в конкретной ситуации, но внутренне
                царь как носитель власти вполне соответствовал объекту его поклонения. Так или
                иначе, но нам представляется вполне естественным предположить, что человек,
                написавший столь значительное и торжественное произведение, посвящённое блеску,
                великолепию и священности монархической власти, пусть лишь частично относящейся к
                буддийской цивилизации, предполагал священной монархию как таковую. Известие о
                расстреле царской семьи было распространено российскими газетами летом 1918 года, и
                можно догадаться, какое впечатление могло оно произвести на человека, считавшего
                императора божественной персоной. Могла ли в его глазах быть легитимной любая другая
                власть, будь то Временное правительство, Советы, органы власти бурятской автономии
                или атаман Семёнов? Уничтоженную монархию могла заменить только другая монархия,
                обладающая прямыми атрибутами сакральности — власть просветлённого божества.</p>

            <p>Эта идея, по всей видимости, появилась в голове у Цыденова осенью 1918 года, когда
                конфликт между представителями Бурнардумы с одной стороны и антиаймачниками с другой
                по вопросу о военном призыве достиг острой фазы. Противникам призыва и автономизации
                не хватало единого лидера и альтернативной идеи, которая могла бы быть
                противопоставлена движению автономизации Бурятии. Идея буддийской теократии была уже
                весьма популярна в среде верующих буддистов, ибо перед их примером стояли живые
                примеры Далай-ламы или Джебцун-Дамба-хутукты, стоявших на вершине теократий Тибета и
                Монголии. В те же годы атаман Семёнов оказывал поддержку планам создания
                панмонгольского государства под руководством ламы из Внутренней Монголии
                Нэйсэ-гэгэна Мэндэбаяра<a href="#citation-33" id="ref-33" class="citation-mark"><sup>33</sup></a>. Идея витала в
                воздухе, была понятна и близка бурятским буддистам. Лично или через своих
                приближённых Лубсан-Самдан Цыденов передаёт эту идею авторитетным кижингинским
                антиаймачникам, которые начинают действовать.</p>

            <h2 class="text-center text-xl font-bold text-gray-800 mt-6 mb-4">Как строилась
                Кудунская теократия</h2>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Приглашение на престол</h3>
            <p>В условиях административного хаоса, воцарившегося по всей России в годы Гражданской
                войны, кредитные товарищества и потребительские общества часто были единственными
                структурами, выполнявшими какие-то организационные функции. В Кижинге кредитное
                товарищество фактически стало той площадкой, на основе которой создавался проект
                конституционной теократии Лубсан-Самдана Цыденова. В январе 1919 года члены
                правления Кижингинского кредитного товарищества были созваны на внеочередное
                закрытое заседание. Речь на этом заседании шла не о финансовых делах, а вопросе
                политической важности: выходе Кижинги из состава Хоринского аймака и создании на
                территории бывшего Бодонгутского сомона государства на принципах теократии<a href="#citation-34" id="ref-34" class="citation-mark"><sup>34</sup></a>.
                Инициаторами дела стали местные жители С. Генинцыбенов, Г. Гармаев, Д. Иролтуев и С.
                Гончикдараев, которые получат в будущем теократическом правительстве видные посты.
                Активисты предложили членам товарищества организовать церемонию поднесения ламе
                Лубсан-Самдан Цыденову <em>мандалы</em><a href="#citation-35" id="ref-35" class="citation-mark"><sup>35</sup></a> с просьбой
                взойти на престол религиозного главы государства и обеспечить местным
                бурятам-буддистам защиту от военного призыва и вообще от политики Бурятского
                национального комитета. По решению собрания в среде кижингинских бурят начинается
                агитация за поддержку коллективного письменного прошения и организуется сбор средств
                для пожертвований будущему правителю<a href="#citation-36" id="ref-36" class="citation-mark"><sup>36</sup></a>.</p>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Структура государства</h3>
            <p>Цыденов даёт инициативной группе инструкции по вопросам составления конституции
                учреждаемого государства, в соответствии с которой должны быть сформированы его
                основные институты, и созыву учредительного собрания. В созданную конституционную
                комиссию входит 22 человека, среди которых в основном высокопоставленные ламы
                Кудунского дацана и чиновники из волостного/сомонного управления<a href="#citation-37" id="ref-37" class="citation-mark"><sup>37</sup></a>.
                Четвёртого мая 1919 года проект Конституции Теократического государства Кудунской
                долины, состоящий из 36 пунктов, был закончен. В проекте оговаривается структура
                светских административных органов государства, официально получившего имя «Эрхэтэ
                Балгасан Улус», что можно перевести как «Суверенное государство балагатов». Под
                термином «балагат» создатели государства понимали крупнейшую
                административно-территориальную единицу, в которую входили тосхоны, которые примерно
                соответствовали бывшим сомонам. Балагаты же соответствовали хошунам. Территориальных
                образований, соответствовавших аймакам, в новом государстве попросту не существовало<a href="#citation-38" id="ref-38" class="citation-mark"><sup>38</sup></a>.
            </p>

            <p>Согласно основному закону<a href="#citation-39" id="ref-39" class="citation-mark"><sup>39</sup></a>, Кудунское
                государство находилось под верховным управлением Йогачари Номун Хана, Цог Тугульдур
                Дармаранза<a href="#citation-40" id="ref-40" class="citation-mark"><sup>40</sup></a>
                Лубсан-Самдана Цыденова. Он управляет государством по праву человека, реализовавшего
                в себе тантрийское божество Ямантака и выполняющего волю последнего. Следующим во
                властной иерархии Кудунской теократии было представительное собрание — Великий
                суглан (монгол. yeke čiγulγan). Депутаты в Суглан избираются прямым тайным голосованием из расчёта один депутат от
                ста избирателей. Избирательным правом наделялись жители старше 15 лет обоих полов.
                Депутаты Суглана, в свою очередь, выбирают членов президиума. В юрисдикции Суглана
                находится государственный бюджет и назначение размеров жалования.</p>

            <p>В непосредственном подчинении у Дармаранзы находятся следующие должностные лица,
                осуществляющие оперативное управление государством: 1. президент (erkin sayid), 2.
                вице-президент (did sayid), министры (ambôs). В состав правительства входят министры
                внутренних (dotoγad kereg-ün) и иностранных дел (γadaγad kereg-ün), юстиции
                (qauli-yin), царского двора (qan ordon-u)), торговли и промышленности (mayimaγan ba üjübüri-yin), финансов (sang jögeri-yin), земледелия (γajarül edbüri-yin) и
                народного просвещения (arad-i gegeregülegči-yin), а также их заместители (nükür tusalaүči). Все выбранные должностные лица должны были утверждаться Дармаранзой.</p>

            <p>Все важные правительственные решения должны были утверждаться Дармаранзой, однако
                решения по текущим делам в рамках оперативного управления президент принимал
                самостоятельно. Соответственно, правительственные распоряжения проходили
                согласование у президента.</p>

            <p>В состав теократии входило 11 балагатов: кудунский, шелсанинский, кижингинский,
                сарантуевский, оротский, улан-бургасунский, цаганурский, моксохонский, чесанский,
                ехэбулакский, кулькисонский. Балагатские управления (balaγad-unjakiruγ-a),
                находившиеся под руководством глав администраций (balaγad-un ejen) и их помощников.
                Должностные лица балагатского уровня также выбирались жителями балагата на срок в
                два года. Тосхоны, из которых состояли балагаты, в свою очередь управлялись даргами
                (daruγ-a), которые избирались на один год. Балагатское собрание, на котором
                избирались главы балагатских администраций, должны были проходить при участии не
                менее двух третей избирателей. Назначать ли жалование должностным лицам также решали
                сами избиратели.</p>

            <p>Третьей ветвью власти являлся балагатский суд, который возглавлялся главой
                администрации балагата и двумя выборными судьями-шуленгами (siülengge). Кандидатуры
                шуленг должны пройти через утверждение министра юстиции. Все дела, не входившие в
                юрисдикцию балагатского суда (вероятно, тяжкие преступления), рассматривались
                юридическим отделом министерства юстиции с участием не менее чем трёх глав
                балагатской администрации.</p>

            <p>Для охраны внутреннего порядка вводилась должность надзирателя (čaγdaγači) и его
                помощника, в подчинении которых находились балагатские стражи порядка (amuγulang-i kinaγčid).</p>

            <p>Все должностные лица государства Эрхэтэ Балгасан Улус должны были иметь печати и
                знаки отличия. Для утверждения в должностях они были обязаны пройти через процедуру
                принятия присяги на верность Ламе Дармаранзе.</p>
            <p>
                Одновременно с утверждением
                конституции, Лубсан-Самдан Цыденов, ныне именуемый теократическим главой и Ламой
                Дармаранзой, объявил о созыве Великого Суглана 14 мая 1919 года (первого года по
                летосчислению Балагатского государства) в местности Шалсана в долине реки Кудун<a
                    href="#citation-41" id="ref-41" class="citation-mark"><sup>41</sup></a>. В
                назначенную дату было избрано и собрано 102 депутата, которые утвердили рабочий
                вариант конституции, объявили о создании нового государства и приняли решение
                преподнести мандалу Лубсан-Самдану Цыденову с просьбой его возглавить. Кроме того, в
                качестве официального наследника царя-деспота Самдана Цыденова был назначен его
                ближайший ученик Доржи Бадмаев. Депутаты утвердили в высших государственных
                должностях инициаторов создания государства. Так, президентом Кудунской теократии
                большинством голосов был избран Сандылык Гончикдараев, вице-президентом Доржи
                Иролтуев, председателем Совета министров С. Гэницыбенов. После окончания первого
                заседания Великого Суглана его члены в полном составе направились в местность
                Халцагай Толгой для вручения Лубсан-Самдан Цыденову мандалы с просьбой о вступлении
                на трон. Избранные Сугланом должностные лица тут же принимают присягу и приступают к
                своим обязанностям<a href="#citation-42" id="ref-42" class="citation-mark"><sup>42</sup></a>.</p>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Столкновение с реальностью</h3>
            <p>В первых числах мая правительство вновь созданного государства собирается на своё
                первое заседание. В этот же день по приказу Григория Семёнова в Хоринский аймак
                прибывает управляющий Верхнеудинским уездом полковник Корвин-Пиотровский.
                Одновременно с этим из Читы в Хоринский аймак командируется казачий офицер Рабданов
                с вооружённым отрядом. Оба доверенных подчинённых атамана Семёнова имели приказ
                произвести расследование происходящего в аймаке и при необходимости подвергнуть
                аресту инициаторов сепаратистского движения<a href="#citation-43" id="ref-43"
                                                              class="citation-mark"><sup>43</sup></a>.
                В связи с тем, что теократическое государство Лубсан-Самдана Цыденова было
                организовано на принципах ненасилия, а в составе его правительства отсутствовало
                военное ведомство, оно фактически оказывалось беззащитным перед любыми вооружёнными
                силами. Единственным способом самозащиты, имевшимся в арсенале подданных
                Балагатского государства, были обращения и воззвания. Так, узнав о приближении
                семёновских отрядов, члены правительства теократического государства шлют одну за
                другой депеши в Читу в Бурятскую Народную Думу и лично атаману Григорию Семёнову с
                требованием оставить в покое созданную теократию<a href="#citation-44" id="ref-44" class="citation-mark"><sup>44</sup></a>.
                В то же время среди населения распространялось убеждение, что благодаря чудесной
                силе Дармаранзы, все его противники, разумеется, будут посрамлены и обезврежены, их
                оружие превращено в безделушки, а резиденция Цыденова будет окружена чудесно
                возникшими укреплениями<a href="#citation-45" id="ref-45"
                                          class="citation-mark"><sup>45</sup></a>. Между тем, отряды
                полковника Корвина-Пиотровского и Рабданова соединяются и направляются прямиком в
                урочище Халцагай-Толгой, к тому времени переименованное в Соёмпкус. Вместе с ними
                сюда же прибывают должностные лица аймачной администрации — член аймачной думы Дул
                Цыденов и управляющий уездом по Хоринскому аймаку Эрдэни Вамбоцыренов<a
                    href="#citation-46" id="ref-46" class="citation-mark"><sup>46</sup></a>.
                Последний при этом находился в весьма стеснённом положении, поскольку был
                сторонником и последователем Цыденова, однако в данной ситуации представлял аймачную
                администрацию, признанную атаманом Семёновым.</p>

            <p>После двукратного ультимативного требования добровольной сдачи властям, Лубсан-Самдан
                Цыденов вынужден был подчиниться. Источники сообщают, что, увидев вооружённое
                оцепление вокруг своей кельи, Цыденов с удивлённым тоном заявил:</p>

            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                Эти войска, очевидно, мобилизованы для ликвидации моего государства в лице меня и
                правительства и являются охраной пребывающих здесь слухсебцев, что кажется для меня
                чудным делом.<a href="#citation-47" id="ref-47" class="citation-mark"><sup>47</sup></a>
            </blockquote>

            <p>Лубсан-Самдан Цыденов и ряд членов его правительства были подвергнуты аресту. Часть
                министров и других высокопоставленных чиновников успела скрыться в отдалённых улусах
                и лесных заимках. В связи с этим арестам и допросам были подвергнуты их родные, во
                время которых семёновцы допускали насилие и телесные наказания. Офицер Рабданов
                осмелился даже наказать розгами ламу Кижингинского дацана Галсанова за его
                пособничество в деле побега настоятеля дацана Гынина Цыремпилова. Впоследствии из-за
                этого эпизода Корвин-Пиотровский вынужден был отстранить от служебных обязанностей
                некоторых подчинённых Рабданова<a href="#citation-48" id="ref-48"
                                                  class="citation-mark"><sup>48</sup></a>.</p>

            <p>В келье Цыденова был устроен обыск с целью выявления документов или предметов,
                относящихся к делу. В ходе обыска, как сообщается в источниках, в келье среди
                прочего были обнаружены корона с изображением ваджры и человеческого черепа,
                дневники с реестром лиц, внесших пожертвования, буддийская религиозная литература,
                библиотека книг и журналов на европейских языках, а также пояс с электрическими
                лампочками, который Цыденов якобы приобрёл в Петербурге и использовал для оказания
                воздействия на верующих<a href="#citation-49" id="ref-49" class="citation-mark"><sup>49</sup></a>.
            </p>
            <p>Спустя несколько дней Лубсан-Самдан Цыденов, его приближённый Доржи Бадмаев и ряд членов его
                правительства были препровождены в Верхнеудинскую тюрьму и допрошены. В частности,
                как сообщают нам наши источники, Цыденов на допросе заявил:</p>

            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                В связи с свержением царизма и образованием Временного Правительства во многих
                местах России создавался ряд новых государств, объявивших независимое их
                существование, как по инициативе отдельных авторитетных лиц, так и по диктовке тех
                или иных партий и организаций, что создало между ними раскол и вражду, междоусобицу,
                анархию, мобилизацию, объявление друг другу войны и т.д. Как передавали мне
                прибывающие поклонники буряты, жалуясь на действия бурятской интеллигенции, на
                стремление их к призыву бурятской молодёжи на службу в <em>цагды</em> для охраны создаваемой
                по инициативе их национальной автономии и т.д. Отсюда я был уверен в том, что ныне
                открыт широкий путь организации любого государства, любого политического общества и
                любой организации в порядке революционных действий. В данном случае я вынужден
                провозгласить себя царём-деспотом теократического государства, преследующего цели
                борьбы против призыва и войны, за мир на основе специальных правил, предусмотренных
                в религиозных догматах, уповая на то, что буряты-буддисты, как избегающие
                автономного строя, влекущего за собою привлечение их к военной службе, будут стоять
                за мою идею, что данное государство будет охранено сверхъестественной силой
                бога-хранителя и что идеи не будут опровергнуты окружающим русским населением как
                пострадавшим в результате войны и междоусобиц. Но несмотря на это, я считаю свою
                инициативу заблуждением, объясняемым моей старостью, политической неосведомлённостью
                и нахождением в изолированном от общества созерцании во времена упомянутых выше
                событий.<a href="#citation-50" id="ref-50" class="citation-mark"><sup>50</sup></a>
            </blockquote>

            <p>Это заявление Цыденов подкрепил официальным призывом к своим подданным и членам
                организованного им правительства более не считать его своим царём и перейти в
                подчинение властей правительства атамана Семёнова. Этот призыв остановил начавшееся
                уже движение населения других бурятских аймаков, таких как Батанай-Харганатский,
                Хуацайский, Чикойский, Селенгинский, Оронгойский в подданство к Самдану
                Цыденову.</p>

            <p>Этих объяснений и добровольного отречения от власти со стороны Цыденова семёновским
                властям было достаточно. Очевидно, они опасались ухудшения отношений с бурятским
                населением, в среде которого авторитет Цыденова был очень высоким. Власти
                демонстративно наказали милиционеров под началом Рабданова за допущенные перегибы и
                освободили всех задержанных сторонников Цыденова, а также его самого. Для
                Лубсан-Самдана Цыденова покаянные заявления были, как покажут дальнейшие события,
                лишь тактическим приёмом. Возвращение Цыденова в Кижингу было триумфальным. Его
                встречали многочисленные верующие, многие из которых считали сам факт освобождения
                Цыденова как подтверждение его духовного могущества. По возвращению в Соёмпкус,
                Цыденов объявил произошедшее интригами со стороны бурятских автономистов, стремясь
                таким образом направить возмущение его последователей на членов Бурятской народной
                думы, а не на семёновцев. Вероятно, таким образом Цыденов пытался показать своим
                подданным, что со стороны Григория Семёнова к нему претензий нет, а его деятельность
                находится в правовом поле. Многим последователям Цыденова тогда могло показаться,
                что арест и допросы были чистой воды недоразумением. Вероятно, эта позиция в дальнейшем на некоторое время убедит и атамана Семёнова в
                том, что Кудунская теократия безобидна для его режима.</p>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Конфликты вокруг военного призыва</h3>
            <p>Между тем, самым принципиальным вопросом во взаимоотношениях теократов с семёновскими
                властями оставался вопрос о призыве бурят в отряды Цаган Цагда. Антивоенная
                агитация, энергично проводившаяся ими на местах, а также обращения к правительству
                Семёнова с требованием освободить бурят от воинской службы вынуждали Семёнова на
                ответные действия. В период с мая по декабрь 1919 года Лубсан-Самдан Цыденов и
                некоторые его приближённые трижды подвергались арестам и помещались в тюрьмы
                Верхнеудинска и Читы, однако каждый раз время задержания было относительно коротким
                (в среднем около месяца)<a href="#citation-51" id="ref-51" class="citation-mark"><sup>51</sup></a>. В среде верующих
                последователей Цыденова крепло убеждение, что их духовный лидер неуязвим перед
                репрессиями властей и всякий раз чудесным образом выходит из заключения. После
                одного из арестов близкие Цыденову Д. Бадмаев и С. Гынинцыденов скончались от тифа,
                и рядом с кельей Цыденова в Соёмпкус в их честь были возведены ступы. Причём в
                течение нескольких дней телу Доржи Бадмаева поклонялись верующие, а затем оно было
                помещено в ступу как в мавзолей. В связи со смертью Бадмаева наследником Цыденова
                был объявлен его трёхлетний пасынок Бидья Дандарон<a href="#citation-52" id="ref-52" class="citation-mark"><sup>52</sup></a>.
            </p>

            <p>Между тем теократы продолжали противодействовать инициативам власти по вопросу о
                привлечении бурят к военной службе. Это противодействие происходило на фоне
                успешного сотрудничества атамана Семёнова с другими представителями буддийского
                духовенства Бурятии и Монголии, такими как Бандидо Хамбо-лама Гуро-Дарма Цыремпилов,
                а также Нэйсэ-гэгэн и Тойн-хутухта из Внутренней Монголии<a href="#citation-53" id="ref-53" class="citation-mark"><sup>53</sup></a>.
                Так, в рамках созванного по инициативе Бурятской Народной Думы в октябре 1919 года
                Съезда представителей бурят Забайкальской области, почётными делегатами которого
                были и вышеназванные духовные лица, Думе было рекомендовано организовать военное
                управление с целью организации призыва бурят в национальное бурятское войско с целью
                «возрождения родины и установления порядка внутри страны»<a href="#citation-54" id="ref-54" class="citation-mark"><sup>54</sup></a>.
                Кудунские теократы участия в работе съезда не приняли и, более того, заявили
                Семёнову, что ничьих распоряжений о проведении военного призыва в своём аймаке
                выполнять не намерены. Раздражённый упорством Цыденова, Семёнов в очередной раз
                подверг его непродолжительному аресту<a href="#citation-55" id="ref-55" class="citation-mark"><sup>55</sup></a>.
                Однако уже в марте 1920 года его режим пал, а сам Семёнов бежал в Маньчжурию.</p>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Противостояние с новыми
                властями</h3>
            <p>Стремительное наступление Красной армии в Забайкалье, как известно, не привело к
                немедленному установлению здесь Советской власти. В апреле 1920 года Хоринский аймак
                оказывается на территории Дальневосточной республики, буферного государства,
                созданного Учредительным съездом трудящихся Прибайкалья. Тогда же в ДВР учреждается
                национально-территориальное образование Бурят-Монгольская автономная область, в
                состав которого были включены Агинский, Баргузинский, Хоринский и Чикойский аймаки.
                Предполагалось, что область должна была пользоваться всеми правами автономного
                образования и действовать самостоятельно в судебной, административно-экономической и
                культурной жизни. Часть бурятского населения, проживавшего на территориях
                Баргузинского, Верхнеудинского, Селенгинского и Троицкосавского уездов, оказалась
                включённой в состав Прибайкальской области со столицей в Верхнеудинске<a
                href="#citation-56" id="ref-56" class="citation-mark"><sup>56</sup></a>.</p>

            <p>Для руководства процессом советизации в бурятских аймаках ДВР создаётся Бурятский
                народный революционный комитет (Бурнарревком), во главе которого был поставлен
                представитель бурятской интеллигенции Пётр Дамбинов<a href="#citation-57"
                                                                      id="ref-57"
                                                                      class="citation-mark"><sup>57</sup></a>.
                Воспользовавшись сменой власти, ряд видных деятелей теократического движения входят
                в состав вновь учреждённых сомонных и хошунных ревкомов. Кроме того, теократам
                удалось установить близкие союзнические отношения с высокопоставленными должностными
                лицами Прибайкальской области — начальником участка Верхнеудинской уездной милиции
                Алексеем Уваровым, старшим следователем Прибайкальского Политнарсуда Карповичем и
                др. С их помощью теократам удаётся усилить свои позиции в аймаке и даже развернуть
                вооружённую борьбу против своих противников, которых теократы обвиняли в
                сотрудничестве с режимом атамана Семёнова<a href="#citation-58" id="ref-58"
                                                            class="citation-mark"><sup>58</sup></a>.
                Вообще сторонники Цыденова активно привлекали на свою сторону союзников. Так, через
                свои связи им удалось издать через Военное управление Прибайкальской области брошюру
                «За что борются теократы», в которой некто Григорий Стрефьев проводил мысль о том,
                что теократы являются тем самым угнетаемым меньшинством, свободу которым призвана
                обеспечить новая власть. Брошюра, впрочем, была почти сразу же конфискована, а её
                автор привлечён к ответственности<a href="#citation-59" id="ref-59"
                                                    class="citation-mark"><sup>59</sup></a>.</p>

            <p>Однако правительство ДВР с самого начала берёт курс на усиление аймачных комитетов в
                БМАО и борьбу с сепаратизмом. В рамках этого курса правительство ДВР распоряжается
                арестовать Самдана Цыденова, президента Балагатского государства Сандылыка
                Гончикдараева и ряд министров теократического правительства. Цыденов был помещён в
                Верхнеудинскую тюрьму, откуда уже более в Кудунскую долину не вернулся<a
                    href="#citation-60" id="ref-60" class="citation-mark"><sup>60</sup></a>.
                Сохранились тюремные записки, которые Цыденов вёл в этот период своей жизни<a
                    href="#citation-61" id="ref-61" class="citation-mark"><sup>61</sup></a>. Среди
                прочего, они содержат выписанные из имевшегося в его распоряжении энциклопедического
                словаря многочисленные европейские термины из области политической теории,
                государственного устройства. Значительное место в его конспектах занимает термин
                "теократия" в следующем его определении:</p>

            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                Теократия — образец правления, при котором главой государства считается бог, будто
                бы передающий повеления и запрещения жрецам; управление государством духовенства,
                действующего будто бы по внушению божества; самое государство с таким строем.<a href="#citation-62" id="ref-62" class="citation-mark"><sup>62</sup></a>
            </blockquote>

            <p>Уже постфактум Цыденов целенаправленно ищет в литературе примеры иерократической и
                теократической форм правления в других странах мира, теоретическое обоснование той
                формы власти, которое он избрал для своего государственного проекта.</p>

            <p>Помимо этого, сохранился фрагмент черновой рукописи обращения Цыденова в Политический
                суд Прибайкальской области ДВР. В нём он даёт собственное (хотя и несколько
                официозное) объяснение мотивов создания теократии:</p>

            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                Я действительно являюсь Царём Учения (Дхармараджей) Трёх Миров. Эта власть
                ниспослана мне божеством. Поскольку для своих последователей я являлся спасителем,
                они во время встреч со мной при поклонении проводили и богослужение о здравии с
                подношением даров. Приведу пример для объяснения того, какой должна быть степень их
                подношений для приобретения собрания заслуг. В давние времена один хан Чакравартин,
                покоривший четыре материка, силою сокровища Чинтамани, исполняющего желания, излил
                драгоценный дождь, пока он не наполнил материки, и преподнёс буддийской религии своё
                объединённое ханство. Так вот, чтобы понять степень и меру этих подношений, нужно
                понимать их равными этому подношению. Понимать это как жажду к наживе есть большое
                заблуждение. Возглавляемая мною вера содержится в океане тантрийского учения,
                достигшего пределов всех благих наставлений Будды. Поскольку источником этих учений
                является вместилище десяти сторон и трёх времён Великий Владыка будд Ваджрадхара, не
                буду распространяться на этот счёт.<br>Так как у нас теократия, то большинство
                сторонников являются адептами, а потому им необходимо иметь атрибуты, знаки,
                придерживаться подобающего поведения. Они связаны запретом на убийство и прочими
                обетами чистоты. Согласно им, ни в коем случае нельзя служить в армии. Основывая
                теократическое государство, я руководствовался принципом совмещения религиозного и
                мирского принципов. Он не предполагает разделения на религиозные и гражданские дела.
                Причиной этому является то, что власть совмещает в себе религию и государство.
                Поскольку теократическая политика связана с религией, она рассматривает политику и
                религию в тесной связи. Таким образом, поступки, связанные с "ламой-деспотом",
                являются религиозными, и нельзя считать всё совершённое мной преступлениями.<a href="#citation-63" id="ref-63" class="citation-mark"><sup>63</sup></a>
            </blockquote>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Тучи сгущаются</h3>
            <p>Несмотря на длительное тюремное заключение Самдана Цыденова, его сторонники не
                сбавляли своей активности. В феврале–марте 1921 года ламы Чесанского дацана
                Хоринского аймака в числе 300 человек объявили о своём отделении от дацана и вообще
                от централизованной буддийской общины Бандидо Хамбо-ламы. Отколовшаяся группа лам
                обратилась к Лубсан-Самдану Цыденову, в скором выходе из-под ареста никто, похоже,
                не сомневался, с просьбой взять их под своё покровительство. Этими ламами и их
                последователями был сооружён новый дацан в местности Чулутай Чесанского сомона
                Цаганского хошуна. Однако после того как Хамбо-лама Гуро-Дарма Цыремпилов подал
                протест в Бурнарревком, последний 29 апреля 1921 года вынес постановление о запрете
                самовольного строительства дацана<a href="#citation-64" id="ref-64" class="citation-mark"><sup>64</sup></a>.</p>

            <p>Тогда теократы пошли в наступление в другом направлении. В том же месяце при
                поддержке милицейского начальника А. Уварова и других должностных лиц администрации
                соседней Прибайкальской области, лидеры балагатского движения созывают собрание
                жителей Хальбинского, Цаганского и Бодонгутского хошунов, на котором принимается
                решение о выходе этих хошунов из состава бурятской автономии и переходе в подчинение
                правительства Прибайкальской области. Для этого была реанимирована вся та
                административная система Балагатского государства, которая двумя годами прежде была
                прописана в конституции: балагатские и тосхонные управления во главе с эзэнами и
                даргами. Теократы отказывались платить налоги в пользу Бурят-Монгольской автономной
                области, организовали собственные милицейские отряды. Руководство некоторых сомонных
                и хошунных администраций Хоринского аймака добровольно сдавали дела балагатским
                начальникам, а по всему аймаку началась травля так называемых автономистов. При этом
                деятельную помощь в задержании сторонников автономии теократам оказывали милицейские
                работники Прибайкальской области, чьи действия координировались следователем
                Карповичем. Кампания против автономистов проводилась под видом борьбы с остатками
                «семёновских прислужников». В результате множество сомонных и хошунных работников
                вынуждено было бежать в соседние аймаки или скрываться в лесах<a href="#citation-65" id="ref-65" class="citation-mark"><sup>65</sup></a>.
            </p>

            <p>Летом 1921 года по просьбе всё ещё находившегося в тюрьме Лубсан-Самдана Цыденова
                старые теократы Г. Цыремпилов, Н. Сампилов, Ш. Цыбиков, Д. Доржиев, Ц. Базаров и
                другие организуют при вновь открытом дацане в местности Чулутай торжества в честь
                восстановления Балагатского государства. В рамках празднеств, продолжавшихся в
                течение 2–3 дней, были проведены молебны, конские скачки и борьба. Союзники
                кудунских балагатов — должностные лица Прибайкальской области Уваров и Карпович —
                были торжественно награждены представителями балагатского движения хадаками и
                меховыми шкурками за помощь, оказанную ими Кудунской теократии. Главным пунктом
                программы празднеств стала торжественная интронизация наследника Лубсан-Самдана
                Цыденова — девятилетнего пасынка Доржи Бадмаева Бидьядары (Бидьи) Дандарона,
                впоследствии известного религиозного философа, учёного-тибетолога и буддийского
                диссидента<a href="#citation-66" id="ref-66" class="citation-mark"><sup>66</sup></a>.
                За некоторое время до этого Дандарон был признан Лубсан-Самдан Цыденовым
                перерождением неоднократно посещавшего Кижингу тибетского ламы-хубилгана из
                амдоского монастыря Гумбум Джампалинг<a href="#citation-67" id="ref-67" class="citation-mark"><sup>67</sup></a>
                Джаяк-гэгэна<a href="#citation-68" id="ref-68" class="citation-mark"><sup>68</sup></a>. Впоследствии за свой статус
                перерожденца и невольную причастность к теократическому движению Бидья Дандарон
                подвергнется многолетним репрессиям и гонениям.</p>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Движение к компромиссу</h3>
            <p>Между тем, ситуация в Хоринском аймаке обострялась в связи с участившимися стычками
                между молодёжью, поддерживавшей теократов, и аймачниками<a href="#citation-69" id="ref-69" class="citation-mark"><sup>69</sup></a>.
                Активизация антиаймачных настроений заставляет правительство Дальневосточной
                республики действовать. В июле 1921 года сюда командируется правительственная
                комиссия под руководством члена правительства ДВР Д. С. Шилова. После проведения
                расследования, комиссия категорически потребовала от участников балагатского
                движения сдать огнестрельное оружие и подчиниться аймачным и хошунным властям. Члены
                комиссии также потребовали от должностных лиц Прибайкальской области прекратить
                любые вмешательства в дела одного из аймаков Бурят-Монгольской автономной области.
                Кроме того, 1 августа 1921 года в местности Чулута Бодонгутского хошуна Хоринского
                аймака Д. С. Шиловым было проведено собрание с участием представителей враждующих
                сторон, на котором была осуществлена попытка найти компромисс путём привлечения
                некоторых лидеров балагатского движения к работе в аймачной и хошунных
                администрациях. Из-под стражи были выпущены на свободу задержанные ранее теократы
                (Лубсан-Самдан Цыденов, впрочем, в это число не вошёл)<a href="#citation-70" id="ref-70"
                class="citation-mark"><sup>70</sup></a>.
                Тем не менее, личным распоряжением Шилова были арестованы и препровождены в
                Читинскую и Верхнеудинскую тюрьмы Уваров и Марков. Позднее был задержан и Карпович.
                Источники сообщают лишь о судьбе Карповича, который впоследствии был расстрелян по
                решению Амурского губернского суда как контрреволюционер<a href="#citation-71" id="ref-71"
                 class="citation-mark"><sup>71</sup></a>.
            </p>
            <p>Результаты работы комиссии Шилова были закреплены работой ещё одной комиссии,
                командированной правительством ДВР. Руководителем этой комиссии был назначен Н.
                Преловский. Деятельность комиссий Шилова — Преловского привела к компромиссу с
                теократами. Как и семёновцы, власти ДВР не были заинтересованы в ухудшении отношений
                с хоринскими буддистами. В сентябре 1921 года на выборах в Бурят-Монгольское
                областное народное собрание по Хоринскому аймаку депутатами были избраны видные
                деятели балагатского движения: Цыдып Цыденжабон (сын бывшего министра земледелия
                Кудунской теократии Ц. Ж. Цыремпилова), Бадмацырен Бониев (бывший министр
                иностранных дел), Базарсада Шагдаров (бывший товарищ министра торговли), Данзан
                Сампилон (бывший товарищ министра народного просвещения) и др. В самом аймаке 25
                сентября 1921 года на собрании жителей Бодонгутского хошуна было достигнуто
                перемирие между теократами и автономистами, которые согласились восстановить мирные
                отношения и начать сотрудничество под эгидой правительства БМАО<a href="#citation-72" id="ref-72" class="citation-mark"><sup>72</sup></a>.</p>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Точка невозврата</h3>
            <p>Депутаты от теократов в Народном собрании активно участвовали в работе парламента,
                хотя в состав областного правительства — Бурмонавтупра (Бурят-Монгольского
                автономного управления) — не вошли. Тем не менее, прогресс был налицо и многим,
                возможно, стало казаться, что проблема Балагатского движения постепенно находит своё
                решение. Однако в реальности вскоре наступает новый виток противостояния, переросший
                в итоге в стадию вооружённой борьбы. Новый конфликт был спровоцирован
                небеспочвенными слухами, которые начинают муссироваться в среде бурят, о планах
                Бурятмонавтоупра ввести военный налог и возобновить призыв бурят в ряды Красной
                армии. Возмущённые этим представители теократов снова ставят перед правительством
                БМАО вопрос о выходе Бодонгутского, Цаганского и Хальбинского хошунов из состава
                области и присоединении этих хошунов к Прибайкальской области<a href="#citation-73" id="ref-73" class="citation-mark"><sup>73</sup></a>.
            </p>
            <p>На этот раз реакция Бурмонавтупра была решительной. Сначала глава правительства
                Матвей Амагаев призвал теократов примириться с идеей бурятской автономии, что никак
                не повлияло на позицию сторонников Балагатского движения. Тогда правительство БМАО
                обвинило теократов в попытках уклониться от уплаты налогов и отбывания повинностей,
                главным образом подводной и военной. С такой формулировкой в феврале 1922 года
                правительство Бурмонавтупра представило дело правительству ДВР<a href="#citation-74" id="ref-74" class="citation-mark"><sup>74</sup></a>.
                Уже в марте того же года по распоряжению правительственного инспектора милиции К.
                Ильина аресту подвергаются главные участники Балагатского движения: бывший президент
                Балагатского государства Сандылыг Гончикдараев, настоятель отколовшегося от
                буддийской общины Чулутского дацана Найдан Сампилов, бывший министр иностранных дел
                Балагатского государства Бадмацырен Бониев, бывший товарищ министра финансов
                Бадмацырен Гармаев и другие<a href="#citation-75" id="ref-75" class="citation-mark"><sup>75</sup></a>.
                Ответом сторонников теократии стало вооружённое сопротивление. Теократы собирают
                вооружённый отряд в 150 человек и направляют его к управлению Бодонгутского хошуна,
                где, по их сведениям, содержались арестованные. Освободить своих товарищей им не
                удалось, в связи с тем, что те к тому времени были переведены в Верхнеудинск<a
                    href="#citation-76" id="ref-76" class="citation-mark"><sup>76</sup></a>. Однако
                этот шаг со стороны теократов привёл к необратимым последствиям. Точка невозврата
                была пройдена.</p>

            <h3 class="text-lg font-semibold text-gray-800 mb-2">Разгром движения</h3>
            <p>Попытка освобождения теократами своих лидеров привела к тому, что все арестованные
                сторонники Лубсан-Самдан Цыденова, включая его самого, были отправлены в
                административную ссылку в отдалённые края и области РСФСР. Вооружённым отрядам
                теократов Бурмонавтупром был объявлен ультиматум: «Настоящим предлагается всем
                балагатцам в 7-дневный срок со дня объявления сего, оставить и забыть распри и
                безоговорочно подчиниться законным органам административного управления, начиная с
                центрального управления и кончая сомонными управлениями, выдав по требованию этих
                органов и милиции всё имеющееся оружие и тех из руководителей движения, изоляция
                которых властями будет признана необходимой. Настоящий приказ есть последнее
                предупреждение. И если по истечении указанного срока не наступит исполнение и полное
                и безоговорочное подчинение, то будет приступлено к беспощадному подавлению
                балагатского движения всей силой карательного воздействия»<a href="#citation-77" id="ref-77" class="citation-mark"><sup>77</sup></a>.
            </p>
            <p>Лубсан-Самдан Цыденов отправился в принудительную ссылку, и следы его теряются.
                Имеются сведения, что в мае 1922 года он был госпитализирован в городскую больницу
                Ново-Николаевска, где скончался от левостороннего плеврита<a href="#citation-78" id="ref-78" class="citation-mark"><sup>78</sup></a>.
                Фактически история Кудунской теократии заканчивается вместе с его смертью.
                Разрозненные остатки теократов уничтожались органами власти сначала ДВР, а затем и
                РСФСР все 20-е годы ХХ столетия. Дальнейшая история Балагатского движения является
                историей вооружённой борьбы, репрессий, судебных процессов, расстрелов и заключений
                по решению судов различных инстанций, актов мести, самосудов, партизанской войны и
                полного поражения сторонников идеи Кудунской теократии. Последний судебный процесс
                над группой сторонников Балагатского движения состоялся в с. Анинском в июне 1929
                года, в результате которого один человек был приговорён к расстрелу, а ещё пятеро
                получили сроки тюремного заключения от 5 до 10 лет<a href="#citation-79" id="ref-79" class="citation-mark"><sup>79</sup></a>.
            </p>

            <h2 class="text-xl font-bold text-gray-800 mt-6 mb-4">Заключение</h2>
            <p>Кудунскую теократию нельзя назвать состоявшимся государственным проектом. Как это
                видно из представленного очерка, несмотря на разработанную структуру государства и
                проведённую процедуру формирования органов власти, теократическое правительство так
                и не смогло полноценно приступить к своим обязанностям. И всё же использованные в
                этой статье источники позволяют сделать некоторые осторожные выводы о существе
                Кудунского государственного проекта.</p>
            <p>Как известно, модернистские идеи в традиционных восточных культурах никогда не были
                простым отражением европейских моделей, но получали свою специфику и свою уникальную
                конфигурацию, пройдя через прокрустово ложе их собственного мировидения. Способы
                апроприации политических идей имели глубокую связь с европейским колониализмом, что
                очевидно при анализе истории бурят-буддистов, которые всегда составляли часть Pax
                Mongolica и тибетского буддийского мира, даже после того, как вошли в состав
                российского государства. Подробное рассмотрение таких эпизодов истории буддизма, как
                Кудунская теократия, даёт исследователям возможность понять природу трансформации
                теократий Внешней Монголии и Тибета. Под воздействием европейской парадигмы
                государственного строительства буряты стали одним из первых буддийских народов,
                которые должны были пересмотреть многие традиционные политические концепции.</p>
            <p>Бурятская версия национализма имитировала российскую политическую культуру, в которой
                православию отводилась ключевая роль гомогенизирующей силы, с помощью которой
                общество получало единую политическую и религиозную идентичность. Для забайкальских
                бурят буддизм служил своего рода главным диакритическим знаком, который позволял им
                в имперское время отделять себя от окружающего их русского православного
                большинства, а позднее создавать на его основе государственные структуры и
                институты, что мы, собственно, и наблюдаем на примере Балагатского движения. И в
                этом смысле этнорелигиозная идентичность бурят-буддистов выступала в качестве
                альтернативы той версии бурятского национализма, которую предлагали бурятам сначала
                буржуазные националисты (часть которых активно сотрудничала с режимом атамана
                Семёнова), а затем большевики. На примере Кудунской теократии мы видим, как легко
                буддизм становился основой для государственного строительства, и это прекрасно
                понимали и сами бурятские националисты, и семёновцы, и большевики. Проект Даурского
                правительства во главе с Нейсе-гэгэном, инициированный атаманом Семёновым в 1918
                году, и использование большевиками фигуры Богдо-гэгэна VIII в переходный период к
                социалистическому строительству в Монголии прекрасно иллюстрируют этот факт.</p>
            <p>Бурятские националисты, в разное время сотрудничавшие и с царизмом, и с семёновцами,
                и с большевиками, находились под большим влиянием идей романтического панславизма и
                пангерманизма. В их картине будущего тибетский буддизм был силой, способной
                подтолкнуть монгольские народы к объединению. Однако религия в их представлении
                занимала подчинённое положение и мыслилась как орудие, а не фундамент для
                государства. Их национализм включал религиозный аспект, но лишь как маркер групповой
                идентичности, а не основу для выдвижения национальных претензий. Они сами могли быть
                буддистами, оставаясь при этом светскими людьми, и модель их воображаемого
                государства была также сугубо светской. Их оппоненты, в числе которых был и
                Лубсан-Самдан Цыденов, выступали за чисто религиозное государство и буддийскую
                нацию. Именно поэтому Кудунская теократия имеет столь сложную и драматичную историю
                взаимоотношений именно со светскими бурятскими националистами, в которых её
                последователи видели своих главных врагов и оппонентов.</p>
            <p>Кудунский государственный проект интересен также тем, что, внешне стремясь воссоздать
                идеальную архетипическую модель буддийского государства по модели империи Маурья
                времени Ашоки, был первой попыткой встроить такого типа теократию в рамки
                конституционной республики. Выборный принцип пронизывает всю систему
                представительства в Балагатском государстве. Несмотря на то, что фигура верховного
                правителя находилась над основным законом, тем не менее, сама попытка примирить
                монархию с республикой подтверждает тезис о том, что Кудунский проект созрел в
                контексте российской политической действительности, хотя формально и копировал
                образцы буддийских теократий Тибета и Монголии. То, что роднит Кудунскую теократию с
                другими буддийскими монархиями Внутренней Азии, это устойчивое идеологическое
                неприятие идеи о вооружённой защите своей государственности, что в условиях
                революций и Гражданской войны создало условия для насильственного уничтожения этого
                государственного проекта и его инициаторов, но в то же время, возможно, сберегло
                множество молодых людей, которых должны были призвать в ряды различных вооружённых
                формирований.
            </p>

