                    <li class="my-2">Тиб. <em>de bzhin nyid kyi ting nge 'dzin</em> или санскр.
                        <em>tathatā-samādhi</em> (<em>татхата-самадхи</em>), связано со стадией
                        порождения тантры Махайоги. <em>de bzhin nyid kyi ting nge 'dzin</em> или
                        схожее по названию созерцание описывается как первая самадхи в упорядоченной
                        последовательности трех самадхи в нескольких текстах махайоги из Дуньхуана,
                        включая IOL Tib J 436, IOL Tib J 437, IOL Tib J 579, IOL Tib J 716/1, PT 626
                        и PT 634. IOL Tib J 508 и IOL Tib J 454 также упоминают перечень из четырех
                        самадхи. Во многих из этих текстов стадии порождения и завершения
                        описываются
                        как поток практики. В них <em>de bzhin nyid kyi ting nge 'dzin</em>
                        представляет
                        собой созерцание на пустотности перед представлением построения мандалы.
                        DALTON 2004. Пример такого употребления в каноническом тексте, приписываемом
                        Вималамитре, можно найти в его комментарии на «Гухьягарбху» —
                        <em>Māyājālopadeśakramatraya.</em>: «Говорится, что ступени символического
                        созерцания и созерцание Таковости суть два типа» (<em>mtshan mar bsgom pa'i
                            rim
                            pa yang/ /de bzhin nyid du bsgom pa yi/ /byed brag rnam pa gnyis su
                            gsungs</em>).
                        Q. 4742: 568a1. К сожалению, поскольку это единственная ссылка подобного
                        рода в
                        <em>Zhus lan</em>, неясно, к какому контексту относится упоминание Пэльянга.
                    </li>
                    </ul>
