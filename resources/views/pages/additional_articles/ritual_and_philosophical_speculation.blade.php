            <h4 class="text-center text-gray-800 font-bold text-xl my-8">ВОПРОСЫ: ОБРЯДЫ МАХАЙОГИ,
                ЗАБЛУЖДЕНИЯ И ПЛОДЫ</h4>
            <p>Теперь, когда мы увидели, что <em>Zhus lan</em> прочно построен на эзотерическом
                основании, какие свидетельства ранней, ритуализированной <em>Махайоги</em> конкретно
                мы можем увидеть в этом тексте, кроме собственного утверждения Пэльянга в начальных
                строках, что <em>Zhus lan</em> является объяснением "<em>Махайоги</em>, высшей
                системы"?<a href="#citation-10" id="ref-10" class="citation-mark"><sup>10</sup></a>
                Ключи к практической матрице учений Пэльянга лежат в вопросах собеседника
                — в предположениях, целях и ожиданиях его ученика, как он/она представлял практику
                <em>Махайоги</em>. Сосредоточившись на вопросах, мы получаем представление о пути, с
                которым его ученики должны были быть достаточно сведущи. Он содержит многие аспекты
                практики и перспективы, которые либо уникальны для типа практики <em>Махайоги</em>,
                который мы видим в других тибетских рукописях Дуньхуана, либо, по крайней мере,
                характерны для него, хотя, конечно, не все. Как очевидно из различных ссылок в
                вопросах на саму <em>Махайогу</em>, практическая система уже утвердилась, и набор
                авторитетных писаний <em>Махайоги</em>, по всей видимости, был известен ученикам
                Пэльянга. Созерцательное творение себя как божества-покровителя с полной свитой в
                пространстве
                <em>мандалы</em>, по-видимому, было основной практикой, с сопутствующими ожиданиями
                приобретения сил и йогических достижений, а также знаков и признаков для
                подтверждения таких достижений. Хотя нет упоминания о том виде организованного
                собрания мирных и гневных божеств, который мы видим в собрании ста мирных и гневных
                божеств <em>Гухьягарбхатантры</em>, собеседник Пэльянга упоминает особый вид
                практики, в котором гневное божество, как форма будды, визуализировалась как
                ритуально попирающая ногами высших божеств. Собеседник также выражает явную
                осведомлённость с практиками проявления и растворения, хотя детали этих практик не
                приводятся. Ваджрасаттва, по-видимому, понимается собеседником Пэльянга как основная
                форма высшего божества, своего рода мета-будда для пяти будд, а <em>видьядхара</em>
                предполагается как высшее состояние, которого можно достичь благодаря практике <em>Махайоги</em>.
            </p>
            <p>В вопросах собеседника есть также некоторые свидетельства социального аспекта этой
                группы практикующих. Посвящения от своего учителя были принятой предпосылкой для
                практики созерцания, а необходимость подношений и послушания своему наставнику в
                дальнейшем, хотя и подвергались ими сомнению, не были новыми концепциями для
                учеников Пэльянга. К сожалению, из текста неясно, была ли аудитория Пэльянга
                монашеской или они образовывали какой-то другой вид сообщества. Однако очевидно, что
                роль учителя была центральной. В заключительном разделе ученик задает вопросы
                Пэльянгу об авторитете наставника и необходимости стать учителем, чтобы получить
                должные посвящения. Такие вопросы являются указанием на то, что злоупотребление
                властью и ложные претензии на мастерство были реальными проблемами для махайогинов
                во времена Пэльянга.</p>
            <p>Мы также видим в вопросах <em>Zhus lan</em> разного рода напряжённости, которые могут
                возникнуть ранее в развитии традиции и практической стандартизации. Что касается
                образов своего божества-покровителя, то ученик в замешательстве из-за противоречивых
                советов, которые он получал. Например, ученик спрашивает, правильно ли практиковать
                с разными формами божества — мирной самбхогакайей как основным
                божеством-покровителем, гневным проявлением как "божеством действия" (<em>las kyi
                    lha</em>) для конкретных ритуальных целей, и так далее. Достаточно ли практики
                исключительно одного божества? Необходимо ли для обретения достижений (что-то ещё)?
                Собеседник Пэльянга также выражает озабоченность многих практикующих тех времён,
                когда <em>Махайога</em> утвердилась до такой степени, что начала конкурировать с
                автохтонными традициями и начинала поглощать их практики как свои собственные. "В
                Вопросе 36 ученик спрашивает: "Если йогины делают подношения тибетским богам и
                демонам, соответствует ли это священным писаниям?" Видна также его озабоченность
                тем, как тантрическая практика соотносится с обычными буддийскими учениями:
                "Насколько важны для практики мантры принципы, изложенные в сутрах?" и "Если обрел
                великую силу, важно ли при этом изучать Дхарму?"</p>
            <p>Хотя эта картина далеко не полная, вопросы ученика позволяют представить практику
                Махайоги девятого века, связанную с методами Гухьягарбхатантры времен ее раннего
                распространения в Тибете. Как мы видели, ученик воспринимал характерные для Махайоги
                практики умилостивления гневных божеств и ритуалы подчинения как основу своей
                традиции. Сам Пэльянг охотно использует образы укрощения и покорения. Отвечая на
                Вопрос 35, он дает такое наставление:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <ul class="list-none pl-2 space-y-1 mb-4 italic">
                    <li class="my-2">Всегда, без страха и желаний,</li>
                    <li class="my-2">Словно Великий Завоеватель, покоривший земных [божеств],</li>
                    <li class="my-2">Властвуй и правь всеми без исключения.</li>
                </ul>
            </blockquote>
            <p>Явно указывая на особую традицию, Пэльянг называет <em>Zhus lan</em> описанием "<em>Махайоги</em>,
                высшей системы", и его собеседник задается вопросом, как следовать авторитетным
                писаниям <em>Махайоги</em> (<em>ma ha yo ga'i gzhung</em>)<a href="#citation-11"
                                                                             id="ref-11"
                                                                             class="citation-mark"><sup>11</sup></a>.
                Различие между <em>Махайогой</em>
                и менее действенными, более древними формами тантры четко проводится в сочинении
                Пэльянга <em>Thugs kyi sgron ma</em>. Там он утверждает: хотя у низшей, внешней
                формы пятой, непревзойденной колесницы есть три подразделения, <em>Махайога</em>
                представляет ее высшую, «тайную» форму<a href="#citation-12" id="ref-12"
                                                         class="citation-mark"><sup>12</sup></a>.
                Более того, ни в <em>Zhus lan</em>, ни в
                других текстах Пэльянга не признается существование разных типов <em>Махайоги</em>
                или каких-либо более высоких форм тантры. Несомненно, Пэльянг считал
                <em>Махайогу</em> высшим методом эзотерической буддийской практики.</p>
            <p>Дэвидсон выделяет два типа индийской тантрической практики: старый институциональный
                эзотеризм, где темы насилия и власти воплощались в тантрических ритуалах захвата и
                использования силы для укрепления иерархических структур вроде монастырей; и новый
                неинституциональный эзотеризм с сексуальной йогой и другими антиномическими
                практиками, подрывавшими устоявшиеся иерархии<a href="#citation-13" id="ref-13"
                                                                class="citation-mark"><sup>13</sup></a>.
                Хотя в <em>Zhus lan</em> широко
                признается гневный аспект <em>Махайоги</em> и используются царские метафоры, там нет
                упоминаний практик «соединения и освобождения» (<em>sbyor sgrol</em>), обычно
                связанных с <em>Гухьягарбхатантрой</em> и осужденных в Тибете с конца X века. Нет
                там и явных свидетельств сексуальных практик, подробно описанных в многочисленных
                махайогических садханах из Дуньхуана, или крайнего многозначного языка, характерного
                для более поздних форм тантры, таких как йогини-тантры. Ничто не указывает, что
                упоминания «печатей» (<em>phyag rgya</em>) относятся к партнерам в сексуальной йоге.
                Более того, хотя медитативное взращивание божества как себя центрально для пути <em>Махайоги</em>
                по Пэльянгу, ни он, ни его собеседник не упоминают внутренние практики с тонким
                телом — работу с каналами, каплями и ветрами, что могло бы указывать на более
                поздние развития йоги божества<a href="#citation-14" id="ref-14"
                                                 class="citation-mark"><sup>14</sup></a>. Фактически
                определение ваджры у Пэльянга, где была
                бы лучшая возможность для эротической полисемантики, выглядит совершенно лишенным
                эротизма. В ответе на Вопрос 1 он говорит: «Нерожденный, спонтанно [возникающий]
                простор изначальной мудрости, неизменный, неразрушимый — таково определение ваджры».
            </p>

            <p>Эти отсутствия озадачивают, особенно если учесть, что годы жизни Пэльянга можно
                довольно уверенно датировать девятым веком — самым ранним периодом, когда эти
                литературные элементы впервые появились в тибетских материалах Дуньхуана и начали
                описываться соответствующие ритуалы. Хотя в <em>Zhus lan</em> есть несколько
                двусмысленных терминов, которые можно интерпретировать как возможные отсылки к
                развитым йогическим системам — например, к трем самадхи и трем йогам<a
                    href="#citation-15" id="ref-15" class="citation-mark"><sup>15</sup></a>, которые
                мы
                видим в большей части дуньхуанского материала по <em>Махайоге</em>, — было бы
                натяжкой считать автора <em>Zhus lan</em> современным представителем этой более
                продвинутой традиции <em>Махайоги</em>. Скорее <em>Махайога</em> Пэльянга занимает
                промежуточное положение между ранней индийской <em>Махайогой</em> и более полно
                развитой <em>Махайогой</em>, которую мы видим во многих дуньхуанских текстах. Однако
                следует проявлять осторожность и не спешить с датировкой <em>Zhus lan</em>
                исключительно на основании этих общих тематических критериев. Хотя нельзя
                игнорировать тот факт, что <em>Zhus lan</em> столь точно соответствует первой
                категории тантры по Дэвидсону и практически не содержит признаков обращения ко
                второй категории, мы не можем исходить из предположения, что этот текст
                хронологически появился между ними. Схемы Дэвидсона, при всей их полезности для
                понимания последовательности возникновения определенных практик, не могут служить
                основанием для точного определения даты создания конкретной тантры или тантрического
                толкования, появившихся после самых ранних известных образцов — будь то в Индии или
                Тибете, — поскольку обе формы эзотеризма (институциональная и неинституциональная)
                продолжали развиваться приблизительно параллельно, по крайней мере с VIII века.
                Неясно, были ли ученики Пэльянга действительно знакомы лишь с первым типом
                эзотеризма, или же, выбирая вопросы для рассмотрения в своем <em>Zhus lan</em>
                Пэльянг сознательно решил полностью обойти стороной то, что могло считаться наиболее
                радикальными чертами второго, неинституционального типа тантрической практики.
                Большинство дуньхуанских текстов, описывающих сексуальные обряды и практики <em>Махайоги</em>,
                датируются приблизительно IX-X веками. Поскольку мы не можем датировать ни одну из
                сохранившихся рукописей <em>Zhus lan</em> точнее этого же периода на основании
                орфографических данных<a href="#citation-16" id="ref-16" class="citation-mark"><sup>16</sup></a>,
                в настоящее время можно лишь утверждать, что <em>Zhus lan</em>, по-видимому, был
                создан в то же время, что и большинство дуньхуанских текстов <em>Махайоги</em> но
                отличается от них по характеру излагаемой тантрической системы.</p>

            <h4 class="text-center text-gray-800 font-bold text-xl my-4">ОТВЕТЫ: УСИЛИЕ,
                СПОНТАННОСТЬ И РАВНОВЕСИЕ</h4>
            <p>Отсутствие явных сексуальных отсылок — не единственный аспект, которым учения <em>Zhus
                    lan</em> отличаются от других текстов <em>Махайоги</em>, обнаруженных в
                Дуньхуане, несмотря на их прочную основу в традиции <em>Махайоги</em>. Отвечая на
                вопросы текста, которые сосредоточены почти исключительно на технических аспектах
                практики, <em>Zhus lan</em> указывает на тонкие, спонтанные состояния реализации,
                характерные для современной ему поэзии <em>Семде</em> и более поздней литературы
                <em>Дзогчен</em>. Если мы вернемся к определению ваджры, данному Пэльянгом, то
                увидим, что он
                использует не только стандартные термины из <em>Vajraśekharatantra</em> —
                «неизменный» и «неразрушимый», — но и расширяет определение, включая описания, более
                характерные для литературы <em>Семде:</em> «непорожденное, спонтанно [возникающее]
                пространство изначальной мудрости». Это типично для ответов учителя. Ответы Пэльянга
                часто не только превосходят кругозор и цели вопросов, но и, обнажая невежественную
                привязанность к техникам и наблюдаемым преимуществам практики, выворачивают
                наизнанку ложные предпосылки собеседника, делая сам вопрос нелогичным. Например, в
                Вопросе 19 происходит следующий обмен репликами:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <ul class="list-none pl-2 space-y-1 mb-4">
                    <li class="my-4"><strong>Следует ли пребывать в «не-медитации»?</strong></li>
                    <li class="my-2">Если бы тема твоего вопроса существовала (т.е. медитация),</li>
                    <li class="my-2">Был бы и медитирующий.</li>
                    <li class="my-2">Если ум на самом деле не имеет порождения,</li>
                    <li class="my-2">То что тогда пребывает в «не-медитации»?</li>
                </ul>
            </blockquote>
            <p>Когда в Вопросе 10 ученик спрашивает, какие признаки свидетельствуют о
                достижении, Пэльянг отвечает:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <ul class="list-none pl-2 space-y-1 mb-4 italic">
                    <li class="my-2">Природа Победоносного — это просто праджня и самадхи.</li>
                    <li class="my-2">С ними внешние знаки и признаки теряют значение.</li>
                    <li class="my-2">Например, если хочется огня, и сам огонь уже найден,</li>
                    <li class="my-2">Тогда дым как признак огня уже не имеет значения.</li>
                </ul>
            </blockquote>
            <p>В двух строфах, почти дословно заимствованных из <em>Sbas pa'i rgum chung</em><a
                    href="#citation-17" id="ref-17" class="citation-mark"><sup> 17</sup></a>
                Буддхагупты, Пэльянг утверждает, что нет причины для преображения, а пробуждение —
                беспричинно. В ответе на Вопрос 7 он говорит:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <ul class="list-none pl-2 space-y-1 mb-4 italic">
                    <li class="my-2">Природа ума подобна небу. Это значит,</li>
                    <li class="my-2">Что нет нужды превращать (ему) себя в небо.</li>
                    <li class="my-2">Сам ум — это небо, пространство пробуждения;</li>
                    <li class="my-2">У пробуждения нет причины.</li>
                    <li class="my-2">Без основания и корня сам ум,</li>
                    <li class="my-2">Подобно небу, не очищается очищением.</li>
                    <li class="my-2">Пробуждение, свободное от порождения,</li>
                    <li class="my-2">Не имеет ни причины, ни плода пробуждения.</li>
                </ul>
            </blockquote>
            <p>Позже, в ответе на Вопрос 13, Пэльянг утверждает, что оптимальные условия для
                приближения к божеству — это те, что возникают спонтанно:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <ul class="list-none pl-2 space-y-1 mb-4 italic">
                    <li class="my-2">Когда оно проявляется как «я», если Тело Реальности</li>
                    <li class="my-2">Осознаётся как неизменное, подобное небу,</li>
                    <li class="my-2">И если этот ритуал не воспринимается как объект и субъект,</li>
                    <li class="my-2">Без усилия и старания — это высшая форма приближения.</li>
                </ul>
            </blockquote>
            <p>Фактически, Пэльянг ясно заявляет, что нет ничего, что нужно взращивать. В ответе на
                Вопрос 18 он говорит:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <ul class="list-none pl-2 space-y-1 mb-4 italic">
                    <li class="my-2">Победоносные трех времен не измышляют;</li>
                    <li class="my-2">С самого начала собственный ум непорожден.</li>
                    <li class="my-2">Если непорожденный собственный ум — это реальность,</li>
                    <li class="my-2">Реальность не является чем-то, что следует взращивать в
                        созерцании.
                    </li>
                </ul>
            </blockquote>
            <p>Тем не менее, несмотря на регулярные утверждения Пэльянга о непорожденности ума и
                беспричинности пробуждения, он не отрицает полностью ценность ритуалов и
                созерцательных практик с божествами и мандалами в пользу чисто естественного,
                гностического подхода. Напротив, его учение сохраняет тонкий баланс между обоими
                аспектами, и игра между усилием и расслаблением проходит красной нитью через все
                ответы — хотя, как мы увидим, в конечном счёте превалирует расслабление.</p>
            <p>В ответе на Вопрос 10 Пэльянг подчёркивает важность усилия:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <ul class="list-none pl-2 space-y-1 mb-4 italic">
                    <li class="my-2">Два вида йоги — основа Победоносных трёх времён —</li>
                    <li class="my-2">Возникают внутри самого себя.</li>
                    <li class="my-2">Осознав это,</li>
                    <li class="my-2">Стремись узреть переживание ума.</li>
                </ul>
            </blockquote>
            <p>В следующем ответе он уточняет:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <p>Достижение будет только благодаря праджне и самадхи — воистину.
                </p>
            </blockquote>
            <p>И далее, как в ответе на Вопрос 13:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <ul class="list-none pl-2 space-y-1 mb-4 italic">
                    <li class="my-2">Мудрый, обладающий таким постижением,</li>
                    <li class="my-2">Ясно взращивает три печати признаков в созерцании.</li>
                    <li class="my-2">Стойко, без отвлечений, не оставляя практики,</li>
                    <li class="my-2">Он применяет все ритуалы и приближается к божеству мудрости.
                    </li>
                </ul>
            </blockquote>
            <p>Как же Пэльянг примиряет, казалось бы, противоречивые утверждения об усилии и
                неусилии, о необходимости ритуала и об освобождающем его отрицании? Ключ — в его
                итоговом ответе на Вопрос 47, где он обсуждает высшее состояние — состояние
                видьядхары: «Через собственную йогу, благословения Победоносного и непревзойдённые
                добродетельные корни — состояние [видьядхары] реализуется». Первый из этих трёх
                факторов — <em>собственная йога</em> — должна происходить без усилия, но усилие всё
                же не отвергается полностью. Естественное вхождение в самадхи и состояние
                спонтанного достижения, описываемое Пэльянгом как оптимальное, возможно только после
                повторных усилий к этому. В ответе на Вопрос 31 он говорит:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <ul class="list-none pl-2 space-y-1 mb-4 italic">
                    <li class="my-2">Что касается того, что должно быть достигнуто,</li>
                    <li class="my-2">Сделав усилие, снова и снова пребывают в равновесной
                        медитации.
                    </li>
                    <li class="my-2">Затем, созерцая и медленно входя в поток практики,</li>
                    <li class="my-2">Оно осуществляется без усилия, спонтанно.</li>
                </ul>
            </blockquote>
            <p>И в следующем вопросе он продолжает:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <ul class="list-none pl-2 space-y-1 mb-4 italic">
                    <li class="my-2">Если очевидно, что живые существа должны прилагать усилия,</li>
                    <li class="my-2">Разве ложно утверждение, что состояние Победоносного
                        достигается спонтанно?
                    </li>
                    <li class="my-2">Это как с буквами и прочим: через прилежание, снова и снова,
                    </li>
                    <li class="my-2">Став умелым, навык чтения проявляется без усилия.</li>
                </ul>
            </blockquote>
            <p>Второй фактор — <em>благословение</em> — также рассматривается подобным образом.
                Сначала Пэльянг объясняет, что благословения приходят согласно усилию, но затем, в
                ответе на Вопрос 11, говорит:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <ul class="list-none pl-2 space-y-1 mb-4 italic">
                    <li class="my-2">Подобно тому, как осадок оседает, и вода снова становится
                        чистой,
                    </li>
                    <li class="my-2">Нет нужды прилагать усилия, чтобы появились отражения солнца и
                        луны.
                    </li>
                    <li class="my-2">Когда собственный ум проявляется в чистой йоге,</li>
                    <li class="my-2">Нет нужды стремиться к возникновению благословений
                        Победоносного.
                    </li>
                </ul>
            </blockquote>
            <p>Наконец, Пэльянг подчёркивает необходимость <em>обычной, направленной практики
                    Махайоги</em> в описании третьего требования для состояния видьядхары — особых
                добродетельных корней. Эти непревзойдённые, уникальные добродетельные корни
                созревают естественно в этой жизни, но являются результатом эзотерических ритуалов и
                практики тайной мантры. Таким образом, во всех трёх элементах пути к состоянию
                видьядхары Пэльянг объясняет: усилие и определенный комплекс ритуалов и созерцаний
                необходимы для большинства практикующих на пути <em>Махайоги</em>, но они не
                вызывают ни пробуждения, ни сиддхи. В конечной стадии они отпадают, и высшее
                состояние проявляется как спонтанная, свободная от усилий реализация через
                философское созерцание пустотности практики и её проявлений.</p>

            <h4 class="text-center text-gray-800 font-bold text-xl my-4">ЗАКЛЮЧЕНИЕ</h4>
            <p>Три версии <em>Zhus lan</em> были найдены в Дуньхуане. Все три, по-видимому, были
                созданы и собраны как независимые тексты, что подтверждается внутренней нумерацией
                страниц старейшей рукописи (<em>PT 819</em>) и форматом свитков у двух других — <em>PT
                    837</em> и <em>IOL Tib J 470</em>. Все три написаны тонким, свободным письмом с
                помощью тибетского деревянного пера, что позволяет предположить: тексты
                переписывались для личного использования монахами или практикующими. Как отмечал
                Уэяма Дайшун, свиток <em>PT 837</em> имеет следы исправлений на верхнем стержне —
                оригинальном, — что говорит о его бережном хранении и высокой ценности<a
                    href="#citation-18" id="ref-18" class="citation-mark"><sup>18</sup></a>. <em>Zhus
                    lan</em> цитируется как минимум в одной дуньхуанской рукописи (<em>PT
                    699</em>), а также широко в тексте X века <em>Bsam gtan mig sgron</em>. Он также
                включён в пекинское, нартанское издания <em>Bstan 'gyur</em>, а также в более
                позднюю факсимильную публикацию «Золотой рукописи Ганден» (<em>Kinsha</em>, как её
                называют японские исследователи) <em>Bstan 'gyur</em><a href="#citation-19"
                                                                        id="ref-19"
                                                                        class="citation-mark"><sup>
                        19</sup></a>.
            </p>
            <p>Традиция ньингма, которая считает махайогина Пэльянга представителем одной из ранних
                тибетских линий передачи, отличается уникальным — но необходимым — соединением двух
                подходов: постепенного, иногда сурового эзотеризма тантр, с одной стороны, и
                философского утверждения изначальной спонтанности и освобождения, достигшего своего
                апогея в учениях <em>Дзогчен</em>, с другой. Хотя на популярность <em>Zhus lan</em>
                в Дуньхуане и Тибете, вероятно, повлияло множество факторов, именно это раннее
                соединение практики и философии во многом объясняет её устойчивый успех.
                Действительно, в <em>Zhus lan</em> можно наблюдать, по крайней мере, один эпизод
                рождения философской тибетской Ваджраяны.</p><br>

