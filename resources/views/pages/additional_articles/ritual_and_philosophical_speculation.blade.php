                        божеству,
                        что видно в парах вопросов и ответов 17 и 36, так и к более абстрактной
                        концепции внутренней природы всего сущего, как в парах вопросов и ответов 1,
                        2, 3 и 22. Подобная эквивалентность между этими терминами встречается в
                        «Сарвататхагатататтвасанграхасутре»
                        (<em>Sarvatathāgatatattvasaṃgrahasūtra</em>). WEINBERGER 2003.<br>
                        Автор IOL Tib J 647, комментария на <em>Rig pa'i khu byug</em>, очевидно,
                        ощущал
                        потребность в прояснении этого вопроса: «Во всех тантрах утверждается, что
                        Ваджрасаттва — главный среди всех йог, но здесь главным называется
                        <em>Kun tu bzang po</em>. ...Считается, что Ваджрасаттва упоминается, когда
                        речь идет о стремлении к желаемой цели и когда существуют различные степени
                        достижения. Но здесь не стремятся ни к какой подобной цели. Принимая это во
                        внимание, <em>Kun tu bzang po</em> еще более подходящ». KARMAY 1988.
