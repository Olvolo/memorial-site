            <h4 class="text-center text-gray-800 font-bold text-xl my-8">ТАНТРИЧЕСКАЯ ИДЕОЛОГИЧЕСКАЯ
                ОСНОВА <em>Zhus lan</em></h4>
            <p>Хотя несколько более поздних текстов Дуньхуана, упоминающих <em>Махайогу</em>,
                по-видимому, не были предназначены для аудитории исключительно эзотерических
                практикующих или даже для комментирования эзотерической традиции<a
                    href="#citation-6" id="ref-6" class="citation-mark"><sup>6</sup></a>, <em>Zhus
                    lan</em>
                был явно написан для тех, кто глубоко вовлечен в тантрическую практику. Если мы
                следуем политетическому<a href="#citation-7" id="ref-7"
                                          class="citation-mark"><sup>7</sup></a>подходу Р. М.
                Дэвидсона к определению категории тантры<a
                    href="#citation-8" id="ref-8" class="citation-mark"><sup>8</sup></a>, <em>Zhus
                    lan</em>
                содержит большинство ключевых признаков, перечисленных Дэвидсоном. <em>Zhus lan</em>
                открывается описанием Ваджрасаттвы, главенствующего будды тантр <em>Майяджала</em>,
                и касается таких тем, как пять изначальных мудростей и их соответствие пяти буддам,
                важность <em>мантры</em> и <em>мудры</em>, правильное отношение, с которым следует
                подходить к использованию сакраментальных веществ, необходимость соблюдения своих
                обязательств <em>самайи</em> и преображение практикующего в божество.</p>
            <p><em>Zhus lan</em> также указывает на то, что Р. Дэвидсон определил как основной
                нарратив тантры — акт принятия практикующим власти и осуществления господства как
                владыки или правителя посредством различных тантрических ритуалов, важнейшим из
                которых является церемония посвящения (<em>абхишека</em>). Пэльянг использует этот
                подтверждающий нарратив на протяжении всего текста. В Вопросе 9 собеседник
                спрашивает: "Тогда в чем различие достижений, обретенных через йогу?" В ответ
                наставник сопоставляет два метода обретения достижения, внешний и
                непревзойденный, сравнивая источники власти министра и короля.</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
                <ul class="list-none pl-2 space-y-1 mb-4 italic">
                    <li class="my-2">Как король, назначающий министра,</li>
                    <li class="my-2">Власть вручает свыше — внешний путь.</li>
                    <li class="my-2">Как народ вручает трон по собственной воле —</li>
                    <li class="my-2">Так самопроявление — великое совершенство, путь
                        непревзойденный.
                    </li>
                </ul>
            </blockquote>
            <p>Во "внешнем методе" практикующий, как простой министр, получает посвящения и получает
                власть и достижения от внешнего, высшего источника в форме благословений. В
                "непревзойденном методе", напротив, практикующий-ставший-сувереном развертывает свою
                собственную естественную силу, происходящую от внутреннего, всегда присутствующего
                источника, как подданные своего собственного царства.</p>
            <p>Далее, в ответе на Вопрос 36, Пэльянг утверждает, что королевское сравнение
