                    <li class="my-4"><strong>К вопросу 41:</strong></li>
                    <li class="my-2">Хотя система Пяти Глаз (тиб. <em>spyan lnga;</em> санскр. <em>pañcacakṣuḥ</em>)
                        является
                        более распространенной, по крайней мере в тантрической тибетской литературе,
                        Пэльянг здесь,
                        по-видимому, ссылается на менее известную систему Трех Глаз (тиб. <em>spyan
                            gsum;</em> санскр.
                        <em>trayaścakṣuḥ</em>). Три Глаза упоминаются в различных сутрах и
                        экзегетических текстах,
                        включая <em>Сангитипарьяю</em> (<em>Saṃgītiparyāya</em>), <em>Итивриттакасутру</em>
                        (<em>Itivṛttakasūtra</em>), <em>Йогачарабхумишастру</em> (<em>Yogācārabhūmiśāstra</em>),
                        <em>Абхидхармакошу</em> (<em>Abhidharmakośa</em>) и <em>Аватамсакасутру</em>
                        (<em>Avataṃsakasūtra</em>). Три описываемых там глаза: 1) Физический Глаз
                        (тиб. <em>lus kyi spyan;</em> санскр. <em>māṃsacakṣuḥ</em>); 2) Божественный
                        Глаз (тиб. <em>lha'i spyan;</em>
                        санскр. <em>divyaṃcakṣuḥ</em>); и 3) Глаз Мудрости (тиб. <em>shes rab kyi
                            spyan;</em> санскр.
                        <em>prajñācakṣuḥ</em>) или Благородный Глаз Изначальной Мудрости (тиб. <em>'phags
                            pa'i ye shes kyi spyan;</em>
                        санскр. <em>āryajñānacakṣuḥ</em>). Пэльянг использует комбинацию двух
                        последних терминов для третьего.
                        Ссылки на Глаз Прозрения (<em>shes rab spyan</em>) в <em>Thugs kyi sgron
                            ma</em> Пэльянга
                        определяют <em>shes rab spyan</em> как высший глаз (<em>mchog gi spyan</em>).
                        Q. 4446: 284b.
                        Несмотря на сходство этого термина с названием церемонии посвящения,
                        описываемой во многих
                        текстах Махайоги из Дуньхуана, <em>shes rab ye shes dbang</em>, я не вижу
                        оснований подозревать
                        связь между ними.
                    </li>
                    </ul>
                </li>
