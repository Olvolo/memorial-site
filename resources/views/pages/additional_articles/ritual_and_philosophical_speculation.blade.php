                    <ul class="list-none pl-2 space-y-1 ml-2 mb-4">
                    <li class="my-4"><strong>К вопросу 33:</strong></li>
                    <li class="my-2"><em>Dam tshig</em> обычно относится к обязательствам,
                        принимаемым на церемониях посвящения, хотя их содержание и контекст
                        получения
                        в данном случае не раскрываются. Другой дуньхуанский текст, IOL Tib J 647,
                        комментарий на <em>Rig pa khu byug</em>, обсуждает обязательства схожим
                        образом, отвергая саму возможность нарушения для тех, кто постиг таковость.
                        Как и <em>Zhus lan</em>, он описывает состояние естественного, спонтанного
                        соответствия глубинным принципам обетов. После обсуждения важности
                        отбрасывания привязанностей текст гласит: «Когда пребывают в учениях
                        Таковости, даже обеты практики полностью пребывают в спонтанности»
                        (<em>ji bzhin pa'i lung la gnas pa'i dus na/ spyod pa'i dam tshig kyang lhun
                            gyis grub pa nyid du tshang bar gnas</em>). IOL Tib J 647: 3b5.
                        Для детального рассмотрения обращения к этой теме в дуньхуанских рукописях
                        см. главу ван Шайка в данном томе.
                    </li>
                    </ul>
