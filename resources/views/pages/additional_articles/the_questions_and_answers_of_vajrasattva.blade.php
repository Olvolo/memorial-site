                <p class="mb-3" id="citation-3"><sup>3</sup> Дхармадхату
                    (санскр.<em>dharmadhātu</em>,
                    тиб. <em>chos dbyings</em>) — это фундаментальное понятие в буддийской
                    философии,
                    обозначающее изначальную природу реальности, абсолютное, невыразимое естество
                    всех явлений, пустотное и одновременно способное проявлять всё.
                    В буквальном смысле термин переводится как «сфера (или элемент) дхарм», где
                    дхарма означает «качество», «абсолютное», «феномен», «учение» или «природа», а
                    дхату — «основа», «сфера», «область». Таким образом, дхармадхату — это
                    изначальная основа, в которой возникают и исчезают все явления, не нарушая её
                    целостности. С точки зрения махаянской и ваджраянской традиции, дхармадхату —
                    это пустотная, ясная и недвойственная природа всего существующего. Она не
                    создаётся и не разрушается, не возникает и не исчезает, не является ни единым,
                    ни множественным — она свободна от всех умопостроений. Это не вещь и не объект
                    познания, а скорее пространство таковости (татхата). В контексте буддийской
                    медитативной практики дхармадхату — это то, что становится непосредственным
                    опытом при постижении природы ума. В учениях махамудры и дзогчена дхармадхату
                    тождественна изначальной чистоте ума (кадаг), не загрязнённой омрачениями и
                    всегда присутствующей, хотя и неузнаваемой в обычном состоянии сознания. В этом
                    смысле дхармадхату — это и есть то, чем в глубине является наш ум, когда он
                    свободен от цепляний и концепций. Также в махаянской доктрине трёх тел Будды
                    (трикая) дхармадхату отождествляется с дхармадхатукаей — телом реальности
                    (иногда отождествляемым с дхармакаей), которое представляет собой совершенное,
                    невыразимое бытие Будды, недоступное обычному восприятию.
                    Дхармадхату неотделима от всех явлений: всё, что существует, возникает и
                    исчезает
                    внутри дхармадхату, не выходя за её пределы, как волны не покидают океан.
                    Поэтому осознание дхармадхату — это не уход от мира, а прямое видение того, чем
                    мир всегда был по своей глубинной сути.<a href="#ref-3"
                                                              class="text-blue-500 hover:underline">↩</a>
