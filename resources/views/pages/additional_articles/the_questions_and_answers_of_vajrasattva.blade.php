                            предрасположенности (васаны),
                        </li>
                        <li class="my-2">[Даже те] живые существа будут снова и снова падать в ад —
                            разве не об этом говорят учения?
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Означает ли это, что не следует стремиться к
                                однонаправленному покою ума (шаматхе) или сосредоточению?</strong>
                        </li>
                        <li class="my-2">Шаматха, сопровождаемая безошибочным постижением,</li>
                        <li class="my-2">И самадхи, проясняющее символические мудры, —</li>
