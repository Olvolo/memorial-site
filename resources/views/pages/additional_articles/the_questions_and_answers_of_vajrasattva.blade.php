                            его собственной реализации.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Для тех, кто практикует мантру, какое воззрение
