                                проявления и растворения?</strong></li>
                    </ul>
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-2">Что бы ни проявлялось и ни растворялось, откуда бы ни
                            происходило и куда бы ни направлялось —
                        </li>
                        <li class="my-2">Всё имеет один вкус в состоянии Самантабхадры.</li>
                        <li class="my-2">Следовательно, в пространстве [реальности] невозможно
                            наблюдать проявление и растворение самого пространства.
                        </li>
                        <li class="my-2">Тем не менее, это не делает метод бесполезным — такова его
                            логика.
                        </li>
                    </ul>
                </li>

