                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Тогда в чём же достоинства выполнения практики
                                проявления и растворения?</strong></li>
                        <li class="my-2">Чтобы нечто проявилось там, где вообще ничего нет,</li>
                        <li class="my-2">Многочисленные Будды нисходят и проявляются ради блага
