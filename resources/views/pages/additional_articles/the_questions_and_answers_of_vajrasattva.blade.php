            <p>Текст был написан специально для двух кланов, хорошо известных аристократических
                семей,
                поддерживавших тесные связи с императорским двором. Возможно, имея в виду именно эту
                аудиторию,
                Пэльянг (Pelyang) в сорок втором стихе упоминает, что правильное выполнение йоги
