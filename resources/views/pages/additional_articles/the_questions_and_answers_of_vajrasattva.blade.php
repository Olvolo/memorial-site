                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-2"><strong>Даже если условно всё рассматривается как "только
                                ум",</strong></li>
                        <li class="my-2"><strong>если признать, что явления полностью не существуют,
                                —</strong></li>
                        <li class="my-2"><strong>как тогда возможно сосредоточение
                                (самадхи)?</strong></li>
                        <li class="my-2"><strong>Это бессмысленно<a href="#citation-26" id="ref-26"
                                                                    class="citation-mark"><sup>
                                        26</sup></a>.</strong></li>
                    </ul>
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-2">Даже если практикуются иные виды самадхи,</li>
                        <li class="my-2">Не связанные с умом,</li>
                        <li class="my-2">Ничего не происходит вне сферы ума.</li>
                        <li class="my-2">Даже само несуществование</li>
                        <li class="my-2">Не может проявиться для ума как нечто определённое.</li>
