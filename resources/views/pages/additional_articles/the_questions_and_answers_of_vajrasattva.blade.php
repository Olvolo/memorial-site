                        <li class="my-2">Превосходит методы самадхи с признаками<a
                                href="#citation-25" id="ref-25" class="citation-mark"><sup> 25</sup></a>,
                        </li>
                        <li class="my-2">Поэтому они не взирают [на природу реальности через призму]
                            даже малейших умопостроений.
                        </li>
                        <li class="my-2">Все подобные самадхи</li>
                        <li class="my-2">Не имеют отношения к истинной природе ума, поэтому ни одно
