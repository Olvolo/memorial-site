                                безусильности нужно достигать через усилие?</strong></li>
                        <li class="my-2">Очевидно, что живым существам необходимо прилагать усилия
                            ради достижения чего-либо.
                        </li>
                        <li class="my-2">Но делает ли это ложным утверждение, что состояние
                            Победоносного достигается спонтанно?
                        </li>
                        <li class="my-2">Это подобно изучению букв и прочего: снова и снова
                            прилагаешь усилия,
                        </li>
                        <li class="my-2">И постепенно возникает навык, и тогда чтение совершается
                            уже без усилий.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Противоречиво или нет утверждение, что нет
                                необходимости защищать свои самайи (обеты)?</strong></li>
                        <li class="my-2">У того, кто постиг отсутствие «я» и действует согласно
                            этому видению,
                        </li>
                        <li class="my-2">Поскольку нет цепляния за «я», в трёх вратах (тело, речь и
                            ум) не возникает ни малейшего проступка.
                        </li>
                        <li class="my-2">Поскольку не появляется даже мельчайшей частицы омрачения,
                            то и нет нужды ни в соблюдении, ни в охране [обетов].
                        </li>
                        <li class="my-2">Так задай себе вопрос: не в цеплянии ли за «я» коренится
                            [всякое] противоречие?
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Как следует созерцать собственное тело как
                                махамудру, и мандалу собственного самадхи?</strong></li>
                        <li class="my-2">Проявленное тело, равно как и махамудра, —</li>
                        <li class="my-2">Это проявления ума, поэтому само тело не обладает истинным
                            существованием.
                        </li>
                        <li class="my-2">Подобным же образом — и эманации мандалы в [их] проявлении
                            и расположении<a href="#citation-29" id="ref-29"
                                             class="citation-mark"><sup> 29</sup></a>—
                        </li>
                        <li class="my-2">Являются проявлением самадхи, то есть аспектом собственного
                            ума.
                        </li>
                    </ul>
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-2">Ум и характеристики его проявлений нераздельны.</li>
                        <li class="my-2">Поскольку восприятие себя и других — одинаково,</li>
                        <li class="my-2">Нельзя установить ни собственное тело, ни главное божество
                            как «вот это».
                        </li>
                        <li class="my-2">Итак, всё — есть тело [йидама], и всё следует воспринимать
                            как проявления ума.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Как следует действовать в соответствии с системой
                                махайоги?</strong></li>
                        <li class="my-2">Постижение и самадхи — это Ум и Тело Победителя.</li>
                        <li class="my-2">Никогда не имея ни малейшего уныния, ты поэтому свободен от
                            всяких
                            желаний.
                        </li>
                        <li class="my-2">Подобно великому царю, покоряющему землю,</li>
                        <li class="my-2">Ты повелеваешь и управляешь всем без исключения.</li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Некоторые йогины поклоняются богам и злобным духам
                                Тибета. Соответствует ли это учению йоги?</strong></li>
                        <li class="my-2">Для того, кто связал себя обетом с
                            Самантабхадра-Ваджрасаттвой,
                        </li>
                        <li class="my-2">Обращаться к мирским богам и нагам как высшим —</li>
                        <li class="my-2">Это как царю исполнять дела простого человека.</li>
                        <li class="my-2">Не просите у них даже пропитания — такое поведение
                            расходится с самой целью йоги.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Тела благородных существ часто изображаются
                                попранными под ногами гневных божеств. Не возникает ли противоречия
                                при таком созерцании?</strong></li>
                        <li class="my-2">В предельной истине, где всё одного вкуса, без понятий
                            высшего и низшего,
                        </li>
                        <li class="my-2">Для ума, отбросившего цепляние за понятия «я» и «другое»,
                        </li>
                        <li class="my-2">Если всё понимается как искусные средства — противоречия
                            нет.
                        </li>
                        <li class="my-2">Когда различия постигнуты и преодолены, ничто не
                            утверждается как определённое.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Достигаются ли тантры деяний всех Будд, если
                                совершать почитание одного Татхагаты?</strong></li>
                        <li class="my-2">Совершая почитание тех или иных сугат,</li>
                        <li class="my-2">Если постигнешь глубокое и обширное воззрение,</li>
                        <li class="my-2">То хотя [возможно лишь] приступаешь ко всем тантрам деяний
                            многих Победителей,
                        </li>
                        <li class="my-2">Всё будет достигнуто — так объясняют знающие.</li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-2"><strong>Следуя великому обету (махаврата), касающемуся
                                внутренних сакральных субстанций,</strong></li>
                        <li class="my-2"><strong>Неужели можно достичь сиддхи без дальнейшего
                                созерцания?</strong></li>
                    </ul>
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-2">В учениях о веществах для обретения сверхъестественных
                            способностей
                        </li>
                        <li class="my-2">Есть практика достижения сиддхи и взращивание праджни и
                            самадхи.
                        </li>
                        <li class="my-2">Без этого сиддхи не обретешь — иначе уподобишься скотине.
                        </li>
                        <li class="my-2">Поэтому следует исполнять йогу, сочетая её с необходимыми
                            ритуальными элементами.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>После обретения великих способностей — имеет ли
                                значение, понимаешь ты Дхарму или нет?</strong></li>
                        <li class="my-2">Не имеют равных в трёх мирах силы Победоносных —</li>
                        <li class="my-2">Они рождаются из понимания и постижения природы явлений.
                        </li>
                        <li class="my-2">Если всего этого нет, и отсутствует бодхичитта,</li>
                        <li class="my-2">То что за толк в силах якш и ракшасов?</li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-2"><strong>Что считается достоверным признаком йоги,
                                наделённой осознаванием?</strong></li>
                        <li class="my-2"><strong>Являются ли таковыми острое зрение и
                                прочее?</strong></li>
                    </ul>
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-2">Постижение нерожденного смысла — это око праджни и
                            джняны.
                        </li>
                        <li class="my-2">Силой йоги божественное око полностью очищается,</li>
                        <li class="my-2">Но иные явления, широко распространённые в обыденном мире
                            —
                        </li>
                        <li class="my-2">Острое зрение и тому подобное — не являются достоверными
                            признаками [успеха] в йоге.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Как следует действовать, чтобы обрести
                                могущество?</strong></li>
                        <li class="my-2">Тот, кто постиг безошибочное воззрение — суть двух истин,
                        </li>
                        <li class="my-2">И осознал нераздельное равенство себя и Будды,</li>
                        <li class="my-2">Кто владеет тайными мантрами, мудрами, самадхи и ритуальной
                            практикой,
                        </li>
                        <li class="my-2">И неустанно следует пути созерцания и осуществления —
                            обретает силу ума и силу рода.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Как понимать утверждение, что пробуждение можно
                                достичь за одну жизнь?</strong></li>
                        <li class="my-2">В этом самом остаточном теле</li>
                        <li class="my-2">Можно достичь уровня видьядхары, владеющего жизненной
                            силой.
                        </li>
                        <li class="my-2">И в течение жизни такого видьядхары</li>
                        <li class="my-2">Может быть достигнуто несравненное пробуждение.</li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Что означает имя "видьядхара" и каковы его
                                ступени?</strong></li>
                        <li class="my-2">Тот, кто постиг признак осознавания, присущего праджне<a
                                href="#citation-30" id="ref-30" class="citation-mark"><sup> 30</sup></a>,
                        </li>
                        <li class="my-2">Удерживает осознавание (<em>rig pa</em>) в практике мантр.
                        </li>
                        <li class="my-2">Когда божества и другие созревают как носители ваджры</li>
                        <li class="my-2">В соответствии с их семейством Будды, то пребывают на
                            уровнях видьядхары.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Как пересекают и пребывают на уровнях
                                видьядхары?</strong></li>
                        <li class="my-2">Всё, что может предстать как уровень ума —</li>
                        <li class="my-2">Лишь аспект собственного ума, а потому не является
                            [действительным отдельным] уровнем.
                        </li>
                        <li class="my-2">Куда бы ни направлялся ум, который нигде не пребывает,</li>
                        <li class="my-2">Он вызревает согласно своей кармической доле — всё есть
                            лишь явление.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Согласно пути Махайоги, чем завершается практика
                                созерцания?
                                Как описывается возникновение как видьядхары?</strong></li>
                        <li class="my-2">Созерцая собственное тело как великую печать (махамудру)
                            Победоносного,
                        </li>
                        <li class="my-2">Становятся проявленным божеством,</li>
                        <li class="my-2">Наделённым знаками, признаками и высшими способностями.
                        </li>
                        <li class="my-2">Такого именуют «видьядхарой махамудры».</li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-2"><strong>Если созревание корней добродетели, накопленных в
                                прошлых жизнях, происходит лишь в будущих перерождениях,</strong>
                        </li>
                        <li class="my-2"><strong>то почему состояние видьядары, достигаемое через
                                практику мантры, наступает уже в этой жизни?</strong></li>
                    </ul>
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-2">Для некоторых особых заслуг и проступков</li>
                        <li class="my-2">Плоды могут вызревать уже в этой самой жизни.</li>
                        <li class="my-2">А потому особая практика Тайной Мантры</li>
                        <li class="my-2">Даёт плод немедленно, не откладывая его на будущее.</li>
                    </ul>
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-2">Другие же корни добродетели обладают меньшей силой</li>
                        <li class="my-2">И потому неспособны дать столь сильного результата.</li>
                        <li class="my-2">Но собственная йога и благословения Победоносных —</li>
                        <li class="my-2">Несравненные корни добродетели, дающие зримый плод.</li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Можно ли достичь сиддхи (достижений), не получив
                                посвящений от Учителя?</strong></li>
                        <li class="my-2">О великом Сокровенном, скрытом Победоносными трёх времён
                        </li>
                        <li class="my-2">Утверждать, будто оно может быть достигнуто самостоятельно,
                            самопроизвольно —
                        </li>
                        <li class="my-2">Даже если исследовать все океаны священных учений без
                            остатка,
                        </li>
                        <li class="my-2">Такого объяснения не найти, да и не должно быть найдено.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-2"><strong>Есть ли опасность в том, что кто-то действует как
                                учитель (<em>ācārya</em>), не получив посвящения
                                наставника?</strong></li>
                        <li class="my-2"><strong>Если такой человек даёт посвящение — будет ли
                                польза в получении этого посвящения?</strong></li>
                    </ul>
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-2">Тот, кого назначил на пост министра заблудший ребёнок,</li>
                        <li class="my-2">Никогда не станет царём, и сам утратит достоинства.</li>
                        <li class="my-2">Ваджра-царь, лишённый благих качеств и силы,</li>
                        <li class="my-2">Если он наделяет [других] полномочиями, — губит и себя, и
                            других.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Является ли утверждение, что нужно делать
                                подношения Учителю при получении посвящения, вымыслом?</strong></li>
                        <li class="my-2">Мы скитались неисчислимые кальпы в поисках давно
                            утраченного пути —
                        </li>
                        <li class="my-2">Пути освобождения и несравненного пробуждения — безмерного
                            сокровища.
                        </li>
                        <li class="my-2">И если даже миллионы раз жизни и тела были бы пожертвованы,
                            это не превзойдёт его ценности.
                        </li>
                        <li class="my-2">Что уж тогда говорить о каком-либо ином подношении? —
                            Полностью положись на тайные Тантры.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Насколько велик грех неповиновения слову
                                учителя?</strong></li>
                        <li class="my-2">Все совершённые злодеяния трёх миров</li>
                        <li class="my-2">Не сравнятся даже с долей греха неповиновения слову
                            учителя.
                        </li>
                        <li class="my-2">Так накапливаются несомненные причины падения в ад.</li>
                        <li class="my-2">Если наставление не было услышано с самого начала — нет ни
                            вины за ослушание, ни особой добродетели<a href="#citation-31"
                                                                       id="ref-31"
                                                                       class="citation-mark"><sup>
                                    31</sup></a>.
                        </li>
                    </ul>
                </li>

                <p>Это сочинение было составлено учителем Пэльянгом. Как можно заключить из
                    названия, собрания и тантры класса Махайоги могут различаться от одного текста к
                    другому. Поэтому настоящая [работа] была написана с целью прояснить любые
                    неясные моменты в тантрах или возникающие сомнения. Она была преподана для
                    кланов Нанам (Sna nam) и Донг (Ldong), а также ради будущих поколений йогинов —
                    с целью прояснения любых неясных, сомнительных или трудных мест. Эта цель,
                    проходящая через всё сочинение от начала и до конца, выражена в пятидесяти трёх
                    вопросах в стихах, каждый из которых состоит из двух частей — вопроса и
                    ответа.</p>
                <p>Записано <em>Пху ши менг хве-и гьогом</em> (Phu shi meng hwe'i 'gyog).</p>
