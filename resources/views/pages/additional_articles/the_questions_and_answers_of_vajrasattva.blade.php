                                    23</sup></a>.
                        </li>
                        <li class="my-2">Природа его речи содержит семенные слоги, из которых
                            возникает божество.
                        </li>
                        <li class="my-2">Поскольку неравное становится равным<a href="#citation-24"
                                                                                id="ref-24"
                                                                                class="citation-mark"><sup>
                                    24</sup></a>, [такого йогина]
