                        <li class="my-2">Одним лишь произнесением гармоничных и чистых слов</li>
                        <li class="my-2">Невозможно преодолеть омрачения и достичь освобождения.
                        </li>
                        <li class="my-2">Но для разумного человека, который отбросил грубые дхармы и
                            гордыню,
                        </li>
                        <li class="my-2">Логические рассуждения о природе осознавания могут быть
                            чрезвычайно важны.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Каков корень пороков, делающих нас неравными
                                Благородным Существам?</strong></li>
                        <li class="my-2">Единственный корень — пагубная причина круговорота сансары
                            —
                        </li>
                        <li class="my-2">Неведение собственного ума, цепляние за "я".</li>
                        <li class="my-2">Поскольку этот великий яд присутствует в сердцах живых
                            существ,
                        </li>
                        <li class="my-2">Они не достигают пути освобождения и бесконечно
                            перерождаются и умирают.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Если пребывание в покое без мыслей на внешних
                                объектах считать достаточным, то возможно ли достичь освобождения
                                даже с представлением о «я»?</strong></li>
                        <li class="my-2">Если цепляние за «я» полностью оставлено,</li>
                        <li class="my-2">Не возникает ни малейшего цепляния за дхармы.</li>
                        <li class="my-2">Пока цепляние за «я» остаётся причиной функционирования
                            ума,
                        </li>
                        <li class="my-2">Даже достижение шаматхи, подобной горе, не приведёт к
                            освобождению.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Говорится, что через медитативные состояния
                                (дхьяна)
                                обретаются сверхзнания и магические силы. Достаточно ли тогда просто
                                пребывать в покое ума (шаматхе), не стремясь ни к чему?</strong>
                        </li>
