                        </li>
                        <li class="my-2">К этому следует снова и снова стремиться, углубляя и
                            укрепляя его.
                        </li>
                    </ul>
                </li>

                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Достигается ли самопроизвольно реализованное
                                (спонтанное) сосредоточение (самадхи)<a href="#citation-27"
                                                                        id="ref-27"
                                                                        class="citation-mark"><sup>
                                        27</sup></a>
                                с усилием или без усилия?</strong></li>
                        <li class="my-2">Что бы ни подлежало достижению,</li>
                        <li class="my-2">Следует снова и снова прилагать усилия, пребывая в
