                предполагает, что такие условные аспекты не являются необходимыми. Эти два
                направления можно
                соотнести с двумя истинами более раннего экзотерического буддизма, то есть с
                условной и абсолютной истинами, но это различие также подпитывалось
                внутренними противоречиями, с которых мы и начали. Йога одновременно включает в себя
                условные практики, способствующие постижению своего изначального единства с
                божеством, и подразумевает, что изначальная природа этого единства в конечном счёте
                делает такие практики несущественными. Хотя оба эти риторических направления
                распространены в тантрах Махайоги, несколько более поздние трактаты так называемого
                класса Атийоги в большей степени сосредоточиваются на абсолютной перспективе
                второго направления. «Вопросы и ответы о Ваджрасаттве», возможно, отражают тот
                момент в
                развитии тантрического буддизма, когда подобная риторика Атийоги ещё не отделилась
                полностью от своих истоков в Махайоге.</p>
            <p>В самих «Вопросах и ответах» во вступительных строках говорится, что текст был
                написан для «тех, кто желает с осознанностью постичь путь высшей Махайоги», и на
