                <li class="my-2">
                    <ul class="list-none pl-2 space-y-1 mb-4">
                        <li class="my-4"><strong>Говорится, что если йогин обладает способностью в
                                праджне и самадхи, он равен Будде. Что это означает?</strong></li>
