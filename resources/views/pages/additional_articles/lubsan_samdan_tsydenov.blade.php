            <p>В такой творческой, созерцательной практике Лубсан-Самдан Цыденов со своими учениками
                провел более двадцати лет. Возможно, кудунское созерцание могло бы продлиться еще
                несколько лет, однако война нарушила созерцательный образ жизни Л.-С. Цыденова. В
                конце августа 1918 г. власть в Забайкалье захватил атаман Семенов, ранее бывший
                представителем Временного правительства. С этого момента у Лубсан-Самдана Цыденова
                начинается новый этап жизни.</p>

